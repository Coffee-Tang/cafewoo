<p>周一直在这家熟悉的咖啡店看着那本永远看不腻的《政治经济学原理》，算起来，这应该是他第3次冲击研究生选拔考试，他所期待的命运并没有如期的眷顾他。两次落榜给他带来的压力很大，家里下了最后通牒，身为某纸制品企业副总的父亲要他速速到公司任职。但周并不想进这家有走私和贩毒原罪的大公司，况且他对女性用品过敏，他很难想象自己为一个卫生巾做事。</p>

<p>咖啡店是非常朴素的摆设和布局，没有任何花哨，唯一的看点是夕阳斜照进落地窗，有那么一刹那的光环，整个店堂仿佛坐落在天堂的某个最金碧辉煌的地点，而里面的顾客，就好像有幸在活着的时候到天堂旅游过一样。所以这间咖啡店的名字叫天堂辉煌。原本最早不是这个名字，是老板在听取了一些顾客的意见以后改的，很俗，与店面本身很不相称，但因此在黄昏时刻的顾客比原先多出了2倍。</p>

<p>周想考研并没有什么理由，他总觉得自己还不到工作年纪，他还有一些事情没有做完，例如旅游：到尼泊尔喧闹的集市去接踵擦掌；到香格里拉精密的草原上小憩；到印度骑象；到巴西烤雪茄。他还想带着心爱的小白，去一个没有无线通讯的地方，画一些更接近于涂鸦的油画。他考研的志愿是宏观计量经济。</p>

<p>小白是一只猫，在周一次烂醉后的清晨舔醒他的野猫，他哭了，于是就把小白抱回了在学校外面租的房子。小白是一只杂种花斑黑猫，牙齿有点钝，硬的东西吃不来，包括鱼骨头，所以经常和周有一顿没一顿地吃午餐肉罐头。小白几乎是周身边唯一能动的生物，因为周没有什么人际关系。当然，来这个咖啡店，老板也没有亏待这个小跟班，总会丢一点烹制午餐剩下的佐餐小肉给它。</p>

<p>下午3：14，非常闲暇的时间，顾客和老板皆是。</p>

<p>“要不要换个口味，我请你，拿铁你喝了有2年多了。”老板在周身旁坐下来。
<br/>“你准备请我喝什么？”周漠然地抬起头，机械地往右注视着老板。
<br/>“卡普奇诺或者巴西或者蓝山，都比你现在喝的拿铁好。”老板的声音一直以来都有点低沉，并且沙哑。
<br/>“其实我并不喜欢喝咖啡，只是你这个地方我呆得惯。”
<br/>“……谢谢，我换杯热拿铁给你。”老板端起周桌上剩余薄薄一层咖啡残渣的杯子，起身回到吧台。
<br/>“谢谢。”周低头继续看书。
<br/>小白很无力地叫了声，屁颠地跟在老板后面跳上了吧台。</p>

<p>很无力的下午，黄昏的阳光，依旧无力。</p>

<p>=====================================无力的分割线=========================================</p>

<p>“如果有一辆120急救车从你面前驶过，你对着它许愿，祝福它承载的病人，那么无论这个病人是否康复，都会有天使守护你的下一段路。”小文很认真地说。
<br/>林涛笑了一下，他面前的女孩纯真得无可挑剔。“一定会的。”</p>

<p>他们路过市中心的医院，在医院门口伫立了很久，但一直没有急救车飞驰的身影出现。
<br/>“或许一定要心诚才可以，我们这样是不是太自私太猥琐了？”小文又一次很认真地说。
<br/>林涛又笑了一下，他面前的女孩纯真得无可取代。“或许是吧。”</p>

<p>或许小文这个时候应该在某个高二学生应该呆的教室里，或许她此刻应该在钻研某道线性方程或者立几的难题，或许她应该在4年前就跟父亲到加拿大去。但她现在跟林涛在一起，一个对她很好的男人在一起。</p>

<p>“走吧，我们去海滨公园。”林涛说。
<br/>“为什么？”
<br/>“因为夕阳虽然不能守护你，但它会用仅剩的余温温暖你。”林涛解释。
<br/>“好，走吧。”</p>

<p>下午5：27，匆忙的马路，悠闲的海滨公园。</p>

<p>海边的棕榈树在海风中轻轻地摇曳，发出沙沙的声音，泛着金光的海面有一种让人陶醉的错觉。小文突然跳到石头防护栏上，张开双臂大喊：“我是海鸥！”右臂露出的袖口依稀可以看到两道深刻的刀疤。这两道疤痕，让林涛很是心痛，他在想，如果他早点认识小文，或许就不会有这样的伤疤在小文的手上，和心上。</p>

<p>“你吓着旁边的老爷爷了，快下来。”林涛微笑着。
<br/>“噢。”小文像麻雀一样敏捷地跳下来，略带着歉意。
<br/>“晚饭吃什么？”林涛问道。
<br/>“吃夕阳。”小文看起来很认真，谁都没办法拒绝她真切的眼睛。
<br/>“走。”林涛带着她跑起来。</p>

<p>天堂辉煌。</p>

<p>“真的有夕阳耶！”小文有点小开心。
<br/>“请问要点什么？”老板靠过来。
<br/>“这位小姐说她要夕阳。”林涛耸耸肩，表示无奈。
<br/>“好的，先生您呢？”老板表示理解。
<br/>“给我朝阳吧。”林涛对着小文吐了舌头。
<br/>“请稍等。”老板收起了ＭＥＮＵ回身吧台。</p>

<p>小文趴在桌上小声对林涛说：“老板不会抓狂吧？”
<br/>林涛很无奈地说：“抓狂你就用宇宙超级无敌美少女的宇宙超级无辜眼神来对付她啊！”
<br/>小文立刻很黯然地收起表情，用食指挠着桌面，然后抬头凝视着林涛，鼻翼翕动：“是这个样子吗？”
<br/>林涛见状，掏出随身带的ＰＤＡ，在上面写了：“１０．０”。</p>
