“谁说我们互相喜欢？！她根本就不喜欢我！”他大声吼道。

四下里一片寂静，卫蓝好象被他的声音吓到了，半晌没有声音。良久以后，她转身离开。走到门口的时候突然转身，依旧是平平的语气，淡漠的表情。“林希是喜欢你的，至少现在是。”

“你凭什么这么说？”

“女孩子的直觉。”

卫蓝转身走了。他用力叹了口气。女孩子的直觉？女孩子都是怎么想的？他永远也弄不懂。

当初他就在这个教室里，对林希说了那四个字，然而林希只是给了他一个浅浅的笑容。

“我们是不可能的，张陌。不要再对我抱有幻想了。不会有结果的。”

去他妈的女孩子的直觉！

I.

终于又到了周末，回到家以后他就开始守在电脑旁，在上次遇到More的那个聊天室里，以暗色的身份默默的守候着，一直等到深夜。

有些困了，他眨了眨疲惫的眼睛，More依然没来。也许只是偶然吧，他这么想，但是始终都不甘心，于是他对自己说，最后五分钟。4分03秒的时候More终于出现了。他很高兴，立刻和More打招呼。

More对暗色说：是你啊。

暗色对More说：我用五分钟做为赌注，如果我输了，我选择消失。

More对暗色说：那么现在你赢了，又如何？

暗色对More说：我只是想告诉你，我不怕了。

下了线，他感觉浑身轻松，就好象完成了一件积压多年的任务。

他很开心，非常开心，开心得有些不知所措。于是他不停在房间里来回地走着，想着他很开心。

睡前忽然有一道灵光闪过他的脑海，他有个很大胆的念头，觉得这个More就是卫蓝。为什么会想到卫蓝，他不知道，只是这些天来，只要他一闭上眼睛，就会想起卫蓝面无表情的脸和那对淡漠至极的眸子。

他真的被她吸引了，这个集合了红与白于一身的陌生女孩。

J.

“你又错了。”卫蓝的声音几百年如一日，淡漠得令他几乎要抓狂。

“是吗？”他不以为然的应了一句。

卫蓝站起身，收拾好东西，转身要走。

“你去哪？”他急忙问。

卫蓝头也不回，淡淡地说：“心不在焉的话，就不要糟蹋别人的心血。”

这话的份量很重，他一下子懵了，傻傻地站在原地半天不得动弹。他能很清晰地听见自己的心跳。等他回过神来，便猛提一口气追了出去。

“卫蓝！”他拉住她，在教学大楼的入口，一时间很多路过的人都盯着他们看。

卫蓝回过头，“干嘛？”她似乎并不在意周围的环境。

他却感觉很不自然，四下里看了看，便拉着卫蓝跑去了操场。

卫蓝有些轻微的喘气，但她却努力地不表现出来，依旧淡淡地问：“有什么事吗？”

“你是不是很讨厌我？”他死死地盯住她，压低了声音问。

卫蓝抬头看看他，“没。”

“那你为什么对我这么冷淡？”心里压抑的东西呼之欲出了，他的声音开始颤抖起来。

卫蓝依然只说一个字，“没。”

他生气了，很生气很生气，一把抓住她的手。

卫蓝轻轻叹了口气，“为什么是一半？”

“什么？”他松了手，“什么一半两半？”

“别骗自己了，你真正喜欢的人是林希，不是我。”

“为什么你总要扯到她？！”

卫蓝静静地看着他的眼睛，这是他们第二次长时间的对视，他从卫蓝的眼睛里看见了自己，一脸的茫然。卫蓝说：“林希要退学了。”

K.

退学两个字来得很突然，他丝毫没有防备，于是他下意识地跑去女生宿舍找林希，可是林希已经搬出了寝室。搞什么？一定要人间蒸发这么快吗？他忿忿地骑着单车在学校周围晃着，没有方向地乱骑，心里一团乱麻。

肚子咕咕叫的时候他发现自己不知不觉骑到了郊区，接着他看见了一幕自己无法相信的场景――

她静静地立在郁郁葱葱的榕树下。穿一件设计简单的白色淑女裙。带一顶可爱的帽子。双手很自然地垂着。手里提着一个小巧精致的包包。微微昂着头，目光落在某一片绿色的叶子上，嘴角挂着微微的笑。阳光洒在她的脸。肤色白晰而红润，像苹果一样。微风轻轻地拂着她的裙角，伴着她浅浅的笑容在空气里飘。他仿佛听见了很舒缓却非常美的钢琴旋律。

――那个女孩，是林希。

他想起来了，多年的记忆猛地从脑子里蹦出来，这白色的画面是林希给她的第一印象，他第一次看见林希时她就像现在这样站着，很美，很美。所以他才会故意把球踢向她，却不料把她撞倒。所以他才会加入戏剧社，却不料被她毫不客气的拒绝。

没想到左转右转之后，他竟然已经忘记了初衷。只记得熟悉的画面，却忘了画面里的人。

原来白色是林希。

L.

他陷入了迷乱。闭上眼想起卫蓝，睁开眼看见林希。一个是红的感觉，一个白的画面。红与白，爱与不爱，很复杂的问题，他渐渐有些晕了，不知道自己是谁，不知道该不该面对，不知道自己是睡着枯萎，还是枯萎着入睡。

More对暗色说：那么你到底是要红还是要白？

暗色对More说：一定要选吗？

More对暗色说：选择是最好的选择。

暗色对More说：可是我不知道怎么选。

More对暗色说：我给你讲个故事吧。

