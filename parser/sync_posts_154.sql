卫蓝，蔚蓝，很不错的名字，只是好象不适合那张脸。她不算漂亮，但是奇怪的是，他似乎正被她吸引。

“张陌，你过来。”林希走过来，把他拉到卫蓝眼前。

卫蓝在看见他的时候楞了一下，似乎也想起了VCD店里的事情。

