他看看林希，又看看卫蓝，不知道该怎么回答。毕竟只是一面之缘而已。

“不认识。”卫蓝抢过话，淡淡地说。

他诧异地看了卫蓝很久，虽然她说的也算实情。

林希停了停，笑着说：“卫蓝，这是张陌，新戏你和他搭档。”

他奇怪地问：“那你呢？”

林希勉强地笑笑：“我要退社了。”

H.

“你喜欢林希？”

他正背着对白，卫蓝突然问。

“什么？”他抬起头看她。

“你听见了的。”卫蓝低下头，继续看剧本。

他憋了很久，然后故作轻松地笑：“你别瞎说。”

“你又背错了。”卫蓝站起身，把她手里的本子给他看，上面用铅笔画了很多横线。“画横线的地方都错了。”

她面无表情地看着他，“你心不在焉。”

他有点生气，把本子扔到桌上，闷闷地不说话。

卫蓝走过去拿起本子，“你喜欢她对吗？”

