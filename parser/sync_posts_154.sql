还记得他第一次看见林希的时候他就在踢球，那时候林希还没当上戏剧社的社长吧。恩，还没。他又吸了口烟，嘴角浮起了笑意。她梳个小马尾辫，背一个蓝色的大书包，蹦蹦跳跳的，远远看过去就像一只蓝色的兔子。以前那样子多好啊，干嘛非赶流行跟别人一样把头发烫得直直的，一点都不自然。她还是扎马尾好看。呵呵。

他正笑着，突然一个东西直奔他的脸飞过来。他下意识地一摆手，但还是被那东西击中了额头，差点向后摔倒，顿时整个脑袋晕晕的。

“哎――球！不好意思啊！”踢球的小子一脸不羁地跑过来跟他赔不是。

他收起笑容，揉了揉额头，把球还给那小子，看着他跑远。

记得当初他就是这样一脚把球踢到了林希的头上，那小丫头一下子就摔倒在地上了。等他跑过去，还没开口，她就一下子蹦起来，噼里啪啦冲着他骂了一大堆话。他一下子就懵了，也没反应过来，就站在那乖乖地听着她骂。后来她气呼呼地跑掉了，他还站在那里发呆，因为她骂的那一大堆话他一句也没听懂。**，不就是踢痛你了吗，说那么多鸟语出来！

后来他在戏剧社每年的固定剧目里又见到了那小丫头，这才知道她叫林希，英文系的，要扮演的角色是他的茱莉叶，骂人高手茱莉叶。

C.

他又看见了那红色的画面，在一家租看盗版VCD的小店里。

李嘉欣扬着脸，拿烟的手略微颤抖，一枚偌大的闪亮的戒指戴在她的右手食指上。黑发遮住了她的眼睛，只有艳的唇……字幕出现，红的字，桃红的底：Fallen Angles――原来是王家卫。

他对着屏幕眯了眯眼睛，记忆里依稀有了模糊的印象，《堕落天使》，似乎是95年左右的电影。于是他一动不动地站在屏幕前，看着那一幕幕在梦里反复播映过的镜头，目不转睛。

李嘉欣身着豹纹的紧身上衣，脖子上挂一条红色的圆形的项链，独坐在那儿吸烟……她站起身，摇晃着身体，嘴里嚼着口香糖，去查看麻馆里间的一个有着红色印花门帘的包间。为了不引起旁人的注意，她假装和一个看守的男人调情。然后轻松地离开……她游走于喧嚣而略显虚幻的街头。红的唇。因冷漠而略显惨白的脸。时不时回敬路人挑衅的目光……背景音乐响起：金属的敲击声，一个略带凄怆的，如梦呓般的男声在哼唱。

和梦里的一模一样，红与黑反复交错的印象。

突然镜头定格，他楞了一秒，但很快反映过来，因为屏幕右上角出现了一个暂停标识。

于是他转过身，冲着小店老板说：“这片子我租。”

小店老板手里捏着遥控器，笑盈盈地摇头，“有人已经租掉了。”

他这才注意到身边有一个个子小小的女孩，正面无表情地盯着他。那张脸给他的感觉很异样，他不由自主地又想起了《堕落天使》里的李嘉欣。两人对视了几秒，没有声音。最后他看看那女孩无神的眼睛，又看看屏幕上定格的镜头，轻轻点了点头，说：“哦。”

D

中午接到林希的电话，叫他去拿《左岸，右转》的本子。

“你一定要认真地读，是读，不是看。”林希很郑重的把本子递到他手上，眼睛里写着一种无法言喻的忧伤。

“哦。”他盯着林希红红的眼睛，怔怔地点了点头。

林希很勉强地冲他挤出一个笑容，然后黯然转身，右手在空中轻轻抬了抬，说：“再见。”

她的影子印在树影班驳的水泥地上，沉沉的，让他很自然地又联想起了那种着色很深的文学电影。他开始觉得，这个步履沉重的女孩并不是林希。

她为什么这么忧伤？他低头看看手里的本子。因为这个？不可能吧。林希在社里可是最能“跳”的人。无论多么伤感的场景，她都可以轻松地跳进跳出，入戏和出戏间隔可以不到三秒。还记得排演《罗密欧与茱莉叶》的时候，有一幕罗密欧杀了人逃跑之前和茱莉叶见面的场景。她静静地直立着，身体微微向前倾，双手抚在胸前，脸上写满了悲伤和不舍，泪光在眼眶里若隐若现……正当大家都沉浸在那种气氛里寂静无声的时候，门外突然传来声音：　　“林希，电话！”，她二话不说就收起了泪光，微笑着跑出去，一如常人。

那么……不是因为这个本子了。他的脑海里又浮现出林希红红的眼睛――可是她为什么哭呢？

随意翻了翻手上的剧本，里面密密麻麻，于是他翻回扉页，只见上面写着三行字：

整个城市，陷入茫然的猜疑。

每个人，低下头来，

看着自己的脚。

E.

在塞纳河左岸的咖啡座，男人遇到了女人。男人是个演员。女人面无表情的样子给男人一种巴黎红砖墙下寂寞女子的感觉。于是男人前去和女人搭讪，女人出乎意料地一口答应了男人的邀请。约会之后，男人吻了女人，两人一度缠绵。然而天亮的时候女人却走了。男人在心灰意冷之后回了国，没想到竟然在开拍的新剧里又遇到了女人。女人正要和他演对手戏，然而这时的女人似乎早已忘了男人。

一连串戏里戏外的遭遇之后，女人内心的脆弱终于被男人发掘出来，男人也越发不能控制自己的感情。在戏关机之前，男人再次约女人去塞纳河左岸的咖啡座，并说明如果女人接受了他的邀请，那么就是接受了他的求婚。最后女人终于去了，男人却不在。她在咖啡座等到打烊，都没有看见男人。于是她又恢复了往常面无表情的样子，在左岸独坐到天明，准备离开时，却发现男人正站在她的右方……

画外音：整个城市，陷入茫然的猜疑，每个人，低下头来，看着自己的脚。

他掂了掂手里的本子，似乎觉得比先前沉了一点。再读一遍，又沉一点。如此反复，越来越沉，越来越沉，尤其是最后的画外音，象是点睛之笔，每次读它的时候都会有不同的感触。

其实这样的情节并不复杂，也不算特别，只是一个很普通的爱情故事，或者更适合拿来拍电影，但是写它的人却能轻易地让文字钻进人的内心，总是吸引着他不停地看下去。他不是在看故事，而是在找感觉。

这个中文系的女生，帮他描绘了一幅灰白的画面。

F.

夜里两点钟他突然醒了，楞楞地坐在床上半晌没有动静。接着起身爬向电脑，随便钻进个热闹的聊天室在里面静静地呆着，一边喝水一边看里面的人真真假假亲亲密密地交谈。

每个周六晚上他都会有这样的习惯，他喜欢在热闹中寻找安静的感觉，也常嘲笑自己做作，但却始终坚持。这期间偶尔也有人会来找他聊天，他不常回应，因为他打字很慢。

今天他随便取了个昵称叫暗色，躲在一群或长或酷的男人名字中间，显得很不起眼，因此一直没有人把鼠标点在他的名字上。

聊天室里大约有50多人，屏幕刷新得很快，但是他却注意到了一个句子，只是简单的一瞥，却记住了里面的几个词，似乎有“白色”和“绿叶”。他下意识地想起了那个白色的画面。于是立刻抖擞了精神，关了滚屏在聊天记录里寻找。

终于找到了――

More对所有人说：阳光。天空。绿树。女孩。白色。微笑。

他非常仔细地看着这行字，许久，深深吐出了一口气。他开始打字，十分费力却非常认真。

暗色对More说：你好。

等了很久，没有回应。他不泄气，又打了一遍。

暗色对More说：你好，在吗？

More对暗色说：有事吗？

暗色对More说：没，只是很单纯地想和你聊天。

More对暗色说：你很单纯，我也很单纯，两个单纯的人在一起就不单纯了。

暗色对More说：你怕？

More对暗色说：难道你不怕？

暗色对More说：也许。

More对暗色说：那么等到不怕的时候再聊吧。

G.

