<br>����我把窗户全都打开，把风放进来，吹得他冷死了，他老是只穿件球衫，将近冬天了还这样，门窗关的死死的，捂得屋子像蒸笼，一股扑鼻的臭脚丫子和肥皂水的味道。我坐在窗台上看他，看他冷得发抖，他看见我看他，就跑过来把我钳下来，说：不要坐在这里。我问他为什么不要坐在这里，他又说不出个所以然，因为这里是一楼，很安全，但是他说：就是不要坐在这里。我还住在学校宿舍，自从他搬出去之后，他就说：那个破宿舍。但他还得在那个破宿舍的楼下等我，因为他上不来，只好等着。他不喜欢多穿衣服，老是冷得发抖，，冲上面喊：快点。我经常不化妆，甚至也不换衣服，就冲下去了，他把我往车座后面一放，就骑着跑了。别人劝我，说应该换件衣服，化化妆什么的，但他老喊快点，所以老也化不成，再说和他在一起也没必要化妆，他又不是因为我漂亮才喜欢我的，我也没什么漂亮的，瘦巴巴像根晾衣杆，还戴副大眼镜。等骑出一身汗来，他就放慢步伐，最后不骑了。把车往树丛里一扔，说：让我抱抱。我说：你抱就是了，谁不许你抱了。他就两臂一张，把我钳住，等他抱够了，就去闲逛，谈恋爱真是无聊死了。
<br>
<br>����我们在街上闲逛，有时侯去吃烤羊肉和烫海带，逛到最后总是逛到他家里，除非去看通宵电影。他不喜欢看电影，那电影院的沙发又脏又破，不知多少人在上面打过滚了，海绵耷拉在外面，地上密密层层铺着瓜子壳和话梅胡，还有痰和鼻涕。他说这些电影放的尽是碟片，还老放质量很差的盗版碟片，马赛克特别多，在那块充当荧屏的白布上跳上跳下的，傻死了。他到电影院是为了可以睡在沙发里，把乱蓬蓬的脑袋枕在我腿上，因为老睡地上容易得关节炎，我也不肯让他枕在腿上。这倒是真的，几场电影下来，我的腿就麻得不能动了，得让他钳到自行车上去。后来我学乖了，总是带件棉衣，等他睡着了，就把他的脑袋搬到棉衣上去，反正他睡得像猪一样，什么也不知道。他问我带棉衣做什么，我说怕冷，他说你多锻炼锻炼不就不怕冷了？我说怎么锻炼？――坐自行车吗？有部电影我还有印象，是部爱情片，叫《泰坦尼克号》，傻得要命，据说还是部大片。那天下雨，我本来不肯去，他非要去，还说那片子特浪漫，我说你租张碟子来看不就得了？他说大家一起看才有气氛，别人一哭，你的眼泪自然就下来了。我说好端端的哭什么，他把我往车座后面一放，冒雨就冲了出去，我冲他嚷：我要感冒了！他就把外套往我头上一蒙。到电影院的时候，那片子还没有开始，长排座椅上只有几个人，还都头一点一点地在打瞌睡，荧屏上在放一部哑巴舞剧，几个野蛮人舞到东舞到西的，难看死了。他觉得那女主人公像我，就推我看，说：看，像你。我说像什么？那是个野蛮人。他说那可是个公主，别不知足了，给你买话梅吃要不要？谈恋爱真无聊，我想。他又追问：要不要？我说随你的便。他说：随我的便可不行，要去一起去。我说：我怕淋雨。他说：不淋雨，楼下大厅就有。大厅灯光很亮，但白得发绿，惨兮兮的，里面游着几条惨绿色的鱼，是柜台小姐，白衣服映得皮肤暗淡无光，像得了黄疸肝炎，我真想把她们的血管切开，看看里面流动的是什么颜色的液体，看见我们走过来了，就咧嘴微笑，露出绿幽幽的牙，像森林里的大熊，一齐吼：“请问两位要点什么？”他问：要什么？我说随便，有什么？他就问：有什么？有好几种，一种叫情人梅，一种广澳梅，还有几种我记不清了。他各买了几包，装在一个塑料袋里拿回去，哗啦哗啦撕了半天，直到野蛮人的哑剧演完了，他说：好了，大片来了，吃吧，边吃边看。说着就把撕开的话梅递给我。我说：还有的没撕开呢。他说回去慢慢撕吧，别烦了。但正片还是没有开始，又放了好些广告，可能是剧院经理觉得人太少了，就这么放有点划不来，也可能他觉得这是部大片，得拿点架子，总之我们又等了好久，一包话梅都吃完了，他把袋子一丢，说：烦死了。就往下一躺，大脑袋搁到我腿上，我拼命推他，说：别睡别睡，你的电影来了。可他已经睡着了。那部电影的主人公是个痞子，叫杰克，杰克本来就是痞子的意思，和一个订了婚的贵族小姐偷偷摸摸地相好，两人跑到旧汽车里去做爱，后来船沉掉了，那男的掉在海里淹死了，我把话梅胡一吐，说：该。旁边一片呜咽声，黑洞洞的电影院里只看见更黑的头颅两个两个地靠在一起，像油炸豆腐，我看见他动了动，像是要醒了，就把棉衣抽掉，把他的脑袋搬过来放在腿上，他迷迷糊糊睁开一只眼睛，皱着眉头，问：开始了没有？我说：放完了。他说：哦。转过去又睡，我推他，说：别睡了，要散场了。正好灯光亮了，那些男男女女都赖在那里不肯走，眼泪巴巴地傻盯着那块白布，大概是给镁光灯刺了一下，他也醒了，抹抹眼屎，说：开灯做什么？我说散场了，他还不信，说：哪有这么快，我还没睡着呢。我说反正散场了，信不信由你。他说可惜可惜，那他们还坐在这里干什么？我说：那谁知道。他站起来拍拍屁股，说：可惜可惜，明天再来看吧。大概拍的声音很响，那些人都陆陆续续站起来走掉了。我说：你自己租碟子看好了，我不高兴看了。他说：那多没劲。我说：那有什么办法，谁叫你睡觉来着。他说：话梅都吃完了吗？我说：吃完了，酸死了。他说：怎么不给我留两颗？我渴死了。我说：你不会喝水吗？他说：我不喜欢喝水。
<br>
<br>����外面雨已经停了，时间是半夜四点，马路被雨洗了一番，亮晶晶的，冷得很，人行道上的砖块都松动了，踩上去，一脚的水。我说骑车回去吧，他说急什么？外面空气多好。我说踩一脚的水。他说：你不会小心点？我就往自行车后面一坐，说：那你推我。他说：推不动。不过他还是推了，摇摇晃晃的，不知道是真推不动还是装蒜，我觉得是装蒜，下次好说：你看，一推就摇摇晃晃的。推了一会儿，路过一所小学校，他说：进去玩会儿吧。我说门都关了。他说爬墙好了。我说：小学校有什么好玩的。他说：爬爬双杠。但他还是放弃了这个计划，因为他绕到后面一看，发现那些双杠都很矮，显不出本事来。他就喜欢显本事，在双杠上荡前荡后的像钟摆，在单杠上转来转去的像陀螺，还喊我看，说：快看快看。我拍他马屁，说：不敢看，看了头晕，他就很得意。不过我也不老是拍他马屁，我说：我近视眼，看不见。他说：你不是戴了眼镜了吗？我说：我眼镜度数不够。他说那就重配一副好了，我说：没钱。谈恋爱真是无聊死了。
<br>
<br>����我给他统计了一下，我们平均每星期看两次通宵电影，吃三次油炸豆腐和烤羊肉，到他屋里去看五次碟片，他在我楼下嚷嚷十四回。我把这个数据摆在他面前，他咬着手指想了半天，说：对不起，实在想不出什么花样来，不就是谈恋爱吗？要不你每天早上跟我去跑步好了。我说：跑不动。他说：跑多了就跑得动了。我说：我起不来。他说：我在楼下喊你。我说：那不把人家也吵醒了吗？他说那倒也是，要不你就别回去了，我把塌塌米让给你。我说才不呢，我不上你的当。他说：我不揩你的油，真的。我说：鬼才相信你呢。他说：你这人真没劲。我说要不这样吧，你再找个女朋友去，话梅我吃，揩油揩她的。他说那多不好。我说那有什么，省得你老是缠着我，烦死了。他想想也是，就说：那我试试吧，找不到怎么办？我说：找不到慢慢找。
<br>
<br>����过了两天，他又喊我去看碟子，我说：你女朋友找到了吗？他说：还没，要不你给介绍一个吧。我说介绍哪一个？他说：随你的便吧。我说：那就把Q介绍给你吧。他说那不好，Q知道我和你谈恋爱。我说那又怎样？我看她挺喜欢你的，老在我面前夸你。他说：夸我什么？我说夸你长得帅。他说：我有什么帅的？我说：我也这么说，可她就是觉得你帅么。他说真的吗？那就她吧，你叫她下来，我追她。我说：那你带她去看碟子吧，我不高兴去。他说：我要带她去看通宵电影。我说随你，别忘了带话梅给我吃。他说：忘不了。
<br>    
<br>����我就跑上楼去。正好Z出去了，Q一个人在，我说：Q，他想请你看电影。Q正在照镜子，听了一愣，说：“请我？”我说对，就是请你。她说：“请我做什么？”我说那我可不知道，她说：“别是有什么事吧？”我说你去问他好了。Q犹豫了一下，说：“那你去不去？”我说不去，我困死了，要睡觉。Q还在犹豫，我说：别婆婆妈妈了，去就去，不去就别去。Q想了想说：“他待会儿来接我吗？”我说：他在楼下侯着呢。Q听了想伸出头去看看，不过还是没看，说：“那你让他稍等一会儿，我这就下去。”说着就摆开镜子，开始找衣服。我把脑袋伸出窗外，冲下面喊：等一会儿。他说：快点。我说：快点。Q说：“就来。这件怎么样？”我说挺好，快穿上吧。她穿了，匆匆忙忙化了把妆，照了照，说：“那我去了。”就提上包走了。我往床上一躺，看着天花板，忽然想起忘了叫她带棉衣了，朝窗外看看，他们早就走了，远远看见两个人影拐了个弯，消失在树叶下面。我想算了吧，下次她就知道了。
<br>
<br>����我很快就睡着了，还做了个梦，梦见Q跑过来，两眼哭得红红的，哑着嗓子说：“他死了。”我说：死了就死了吧，哭什么？可是她两只眼睛红得像桃子，很好玩，还水灵灵地泛着光，两片嘴唇也红红的，在轻轻抖动。我觉得她可爱得很，想吻她。我低下头去吻她，还没吻到，忽然听见呀的一声，吓了一跳，醒了。Q回来了，黑乎乎地站在床前换衣服。我说：Q你回来了吗？她说：“我还以为你睡着了呢。”我说：刚醒。她说“对不起，把你吵醒了。”我说那有什么，把灯打开吧。她跑去把灯打开，递给我一个纸袋子，说：“他让我给你带的话梅。”我说谢谢。停了一会儿，她说：“他问我考试的事情。”我说他反正过不了，问了也白问。她说：“可不能这么说，我看他挺认真的。”我说：别是让你帮他作弊吧？她说：“说到哪里去了？他说想让我帮他复习复习。”我说那你早点睡吧，明天好帮他复习，现在几点了？她说：“十一点了。我还真有点困了，睡吧。”我就爬到床上接着睡。这回我吻到Q了，她软软地趴在我怀里，哭得两个肩膀直耸，不断地说：他死了，他死了……说了不晓得多少遍，天就亮了。我听见Q从床上起来，我说：Q，你起这么早干什么？她说：“我得去帮他复习功课了。早上空气清新，人脑子也好使。要不你一起去吧。”我说才不，我还没睡醒呢。你们昨天看的什么电影？她说：“《泰坦尼克号》，是部爱情片，特感人。”我说：外面炒得正热呢。她说：“可不是么。”我说：那你去吧，我再睡会儿。她说：“我那儿有饼干，在抽屉里，醒了你就拿着吃。”我看着她带上门走了，就迷迷糊糊又睡着了。这回没做梦，不过也可能做了，后来又忘了，要不就是梦到一句话，总之我记不太清楚了。下午他把Q送回来，叫我下去，问：中饭吃了没有？我说吃了，吃的饼干。他说走，我带你吃面条去。我怎么，你都没请人家Q吃饭吗？他说吃了，在茶社吃的，根本吃不饱。我说那好吧，我上去洗把脸，我早上起来还没洗脸呢。他说洗什么脸？走吧。我说不，我还穿着拖鞋呢，你骑车那么快，弄掉了怎么办？他说那你快点，冷死了。我说知道知道。我们跑到学校外面的一个小馆子里吃面条，他要了一个大碗的牛肉面，给我要了个小碗的。我说为什么你吃大碗，要我吃小碗？他说你是女孩子，要不也给你要个大碗的吧。我说算了算了，人家都去下了，找位子吧。馆子里人很多，我们站在一张桌子旁边等了半天，那两人正在聊天，本来是旁若无人的，后来可能被我们站得有点不好意思了，就站起来走了。我说：你的功课复习得怎么样了？他说什么功课，她讲什么我都没听进去，直犯困，我昨天晚上看碟子看到三点半，今天下午还约了人打球呢，你去不去？我说：我去干什么？又看不懂。我昨天梦见你死了。他说：哦，是吗？我死了是什么样子。我说没看见，是Q跑来告诉我的，两眼哭得红红的，像桃子。他说：昨天她还真哭得像桃子了，不过不是哭我，是哭杰克。那片子可真无聊，早知道不去看了。我说那天下那么大的雨，你还非逼着我去看。他说谁知道会这么无聊？放了好长时间了吧？一、二……四天了，听说明天还有，一直要放到星期六。面来了，他说：大碗给你吧，你够不够？我说算了，我也不太饿，把牛肉给我吧。他说可不能都给你，我也喜欢吃，分你几块吧。我说：你这人可真小气。他吃面条很快，呼啦呼啦两三口就吃完了，揪出把纸巾来抹了抹汗，坐在那里看我吃。我说太辣了。他说：要不要叫他们加点汤？我说加就加吧。老板娘是个大胖子，一口四川话，拿了把大勺子来给我加汤。他说：老板娘，下次少放点辣。她说辣啥子？不辣就不好吃了，说着就跑了，去招呼别的客人。他说：听到了吧，不辣就不好吃。我说：我又不是川妹子。他看看表，说：快点吃吧，下午我约了人打球呢。我说你先去吧。约的谁？他说说了你也不认识，要不我先走了，晚上上我那里去看碟子，打完球我来接你。我说怎么一天到晚看碟子？不看。他说那晚上再说吧，我先走了。说着就跑了。
<br>
<br>����我又吃了好久，吃得一身的汗，觉得神清气爽，就想回去冲个澡。Q一个人待在宿舍里，正在窗前发愣。我就问她：Q，Z还没回来吗？她说：“没有，我也在纳闷呢，别是回家了吧？”我说有可能，你在干吗呢？她说：“没干吗。你怎么满头是汗？”我说：吃面条吃的，我要冲澡去了。水浇在身上很舒服，我冲了很长时间，把热水器里的水都用完了，才套上睡衣出来。Q正在写作业，我就爬到床上，缩在被子里看小说。她突然问我：“你说他挺用功的，怎么成绩老是上不去？”我说笨呗。她说：“才不呢，我看他挺聪明，反应特快。”我说那我可不知道了，可能是贪玩吧。她说：“这倒有可能，你得多提醒他。”我说我哪提醒得了他？她说：“不一定，我看他就是少个人督促。”我说我可没那个本事，我跟他是半斤八两。
<br>
