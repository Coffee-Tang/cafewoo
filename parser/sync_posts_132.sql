"我杨家老爷曾任朝中三巡抚一职，因被朝中奸人所害。家道虽已中落，但也是书香世家。杨家少公子为令爱所救，无以回报，愿与其一世相守。恳请夫人接纳这门亲事。"老者说。

李嫂当下明白，明代讲究理法，男女授受不亲，自己的女儿在河内救人，这肌肤相亲恐怕不只"授受"那般简单。杨家也实不愧书香之第，是因为想到这点，才会令儿子来娶一个半生半死的傻姑娘吧。

