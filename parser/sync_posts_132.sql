"李夫人好生客气。李家连救杨家两代人的性命，这恩德令杨家莫齿难忘。"老者言。

"你家老爷？…………"李嫂大惊。

"是，我家老爷是杨桐，字守时。"老者起身恭敬地答道。"杨李两家若能联姻，正是佳事。先祖地下有灵，定当额手称庆。"

