是她紧紧握住我的手，拉着我迅速地穿过马路，经过一个又一个美丽的橱窗，在人群里快乐地欢笑，像两个刚刚放学的小孩子一样大摇大摆在大街上穿行。

我想，即便是不说话，静静地这样牵手行走在阳光明媚的大街上，也是再幸福不过的事了，我爱这个阳光午后。

时间如果在这个时候停留该有多好。

可是天渐渐黑下来了。

我以为还有一点时间，但是她抬头看了看天，皱着眉头说，“我得回去了！”

“嗯，你该回去了，”我沮丧地说，“下午过得可真快。。。”

“我们敬爱的爱因斯坦什么会发明相对论呢？”她笑着从我手中接过她的包，“因为有一个可爱的女士陪了他一个下午。。。”

“你开心吗？”我鼓起勇气看着她的眼睛。

“当然，呵呵”她松开我的手，“你简直就是一个活宝。。。”

我只是一个有趣的笑话而已。

我居然还傻傻地跟在她身旁，笑得那么甜。

我恨自己不争气。

一只发了春却只会沉默的公河马。

九

我想就这样和她继续暧昧下去。

因为我害怕失去她。

越是害怕，就越懦弱。

越懦弱就越是不敢说出对她的感觉。

我只希望她留在我身边久一点，让我暧昧久一点。

不爱那么多，只爱一点点。

春天的时候，沙漠都有春意，天知道会发生什么事？

我的心田里一直下着贵如油的春雨。

一如甘泉一般的灌溉。

我在她离开后无可救药地想念她。

我开始着手写给她的歌，我给吉它换了一套新弦。

一整个晚上，我都在和吉它和键盘做斗争，和七个可怕的音符做斗争。

一整个晚上，一无所获。

我在想，我对她的感觉是不是还不够深呢？

“我总是写不好给你的歌，”我叹了一口气。“伤脑筋哦。”

“我原本就对你不抱希望，呵呵”她的口气装得很同情。“那就别写了嘛”

“你可以怀疑我的能力，”我嘿嘿一乐，“但是你不能怀疑我的动机。”

她的声音有沙哑，以至于我担心她是不是感冒了。

“别跟我说一些我不想听的哦，”

“那我就不说好了，”我突然觉得很沮丧，“其实我也没想说什么。”

“你什么都不该说！”她突然大声地对我说。

我们在电话前沉默了几十秒钟，我听见她叹气的声音。

“我们这样不是挺好的吗？”她幽幽地说道。

我把房间的灯都关掉，试图让自己在黑暗里沉静下来，在寂静无声的深夜里，

我的心跳格外清晰。

我一直逼自己这样认为：如果她觉得好，那就好了。

我们这样可以继续多久？

有一天，她会离开我还是会留在我身边？

不爱那么多，只爱一点点。

现在我这么想。

