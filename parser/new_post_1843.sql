阿保的婚期定在九月。

“你不必请我吃饭，”我开心地笑着，“说什么我也愿意当你的伴郎。”

涮羊肉的香味在空气中涌动着，浓香的汤一直在沸腾，我觉得此时的心也很滚烫。香菜，冻豆腐，芋头还有牛肉丸，可口的火锅料整整摆满一桌。

还有冰啤酒，情绪的催化剂。

“老板，把空调开到最冷，”阿保冲门外喊，“我就喜欢在18摄氏度的房间吃火锅！我是羊肉的天敌。”他嘿嘿一乐，杯中酒一仰而尽。

“听说结婚证都领啦，”我也一仰而尽，“没检查出什么毛病吧？呵呵”

“有，”他微皱了一下眉头，“有大毛病。。”

“怎么？”我大为惊奇，“什么毛病？能治好吗？”

“大夫说没有特别的治疗办法，顺其自然吧。”他专心致志地涮他的羊肉。

“你别吓我！”我直勾勾地看着他，“到底什么病呀？”

“说出来吓你一跳！”他一副没正经的样子。“-----精子活力过剩综合征！”

我们都哈哈大笑起来。

他从提包里拿出结婚证给我看，“看看，我现在和女人睡觉受法律保护！”

我看着那张盖着刚印的结婚照，和所有的结婚照里看到的姿势是一样的，女方一脸幸福地把头倾向男方的肩膀，尽管两人都素面朝天，仍掩不住甜蜜的微笑。

他歪着头，自言自语地笑道，“就是-----光明正大的爱情！”

