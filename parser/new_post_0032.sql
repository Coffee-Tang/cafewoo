<br>“拿钢琴来说，键盘有始有终，有88个键，不会错，并不是无限的，音乐是无限的，在琴键上奏处无限的音乐，我喜欢。我应付得来。走过跳板，前面的键盘有无数的琴键，事实如此，无穷无尽，键盘无限大，但无限大的键盘，怎奏得出音乐？！不是给凡人奏，是给上帝。只是街道，已经好几千条，上了岸，何去何从，爱一个女人，住一间屋，买一块地，望一个景，走一条死路。太多选择，我无所适从，漫无止境，思前想后，你不怕精神崩溃？那样得日子怎样过？”
