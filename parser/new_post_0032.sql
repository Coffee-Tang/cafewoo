一九零零就是这样又回到了船上，再没动过离开的念头。

维吉尼亚号就要被炸掉了，而Max坚信一九零零还在船上，他一次次地寻找，未果。最后，他向店主借来了唱机和那张唱片来到船上，他在那个废弃破败的船舱的每一处放那段悠扬的旋律，回忆过往，我个人觉得，这是全片最为令人感动的场面。Max提着沉重的唱机，步履艰难的攀爬在阶梯上，他终于又一次看到了一九零零。他向第一次相遇时一样地问他：“嘿，什么事，晕船浪吗？”

Max劝说一九零零离开这艘将要爆炸的船，去开始新的生活。一九零零向他讲述了自己当年没有下船的原因，城市的漫无尽头吓到了他。对与一个没有出生证明、没有国籍、没有一切的人，他无法想象在陆地上的生活。

“拿钢琴来说，键盘有始有终，有88个键，不会错，并不是无限的，音乐是无限的，在琴键上奏处无限的音乐，我喜欢。我应付得来。走过跳板，前面的键盘有无数的琴键，事实如此，无穷无尽，键盘无限大，但无限大的键盘，怎奏得出音乐？！不是给凡人奏，是给上帝。只是街道，已经好几千条，上了岸，何去何从，爱一个女人，住一间屋，买一块地，望一个景，走一条死路。太多选择，我无所适从，漫无止境，思前想后，你不怕精神崩溃？那样得日子怎样过？”

他说着这番话的时候，我突然想起了〈肖申克的救赎〉里的老布，那个在监狱里受人尊敬，但出狱后却无法生存的老布，他最后选择了死亡，那么，如果，如果一九零零的双脚踏上陆地，同样的命运是否也在等待着他呢？

“我生于船，长于船。世界千变万化，这艘船每次只载客两千。既载人，也载梦想，但范围决不离开船头船位之间，在有限的钢琴上，我自得其乐，我过惯那样的日子。陆地？陆地对我来说，是艘太大的船，是位太美的美女，是条太长的航程，是瓶太浓的香水，是篇无从弹奏的乐章，我无法舍弃这条船，我宁可舍弃自己的生命……”

一九零零最终都没有离开那艘船，在轮船爆炸的时刻，他的用他那神奇的手指在飘渺的空气中弹奏着那首挚爱的乐章，他抬起头，嘴角轻轻掀起一抹笑意，他看到了什么？是那一个爱过的姑娘，还是天堂中的又一架钢琴？在6吨半炸药的爆炸中，他与他的音乐一起灰飞烟灭……

