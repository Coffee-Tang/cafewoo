<br>这张碟我找了很久，因为错过了第一拨的盗版，所以只能苦苦寻找，外加等待。
<br>
<br>该片又被翻译成〈声光伴我飞〉，但，看过影片后，我还是觉得〈海上钢琴师〉这个名字更为切合。该片的导演是以〈天堂影院〉、〈玛莲娜〉而扬名的意大利写实电影流派的新贵导演吉赛贝&#183;托纳多雷。
<br>
<br>影片是从爵士乐小号手Max把自己心爱的小号买给一家乐器店开始的。成交后，Max看着陪伴自己多年的小号不忍离去，请求老板让他再吹最后一次。Max吹得忘情而投入，令店主惊奇的是Max吹奏的悠扬乐曲竟和他无意中粘好的一张唱片模板上的曲子完全相同。唱片中的乐曲与Max的小号水乳交融，Max吃惊地停了下来，这熟悉而美丽的曲子唤醒了他的一段记忆。当店主问他，这个曲子的弹奏者究竟是谁的时候，Max眼含热泪的说：“说世界上从来没有这个人也不足为怪……”
<br>
<br>影片由此缓缓展开。1900年，船上的铲煤工丹尼在一等客仓发现了一名弃婴，他收养了他，并为他起名丹尼勃曼TD柠檬一九零零。这个在20世纪初出生的可爱婴儿没有出生证、没有国籍、没有护照，他只能在船上生活。夜晚，当铲工们都进入梦乡，一九零零躺在大幅摇晃的船上摇篮里，若有所思。在一九零零8岁的时候，好心的丹尼因意外而丧生。丹尼只得随着轮船无数次的往返于欧美大陆之间，但是，他从未上过岸。一个夜晚，他无意中来到头等舱，第一次见到了钢琴，并为它的美妙所吸引。在人去夜静时分，他弹起了那架钢琴，音乐如水流泻，静动了全船的客人。从此，一九零零成了这艘船上的钢琴师。
<br>
<br>一九零零生活的那艘轮船被称为维吉尼亚号，多年的羁旅生涯已使其破败不堪。人们决定炸毁它。Max在码头上找到了这艘曾经生活过多年的轮渡，思绪万千。当年，他应聘称为维吉尼亚号的乐手，但因为受不了大规模的海浪而晕船。那个夜晚，一九零零出现在他的面前，带着他来到大厅，要他放开钢琴的固定栓，然后和他一起坐在琴凳上演奏。船外，是海浪滔天，而大厅内，是随着海浪而肆意滑动的钢琴。空旷的大厅，垂落的华美的吊灯，斑斓的玻璃幕墙、美妙的音乐……一切显得如此大胆而又如此的充满诗意。
<br>
<br>Max与一九零零成为了好友。他发现，一九零零从不按照乐谱弹奏。而他那即兴的音乐却激发了人们无与伦比的热情、活力与想象。有的时候他禁不住要问，你的“手在键上，心在哪里？”是啊，在一九零零弹奏的时候，他的眼睛总是望向远方，他总是若有所思，而又若有所待。虽然，他从未登临陆地一步，但是，他却神游大地，无所步至！听听他对新奥尔良那一段美妙的描述吧：
<br>“冬季，那个城市别具美态。三月时分，总有个下午，雾海突如其来，飘然而至，像一层层纱帐，垂在街灯下，也像块利忍，把一切削成两截，奇妙啊。楼不见楼顶，树不见树枝，圣路易教堂不见塔尖，四周的路人，见身不见首，总之头以上的部分，统统消失，杰克逊广场上，一个个无头的孤躯，跌跌撞撞，撞到对方，问一声：府上各位可好？”
<br>一九零零不但神游大地，更从身周人们的神态动作上寻求他的音乐灵感，神乎其神、妙不可言。一九零零肆意的演奏技巧与独特的风格为他博得了无尽的声誉，许多旅客慕名而来，只为一睹他的神奇。
<br>
<br>然而，声誉背后是一九零零无声的孤寂。每一次，当乐曲奏毕，船达彼岸，喧嚣与围拢的人群就会散尽，提拿行李奔向自己的前程。留给他的，只是一片静寂。
<br>
<br>Max曾数度劝他下船，为他描述他在陆地上可以成就的美好前景，Max告诉他，“世界近在咫尺，只在跳板的另一端。跳板是什么？几级梯而已……”但是，一九零零不肯离去，他说“陆地上的人喜欢寻根究底，虚度了很多光阴，冬天忧虑夏天的迟来，夏天担心冬天的将至，所以你们不停地到处走，追求一个遥不可及四季如夏的地方，我并不羡慕。”
<br>
<br>就是这样，一九零零从未离开过那艘轮船。然而，他声明远播，引得爵士乐王莫顿要来船上与他一拼高下。莫顿傲慢地点燃一支烟，放在琴沿上，然后有力地弹奏起爵士乐来。曲毕，他举起那支未燃尽的香烟，在众人的掌声中走到一九零零的面前，说：“该你上场了，水手！”
<br>一九零零坐上琴凳，略作思考后弹了首《平安夜》。众人不免有些吃惊，被戏弄的莫顿更是铁青着脸又演奏了自己的拿手曲目。一九零零深深地为对方的乐曲所感动，泪流满面。轮到他演奏时，他竟将莫顿的曲子重新弹了一遍！第三轮较量前，一九零零向Max要了一支烟，也将其放在琴沿上，突然，他双手猛烈击键，瞬时，键盘上好象有无数双手在分扬，音乐如狂风暴雨而至，充塞天地，天啊，我真希望自己能有语言可以描述那一场惊心动魄的乐曲。在场的所有人都惊呆了，莫顿也是吃惊地睁大了眼睛。一曲终了，全场鸦雀无声。一九零零拿起放在琴沿上的未燃的烟放在琴线上，居然，滚烫的琴线点燃了香烟！观众入梦方醒，掌声雷动。
