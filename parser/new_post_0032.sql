<br>一九零零就是这样又回到了船上，再没动过离开的念头。
<br>
<br>维吉尼亚号就要被炸掉了，而Max坚信一九零零还在船上，他一次次地寻找，未果。最后，他向店主借来了唱机和那张唱片来到船上，他在那个废弃破败的船舱的每一处放那段悠扬的旋律，回忆过往，我个人觉得，这是全片最为令人感动的场面。Max提着沉重的唱机，步履艰难的攀爬在阶梯上，他终于又一次看到了一九零零。他向第一次相遇时一样地问他：“嘿，什么事，晕船浪吗？”
