终于，我长到了怀春的年龄，并且，确实美丽无比。当青年男子们开始寻找爱情隐秘的欢乐时，我就要出卖我的爱情了。我要卖给你，高价地卖给你，我要你千金散尽，双眼空空。我要你写尽一生中最香艳最缠绵的诗歌。无论是在物物交换还是在货币流通的商品时代，我要你爱我一生，仅仅次于你的诗歌。

诗是你的妻。我是你的妾。

我甘心为妾。妾美丽，自称奴家。

奴家温顺，善解风情。

你会知道的。

你若买了爱情你会知道。我冒着最大的风险，和天下男人赌了一把。一定要赌的，输了便喝酒死去，死时手指苍穹星汉无限说噫吁呼！六弦琴又重拔一次，我宛如名门闺秀小家碧玉青楼女子和女子学堂白衣黑裙的女学生。弦断时我掩面而泣。

那将是我一生中最真实也最廉价的泪水。

我眉眼漆黑，肌肤如玉。我生来不是为了被爱。只是为了尖叫和哭泣。

你要收留我，并容忍我飘忽的一生。

我将洗尽年华，布衣荆裙，把祖传的金钗当了买酒喝。你一杯我一杯很多很多杯。我和你喝过很多很多的酒，然后我就是你的人了。

天下女人就只有这一句台词。

你不会以为我下贱，你要与众不同。我的良人。你会爱上脸色苍白命如纸薄的我。如果你要放弃我，你一定要和我喝过许多酒，然后，一剑剌穿我的胸膛。我解开相思结的衣襟，你要置我于不生之地，你要很快，我才不会痛苦。也不会悲伤。

你不要把我交给岁月，不要让我和写怨词的女子们一样老去容颜。你不要让我不宽恕你，也不宽恕自己。你要关心我，让我避免成为别人的情妇。我不愿成为娼妓，与他们寻欢作乐，共渡云雨。

（四）

“一夜春宵也是可能的”。我们的寿命是五百年，五百年来我们都如此悲伤，死亡和幸福一样遥不可及。

而你竟然已经死去。

我终于明白了这个简单和重要的事实。

那个把你从我身边拿走的人也会死，她将死于多年后的－－寒冷的秋天。她会在分娩时，流血不止而死。她会死得象一个完全真实的女人。从来没有过那么多的血，淹没她，却永远不能沾染你洁白的衣衫。

你那时必须作为一个陌生人，以你特有的冷漠站在很远的地方。她的血无法触及你长衣，

就像我的血始终无法到达你一样。

从一个秋天到另一个秋天，所有和你肌肤相亲的女子都要受到诅咒。而我，我远远地冷漠地爱着你们，象一个阴谋，悲伤而愤怒。

（五）

我不能给你钱，不能你给性，只能肤浅地抱着你。让我们一起冬眠，象善良的熊，不刷牙，不吃早餐。

让我们冬眠，冬眠。

廉价的食物，廉价的脂粉，刚刚唱了一宿的歌，客人刚刚离去。

地上的雪还没融化。

我想睡了。请给我一张床。

我要睡了。凌乱的头发里全是你的烟味。我呼吸困难。

陌生人，等到雪化的时候，我们就到南方去，那里温暖一些。

屋子太小了，我们只能并肩而卧，你来不及替我脱下衣裳我就睡着了。你哭着说“要么离开，要么和我做爱”。

可是，我唱了整宿的歌，客人刚刚散去。地上的雪还没有融化。

我喜欢你带我回家，喜欢亲吻包围着你的冰冷的空气。

陌生人，你是这个城市里我最关心的人。我不能离开，我怕你害怕。

但我不能和你做爱。我已经有了心爱的人。在他的葬礼上，他刚刚挽着我跨过他渐渐冷却的尸身。

我无法给你钱，也不能和你做爱。尽管你因为失业、贫穷和情欲彻夜不安。如果你抛弃了丰腴的前任女友，转而爱上你的邻居－－一个说话大声的妓女，你要尊重她，你可以和她做爱，但不要和她说话。不要在夜里向我索要爱情和温暖，别用你敏感和遥远的乡愁折磨我。我疲惫不堪。

让我睡去。

你知道女人想要什么？她只要幸福和死亡。在满是面包屑的窄床上，握着我赖以生存的琴，让我好好地睡一觉。无分昼夜。

不要脱去你的衣裳，也不要褪下我的。请与我并肩而卧，亲如兄妹。让我做一个纯洁的妇人，与无辜的你共度寒夜。让我在你的地下，不再爬出地面，不再认识他们。如果我默然死去，请不要抛弃我的尸身，不要让我爱的人看到我的死。还有，你要说，你死得美丽，比他们美丽。

（六）

渐渐地，我成长为一个为圣洁的女人，就象古希腊黑铁时代献出贞操的女子，虔诚地等待着丈夫远征归来。年代久远的史诗里，苦守二十年的妇人不曾老去分毫。她们依然坐在纺车前，毫无欲望。她们不停地纺纱，用机智和沉默拒绝求婚者无礼的纠缠。

盲目的诗人，年迈的诗人，睿智的诗人！

你忘了告诉我理由，而只是描写了结局：回归的战士杀死了求婚者，他们收回了名下的土地、牛羊、义仆和妻子。我多么羡慕那些拥有美貌和田产的女子，因为她们永远都不会被放弃，永远不会失去贞洁。

一个情人和另一个情人的方式究竟会有什么不同？我愤怒地逃离那些熟悉的黑暗角落，喃喃自语并厉声地诘问。

一切抚摸都是合理的，你甚至保持了我的完好。

你可曾见过莫迪利阿尼的蓝衣女子，脸部狭长，写满优雅而平静的忧伤的平静。意大利的法国画家，死于年轻的肺结核和放荡。他的妻子在第二天死去，象蝴蝶一样从高处下坠，杀死一个年轻的女子和四月的胎儿。

（七）

98年的夏天，初次和一个陌生人调情。他刚在院子里洗过澡，推门而入。

我起身，象一个小小的女孩子，迎向他。

我闻到，他身上淡淡的香皂味，和你一样。

请你与别人相爱，她们美丽并且性感。

但是我想要你，他严肃地拍着我的胸口。

我什么也没有，我小声哭泣。

嘘－－别让邻居听见，他说。

我们听到邻人在院子里走动。

你会不会爱我？我摇摇头，抱歉地说，不会，我永远不会爱你。我永远不会爱上别人。

我只是一个诗人，我不酗酒，也不吸毒。我只是寻章摘句，矫揉造作。

海子、凡高、柯尔德林，短命的诗人们，我向你们致敬，而我将在82高龄才得以死去。

（八）

我们将以一流的处女建设世界一流的大学。

“不，你错了，我不是写民谣的，也不写非主流”。我只在毕业之前严肃地论述一个重要的问题。是时候了，我们必须烧毁这个城市的所有垃圾。我有翔实的数字以及确凿的证明，但这个学院所有德高望重的教授都愤怒地谴责我。“请为我们的子孙留下一点绿色”，他们头发花白，痛心疾首。

我谦恭地等待他们的裁决，在爱情和学术上，我同样犹豫不决，固执和偏执，然而毫无过错。和这个城市里所有飘落的杨花一样，我善良、懦弱，然而毫无过错。

终于有一天有人来通知我拍毕业照。

我谦恭地端坐于像机之前，想象着你，你始终没有前来和我相遇。你只在岁月的幕布之后注视我，殷切地注视着我慢慢长大。

这个学校每年都接纳大量的年轻女孩，和我当年一样，仿佛生来是被男人所宠爱。你可以爱上他们，就像多年之前爱上我一样。

你只要一只手轻轻落在她们的肩上，另一只手撩起额前的头发。你就可以抚摩她们的乳房，喏，就是这里了。它们为你的抚摸而设置。它们饱含了女孩子的岁月和梦想，它们是否令你感到忧伤。

我们将先后毕业于这所著名的工科大学，“为祖国健康工作五十年，请注意使用安全套”。你就在这个城市里，在人群中推推搡搡，每一个街的拐角我都会伫足翘首。

可是每一个人都不是你。

