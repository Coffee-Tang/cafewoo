<p>童年是我一生中最快乐的时光。其中最精华的部分要数太阳日下午父亲和叔叔坐在九湖边，和着满眼的浓绿与口、鼻、耳、腔内充斥着九湖独特的香味，品白葡萄酒的韶光。我和小仆庄哥则在草丛间嬉戏扑蝶。那时的太阳是绿色的。</p>

<p>当眼前的最后一抹绿淡入苍穹之际，我突然间明白自己已不再是个少年了。
<br/>明天的太阳是什么颜色的？真希望是绿色的。因为我的父亲——九城城主——在看过我幼稚的涂鸦后，指着画纸上的绿太阳问，这是什么？我答，太阳。语气自豪而灵动。象征童年的绿不能伴我一生，就像父亲母亲乃至我的仆人们一般，在那阴冷血腥的杀戮夜之后我只能舔着午夜梦回的蓝色眼泪来回忆当初烂漫的幸福。庄哥，你在哪儿？你为什么不应我？为什么你在草丛前为我追逐蝴蝶的喘息声还在我的耳边萦绕？
<br/>父亲说，九城名出“十过满，九为尊”。九城，一个美仑美奂的城市，一个洋溢着幸福和忧郁交杂气质的蓝色梦幻之城。是啊，九城的夙敌不也正觊觎它散发的芳馥么？如果哀伤可以染红九城的天空，父亲出殡的历日里，九城的天空一定是红色的。然而父亲，请你宽恕我，我的眼泪要迟一些还给你。因为悔恨的泪珠不能凝结成九湖上空的星星。这，也是您说的。我还清楚地记得您脸上的微笑。
<br/>九湖是九城郊外的名湖。因为无论什么季节，它总是蓝色的，所以我更喜欢称它作蓝湖。叔叔抱着年幼的我站在蓝湖边时，说，九湖总有一天会变红的。我注意到叔叔的眼中映着九湖的忧郁。等到忧郁的冷凉触及我手背的时候，我才发现那是叔叔滴落的泪。里面还有幽蓝的光在波动。</p>

<p>“圣王——！”母亲撕心裂肺的哭声把我惊醒了。我呆坐很久了吧，在犹豫什么呢？胸中忽然起了一阵翻腾，天地眩晕，手一抖，是什么跌落在地毯上？哦，那是叔叔口中所谓百年难得一见的“圣药”，也是我给父亲的生日礼物。眼前掠过叔叔的双眸，闪着光。我不知道这意味着什么。叔叔，你看见父亲垂落的头和嘴角的血了吗？他的嘴角甚至还挂着那抹微笑。母亲说，他很幸福，因为这是送给他的第一份礼物。他温暖的目光如湖水般包住我，小心翼翼地，浸透我的每一寸肌肤。
<br/>在那一霎那，我仿佛看到九城上空的太阳变成了绿色。
<br/>父亲说，绿是新生，也枯败的前兆，因为绿无常绿。当我馨甜的梦被一阵阵急促无章的脚步打断时，绿消失了。它在我的前面渐行渐止，我奔跑着努力伸出双臂，企图抓着它，助跑着向上一跃……摊开掌心，睁开眼睛，只有满屋子穿着丧衣僵立的人们，站在最前头的母亲的张了张口，试图用沙哑的声音告诉我：“圣王，殡天了。”</p>

<p>刹那间周遭的黑暗铺天盖地地向我袭来，错骨分筋般的痉挛撕咬着我的每一根神经，我的理智如雪崩般倒塌。是我杀死了父亲。
<br/>那一刻我仿佛又看见父亲湖水般包裹我的目光，在黑暗中一如我企盼的明灯。我急忙睁开眼，却只看见九城上空的太阳如此的耀眼，如此耀眼的红，那是如血般凄绝惨烈的红，万物都在它之下无所遁形。我转过身。
<br/>我看见那双眼睛。
<br/>我看见那双眼睛。
<br/>我看见那双眼睛。
<br/>那是一双即使在血色阳光照射下依然阴暗如地狱般的双眼，眼中因兴奋而暴满了血丝。</p>

<p>母亲尖叫着望着浴血而立的我，和我手中淌的血的匕首。匕首上的血顺着刀脉滴落到水晶铺就的地板，像破碎的珍珠一样飞溅。我忽然想起幼时叔叔滴落在我手背上的泪。捂着伤口狂笑的叔叔眼角也溢出些许液体，像极了九湖深邃的湖水。那眼神慢慢空洞，生命正在离开这个曾经壮硕的身躯。
<br/>“他才是你的生身父亲！”母亲掩泪托着瘫软的叔叔。</p>

<p>我仿佛又听见圣主临死前那句话：宽恕他吧，他所做的他不知道。</p>

<p>    庄哥的脸铁青。他面前的水晶地板上仍跳跃着他失手打翻的圣城之杯，还有渗润大地的九湖之醉。我不知道这两大九城这宝翻腾激耀的翩翩舞动能否与九湖蓝相媲美。真的很美。我偏过头，向庄哥笑了笑——如果可以算是笑的话。笑曾经是我和庄哥默契的标志。可惜的是这次他没有回应我。我孤独。 
<br/>小时候常想，庄哥真好，我情愿他一生都好。 
<br/>在母亲把庄哥逐回老家时，我透过九城的雾盯着他憨憨背影，一步一步一步。唉，庄哥傻了。我最好的朋友终于在母亲的一杯忘忧药之后，彻底地放弃了我们共同的过去。 
<br/>我狂吼，为什么！？为什么这样对他？！ 
<br/>因为他知道太多了。母亲冷冷地回答。 
<br/>多少个日日夜夜，多少次看见绿色太阳……听说他后来成了亲，有了一个可爱的女儿，她的眼中蕴育着深深的九湖蓝，真好，庄哥，真的。 </p>

<p>后来，后来，再后来。九湖水由浅蓝变到深蓝，又从深蓝变回浅蓝，却从来没有变红。它永远平静没有温度，就像庄瑶的手。庄瑶是我后来爱是的女孩。每当我微服去九湖边呆坐的时候，总会看到她形体姣好茕茕孑立的背影。偶尔我还能看到她那双总是若有所思的，像黑珍珠一样的眼睛。她在缅怀什么？抑或在憧憬什么？这个谜后来成就了我一生的思索。我没有询问她，因为她的发际散发的是九湖水的清香。我喜欢在被回忆惊醒的三更时分，闻闻她的发香，默默地喝杯白葡萄酒。
</p>
