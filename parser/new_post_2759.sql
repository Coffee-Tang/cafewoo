刹那间周遭的黑暗铺天盖地地向我袭来，错骨分筋般的痉挛撕咬着我的每一根神经，我的理智如雪崩般倒塌。是我杀死了父亲。
那一刻我仿佛又看见父亲湖水般包裹我的目光，在黑暗中一如我企盼的明灯。我急忙睁开眼，却只看见九城上空的太阳如此的耀眼，如此耀眼的红，那是如血般凄绝惨烈的红，万物都在它之下无所遁形。我转过身。
我看见那双眼睛。
我看见那双眼睛。
我看见那双眼睛。
那是一双即使在血色阳光照射下依然阴暗如地狱般的双眼，眼中因兴奋而暴满了血丝。

母亲尖叫着望着浴血而立的我，和我手中淌的血的匕首。匕首上的血顺着刀脉滴落到水晶铺就的地板，像破碎的珍珠一样飞溅。我忽然想起幼时叔叔滴落在我手背上的泪。捂着伤口狂笑的叔叔眼角也溢出些许液体，像极了九湖深邃的湖水。那眼神慢慢空洞，生命正在离开这个曾经壮硕的身躯。
“他才是你的生身父亲！”母亲掩泪托着瘫软的叔叔。

我仿佛又听见圣主临死前那句话：宽恕他吧，他所做的他不知道。

    庄哥的脸铁青。他面前的水晶地板上仍跳跃着他失手打翻的圣城之杯，还有渗润大地的九湖之醉。我不知道这两大九城这宝翻腾激耀的翩翩舞动能否与九湖蓝相媲美。真的很美。我偏过头，向庄哥笑了笑——如果可以算是笑的话。笑曾经是我和庄哥默契的标志。可惜的是这次他没有回应我。我孤独。 
小时候常想，庄哥真好，我情愿他一生都好。 
在母亲把庄哥逐回老家时，我透过九城的雾盯着他憨憨背影，一步一步一步。唉，庄哥傻了。我最好的朋友终于在母亲的一杯忘忧药之后，彻底地放弃了我们共同的过去。 
我狂吼，为什么！？为什么这样对他？！ 
因为他知道太多了。母亲冷冷地回答。 
多少个日日夜夜，多少次看见绿色太阳……听说他后来成了亲，有了一个可爱的女儿，她的眼中蕴育着深深的九湖蓝，真好，庄哥，真的。 

后来，后来，再后来。九湖水由浅蓝变到深蓝，又从深蓝变回浅蓝，却从来没有变红。它永远平静没有温度，就像庄瑶的手。庄瑶是我后来爱是的女孩。每当我微服去九湖边呆坐的时候，总会看到她形体姣好茕茕孑立的背影。偶尔我还能看到她那双总是若有所思的，像黑珍珠一样的眼睛。她在缅怀什么？抑或在憧憬什么？这个谜后来成就了我一生的思索。我没有询问她，因为她的发际散发的是九湖水的清香。我喜欢在被回忆惊醒的三更时分，闻闻她的发香，默默地喝杯白葡萄酒。








	
	
	
