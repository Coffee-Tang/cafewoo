<br>          其实以现在的眼光看来，老珠子的流氓行为实在算不了什么。一个从未出过山门的山里娃，在大学里疯狂的喜欢上了一名女生。老珠子因打报告申请到厨房烧锅炉，赚得两套伙头的黑棉袄，赢得了“烤黑薯”的称号，即便他使尽了浑身解数，那女孩都只是躲着他，但在背地里却暗暗鄙薄他的土。某一天，老珠子在图书馆遇上她，她坐在长板凳上聚精会神的看书，一点也没察觉老珠子已陶醉在她清淡的雪花膏香味中――那是他一生中闻过的最好的味道。他细细的打量她身体的一切细节，褡袢的白凉鞋，圆润均称的小腿，白皙娇嫩的脖颈……不知不觉的，他的身体象是吸入了乙醇，焕发着从未有过的亢奋，有一股躁热的气流在他周身流窜。她那薄薄的连衣裙勾勒出了发育姣好的身段，他梦游似的站在她身后，也站在所有人的瞳孔里。就在她起身时，他突然发现她的胸部有明显的乳头顶起的浮凸轮廓，身上那股气流已将他所有的理智撕成碎片，在众目睽睽之下，他就那么直通通的一把抓住她的乳fan！直到清脆的一声耳光，众人哗然而上把他揿在地上，他才明白自已做了什么傻事…… 
<br>
<br>           泥洋人形容不会干农活的人宽容的叫先生，但老珠子不属于宽容的范畴，他只能被奚落和嘲讽。农活做不来，犁田倒是将铁犁拗断了几把。耙田时更是糟糕，民选有心要看老珠子出丑，竟破天荒把大牛古借给他使用。牛古没吃饱，又欺生，老珠子第一声吆喝时，那牛古竟诧异的回头望了他一眼，复转头，屹然不动，反刍胃草，老珠子运足了气大喊一声“驾”！牛古优雅的一甩尾巴，将他甩了一脸水，引来不少蹲在田头观赏的闲人的哄笑，民选大笑说先生你没牙黄呵牛都不听你使唤哩！好歹走了步，却总是慢慢吞吞，拐弯时干脆赖着狠啃几口隔田坎上乌嬷的豆子苗。乌嬷闻讯赶来，在旁边虎着脸盯老珠子，再一次拐弯时，牛古竟不管老珠子声嘶力竭的吆喝，也不顾牛鼻圈简直要把鼻子分裂，专心致志的啃豆子苗……老珠子气急败坏之下狠狠一竹鞭，误中牛古臀下�G丸，牛古猛然一惊，竟跃坎跳下，老珠子卒不及防，手中的铁扶耙也脱手拖去，破空滑落时狠狠的扎在牛古臀上，牛身上马上出现了七八个血泉眼，剧痛之下更是落荒狂奔，那铁耙便被拖曳、磕碰、翻腾着在牛身上制造出更多血洞。下坝一马平川的田原，在牛蹄的狂乱下泥浪飞花，鲜红的牛血象数条散乱的长彩带，漂在水田新鲜的混浊中泅散。最后，牛古血尽力衰，被两根朽烂的篱笆一绊，即轰然倒地……一头壮年大牛古变成屠宰场里不值钱的肉，民选已脸色铁青的窥视老珠子的一间老房地基，若不是远房江西表叔出面，老珠子还真的作了人力拉犁的准备。 
<br>
<br>           在表叔的帮助下，老珠子开始往江西偷偷跑生意，不过所谓的生意，无非是贩卖些蔬菜、兔狗等，披星戴月往返于两省间，赚点物价差，终究脸生，又是带些书生气的嫩手，时常会被小贩们耍弄。他还随表叔跑过一次“赶牛岗”，从江西偏僻的农村费尽心机买来二十几头黄牛。比不得肉牛可以不管它们死活用车载，这些耕牛是土命，离不得土的，只能让它们慢慢的走。老珠子和表叔两人餐风露宿，每天要找到草甸子待牛群吃个半饱才能继续上路。一百多里的路，他们走了半个多月！老珠子跟在骚烘烘的牛群后走，性子被磨得简直要发疯。一路上表叔一直向老珠子兜售他可怜的人生观，一个老婆两亩地三餐温饱的低廉，到表叔最后说出要老珠子娶了他那残疾的女儿时，老珠子终于愤然“过河拆桥”，重新独自面对那些奸诈的小贩们。 
<br>
<br>           在一次从江西进年货时，由于货源紧缺，表叔不计前嫌，帮忙张罗了三十只活鸡，半路上又遇上一个拎鸡鸭的老表，老朱子看他似急需用钱，便把价钱压得极低，那老表居然也卖给了。原想今年市面上鸡鸭紧俏，奇货可居，家里先养上一阵子，等年关出售，好多赚一点还债，不料几天内全部发瘟病死掉，还使全村的鸡鸭在过年前一个月也被传染死光――那是泥洋村唯一一次没有鸡鸭过的年。债主们年关索债――尽管有的也就才那么十来元钱；乌嬷在大年三十还冲到家门口来跳大神似的骂。老珠子在众人的斥骂声中，脸色慢慢的失去了红润，胡子以惊人的速度疯长，斥骂声在他耳边整夜整夜的萦绕，房屋四周鸡鸭的烂臭味将他的所有情感催毁贻尽。整个正月，老娘几乎是在家门口跪着过完的，一名撑黑布伞的老师用手帕捂着鼻子，在门口凝视了五分钟，终于象救命稻草漂进了老珠子黑暗的空间。 
<br>
<br>           说来九一老师也算是老珠子拐了七个弯的表亲戚，他以前也曾想过让老珠子来小学代课，却因为老珠子的流氓罪实在让老师们忌讳而作罢，现在看到老珠子和老娘的年过得着实凄惨，动了隐恻之心，便向已经开始接受牛仔裤的老师们极力说服。总算从厨房伙头兼一年级语文课开始，三十元一个月工资，连工带教过了七年。在这七年中，老珠子沉默寡言，起初人们还会提起他以前意气风发的青春模样，到后来看他整天闷不吭声，小孩子捡了小石子丢他也只是傻笑一下，便直称老珠子为那个小学伙头了。他甚至承包了学校的屎缸卫生，每到周未便戴着口罩一担担粪便挑得一路臭气；有些年少气盛的师范生不满学校的分配，拿老珠子来出气，他低着头，不无认真的听从训导，一脸不知所衷的谄笑。九一老师偶尔也含蓄的提醒老珠子做人要有点脾气，有点自尊，但时间一长，也就懒得说了。某个冬天，调皮的四年级学生调唆了发颠的四姑婆说伙头骂她，她冲到小学厨房，持着撖面杖粗的木棒追打，身材不算矮小的老珠子惊恐的满学校跑，破袄棉絮满天飞扬……在好一段时期，伙头一词还成了泥洋村最流行的损人词语，它代biao着无能、怯懦、低贱、龌龊、下作……事实上，老珠子教的学生成绩虽没有名列前茅，但受他版书影响，字都写得漂亮、方正。有时人们提起泥洋小学学生的书法水平普遍不错，想到那个伙头，终究他是个上不得桌面的代课老师，对外宣传不得。即便学区有领导光临，问起是谁的功劳，老师们也是含糊其词，支吾过去。有一次学区校长下访泥洋小学。就餐时，学区校长问到这次勇夺书法比赛特等奖的作品倒底是谁的手笔，正好老珠子躬着身上鱼汤，老师们都含着泥汤匙望着本校九一校长，看他如何作答？老珠子竟然忘记了手中鱼汤的滚烫，学区校长听到老珠子的手指被烫得滋滋作响，不禁扭头莞尔一笑：难道是你么？佝偻着背的老珠子激动的一点头，竟然一弯鼻涕坠入鱼汤中。因此落下美名：“老珠子”。隔年，因下海风潮汹涌，院墟中学老师青黄不接，需一批代课老师，向小学借用，学区校长在开会时无意中回忆起鱼汤旧事，脸有笑意，被其他老师“揭发”，便供出了老珠子。这时老珠子已快四十岁了。 
<br>
<br>           在四十岁以前，邻近没有任何姑娘肯嫁给老珠子，也不知道他成年以来性问题是如何解决的？促狭的泥洋人编造出许多关于老珠子性方面的逸闻，有说他老是偷窥妇人喂奶;七十九的金秀奶奶在自家篱笆上丢了件侧面襟的秋衣，还几度踱到老珠子门口张望；老八古说每天晚上那个伙头名义上是为猪嬷加夜料，实际上从他穿着背心短裤就可以想象他真正是要干什么丑陋事――明山还信誓旦旦的说的确看过伙头的猪嬷下过一窝人头猪身的崽，连猪嬷自已都不认，全部被它吃回肚去了！也有的说那个伙头总是爱把人家的牛嬷往林子里赶……这倒冤枉他了，他顶多只瞟过它们的性器几眼，并作了些臆想。还有鸡鸭狗之类的，银真嬷她们女人捉起鸡嬷手指伸进去搜蛋是天经地义，但轮到那个伙头身上，手指便成了另一样龌龊的器官；有时这些非议倒是类似于一种提醒，但他毕竟还是艰难的熬过来了。 
<br>
<br>           院墟中学是老珠子新生的开始。中学很多年轻新老师，同批来代课的小学教师大部分也是不认识的，人们似乎已经淡忘了他的经历。校领导班子对代课老师们寄予极大的希望，要他们尽量发挥自已的才干。老珠子开始执教不同年段的学生，并当上了班主任。年底娶了一个女人，叫金玉良，大他三岁，因为不会生育而被前夫抛弃。老珠子无所谓孩子，而且玉良嬷喜欢用的也是百雀灵雪花膏――他一辈子都怀念那味道。他勤恳教学，学生成绩也不错，从同事们偶尔请他聚餐和学生们对他的尊敬中，他慢慢的找回了自已，恢复了脊背的挺直。在校外租了间民房，偶尔也接老娘下来尽尽孝。 
<br>
<br>           某年春夏之交，平坝学区发生一起影响恶劣的师生恋，因为老师竟和学生私奔海南去了。全学区严肃整顿校风校纪，院墟中学照例举行全校性大会，所有班主任带班坐在前排聆听训话，校长、副校长、教导主任、各级年段长逐一发言，在校长着重强调女生绝不可单独到男老师房间时，左眼皮发痒，抓挠了一下，刚好眼风就瞟了老珠子方向一会，台下学生扭头眼神紧跟，竟引起一片苍蝇般嗡嗡私语。老珠子脸色煞白，虽然心里明白校长也许并非故意，但此刻的难堪实在是扫荡尽了他多年以来苦心经营的脸面。而且关于他犯过流氓罪的事实象星火燎原在学校迅速蔓延，还传走了样。少数维护他的说只是别人对他的一种污蔑，其实他并没有耍过流氓，是被陷害的，那女生自已作骚勾引老珠子。更广泛的是演进成强奸罪――那可是最下作的事。因此，也不管事后校长委婉的表示歉意，心中蓄下了狠，趁校长向其询问校舍扩建规划时，暗暗将明细帐复制了一份，惊喜的发现校长不仅有将公家钢材拉回家盖房子的隐私，而且还掌握了他贪污二万余元的真凭实据。后年，老珠子侄儿竞争院墟中学校长一职，他轻描淡写便将侄儿的对手踢下马来。 
<br>              
