Schaffhausen像欧洲其他地方的小镇一样，一条商业主街就是整个镇的中心。在这样的商业街上，即便是麦当劳，通常也只会开在一间外观老旧的屋子里。大概因为今晚是平安夜的缘故，街上的人并不多。街头艺人用竖琴弹奏着圣诞节的歌曲，欢快清脆的琴声传出很远。

按图索骥找到了那对老夫妇所说的古堡，古堡建在山上，要走上一段长长的石阶才能到达。古堡的外观就像是一个装生啤的酒桶，不同的是酒桶顶上开了四个大洞。古堡内的结构也很简单，白天的照明就靠从壁上的箭剁口和顶上的四个洞漏进来的光。依此判断建古堡的初衷只是为了单纯的军事防御。从堡内一侧的旋转石阶可以上到顶层，在那里可以俯瞰整个小镇。顶层上排了不少长椅，四个大洞也都盖上了粗铁网，估计这就是那位老太太所说的舞池了。有趣的是顶层的造型与常见的圆顶截然相反，像个浅浅的漏斗，四周高，中间低。我想这样的堡顶实在是宜古宜今，因为从前在古堡被围困的时候，它能用于积蓄雨水，现在它又能保证人们在跳舞时不会因为转得太欢快而从古堡边摔了出去。

下了古堡，心情舒畅，回到主街上。两天的旅行让我感到瑞士人不论男女，都极爱抽雪茄，不论是室内还是户外人多的地方，都能闻到浓浓的雪茄味。我又一次向自己的好奇屈服，拐进一家雪茄店，把它当博物馆一般参观了各式各样的烟丝和烟斗，最后决定买上一盒。迫不及待抽出一根点上，难抽无比，几欲昏厥。

