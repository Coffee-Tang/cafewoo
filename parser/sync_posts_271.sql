我弹我最喜欢的《蝶恋》给她听，她说我弹琴的时候一定有一个很安静的心，我点头，她又忽然问我有没有想过把这一刻的安静留下来，永远地留下来？
我不知道怎么回答。
后来她拉我去她家，来到她的房间，她的口琴摆在最显眼的位置，她象宝贝一样拿起来，银白色的，看样子是一架很好的口琴，接着她又拿出许多录音带，很神秘地说，还记得我问过你可不可以把一刻的心情留下来，看，这就是我所有的心情。她开了录音机，口琴声传出来，一首接着一首，有得很悠扬，有的很悲伤，就算是同一首曲子给人得却是不同的感觉。然后她告诉我关于一首首曲子的故事，关于她的快乐她的悲伤，还有那个深深伤害过她的男孩子。我静静地听，没有插话，看着她的笑脸和她的眼泪，最后她说，能不能让我听听你的心情呢？
我点头。
我花了整整3个月的时间录了很多首我自己弹的曲子，很细心地录，不容有一点瑕疵，还有那首她最喜欢的《蝶恋》，当我把带子交到她手里的时候，她俏皮地问我，这里有我的什么心情呢？我一字一顿地说了五个字“认识你真好。”她又笑了，紧紧拉着我的手。

