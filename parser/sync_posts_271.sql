我不知道三楼到底住着谁，初来咋到的不好去打听。
以后就常常看到她的背影了，我觉得她的头发很漂亮，肯定很黑很顺滑。我还喜欢她关灯的时候头发一甩的姿势。背影我是熟悉的，但是人却是很陌生的。
和朋友在KFC里吃饭，忽然看见一个同楼的女孩走来，相对笑笑，经常见面就当是认识吧，可是从没说过话。她点点头，转身走了，转身那一刹那，她甩头发的姿势让我好熟悉，我一惊之下，站起来很大声地问她：“你，你是那个吹口琴的人？”
后来她笑我问话怎么会问的那么直接，那天她也楞了一下，然后笑了，笑容很透明，她走过来坐在我旁边，你是弹钢琴的吧？那首《蝶恋》是你弹的？
那几天我一直在弹《蝶恋》，调子是我从游戏中听出来的，伴奏是我自己配的，没想到她会注意，我也笑了：“你也知道《蝶恋》吗？”然后我们靠在一起笑，有点放肆地笑。

相遇是缘，这话她说的，她来看过我的钢琴，她说我是一个两面人，单独和她在一起的时候会出奇的安静，她说我不应该把真正的自己藏起来，就象我藏钢琴一样。她很了解我，很少甚至没有人知道我会弹或者我有一架属于自己的钢琴，钢琴上盖着一层布，白色的，上面放满了我所有的娃娃，把钢琴严严实实地遮起来，每个人只会注意我那些很漂亮的娃娃而忘了娃娃下面的钢琴。
