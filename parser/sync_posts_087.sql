<br>　　我曾对乔说，我很清楚自己要什么样的男人。我的判断只需要十分钟。十分钟。会知道我的一生是否会和他有关系。 
<br>
<br>　　如果他能给我带来爱情，那么我的痛苦会受他控制。所以，生命中会邂逅一段一段的十分钟，随时都会有遭受意外之前的预感。所以我相信，每一个有直觉的人，都放不掉他的惶恐。 
<br>
<br>　　乔是一个女子。我们在夜校的英语课上相遇。 
<br>
<br>　　她穿灰绿色的纯棉绣花上衣，那种绿，像潮湿的没有见过阳光的苔藓，寄生在幽凉的墙角里。墙角是能带来安全感的地方，所以我选择坐在她的身边。我们把书本竖起来，埋下头看彼此的手相，恍若回到少年的校园时光。我喜欢她的头发轻轻拂在我的脸上。 
<br>
<br>　　你的手心上没有任何多余的纹路。乔说，你是个可怕的人。 
<br>
<br>　　为什么。 
<br>
<br>　　因为上面写着一些夭折和意外。 
<br>
<br>　　很可怕吗？ 
<br>
<br>　　也许。她的脸上有震慑。 
<br>
<br>　　我淡淡一笑，反捏住她的手指。女人的皮肤柔软清香。就像花瓣。 
<br>
<br>　　上完课，我们去酒吧喝酒，或者只是站在小店铺旁边，买上一杯加冰的可乐。她在一家广告公司工作，有一个做软件的男友，她叫他朝颜。 
<br>
<br>　　我们认识十年了。她说，睡觉的时候我要抓着他的手才可以。你要嫁给他吗？ 
<br>
<br>　　是。我要嫁给他。肯定。我想给他生10个孩子。她笑。天真无邪地把她的脸贴在我的肩上。 
<br>
<br>　　我看着她，微笑，抽烟，不说话。 
<br>
<br>　　小时候我是个沉默的孩子。一个沉默无语的孩子会带来恐惧。如果她在该笑的时候没有快乐，该哭泣的时候没有眼泪，该相信的时候没有诺言。她有残疾的嫌疑。 
<br>
<br>　　我喜欢花朵，喜欢把它们的花瓣一片片撕扯下来，留下指甲的掐痕，或把它们揉成汁水。 
<br>
<br>　　我不明白它们为什么会没有血液。这是不知道疼痛的生命，让人陡生恨意。 
<br>
<br>　　母亲常常在一边，独自抽烟，神情淡漠地看着我。她是个眼睛幽蓝，笑容悲凉的女子，她把我当成她的同龄人，而非孩子，因为她是与众不同的母亲。 
<br>
<br>　　第一，她很孤独。第二，她没有结婚，第三，她在我12岁的时候死了。 
<br>
<br>　　那个夜晚我第一次看见朝颜。他是一个短发喜欢穿黑色衬衣使用爱立信手机的男人。他是乔的男人。 
<br>
<br>　　他告诉我他喜欢爱立信的原因。因为它的辐射大。他说。我想让自己早点长脑癌，然后可以颠倒地思考这个世界。他的牙齿很白，笑起来的时候，唇角温柔地倾斜。他有干净的眼神。水一样干净而流动的眼神。 
<br>
<br>　　我笑。乔也笑。我们三个人走在夜校放学后的路上。她左手搂着我的肩膀，右手搂着朝颜的脖子，有时候她快乐得似乎歇斯底里。我知道这样的纵情下面隐藏着什么。乔是毫无预感的女子，所以她的眼角下面有泪痣。但我能识别眼睛幽蓝的女子。她们是苔藓。黑暗给她们水分，生命甜美而脆弱。 
<br>
<br>　　我们去的酒吧叫LIFE. 生命是幻觉。我问老板要威士忌加冰和555 香烟，然后坐在吧台边，看乔在舞动的人群里像鱼一样游动。 
<br>
<br>　　朝颜说，我和她十年。 
<br>
<br>　　我说，我知道。 
<br>
<br>　　我一直在想我是否真的能够给她带来幸福。 
<br>
<br>　　很多事情不需要预测。预测会带来犹豫。因为心里会有恐惧。 
<br>
<br>　　你看起来好象从来不会有恐惧。他在昏暗的光线下看我。 
<br>
<br>　　那是因为我知道有些事情在劫难逃。 
<br>
<br>　　在劫难逃？ 
<br>
<br>　　是。打个比方，比如你遇到乔，乔遇到我，然后我又遇到你。 
<br>
<br>　　我笑，对他举起手中的酒杯，轻轻碰他的啤酒瓶，cheers，朝颜。 
<br>
<br>　　他也笑，抬起头喝酒。 第一次跟着朝颜去他在西区的房子的时候，是台风的天气。 
<br>
<br>　　我对他没有任何目的。只是我想我的时间无多，10月份乔将有可能成为别人的新娘。但是她不应该离我而去。 
<br>
<br>　　那幢颓败破旧的法式洋楼，走上木楼梯的时候能听到咯咯扭曲的声音。为了不吵醒房东，我把鞋子脱下来拎在手里。 
<br>
<br>　　黑暗中听到风和云层掠过城市天空的声音。寂静无声，让我想起童年时通往母亲房间的那段楼道。她从不拥抱亲吻我，她带陌生的男人回家，她不会告诉我原因。在失眠的时候，我光着脚走在沾满灰尘的楼道上，听到她房间里的声音或者她歇斯底里的哭泣，犹豫着，徘徊着，最终只能蹲在墙角捂住自己的耳朵。我渴望她的皮肤靠近我。 
<br>
<br>　　我转过头看朝颜。我的眼睛凝望着他。 
<br>
<br>　　朝颜的神情带着狼狈，他说，未央，我没有想过要爱上你。 
<br>
<br>　　我微笑，我也没有。我说。 
<br>
<br>　　但是我已经知道什么叫在劫难逃。他叹息。他的嘴唇轻轻地压在我的眼睛上。他的气息和拥抱覆盖了我。我听到自己手里的鞋子，陡然地掉落在地板上。 
<br>
<br>　　那是一双有白色丝带的麻编凉鞋。 
<br>
<br>　　我从不穿高跟鞋。 
<br>
<br>　　母亲有很多双高跟鞋。她把它们一双一双地排在柜子里，有丝绒的，绸缎的，软皮的，刺绣的，珠片的……细高的鞋跟流泻突兀的凄艳。她光着脚穿它们，有时候她独自在房间里走来走去，地板发出寂寞的扣击声。她是美丽的女子，可是在她最美好的时候，她爱的男人不在她的身边。 
<br>
<br>　　那个男人是什么样的，她没有告诉过我。可是我知道，他曾经喜欢她穿着高跟鞋的样子。 
<br>
<br>　　他给过她无法遗忘的记忆。除了承担和诺言。 
<br>
<br>　　我想抓住一些东西，她笑，所以我抓住你，但后来才发现我的后悔。因为对不爱我们的人，不能付出。一旦付出，就罪孽深重。 
<br>
<br>　　你就是我难以逃脱的罪。她会突然地尖叫，失去控制，然后她的鞋子一只一只地扔在我的身上。她追着我跑。她的脸上都是泪水。她的浑身都在颤抖。 
<br>
<br>　　这样的愤怒不断地循环。她除了孤独，就是我。我是她唯一的爱人，敌人，对手，朋友。 
<br>
<br>　　终于她疯了。 
<br>
<br>　　凌晨的时候我回家。朝颜睡得像个孩子，我没有亲吻他。走到大街上的时候，发现风势凌厉，树叶满地打转。天空被吹洗得清澈异常，大群大群白色的云层急速地掠过，掠过这个孤独的城市。我躲到街角的夹缝里，给自己点燃了一枝烟，然后沿着空荡荡地大街往前走。 
<br>
<br>　　冰凉的雨滴，大滴大滴地，间断地，打在我的脸上。 
<br>
<br>　　在公用电话亭，我给乔打手机。她在睡觉，声音模糊。我说，乔，你准备在10月结婚吗。 
<br>
<br>　　10月的确是好天气。 
<br>
<br>　　不要和我在台风夜晚商量这个问题。乔懒散的声音。 
<br>
<br>　　男人不爱女人。他们只是需要女人。比如他生病了，明天一早你得去看他。 
<br>
<br>　　他打电话给你？ 
<br>
<br>　　是。因为他找不到你。我轻轻地吐出烟雾。9 月我要带你去北京。 
<br>
<br>　　我们去北方。乔。记得我的话。 
<br>
<br>　　我挂上了电话。 
<br>
<br>　　我有把握第二天的下午会有人来找我。打电话过来的是朝颜，他的声音很疲惫。乔看到放在我床上的手镯。我不敢告诉她，这是你的东西。 
<br>
<br>　　这的确不是我的东西。我说。我从不戴首饰，她知道。 
<br>
<br>　　她要离开我。 
<br>
<br>　　我无能为力，朝颜。 
<br>
<br>　　你爱我吗。他说。 
<br>
<br>　　这是我不愿意回答的问题，抱歉。 
<br>
<br>　　我想娶你为妻。我沉默。他深深叹息，然后他说，我知道你的孤独。 
<br>
<br>　　电话里响起断线的盲音。消失不见。 
<br>
<br>　　晚上乔来找我。她什么也不说，只是躺在床上蜷缩着身体。黑暗中她有轻微的颤抖，我走过去，把手放在她的头发上。我说，乔，离别有这么痛苦吗。如果我们一直是在离别中，比如和爱的人，和伤害，甚至和时光……一切又有什么不同。 
<br>
<br>　　乔背对着我，冷冷地说，我讨厌欺骗。 
<br>
<br>　　12岁的时候，我曾祈祷上天能让我迅速长大，这样我可以控制母亲，这个眼睛幽蓝，笑容悲凉的女子。我爱她。可是她疯了。她每天都会突然地爆发，把高跟鞋到处乱砸，我的头上脸上常有伤疤。我要读书，我要恋爱，我要有人亲吻和抚摸我，我要升上大学有一份工作有自己的家，我要去远方看看大海……。我听到无声的哀求把我的心脏顶得破碎。我独自在黑暗中握着满手心的花瓣，用力把它揉干揉碎，满手汁液…… 
<br>
<br>　　母亲一星期以后死了。她穿着她的高跟鞋走路，刚走到楼梯口，鞋跟断了。 
<br>
<br>　　她尖叫着伸出双手，想抓住能够阻止下滑的物体，但什么也没有抓住。摔到楼梯下面的瞬间，她的头碰撞在墙上。她的血喷射在墙上，在此后的5 年里，那面被洗得斑驳的墙壁每天散发出浓稠的腥味。我每天夜晚一边流泪一边用湿布擦洗它，直到我终于17岁了。我长大了。 
<br>
<br>　　我离开了那个南方小城，来到上海。17岁以后我再没有眼泪。 
<br>
<br>　　有谁能够相信我的第一个男人是朝颜。 
<br>
<br>　　我没有让他看到我身体里面流出的血，我怕它是蓝色的。暗蓝暗蓝的颜色充满孤独的负罪。我已经不是童年的小女孩，我想我在憔悴和苍老中。可是在我最美好的时候，我爱的人不在我的身边。 
<br>
<br>　　朝颜。我想起他的气息和身体，他温暖的手覆盖着我的皮肤。从来没有人拥抱我，没有人亲吻我……这是我唯一的男人。 
<br>
<br>　　9 月终于来临。他打电话给我，他说，公司想公派我去日本工作两年。如果你愿意嫁给我，我就留下来。 
<br>
<br>　　我说，你错了。我爱的是乔。 
<br>
<br>　　如果你想让我走，我会离开。两年以后如果你还没有嫁人，我要娶你。 
<br>
<br>　　我挂掉了电话。 
<br>
<br>　　台风过去。秋天的天空是清澈的蓝，阳光温暖，空气凉爽。我想去北方。 
<br>
<br>　　乔变得憔悴和颓丧，每天晚上流落在都市夜店，快天亮的时候才醉醺醺地回来。我喜欢所有眼睛幽蓝，笑容悲凉的女子，她们像我的母亲。包括母亲手指皮肤上的清香。那曾经在我的手心里被揉出汁液的花瓣。 
<br>
<br>　　我脱下她脚上的高跟鞋。我把它们一个一个地扔出去。我说，我的母亲穿着高跟鞋摔死了。因为她曾经爱过一个男人，那个男人喜欢她穿这种鞋子。她为他孤独，为孤独而疯狂。 
<br>
<br>　　她死了？乔把脸埋在床上模糊地发出声音。 
<br>
<br>　　是的。她必须死。因为生命对她已经没有意义。 
<br>
<br>　　是你要她死？ 
<br>
<br>　　我只想让她脱下那些鞋子。那些会突然地打破我的头的鞋子。那些已经不再有爱情残留的鞋子。 
<br>
<br>　　乔伸出手拥抱住我。她的长发盖住了我的脸。她哭泣。她说，我知道，是你杀了她。 
<br>
<br>　　我尖叫：我没有，我没有。我说，我只是不想让她痛苦，为什么，为什么，她要一直穿着那些鞋子？！！ 
<br>
<br>　　乔扑过来，紧紧地抱住我的头。她把我的脸压在她的肩头上，她说，不要恐惧，不要害怕，亲爱的，我在这里……她的嘴唇贴在我的头发上。 
<br>
<br>　　我推开她。我说，我不相信你。我拉住她的头发，把她拖到阳台上，然后让她的身体仰后靠在铁栏杆上。当风吹散她的长发，乔发出恐惧的叫声。 
<br>
<br>　　我说，告诉你自己，男人是不可靠的。你要和我在一起。 
<br>
<br>　　乔在恐惧中哭叫，可是我爱朝颜，我每天都在想念他，我想和他结婚。她的眼泪飘落在大风中。 
<br>
<br>　　我放掉了她。看着她掩住脸跪倒在地上，我说，他爱的是我，不是你。他要去日本了。 
<br>
<br>　　你永远不再会见到他。 
<br>
<br>　　朝颜离开上海的时候已经是深秋。我去送他。 
<br>
<br>　　他伫立在机场的人群里，背着包，寥落的样子。他把他的手机递给我，这个留给你用吧。 
<br>
<br>　　我打开盖子，看到上面还留着一张发黄的即拍得的小照片，乔甜美的笑容，朝颜从背后拥住她，下巴贴着她的耳朵。我笑。轻轻地盖上盖子。 
<br>
<br>　　我说，乔现在留在我的身边，你可以放心。 
<br>
<br>　　他说，我能为力，你知道，未央。 
<br>
<br>　　我说，我知道。 
<br>
<br>　　遇到你是我的劫难。朝颜说。你是一个破碎的女子，未央。你所有没有来得及付出的感情。 
<br>
<br>　　我微笑。可是你要娶我。 
<br>
<br>　　是的。我要娶你。 
<br>
<br>　　两年以后你还会这样想吗。 
<br>
<br>　　他低下头，抬起脸的时候眼睛泪光闪动。 
<br>
<br>　　200 年以后我还会记得那个台风的夜晚，楼道上你回过头来看我。 
<br>
<br>　　你光着脚。 
<br>
<br>　　我微笑。在任何我难过或者快乐的时候，我只剩下微笑。他又拥抱我。呵，有很久没有人拥抱我。我把脸紧紧地埋入他的胸口。他的心跳强劲有力，他的气息温暖清晰。我唯一的一个男人。他走了。 
<br>
<br>　　可是我已经有了他的孩子。 
<br>
<br>　　我决定去北方。要带着乔走。 
<br>
<br>　　在上海我会有可能失去她。因为她日渐憔悴。 
<br>
<br>　　每天晚上她四处游荡，一次在酒吧喝酒闹事，被警察抓走。我去拘留所带她回家，一个人转了很多车，冒着雨跑到那里。乔一声不吭地蹲在墙角。她的浓妆残缺肮脏。披散着头发，裙子被撕破，脸上有玻璃碎片划过的血痕。 
<br>
<br>　　乔，跟我回家。 
<br>
<br>　　她慢慢抬起头，她说，为什么你一定要和我在一起。 
<br>
<br>　　因为你像我的母亲。 
<br>
<br>　　我知道她已经死了。 
<br>
<br>　　是的，她死了。她是因为孤独而死的。所以我要你和我在一起。 
<br>
<br>　　我要带你走。 
<br>
<br>　　你和她一摸一样。我爱她，乔，你明白吗。她是我唯一的朋友，唯一的亲人。 
<br>
<br>　　但是，为什么，为什么，你要选择我？乔推开我，她流着泪笑。 
<br>
<br>　　因为这是宿命。乔。 
<br>
<br>　　这是你逃不开的宿命。 
<br>
<br>　　你以为你能控制我吗？她冷笑。 
<br>
<br>　　我的耳光用力地扇过去。我说，我能够控制你，乔，你要清楚这件事情，我能控制全部。 
<br>
<br>　　她的脸靠在墙上发出崩溃的哭泣。 
<br>
<br>　　我们的机票订在晚上。从上海到北京。 
<br>
<br>　　乔和我坐在候车大厅上。我的肚子稍微有些隆起，所以我已经不再穿牛仔裤。 
<br>
<br>　　我穿淡粉色的厚粗布裙子。我已经找好房子和工作，我也依然能够写作。还有乔。 
<br>
<br>　　我爱的人。 
<br>
<br>　　那天她还是穿着我第一次见到她时，那件刺绣的灰绿棉布上衣。 
<br>
