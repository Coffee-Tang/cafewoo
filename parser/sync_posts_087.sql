<br>　　ROSE在北京。我在上海。我们一直以EMAIL 联系，从未见面或致电。我不知道她的性别，只能暂时认定她为女性。也不知道她是否比我年轻，但这些都已经不重要。有时候身边很多熟悉的人，他们却只如空气般的存在。 
<br>　　请看她在我发出EMAIL5分钟之后给我的回复。亲爱的VIVIAN，我如此依赖你，你好象在我隔壁办公，而且从不曾让我失望。 
<br>　　我微笑。此时已过深夜11点，别人看完电视，许是打着哈欠洗脸刷牙准备上床。而我一天的工作，刚刚开场。窗外的天很蓝很深，五月的夜风清凉里面已经有醺然的暖意。光着脚坐在大藤椅上，一杯泡得浓黑的咖啡，红双喜的特醇香烟，还有空白的电脑文档。我的工作就是在寂静的空气里，听着自己的手指敲击在键盘上，直到把眼前的那一面空白用黑字填满。 
<br>
<br>　　我是以卖字为生的女子。在我25岁的时候。 
<br>　　如果时间倒退五年……也许依然只能如此。 
<br>
<br>
<br>　　2 遇见绢生纯属偶然 
<br>
<br>　　很多女子的25岁，应该会有一个自己的家。即使是小小的家，只要放得下自己的一橱衣服和从小抱着睡的枕头，也会心安。有一个男人。临睡之前他的手指抚摸在头发上，可以闻着他脖子皮肤上的味道闭上眼睛。还会有一个孩子，从此这颗心就放在了身外，跟着另一个人晃晃悠悠。 
<br>　　而我的25岁。我单身。靠着一台电脑和数位杂志编辑的电子信箱生活，并养了一缸热带鱼。 
<br>　　那些美丽的小鱼，它们睡觉的时候也睁着眼睛。不需要爱情，亦从不哭泣。它们是我的榜样。 
<br>
<br>　　ROSE偶尔在EMAIL 里对我说，亲爱的VIVIAN，为什么你的爱情小说总是以分离告终，虽然我喜欢你的文章，但依然困惑不已……我给她回信，亲爱的ROSE，那是因为我曾经被很多男人欺骗，遭受种种劫难，心如死灰……一边打字与她调侃，一边笑着抚摸自己裸露在空气里的冰凉的脚趾。 
<br>　　爱情，那是很遥远的事情了。15岁的时候，和班里的男生恋爱。纯纯的恋情。冬天的黄昏，在自己的房间里，看着他的手笨拙地伸入到胸前，他的呼吸有柠檬的清香。还有他喀哒喀哒响的旧单车，坐在前面的横杠上，他的嘴唇轻轻贴在头发上。美丽的诺言让人看到海枯石烂……10年过去，如果再对爱情欢天喜地，执迷不悟，那才叫可怕。 
<br>　　我想我的生活估计是到不了头。 
<br>　　我所要的，只是一个人。能在我睡觉的时候，轻轻抚摸我的膝盖，把我蜷缩起来的身体扳直。 
<br>　　如果没有，那么一切继续。 
<br>　　虽然有时候我恐惧白雪茫茫般空洞的生活到不了头。 
<br>　　直到我遇见绢生。 
<br>
<br>　　遇见绢生纯属偶然，但非虚构。虚构是我文字里的概念，如果没有虚构，我就无法得到食物和住所，无法像任何一个正常的路人，行走在城市高楼耸立的大街上，即使不踌躇满志，也可以心定气闲。 
<br>　　我喜欢城市的阳光透过污浊的空气和阴冷的楼缝，轻轻抚摸在脸上。 
<br>　　我喜欢在吃完一顿丰富的晚餐以后，想起还可以去哈根达斯买一杯瑞士杏仁香草冰激凌。 
<br>　　自然有时候我的生活也会变得糟糕，比如在这三个月里，一共：抽掉30包红双喜，平均每三天一包烟。由于买烟的地点杂乱，常常抽到假烟。假烟带来的灾难是头痛和呕吐。可是独自在深夜的时候，它像一场往事，让人镇静，并带来泛滥。 
<br>　　逛了80次街。每天下午醒来，在深夜之前的这段空白，时间必须大量挥霍。坐车到陕西路，然后步行至淮海路。有时候只是坐在太平洋前面的石阶上，看着陌生人走来走去。然后在STARBUCK买咖啡。然后往回走。 
<br>　　泡吧50次。有2 次因为滥醉而爬到桌子上。5 次被人拖上出租车送回家。 
<br>　　约会过10个男人。无疾而终。 
<br>　　卖力地写作。写了40万个字，卖掉30万个字。 
<br>　　吃掉镇静剂3 瓶。 
<br>
<br>　　从冬天开始，我的生活就是这样。 
<br>
<br>　　春天到来的时候，我觉得应该找个人同居。仅仅是想更温暖地生活，迎接这个美好的季节。 
<br>　　因为我要努力写稿，争取得到更多的享受，包括我向往已久的去越南和泰国的旅行。或者还可以更远一点，印度或者埃及。我的地点和其他人有所不同。 
<br>　　我决定搬到离市区较近的地方。我在网络上登了一则征求室友的广告。我们可以分担费用。 
<br>　　失眠的时候还能找到一个人说话，即使仅仅是听到彼此发出的声音。万籁俱寂，仿佛失聪。可是我有因为独处而过分灵敏的听觉。 
<br>　　卧室分开。客厅，厨房和卫生间共用。 
<br>　　我留下自己的EMIAL 和电话号码。三天以后收到回音10条。只有一条是对方打电话过来。 
<br>　　你好，VIVIAN，我是绢生。她说。 
<br>　　她的声音仿佛16岁少女一样的清醇。外省人。在一家德国电器公司做事。 
<br>　　我记得我们的对话是这样的。 
<br>　　我说，你现在住哪里。 
<br>　　北京西路。 
<br>　　那里地段很好。 
<br>　　但是晚上找不到水果摊和有热鱼丸出售的小超市。 
<br>　　我会尊重你的自由。包括养宠物或者男人。 
<br>　　前者我没有时间。后者我没有机会。她笑。 
<br>　　这是我喜欢的女子。聪明有流转，说话简洁至极。 
<br>　　我们决定一起去看房子，房子的主人是一个老教授，准备去德国两年，所以想把房子租出去。 
<br>
<br>　　我们约在北京西路。 
<br>
<br>
<br>　　3 时间不会走了 
<br>
<br>　　那天下雨，阴冷潮湿。春天缠绵的雨季，使本来已经污浊不堪的城市空气更加粘稠。 
<br>　　我早到20分钟，独自站在大厦门口避雨。作为高级的写字楼，里面汇聚多家著名的集团公司。 
<br>　　现在已到下班时间，旋转门不断有人进出。很多人衣冠楚楚，然而神情困顿。我已经过了很多年没有工作的生活，不太清楚工作的意义和目的。 
<br>　　18岁的时候我去街头冷饮店打工，每天夜晚工作三个小时，推销冰激凌兼收钱送货，月底能拿到几百块钱。迫不及待地去买看了整整一个夏天的碎花裙子…… 
<br>　　毕业以后，进入大机构。很快辞职。 
<br>　　从此不再有工作。多年的无业生涯，很快使我变成一个邋遢的女子。神情时而萎靡时而激越无比。 
<br>　　绢生出来的时候，怀里抱着一盆绿色的羊齿植物。她很瘦，眼睛漆黑。神情冷淡的时候像沧桑的的妇人，笑起来则变成甜美的孩子。大抵只有内心纯真而又经历坎坷的人，才会如此。她穿织锦缎的暗红牡丹短旗袍，下面是破洞的牛仔裤和褐色麂皮靴子。一头海藻般的长发，光泽明亮。 
<br>　　她的名贵靴子一脚就踏进了泥泞里面。 
<br>　　平时喜欢养花？ 
<br>　　不。今天在花市看到，非常喜欢，所以想买下来。她从包里拿出一盒烟。她说，你抽烟吗。 
<br>　　我看到她手里的烟，是一盒红双喜。8 块钱的特醇。我笑。两个人互相低着头点燃了烟。她手里的绿色大叶子轻轻碰在我的皮肤上。 
<br>　　是在接下来的一秒钟。我刚刚直起身体，吐出第一口烟的时候。 
<br>　　那个男人突然掉落下来。他没有任何声音地随着犀利的风速下滑，撞击在前面停留出租车的宽敞空地上。就像一只沉重的米袋子。爆裂的是他的脑壳。白色的红色的液体混杂在一起飞溅。 
<br>　　雨下得不大，他的白色衬衣被泥水包裹。 
<br>　　我惊叫一声。绢生的手迅速地控制住我的肩，一把将我拉到后面。 
<br>
<br>　　我们目睹了此后的过程。保安报警，警察封锁现场，众人围观。死者是某广告公司的副经理。那个男人因为涉嫌贿赂和贪污，已经被调查了一段时间。绢生和我坐在台阶上，看着那具破碎的尸体被装进黑色的塑胶袋里拖走。 
<br>　　他的一只鞋子还在那里。绢生说。 
<br>　　一只黑色的男式皮鞋，孤零零地掉在花坛偏僻的角落里。 
<br>　　不知道他在丧失思维之前，是否会后悔自己穿着鞋子。如果光脚的话，去天堂的路途会走得比较轻松。她说。 
<br>　　我不明白她为什么会笑。这样诡异的笑容。我记得那个男人的脸，是像突然伸过来的手一样，出现在我们面前。他的眼睛睁开着。空白的眼睛。 
<br>　　你害怕死亡吗。她看着我。小时候，家里死人，我站在棺材旁边看，不明白一切为什么可以这样完美地停顿。 
<br>　　手指不会动了，眼泪不会流了，时间不会走了。 
<br>
<br>
<br>　　4 有些人的生命是有阴影的 
<br>
<br>　　我们租下的那套老房子很陈旧。房间光线阴暗，前后院子里种了大片茂盛的橘子树，叶子暗绿得发亮。还有鸢尾，雏菊和玫瑰。绢生把她的羊齿放在卫生间的窗台上。那盆小植物长得很野性。卫生间铺洁白的马赛克，虽然狭小但是干净。可以在里面喝酒，发呆，洗澡的时候收听音乐。 
<br>　　露台的铁栏杆已经完全发锈。有一张厚重的红木雕花书桌，手抚摩上面冰凉光滑，散发隐约的木头清香。 
<br>
<br>　　我的同居伙伴。深夜她光脚在地板上走来走去，散乱着海藻般的黑色长发，湿湿的脖子。像在地穴里穿行的寄生昆虫。当我在电脑前抽烟和写作的时候，她坐在地板上看卡夫卡。 
<br>　　周末的深夜，挤到我的床上，一起看电视的经典黑白老片回放。然后喝威士忌加冰块，配新西兰起士。常常会看得流泪。红着眼睛在那里抽泣。电影打出了END ，于是狠狠咒骂一句，愤然地进卫生间洗脸。 
<br>　　她是那种会把手指甲剪得短而干净的女子。喜欢奢华的黑色蕾丝内衣。并且果然是没有宠物和男人。 
<br>　　一早起床。洗澡，在衣橱里选衣服。她的衣服排列在熏衣草的芳香里，丝缎，纯棉，细麻，麂皮等所有昂贵而难以服伺的天然料子，颜色大部分为黑，白，暗玫瑰红。细细的蕾丝花边，精致的手工刺绣，大红大绿的民俗风情。她的生活极尽奢华。但我知道这里面的缺陷。这所有的一切，都是她以自己的工作获得。 
<br>　　一个没有男人可以依靠的女人。公司里的工作忙碌，常日夜颠倒地加班。有时候打电话过去，话筒里始终是杂乱的声音，电脑，电话，传真，打印机……每天喝泡得浓黑的咖啡来维持睡眠不足的体力。商业社会，不进则退，一旦失去被利用的价值，就是沦落。绢生在销售界的名声刚刚有好的开始。我相信这是她以天分获得，她是散漫的人，性情纯真然而并无上进心。 
<br>
<br>　　我曾去参加过她公司的庆祝酒会。绢生的销售业绩做得如此之好，众人均过来和她招呼寒暄。 
<br>　　她端着酒杯站在她的外籍老板旁边，穿黑色丝绸长裙，肩上的细吊带均为水钻，长发柔滑，胸前别一小束风信子。我看着她在人群里得体地微笑，身体微微有些僵直。可是她是能够控制自己的。 
<br>　　我知道。这是她的外壳，她柔软纯白的灵魂躲藏在里面，小心翼翼地爬行。 
<br>　　半夜她回家。踢掉鞋子先开始洗澡，在卫生间里一泡就是几个小时，在里面香薰沐浴，看小说，听收音机，不亦乐乎。这是绢生放松的时候。我亦知道她在公司里为工作和同事争辩，回来后因为气愤胸痛难忍。 
<br>　　有时候独自衣锦夜行，涂发亮的唇膏，抹了兰蔻的香水，花枝招展地出去。快凌晨的时候回来。手里拿着从超市买来的威士忌和大块起士。卸妆，洗澡，穿着内衣半夜看旧片，一个人坐在阴影里，对着威士忌和香烟。长长的头发披泻在胸前，眼神疲倦。 
<br>　　大部分人的生活未必象我这样目的明确，因为我知道如果不写作就无法生存。而绢生，她是可以有选择的机会。自然她也曾对我说起那些和她在一起的男人。她与他们吃饭，跳舞，看电影，深夜回家，却始终只有一个人。她从不带男人回家或在外留宿。亦不要他们买东西给她。吃饭也要坚持AA制度。因为不爱，所以分得很清楚。 
<br>
<br>　　为什么你似乎不是很快乐呢。我问。 
<br>　　他们想玩的，我未必想奉陪。我想玩的，他们又玩不起。 
<br>　　玩不起吗。 
<br>　　比如诺言，比如责任，这是比金钱更奢侈的东西。她笑。我是很传统的女人，VIVIAN. 我要一个男人养我，然后我给他做饭洗衣服生孩子。就跟两千多年来中国女人做的事情一样。 
<br>　　谁要养你。买条裙子就要一千块钱。 
<br>　　那是我花自己的钱。如果他养我，扯块棉布自己做就行。 
<br>　　这未必能让你感觉安全，绢生。 
<br>　　我现在的感觉更不安全。她说。 
<br>　　谈话结束。绢生独自坐在黑暗里，继续看片子，喝酒，抽烟，她可以把这样的状态持续到凌晨天亮，然后穿上衣服和鞋子，拦出租车去公司上班。一个失眠的女子，可以若无其事地出现在公司里，然后冷静地开始她一天的工作，和同事开会，讨论，打电话，应对…… 
<br>　　半夜她放王菲的《但愿人长久》，这样哀怨的靡靡之音，苏轼的词在王菲的唱腔里让人听着难受。她走来走去，哼着里面的句子，一边轻轻抚摸自己的长发。 
<br>　　我从来未曾把绢生当作普通的女孩。 
<br>　　有些人的生命是有阴影的。 
<br>
<br>
<br>　　5 我在等待着什么 
<br>
<br>　　七月，绢生去北京参加会议。 
<br>　　整个夏天是我的休眠期，每天除了睡觉和晚上去酒吧，没有办法写超过两千以上的字。ROSE来信催我，亲爱的VIVIAN，我想念你的故事，但愿你不要从我的隔壁办公室搬走……我微笑。那天，我看到自己开始脱头发。在卫生间的瓷砖上，看到大团大团的黑色头发，纠缠在一起。我蹲在地上玩了一会儿头发，发现自己的心里很冷静。 
<br>　　在绢生去北京的这段时间里，我要服食比平时多一倍的镇静剂才能入睡。可是副作用也很明显，头晕，出现幻觉。开着空调的房间里，我觉得自己血液的速度开始变得缓慢。黑暗中，万籁俱寂，我痛恨这种失明失聪般的包围。我躺在床上观望着自己的痛恨。 
<br>　　如果我的背后有一个男人。我希望他抚摸我睡觉时蜷缩起来的膝盖。用温暖的手指，一寸一寸地抚摸我，把我冰冷的身体扳直。我蜷缩得像回到母亲子宫的胎儿……我害怕自己的身体以扭曲的姿势僵硬。他要完全地占据我。这样我才能安全。 
<br>　　我的眼睛开始出现一团一团的阴影。然后是那个男人。那个坠落下来的男人，他的身体发出犀利的风的声音。白色的红色的液体四处飞溅。 
<br>　　他脚上的鞋子不见了。 
