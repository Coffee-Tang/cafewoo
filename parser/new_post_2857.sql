<p>
哪天看了LUDO的冬瓜鸭肉汤，香飘满咖啡。便仔细琢磨着怎么着也得为自己煲一锅汤。好远离这个冬天的寒冷。
<br/>巧妇难为无米炊，笨女子更少不了那些瓦瓦罐罐。去了附近的商场，拎了个电子煲回来。据说，这种罐炖出来的汤原汁原味。不过，话又说回来，还真有点担心营养过剩。左右为难阿。^_*</p>

<p>莲子。红枣。板栗。枸子。还有熬汤用的枯枝杂草。统计起来共花了我半个小时在人声鼎沸的菜市场悠悠绕绕。其实我不晓得这些东西是否缺一不可。不过我牢记好友蓓的经验，什么乌七八糟的东东往罐里一扔，细火慢煮，时间一到，香飘千万里。佛跳墙的美味就是这般道理。当然，佛跳墙还从未吃过。是否真的顶级美味一时间也难以求证。可实验出真知却是颠仆不变世世流传的一句话，先试试再说啦。
<br/>最后只余炖汤的主体了。我望着一溜血淋淋的鸡鸭心里寻思着，来世定要做只会飞的鸟。风雨中自己觅食。被人饲养的下场就只有躺在砧板上的份了。
<br/>装模作样的左挑右选。最后要了一只乌鸡。谁说过，这最好。我当然是要最好的。</p>

<p>回来后洗干净所有的东东，一古脑就往罐里丢。插上电，坐在椅子上美滋滋地想，多简单方便的一锅汤。为了让汤熬得更久更香些，我还特意将电子煲的温度调到最低档。
<br/>打了电话招呼熟悉的朋友来吃。低头一看表，已近中午。磨磨蹭蹭将饭做好了。朋友也到了。那汤还没动静。掀开一看，水只有丁点儿热的迹象。心里大呼不妙，嘴里已愤愤地嚷了起来——伪劣产品。
<br/>拿起说明书一瞧，顿时脸红得象只苹果。上面明白写着，水沸腾时间：强档，2小时。慢档，5小时。</p>

<p>就这样，我准备中午喝的汤成了晚餐。不过，炖出来的板栗真是香。
</p>
