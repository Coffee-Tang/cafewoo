我说好。然后我握住我冰凉的手。你修长的手指温暖而柔软，你的微笑明澈而温柔。你说沉，有个前提，那就是你不能嫁给别人。我笑。然后你又说，宝贝你多么特别啊。茫茫人海，只有你是独一无二的。

我把头靠在绿色的靠背上。没有泪水了。寻，你给了我多少年的阳光，到最后，我为你流尽了我所有的泪水。

终于到了这个地方。滚滚的江水，群山的伤口，苍白的天空。和刚修好不久的大路。我还记得多年前这里的模样。弯曲的小路，紧逼的山脉，江水急速流转，遇到下雨就经常塌方。你每次带我回你外婆家都会精心挑天和日丽的一段时间去。可，为什么，那最后的一次你就忘了呢？

车翻入江水里。多么庞大的水花溅起。直冲云霄的悲伤。

