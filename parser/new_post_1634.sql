三十几个小时的火车，带着轻微的眩晕感。铿锵有力的声音在清冷的空气中响起。窗外一闪而过的树木，一再滑落的耳塞，对面男人没刮干净的胡子。我的心在低低地忧伤着。

是的，忧伤。我每天向上帝祈祷，只希望可以离开这张网，因为寻说意沉你要快乐，我喜欢你笑的样子。

上帝，如果你在那遥远的地方听到了我的祈祷，那么，这一切是不是可以结束了呢？

