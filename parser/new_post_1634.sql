亚亚很爱怜地看着我说，丫头要好好照顾自己。

我点头，然后说，新年快乐，你们两个。

他们都笑了。干净的笑意湖泊一样水光潋滟。亚亚把行李塞到他手里让他先上车，自己伸出手抱住我，在我耳边说，都会好的。一切都会好的。

她的温暖的气息混合着芳香。我深深吸了口气。

她男朋友没有上车。在车下等着亚亚。我说你们上车吧，我也回去了。

