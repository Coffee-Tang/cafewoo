寻，那时候我会坐在你自行车的前杠上。留很长的头发，在风中飘啊飘啊多么地骄傲。

我不肯吃药，每次你都守着我吃下去然后再盯我5分钟怕我吐出来。

我第一次给你写了I LOVE YOU。用蓝色的笔写在一片梧桐树叶上。你把双手插进牛仔裤的口袋，一脸坏笑地问我，意沉，你写的什么意思啊？人家尴尬死了的告诉你，是我喜欢你们大家的意思。然后你靠在树上挑着眉毛说，是吗？小孩子不许撒谎。

