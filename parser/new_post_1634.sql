你带我去听你外婆将圣经故事。信基督教的婆婆，眉目中写满慈祥与怜惜。我们一左一右地坐在她的农家小院里陪她度过孤寂的时光。有时候你会悄悄握住我的手。她老人家看见了都只是笑，摸着我的头说，这是个好丫头。后来你嬉笑着告诉我，我外婆都认同你了哦。我将就手上的一把瓜子壳给你甩过去，你白色的上衣在阳光下那么干净美好。

