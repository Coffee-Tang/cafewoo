汽油味道浓浓地弥漫开来，我有些头晕。迷糊中看见一双扬着笑的眸子轻轻地靠近。那么明亮，湖泊色地瞳孔拉开了一丝丝的光斑。然后一个声音在耳边响起，意沉，我的意沉。

