有的时候可以看见星星。我拿出手机给寻发短信。黄屏的手机，我坚持不要蓝屏或者彩屏的，因为黄色是星星的颜色。我说陈寻，你看见星星了吗？

手机一直安静。他不回答。

我抱着膝盖坐在天台上。冷风呼呼地吹，吹在脸上像刀片轻轻割过一样疼痛。

我又发了一条短信过去。我说陈寻你还记得以前我们一起研究星相的日子吗？

继续的安静。我盯着手机看它出闪着黄光到瞬间的熄灭。眸中突然的一片漆黑。像垮掉的柏林墙那样轰然作响，然后万劫不复。

我又固执地发了第三条短信。我说陈寻你还认识你送给我的那颗星星吗？你说要是我们分开了，你抬头看天，看见它闪光就像我对你微笑一样。

我俯下身子把脸埋在手心。泪水顺着肌肤的纹路渗透开去，蔓延开来。我反复地叫陈寻陈寻陈寻。我全部都记得，难道你都忘了吗？

眼泪是不争气的东西。可我如此想念你。

亚亚把我拖回房间的时候手机已经自动锁上了，锁在我铜锈斑斑的心上。我的泪水纱一样地蒙住了眼睛，我说亚亚啊，难道我的祈祷真的没有用吗？上帝真的听不见吗？

她咬住嘴唇，眼里也幽幽地泛起了泪光。她揉了揉我凌乱的短发说，丫头，上帝会守护所有的人。我们以不同的方式祈祷，他听到，微笑，然后认真地来安排你的命运。

God is watching you .God is holding your grief.

