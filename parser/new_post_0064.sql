<br>　　王菲的专辑从来不缺KTV歌曲。从《红豆》、《当时的月亮》再到《笑忘书》，王菲手中要个性也要市场的唱片运作大旗从未停止过挥舞，新专辑中的《流年》正是一支雅俗共赏叫好又叫座的芭乐歌。陈晓娟的旋律向来适合王菲声线中简洁的音阶范围，细致微温到只想沉醉，不由地怀疑竟是与那些离经叛道的行腔出自同一副嗓子。而林夕操刀的词除了“有生之年，狭路相逢”外叫不出更多的好。“手心忽然长出纠缠的曲线”隐约是“你掌心的痣，我总记得在哪里”的姊妹篇，既缺温婉又乏新意。也许，对于一个月几十首词产量的林夕，我们苛求了。
