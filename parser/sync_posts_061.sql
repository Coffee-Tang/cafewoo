她终于走了。

屋子里恢复了死一般的沉寂。又一次只剩下她一个人独守空城。人们总是憎恨离别。而远离它的最好办法就是躲避相聚。只要在一起，就有分开的那一天。除非，她不在乎对方。

所以，她一个人住。

吴可一个人生活。这样的生活说不上好坏。她不是那种一个人住就觉得是孤寂的女子。事实上她有点乐得一个人清闲。年前刚和第七任男友分手，是该好好休息一下了。这个数字听起来，让人觉得她滥情。可她从来不觉得自己是花心的人。

事实上，她是个被动的人。有人追求她，只要他不讨厌，一般她都不会拒绝。当然，如果对方再提出分手，她也欣然答应。她甚至不知道这些人在她生活中是扮演什么样的角色。总之，他们都是这么样的开始并结束的。

或者，他们叫过客。

这样的情形还会持续多久，她自己也不知道。她是个懒散的人。也不想做太多无谓的改变。觉得这样也挺好。有时候她笑自己是耐得住寂寞。

日子一天天地过去，农历年快到了。她算了算，有点害怕起来。

是的，她突然觉得自己老得太快了。元旦的时候老了一岁，过了农历年又要老一岁，农历二月份是她的生日，到时候还要再老一岁。还有若干的离别也会让她老去。她发现自己正在以比常人快三倍以上的速度迅速老去。

她常常站在镜子前，看着看着，就看见了一位满面皱纹白发苍苍的老妇人。她哑然失笑。

她突然想看看今天是什么日子。这才发现墙上挂的还是去年的旧历。才翻到七月份。已经积上了一层灰了。她从墙上将旧历取下来，可新挂历还没买，换不上新的。墙上陡然添加了一大块刺眼的空白，让原本空洞的屋子更加苍白。她泄气地再将旧历挂会原来的地方。她决定回去拿一卷新挂历回来。

父母家离自己的住处只有一辆公车的距离。只要坐11路公车，从第一站坐到最后一站就到了。可是她却很少来回走动。倒是退休的母亲有时会到她那儿看她。她知道回去后，父母不外乎再对她说家里不缺她赚的那点钱，叫她搬回来住。事实上，大家都明白她赚的那点钱能养活她自己就算不错了。这只不过是她出去一个人住的借口罢了。

吴可是个自由职业者。这个称号时下正流行得如火如荼。似乎还算得上是一种时尚。可她自己知道这个美其名为自由的职业，其实就是“吃了上顿顾不了下顿”的代名词。用她自己的话说，她是“以卖字为生”混口饭吃。

偶尔给某些杂志写些专栏。大多时候，她都是稿约期限拖得不能再拖了，或者是她快连买酸奶的钱都没有了的时候，才开始挑灯夜战。连熬几个通宵赶稿子，然后顶着双熊猫眼去送稿。

说到酸奶，只要是吴可的朋友都知道她“嗜酸奶如命”的习性。她喝起酸奶来，用一个词来形容就是“拼命”。情形大概能赶上三天三夜没吃过东西的饿鬼见到食物的样子。更厉害的是，她可以喝得不露痕迹。直到发现边上大堆的空盒才意识到她喝了多少。用一个准确点的参数，大约是两天一箱。

奇怪的是，身边的人都没发现她什么时候开始了这个习惯，也不知道为什么。阿苏说，你的血都快成奶白色的了。她乐得不用去献血。谁也没办法。

接到母亲的电话时，正是她睡得最香的时刻。母亲催她赶快回家过年。电话只讲了三分钟，却浪费了她三个小时的睡眠时间。平日里她总要睡到十一二点才起床。她看了看闹钟，7点半。想睡回笼觉，却已经清醒了。干脆起来收拾一下，今天就回去。

她去洗手间，经过客厅。从冰箱里拿出几罐酸奶，放在桌子上冷却。回来后，一口气喝掉。接着开始打扫房间，流流汗，消化消化。然后再上个洗手间，再洗个澡。基本上大功告成。

她站在衣柜前考虑要穿什么衣服。镜子里那张苍白的脸看了叫自己难过，更何况是父母。她最后换上了明黄的外套和猩红的长裤。整个人显得明亮了许多。

人们穿着色彩艳烈的服装，只是为了掩饰苍白的面容和孤寂的灵魂。走在大街上，就如同成群的热带鱼。用抢眼的外衣来掩盖空洞的神情。

她没忘记挎上一个大包，往里面塞满酸奶。然后出门。

她决定不坐公车。要徒步走回去。只是因为突然怀念起这座城市的味道。她几乎可以闻到女人脸上的彩妆和男人身上的烟草。她快步地在大街小巷里穿梭。像一尾自由的鱼。她大步走路，让人以为是在赶时间。即使是经过大扇橱窗时，不经意回头看一眼自己的侧影，都要相信了自己的“行色匆匆”。

她终于在路边的广场上休息。坐在长凳上，掏出包里的酸奶，一边喝着酸奶，一边睁着眼睛看着广场上来来往往的人。她看他们的鞋，他们的衣服，他们的脸和他们的眼睛。一个个看过去，不厌其烦。

她突然觉得恍惚起来。阳光细碎地洒落在过往行人的身上脸上眼睛上。还有广场边上的车来车往。这一切都在无声有序地上演着，像是天堂。阿城住的地方。她眯起眼睛，就看见阿城站在街对面向她挥手，然后跑过来――四周的一切都暗淡下来，只有他的身影――她在瞬间迷失了方向，一脸茫然。一直感觉到小腹有股涨意。她才回过神来。她看见身边的一大堆酸奶空盒，就知道喝多了。她开始东张西望。

如果附近有公厕，她就先就近解决一下。如果找不到，她就直接打的去父母家，反正也快到了。先忍一忍。做好决定后，她抱起那堆空盒子往垃圾筒里送。

发现对面有个男人用一种很奇怪的眼神看着自己。她纳闷刚才怎么没看见他。她瞥了他一眼。正好男人的行动电话响了，她回头就走了。

事实上康伟很早就注意到那个一直喝酸奶的女孩了。是的，他以为吴可还是个孩子。

栗色短发，素面朝天。明黄上衣，猩红长裤，帆布球鞋。还有一个装满酸奶的拼图挎包。无论怎么看，她都像个阳光少年。

