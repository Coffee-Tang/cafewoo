<br>　　男人约吴可出来吃饭。  
<br>　　嗨。吴可轻巧地站在男人面前。  
<br>　　请坐。男人温文有礼地为她拉开椅子。  
<br>　　谢谢。吴可看着男人在她对面的位置坐下来。目光很自然地爬上了他的脸。  
<br>　　当她站在这个男人面前时，已经清楚地知道了关于他的事情。当然，是她应该知道的。比如，他的太太。他刚上小学的女儿。他开的代理公司，状况良好，酸奶是其中的一个项目。还有，就是他叫康伟。这些都是他亲口对自己说的。当然包括不能娶她。  
