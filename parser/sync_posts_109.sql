声音与文字的混和间有一种惊人的孤独悄悄蔓延。他在歌声里挣扎，在旁观者的冷静侧视与当事人的痛苦孤独间挣扎。这首歌连同这张唱片，都象一部经典的电影。

8.向前走/ 林强　词曲/ 林强闽南语歌曲一向给人以苦情、江湖味的印象，林强的《向前走》把摇滚乐元素引入闽南语歌曲，世界由此摇身一变。陈升、李宗盛、周世晖和友善的狗联手打造了这张唱片，《向前走》是其中最出色的作品。这首歌在台湾经济低迷的1990年推出，引起轰动，成为89年《梦醒时分》之后的又一话题，也成为流行歌曲引领社会心态的代表作。

9.铁血丹心/ 甄妮、罗文　词/ 黄　　曲/ 顾嘉辉作为影响国人最深远的武侠题材歌曲之一，《铁血丹心》的悲壮、侠烈之气已经深深留在我们的记忆里――对每一个看过《射雕英雄传》的人来说，十余年后的今天，靖哥哥在那一轮血红落日中弯弓射雕的身影依然英姿勃发。

歌曲充分利用男女声对唱的优势，把“逐草四方、沙漠苍茫，哪惧雪霜扑面”的豪迈侠义同“应知爱意似流水，斩不断理还乱”的儿女柔情结合得相当完美。也许，每个男人都追求这样的生命：豪情万丈地行走天涯，身边有伶俐娇俏的如花美眷相伴.也许，每个女子都曾梦想这样的情缘：抛开世事断仇怨，相伴到天边。

10. 梦回唐朝/ 唐朝　词曲/ 唐朝他们歌曲中传递出的古意、杀气和豪迈，正如“菊花、古剑和酒”。他们的传奇尽人皆知，他们的热血烧起多少灵魂的摇滚梦想。纯粹以作品而论，很少有后来者能超越他们的深沉、大气、坚忍和自信。

在他们身上，我们可以清晰地看到中国第一代摇滚人不自觉的使命感和大时代意识，和后来者相较，唐朝的作品形式不算新鲜，文字之间缺少更细致的个性感受，但他们酣畅淋漓的气度和锐利杀伐的性格迎合上摇滚乐的脉博，因而谱出最撼人心的音乐。此外，飘扬悠然的东方气质也是唐朝乐队得以傲视群雄的资本所在。

11. 明天会更好/ 合唱　词/ 罗大佑、张大春、许乃胜、李寿全、邱复生、张艾嘉、詹宏志　曲/ 罗大佑　制作/ 李寿全1986年是世界和平年，以群星合唱的方式祈愿世界和平，援助贫困人群成为流行一时的娱乐界盛事，此过程中诞生了不少出色的中文歌，《明天会更好》堪称其中的扛鼎之作。王芷蕾、王梦麟、文章、百合二重唱、李碧华、李宗盛、李建复、巫启贤、吴大卫、林淑蓉、林慧萍、洪荣宏、施孝荣、唐晓诗、徐乃麟、娃娃、陈淑桦、黄莺莺、张清芳、童安格、费玉清、杨林、甄妮、齐秦、齐豫、潘越云、郑怡、蔡琴、蓝心湄、苏芮等打破签约公司的限制，共襄盛举。除群星合唱版外，还录制了松江儿童合唱团的童声版。这首歌热情、执着又充满期待，至今未有超越者。这也是流行歌曲直接介入社会生活的一个象征。

12. 没有烟抽的日子/ 张雨生　曲/ 张雨生大多数人听熟这首歌曲，想必是在张惠妹那些销量动辄以十万计的正版或盗版唱片里。它的旋律多少有点晦涩，歌词也不知所云，如果不是张雨生的死形成的新闻热点和张惠妹的人气，根本没有资格做一首街知巷闻的流行歌曲。

只是在没有烟抽的日子里写出来的歌，生来不是为了被传唱的。

13. 姐姐/ 张楚　词曲/ 张楚　制作/ 贾敏恕张楚是现代社会的游吟诗人。他的音乐创作出自本能，文字的部份则具有惊人的质感和强烈的歌唱性。他的忧伤是冬日里的轻烟，真实而且温暖。他不会太过愤怒，因为他始终不太相信自己的力量。《姐姐》是一首叙事性的歌曲，尽管它并没有复杂的故事情节。青春期的少年对“温柔”但“爱流泪”的姐姐的依恋和弱势群体“我要回家”的呼唤各自独立又互相映合，成为90年代中期令人深思的文化意像。

14. 马不停蹄的忧伤/ 黄舒骏　词曲/ 黄舒骏黄舒骏是罗大佑之后台湾最好的诗人歌手，清新明快的文字和以民谣为基础的音乐架构起一个才子充满张力的音乐世界。和流水线上生产出来的陈词滥调不同的是，黄舒骏的歌声中总有一种学子情怀，诗意而纯真。“马不停蹄的忧伤”是个绝妙的意象，一览无遗又意味深长。这个青春期的爱情故事里没有矫情和虚妄，一派自然、淳朴天真。

15. 大地/Beyond 　词/ 刘卓辉　曲/BEYOND Beyond在香港有着仅次于达明一派的声望。他们的歌里，少年人飞扬的青春和起起落落的爱情都占很大的份量，这首《大地》是其中的异类。中规中矩的编配录音和流畅犀利的旋律是Beyond的特点，他们或许没有达明一派的冷静与智识，但黄家驹的摇滚偶像气质和他们的

的热情执着影响了许多人走上摇滚之路。

16. 恰似你的温柔/ 蔡琴　词曲/ 梁弘志蔡琴一出道就是成熟的，所以她少了很多磨难，也使制作人如获至宝。相对于校园歌曲时代旺盛的创作量，歌手的表现形式一直受到相当的限制。蔡琴的声音中自有一种低回婉转的诱惑，有别于他人的清亮高亢。梁弘志是一位谨慎的创作人，数量不多，曲曲精致，常在平易近人的旋律之后浮出水一样的温情。梁弘志不是上天入地的大手笔，却是精巧小品的掌门人。《恰似你的温柔》气质清妙，意味悠远，几经翻唱，仍不觉老旧，功力在此。

17. 鹿港小镇/ 罗大佑　词曲/ 罗大佑都市生活与乡土情怀的纠缠，现代意识与传统观念的碰撞，一直都是罗大佑笔下的重点。这首歌是七十年代台湾经济起飞后，知识分子对于工业社会迅速到来作出的本能的反应。“台北不是我的家”成为撼动人心的疑问，其后多被引述。

18. 如果还有明天/ 薛岳　词曲/ 刘伟仁薛岳的英年早逝常会令听者扼腕痛惜，这首歌正是他对生命的总结。中文歌曲对生命的表述大多太过轻飘，这一次，有薛岳的亲身经历，份量自然重了许多。薛岳和幻眼合唱团的合作是他勇气的体现，一派摇滚曲风，团队形象，在消费方向狭窄的台湾歌坛绝对是个异类，只有以十二分的努力和坚持才能做到。明知自己去日无多，还要强忍病痛，做演唱会，出唱片，其间的执着，已非名、利二字可以解释。这样的勇气和诚意，都在这首绝唱之作里。

19. 大约在冬季/ 齐秦　词曲/ 齐秦齐秦是对中国流行音乐发展影响最大的歌手之一，其他的名字还有邓丽君、刘文正、叶佳修、侯德健、苏芮、罗大佑、崔健、童安格和Beyond等。民谣时期的齐秦出奇地拥有音乐和个人气质的完全统一，在那样一个唱片工业还不完全成熟的体制下，他自由地成长起来，这是幸运，也是奇迹。他的天才在各个方面一直地被确认，并且在民谣摇滚的方向上证实了流行音乐的进步与成熟。《大约在冬季》是一首中庸的离别情歌，因其中庸，才得以广为传唱。它有淡淡的不绝望的愁绪。承诺着一个没有未来的未来，一切都完全合乎风华少年的经验与想象。它可以作为范本，写进教科书。

20. 开场白/ 李宗盛　词曲/ 李宗盛这样短小的一首歌，透着无比的认真和诚实，不是乖巧的呻呤，也不是故作姿态的呼天抢地，就像朋友之间的对白，简单直接，又意味深长。李宗盛并未把自己当作旁观者，在这首歌里，他很清楚地点明了自己和听者的关系，这样的交流，才更具有可信度。

21. 酒干淌卖无/ 苏芮　词曲/ 侯德健侯德健、李寿全、梁弘志、罗大佑、吴念真等人在创作这张唱片的时候，就决心要让歌声里有故事有画面有生活，还要让苏芮成为活在歌里的声音。如果说《一样的月光》是整部电影想要表达的内核，《酒干淌卖无》就是电影情节在歌声中的浓缩。前者是冷静的思索之后热切的追问，后者是深切的感情在瞬间的迸发。由此，电影在理性与感性两个方向上的表达都得到了完整而精准的音乐表达。苏芮的人生经历成为她表达这首歌最重愤怒的天穹之间，那些射日的勇士该去向何处？他们的抗争是为什么？《天问》不仅是问“天”，更是在问芸芸众生。

26. 相思三态/ 张洪量　词曲/ 张洪量《有种》是张洪量最好的唱片，惊险奇绝，充满想像力和实验精神。和张洪量其他的一些作品相比，虽然流传度不广，音乐的份量却要重许多。《相思三态》写人爱到极处，为痴情所苦，几欲疯狂的神态和心情，着墨不多，却维妙维肖。

27. 我终于失去了你/ 赵传　词/ 沈光远　曲/ 赵传《我终于失去了你》超越了情歌的套路，以赵传的真实经历作为虚拟背景，人物和歌手水乳交融，其间的情感又可作多种解释，比之《我很丑，可是我很温柔》的征婚口号更真诚平实，比《我是一只小小鸟》中的悲伤失落又多了些许力量和勇气，堪称赵传最好的声音。

28. 似是故人来/ 梅艳芳　词/ 林夕　曲/ 罗大佑聪明的歌手总是会聪明地表达自己，梅艳芳的聪明就在于她除了“百变”的神话之外，依然留给我们这样隽永的声音。饱经人生历练，阅透世情冷暖之后，依然真情不变，那样的从容不悔，只有这样聪敏通达的歌手才能承担。

29. 信天游/ 程琳　词/ 刘志文、侯德健　曲/ 解承强在程琳之前还有一位知名的歌手王斯唱过这首歌，但反响不大。程琳在1987年春节联欢晚会上唱了这首歌，引起轰动，这也是“西北风”的开始。令人惊奇的是，它竟出自岭南音乐人解承强之手。联系当时《红高梁》、《老井》等电影的流行，我们可以想到，它的走红绝不是侥幸和偶然。

30. 其实你不懂我的心/ 童安格　词/ 陈桂珠　曲/ 童安格好的流行歌曲除了听觉上的美感之外，更会具有文字上的象征意义。流行歌曲中对爱情的描写常被淡化，变成对社会心态的概括。《其实你不懂我的心》以一个简单的爱情故事，象征了现代人心灵的疏离。童安格的唯美音乐也得以彻底展现。

31. 外婆的澎湖湾/ 潘安邦　词曲/ 叶佳修潘安邦的故事，叶佳修的代表作，校园民歌运动的纪念碑，乡土情怀的音乐写真，童年生活的纯真回忆，影响一代人的不朽之作――《外婆的澎湖湾》。

32. 葬心/ 黄莺莺　词曲/ 小虫黄莺莺的好歌很多，但大多都显得职业化有余。她的表现太过完美，毫无瑕疵的声音中缺少了凡人心中的激动和热情。这也许和她的声音特质有关。而《葬心》是一首能很好地表现她的冷艳的作品，文字凄楚，音乐缠绵。写成熟女性心灵的苦楚与寂寞，小虫可算是个中高手。

33. 光阴的故事/ 张艾嘉　词曲/ 罗大佑青春的风花雪月总是再也难觅，只剩这歌声似乎还在耳边回响，那单纯的日子里轻轻飘来的旋律。些许忧伤，些许怅惘，闪亮的日子，总有这样的故事和那样的希望。写青春的美丽，无出其右。

34. 前尘/ 林忆莲　词/ 周礼茂　曲/Dick lee 八十年代末的香港歌星，大多都有一部血泪斑斑的个人奋斗史，这使得他们唱歌更加努力，更加用心。林忆莲以不出众的外形，不顶尖的嗓子，唱到顶尖歌手的位置，全凭个人的刻苦与拼命，个中甘苦，唯其自知。“都市触觉”系列和《野花》都可以证明她的勇气和决心。《前尘》中的遣词炼字皆属精妙，潮湿的雨天再度相逢在这偏僻的小马路，

一派清灵幽雅的怀旧恋曲。

35. 禁色/ 达明一派　词/ 周耀辉　曲/ 刘以达如果一定要寻找深刻，我们完全可以把《禁色》当作香港同志们悲壮的公开声明来听：别怕，爱本是无罪。但是，令《禁色》不朽的原因，是它能带来接近完美的听觉享受：在凄凉的雨夜，低沉的歌声中哀愁如雨丝飘飞，忧伤的旋律缠绵入骨。

36. 恋爱症候群/ 黄舒骏　词曲/ 黄舒骏和中文诗歌类似，中文歌曲中一向缺乏这样叙事性作品。黄舒骏很聪明地描写了一个神经质的爱情故事，所有细节都在合理的放大和串接后变成“惊爆笑料”。风趣诙谐又无伤大雅，风格奇绝，堪称天才级的杰作。

37. 沉默是金/ 张国荣、许冠杰词/ 许冠杰　曲/ 张国荣这首歌体现了流行音乐的一大功能：对传统概念进行重新整合与解释，对社会情绪进行新的总括。"沉默是金”原先是指遇事小心，多做少说，不轻易发表意见，在这里，被合理地解释成：不理他人的闲言碎语，“洒脱地做人”。现代社会里个人的自由度相应地扩大，这首歌是音乐上的说明。张国荣和许冠杰情深意切的配合也是不能遗忘的声音。

38. 浮躁/ 王菲　词曲/ 王菲或许我们可以称这首歌为一篇中文流行歌曲版的“生命中不能承受之轻”。体裁的特点决定它不会拥有昆德拉小说史诗般的纵深，但音乐的介入赋予了它另外一种表现的可能性。王菲以她冷漠慵懒的声音表情和肢体语言身体力行地为我们表演了这样一种生命状态：没有烦恼，所以也谈不上真正的快乐；因为不喜欢疼痛的感觉，所以不要心；为了轻装上路，便丢掉一切不“必须”的东西，哪怕它们才是真正有价值的。是否是生存的本能，或者

可以像其他许多事情一样一概归咎于“世纪末”不得而知；可以肯定的一点是，唱歌的那个声音，和把这个声音真正听进了心里的人，都是人群中因为自省而沉重的灵魂。

