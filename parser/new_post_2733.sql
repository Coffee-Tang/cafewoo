<p>
又是好久没来了，这三个多月除了忙正事外，大半时间花在了准备GRE上。因为怕分心，所以勒令自己不准上网闲逛。刚才顺着留言板往下看，看着大家say you say me，笑笑不知道说什么好。 </p>

<p>前天去伦敦，昨天考完试，2240。如果三、四年前考，能有这个成绩的话，我一定会得意到忘形，现在才来考，感觉更多的是尴尬。 </p>

<p>不过比尴尬更多的是后悔。当我发现在英国一年多积累的英语对考G几乎没有帮助，而不得不回头求助于红宝书和新东方的时候；当我倒回头把概率论、微分方程、线性代数重学一遍的时候；当我寻思着要学车考驾照的时候，我都不知道大学四年自己怎么会那么懒散，那些不用怎么动脑筋，只要肯花时间都能做完的事居然一件都没有做。哪怕一天背30个单词，红宝书一年也背完了，昨天考试还是错在Verbal上，640分，报应。 </p>

<p>时间流逝，环境也在变，感觉到人也有所改变，难免的。人枯燥久了，也有点呆了，不知道还能聊什么。 </p>

<p>做GRE练习题的时候，有几句很不错的填空，摘录出来，算是除了一个功利的分数外，还有点小收获，也算是“为了忘却的纪念”： </p>

<p>Rumors, embroidered with detail, live on years, neither denied nor confirmed, until they become accepted as fact even among people not known for their credulity. </p>

<p>People should not be praised for their virtue, if they lack the energy to be wicked; in such cases, goodness is merely the effect of indolence. </p>

<p>Unenlightened authoritarian manage rarely recognize a crucial reason for the low levels of serious conflict among members of democratically run work groups: a modicum of tolerance from dissent often prevent schism. </p>

<p>还有一句我认为是所有GRE句子中最有人情味的： </p>

<p>The trick for Michael was to conjure for his son an illusory orderliness; only alone at night, when the boy was asleep, could Michael acknowledge the chaos he kept hidden from his son. </p>

<p>还有一篇很有趣的阅读，说的是猴子天生的攻击性与猴群中相应而生的社会秩序，因为只要把文章中的“monkey”换成“human”，就成了另一篇好文章。 </p>

<p>毕业三年，是以为记。</p>

