这个男人如同刀片一般，坚决地切割着我的身体。伤口处，血液为之沸腾。空气中飘荡着丝丝甜腻的腥味。我露出心满意足的微笑。转过头。

房间里只剩了窗口还是灰白色。光线并不充足，但女孩的背却异常的光亮。

她的背挺得很直，像一株盛放的水仙。空气中飘荡着隐隐的花香。我甚至以为在下一刻，她背上如绸缎般光滑的皮肤会清脆地碎裂，然后一朵朵鲜花喷薄而出。

我期望看到她的脸，如愿以偿的，她慢慢地转过身子。这是一个幽雅而怵目的慢镜头。柔软的身体如麻花一般，停止不了地旋转。看不清她扭曲的脸。

一阵晕眩。仿佛谁往沉静的湖心投进了一颗小石子。涟漪。

剧烈晃动。

我睁大眼睛。身体躺在床上，意识却在黑暗的街道上漫游，在寂静中飞快地掠过一扇扇静止的窗户，就如一只在夜里飞舞的蝙蝠。这好像是在做梦。醒着的梦。

不想动，但觉得渴。下床，走到饮水机前兑了一杯温水。端着透明的玻璃杯，在屋里漫无边际的走来走去。蓝白相间的塑料拖鞋在地板上不断发出哒啦哒啦的声响。单调频繁的声音让我显得有些烦躁而焦虑。越走越快。停不下来，像是谁在身后不停地用力推着我。

明明，你在干吗。男人的声音在黑暗中闷闷地传了过来。像从某个隐密的角落里突然跳出来。我有些害怕而警觉地侧过身子。然后想起了在这儿过夜的彻。

慢慢地挪动到房间里。越接近那张床，心就跳得越是厉害。隐约看得出床上的人侧卧的身形。就是这样的姿势，这样的黑夜，空气里也同样飘荡着一丝甜腻的腥味。

