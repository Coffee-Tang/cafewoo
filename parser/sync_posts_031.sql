这个男人的温情顿时让我平静下来。我安静地爬到床上，平躺下来。良久。我听到他沉稳均匀的呼吸声。

谧寂的空气慢慢凝结。疯狂地生长成一株爬藤植物。无声而迅速地穿过黑暗，向我袭来。它一圈又一圈地缠绕住我的身体。紧紧地勒住我的咽喉。喘不过气。快要窒息。

