还有一次， 是同一个雨天吧？ 不记得了。 也是在Hornsby附近， 车在一个路口停下来， 很多穿着校服的小孩子下课回家。 我不经意地向马路对面望去， 只见一个长得笨笨的东方女孩子站在那里等着过马路。 她的头微微向前倾着， 眼睛呆呆地盯着地面上的一处， 在那张那个年纪女孩子惯有的圆圆胖胖的脸上， 布满了整个城市所有的不高兴， 阴沉得要命。 她有着中学生式的齐耳短发， 厚厚重重地盖在头上， 颜色发旧的艳粉色裙子， 配着一件紫红色上衣， 黑色的平底皮鞋， 白色的袜子， 天呐！ 要多难看有多难看！ 这让我想起陈丹燕说的话：人要想穿得难看其实比想穿得漂亮还要难。 上帝原谅我的不敬。

雨天是森林里的一个陷阱， 她让你带着不可自拔的情绪不由自主地往下陷， 往下陷。 那个陷阱是只属于一个人的， 一个人的雨天， 一个人的暖暖羊毛袜， 一个人在记忆里随便翻捡的一些陈年和旧事…… 而后， 你抬头望望窗外， 看到所有的雨水都扑向大地， 心里想， 她们是知道的， 如果走得太远， 会有很多东西迷失掉。

