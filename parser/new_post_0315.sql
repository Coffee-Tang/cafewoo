时间是下午三点。我和颖在拐角的咖啡屋里见面。她对我说了上述的那些话。当然我说的只是精缩。她长篇大论一直让我耗到晚上七点。我拒绝了她作施舍状时提出的晚餐。但我难受得胃抽筋。

走出咖啡屋的时候，我脸色苍白，直冒冷汗。但我依然挺直了我的背，甚至感觉背上一阵疼痛。我容不得别人比我更骄傲。

我不想回家。我在大排档里随便吃了点东西。一个人在街头游荡。

夜晚的街道上漂浮着绚丽灿烂的霓虹灯光，行人像鱼群一样游来游去，秩序井然地穿越十字路口和建筑物，穿越另外的像鱼群一样游来游去的行人。街景总是恰如其分地映现人的心情。到处了无生气，结伴而行的女中学生脸上的笑是幼稚而愚蠢的。整个城市跟我一样闷闷不乐，我想这是因为人们的叫声此起彼伏的缘故。走在人行道的最内侧，我的脚步忽紧忽慢，简短地回忆了与默这段生活的全部过程，奇怪的是我几乎想不起重要的细节和场面了。

走累的时候，我在街边的杂铺点里买一串易拉罐啤酒，坐在马路丫子边，有一口没一口的喝了起来。

喝完了六听啤酒，也差不多了。看了看时间，是夜里一点半了。我晃晃悠悠地往家走。

屋里一片漆黑。对此我早习以为常。

我想我是醉了。有点昏昏沉沉的向床走去。空气中隐隐着飘荡着丝丝甜腻的腥味。并且这味道越来越浓。直到我走到床边，我突然意识到什么。酒全醒了。我慢慢掀开被单。

默象是醒着，眼睛微睁。朝着墙上那张画的方向。被单掀到一半的时候有点拉不动了，是凝固的血把被单粘住了。我被浓重的腥味呛的有点晕，想吐。我用力往下咽口水，忍住那股向上冲的恶心。

我出奇的冷静。我担心的事情发生了，反而让我显得镇定自若。难道这就是我所希望的吗？

打报警电话，做笔录，找颖做不在场的证人，等等一系列程序。我在默默的承受。如果有一天我受不了了，就让我崩溃吧。

i have a dream..strange it may seem..it was my perfect day..open my eyes..i realise..this is my perfect day..i hope you''ll never grow old....

birds in the sky..they look so high..this is my perfect day..i feel the breeze..i feel at ease..it is my perfect day..i hope you''ll never grow old..

forever young..i hope you''ll stay..fovever young..

天空一片阴霾。我抱着大堆被血浸透的床单到阳台上洗。看着血水流过我的手心，心里一片寂静。

有人说，当你握紧拳头的时候，什么也没有。当你摊开掌心的时候却拥有一切。

但手心向上是一个乞讨的姿势。手心向下是一种施舍。

我又应该何去何从呢。

我依然睡在那张默离去时睡着的我们的床上。我总是重复着默的姿势，安详地躺在这张床上。我可以冷静地睡去。虽然浑身冰冷。直到有一天，我听见一声凄厉的尖叫。我从噩梦中惊醒。从那天起，我再也睡不着了。

这其间，颖来看过我一次。我以为那只是她的故作姿态。她不过是想看看我这个一脸高傲的人事到如今是不是终于要软弱地向她求救了。我当然不会让她如愿。如你所知，我依然睡在那床上。她被吓跑了。为此，我感到满意。

彻重新出现。

这段时间他到北方去出差了。他回来后，只是知道默自杀了，却不知道是怎么样的情景。颖没有对他说，我自然也没有提起。

彻接替了默的工作，也接替了默的女人。

彻是忙碌的，我是安静的。

我依然在黑暗的衣柜里沉思。回想着黑暗中飘荡着丝丝甜腥味。实在忍不住的时候，我会拿出水果刀在身体的某个部位割上一刀。

隐约的疼痛会让人上瘾。我喜欢在黑暗中感觉伤口处跳动的疼痛。象是脉搏。显得伤口也充满生机。

最好不要让彻发现。他什么都让着我，惟独我身上的一些莫名伤痕会引起他的满腔怒火。

颖又来电话。让我去参加她和彻的订婚酒宴。

你们不是订过婚了么？我下意识地想要排斥这个事实。

那只是内定的嘛。别人都还不知道呢。我想不如早点让大家高兴高兴。反正一样都是要结婚的。万一要有个什么差错，可不是谁都受得了的。颖在电话那头若有若无地刺探着我的伤痛。我甚至在心底暗暗诅咒她。

在人们眼里，我是个高傲的人。我怎么能软弱呢。

准时赴宴。

酒席上人声鼎沸。颖打扮得花枝招展。笑得灿烂。多少让人觉得刺眼。彻还是一脸沉默，谁也看不清他脸上的表情。

向他们敬酒时不看彻的眼。颖笑得让我的胃又是一阵难受。

向同桌酒席的认识的不认识的人敬酒。喝得很潇洒。

重新回到黑暗中。

我不知道自己这是怎么了，但我知道自己很难受。也许因为已经一无所有，所以我输不起。彻象是我生活中的一根救命稻草。我想紧紧拽住他。无奈他终将漂向它方。

胸口一阵阵的疼痛。我只能拿出水果刀在身体上用力地拉扯，以减缓内心的苦楚。我立刻感觉到血液从伤口处争先恐后地涌出。棉布质地的睡衣迅速将粘稠液体吸附。

我坐在黑暗中。一脸彷徨。

你在干什么？！我被吓了一大跳。看向来人。呵。是彻。

你不是今天订婚么，怎么跑来我这儿了。我对他虚弱地笑笑。

他早已打开了电灯。向我冲了过来。你这是干什么？！他用力地拽着我的臂膀，用力地摇着。摇得我一阵晕眩。

你轻点，你弄疼我了。我不小心呻吟出声。

他把我抱住，抱得很紧。都快喘不过气来了。你这是干什么呢？他的口气软了下来。如果我没醉得那么厉害的话，如果我还有点清醒的意识的话，我会以为他很痛苦。或者他就是呢。

我在放血啊。你不知道么。在过去技术落后的年代里，人们病了就要放血的。我轻轻地对他耐心讲解。我病了，我要为自己治病。彻。我病了。

脸上有暖暖凉凉的小虫在爬。我想我是哭了。

我是如此骄傲的人，怎么能哭呢。我用力擦掉脸上的眼泪，却有新的泪水流下来。我把这一切怪罪于酒精。它让我变得软弱。但我终于还是放弃了斗争，任眼泪如大开的水龙头肆意泛滥。

明明，你不要这样。不要离开我。你会让我以为你要离开我了。象默那样。彻的声音很模糊。但我听清楚了。

默？！晴天霹雳。我在做什么呢？！默。我用力把彻推开。

你走开，离我远点。我的酒全醒了。酒精终于打不过意识。醉不醉只是看自己愿不愿意罢了。眼泪也不再流了。我闭上眼睛，强迫自己冷静下来。再次开口的时候，我已经恢复原来的平淡了。

你知道默是怎么死的么。我看着彻，他一脸莫名。嘿，我决定对他也残忍一回。我指向床的位置。

就死在那儿。死在那张床上。你以为颖为什么不来看我。因为她知道，哈哈，她知道默死在了那张床上，而我还夜夜睡在那床上。她怕死了。

我恨她。是她让我出去见面的，结果就出了这样的事。我恨你，是你给默一个什么狗屁期限，就丢下他不管了。我恨默，他为什么那么懦弱。随便的扔下我就不管了。我也恨我自己。要不是和他在一起，也不会这样。我不知道这世界上还有谁是我恨不下去的。

你知道吗？我回来的时候，他的血都流干了。屋子里到处充斥着血腥味。被单被凝固的血粘得拉不开。血已经变成褐色的了。他死的时候眼睛都没闭上，就看着墙上的那幅画。我知道，他还是爱我的。

我也爱他。

我爱默。我把床单被单全部洗干净了。再重新铺着用。呵。我就睡在默离开的地方。你也不例外。默都看着呢。嘿。

我看着彻，他的脸色惨白。突然冲到卫生间里，接着我就听见一阵阵呕吐的声音。我冷冷的笑了。

彻得了精神分裂症。

一开始的时候只是天天洗澡，然后洗被单。最后哪儿也不敢去了。天天在家里擦擦洗洗。

颖再也无法在我面前得意了。或者我的诅咒真的灵验了。

天空一片阴霾。洗完的被单晾在阳台上，在风中猎猎作响。

圣经上说，爱如捕风。我想编织一口捕风的口袋。却成为我的茧。

“让世界拥有它的脚步，让我保有我的茧。当溃烂已极的心灵再不想做一丝一毫的思索时，就让我静静回到我的茧内，以回忆为睡榻，以悲哀为覆被，这是我唯一的美丽。”

