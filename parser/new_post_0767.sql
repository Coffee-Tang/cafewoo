整整一个月后，韬光发了封电子邮件，只有一个单词，NO。

我觉得自己要崩溃了，唐菲给我的“失恋”都不曾让我用崩溃这个词。韬光的性子我最清楚，她

的事没有悔改的余地，她有好马不吃回头草的脾气。而且，我可能永远推动她了，无论作为老朋友、铁哥们儿、还是女朋友。

果然，韬光像从地球上消失了一样。电话那端永远是她的室友；写信永远石沉大海；发电子邮件竟然老被“拒收”；假期里去她家，她没回家；曾以旅游的理由去过北京找她，没想到她竟然也去“旅游”了(省略号)

我觉得自己“玩真的”了。但爱情是双人游戏，我在这边起劲地跑跳蹿叫，可是对手不在。愤怒也好，忧伤也罢，都只是对着空气，研究生一年级过得晕晕乎乎，干什么都有点漫不经心，跟室友伟整天泡在寝室里上网。伟是电脑鬼才，不是黑客却爱在网上干无伤大雅的坏事，曾“破译”了不少人的电子邮件密码。人有时候并不明白自己在做什么，一日，闲极无聊，我鬼使神差输入了韬光的电子邮箱，学伟的样子试着输入了觉得可能的密码，最后，我进入韬光的信箱。

没人会相信我看到了什么，韬光给我的信，或者，韬光给自己的日记。21封没有民出却是以我的名字为抬头以信的格式写成的文章，一封封堆在草稿箱里。

于是那个黄昏我明白了很多很多：为何她会对我错叫她唐菲那么生气，为何她会一心一意要我考到北京，为何她会把别人写给她的情书寄给我(省略号)原来她比我希冀友情转化成爱情早了那么久!

没有人明白我看到那些邮件的感觉，甜、痛、酸、悔，心里狂跳不止，大脑像严重缺氧一样。数年来海上漂流终于看到了一叶小舟，有救了。

然而，当看到一封名为《替补》的邮件时，一个惊雷在我脑上炸开了，我蓦然明白了一个现实，我终于失去她了：“他有他的生活和选择，我也是。毕竟，我只是他的高中同桌，更为重要的是，我不是他‘同桌的你’。没想到他也像其他男人一样学会了退而求其次，其实，我可以在他身边默默做任何他需要的角色，除了那个‘替补’(省略号)”

远处，有人在夜幕里哼《同桌的你》。

