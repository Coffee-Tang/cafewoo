此精灵不堪其辱，于酒吧之内当众引颈自杀。据官方消息，该美眉账号，已被自行删除。

据当时目击人声称，这名智力精灵美眉，名叫嘉嘉。

而当时与之发生口角的法师，正是圣者盟盟主――朱子七。

事情发展至此，仍然不是一个结束。一个月后，一个不知从哪里冒出来的高等级翅膀法师，公然发布挑战书，声称要在三日内与圣者盟盟主在天空之城会战，与圣者盟盟主朱子七血海深仇，一并清算。

三日之限，江湖英雄翘首企盼。

