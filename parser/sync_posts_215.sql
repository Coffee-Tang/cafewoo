圣者盟马上就有盟众叫喊了起来：“你算个什么东西？也敢自称评判？”

裸体法师轻轻抚摸了一下云鬓：

“我是谁无关紧要。只是今日这场盟战，场内众人只可观看，不可插手。小生所立之处，就划地围墙，入此墙内者――套用圣者盟经常说的话：人挡杀人，佛当杀佛！”

说着，裸体法师戈魔在场内走了一小圈，所到之处，众人情不自禁的向后退去。

只是到了圣者盟盟众所立之处，一个圣者盟战士忽然高喊：“什么东西？”手中破坏之剑甩手向戈魔挥了过去。

剑过，戈魔眼皮不眨。

战士看一招不成，高喊道：兄弟们，飞了他！

说是迟那是快，五个圣者盟盟众一同想着戈魔扑了上去。十余把破坏之间当头劈下。

之间戈魔弯腰转身，一个招式使出，五个圣者盟战士立刻倒地。

人群中间又是一群轰响。

法师戈魔使出的，不是毁灭烈焰，竟是空手使出霹雳回旋斩。

当下再无他人做声。

五、天战

我站在广场上，天空之城在朱子七的身后。

云动、风动、心动。

我说不出话，只有长叹一口气。朱子七却忽然讲话：

“久别多日，你好啊，老大！”

“不敢当……朱子七，你我的恩怨情仇，远非一两句话可以说得清楚。此时此刻，我也不想多骂你什么了。”

“嗬嗬，神舞门主客气了。你今日找我寻仇，距离你之前被封，相隔不过三个月，如今看你的装备，怕有300级不止。你神舞门号称举凡外挂者，皆为你门下之敌。怎么你神舞门门主之尊，不用外挂，就可以到今日的级别呀？”

我感觉自己的心和胃都在剧烈的搅动。

“不错，只为了今日与你一战，我已入魔道，只是先前你害我神舞门门下37条人命，辱我妻子嘉嘉，毁我神舞门清誉。此愁此恨，我若不将你千刀万剐，你要我如何咽得下这口气？！”

朱子七不再说话，只是从背后拿出了自己的杖。我听到周围的人低低的说道：

“大天使杖。”

我两手空空，没有杖，也没有盾。我唯一想做的，就是亲手掐死他。

