“呵呵。”朱子七并不回答，继续讲道。“你对盟下众兄弟无比信任，甚至连账号装备之类也并不隐瞒。如有所需，尽管到你的仓库去取。神舞盟主，你这老大做得好呀。只是你放眼看看周围，神舞门已经从最初的江湖顶尖垛口，沦落为二流小盟。上月三日盟站，神舞门被满门抄斩，输得一败涂地。小弟自以为是个有抱负的男儿。这样的战盟，你要我如何停留的下去。”

“我们战盟败北，是对方使用外挂之故，你不是不知。”神舞答道。

“正是，我入江湖，为的一个“强”字。今日我用外挂，你又怎知明日我不自成一派，凌驾于神舞门之上？“

“好小子，原来早就有贼心了！用外挂！你等着被封吧！”众人又是一片骂声。

朱子七听众人所骂，不怒反笑。

众人此时留意到，朱子七头上的的“神舞门”字样，已然消失。

“神舞盟主，你近日召开大会，称我为本门叛徒！与众人之前羞辱于我，这个仇，我一定会报。而且是马上就报！神舞盟主，你可知现在是几时？”

“快到下午三时了，如何？”

“好，很好，你等命不长久了。！”

朱子七话音刚落，忽然边上一个神舞门门徒大喊：

“怎么回事？霸王不见了？”

众人这才留意，战士霸王不知何时已然在自己的座位上消失。

神舞马上打开本盟花名册，“霸王”二字，也踪迹皆无。

***

冰风谷冰后区，漫天飞雪。

翅膀法师此时才认真看到，这个裸体法师的名字叫做“戈魔”。

“戈魔？什么来头，好像从没见过呀？”

翅膀法师意识到自己遇见了高人，欠身说到，

“这位戈魔朋友，不知道兄弟有什么地方开罪于你，让你一直跟随我大半个时辰了。”

戈魔立于雪原之上，半晌终于开口

“你用外挂！”

翅膀法师听到这话哈哈笑了起来。

“原来如此。你是想举报我吗？那么现在就去吧。照片应该也拍够了。要不要我再给你表演一下，好让你拍一张足够证明的照片？”

“好！”戈魔淡淡说到。

“既然如此，兄弟我就不客气了。”翅膀法师说着慢慢走到戈魔的身边，戈魔似乎并没有逃避的意思，只冷冷得看着翅膀的接近。

眨眼之间，翅膀法师一个拧身，高高跃起，两秒之内，十一记地狱火连续拍出，且招招命中戈魔。翅膀法师的这一意图，是让戈魔回城，自己也免去骚扰。

但是，十一记地狱火过后，戈魔仍然站在原地，除了满头银发仍在飘舞，似乎眉毛都不曾动一下。

翅膀法师收身不懂，想起戈魔不穿鞋却可以连续跑动，对他的身份也猜出了几分。

“你到底是谁？”

戈魔向前慢慢迈出一步，手捋银鬓，淡淡说到。

“我是GM”

***

她叫做嘉嘉。

十五级。

今天我要娶她过门。

之前也见识过别人的婚礼，那真的叫做气派，教堂里上百人到场祝贺，新郎官把满仓的祝福一颗一颗的扔到地上，新娘子一边捡一边就走到了新郎的身边。这表示他们的爱情一路走来，都是祝福。新娘子把一颗灵魂教导新郎手中，代表从此非你莫属。

然后，两人面对面说到：“我爱你”，教堂里掌声雷动。

那真的是动人的一刻。

可是我也只有30级而已，哪里来的那么多祝福呢？

嘉嘉并不在意这些，她只是不停的快乐着。她毫无来由的认定我是一个好男人，一万分的肯定我就是她这一辈子最爱的人。

我去市场的时候，忽然间发现她在人群聚集之处不停的喊着：“十五级小妹妹今晚八点结婚，请大家到冰风谷教堂啦！”

她大概没有发现我，只是在人影攒动之间，我看到她娇小的身影人群之间显得无比脆弱。

忽然就有一份感动冲击着我的眼眶，我明白我真的爱着这个小女人了。

晚上八点，冰风谷教堂。

我推门走进教堂，嘉嘉已经站在教堂最深处。

除我们两个之外，教堂之内空无一人。

“亲爱的，你来啦？”

嘉嘉跳到我的身边，我看着她走过来，心里温暖而且紧张着。

“亲爱的，我很抱歉，我现在没有什么朋友，所以也没有什么人能来参加我们的婚礼。”

“不用不用，我才不希罕别人来打扰我们呢。”她轻松的说着。

我知道她真地想让很多很多人知道，但她偏偏又不肯说，只是不想让我难过。

“你来，我们开始举行婚礼了。石头带来了吗？”

“带来了。”我一边说一边开始向教堂最深处走去，一边走，一边丢下石头。

――治疗之石。

嘉嘉跟随着我的石头走到我身边。然后走到我身边，将仅有的一颗玛雅放在我的手中。

“你瞧，将来不论我们受到什么创伤，都有彼此可以互相疗伤了。”嘉嘉说。

我想我们的眼泪应该是一起流了下来。

我拉着她的手，说到：“亲爱的，在你说愿意之前，我要告诉你一件事情。”

“怎吗？你有老婆啦？！我可不想做小的。”

“不是不是，我想说的是――我是外挂。”

三、灭门

神舞盟主的手已经在颤抖。

转瞬之间，9名兄弟消失无踪，偏偏朱子七却仍在大堂中间站立着。

“朱子七，你到底用了什么手段？！”

朱子七嘿嘿冷笑。

“神舞盟主，这全靠你呀。三日之前，你说你要出差，我已取用你的账号，其间与神舞门众家兄弟聊了聊天，他们的账号，当日之内就被我问了一个一清二楚。”

“你对他们做了什么？”

“没什么，只是用外挂帮他们的账号升了升级而已？”

“无耻！”神舞门剩下的众人骂道！

神舞此刻方才明白，周五下午三时，正是外挂帐号停权的时间。放眼看去，又有5名兄弟不知去向。神舞门所剩门众，已仅存十人不到。

“你到底想要怎样？”神舞咆哮道！

“想干什么很明白，凡盗用外挂者，神舞门之敌，神舞门一灭，我朱子七在此江湖，再无敌人。你三日前发出通告，说于近日此时召开全盟大会，我就料到是要与我作对。昨日我全面举报，今日此时，你们不死也难。”

朱子七说完，忽然将全身装备脱下，丢到了地上。又一眨眼的功夫，一身传说装备已是穿在身上。

神舞此刻已是怒发冲冠。

“垃圾！你朱子七就是一个垃圾！竟然作出天下如此卑鄙龌龊之事。”

忽然，神舞得忽然传来一阵声音。

“五秒，四秒，三秒，……”

“朱子七！我会找你报仇的！”神舞说完这句话，消失在英雄堂宝座上。

11月15日，下午三时许。

江湖第一门派神舞门，满门被封。

***

“这么说来你是专门赶来封杀我的。”翅膀法师问道。

戈魔点一点头。

“你三面之内，连用十记地狱之火，在这个世界上，是不可能有人做到的。”

“如果你是GM，为什么还不动手呢？”

“我只是感觉很奇怪，为什么你的数据会毫无不当之处。”

“原因很简单，这个外挂程序，使我自己写的。”

“果然是高手。你可知道大陆之上曾有一个战盟叫做神舞门？是当时大陆最强的战盟。”

“不知道。我只知道当下大陆最强的战盟是圣者盟！”

“神舞门门众共37人，当时江湖等级排名前20人，都在这个战盟里面。”

“怎样？”

“然而这个战盟却因使用外挂于去年11月15日被满门抄斩。”

“那又怎么样？”翅膀法师的面色开始不对。

“根据我的纪录，神舞门被封杀之前5分钟，一个名法师退出了战盟。这个人，也是神舞门唯一一个活下来的法师。”戈魔仍然不紧不慢的说着。

“你怀疑我就是那个法师？”

“是的。”

翅膀法师忽然开始仰天长笑，笑声划破长空，渐渐低沉了下来。翅膀法师说道：

“你可知道我此刻要去哪里？要去做什么？”

“不知道。”

“好，我告诉你。但我有一个条件，容我将此事，再将我封杀。”

“你说，如果理由充分，我可以考虑。”

翅膀法师沉吟半晌，从口中逼出四个字

“天空之城。”

“天空之城今日有一个重要的盟战，据说是一位不知名法师挑战当今江湖第一大门派圣者盟，这个热闹，你还一定要去看吗？”

翅膀法师淡淡说道：

“我不是去看热闹，我就是那名法师。”

***

“你为什么要用外挂？”嘉嘉满面费解。我看得出，她并不喜欢外挂。

“因为我要做一件大事情。”

“什么大事情？好啊，我帮你。”

“你知不知道现在大陆之上最大的门派是圣者盟？”

“知道，里面全都是带翅膀的人，好漂亮啊！不过他们的名声好像不是很好呀。”

“是的，圣者盟里的人，都是外挂。他们的盟主，已有350级之高，而我，要在两个月内，超越圣者盟盟主，向他发起挑战。”

“噢。这样子。你们男人……”

嘉嘉说着走到一边，在椅子上坐了下来。我走到他的身边，轻轻坐下。

“对不起。嘉嘉，如果你不喜欢，我们可以不在一起。”

嘉嘉沉默着，并不说话。

我之感动心里阵阵发酸。如果他能够明白我心中所想，也许就不会如此难过。

“我愿意……”嘉嘉忽然轻轻说道。

“啊？”

“我说我愿意！谁让你是我老公呢？你要做什么事情，必定有你的理由对不对？！来吧。”嘉嘉说着又从新跑到教堂上面，“不过，我有个条件！”

“说吧，什么条件都答应你！”

“昨晚这件大事情，我们就一起从新练一个小号，不用外挂，一直练到一千一万级！”

我看着眼前的嘉嘉，心中的喜悦难以言表，只淡淡地说道：

“我愿意。”

我和嘉嘉从此开始了练级的旅程。和所有的女孩子一样，她喜欢在练级的时候不停的说话，以至于总是被打回城内。每次被打回城，总还要像掉下悬崖一样的喊上一声：宝宝，我爱你~~~~~~

我以为这会是我生命的全部内容了，以至于有时候我甚至会想心中那件所谓的大事情，不如就算了吧。现在，我和嘉嘉，不是一切都很好吗？

然而，就在我们都进入100级的时候，嘉嘉忽然之间，消失得无影无踪。

四、归源

近日大陆上发生了一件大事情。

事情炒得沸沸扬扬，大致内容如下：

一个智力精灵在失落之塔八层与江湖第一门派圣者盟得门人发生口角，圣者盟门下随即派出三名魔头前往争执发生地点。在双方争执不下的情况下圣者盟魔头动用武力，将该智力精灵连杀一十七次。

