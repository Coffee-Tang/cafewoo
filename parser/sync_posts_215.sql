“不错，当年那名留下来的法师，正是当今圣者盟的盟主朱子七。”翅膀法师要碎钢牙，说出最后三个字。

“我知道你是谁了，你是神舞。”

“不错，我是神舞，但你不知道，我还是嘉嘉的丈夫。”

