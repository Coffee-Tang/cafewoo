<p>凌晨近6点，我已睁开了眼睛。拉开窗，天边开始发亮。我正在诧异自己怎么一丝困意也无，才想起已在伦敦。</p>

<p>在伦敦酒店里唯一的好处就是房间里有烧水壶。我可以泡杯茶然后从容地开始新的一天。</p>

<p>早春伦敦的清晨是阴冷无比，风叟叟地往脸上抽。但我很高兴可以用上紫红色的围巾和蓝色大衣，甚至期待着来场大雪。</p>

<p>给前台拿了地图，就出发了。伦敦的地铁人潮汹涌。伦敦人好象不愿意浪费过多的时间在路上，他们健步如飞直奔目的地。所以上下电梯一定注意，靠右边站，左边留给他人快走时用，如果你也象我一样悠闲的话。要不，很快就会被他人闪了一下腰。</p>

<p>伦敦人感觉上是蛮热情的，这体现在问路的时候。不管老的少的，男的女的，知道的不知道的，都会很的热心告诉你该往左还是往右，是上还是下。可你一定得注意，有一些人千万不能问，象你一样拿着地图的，推着行李箱的，年纪太大的。。。。。他们明明不知道，或者记糊涂了，但一样热情地拉着你七拐八弯，结果是你在整个地铁站绕了一圈，更糊涂了。在第五次走错道我终于得出了这样的结论。</p>

<p>先到的是TOWER BRIDGE，在东部。由四座塔楼组成的。塔楼里面有博物馆，展览厅，咖啡厅等。而不远处，便是TOWER OF LONDON。
<br/>下了桥在一座小咖啡馆里喝了杯咖啡，已是下午2点。沿着河边向伦敦塔走去。那是一座古老的城堡。正犹豫着要不要买票进去。看到一个亚洲的年轻人拿着一本书在看，很高兴地凑上去正想问问是不是中国人。可惜是一本日语书，上面有伦敦塔的图片。我便打消的搭讪的念头。同时心生惭愧，觉得自己太过散漫。每到一个地方，总不事先做好功课，走马观花地观望，过后便只剩丁点印象。
<br/>想到这便止住了脚步，认真地看起了城堡的介绍。这里是征服者威廉一世的皇宫。是一座上演着宫廷历代血腥斗争的地方。它不仅仅是皇宫还是监狱、刑场。伊丽莎白一世的母亲安妮便是冤死于这里。</p>

<p>由于时间来不及，我没有进去。如果明年再来的话，我会准备一天的时间来了解这座城堡。</p>

<p>我沿着这座城堡，找到了地铁。下一站，是大本钟。刚出地铁，没想到一抬头BIG BEN 就是眼前。就是一口大钟镶在一座耸入蓝天的塔顶，据说它重达21吨。但愿它不会哪天不高兴，便砸了下来。我一向有点杞人忧天。</p>

<p>LONDONEYE 在泰晤士河的边上。傍晚的泰晤士河十分迷人。夕阳的余辉逐渐在水面上晕染开来，有船只在上面飘荡。河的两边是一些几百年来依旧保存完好的建筑。</p>

<p>LONDONEYE 是一座顶天的大转盘，直径450英尺。上面有60个封闭座舱，每个座舱可以放进去一二十个人。这个大转盘据说是为了新千年而造的。站在座舱里面，它缓慢地转动了近半个小时。然后，你到了这个城市的最上端，你看到了它们统统在你的脚下。</p>

<p>我好象看到了一个世界，可当它把我放下，我又忘了。
</p>
