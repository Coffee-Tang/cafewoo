（外贸店篇）

珠三角地区的服装加工企业造就了广州的“外贸货”市场。好多人经常会问我：“外贸店”的货是从哪里来的？其实主要有三个渠道：一个就是“货尾”，做来料加工的厂家只管交够合约上货量，剩下的部分厂家就处理了。二是“QC”（质量检查）货，当发现货品出现些许的质量问题时会被退回，厂家就要自行处理。三是板衣，部分加工厂为品牌服装制造商制造的衣样样板，积累后流入市场。

所以，去看外贸货时总是会发现要么衣服不是有瑕疵，就是剪了牌。当然，也有些“外贸店”里出现了好些抄牌货或A货（仿制品），买家小心就是了。

不想和人撞衫的“美眉”、过于“圆润”的师奶、有心要穿出自己风格的型男，越来越自觉地去寻找广州城内的“外贸店”，慢慢地在衣服堆里掏出自己的心头好来。“外贸店”款新、特别、用料天然等优点都能把衣服上的小小瑕疵抵消掉，更重要的是，价格相当合理。

所以，我找出了城内最有口碑的几间“外贸店”，它们都有一大班忠诚的顾客群，没有名字或者身居陋巷都完全不是问题―――我有我的平靓正。

