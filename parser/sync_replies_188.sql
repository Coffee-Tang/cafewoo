个性商品讨好人心

人们喜欢这些小店，还因为它们出售的商品都有共同的特点：款式多，但是市面少见，而且每种款式通常只有一两件，以后穿着可以减少和别人“撞衫”（碰巧穿到同款衣服）的尴尬。

小店的商品之所以少见，是因为他们货品的来源很特别，一般有四种：一是出口转内销的产品，衣服样式多是日韩、美国或者欧洲的；二是国外进口的，数量不多，主要是男装的；三是品牌和专卖店的削价商品，所以一到换季或者换款的时候，这里就变得异常热闹；四是店子自己设计和制作的衣服，数量更少。

因为店子小而多，光顾的人还注重店主的品味。小蕾买衣服一般先挑店子后挑衣服，她说：“信得过店主的品味，以后扫货就径直往他的店子去，这样买衣服才省时省力。”

发现三条精彩小街

所在地区的文化氛围和生活方式，对小街小店的风格会有所影响，于是，不同的地区的店子也各具特色，以下是记者推荐三条新近发现的精彩小街。

◆东山庙前西街

从署前路往东华东路去经过的一片闹中取静的小天地，老房子、绿树荫、小鸟鸣，有老广州的味道。街上店铺主要集中在两头，也有着两种风格，从东山口进来，前半段主要是妇女儿童的用品，婴儿小衣袜、成人睡衣、内衣、孕妇装等一应俱全，妈妈们在给自己的小宝贝挑选用品时，也可以为自己添置衣物。后面一段集中断码的名牌运动服饰和用品，比较适合有个性的年轻人。

◆华乐路

可说是时尚小街的代表，地处花园酒店的背面。一路上集中了两类商铺：名牌特卖场和白领行政休闲服饰。最近新开了几间行政休闲的牌子店，据说部分是从香港过来的。

◆淘金路

从隧道走过去，是广州早期一片住宅小区。这里的小店更隐蔽，开在每一栋住宅楼下的小街上。服装店的衣服都是直接从香港进货，有日本、韩国还有欧美的，样式很新。精品价格比市区的饰品店要便宜得多。不少演艺人员都在此购置演出服装。

逛小街的小心得

逛这些小店必须学会两个字：耐心。因为小店的位置比较分散，每条小街都有几间，而且店铺的面积都不大。经常逛小街的人的心得就是：越是耐心，就越能找到合心水和罕有的货品。

