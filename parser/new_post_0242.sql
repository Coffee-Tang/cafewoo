《不能承受的感动》，其实有盘CD上收了这首歌的，放了在家忘记带到学校，又突然间很想听。记得有一次在J的BBS上胡闹，他的BBS是宠物版，可我偏在上面贴歌词，“若不是因为爱上你，怎么会夜深还没睡意。”还贴得振振有词的说，宠物就不能有为之失眠的感情么？BBS上的朋友都或真或假的抗议，群众意见说这个回声得管管了，不能纵容。版副就说老大，你来处理吧。J回了第一贴说阿回是我的朋友，你们对她宽容点好么？有什么事我担着。第二贴贴了一段文字，当时我楞是没看懂是歌词，“全世界看不起你，难道我就能对不起你，就是爱看你这样子，样子又有什么关系。”他也一直没和我说，J对我很好可是有时候他不和我说我一直都不知道。直到一切都过去后我自己才发现的。全世界看不起你，难道我就能对不起你么？有时候有一个人的信心支持我就能长久地坚持下去。

《千年等一回》，和我差不多年纪的孩子应该都对这首歌印象很深吧？哈哈。我想了半天没一首歌有这首这么能体现我们那时候的电视文化的：D

《只有情永在》，张学友和邝美云的合唱老歌。经典。

《俩忘烟水里》，很早以前我买过一盘港剧主题曲的磁带，拉开了我学习粤语歌的帷幕：），里面有《射雕》的主题曲和插曲，有一首歌很是令我印象深刻，“桃花开，开得春风也笑，笑春风，风暖象我情。”女声尖又婉转，不停在很高的音域盘旋，很有那时候的时代特征：D，《俩忘烟水里》的歌词也写得挺好，可惜记不住了，不过光瞅那歌名就猜得到歌词差不到哪儿去^_^

《爱我别走》，张震岳的。我对他的印象是一个弹着吉他摇头晃脑的英俊的男孩子。有很深邃的眼睛。

《在别处》。我有关许巍的全部印象只有《那一年》专辑和《爱情》。

《我想忘了你》，张信哲的《用情》前面曾经摘了一句，仅仅一句，多的没有了，用夜上海的灯红酒绿夜夜笙歌遮盖过去了。

《白色婚礼》，收录于许哲佩《气球》专辑。J在似乎很久以前和我推荐过。我正在挖掘所以关于J的记忆，所以就下了：）

