黄耀明的《下一站天国》，号称香港最妖娆的男声。一曲《春光乍泄》被他唱得香艳动人。然而最爱的还是《小王子》，黄伟文的词，黄伟文凭一首《北极光》令我对他瞩目不输林夕，现在喜欢的词人黄林二人，再加上一个方文山，桔子说过周杰伦是个奇迹，这我倒是很赞同，而方文山也是这个奇迹的一个缔造者，他有林夕刚好缺乏的那种光怪陆离的想象力。小王子已经是众口评论的东西，不再多说。

