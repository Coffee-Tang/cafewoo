>>>> 吃一顿，可是我们还得再走三天！

>>>>

>>>> 五月的天气虽然已经比较暖和，但浑身湿透的我还是禁不住打起了寒战。

>>>>

>>>> 当晚，我们找了一个干燥的地方扎营，为了节约食品，大家都没有吃东西。最受不了的是

>>>> 野山雀，他不停地喝了很多水，仍然无法压下饥火。他看上去很痛苦，但是我们也没有其他

>>>> 办法，只能把唯一的帐篷让给他。

>>>>

>>>>

>>>> 【六】

>>>>

>>>> 第五天早上，雨仍然细细密密地下着。

