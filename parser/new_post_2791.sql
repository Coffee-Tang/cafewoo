<p>朱瑞光3000美金游遍了世界。虽然心中存有疑问，但我还是认真的学习了一下不同航空公司不同的价位。没有发现一美金的机票，却找到了爱尔兰航空公司低廉的票价。从米兰到瓦伦西亚只有38欧元，而意航需要280欧元。</p>

<p>我为自己的发现激动不已。飞机不大，但相比起票价已让人心满意足。起飞时，推着饮料车的空姐边递饮料边收钱，才恍然大悟，低成本是如何运作的。</p>

<p>一个小时候就到了瓦伦西亚。</p>

<p>不似米兰的阴冷，这个城市明媚的阳光如同厦门，心一下子温暖起来。</p>

<p>房间在23楼。每到一个陌生的酒店，我总习惯先拉开窗帘看看外面的景色。</p>

<p>有一条河，再过去，是绿地，喷泉，半球形的建筑群。白色的，如同一轮轮在河上的月亮。</p>

<p>无比热情地爱着美食，所以对这个城市十分心满意足。</p>

<p>午饭时间在下午两点。海边一溜都是餐厅。干净的天空，绿起来没个尽头的海，绵密的细沙。。。。。餐厅里满是高高兴兴享受美食的人们。
<br/>P问吃什么。我说西班牙的米饭很出名，就来它吧。米饭是现煮的，等待的时间较长。
<br/>先上来的是JAMON，纸般的薄透明如红玉，是有名的生火腿，入口鲜嫩咸香。
<br/>很快，椒盐小墨鱼、奶油淡菜、美味的鹅肝。。。还有一种黄色蘑菇，蒜泥干煎的。P说只有十月份才有。我算是幸运的。
<br/>P不晓得我喜欢什么口味，便点了三份米饭，龙虾、墨鱼还有鸡肉饭。米饭上来时，我着实吓了一跳，是三个比洗脸盆还要大的盆子。黑乎乎的是墨鱼饭，其余两份两澄澄的缀着诱人的龙虾和鸡肉。
<br/>上菜的小伙子见我吃惊的样子，便把盆子伸到我面前。仔细一看，上面只有薄薄的一层米饭。P说米饭必须要铺开来，海鲜或肉的味道才能融进去。</p>

<p>好象墨鱼饭味道最为鲜美。</p>

<p>吃完饭，已近四点。在沙滩上舒舒服服的对着地中海伸了一下懒腰，明晃晃的阳光照得海面如同一块绿宝石。
<br/>西班牙人应该是最会享受生活的。</p>

<p>P又把我带到了酒店面前那白色的建筑群。</p>

<p>建筑群前面的河是属于VALENCIA公园的。这是一座长形的公园，绕着这个城市全长约20多公里。
<br/>而那一幢幢白色的建筑有电影院，博物馆等。我们去的时候楼下都是保安人员，电视台的车不知道在那折腾什么。P说，下个月国王卡洛斯要陪胡景涛来这里参观，在准备一些安全工作。</p>

<p>接下来我们便到了斗牛场。去的时候有点晚，还站得稳稳的牛一下子倒了下去，血涌了出来，它挣扎着还想起来，又被长予刺了一下，抽搐了数下，便不会动了。</p>

<p>全场都欢呼起来，可我满眼都是那红艳艳的血，眼泪一下子掉了下来。</p>

<p>这个城市如此招人喜爱。可我不懂，这一抹鲜血能够锦上添花么？
</p>
