夜空里回荡着我们的尖叫声和大笑声。

李听再没有来过信。

后来某一天，我慕名去听计算机系一个挺不错的英语论坛，旁边有个学姐告诉我，那个主持人很厉害的，叫李听，我才第一次见到了他。他那天显然也看见了我，有一点紧张，但依然是很出色的。结束后我平静地收拾东西走出去，在门口被他拦住了，轻声问我，你是不是有男朋友了？

