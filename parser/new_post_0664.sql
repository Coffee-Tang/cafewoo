时间证明了奇蒂拉决策正确的好处，河滩的路较森林内部更平坦，节省了行军所要花费的体力，一行人也走得挺快。虽然身处险境，大家倒也自在，除了“欣赏”爱蕾尔和摩亚无止境的拌嘴外，最大的乐趣莫过于每日的三餐。靠河的缘故，鱼成了众人每天的主食，有时摩亚会射些飞鸟或兔子什么的改善大家的伙食。格里芬天生就是烹调好手，将一干人的肚子照顾得服服帖帖的，也因为这个原因，爱蕾尔对格里芬崇拜到了极点，“没有我们，你们三个臭男人还能吃到这样美味的食物吗？”成了她的口头禅。她总是在格里芬料理的时候在一边蹦蹦跳跳。

看得其他四人大皱眉头，摩亚一次更是毫不客气地把她臭骂了一顿。结果当然是明显的，两个人花了一天时间互相漫骂甚至提出决斗，最后还是格里芬和奇蒂拉出面制止了事态的进一步恶化。

夜宿晓行，一行人沿着森林内的河流走了五天，然后折道进入森林深处。进入森另深处后道路开始没有几天前的那么好，在队伍前开路的星云和摩亚的任务也越来越重。爱蕾尔是队伍中体力最差的一个，她不得不把自己的行李交给欧文，还好这么多的行李对欧文来说似乎并不是什么大问题。

就这样走走停停地过了一天，一行人终于赶在天黑前在“巨树阵”内找到一块空地安营休息。用过简单的晚餐后，一整天行军的疲惫使得几个女孩早早就入帐篷休息，只剩下星云、摩亚和欧文围在篝火边聊天。

“还有一天就到目的地了……”摩亚道。

“嗯，不知道迎接咱们的是怎么样的一个邪灵法师”星云一边拨弄着篝火中的柴禾一边回答着。

三个人陷入了一阵沉默，听着柴禾燃烧时发出的响声。月光透过巨树茂密的枝叶照射在三个人的身上，一袭雪白。

欧文叹了口气首先站了起来面向着月亮。

“我们没问题的！”欧文大声道

“对！”星云和摩亚点头表示赞同。

“今夜有点静地让人害怕啊……”欧文喃喃道。

“喔呜~~~~~~~~~~~~”

摩亚警觉地醒了过来，他顺手拿了柄长枪走出帐篷。眼前是星星般点点绿荧荧的光芒。

“该死，大家快起来！”摩亚大叫，“快，结阵防御，咱们遇到狼群啦！”

第一个冲到摩亚身边的是星云，手里握着长剑，随后另外四人也冲出了帐篷。六个人站成了一个三角型，星云、摩亚和欧文各站一角，格里芬、奇蒂拉和爱蕾尔站中间。

“先别动，这些畜生似乎还不急着攻击呢。”大敌当前，摩亚还是‘嘿嘿’地笑着开玩笑，“爱蕾尔，被狼吃掉的感觉可不好。”

“行了，都什么时候了还有心思吵架。”奇蒂拉手握魔法杖瞪了摩亚一眼。“我先看看它们的数量。卡金亚啦！”

一个光球迅速在三角中央形成，白光照亮了周围的树林。

“我的天，这么多！”星云道。

“大家千万不要乱。格里芬、爱蕾尔咱们三个负责攻击；星云、欧文、摩亚，你们三个保护我们……开始了！”格里芬说话的同时，狼群发动了进攻。

“冰之女神，以你的圣明保护我们，冰之盾！”格里芬迅速的在三角阵型边建立起一面面的冰盾。“大自然的主宰者，请给我力量，破地咒！”爱蕾尔不敢怠慢，使出土系攻击魔法将群狼面前的大地撕开了一个壕沟，不少狼刹不住脚掉落进去。一些跳过壕沟的狼继续向六人发动冲击，但都被格里芬建筑的冰盾挡了回去。

冰盾前聚集的恶狼越来越多，它们有的用头将冰盾撞得砰砰直响，有的用爪子不停地抓磨着。

“冰盾还能能维持多久？”摩亚转头问格里芬。

“我不清楚，不过再这样下去我看撑不了多久的！”格里芬满头大汗回答道。

“喔呜~~~~~~~~~~~~”又是一声长啸。

狼群停止了攻击，并让出一条通道，一只奇特的生物从远处慢慢走进。

“魔狼！”欧文大叫，“是狼王，大家小心啦！”

话音刚落，一道闪电从魔狼挥出的爪里射出，击在一面冰盾上发出一声闷响。被击中的冰盾应声碎成粉末，狼群乘机重新发动攻击。奇蒂拉挥动手中的魔法杖弹出一个个火球，试图冲进冰盾缺口的狼被火球击中，在一阵哀号声后化为灰烬。但这并不能阻止狼群的攻击，哀号声和鲜血反倒更刺激了群狼的兽性，攻击更加猛烈。

魔法盾一面面的被击碎，六个人开始正面迎接狼群的攻击，杀机四起。枪剑挥舞时破空的响声，施展魔法的咒语声，恶狼死前发出的另人毛骨悚然的哀号声混成一片，地面上不断增加的恶狼的尸体反映了战斗的激烈。

一只狼倒下了，立刻有另一只狼踩在倒下的狼尸体上继续向六人发动攻击。时间越久，六个人防御越觉得吃力，战斗经验丰富的摩亚都出现有力不从心的感觉。

“可恶，怎么杀也杀不完！”星云挥剑将一只扑上来的恶狼劈成两半。

“难道咱们真要葬身狼腹不成。”爱蕾尔用颤抖地声音询问。

“不要放弃，快！在前面再弄个壕沟！”即使是这样危机的时刻，奇蒂拉仍不忘带领大家继续进行防御。

“啊~”爱蕾尔发出一声惨叫。

“糟了……”虽然欧文一枪挑飞攻击爱蕾尔面的恶狼，并迅速补位，但防御阵型已经被打破。

