这是否意味着我曾经拥有过爱情呢？或许吧，对我来说那是一份爱情。像每一个沐浴在爱情中的人一样，我渴望过，我幻想过，我高兴过，我曾经觉得我的生活每一天都是精彩的，我的每一天都因为有她才有动力，当然也曾有过伤心的支言片语。比起现在，所有的字符都是苍白的，无力的。

我曾经有过爱人。

我的爱人并不一定是爱我的人，但一定是我的最爱。更何况她并不是不爱我，至少在表面上她是爱我的。

我们曾经坐在带着泥土馨香的草坪上，阳光很散慢的洒了一地。她喜欢倚在我的肩上。她说好暖和。她的眼神深邃的凝望着我，我的心中总是阵阵的惶恐，惶恐她那带有光泽的唇。然后，我不由自主的吻了她，不由自主。

我已经是我们最亲蜜的交流方式了，虽然有好多次，都可以更进一步，但我却总是很艰难的控制了自己。我是一个很负责的男子，我觉得女孩子的贞洁是很宝贵的，虽然有的时候女孩子会因为冲动而轻易的献出了自己的贞洁，但作为一个男人，在无法给女人一些实质性的依靠之前，我想不应该去侵犯那一层神圣的。曾经我的朋友说过我太落伍了，但我并不以为然，只是莫名的执着并欣欣自喜自己还保留着那一份纯真。

我觉得必须用实际的生活给她承诺。

当我拥着鲜花向她求婚姻的时候，她却显出一副出奇的冷淡。

“你会爱我吗。”她的语调并不曾有高低的起伏，像一个痴呆的老者一般对着苍白的墙自言自语。

“你可以给我一个理由吗。”她点燃了一支烟，那一只修长的手在我的眼前划动着，她深深的吸了一口，并吐了出来。说道。

我有些茫然。

理由？我的脑海中坦然无物。

“给我一个理由，好吗？”她重复着。

“我对你有感觉，我会好好的照顾你。给你幸福。”我的语气如先前般的肯定。

“是吗？哈。”她笑着，讥笑。

我忽然发现自己是一个不折不扣的大傻瓜，身上穿着的是名牌西服，手中硕大的玫瑰在眼中异样热烈，单膝跪在并不萧条的商业街上，接受着来自四面八方带有观赏性的好奇的目光。更糟的是，女主角那眼神却是那么的不屑一顾。

这一刻，我很想站起来，但爱情的火焰却极力的阻挠着我，它让我尽力的保持着那种浪漫的姿势。一直到女主角心软为止。我已经不知道该做些什么了。

“你能给我什么幸福？”

“我可以关心你，照顾你，呵护你。还有我的全部。”我已经用尽了自己迄今为止最完美的形容词了。

“只是这些。”她的眼神依旧颓然不屑。

“是的。”

“你太幼稚了。”她笑着。

“为什么，你想要什么？”我发现自己的声音有些变化。

“那么你听着。”她说。“我需要的不仅仅是在精神上，还有物质上的。你能给我远远不够。现实就是这样，残酷又现实。”

“我需要更多的金钱来满足我的奢侈。我要让自己永远的美丽。你，没有。”

“我需要更多的甜言蜜语来满足我的虚荣。你，没有。”

“我需要更大的肩膀来给我足够的安全感。你，没有。”

“我为不同的需要，建立不同的感情，但我从来不真心的投入。只是尽力的去维护它，你明白吗？爱情只是一场宴席，总会散的，更何况那谈不上爱情。”

“那么你给我的吻呢，那算什么？”我并不完全的放弃对她的追求。

“那只是维持我们的关系罢了，最基本的工具。”她的言语正在完全背离着她在我脑海中的印象，所有的动作都并不曾在我的脑海中浮现过，也只有这一刻她才真实而痛苦的在我的眼前展示着。我突然感觉想吐。

“那你与我交往的目的是为了什么？”

“因为你纯，纯得好可爱。”

她上了车。扬长而去，那排气管留下的黑烟在空气中凝固成一种魔鬼的形状。我的心碎了。孤单了许多。

当然。

我并没有一直跪着，因为女主角的所有真实的面孔都已经展露了，一览无余，一点点的希冀都没有了，已经不再想拥有了。

现在的我只是在感悟，这个故事，至始至终只是一个游戏。我开始尝试用另一种心态去生活。

不知不觉间，我与女孩已经跳了好几支舞了。在融洽的背景音乐配合下，我们的关系发展得很迅速。

对于这种故事的情节我已经习己为常。

我并不需要去问她的名字。因为她会主动的告诉我。“我叫芷莘。你呢？”

我无需要知道她的芳龄，因为她会对我说。“我二十一岁。”

我所知道的只是她的数字是二十三，我编辑的游戏中的第二十三位。如此而己。

我所要做的，就是编造更多的童话故事。然后再留下一些若隐若现的情节让她们去猜想。我相信距离会产生美，而幻想会创造传奇。

随后，她会不经意的流露。

“在我十七岁的时候。”

我又一次刻意的摆了一个很酷的POSE.用最深沉的目光凝望着她，并在她忍不住想笑的时候附和着。

当一个女孩将自己的心事掏空给另一个人的时候，当一个人分享了女孩所有的心情的时候，女孩是否会对他（她）的感情有所不同呢？很多的时候我都在想着这样一个问题，这个答案我在每一天都在进行着，像现在已经是第二十三个答案了。

曾经有几个女孩向我表白，在她的眼里溢满了幸福的光彩，像当初的我。我看着他们光滑的唇映出我想说的话：“不！”

“为什么？”熟悉的话在我的耳边每每的回响着。像当初的我，这声音非常刺耳却为我所喜欢。

“爱情只是一场游戏。”我总是以这一句话结尾。

女孩的脸色会苍白，像当初的我。

我的脸上会泛出一丝丝冷冷而残酷的笑。讥笑，讥笑眼前的女孩如此的无知。就像讥笑当初的我一样。讥笑自己，一切都说不清原因。

我知道这仅仅是一种复仇似的满足，为什么这些女孩会像当初的我一样执着呢？她们并不曾与我有任何的仇恨啊。对于同样命运的她们，我如此的残酷让我开始恨起自己来了，我恨，恨自己。

我知道我的心理已经扭曲得历害，甚至我不否认其中有变态的因素。但我已经无法控制自己。

无能为力。

“你太幼稚了。”

这一句总在夜阑人静的时候在我的耳边回响着。而且在现在的生活中我也总会听到，只是说话的人物已经改变。细细想来，曾经的我的确是这样的，现在也是。

我觉得自己像一个遭人灭门的人小孩，在全家被杀光后，发誓要报仇。

刚开始的时候我只是单纯的想报仇，但我将银白的刃染成热烈的红色的时候，便在想着下一个该是谁？我简直成了一个杀人的工具。虽然每一次杀人的时候我的心中都有隐隐的痛，但我还是继续的想着杀人。

又过了一个小时。二十三号女孩看了看腕上的表，这动作在我的视线中进行着。

“该走了。”我说。

“是啊。”她有些依依不舍。

我察觉到了。

我们走出了那一个咖啡厅。已经没有那醉人的轻音乐了。但我们的故事并不因为音乐的褪去而马上结束。清新的空气弥漫在路旁，我们迈着轻盈的步伐。不觉间走出了我们见面的地点。

“我该走了。”

“问我电话号码啊？”我的心焦灼的呼喊着。

“谢谢你的款待，那么。”

“什么？”我脱口而出。

“有缘再见吧。”二十三号女孩就这样走了。没有回头。

难道我没有吸引住她？难道只是仅仅为了跳几支舞？难道她已经有了男友？难道她从来就不懂得把握机会？我为什么会再一次的感到失落。游戏的本身岂非不包含着失败与成功？

我的思绪已经混乱。一丝丝的故事情节也想不起，也忘了我下一步该做些什么。是不是挽留？还是任她一走了之？

“你知道吗？”她回过头。

我成功了，我想，她是舍不得我的，只是她刚才想吊吊我的胃口。这一回我一定要让你输得很惨，很惨。我又一次很清楚的看到了自己心理中那一份变态的因素。

“你第二次看到我的时候，其实我刚刚出门。”她把重音落在了“二”上。

至始至终，这个游戏，我的角色只是一只老鼠，她才是猫。在自编自演的故事中，我居然又一次的输了，很彻底。

但我在想。

如果我是很坦然的去邀请她呢？如果我不用游戏的方式去对付爱情呢？

结局是否还是如此的失败呢？

永远不要因为自己受过伤害而放弃尝试。因为生活的每一瞬间都是机遇。

但我始终无法面对的，只是自己的无能。

即使爱情只是单纯的一场游戏，但是规则却是可以改变的。

游戏之后，你能做的只有，遗忘。

