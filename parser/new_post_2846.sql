<p>
　　首先声明一下，我不是一个足球迷，最多只能称得上是个体育迷而已，本来对于足球场上的事，不必发太多的议论，只是那天无意中看到《南方体育》上的文章《厦门足球会被他玩死》，对文中提到的一些事实有所了解，这才有感而发，说几句话而已。</p>

<p>　　据我的一个资深网迷朋友说，这篇署名“清群”的文章从风格和使用语言来看，作者绝对是卓越兄，虽然我没有真凭实据，但朋友信誓旦旦的说法让我不容置疑，所以我就姑且认同他的说法，如有出入，还请卓越兄原谅。</p>

<p>　　我曾经在体育中心见过卓越兄，那时正值球迷们和厦门队大闹风波之际，卓越兄在那里分发他的名片，并向球迷们拍着胸脯，要球迷们有事找他或给他打电话，那仗义执言的架势不禁让我对他心生好感，及后又听说了他以往的一些事迹，如率先提出厦足“爱拼甲Ａ赢”以及带头喊出“迟尚斌留下”的口号等等，这些更让我对这位身材瘦弱的记者产生了几许敬意。虽然我并非足球迷，却也对此人的作为感到佩服。</p>

<p>　　但我读了《南方体育》的文章后，当朋友告诉我那就是卓越兄的大作时，我原先对他的敬意消失了，因为在我的心目中，记者应该以事实为准绳的来写文章，胡编乱造不应该是记者的所为。那么，这篇文章到底有哪些失实之处呢？</p>

<p>　　１。“‘厦门足球迟早会被他玩死！’一位六七十年代带领福建足球制造过辉煌的‘援闽’足球教练孙先生看完辽厦之战后，对迟尚斌十分不满。‘拿着东家的巨额薪金，却移情别恋，教练无心执教，队员马虎了事。厦门注入如此重资，被这些人如此‘挥霍’，可惜啊！’孙先生毫不留情地说，”我一字不变地摘抄了这段话，所有对厦门足球的历史稍有了解的人都知道，文中的孙先生指的是原福建足球队教练孙玉明老师，凑巧本人认识孙老师，当我向孙老师求证时，不料惹来了孙老师的一片怒火，“卓越兄在胡编乱造，我什么时候说过那些话了，大家都知道我和迟指导关系不错，我怎么可能对迟指导当面一套，背后一套呢？”听了这话，我不禁大吃一惊，难道报纸的报道和事实的出入如此之大吗？孙老师又对我说，卓越兄这人就喜欢追小道消息，以求制造轰动。</p>

<p>　　２。文中提到一位“姜金丘”的网友及记者蒋某之事，事实的真相是这样的，《足球２０００》的记者蒋锦秋先生曾因报道厦门队在对国安队一战前夕，“于周四提前开溜”，此话引起了俱乐部有关人士的不满，因为厦门队实际上是周五才离开厦门，其实该文并非蒋先生所写，那段时间由于俱乐部不满部分媒体经常无中生有地报道一些有关厦门队的消息，所以迟尚斌对记者声言，不轻易接受任何记者的采访，其时，正逢《人民日报》的体育在线组织了一个迟尚斌、陈理和网友交流的活动，这位“姜金丘”先生在网上向迟尚斌提了不少尖锐的问题，口气极其类似蒋先生，故迟尚斌误以为是蒋先生的化名，但蒋先生实际当时正在采访年维泗指导，后来此事也得到了许多人的证实，蒋先生也和迟尚斌将误会消除了，对于此事，蒋先生至今还愤愤不平，他认为是卓越兄或者是卓越体育网的人故意用那个化名以导致迟尚斌误解，使得他和迟尚斌产生了不必要的误会，他是在帮人背黑锅！</p>

<p>　　３。在文中所提的沟通会，厦门媒体均有受到邀请，并非被排除在外，对于这一点，文中的描写也是虚假的。</p>

<p>　　４。文中最后提到，“球迷风波”后，厦门已有两位常年报道厦门足球的记者离开了原来的岗位。实际上这两位记者的工作调动均属正常调动，而且早在此事之前就已经确定了。在这我可以直说，这两位记者的其中一位指的是原《厦门商报》的足球记者刘清华先生，刘先生现调到《商报》总编室，这次调动在年前就已经决定，只是到最近才实施而已，如有不实，本人愿负责任，有兴趣者也可直接向刘先生落实！</p>

<p>　　５。综观全文，大多都是编造的内容，其文中的手法，据我的朋友说，都是卓越兄的文章中的常用方式，我的朋友还调处了卓越体育网的有关文章给我看，确实如此！大家可以看看那篇文章，除了我上述的，都是一些没有确实证据的某人，某球迷的话，这样的文章，我关在家里，一天可以批炮制几十篇！</p>

<p>　　卓越体育网在报道卓越兄去深圳工作的消息时，称卓越兄为“厦门的名记”，当我看完了所有这些文章和我所了解的部分事实对照时，我不禁怀疑，这样的行为是一个记者应该做的吗？这样的行为配称“名记”吗？</p>

<p>　　实际上卓越兄的文章是针对迟尚斌的，本来轮不到我这样一个非足球迷来打抱不平，何况，我从来也不觉得迟尚斌是厦门足坛的神，也不觉得厦门足球就会被某一个人弄得倒退了（迟尚斌有这么厉害吗？），只是文中涉及一些我所知道的人和事，但又和事实偏差极大，才写下了这些东西。</p>

<p>　　附注：我的朋友和新闻界相当熟悉，感谢他帮我提供部分情况，另外，为什么咬定是卓越兄的文章还有一个原因，“清群”取的是“刘清华”和“周丽群”两人名字的各一个字，周女士是《海峡导报》的记者，听说也经常给《球报》等报纸投稿。据我的朋友说，卓越兄最爱玩这种文字游戏！</p>

<p>　　补充一点：关于球迷被打事件的处理结果</p>

<p>　　阙姓球迷坚称是于远伟踢了他，并称有多名证人，俱乐部对此事极为重视，调查结果是：于远伟坚决否认踢过人，同时也有多名证人证实此事并非于远伟所为。对此，俱乐部的态度是：承认确有球员踢了阙，但无法证实是爷爷，俱乐部愿为此代表球员向阙道歉，如阙不接受，俱乐部也愿奉陪阙打官司，阙最后接受了俱乐部的道歉，俱乐部除为阙支付医药费外，另送阙下半赛季套票一套，此事就此结束。 </p>
