走过街以后，我忽然不见了那头狗，和那个老女人。回过头才发现，他们并没有和我一起过街。他们还站在原地，在这样的一个街头，看着街灯闪烁，星辰日落。
眼睛被打坏的男孩的眼睛已经好了，吃香肠的男孩不再喜欢大叫海门，流川风改掉了后面两个字，新的名字叫流氓熊。阿杜当了福娃，音信全无。
 
汉子现在也大约和婆娘有了孩子，不能再老有这么些想法了。
 
还有无数的路人们要遇上这头梳着中分的狗，再闻到排骨的焦味，再在过街之后回眸。发现可爱的小狗并未随着自己过到街的这边。
 
我只是希望，在你们闻到这气味时能记得，能轻轻的回忆一下，那天的那串排骨，钱是我给的。







	
	
	
