“Give me a fish and I will eat today.Teach me to fish and I will eat for a lifetime.”（你给我一条鱼，我今天将会吃掉它；教我钓鱼，我会受益终生）。

外国佬总是点头点来不及地说：．

“Yeah! Yeah! You’re right!”

这是句中国古语。

可T跟周围的中国人说“授人以鱼，不如授人以渔”，没几个懂他说些什么。

T觉得所谓的素质教育就是“授人以渔”。好像哪个旅美华人教授也是这么说的。可要学怎么钓鱼，时间呢？忙着把放在眼前的鱼吞下吞下好应付中考、高考，谁还有时间学什么钓鱼！

素质教育……

T 抱着吉它的时间里，很喜欢弹唱超载乐队的《九片棱角的回忆》。虽然老妈说他用那种尖叫似的声音唱是“精神失常”，可他还是唱着：

