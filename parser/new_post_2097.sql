<br>　　莫末第一次见到安捷是在她七岁那年。那时她刚刚转到市郊新建工业区的小学，穿着米黄色布裙子和帆布鞋，两条长长的麻花辫细细弯弯地纠结在窄窄的脊背，仰着头望向灰蒙蒙的天空，目光茫然而直接，至少在安捷看来是如此。当时安捷就站在楼顶的天台往下看，鸽灰色天空和潦草的旧楼群之间莫末的米黄色身影。莫末当时很瘦很瘦，从上往下看得见领口突兀的细细的锁骨，安捷后来这样告诉她。是么，很瘦？莫末不记得了，她只记得后来她发现了天台边缘抱着画板的安捷，有着凌乱细碎的短发的安捷，碰触到莫末的目光后马上把穿着旧而肮脏的墨水蓝校服的身体往后缩，躲在画板后面的小小的漠然的身躯，让莫末看了心忽然一抽一抽地痛。
