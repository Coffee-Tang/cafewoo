<br>　　秋天来临的时候莫末和陶常常去城市东边的广场上散步。这座北方城市最好的地方就是有许多像这样的广场，广场上平平整整地铺着旧而干净的石板砖，还有许多的树，在秋天时候亮开又大又薄的黄色叶片，风吹过的时候它们会发出好听的啦啦声，还会在树枝上快速地旋转，好看极了。有的时候陶会牵莫末的手，把她的中指和无名指轻轻地绻在他的手心里，但大多数时候不会，他们只是并排走着，不怎么说话。有时陶会开口对她说，你看，莫末，这多么像法国，我以后就会去这样一个地方。于是莫末也毫不怀疑他要去的法国一定就是这样，有着许多石板的广场的小城，大黄叶子会在树上啦啦唱着旋转。那时莫末以为如果陶不去法国，他们的一辈子就会这样度过，每天黄昏时分到广场上散步，直到又大又圆的月亮从晕蓝的天幕中一点点升起来，把广场上的石板路照成流淌的牛奶一样的颜色。
<br>　　然而七年了，大学四年，毕业后陶在法国呆了三年至今，七年来他们走了这么远，早已回不到大一那年的秋天，她最美丽的十八岁的年华已经被远远地扔在时光的沙漏投下的巨大阴影里。
<br>　　七年前莫末独自一人离家来到遥远的北方念大学。那是一个有着美丽的石板砖广场和美丽的秋天的城市，在那里的第一个秋天走过的第一个广场时她遇到了陶。事实上陶是她的小学同学，但她对他几乎没有印象，小学六年混乱而茫然的童年记忆里只有安捷。也许陶是那个曾把松毛虫放到她凳子上然后被她把凳子狠狠砸下去的男孩，又或许他是那个每每安捷作画时远远地站在一边看着她的男孩，莫末记不起来了。但她会记得七年前那个秋天的傍晚，陶远远地叫住她，他们之间隔着长长的石板路，他向她奔跑过来，莫末转身时闻到自己刚洗过的头发的伊卡璐苹果香味，它湿漉漉地绕过她的肩头紧紧地勒在她的脖颈上让她透不过气来。
<br>　　那晚在陶的宿舍，黑暗中他俯身下来吻她的头发，她闭上眼睛时听到窗外的树枝被霜压断了的喀喀声，心里有什么东西像那天傍晚摔碎在广场石板上的金黄色阳光一样干燥而清脆地疼痛起来。
<br>　　陶不是个很好看的男孩子，他太瘦弱，没有宽宽的肩膀，但是干净而清秀。有时广场上吹过的风会把他软软的头发吹得扬起来很年轻的样子，还会把他的灰色棉Ｔ恤吹得紧贴皮肤凸出肋骨的印记。莫末喜欢黑暗中他亲吻她时嘴唇和脸上皮肤柔软的触感，甚至要用手指一遍遍触摸过他的嘴唇、脸和头发，反复地、直到流出眼泪，才能确定他的存在，他温暖的身体存在，多好，这种实实在在能够把握的物质感觉。所以七年来莫末只能迷恋身体，如此美丽的客观实在，而灵魂终是虚无。
<br>　　
<br>　　（所以后来慢慢地回想，也许我们从来就没有爱过对方的灵魂，从一开始就是通过身体的温度在暗夜里呼吸。但我一直认为陶是爱我的，当他在深夜最黑暗的那段时光里反复地说莫末我是一定要去法国的然后轻轻地拉过我的手指放在唇上不肯再放开的时候，当他一边激烈地吻我一边流下眼泪和被咬破的唇上渗出的血混合成苦涩的咸味的时候，甚至当他把全部的零花钱花在给新结识的女友们买only的衣服和prada的包然后身无分文地徒步穿过大半个城市来找我、我带他去学校的小饭馆吃面的时候，我都相信他是爱我的，并且一直毫不怀疑地认为，我也是爱他的，爱到拼尽全身力气拼得骨头都酸了还喊不出痛。
<br>　　而这种连自己都几乎要恐惧的爱的最终结局当然是离开。陶一如自己所梦想的，一毕业就去了法国。）
<br>　　　　　　　　　　　　　　　　　３
<br>　　莫末毕业后没有再考研，留在了那个北方城市里工作，过着朝九晚五的生活。莫末自己都没有想到曾经那么激烈的自己可以平静淡漠到这种地步。每天中午在公司吃工作餐，下班后去日式拉面馆或者供应套饭的中餐厅，然后回到租住的房子，晚上有时写一些文字，仍然不断有人在她的小说中死去，写到饿了跑到楼下的２４小时便利店买一点优酸乳和牛角包，偶尔在小摊上买蜂蜜煮的粘玉米，有淡淡的甜郁的味道。吃完了就上床睡觉，然后又是新的一天。有时写到没有办法写下去了，她就会让一个人死去，然后才能放下稿子入睡。她想起安捷应该也是这样的吧，没有用浓厚的色彩一层层铺涂自己的愤怒，是没有办法停下来的。也还是和安捷保持着联系，有一封没一封地写信，安捷照例给她寄一些新画，她也会把新写的东西寄过去，有时是已经发表了由稿费转变为的一小瓶新上市的草叶味香水，或者铜版纸的画册。直到有一天早晨，莫末打开信箱看到一张加快的明信片，是安捷，“２４下午抵京西站。”莫末扫了一眼日历，今天是２３日。
<br>　　第二日上午，莫末来到火车站，往人群里张望。阳光透过月台顶上巨大而污浊的玻璃天窗照射近来，白花花地晃着人的眼。莫末在明亮刺眼的阳光中恍惚回到七岁时的冬天，教室外的阳光也是这样穿过留有灰尘和水迹的玻璃窗户照射近来，把最后排的阴暗角落都照亮，安捷和她的画板就站在粘在地上一小方一小方的阳光和尘埃翻舞的光柱之间，一丝模糊的笑容在她的嘴角投下奇异而漠然的阴影。就象幸福，幸福是落在脸上的阳光，瞬间就成了阴影。七岁的安捷就从这些阳光和阴影中站起身，向莫末走来。是的安捷正穿过重重的人群，穿越１４年的时光向她走过来，仍然是苍白瘦削的脸，细碎的头发长长了一点，落在肩上。莫末快乐地尖叫一声，朝她奔跑过去，白色的裙脚打在她赤裸的脚踝上发出细微的声音。安捷笑笑地把行李往地上一扔，伸出手臂朝她拥抱过来。哦，安捷，你来了，真好，莫末不停开心地念叨。
<br>　　那晚安捷睡在莫末的大床上，两人象所有保持亲密友谊的女子一般讲了一宿的话。之前她们从来没有如此靠近过，从小都是不善言语的沉默女子，七岁时不曾一起玩过绒布小熊，十四岁时不曾手拉着手逛过街，二十一岁时安捷和爱过的男人分手，莫末给她发短信说，历史的必然。十几年来只靠画和文字来呼吸和互相表达。即便是写信，也是简洁到几乎匮乏的语言，却默契到可以一瞥就尖锐地直指彼此最深暗繁芜的灵魂。然而那晚莫末与安捷都滔滔不绝，安捷给莫末讲她在大学里组过的乐队“Ｉ’　children”，写许多华丽而嘶鸣的音乐，在酒吧狭窄昏暗的台上把长长纠结的头发扯过来遮住整个脸，还有她爱过的贝司手，有着被烟熏过的修长的的手指，在他破败的城郊小屋里给她煮鸡蛋挂面，直到直到分离。她在话剧社的演出中把深红的颜料用力涂抹在脸颊和手臂上，直到哭喊出来。莫末一点点抚摸着安捷剪短了但仍然纠结的头发和脸上被酒精尼古丁和长期缺少睡眠磨得粗糙的皮肤，心里有隐隐的疼痛。她给她讲陶，讲那些在秋天的广场上兀自灿烂的阳光，讲自己在通宵自习室里拼毕业论文，边上一个穿着感觉的粉色Ｔ恤和洗得软软旧旧的牛仔裤的女孩子在一沓散乱的稿纸上一格一格地填字，她年轻的脸上的做梦一样的光芒，而自己不知什么时候起已经是啃着玉米捧着厚厚高数课本赶论文的理工科女子，那种惊惧和疼痛。
<br>　　窗外的天空一点点明亮起来，晨光沿着窗棂一格一格地移进来，铺在不夜的大床上。莫末说安捷我又开始写了，一个长篇，《漆》，写得很痛很艰难，一直要不能控制地陷进去无法自拔。那个叫做漆的女子，仿佛受了诅咒般的，她身边的人一个一个地死去，我不知道最后是不是只能给她永恒的孤独。
<br>　　不会的，莫末，不会孤独的，你看，我们在一起。
<br>　　会一直在一起吗？
<br>　　是的，一直在一起。
