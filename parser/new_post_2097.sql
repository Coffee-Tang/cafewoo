<br>　　漆是有着明亮笑容的女子，林曦这样说她。林曦也是眼神干净明亮如清晨树林里的阳光的，这样的男孩子是适合初恋的，十六岁时的漆把头轻轻靠在林曦还没有成长得宽厚的肩膀，漆黑的头发的末梢扫过他白衬衫胸前软软的棉布，有一些滑落下来，还有一些挂在上面。
<br>　　那些都是很多年前的往事了，有时候漆隔着这许多年雾霭重重的夜色和迷离的水汽还能够清晰地看见十六岁时的自己和那个一如他名字般干净明亮的男孩子面对面坐在图书馆里的长桌前，午后的阳光穿过落地窗照射进来，有时候照在谁的额角，上边悄悄生长着零星的青春痘。也有拉着整片窗帘的时候，厚重的深枣色丝绒把室内的光线过滤成古老的黄昏颜色。那时的漆和林曦每天中午都在图书馆见面，一起做卷子或者什么也不做。有一阵子林曦每天都带一条瑞士糖来，各种水果的颜色和口味，每天都不同。他总是把瑞士糖藏在衬衫胸前的口袋里，让漆猜今天是什么水果的，猜不中就不给吃。而漆总能从他口袋透出的颜色准确地猜对。
<br>放学的时候林曦送漆回家，在校门斜对面的巷口等她，推着自行车穿过长长的巷子，在拐角的地方停下来，错落的树叶影子印在高高低低的石头台阶上，他的吻很重很重地落在漆明净的额角和花瓣一样微微打开的嘴唇上。远处隐隐有雷声穿过六月湿热的城市上空，仿佛灵魂被一点点噬咬的声音。那么疼痛。那些属于十六岁夏天的阳光、亲吻和吹过巷口的风。
<br>　　漆十七岁生日的时候，他们在城郊租了一间小房子。那个夜晚，漆把所有十六岁的柔软与生涩像销毁罪证一样冲得干干净净，一种更为冷漠和镇定的力量控制了她初到来的十七岁。他们在黑暗中静静地纠缠，纠缠，漆的眼泪雨水一样倾落，林曦修长的手指穿过她纠结漆黑的长发，一遍遍叹息般低声叫她的名字，漆，漆。那些图书馆午后白花花的阳光和古老教室潮湿幽深的空气，瞬间退却到那个夜晚他们窗外明朗星空的背后。诸神默默注视。
<br>　　第二天早晨，当漆忽然被一阵剧烈的心绞痛醒过来，身边的林曦已经没有了呼吸。
<br>　　她挣扎着爬起来，窗外的阳光明晃晃地刺痛了她的眼睛，竟然还是干净明亮如清晨树林里的那种阳光。
<br>
