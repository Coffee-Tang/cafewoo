（1）

我躺在新房间的床上，脸上带着笑望着天花板，回想着十天前的情景。

“什么？你说什么？”老妈一脸的惊愕。

“我说我想搬出去住。”

“为什么突然想？”

“快高3了，想换个清静的环境。”

“恩…还有呢？”

“想换个环境，换个心情。”

“恩，那我和你爸商量商量。”

“不用商量了，我决定了，无论如何不会改变。”

“可是总得商量一下选房的事宜吧？”

“不用了，我已经找好了，首期定金也付了。只是告诉你们一声，大概十天左右就可以搬过去了。”

她用惊异的眼神看了我许久，然后说：

“我第一次知道你可以自己办好这么多事……长大了，事情自己做主吧。”

“其实早就长大了，只是你们一直不知道。”我叹了一口气。

“呵呵…”妈微笑看着我。

“谢谢。”不知为什么，我说了一声谢谢。

然后，是一夜的失眠。

所有关于家的记忆，在同一时间涌进大脑。

的确，离开的时候才会明白曾经拥有的是什么。

即使以前并没有发现。

记得早上我拒绝了他们用车送我过了。

“记得照顾自己！”妈对我说。

“你也是，不要老是和爸吵。”

……

终于，该过去的还是过去了。

