第四天，你回了你的宿舍，我回了我的孤枕。你说你要独自想想我们的可能与不能。你说这句话的时候，我已分明看到邓亚萍去了英国读书，双打桌上只留下孔令辉独守，比赛暂停。

凌晨二点，你给我发来短消息：不管以后我们有否缘分，你都是我最关心的人，我把你当一辈子的亲人、爱人，两人相互扶持，走完这生命的旅途，不嫌不弃，直到永远。

那个夜晚，太黑，天上依旧没有月亮，甚至连星星都消失了；那个夜晚，没有通话，所以也没有声音。

长征进入草地，虽然我们还是一路同行，你已是被困在藻泽地里。我知道你舍不掉你的三年，我看到他正骑着马紧追而来。

