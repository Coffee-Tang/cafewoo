5

第5天，你回了家，但你已连接吻的勇气都没有。躺在床上，你说，你要和你的他生活一辈子，和我相亲相爱一辈子。

一辈子有多长？我可以让太阳和月亮帮我们丈量。

你在我的身边独自睡去，我无奈地睁着眼睛等待天亮，二个人的床上，既然还有一个害怕《天黑》：风若停了云要怎么飞，你若走了我要怎么睡，心若破了你要怎么陪，莫非你只是贪玩的蝴蝶。天都黑了你在想着谁，情都灭了我要怎么追，话都说了，你要怎么退，只会让我掉眼泪。整个世界突然一起天黑，爱在眼前无声崩溃，摔成粉碎。我闭上眼睛就是天黑，一种撕裂的感觉，嘴里泛着血腥滋味，多么伤的离别。我承认我害怕天黑，梦被掏空的感觉，我已不再是你的谁，想到都会心粹。

6

也许，我从来就不是你的谁。我只是你一辈子的亲人、爱人而已。

那一夜你没回家，甚至连说好的电影都没陪我去看。

我开着车在环到路上听着《剪海》：把爱剪粹了随风吹向大海，浓情转眼间变成伤害。

7

在一起的第一天，我就告诉你我讨厌别人骗我，特别是我的爱人。你怎么忍心就一场电影去伤害你的一辈子的亲人、爱人？

我不可能这么去问你，也不需要你的解释。

这一天，我们的世界开始停电。

这一天，是农历七月初七，传说中牛郎织女相会的日子。

这一夜，我也没有回家，独自去悦华会展酒店要了一个房间――2036。房间正对着海，外面有个很大的阳台。坐在阳台上，海浪的声音清晰可听。

你说过，你曾在这个阳台上看过十五的月亮从海中徐徐而出。我没告诉过你，我在今年生日的愿望里许过要在情人节带爱人坐在这阳台上一起等待日出。

没有月亮的夜，天总是很黑。我坐在阳台，天上的星星屈指可数，银河不见了，我担心牛郎织女如何能雀桥相会。

于是，我整夜地默许，直到天露鱼肚白，银河始终没有出现。我只是看到了一个风筝，主人不见了，线系在广场的草地上，留下它独自在空中。

