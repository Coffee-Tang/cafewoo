起床后，你去打开了冰箱，找不到可以早餐的东西。然后，你象只猫一样来到依然在床上的我的身边，说，我去给你买东西回来吃，我要你吃得白白胖胖的。那一刻，我没敢张嘴呼吸，我生怕一张嘴，心脏会跳出喉咙。你这样的女人，让我心跳得厉害。

你买回了一冰箱的面，还有蛋。

我们就象是两幅独立的风景画，忽然一夜之间被神来之笔成一幅。我甚至都害怕去回忆，也不敢期许将来，我只是但愿冰箱里的面和蛋永远吃不完。

那天下午，我们去了轮渡，鼓浪屿是你出生的地方。轮船摆渡着我们上岛，郑成功硕大的屁股始终对着我们，传说他是这个岛的守护神，我搞不懂他为什么连正眼都不瞧一下你这个岛的女儿，是因为你带着我，还是因为什么。

再次回到轮渡，我们遇上一个乞丐，他的碗里有些别人的施舍。乞讨真的可以生活吗？我从不以为，生活如此，爱情如此。

爱情是什么东西，我清楚，无非就是把一个人的事变成两个人的事，就好比乒乓球比赛单打换成双打，默契显得比技术重要。

