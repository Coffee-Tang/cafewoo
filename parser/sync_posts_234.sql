想起昔日流逝的岁月，曾几何时她就坐在我身旁，安静地听我为她弹奏的钢琴曲。那时那景已不在了，只留下美好的记忆，还能搜索着当初的细节点滴。她的手掌很小，手把手握着她小手的感觉很柔软，一个音一个音地教着她，想要将《致爱丽丝》的前奏将她教会。望着她认真的眼神，努力地想弹好每个音阶，那一刻的我俩是多么幸福啊！可恨我没有哈利·波特的魔法能够将时光停留。如果上天能再给我一次这样机会的话，我一定百倍珍惜，一定倾尽我所能将我所学的一切全部教会她为止。只愿一生，拥有她一人，别无他求。

昨日的点滴又重现，语言似乎惆怅了几许。感悟时光无情地流逝，怀念过去美好的小幸福，爱总是让人觉得不满足。我只愿我的心声能够通过这昨日重现的情怀连载传达到她心中。夜已渐深，人仍未眠。伴随着《yesterday once more 》的旋律继续着我的相思。。。

