上课。

关于语文课是否改名的问题，我们已经讨论很久了。因为连上课时唯一认真的人――老师他自己都不相信，这样的课堂能够称之为语文课。按照常规，大家总会在语文课的时候，从书包里摸出面包蛋糕薯片馒头，空气中还弥散着豆浆和咖啡的味道。偶尔一些没有进餐的

同学，也不会浪费这一节难得的“必休课”，他们或闭目养神，韶光养晦；或奋笔疾书，狂补作业。我猜想如果有一天语文老师下岗了，她也一定不会失业。因为她嗓子里发出的那种微妙的声音，不仅刺激着我们的食欲，让我们把馒头嚼得津津有味，而且还能大大提高我们

写作业的效率，笔作行云飞快，几十本作业、弹指一挥间。

“后面打牌的同学如果能像中间吃面的同学一样安静的话，就不会影响到前排睡觉的同学了。”老师无数次指责我们的自私最后却又无奈地纵容了。她所有的情绪总能被收在一脸机械的笑容下，所以，我们总在讥笑老师懦弱的同时佩服她无比宽广的胸襟。能修炼到如此

境界，其实也属不易。

然而，凡事总有个度，当某人公然起身在教室中换裤子，另几个同学因为争上游有没有硬吃的问题而吵起来的时候，她按捺不住了。

“不想听课的都给我出去，我不拦你们！”老师忽然咆哮起来。无奈之余，她也是有一些豪爽的。

常言道，恭敬不如从命。对于这一句话，从来没有谁向我们了解得这么彻底，并且贯彻的这么坚决的。她话音未落，早有７、８个同学从后排拿起了篮球，拍着走出教室了。

老师和同学们面面相觑。几秒钟的静默之后，她温柔地重新说道“来，我们继续上……”

