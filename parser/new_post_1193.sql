waiting for your love…

是谁在等待呢？

等的又是谁呢？

在旋律的会当中，

感情随着划过冬夜的流星，

陨落了。

⑴

很快，已经到了平安夜。

那份暧昧依旧没有被捅破。

不过一切到今天为止。

我下定了决心，今天无论如何一定要告诉她，不管结局如何。

不知不觉，陷入了幻想：

空荡荡的教堂大厅回荡着唱诗班的圣歌，柔美，空灵。

我们望着圣坛默默祈祷，许下愿望。

然后我拉起她的手，说：

“其实…我喜欢你很久了。”

她握紧我的手，说：“我…一直以为我是单恋…”

幻想完，我笑了。笑自己白痴。

不过如果真的如此，死而无憾。

“晓佩，晚上有空吗？”放学前我问她。

“嗯，应该有，怎么？”

“我想带你去一个地方。”

“什么嘛，搞得那么神秘。”

“不会卖你的啦！”

“去哪里干嘛啊？”

“嗯…我有一件重要的事想告诉你。”

“在这里说不行？”

“对，不行。”

“嗯…那好吧。”

“那，放学后我在音乐教室等你？”

“OK！”

放学后。

音乐教室里很安静，一样还是空荡荡的。

她还没有来。

我掀开盖板，做到长凳上，弹起了那首熟悉却不知名的曲子。

在晓佩的调教下，基本上已经能很熟练的弹了。

就这样，一遍一遍的弹，直到天色的金黄转成了玫瑰色再转黑。

站起身，看了一眼时间：

7：30

她还是没有来。

我走了出去，把门轻轻带上。

9：30

手机响了。

“喂。”

“对不起！拨了一个多小时才拨通…”是晓佩。

“怎么了？”

“我不是故意放你鸽子的，对不起！”

“没关系，不过，为什么呢？”

“嗯…因为…我家里临时有事，急着赶回来，没来得及通知你。”

“你家？”

“嗯。”

我抬起头，再次确认了一下晓佩家的灯是熄着的。

没错，确实是熄着的。

“哦…这样啊…那，不然先这样吧。”我尽力掩饰着语气里的失望。

“嗯，对不起噢！”

“没关系的，Bye。”

“Bye，bye。”

盖掉电话，心里很难受。

印象这是晓佩第一次对我撒谎。

也许，她说的家里有事是全家一起外出吧。

我安慰自己。

10：00

最后，还是到了那个教堂门前。一个人。

我最后抽了一口，然后把烟头丢开，两手互相摩擦。

十二月底的天气，已经很冷了。

正当我要推开门进去的时候，恰好有人开门出来。

