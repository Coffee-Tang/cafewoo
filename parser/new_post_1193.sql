手牵着手。

说起Eden，我也是认识的。初中的时候是同学，之后好像是去澳洲读书了。

Eden完全还不了解状况，热情的向我打招呼。

“Hey！好久不见！”

“是啊，好久不见，真巧。”我觉得我的面部表情似乎有点僵硬。

而晓佩的头，一直是低着的。我看不见她的表情。

“我想进去坐一会，你们去浪漫吧，Merry Christmas！”

“Merry Christmas！”Eden笑着对我说。

而晓佩的头，还是低着。

我走进了教堂大厅，唱赞美诗的唱诗班已经散去，只剩下几个神职人员在扫地。

坐到了前排的位子上，久久的凝望着十字架上的耶稣。

“Merry Christmas！”我对自己说。

