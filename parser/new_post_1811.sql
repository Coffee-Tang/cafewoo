回到平的住处。他的脸上有几分焦急。你跑到哪儿去了，我正想出去找你。我心里有些歉疚。我在拐角处的一家酒馆里喝茶。他的脸色有点怪，但慢慢缓和下来。你是去小婉那儿吧。你还挺懂得找地方。

