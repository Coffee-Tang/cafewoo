吃过了吗？他一边问一边挽起了袖子。我摇了摇头，没有告诉他那个热情的女孩早已经做了一盘蛋炒饭给我吃了。我跟着平走进了厨房。我知道这会让拉近一些距离。

一边洗菜一边看着身边的男子一脸沉默的忙碌。这是一种难以言喻的感觉。像是幸福就在面前。但又清楚那只是幻影。

