众所周知，野鸭进化成家禽后就失去了飞翔的能力。而多年的安逸，早已让我丧失了某种能力。比如，养活自己。

我去一个海边的小城镇。那里有我早年的同学。平大学毕业后，就回到那里教书了。说白了，我就是去投靠他。

小镇的公路。尘土飞扬。平指着路的那边对我说，那里就是海。

极目望去，路的尽头，只有天。

听起来很美。

这里的水，盐碱度太高。喝在嘴里像加了两勺盐。这里的风，很大。夜里很冷。脚丫子冰凉到天明。

但我能忍耐。我一向以此见长。不然，我也不会几年一直呆在一个男人的金屋里。有人说，男人是难以忍受的，除非你非常爱他。

什么是爱情？爱情就是点燃火柴的那一刹那。之后是相对稳定的燃烧。再后来，灰飞湮灭。什么都不是。

我是否爱那个男人。答案是没有答案。我从来不问自己毫无意义的问题。爱与不爱，某种意义上说，只是坚强与否的问题。但是，我可以肯定的回答的是，我对他的忍耐度相当高。

一个人漫步。

平在讲台上给学生上课。我路过教室的时候，向里探了一下。喜欢他平静的表情，和温和的笑容。这个不算英俊的男人的脸上自有一股让人安心的味道。

在窗外迎上他的目光，用手对他比画着要出去走走的意思。他大概看明白了。不动声色的对我点了点头。

小镇的街上有些萧条。风里有几分咸味。我拉紧了衣服，可大风还是不屈不挠地往领口灌。忍不住地颤抖。向四周看看，发现了拐角处的一家小酒馆。毫不犹豫地走了进去。

我下意识地抖抖肩，似乎想把寒意驱赶出身体。找到一个靠窗的位置坐下。开始打量这个地方。发现这里并不像一般的酒吧。虽然是阴天，但光线很充足。

屋子的角落里放着大瓶不知名的鲜花。只是开得很灿烂。

一个女孩走过来。请问要喝点什么？她笑得露出了白白的牙。我看着有点难受。这里有什么好喝的？她稍微想了一下，说，太阳花茶吧。我不禁一呆，就这个吧。请稍等。

过了一会儿，女孩端了一杯茶过来。我接过，喝了一口。我说，你们这儿的师傅泡的茶真不错。闻言，她笑得更开心了。真的吗，谢谢。我哥说这里面有阳光的味道。我突然觉得她的笑容里也有相同的味道。让人觉得酸。

这是你泡的？我忍不住问她。是啊。她点了点头。其实很简单。只要几朵干菊，几片柠檬，和一点蜂蜜就可以了。你回家可以试试的。

我慢慢地点点头。我当然没有告诉她，我自己也泡太阳花茶，味道几乎是一样的。

你不是本地人吧。我从来没见过你。她开始打开话匣子。你像是城里来的。

