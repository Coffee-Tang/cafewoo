我总坐在那个靠窗的位置。可以看见冬天里的太阳在这个小镇上移动的轨迹，偶尔路过的行人脸上的漠然和自己心里的斑驳记忆。

白天这里的生意并不是很好。小婉说到这儿，脸上的神情是安详的。但是晚上就不会了，因为哥哥晚上都会过来打理。再说镇上的人本来就不多，人人都跑到城里去了。

你不想去城里吗？她脸上露出隐隐的害怕。我不确定是因为对陌生的地方产生恐惧感，还是因为曾经受到伤害。我宁愿相信是前者。伤害不是人人都承担得起的。眼前的这个姑娘脸上看不出太多的复杂。

你坐会儿，我去给你泡壶茶。小婉急急地离开了我的视线。

窗外的阳光唐突地撞进我的眼睛。脑子里一片空白。

一阵电话铃声，小婉还没出来。我想置之不理。小婉的声音从里面传来，帮我接下电话好吗？

我无法拒绝。喂？我接起。

那端一阵沉默。你是谁？

我突然回答不出来。这个人的声音很沉，很轻，很苍白。但有一种凄厉的感觉。你找小婉吧。请过会儿再打过来，她现在忙不过来。

那个人不再作声。挂断了电话。

我听着那端传来的阵阵盲音，心下一阵茫然。

是谁的电话？小婉端着茶出来。

不知道。他没说。肯定是我哥，他就那样。特没礼貌，你别理他。她脸上浮现出愉悦的神情。也不知道他有什么事，大概又是晚上没空。也不交代清楚。她自言自语了起来。

我不知道那天晚上她哥哥是否真的没来。因为我之后就离开了。我知道我不能出来太久，平会担心。

晚上的生活其实很简单。

只是等平下课回来。然后一起做饭。吃完饭，他收拾，我洗碗。干完活，到客厅喝一杯茶。接着他开始备课，我就安静地坐在一边看书。

我们之间的话不多，但有一种默契。我想，毕竟是多年的朋友吧。

这样平淡的生活让人安详。但我知道，我势必呆不长久。这种宁静，只是我生活的调剂品。就像一件衣服上的补丁，因为与众不同而越发的显眼。但补丁只是补丁，它永远不可能成为一件衣服。

我依然会在半夜里醒来。睁大眼睛，听着窗外呼呼的风声。蜷缩起身子，不时地搓搓冰凉的脚丫。但却不会因此而暖和起来。

我知道，这个时候，只要身边有个人，我就会暖和起来。我可以将脚贴在他火热的身上。但我也清楚地知道，薄纱的后面是什么。模糊永远比清晰来得安全。

我是一只喜欢生活在浑水里的鱼，这样就不易被捕获。

清早被窗外呼啸而过的拖拉机吵醒。

平在餐桌上放着一张便条。晚上教师聚餐，不回来吃饭了。自己照顾好自己。

站在煤气灶前煮牛奶，向窗外看出去。

天空一片阴沉。灰蒙蒙的，压得很低。心里有些沮丧。

我决定去看海。

沿着这条公路。只要走到尽头。我坚信。

出发。

公路两旁是大片收割完的农田。一片萧条。还有一排排防护林。在风中摇晃。楚楚可怜。

走了很久。很累，也很渴。

路边没有店家。

我像只被遗弃的流浪狗。又冷又饿的在寂寞公路上穿行。

我终于躺在公路上。走不动了。

天还没黑。蓝灰色的云层在迅速涌动。眼睛被光刺得有点疼。

有人说，天空中的星座每天都出现在我们头上，可我们总吝啬于己抬起头来看上一眼。如果星座一千年才出现一次，人们一定会彻夜争相观看。

这条僻静的路几乎没有过往车辆。也许，今天晚上我可以好好看看星星。

如我所愿。

天黑了。但星星却很少。大多被厚厚的云层遮住了。

快撑不下去了。几乎虚脱。我听见远处传来微弱的机动车声。可车却始终没有出现。又是幻觉。我不知道该不该绝望。

风在耳边呼啸而过。送来了海水的咸味。

很困。

也许，我熬不过这个寒冷的黑夜。

迷糊中被人带上机车。风很大。刮在脸上一阵阵疼。突然想哭。

紧紧抱住他的腰。把脸埋在这个人的背上。热泪止不住地涌了出来。这是一个缺口，脆弱如同巨浪一般，冲破了坚强的堤防。一发不可收拾。

他的衣服有股女人的香水味。

哥？你今晚怎么这么迟啊。这声音太熟悉了。

