阿平？蝴蝶男人突然开口说话。怎么，一来就要走了。

你们认识。我不禁脱口而出。似乎是个意外，但又在情理中。

对啊。我和阿平从小到大就好哥们儿。他的口气有点怪。我知道这是他们之间的问题。

你在这儿呆了一天了？平没回答他，只是问我。

