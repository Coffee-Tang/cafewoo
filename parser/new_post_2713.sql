<br/>我哭笑不得，在老师的唾沫横飞下有如打焉的白菜，蜗牛般地蠕动回教室，一路上看见许多女生在编手链，各种颜色的彩线飞舞，一个个色彩斑斓的梦，一种颜色代表一个梦，梦太多了会让整个城市都在哭泣，梦在风季，今夏无风，我拥有的手链绿白相间，再配上点低调的黑色，我的梦是什么颜色的？
