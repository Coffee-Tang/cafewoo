<br/>晚上，小D打电话来说十呀十，你可真行，在课上画李逍遥，被逮着捉着骂，活该。我早就没好气了，冲着他吼—你算什么，你懂什么，这叫艺术知道吗？艺术？小D失笑道，你知道什么叫艺术？你懂得艺术就应该象凡&#183;高一样画完向日葵就向自己的脑袋开一枪，把永恒永远定格的自己最成功的画上，你做不了他们。你现在能做的就是考大学，读书再读书。我承认我承认，我连你小D都做不了，我扔掉了电话。
<br/>我有点迷茫，我的梦到底失什么？大学吗？我又问自己，我有梦过吗？
