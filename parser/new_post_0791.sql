TOTO自梦中醒觉，身边空寂而冰冷。他平躺在床上，瞪视着紫灰色的天花板，胃部隐隐泛着酸涩的痛感。

手机铃响，荧蓝的机屏闪烁着熟悉的号码。

游。

少年因奔跑而紊乱的灼热气息片刻间融化了一部分月的蜡质。

少女微笑着，递上一瓶冰水。

“我哥哥好么？”

“恩？”

“哥哥他…答应替我照顾你。”

“游，你想说的是什么？”

“我要去找一根弦。”

“游？”

讶异地绞着指间湿润的长发，TOTO怔然望着自阳台落地窗斜灌进房内的月色。

“弦的话，不是已经为你买了？”

话语掷在月华深处，发出水样冰冷的回音。

“……”

游下意识舔着指尖依然冶艳的血痕。

TOTO买的第一根弦被遗在空荡的桌面。

女孩消失了半年。

踏入蓝屋的瞬间，乔感到一阵晕眩。

堇坐在收银台前专注地串结细小的绿松石颗粒，弯曲的小拇指无意识地翘着。

名叫伦的男孩扬起端丽的笑脸：

“欢迎光临。”

“听说这里有LARSEN STRINGS？”

“对不起，只有大提琴的A弦。”伦不着痕迹地瞄着堇，而后者并未曾抬起眼帘，“是作为工艺品卖的。”

乔抑制着剧烈咳嗽的意愿，一言不发转身离去。

“伦，这个给你。”堇将用蓝屋的包装纸袋盛着的金属弦拈在指尖，“是我的G弦。”

“你为我拆下来的？”轮廓优雅的眼睛绽出惊喜的光泽。

“但我就要离开了，跟老板一起去欧洲。”

“那么，无所谓了。”

枯瘦的手臂自背后拥住TOTO蜷缩的身体，乔潜入意识深层的海洋。

