“如何？相当给人快感的东西吧？”乔苍白而修长有力的手指抚弄着男孩挑染成金褐色的头发，发出清脆的笑声。

“黑暗而波涛汹涌的海洋，生命随时将被打翻洋底……描绘瓦格纳的逃亡感受…这个东西我拉过。”TOTO低头看着自己微黑而骨节突出的手指，自四岁开始拉了十三年小提琴，指尖平整的趼皮大约一生也不会褪去吧。

乔愣了片刻，伸手按住少年轮廓尖锐的两肩，嘴唇凑近他的耳朵吐出温热的气息：“……不要追究那么多，年轻时只管尽情享受感官上的快乐就好了。”

TOTO默默把头埋得更低。

这个男人是什么呢？沙哑的声音如恶魔一般甜美，散发出午夜沼泽的湿润瘴气。

和游一样，不容任何人抗拒的魅力。

毫无预警地，TOTO的胃一阵痉挛。身体内部仿佛被一只饱含恨意的手攫住任意施虐，少年单薄的身体因痛苦而佝偻起来，透明的汗珠顺鼻尖悄然滑落。

“没事的，放轻松一点。不要抑制呼吸，不要晕过去。”

乔自背后抱住他，温热的手掌覆盖在TOTO冰冷的上腹部，以自己的体温试图平息少年身心的战栗。

“放开你的拳头，放轻松…”

“喂，不要晕过去……”

身体平静下来时，TOTO彻底坠入了无意识的深渊。

“啧，还是不行吗？”乔自暴自弃似地狠狠吸着烟，“游那家伙…到底要我怎么做？”

片刻，他剧烈地咳嗽起来。

以拇指和食指拈住钓鱼线，堇小心翼翼串起细小的绿松石颗粒。

这是在蓝屋的第一个工作日，上午九时二十七分，没有顾客上门。印度香的味道似有若无，异域的装饰物寂然杂陈，敷衍着暧昧的光影。

“你是A型的吧？”一个同样系着蓝紫色扎染围裙的男孩，鬓角干净熨帖，轮廓柔和秀气，给人类似英格兰贵族少年的印象，“爱替别人操心的类型。”

“……”堇望着对方的眼睛，有些不得要领。

“你的手，”男孩指着堇拈住钓鱼线的右手，“翘起小指却又不伸直，是轻微神经质的表现。”

“哦。”堇微微一笑。

“我是伦，上个月来的。”男孩的眉眼弯成一个好看的弧度，“昨天介绍过的，但要一次记得全部人毕竟不容易。”

堇释然，报以标注好感的笑容。

“你的手很漂亮，玩乐器吗？”

“吉他。”

“真好；那下次拜托帮我带一根G弦。”

