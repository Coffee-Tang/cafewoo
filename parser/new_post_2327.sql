那是什么时光。

站在2001年9月的时间和空间，大脑飞快地倒退。

有人说，那一天，是咖啡生辰。

3年是什么概念？

1095天，26280小时，1576800分，94608000秒，若用于恋爱，仿佛就是一生一世了。

说到这里，不禁笑了，说着说着就说到了恋爱，胸无大志兼迷恋声色。

老不正经婆婆。

其实，婆婆有个随意的歪理，听听则罢，恋爱，也不过三年，因用劲力量，才会觉得是三生有幸抑或绝无仅有。

98年我若有个婴儿，现在也三岁了。已经会叫妈妈，或者会背唐诗宋词了。

想象“凤阁龙楼连霄汉……几曾识干戈”被小不点稚嫩的嗓音念出来，我必满足万分，回首云遮断归途。

不过，咖啡里面，有此心境者，不过万一。窃笑，是老太婆心境，不可纵容，嘿嘿。

哎呀，跑了题了。

拽回来到咖啡。

3年于咖啡。

