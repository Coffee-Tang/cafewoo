    诗人是清高、孤傲的，所以当今的社会早已不是他们的天下。如果说顾城、北岛、舒婷、芒克是诗歌盛行的八十年代的偶像代表，那么现在就是继王朔的愤青一族之后的大话时代，十块钱一张的盗版光盘便可以带您步入神圣的艺术殿堂。只需在三个选项中点击一下，选择一个门进入，便可坐在马桶上尽情享受艺术创作的欢愉。在这个一夜成名的时代，试用版很快就会过期，随之而来的是鬼魅幽灵般的世界末日以及内心的惶恐与无助。其实无所谓，这就是荒诞的现实。我们的幸福像痨病患者脸上的红晕，是从小满足到大满足的过程，实现了幸福也就消失了。 
     
    飞机的航线远离小镇、养鸡场主人放弃事业不辞而别、女孩也终于告别了诗人的颜色，一切都来得迅疾而突然，即便最好的朋友也难以洞穿他的内心。青春依旧是灰色的，是我们对生活的妥协与隐忍，逃离只是一种方式，或者是对生活最勇猛的背叛。诗人在31岁时像马雅可夫斯基一样剃成了光头，沿着心灵偶像未竟的事业继续走在路上…… 
     
    《像鸡毛一样飞》实现了孟京辉在话剧舞台上无法实现的梦想，用光怪陆离的形式感影像表达先锋的电影美学思想：诗意与怪诞并存、风格化与自由随意并存、社会反讽与幽默并存。这是迄今为止我看到的一部最不像电影的国产影片，像一架装置作品，又像行为艺术，具有革命精神的后现代的剪接与拼贴如积水般堵塞人的大脑，通过转瞬即逝的画面显示出导演对生活敏锐的触觉。局促的画面感颇有舞台剧效果，自然极富戏剧性，镜头语言时而唯美、时而畸形，烟圈倒回口中、大飞机从头顶划过、鸡群中突兀的驼鸟…… 
     
    陈建斌的表演轻盈而不乏张力，与廖凡大段的对口词显示出良好的舞台剧经验。至于秦海璐，依旧保持着《榴莲飘飘》中那份特别的感觉，气质中蕴含着莫可名状的孤独的美。王劲松饰演的小贩从天而降，神经质地像探寻到宇宙奥秘的精灵。“小国”念着献给新娘的诗：“我愿意是树，如果你是树上的花；我愿意是花，如果你是露水；我愿意是露水，如果你是阳光……我的爱人，如果你是新娘，我愿意付出所有的努力，成为你身边的新郎”，是在现实中长久畏缩后奋力的挣扎。 
     
    听着片花中崔健的《时代的晚上》，想着诗人在色盲女孩身后拼命追赶的慢镜头，理想那么遥不可及，在我们口中又无时无刻不在标榜。你的理想是什么？像鸡毛一样飞。漫天翻飞的只能惹得一身尘埃，轻盈飘起的是现实中的一息宁静与执着。我愿作一支有觉悟的羽毛，目光坚定地从人们头顶飞过，不论风向哪个方向吹…… 
     
    在另一个我的梦结束的时刻 
     
    我精神恍惚地变成了诗人…… 
     
    （选自本人诗作《再别梦境》） 
     
     
     
     
