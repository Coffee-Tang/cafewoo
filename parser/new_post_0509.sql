我哭了。写到这里。没有音乐，身后有人。我还是哭了。

你否认过的，却被我看到千真万确的证实。你解释过的，却让我听到最坏的答案。他们是什么也不肯告诉我的，他们都还愿意保护我，宁可撕裂我的心脏，看我流血，逼我沉浸进去。你听他们说：“去。去。去。你去爱吧。我们是为你好。”我该听，还是不该听？

珍惜手中已有的。

我多想那样做。可是不能在接踵而来的欺骗里继续欺骗自己――我这样试过了，成功了，却痛的更厉害。

我始终不明白，你为什么欺骗我？为什么？为什么？

为什么要欺骗我？

如果仅仅是为了某种目的，你的目的已经达到了，你还不放手? 还不放开我？还要我的什么？我的骨头吗？我的血液吗？还是我的皮肤？或是我的长发？

拿走，一切都可以拿走，只要你放了我。

摊开手掌，就算是从来没拥有过什么，就算是一场关于玻璃鞋子的梦。灰姑娘依然回到厨房去烧火，而你，好好继续你的欺骗，好好地去狩你的猎。无论是好或不好的开始，还是好或不好的结局，走过了，也算是幸福。

