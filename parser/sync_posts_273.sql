<p>    从山上回来的第二天，我还是很累，短暂的快乐过去后，留下的是挥之不去的疲倦。
<br/>    昨天回到家，突然想换被单，早已筋疲力尽的我竟然还能独自一人换床单，换过之后洗了澡，还上了半个小时网，终于体力不支睡着了。过度的疲劳让我没有空想什么，躺在床上只觉得被子很干净，很温暖，外面虽然风很大，气温很低，可是还是很安心地入睡了。
<br/>    一觉醒来是晚上十点，家里人都回来了，起来吃了晚饭，半夜2点的时候还是觉得困，眼睛都张不开了，电脑还没关好我就已经睡着了，12个小时后，我才醒来。
<br/>    这样算来，我总共睡了将近15个小时，好可怕！！！
<br/>    我真的那样疲倦吗？</p>

<p>    山上很冷，因为地势高，所以风特别大，再加上下雨，连空气都是冰冷的，屋子里烤着火，窗户上有一层淡淡的雾气，四周都种满了树，一眼望出去都是绿色，我觉得很舒服，是那种什么都不用想的舒服，自然的，没有负担，没有压力。
<br/>    集体生活是很快乐的，大家吃睡都在一起，朝夕相处，总有许多趣事，即使过了多年提起来，依然是那么有趣而难忘。
<br/>    20多个人一起吃饭，大家挤在三张长桌子上，一般总有一张桌子生意特别好，所有人都要往那里挤，去晚了就没位子了。整个餐厅里满是笑声，吃饭是次要，搞笑才是首要，总有许多超级冷笑话是在这个时候产生，喷饭是很正常的事情，通常一顿饭吃完了，肚子也笑饱了。
<br/>    晚饭后分组收拾，轮到我的那天，刚好是晚饭，在厨房里卷起袖子，穿起围裙，用很烫的热水洗碗，一大叠的盘子全部浸在水池里，泡上洗洁精，看着白白的泡沫漫过手背，手烫得发红，盘子却一个个干干净净地从水里离开，窗户上因为热水又开始聚集雾气，我突然不想回家了。
<br/>    晚上10点过后是我们的自由活动时间，大部分的人都在打牌。坐在桌子前看大家打牌，看他们彼此争执，或者有谁闹出笑话，大家笑在一起，甚至笑到拍桌子蹬脚；有时我自己也玩，牌技不怎么样，老是被人玩，他们联合起来整我，把一大堆坏牌给我，害我要多玩好几轮，可是我心甘情愿让他们整，因为大家开心，而我是令他们开心的原因。也有人打乒乓球，看他们投入的样子，于是大家又开始笑他们要做“国手”，打球的人也不在乎我们怎么笑他们，只是一味的认真。
<br/>    打完牌，通常是半夜2点了，外面黑黑的，风很大，男生女生的宿舍是分开的，于是大家锁好了门，就开始狂奔，跑回宿舍，收拾好衣服，马上冲向浴室。水好凉，不是真的凉，是因为太冷，再热也觉得冷。一大群女生在浴室里叽叽喳喳聊天，边讲着八卦边担心黑眼圈越来越大，一边发誓明天早点睡一边开始计划明天晚上玩什么。
<br/>    或许是因为年轻，我们总是不知疲倦，累了一天，疯了一天，终于关了灯，上了床，还是不肯闭上眼睛睡觉。黑暗里，聊着白天的趣事，有时话还没讲完就听见有人睡着了。早上起来的时候，简直生不如死，恨不得只是梦，可以在太阳高挂的时候起床，衣服穿在身上是
<br/>冰冷的，比不穿还糟糕，连仅有的温度都被剥夺了。
<br/>    很少早起，如今却有机会在山上呼吸到清晨的空气，整座山都静静的，风还是很大，很冷，呼吸的时候整个肺都感觉到冰凉，可是这样的天气里，我很清醒。我突然察觉到，原来我也可以很简单地生活，我也可以什么都不想，我也可以只是一个很简单的19岁女生。</p>

<p>    感谢爱我的人，是他们让我知道什么是感动；也感谢恨我的人，是他们让我知道如何放手；爱恨都没有理由，伤害总有一天是要淡去的，没有人永远沉沦，明天总会是新的一天。</p>

<p>    写了半天，终于还是这样零碎的文字，没有完整的情节，只是生活里的点滴，仿佛我生活在碎片里，记忆的碎片。因为往事的沉重，我的现在很轻松，所有的不快都留在了过去，留在了咖啡的某些角落，留在我不想再重复的岁月里。
<br/>    现在的我，在这座静谧的清晨的山上，是平静的，风拂过我的发梢，带走往日的悲哀……
