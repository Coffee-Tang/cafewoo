在一次梦境之后，我发现我是那么不堪一击。

那个梦大概是下面一段所描述的那样。

我突然又被人给追赶了。梦的颜色是绿色的。我出现在一个被我想象成机场跑道的狭窄的乡村柏油公路上。突然，几个蒙面劫匪拿着手枪，逐个要钱，气氛极为紧张。我很是紧张自己的钱被抢。匪徒终于来到我的面前，迫我掏钱。然而，我却发现劫匪的头目竟然是我的大学女同学邢XX，劫匪中还有我高中时的铁哥们张XX，我小声问道：“你们怎么干起了这一行？竟然还抢老同学的钱，真没义气！”这时我那大学女同学说道：“配合一下，到时候分你一些钱。”我身体中贪婪的欲念又急速膨胀起来，很快我掏出了钱。就在这时，人民警察举枪及时赶到，我的同学作鸟兽散。我蹲在地上，等待救助。我的女同学又折回对我说：“还不快跑，到时候他们说我们是一伙的，你有口也说不清！”于是我便跟着跑。跑着跑着我就在一个有草的小巷子里，我快精疲力竭了，但人民警察仍很敬业。情急之下，我飞身直上房顶。蛰伏了一会儿，人民警察不追了。我仍不敢喘息，继续沿房顶向前飞奔。不知不觉中到了乡村的菜地。最后决定藏身在堂哥家的蔬菜大棚里（也有可能是坠入蔬菜大棚里的，现在搞不清了），地里有许多西红柿足以充饥。日子虽然在流淌着，而大棚里所面对的市集上的大陆公安仍没减少。有一天，堂哥和堂哥的妈咪――婶婶发现了我，他们对我说：“公安正在通缉你，你在这里再躲一躲。”时光荏苒，又有一天我终于被两个和蔼可亲穿着新式藏蓝色警服的中年公安给抓住了，不知是被发现的，还是自首的，反正是得到了法律的严惩。绝望之际我向他们问道：“我会被判几年？”他们慢条斯理的说道：“逃避追捕，以及勾结匪徒会被判六七年吧！”我一想，完蛋了！六七年，现在是网络时代，知识爆炸像原子弹链式反应那样快，我出狱以后还能干什么。完了，完了，于是悲观厌世的情绪浮上心头。自杀的欲望开始蔓延。突然，我想到了我最中意的女孩子――《大话西游》里的紫霞仙子，想到紫霞仙子就想到了她和刘德华演的《赌侠1999》，在这部片子里，刘德华在狱中精通了电脑，所以我入狱后，要专心学习电脑。又一想，历史上哪位有名的伟人没做过牢，尝试一下也算是多了一种经历。况且，在狱中不再受俗世纠缠，可以安心写作……

梦就记得这些，但那种绝望的心情我永远都忘不了。现实中的外强中干的自己，其实内心是那么不堪一击。

我可能一直是活在梦中的……

梦……

当太阳收起了温暖的光华，疲惫的我想要休息一下。含着泪水闭上沉重的眼帘，一个人默默走向梦的天涯：梦中的世界像一幅画，梦中的自己是那么高大，梦里梦到了你梦到了个笑话，梦到了所有的美好和我的家。于是我就可以让你留下，于是我就可以等你回答，于是我就可以不再流浪，于是我就可以陪你回家。你说梦中的我有一点傻，你说梦中的你不会害怕，千万别把握唤醒，让我去吧，梦里不再有忧伤没有牵挂……

5．欢乐颂

忘记了欢乐，也忘不了忧伤。

大家还记得上世纪末被媒体炒作的沸沸扬扬的九九大劫难、千年虫问题、千禧年，现在觉得不过是昙花一现、过眼云烟。正所谓：一时间江山如画，让人感到人生如梦、长夜漫漫、相逢很晚、造物弄人，多情应笑我，早生华发，一樽还酹江月。

我的1999年最后一天是在中华世纪坛度过的，当是时，才子佳人，春意融融。外国记者在看中国人的热闹（新华社记者语），男女主持人不论从外表还是骨子里都流露出资本主义腐朽的繁华，他们时不时直播现场，时不时采访现场中国学子，不知用意何在。当晚，江主席点燃世纪之火，我们欢呼，我们疯狂挥舞手中的校旗、国旗。江主席等国家领导人发表新千年讲话后，气氛达到高潮，外国记者完全可以从当晚我们的狂热中看出21世纪是属于中国的！

和以往的大型庆典一样：我的嗓子哑了，双手都被兄弟院校的兄弟姐妹们给握红了。

新千年来临之际，我和几个男同学在前方记者区看老外们的热闹、搂搂抱抱。追我的学妹在四处找我，希望我用她的大哥大给家里的老爹老娘问个好。幸好没找到我，要不然劳累一天的父母大人一定会大骂我打扰好梦的。

在新千年来临的时候，我竟没和追我的其实我也喜欢的学妹在一起度过。当时，我应该和她学老外中美丽的女主持人拥抱同事一样拥抱她，可惜我不在……

后来她离开我，现在想想是应该的，这么浪漫的时候我竟不和她在一起……

我将永远后悔当晚的不珍惜……

让我们为21世纪祈祷吧……

欢乐颂……

忘掉苦难忘记忧伤，爱心与我们同在；拥抱和平拥抱欢乐，拥抱灿烂的未来。当太阳温暖每一个角落，当世界从此不再有战火。从天边自由之神飘落，驱走黑暗唤醒你我。擦去那往日惊慌的泪痕，结束了岁月挣扎的错。呼喊住久留人间的虚伪，停止穿梭……当一切恢复所有的生命，当人们重新安宁的生活。从昨天风风雨雨中走过，每一颗心都已经经受得太多。祈祷那幸运降临的时刻，等待着惊喜代替失落。共同去重温久违的感动，远离寂寞……忘掉苦难忘记忧伤，爱心与我们同在；拥抱和平拥抱欢乐，拥抱灿烂的未来……

6．快乐天使

在我的记忆中我就没有几次是真正快乐的。

每一次的快乐之后都是更惨烈的痛苦。

我在明白了痛苦的原因之后写下了下面的诗句：

希望在起点  挣扎在途中  成功在终点

满足在成功之时  失望总是出现在满足之后

永远也也忘不了20世纪经典的话语：

……不开心，长生不死也没用，开心，就算只能活几天也足够了……

……来吧，就像你说的，做人不开心就算长生不死也没用……

……小姐，不可否认我长得很丑，可是我很温柔，而且永远不会说谎……

……经过这两天的朝夕相处，我发觉我已深深的爱上你了……

……爱一个人需要理由吗？……

……以前我看事物是用肉眼去看。但在我死去的那一刹那，我开始用心眼去看这个世界，所有的事物真的可以看得前所未有的那么清楚……原来那个女孩子在我的心里面留下了一滴眼泪，我完全可以感受到当时她是多么的伤心……

……曾经有一份真城的爱情摆在我的面前，但是我没有珍惜等到了失去的时候才后悔莫及，尘世间最痛苦的事莫过与此。如果上天可以给我一个机会再来一次的话，我会跟那个女孩子说“我爱你”。如果非要给这份爱加上一个期限，我希望是――一万年！……

快乐天使……

再一次翻动所有记忆，再一次靠近迷人的你。不知不觉之中又弄脏了自己。再次忘记我曾为你恐惧，又可以拥抱你的躯体，又可以接触你的秘密。不知不觉之中我又开始了游戏。又能暂时满足所谓的空虚，就像快乐天使走进我的梦，于是孤独的心有了冲动。多少次的梦想把你留在身旁，你却流着眼泪说你就要飞。匆匆结束短暂的快乐，重新走进无边的寂寞。难道只有我现在才是我？苦苦忍受欲望的折磨。追过云雾望穿了银河，猜不到你又向哪里堕落……

上个世纪已离我们远去……

送别……

逝去难忘的光阴还留下多少 这个世界有故事让人情迷惑

望远山山外山风在感动我 人生如梦梦醒时悲欢难友

歌舞一曲送别离内心悠悠 谁又知道你看到说到的理由

故事中的世界在慢慢告诉我 也许这样的世纪不再有

长亭外 古道边 芳草碧连天 晚风拂柳笛声残

夕阳山外山 天之涯海之角 知交半零落

一斛浊酒尽余欢 今宵别梦寒

韶光逝 留无计 今日却分诀 骊歌一曲送别离

相顾却依依 聚虽好别虽难

世事堪玩味 来日后会相予期 去去莫迟疑……

