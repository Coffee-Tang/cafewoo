想起《通灵王》里叶把别人的灵魂塞入自己体内那一刹那的流光溢彩，那手去碰了碰屏幕，似乎就要被吸到一大片亮光里马上就要奋勇战斗要不就装死昏睡。

我喜欢危险的事情，让我开心。

于是在旧伤口还没好之前就凭添了更多的新伤口。每日每个晚上看月亮，一天一天在变大，我说这个月的月亮好象比上个月还大了一点。你说我想太多。我说我还可以想更多。可是什么时候才可以看到真正的天狗欢快的从太阳那一头跑出来咬掉抢走阳光的逐渐变大的月亮。

一切都是因为这个暑假太漫长了。

很快的打出这么多文字，相对于现在的我已经很多了，离开一段感情后竟连键盘都不想碰了。做任何事都会想他是不是也在做一样的事，我在聊QQ的时候他是不是也和朋友在聊QQ。

分手后才知道自己有好多缺点，分手后才想起有好多朋友长时间遗忘了。

说话要加上很多个“的说”，嘴唇扭曲成愉悦的形状以为就是快乐的了，所有的“的说”表示了很大很大的肯定否定心底隐隐浮出来的心虚。

想看的电视剧一直没有演，是不是就代表所谓的愿望没有实现，真正想要的埋在心里痛得说不出来话来于是就不说了免得伤心失望。

爱情是千年一次的劫一旦错过分分合合无数都不能再遇到那个可以开花结果的人。

可是暑假都要结束了我依然无所事事不期待不紧张，冷冷的眼睛没有了光芒连恨人都恨不起来了，爱上的是谁也都忘了。

