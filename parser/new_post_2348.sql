一路上，艾薇总可以听见新人歌手潘玮柏的“我不怕”。艾薇很喜欢这首词，忍不住跟着轻声哼唱起来。

“……闭上眼深呼吸/全世界那么安静就像在专心听/你不会晓得/我好几次想吻你吧/小睡了吗……我想要说我那天没说的那句话/就算被没收我也不会怕/对/我不怕/我想要说我永远爱你的那句话/可能我说的地点有点傻/是/是我的/心里话”

很多词都让人觉得是为自己写的，那样更能融入歌的世界，忘却所有的现实，只为梦境清醒。

这时，迎面走来一个高大的男生，由于艾薇也没太注意，所以两个人莫名其妙地就撞到了一起。

“啊……”好痛。

“小姐，你还好吧？”太阳是落山了没错，不过这个声音听起来让人觉得阳光普照，很舒服，有点暖洋洋的。

“哦，没关系，没事的。”捡起掉在地上的KETTY挂饰。还好没弄脏，这是艾薇最喜欢的一个挂饰了，从小带到大的。

“哦。”男生也不知道从地上捡起一个什么，继续往一个相反的方面前进。

