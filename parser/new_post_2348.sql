“喂，你干吗老挂一个女里女气的东西在你的电话上啊？”哇噻，喝冰镇的百事就是很爽。子锐还非常夸张地把嘴巴弄得吧唧吧唧响，真像一个长不大的孩子。

“这是我的事，你少八卦。”被这样讲也不是一天两天的事了，不过梁弈一点也不在意，只要他喜欢就好，怎么样都无所谓。

“我是关心你，你这样会造成很多误会的。喜欢你的女生会以为你是GAY哦。”

“关心？你弹开啦。我还不知道你吗？哪里都坏，嘴巴最厉害，你不要给我说三道四我就谢天谢地，阿弥陀佛咯。”

“好好好，我弹开，我弹开，可以了吧？”说着，子锐还一颠一颠地往边上跳开。“哎，不过说真的，是不是有什么特别的意义啊？”没一下，他有厚脸皮地回过头来问到。

“真的是被你打败，你不是答应弹开了吗？你还问？”有时候真的是有点受不了了，这家伙喜欢打破沙锅问到底。可是梁弈一点也不想说。

“弹开过了，我现在是弹回来了。”一副嬉皮笑脸的样子，就是不肯刚过刚才那个问题，从第一天同时梁弈起，他就觉得自己这个问题是很有深度的，以至于到现在还没有得到确切的答案。

“弹回来？什么逻辑啊？”他还真以为自己是皮球不成？弹开了，还可以弹回来。和这样的人在一起，真的是什么时候得了神经病都不会有一点惊讶。

“罗嗦，我说可以就可以啦，别扯开话题。”砰，一脚踢飞了空掉的百事。

他，冯子锐，绝对，绝绝对对具有当狗仔队的天赋，而且是天赋异秉。

从来不正面回答，其实也不是个办法，不是什么人都像子睿那样的，只要你不说，他一定不强求。现在的人都对隐私极度感兴趣。

并不是不想正面回答这样问题，只是这要从很久以前说起了。抬头望望星空，回忆是甜蜜的，也是令人遗憾的。

小时候，和一个要好的朋友分离，原因是搬家。从此没再见过，走的时候，她送了他一个挂饰，就是现在他挂在电话上的这个可爱到极点的KETTY。那个时候其实并不完全懂事，但却意外地非常珍惜这个挂饰，一直到现在都还好好的。据说是以后“相认”的识别信物呢，呵呵。那时侯，真的是很天真。世界那么大，这样一个小小的挂饰，能有什么用呢？只是当时倒是很具有安慰性，一直怀有一个重逢的梦。随着年龄的增大，这个梦也变得越来越渺茫了。

真的很希望能在看见她，只是现在也许已经认不出来了，该是一个大姑娘了吧。

“喂？”把大手在梁弈眼前晃一晃，子锐对他的沉默开始表示不满。开始还以为他要酝酿情绪然后开是讲一个心酸浪漫动人的故事，结果等了半天什么也没有，等到的就是梁弈的沉默。

“哦，没什么、没什么。”梁弈开始加快脚步，他觉得只有快点到家，才能真正摆脱这次盘问。

站在路灯下，看着梁弈急急离去的背影，子锐不满地撇了撇嘴，但却没有再纠缠下去，那小子的个性他了解，真的不想说的话，就是他子锐磨破嘴皮子，也是无济于事的。

“小样的，肯定是个有故事的人，等着，总有一天……”

夜，越来越深，月亮也开始疲倦了……

