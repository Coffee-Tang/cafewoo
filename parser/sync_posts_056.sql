1． 泥娃娃

不知人们是否还记得这首歌的调调：“泥娃娃，泥娃娃，泥呀泥娃娃。。。”15年前，我一听到这曲子就会条件反射地扭动全身。那是我在幼儿园的最后一年。那一年的联欢会上，我将和另5位小朋友为大家演绎这首歌的舞蹈版本。有整整两个星期，每天下午我们都排练到动画片播完的时间才回家，奖品只是一块皱巴巴的鸡蛋糕。可是，就在正式演出前三天，我有生以来第一次学习了遵循交通规则，而结果是被一辆“永久”牌的自行车撞断了锁骨（有我自己红色的小棉袄上的破洞为证），只能打着甲板在下面客串观众。演出开始了，泥娃娃的音乐从破烂的录音机里吱呀出来----此时我的两臂展开不能动弹，样子就像一只钉在十字架上的雕。

2． 水中花

大概是小学4年级时吧，那时每个星期五下午老师都政治学习，我们不用上学的。这样，有天对面的哥哥就“乘虚而入”，请求用我们家的立体声收录机放一盒磁带。磁带的表面黑乎乎的，连标识都没有，大概是翻录的。那哥哥问我：“你听过谭咏麟的新歌没？”我回答：“谭咏麟是谁？”然后莫名其妙地看着他大吐其血。良久，他才红着眼对我说：“那就现在听！”我于是在惊恐中记住了那歌名叫水中花，并从那以后，一直觉得那是我听歌这些年来歌名和听歌时的情形最贴切的一次---那哥哥在音乐行进过程中唾沫横飞，这让我拼命幻想我正在某个角落孤独地打着伞（也许这就是水中花？），也算第一次学习了浪漫。=）

不过说实在的，这歌现在听来不错。=）

3． 哀乐

11岁那年的春节，应邀到父母的好友家去作客。街上到处都是林志颖的歌，我也跟着哼。总算到了目的地，那家的叔叔十分客气地拿出“机器猫”的连环画供我欣赏，我十分快乐地看了2本后，发现第3本看过，叔叔便叫我到他儿子的房间去拿新的。走到门口，却发现那房间没有开灯，似乎里面还冷冷的。我把门推到半开，光照了一半在他们家的哥哥脸上。此时一段极度让人窒息的曲子慢慢流出来，压在我的后背上，越压越沉。哀乐！！！我的心顿时变得像那年的雪一样冰冷。仔细一看，那微弱的光线旁边躺着一盒封面黑得庄严肃穆的磁带---《世界哀乐精选》。在这曲子播放的后半段中，我采用了立正的姿势，张着嘴，眼睛直直地看着那盒带，直到那哥哥“活”过来。

直到我惨白着脸，发着抖对他说：“我以后再也不听林志颖了。”

4． 你看你看月亮的脸

初中一年级的时候，我忽然狂恋上了宋词，整天凄凄惨惨，国破家亡地吟着。不久我和班上另外的6个才子才女自发组成了一个北斗七星的文学社，伙同年轻的语文老师干起了让人心跳的地下事业。可是，就在我们为文学社的第一期社刊企划的时候，班主任，也就是有“女强人”一称的数学老师却开了一次紧急班会。其间的纷繁复杂我已无从记起，只知道她说文学社是一在班级内部搞分裂，在同学之中分等级的邪教组织，并就此如取缔法轮功一样的将我们“铲除”了：“要拿高分大家一起拿！文学，文学不就是看本书，掉几滴眼泪吗？”而更可怕的是，不久以后，我们的语文老师莫名其妙地变成了手工老师。。。

之后的一个夜里，我们之中个子最高的女孩忽然轻轻唱起了《你看你看月亮的脸》―“月亮的脸偷偷地在改变。。。”不知怎么的，我就哭得一塌糊涂了，后来大家都哭了，连我们中的男孩子也是。在纷飞的眼泪中，这歌的旋律萦绕不去。这便成了我日后诸多青春苦痛的开始。

5． RIGHT HERE WAITING

初二的时候宠爱我的英语老师成为了我幼稚情感的全部寄托，我们一会如师生，一会如母女，一会却又如姐妹。我至今也不知道是先喜欢英语后喜欢她还是先喜欢她后喜欢英语，总之我只愿意将想到的最美的句子与她分享，我懒于在意一些同学的“忿恨”，只记得在她带有薰衣草香味的身边听她背诵着《飘》里面的句子，犹如人在天堂。有一天下午，我去交全班的练习本时，发现她正戴着耳机听歌（被校长发现怎么办？！=）），原来正是著名的RIGHT HERE WAITING。至此，我才第一次真正意义上地进入了英文歌曲的世界，在RICHARD MARX略显沙哑的嗓音中，我完成了有生以来做得最为标准的傻笑，并持续了1分17秒。=)

6．LES DERNIERS SONT LES PREMIERS

跌跌撞撞地进入了原校那全省最著名的高中部之后，没来由地就爱上了法语。我很幸运，此时国内恰好首次引进了真正的法语专辑，即CELINE DION的D’EUX。随后，就是我冒着数理化老师的白眼，在前后左右好孩子们的夹缝中求听歌的阳光灿烂的日子。可不知怎的，却一直对专辑主打歌《POUR QUE TU M‘AIME ENCORE》并无特别印象，只是对其中的一首叫作“结束就是开始”的歌似有所感。（其实一直认为CELINE DION的中音比她的高音更出色，她后来真是把自己丢了）于是，在一个星期三的晚上，看完了环球之后，废寝忘食地翻法汉词典译出了这首歌的中文版。译完后，倒头便睡，却没料想忘记了关掉随身听。。。在凌晨的寂静中，半梦半醒之间，LES DERNIERS SONT LES PREMIERS的旋律奔涌出来：“我们是人行道上的车票，过期的报纸，零乱的阳光，是一切被遗弃的东西。。。尽管还很年轻。但这没有什么，在我们自己的世界里，我们永远是王子---结束就是开始。”

我忽地惊醒，坐了起来，喝了很大很大的一碗水。

7．拉赫玛尼诺夫3号

