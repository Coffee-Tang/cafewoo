我常常会把自己投入到自己的故事里去，让自己亲身体验。我尝试让自己每天哭，我的眼泪哗啦啦像藤蔓植物布满我的脸，然后曲折地从下巴掉到衣服上，往里渗。

我的座右铭是信上帝者得永生，所以我不信上帝。我希望自己早死，下辈子要当一个拥有坎坷爱情的美丽女子，最后殉情。

我认识夏好几年了，始终不知道她的年龄。她的牙齿很洁白，她说话的声音像时钟，嘀嗒嘀嗒很有规律，娇俏的笑容和纤长的手指，手臂从来都是凉的。

故事还在继续――

夏准备去医院装假肢，她开始种新的兰花。装了假肢后她可以一边抹泪一边给兰花浇水。

夏的眼角眉梢脆弱得像一折即断，毕竟它们承受了太多的心酸。

假肢没有想象中坚硬，但没有温度，彻底冰凉。手术也不会疼，只是酸。

夏走到他的坟前，那里依然是一片空地，有人在坟前摆上了一束百合，带有朦胧的香水味。2B铅笔的字迹早已模糊，夏抱着坟头痛哭了起来。

谁晓得谁为谁哭泣，你哭我哭天就下起了雨；谁晓得谁为谁守约，你的泪拴住我的灵在吊唁……

---------2002年. 冉冉

夏离开了我两年，我的故事也写完了。她说她去一个南方的小城镇避寒，我让故事里的夏死了。夏长眠在他的坟前，坟上开了一朵绿色的花，绿色即是夏。

有人说写作的人太过寂寞，我是相信的，并且相信自己甘于承受这样的寂寞。有人说有的灵魂会从书里跑出来，我也是相信的。

2004年，我在计程车上听到广播里的歌声，那声音太像夏，现实里的夏，她一直不知道我的故事里写的是她，我是她前世坟上开的那朵花。

