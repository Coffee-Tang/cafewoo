<br>　　“我昨晚研究了这次试炼咱们的行军路线”利用休息的时间，奇蒂拉开始向其他五个同伴分析她所掌握的路线资料。“我想我们现在所处的位置就是死之森接近中部的‘巨树阵’吧，也就是说咱们再有一天的路程就能到达这次试炼的目的地了……”
