的斗室而感到的失意，因为他有知识，却不成功。大一时那位教"成
才修养"的老师明白我们渴望有所成就的心态，所以告诫我们做人要
比做学问更重要，光有知识是不会成功的。

是的，光有知识是不会成功的，但这不能做为我们不再读书的理由。

                         五

在灯下读《霜冷长河》，听余秋雨谈他"经霜而冷峻了的中年"。《霜
冷长河》里的文章不象《文化苦旅》那样关注历史与文化的领域，而
是在人生与社会的层面上进行思考。从字里行间看得出余秋雨在成名
以后经受过谣言与嫉妒，只是他不躲避也不急于辩白，而是冷静超脱
地分析造谣嫉妒者的心态、谣言与嫉妒的存在方式与对社会文明的影
响，凭着一个学者的良知和社会责任感呼唤善良。读罢这些文章，我
不想再对谣言与嫉妒做更多的思考，我只是佩服余秋雨的冷静与理智
。学者风范，山高水长。

我希望能拥有这样的冷静与理智。我相信这样的冷静与理智是在读万
卷书之后凝练出来的结晶；我相信它能够让人在为琐事奔忙之后在更
高的高度上思考人生；我相信那位教授最深层的期望是他的学生能够
因为多读一本好书而多一分冷静与理智，希望他们不要在事业的追求
中，在琐事的忙碌中沉沦；我相信我的老师与冷静和理智的距离比我
更近，他在冷静理智的思考中一定会为自己的桃李满天下感到真正的
成功，不再失意；我相信做人比做学问更重要，我更相信读书能够教
会我们冷静与理智地做人。

能想到的就这么多了，但我觉得找到了答案。








	
	
	
