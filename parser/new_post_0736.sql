<br>最初有点惨淡。那时的客人，总是一批接着一批地死去。从前捧她场的人都不在了。后来局势稍安，就出现了韩大官人。&nbsp;
<br>“姐姐索性从了韩大官人罢。”烟花总是这么劝说。&nbsp;
<br>韩大官人新去了正房，想要续弦。他是属意竹子的，但是竹子总是心不在焉。&nbsp;
