<br>第一次遇见竹子是在桥头。早春二月，下着雨。我不小心把油纸伞掉进了水里。竹子的手轻轻捞起来还我。&nbsp;她并不很美，然而立在船头风神绰约。竹子开口，一抹吴侬软语漂浮在水面上：“借问这位妹妹，此桥过去，可有一处乌衣巷？”&nbsp;
