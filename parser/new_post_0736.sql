<br>竹子“哎呀”一声。&nbsp;
<br>“我去沽些来。”我推盏起身。&nbsp;
<br>龙泉拊掌说好，竹子却凝视着我，按住我的手。&nbsp;
<br>“翦翦，你不舒服么？是不是菜不合胃口？”&nbsp;
<br>“哪有的事。”我侧头微笑，抽出手。&nbsp;
<br>携着酒，我跨进了药堂。&nbsp;
<br>“家里闹着鼠患，可害苦了！有没有好使的药？”&nbsp;
<br>柜台那边递过来一包砒霜。&nbsp;
<br>这正是我要的。&nbsp;
<br>回到水榭，还没扣门，却听见了烟花的琵琶、竹子的歌声。&nbsp;
<br>归来归来，江南旧曾谙。二十四桥花又开，杨柳深处燕呢喃……&nbsp;
<br>归来归来，吴音旧曾谙。稚儿闹骑竹枝马，慈母细煮稻米饭……&nbsp;
<br>是我写的曲子。依稀间，又回到那从前的庭院：茶的香，花的香，饭的香，高大的凉槐，年幼的弟弟……&nbsp;
<br>我双眼模糊。&nbsp;
<br>“翦翦……”不知何时，有人在我面前呼唤，用帕子擦我的眼睛。我抬眼看见了竹子。&nbsp;
<br>“你这么久不回来，我放心不下，出来寻你。怎么站在门外？你哭什么？”&nbsp;
<br>我摇摇头。“刚才，有个小孩子在那里，一个人，很可怜。”&nbsp;
<br>“在哪里？”&nbsp;
<br>“已经走了。”我说。&nbsp;
<br>提着酒进了门，我把坛子轻轻放在桌上。&nbsp;
<br>“怎么？不是封口的？”烟花撇了撇嘴，“可别被人兑了水。”&nbsp;
<br>“哪有--买的时候尝了一口罢了。”&nbsp;
<br>我拿了一只杯儿，将酒小心翼翼地倒至八成满，递在龙泉面前。&nbsp;
<br>龙泉大笑道：“闻着这味儿，我可也醉了三分。”他执杯唇前。&nbsp;
<br>忽地，他停住了。&nbsp;
<br>似乎是发现我一直在注视他，他放下杯子，抬头问我：“我们……见过面么？”&nbsp;
<br>“有吗？”我转开视线。&nbsp;
<br>余光瞥去，他似乎笑了一笑，又执起了杯子。&nbsp;
<br>“等一下！”&nbsp;
<br>烟花打断了龙泉的动作。她又翻出一只杯子，斟满了酒。&nbsp;
<br>“姓龙的，你须与我姐姐喝一个交杯儿。”&nbsp;
<br>我的心跳速度猛然加快。&nbsp;
<br>“不行！”。&nbsp;
