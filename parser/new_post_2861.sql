<p>    很长一段时间都不知道自己在忙些什么。没了写字的欲望，一本小说老也看不完。我想我是开始习惯于琐琐碎碎的满足中了。不停的买娇嫩的花儿，洁净的百合、滴血的玫瑰、明灿灿的稚菊、柔紫的睡莲，插在透明的瓶子上，自己暗自高兴地笑。</p>

<p>    床头的小白熊少了只眼睛憨憨地冲着我乐，细腻的毛拂在脸象谁温暖的手心。那天趁来雅打折，去转悠了一圈。拿着购物发票换来这么一只小熊，婷突然间笑得不可抑止，我才发现，它左边黑溜溜的眼睛啥时候掉了一只。坐在二楼休息厅里，对着它，我满意地连吃三只冰淇淋。</p>

<p>    和信子去唱歌。服务员在桌上燃起蜡烛时，手机在响，贴在耳朵里听，是辉。他说，别太晚回去。一转身，盈盈烁烁的火光里，我看见自己含笑的眼睛。</p>

<p>    放了很长时间的DISC。于是抱怨起自己不方便的长裙。忍不住还是上去了，变幻莫测的灯光下，我感到快乐可我想流泪。我想起了，在白玉兰纷纷扬扬的飘落中，我是怎样安静地走了出来让所有的片断都随风而逝。</p>

<p>    春节就快到了。打电话回家，爸又开始唠叨，玲子你已经不小了，事事都要认真阿。小的时候一直有丢三拉四的毛病，爸总说最担心的是我。奇怪的是，自己一向都很独立。早早就闹着出来工作，从没依赖过别人，活到现在仍然好好的一点没坏。</p>

<p>    一向喜欢睡懒觉。周末常赖在床上即使醒了也舍不得起来。总要胡思乱想扳着手指着自言自语的叽叽哩哩一翻。久了便有外号是小猪。朋友会准时伴着灿烂的阳光打来电话喊：小猪快快起床。很是窝心的称呼。</p>

<p>    元旦期间去了附近的小镇找朋友玩。小镇凌乱而温馨。一棵古树，几座石桌，几个老人围着打牌悠然自乐。朋友的小男孩有白净的脸和明亮的眼睛，絮絮叨叨地给我们讲蜘蛛和老虎的故事。安静地听他讲了一下午，天黑时，猛地感觉时间过得好快。听说那晚白鹭洲放烟花，许多朋友拼命给我挂电话问在哪里。突然间宁愿就这么错过了。那极致的绚丽，在黑暗中绽放开来随之坠落，象是心的破碎。人是不由得不伤感的。
<br/>回来时车子经过海沧大桥。向外望去，犹如在流星河中摇晃。我无法形容。点点万家灯火，是每个人心中固存的那颗流星吧。我突然间想起了《狮子王》片中，老狮王对小狮子辛巴说过，天上总有颗最亮的星子会不停地指引着你前行的道路。于是，我闭着眼睛，我说了句什么。一转头，看身边的人，正微笑着。</p>

<p>    新的一年，生活是否还会有变化？而我，是否依旧是黑白照片上笑嘻嘻的女孩？伸手，又紧紧抓了些什么？</p>

