<br/>柔弱却又是如此倔强的灵魂。
<br/>她以他为模特，画出了《MARS》。
<br/>希腊神话里战神的名字，带领大家冲破悲剧的黑暗英雄。
<br/>赤裸着上半身的少年有着坚毅而英俊的脸庞，眼睛里面是深不见底的黑却又带着无法形容的光，从熊熊烈火中走出，比任何神明还要英勇辉煌的绚丽光芒。
<br/>MARS。</p>

<p>他的幸福与光亮，从绮罗开始，翻阅却又读出另一部黑暗史。
