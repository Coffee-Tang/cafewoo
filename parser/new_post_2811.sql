<p>　　我给杰画了一幅画,画里是杰跟我描述他梦中的景象,但在田里走着的只是一个黑影,还有几片飞舞的向日葵花瓣,它们像翅膀带着那个只有轮廓和黑色的人飞出尘世的喧嚣。杰把那幅画举得高高的在墙上转啊转。杰问我,你知道那人在干什么吗.我说不知道。杰悲伤无助的说,祭奠。突然感觉杰的影子想凝聚了那些黑色的忧伤,在杰无奈的笑声中破碎开来,我从没这样抬头看过杰,心知肚明,杰和我之间的距离很远,远得就像我无法进入的那片葵花田,但这让我惶恐,全身风中独自颤抖，心口像针钻一样疼痛。 </p>

