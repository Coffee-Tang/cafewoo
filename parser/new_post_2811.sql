    不知谁拿走了那幅被我们叫作《祭奠》的画,杰疯狂的掀着教室里的桌子,摔着椅子。杰被锁在里面,所有的同学,像在动物园里看猴子一样在窗外看着杰对着蓝天咆叫,当杰把教室里的每一个角落找遍都没发现画时,杰开始撞门,我蹲在麻木看着杰的人群中抱头痛哭,听见杰用手打碎了玻璃,满身是血的跌坐在碎玻璃上,不停的说向日葵。突然杰站了起来,在教室里不停的在转圈。我奋力撞着门,所有的人像看马戏团小丑表演一样盯着杰和我,玩味的笑着,我瘫坐在地上,每一个人的脸在我眼前扭曲,变形,最后消失不见。杰转啊转啊,转到他的脸白得像死人一样,最后倒下,清楚的看见有一小撮世界在哭泣,空气中泛着淡淡的咸味和安伤。 

　　很久以后都没有再看到杰，无意中看到疗养院的车子停在学校的操场上，杰的父母到处找着杰，大脑里浮现出他们把杰扔上疗养院的车上，像扔掉一件垃圾一样扔掉杰时，眼前仿佛不是杰的父母，而是两只变了形的猴子。 

　　顺着人群的尖叫，向上望去，看见杰站在教学楼顶层的墙上，抱着那株向日葵在转圈。抬头看着蓝天下的杰，像看最美丽的向日葵站在阳光中，杰的脸上有着幸福的笑容，我想此刻杰在那个世界一定很快乐。所有的人都在赶往顶楼，只有我在看着幸福的杰，杰的身体突然倾斜，随后像只沉睡的蝴蝶，从天空飘下，如《祭奠》中的那几片飞扬的花瓣在空中曼舞，最后杰躺在了我们曾经种向日葵的田里，脸上有开得如一朵正在怒放的向日葵的笑容。杰真的到达了那个世界，那一小撮世界曾在哭与笑之间徘徊，蓝天破了成黑夜，黑夜碎了成蓝天，色彩刺目，声响扎心。于是杰只有等待时间的荒芜，同时那个世界也灰飞烟灭。杰安静的睡在阳光中，身上仿佛蒙上了晕人的光圈，但杰的身体却空洞得如一个黑影，大段大段的孤独，大片大片的悲伤都填不满的黑影。忽然，大脑里闪现《梦旅人》里的孩子把子弹射向太阳的画面。我捡起已沾满杰温暖的血的石头扔向太阳，而太阳却毅然的挂在空中，源源不断地向四周散发光芒。那不知真面目的太阳，温暖了整个人间，却无法照亮杰的一小撮世界。以后的日子，我一个人在墙上转圈，却始终无法转到杰的世界。杰离开的地方曾和杰飞舞过的小嫩苗，长成了一朵有着硕大金黄脸庞的向日葵。在此只前，杰从没真正见过向日葵，但在那个国度杰一定住在了葵花田里，永远。却不知杰还会不会转圈圈。 

　　日子恢复了往日的平静，偶尔听到有人说，有个疯子在这里摔死了。但从没有人知道这样一个世界：向日葵------阿波罗------转圈圈。 








	
	
	
