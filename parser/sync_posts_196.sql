听妈妈说，打小儿开始，我就倦缩在妈妈的怀里经历了三天的旅程回到一个妈妈陌生我也陌生的村庄，看望年迈的爷爷奶奶。再接下来的时光里，我总有机会和父母亲在这条漫长的铁路线上摇晃。

于是，坐火车对于我来说既不欣喜也不恐惧。亲人送我上车，而另一个站点下车以后会有我的亲人，不论熟悉与否，这血缘无法阻隔我的行程。

这样，就在同学中间多了些关于火车与另外一个地方的话题。而素不喜欢动脑筋的我也似乎只将这当成一件事情来做，到现在也只不过是脑海里多了一份回忆的影像罢了。

回到那块土地，一定要经过两片广袤。

西行的途中，最惊喜的莫过于看到断续的长城与孤寂的骆驼。而这些景像就在第一片广袤里洒落。

车，是很旧的那种内燃机车。这在过了兰州的打柴沟之后就成了必须的，前方有一车头冒着白烟牵着，末尾又有一呜呜叫的车头也在冒着白烟，它在推着。缓缓的向高海拔地方爬行着，而沿途经过的那些曾经的丝绸之路的明珠时，你会将燥动不安全部溶化在这翠绿与清凉之中。

外婆家，就在这明珠当中。

小时候，常在暑期的时候和母亲回去。渐渐的长大了，也就是在十四岁那年，我独自一人坐上火车去了那块土地。

埂边的新鲜枸杞与田间的泉眼，都成了我这没见过世面的孩子在日记里在脑海里常常忆起的美好。习惯用马桶与爬楼梯的我，在舅舅家的小院里竟然快活的有些癫狂。总会去抓抓山爷的尾巴摸摸驴子的长脸，更有机会可以在小河里水渠里野个够。

那年暑假，外婆家总是下雨，而夏收的麦子就遭殃了。在阴湿的天气里渐渐的发芽了，我哪里懂那么多，就跟着舅舅发愁就跟着舅妈去一遍遍的查看那些麻袋。

接下来的日子里，我就吃到了从未吃过的好食。就是舅妈用发了芽的麦子磨成粉再和成面，之后再做成包子或面食，甜甜的黑黑的，有些象荞麦面又没有那么黑，吃起来还粘粘的。这可是在村庄里少有的好食，只有我这远道外甥来了，才会遇到。

也常常会坐城里姨父的小车去沙漠景点玩，现在想想生活在那里的人们，对于沙漠与荒凉的态度竟都象年少时的我一样，全部接受尽情享受！

再长大一些，我选择了向更西的地方，在那里生活。

这回要过的广袤就是著名的塔克拉玛干大沙漠了。

我要从喀什到乌市。

坐上汽车运输公司的长途车，感觉像是上了伊朗电影里了一样。周围全是尖鼻鹰眼的中国人，说的全是韵动感很强语速很快的维吾尔语。平和快速的语言中夹杂着笑声，时不时的还合什双掌祈祷着。

对于一个有宗教信仰的民族我总是充满了崇敬之情，而此时此刻我就夹杂在他们中间，怯怯的坐着，随着破旧的客车摇晃着。车轮子行驶在戈壁与沙滩交织的地方，灰尘就会随着干燥的风吹进脖子里，还好那幅土土的太阳眼镜可以不让眼睛被迷。

要知道坐在这车上，窄小的车厢里满满的乘客都散发着长期食用牛羊肉所散发出的特殊出的体味所弥漫，加上听不懂的语言和硬硬的皮革包裹的座位，我在第一天就如坐针毡。可是，前面的路还很长，要走上三天才到乌鲁林齐，要是运气不好的话，还得耽误好久。

这话在这荒无人烟的地方想都不要想，因为发生这事儿的概率总会降临到人烟稀少的大漠里唯一的人群身上。沿途车子就经常爆胎，要不就是水厢开锅，走走停停的就赶上了雨季的塔克拉玛干。

以前总是在电视里看到沙漠的雨季，各种动物与植物争相在雨季里延续生命传宗接代。于是，草长的特别快特别壮，花开的特别艳特别大，而各色动物都会有依照生物链的规则以几何级数增长着变化着。在雨季里，天上飞的地上跑的都是生命。

可是，我真正的在塔克拉玛干里遇到的雨季就是所有的道路被雨水冲毁，客车就只有凭司机的经验就近开到小镇上等待修路工人的到来。

我们停靠的小镇太小了，只有几户人家，没有电也没有自来水。对于我这唯一的汉族来说，真的是很大的考验。于是，友善的笑容与可亲的眼神成了我的交流法宝。

在地图上可以清晰的看到，从喀什到乌市的汽车路线，只是从塔克拉玛干沙漠的边缘绕过，并没有进入沙漠的腹地。所以，见到的大多是一些戈壁与沙漠交织的地带。

在这些地方里，大片的胡杨林很少见到，而就在地平线上就会伫立着一株或是几株粗壮的胡杨树。在炽热的阳光下，石子泛起的光泽与地表长起的热浪将那粗壮、嶙峋的树杆虚掉，汗津津的眼眶看着它，仿佛是天边的梦。只有当车轮驶过树荫时，才从星星点点的碎绿中找到了欣喜。

这一路上，路过的尽是可以在西游记里找得到的名胜宝地。而对于我来说，最盼望的就是能够在近乌市的达坂城里逛上一逛。

偏偏凑巧，从达坂城到乌市的公路在维修，我们客车赶到的时候正好是最后一天，所以，我们眼望着乌市，还得在这里歇息一宿。

在汽车旅馆里洗漱完毕，就急忙走上暮色中的达坂城街头。先要解决的就是吃饭问题，但也要找一家干净、整齐而且有着漂亮服务员的餐厅才好。糟糕的是汽车旅馆不在市中心，我就只好坐上驴车，不甘心的寻找着。

那一夜，我睡的特别香。想想明天就到了乌市了，就可以重归城市的怀抱了；想想达坂城的姑娘不过如此，并不如歌里唱的那般似天仙。以至于我开始怀疑当初的这位歌者所关注的仅仅是歌词中的那句“带着你的妹妹，带着你的嫁妆，坐着那马车来”。

