<p>西堤边的草地上。一轮半圆月，低低垂在一幢高楼上，闪亮着红光，居然误以为是盏彩灯。在暗夜里璀璨。河的对岸，点点万家灯火，是临出门时妈妈含着泪光的双眼。看得见满怀的温馨。</p>

