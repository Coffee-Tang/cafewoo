<p>《天下女人》中梅婷在接受采访说起在巴黎下雨时买栗子的情景，很是感人。我一下子想起了在Florence的广场上，也是一样的雨天，闻到的栗子香。
<br/>懒骨头说把时间浪费在游戏上是可耻的，但我装模作样地坐在电脑前工作也是不对的。并不是很喜欢意大利，但突然间感觉总要写点什么。</p>

<p>10月份去的VERONA，因为工作。不喜欢意大利，是因为我年年在这里丢东西。这里的小偷是非常敬业的，让你在丢包的刹那肃然起敬。还有每到早上，的士司机非常忙碌的样子，前台阿姨会很热心的帮你打无数电话，教育你第二天要早起。老半天约到一辆，一不留神，让别的客人抢走了。
<br/>所以我很是怀念在NURNBERG的时候，有天路边拦了一辆的士。帅小伙司机问你有没有预约，我说没有。他挥挥手说对不起我这车已有人定了。所以第二次，另一个司机再问起时，我赶忙点头。结果这接下来我又傻眼了，他笑容可掬地说：对不起你应该等你的车。我这车没人预定。</p>

<p>结束工作后Andrea 说带我到他的家乡Masa，第二天可以顺便去Florence和PISA。</p>

<p>二个半小时的车从Verona 到Masa，突然一直下雨。到了酒店，电视里是中国的消息，台风从福建刷刷而过，台湾一片狼籍。餐厅就在海边，突然心想，如果一个大浪过来，我是不是可以游到龙宫。但A说Masa 从来不会有风浪。我想因为四周环绕的山。</p>

<p>Andrea 努力回忆着在中国我请他吃的菜式，橄榄油拌的海鲜，胡须长长的龙虾，各式水煮的蔬菜，当然还有米饭，他一直以为我特别爱吃米饭。A 很努力的做着这一切，可却把我给害苦了。我以为第一道菜及海鲜饭上完事了，就坐在那不停的吃。没想到他一共上了10道菜，最后我拿着他切的奶酪不知如何是好，他却说等等，这里的提拉米苏最为美味。</p>

<p>有一首歌，你的盛情我永远不懂。</p>

<p>我不由的想起了什么时候在美国，谁带我排了一个小时的队，问我吃什么，我随口说面条，结果真的只有一盆水煮面条拌西红柿酱。</p>

<p>我一夜没睡，给撑的。</p>

<p>天亮了就往Florence。阳光一直在哭泣，天气较之VERONA阴冷多了。</p>

<p>佛罗伦萨是文艺复兴的发源地，城内有许多教堂，博物馆，画廊等等。而这里是诗人但丁的故乡。
<br/>尼奥利亚广场上我看到了米开朗基罗的塑像。这一切都让你眼花缭乱。</p>

<p>想提的是天堂之门。据说只有复活节才开，跟着主教进去做礼拜就可以直达天堂。我们去的时候天堂之门是紧闭的，隔着铁栏杆，黄灿灿的颜色，刻着不同的图案。一问居然是纯金打造的。我问A可不可以把这门扛回家。A说上帝不介意，但警察会不高兴。</p>

<p>逛完佛罗伦萨，但一路赶往PISA。雨是越来越大，等到了PISA，人是累的不行，也冻的直哆嗦。撑着下车，眼里的斜塔就象一根冲天的柱子，不知自己是不是特傻，老远来看这么一根柱子，而且还有点斜。</p>

<p>伟大的伽利略，据说就是在这柱子上完成了自由落体实验。</p>

<p>比萨是因为斜塔而闻名。这也就是为什么意大利政府花了N年的时间让它恢复到百年前的位置，却刻意不让它改斜归正。</p>

<p>朝着斜塔的方向走去，眼前一亮。原来还有几座华丽的白色大理石建筑群矗立在宽阔的草坪上。有大教堂、洗礼堂和墓园。据说石塔原来是教堂的钟楼。</p>

<p>雨一直不停，天气冷得让你的思维都冻住了。也许是因为太想念厦门明媚的阳光了，我们只匆匆拍了些照片，便对PISA 说了声再见，走了。
</p>
