<p>开始喜欢王菲是看了她唱《天空》时拍的MTV。纯蓝的天空。大片大片绵白的云朵。穿着白衣裙迎风舞蹈的女孩。忽闪忽闪黑亮的眼晴幽深的潭水一般。赤着双足，仿佛每个脚趾头都在渴望阳光的亲吻，让人羡慕的自由自在。画面上王菲扎着两条小辫子，一脸狐媚的妖气。呵呵，我就喜欢这么形容，格外妖气。为此，后来我蓄起了长发就是想知道自己着白色的裙子摇曳着两条长辫子会是啥模样，也算很热切的菲迷一族吧。</p>

<p>王菲的声音带着孩子般很神经质的天真，飘飘忽忽地从你心头掠过。旁若无人地在自言自语。就象她任性的率直，也许语言无法完整的表达，所以才有《浮躁》专辑里那只有“哩哩啦啦”的随兴哼唱。没有歌词，只有笛音的伴奏。音乐才是真正抵达灵魂的语言吧。</p>

<p>“无所谓我什么都无所谓/前面的路也许真的并不清楚。”欲言又止的《誓言》。是她和窦唯合作的。爱情也许不需要去计较一些付出和失去。贵为天后的她跑回北京，开始一段注定不被看好的恋情。有报纸上还说窦唯最喜欢吃她做的菜等等小道消息。然而他们终是失败了，她在《誓言》里一开始试图解释一切都无所谓，未来是那样的飘摇不定。却又不是不在意的：“别以为执着的心就不会被砸碎/别以为我真的无所谓“。歌中伴有窦唯演奏的竹笛，迷幻而诡异。
<br/>《出路》是在他们婚变前合作的歌。“唯一相信爱情/渴望有个幸福家/可算命先生说我们的婚姻并不那么如意/说你到40岁就会有外遇/这样我担心/真令人担心”。没了《誓言》中隐隐约约的假装洒脱，这几句直白是看得见眼泪的担心。</p>

<p>王菲说过，“唯有在你做回自己后，生命才可以拥有力量”。可一个人怎样才能称之为做回自己？其实生命的力量是在你无法停止的迷茫和困惑中延伸。她可以在台上精灵古怪的对着自己的影子唱歌，不理不睬台下欢天喜地的菲迷，只因为她有上千万的身价，便可以自顾的扮酷。而对于生活，是否真的活得开心？在和窦唯感情的挣扎中，她终于筋疲力尽，营造的心灵花园一地凌乱。夜未央，梦魇已至。在为女儿写的歌中，有很真正的悲凉：“你是我的依赖/你是天的安排/你来填补空白/照亮我心底的冷漠。”一切假想和现实重叠，变成越来越深的冷漠。</p>

<p>童话是幻想世界里的绚丽。变幻的都市，很多的故事在它的上空不断飞翔。王菲一向漫不经心的唱着爱情的空洞和无奈。林夕的词写得美伦美奂。暗涌。约定。红豆。蝴蝶。
<br/>很喜欢约定里的一句歌词：忘掉天地  仿佛也想不起自己/仍未忘相约看漫天黄叶远飞。</p>

<p>仍未忘记相约看漫天黄叶远飞。呵呵，也许容颜早已模糊，刹那的感动和欢喜却永永远远。
</p>
