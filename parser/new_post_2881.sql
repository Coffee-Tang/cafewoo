<p>上周练笔，要求以古文《训俭示康》为题材，写一篇读后感……</p>

<p>Stovic练了一个暑假的作文，哼哼~，小菜……</p>

<p>于是，连构思都没有，便挥笔写下《重新看待节俭——读〈训俭示康〉有感》，大致观点是由中国古往今来的节俭观点，总结出节俭的定义，并套入当今社会现状，分析传同“节俭”的局限性，最后说明节俭应该在新时期有新的发展……</p>

<p>文中大量引用了国内外对于“节俭”一词的看法及材料，并佐以金融危机、政府刺激消费等时事材料，充分论证了节俭不是一成不变的，而是随着时代发展而表现出不同形式的……</p>

<p>本文耗时9分多钟，共计800多字[含错别字]……</p>

<p>关于本文的评语有150来字，全部为批判性文字，大意是本文思想错误，歪曲概念……至于本文的写作手法、存在缺点等重点问题却避而不谈……因此，当Stovic看了成绩后松了一口气——D-，还好没有…… E- ……</p>

<p>作文为的是什么？学作文为的是什么？考作文为的是什么？不就是作文能力吗？！既然注重能力，注重素质，为何要在内容上加以如此苛刻要求？且不论言论自由，单从作文的意义上讲就讲不通！难道素质教育只是应试教育在新时期的“同素异型体”？</p>

<p>中国的悲哀，中国人的悲哀，中国人下一代的悲哀！八股制度并没有消亡，而是改头换面，大大方方地登上了大雅之堂；真正的素质教育并不存在，死记硬背是中国人的本性，也是秉性……</p>

<p>看到“D-”是我很平静，真的，对于这种成绩，早已司空见惯，如果说只是记个成绩，就算是“E-”也认了，可就别在篇末讲那么多P话~！还有理有据义正词严地批注：“xxx，此处甚为不当，难道XXXXXX？……因此……所以……以后作文，应该……”</p>

<p>最后要说的是，对于写作文，一定要学会两面派，一面是真正的自己，一面是为师者所希望的你……</p>
