这种方式比较适合女孩子，含蓄而又意味深长。不管怎么说，画机票总比画别的难度要低些吧，否则让心上人看到自己只会乱涂乱划，印象分可要大打折扣了。推而广之，如果你特别有自信，且有能力、有天赋，那么就给爱人画张像吧！建议请参照《蓝色生死恋》的情节，把人像粗粗画在沙滩上，既浪漫又不必为画得像谁而耿耿于怀。

