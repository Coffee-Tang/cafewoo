出处：《网上情缘》

浪漫指数：★★★★

实用指数：★★★★☆

福克斯是连锁书店的大老板，凯则是儿童书店的小老板，福克斯想要吞并凯的书店。这对商场上的“敌人”，却不知道彼此就是对方最亲密的网友。最后，凯的书店终于出局，当她收到网友的信约她在花园见面时，她见到了福克斯，一切恩怨顿时化作柔情蜜意。

写情书是上个世纪的事了，先不说你字写得好不好，现在要找个邮箱寄信都好麻烦。有了E―mail，一切都好办了。

用此招最起码有3个好处。第一是你可能永远不知道正在和你通信的人是谁，也不用担心自己的秘密会泄露，即使分手也不会有后遗症―――最多收到“邮箱炸弹病毒”；第二是你们可以在聊天中畅所欲言，了解对方的生活小细节，《网上情缘》里的福克斯正是根据邮件知道凯喜欢雏菊，才打开了两人的僵局；第三个好处，是地球人都知道的，省邮费呀！

不过，网聊时小心周围呀，万一……参照《网上情缘》里的手段。

No.4基因礼物

出处：《玻璃之城》

浪漫指数：★★★★

实用指数：★★★☆

许港生与韵文是同学兼恋人。在分别时刻，许港生制作了一件特别的礼物送给韵文―――根据自己的左手制作的手模。最特殊的是，模型上的手纹全部都是由“韵文”二字组成的。“我的生命线、事业线、感情线，全部都是你的名字。”

谈恋爱没送过礼的大概没几个吧，不过礼物要送出特色来真不简单，需要多花一点心思和时间去策划和制作。不过，现在有了“人体克隆商店”，做这个手模应该不太难，只是容易了反倒没了原创的浪漫。被大众接受的还有标准情人礼物，还有“基因项链”等等，象征意义远远超过其本身的价值。

其实，更应该花的时间是去实践承诺。像许港生和韵文那样，爱到生命的尽头，那才叫人感动。

