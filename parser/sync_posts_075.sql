内容: 　　作者：白夜

看电影，可不单单是消遣。仔细看看那些浪漫爱情片，老套的求爱方式不少，但每每都能玩出新花样。怎么样，学两手吧！

No.1创意情话

出处：《阿飞正传》

浪漫指数：★★★★★

实用指数：★★★★★

那个慵懒的下午，阿飞对着苏丽珍说：“看着我的表，就一分钟。16号，4月16号。1960年4月16号下午3点之前的一分钟你和我在一起，因为你我会记住这一分钟。从现在开始我们就是一分钟的朋友，这是事实，你改变不了，因为已经过去了。我明天会再来。”

这样酷的情话，相信没有几个人可以抵挡得了吧！反正苏丽珍不行。请听她的内心独白：“我不知道他有没有因为我而记住那一分钟，但我一直都记住这个人。之后他真的每天都来，我们就从一分钟的朋友变成两分钟的朋友，没多久，我们每天至少见一个小时。”

说起情话，感觉是俗，但俗得有效呀―――男人、女人耳根子都挺软的。除了《阿飞正传》，只要是情意绵绵的“创意情话”都会被大家津津乐道，比如《大话西游》里那救命的“爱你一万年”。

当然，情话可不是空话，非但需要主角身体力行，更得注重持之以恒。唉，言行一致，说是容易，做起来难呀！

