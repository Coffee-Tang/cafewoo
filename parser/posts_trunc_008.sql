56. 《赤子》/ 叶德娴　词/ 林夕　曲/ 罗大佑如符咒般没完没了延续和纠结的旋律中，叶德娴用她温和的叙述带来很特别的感觉：原来，香港文化也能孕育出这样平和中透出大气的情怀。

57. 月琴/ 郑怡　词/ 赖西安　曲/ 苏来原来写给李建复的歌，被郑怡“强行霸占”了，也因此生出一首惊世之作。歌词中有对乡土音乐，民间艺人的关注。这是一首真正融合了传统的校园歌曲，代表了流行歌曲的一个方向。自校园民歌运动中对民歌的发掘、再创作到西北风，再到闽南语歌曲的复兴和广东话歌曲的流行以及摇滚乐手的各种尝试，其中都渗透着类似的思索。

