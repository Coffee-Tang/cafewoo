<br>    看你的硬盘里有这100 首吗？
<br>
<br>    1.一无所有/ 崔健　词曲/ 崔健1986年，崔健在首体表演这首歌时引起了轰动，但是他的摇滚乐专辑《新长征路上的摇滚》直到三年以后才得以面世，赶上了八十年代的尾巴，也为八十年代划上了一个句号。当初在首体喝彩的人们大概没有意识到，他们见证了一首史诗的诞生。它一针见血地揭穿了中国人的尴尬：物质上的富有和精神上的幸福我们从来就没有同时拥有过。崔健象个先知，预言了此后十几年里中国的信仰危机，这样的结果很难说是他自己刻意为之。他是用
<br>喉咙唱出了诗人的自觉。时至今日，我们仍无法知道，“一无所有”的状态还会持续多久，可无论何时，这首歌都会像幽灵一样拷问我们的灵魂。它是一把刀子，
<br>也是一面镜子。
<br>
<br>    2.亚细亚的孤儿/ 罗大佑　词曲/ 罗大佑罗大佑的情歌向来是极品中的极品，但他份量最重的作品仍然要数那些充满社会正义感和历史责任感的写实歌曲。从《之乎者也》里对传统规范的怀疑，到《鹿港小镇》里对都市浮华的迷惑；从《未来的主人翁》里对美好未来的希冀到《现象七十二变》中对物质生活的反思；从《将进酒》里对家国沧桑的吟咏到《亚细亚的孤儿》里对民族历史的追问，纵横九万里，上下五千年，罗大佑恢宏的视野借着灵动的文字和撼人的音乐在我们面前展开。他彻底推翻了“流行歌曲就是风花雪月”的陈腐论断，把中文流行歌曲提升到一个新的层面。有别于罗大佑其他歌曲中歌词的绵密、事件的繁杂和曲风的跃动，《亚细亚的孤儿》简单、直接，却又深沉大气，磅礴的气度中是悲愤
<br>的诘问，不仅直面过去，也指向未来。
<br>
<br>    3.石头记/ 达明一派　词/ 迈克、陈少琪、进念二十面体　曲/ 刘以达达明一派是二十年来香港歌坛最重要的音乐力量，虽然他们唱片的总销量并非最多，但他们在音乐上的成就足以令任何香港歌手为之汗颜。他们是高度商业化的香港歌坛的异数，也是一桩奇迹。《石头记》是香港流行乐史上第一首乐队冠军歌，以144 个字浓缩了一百二十回的旷世巨著《红楼梦》，单以文字而论，当属二十年来中文歌词的极品。达明一派受到New －wave的影响，气质不落俗套，音乐严
<br>谨而又毫不生涩，品味独特却不偏执，强烈的唯美主义倾向和摩登的现代气质应当只能在香港出现。他们是二十年来最复杂的音乐创体。
<br>
<br>    4.一样的月光/ 苏芮　词/ 吴念真、罗大佑　曲/ 李寿全《搭错车》电影原声大碟是中文流行音乐史最重要也是最出色的作品。这张唱片和罗大佑的《之乎者也》一道被视为校园民歌时代和现代流行歌曲时代的分水岭。苏芮历经多年磨炼之后，因此一唱成名，成为唱将型歌手的典范和座标。以今天的眼光来看，《搭错车》这部电影并不能算作是十全十美的经典之作，电影中的歌曲倒更具有深刻的感染力和持久的意义。《一样的月光》作为电影的主题曲，对现代物质文明
<br>侵袭人的本性这一主题进行了冷静的批驳。热烈的摇滚乐风有别于传统上对女性歌曲的认知，以强势出击，堪称大手笔。音乐的速度仿佛电影里的慢镜头，充满张力；温暖的音色可以将人包容；李寿全的
<br>声音与文字的混和间有一种惊人的孤独悄悄蔓延。他在歌声里挣扎，在旁观者的冷静侧视与当事人的痛苦孤独间挣扎。这首歌连同这张唱片，都象一部经典的电影。
<br>
<br>    8.向前走/ 林强　词曲/ 林强闽南语歌曲一向给人以苦情、江湖味的印象，林强的《向前走》把摇滚乐元素引入闽南语歌曲，世界由此摇身一变。陈升、李宗盛、周世晖和友善的狗联手打造了这张唱片，《向前走》是其中最出色的作品。这首歌在台湾经济低迷的1990年推出，引起轰动，成为89年《梦醒时分》之后的又一话题，也成为流行歌曲引领社会心态的代表作。
<br>
<br>    9.铁血丹心/ 甄妮、罗文　词/ 黄　　曲/ 顾嘉辉作为影响国人最深远的武侠题材歌曲之一，《铁血丹心》的悲壮、侠烈之气已经深深留在我们的记忆里――对每一个看过《射雕英雄传》的人来说，十余年后的今天，靖哥哥在那一轮血红落日中弯弓射雕的身影依然英姿勃发。
<br>
<br>    歌曲充分利用男女声对唱的优势，把“逐草四方、沙漠苍茫，哪惧雪霜扑面”的豪迈侠义同“应知爱意似流水，斩不断理还乱”的儿女柔情结合得相当完美。也许，每个男人都追求这样的生命：豪情万丈地行走天涯，身边有伶俐娇俏的如花美眷相伴.也许，每个女子都曾梦想这样的情缘：抛开世事断仇怨，相伴到天边。
<br>
<br>    10. 梦回唐朝/ 唐朝　词曲/ 唐朝他们歌曲中传递出的古意、杀气和豪迈，正如“菊花、古剑和酒”。他们的传奇尽人皆知，他们的热血烧起多少灵魂的摇滚梦想。纯粹以作品而论，很少有后来者能超越他们的深沉、大气、坚忍和自信。
<br>在他们身上，我们可以清晰地看到中国第一代摇滚人不自觉的使命感和大时代意识，和后来者相较，唐朝的作品形式不算新鲜，文字之间缺少更细致的个性感受，但他们酣畅淋漓的气度和锐利杀伐的性格迎合上摇滚乐的脉博，因而谱出最撼人心的音乐。此外，飘扬悠然的东方气质也是唐朝乐队得以傲视群雄的资本所在。
<br>
<br>    11. 明天会更好/ 合唱　词/ 罗大佑、张大春、许乃胜、李寿全、邱复生、张艾嘉、詹宏志　曲/ 罗大佑　制作/ 李寿全1986年是世界和平年，以群星合唱的方式祈愿世界和平，援助贫困人群成为流行一时的娱乐界盛事，此过程中诞生了不少出色的中文歌，《明天会更好》堪称其中的扛鼎之作。王芷蕾、王梦麟、文章、百合二重唱、李碧华、李宗盛、李建复、巫启贤、吴大卫、林淑蓉、林慧萍、洪荣宏、施孝荣、唐晓诗、徐乃麟、娃娃、陈淑桦、黄莺莺、张清芳、童安格、费玉清、杨林、甄妮、齐秦、齐豫、潘越云、郑怡、蔡琴、蓝心湄、苏芮等打破签约公司的限制，共襄盛举。除群星合唱版外，还录制了松江儿童合唱团的童声版。这首歌热情、执着又充满期待，至今未有超越者。这也是流行歌曲直接介入社会生活的一个象征。
<br>
<br>  12. 没有烟抽的日子/ 张雨生　曲/ 张雨生大多数人听熟这首歌曲，想必是在张惠妹那些销量动辄以十万计的正版或盗版唱片里。它的旋律多少有点晦涩，歌词也不知所云，如果不是张雨生的死形成的新闻热点和张惠妹的人气，根本没有资格做一首街知巷闻的流行歌曲。
<br>    只是在没有烟抽的日子里写出来的歌，生来不是为了被传唱的。
<br>
<br>    13. 姐姐/ 张楚　词曲/ 张楚　制作/ 贾敏恕张楚是现代社会的游吟诗人。他的音乐创作出自本能，文字的部份则具有惊人的质感和强烈的歌唱性。他的忧伤是冬日里的轻烟，真实而且温暖。他不会太过愤怒，因为他始终不太相信自己的力量。《姐姐》是一首叙事性的歌曲，尽管它并没有复杂的故事情节。青春期的少年对“温柔”但“爱流泪”的姐姐的依恋和弱势群体“我要回家”的呼唤各自独立又互相映合，成为90年代中期令人深思的文化意像。
<br>
<br>    14. 马不停蹄的忧伤/ 黄舒骏　词曲/ 黄舒骏黄舒骏是罗大佑之后台湾最好的诗人歌手，清新明快的文字和以民谣为基础的音乐架构起一个才子充满张力的音乐世界。和流水线上生产出来的陈词滥调不同的是，黄舒骏的歌声中总有一种学子情怀，诗意而纯真。“马不停蹄的忧伤”是个绝妙的意象，一览无遗又意味深长。这个青春期的爱情故事里没有矫情和虚妄，一派自然、淳朴天真。
<br>
<br>    15. 大地/Beyond 　词/ 刘卓辉　曲/BEYOND Beyond在香港有着仅次于达明一派的声望。他们的歌里，少年人飞扬的青春和起起落落的爱情都占很大的份量，这首《大地》是其中的异类。中规中矩的编配录音和流畅犀利的旋律是Beyond的特点，他们或许没有达明一派的冷静与智识，但黄家驹的摇滚偶像气质和他们的
<br>的热情执着影响了许多人走上摇滚之路。
<br>
<br>    16. 恰似你的温柔/ 蔡琴　词曲/ 梁弘志蔡琴一出道就是成熟的，所以她少了很多磨难，也使制作人如获至宝。相对于校园歌曲时代旺盛的创作量，歌手的表现形式一直受到相当的限制。蔡琴的声音中自有一种低回婉转的诱惑，有别于他人的清亮高亢。梁弘志是一位谨慎的创作人，数量不多，曲曲精致，常在平易近人的旋律之后浮出水一样的温情。梁弘志不是上天入地的大手笔，却是精巧小品的掌门人。《恰似你的温柔》气质清妙，意味悠远，几经翻唱，仍不觉老旧，功力在此。
<br>
<br>    17. 鹿港小镇/ 罗大佑　词曲/ 罗大佑都市生活与乡土情怀的纠缠，现代意识与传统观念的碰撞，一直都是罗大佑笔下的重点。这首歌是七十年代台湾经济起飞后，知识分子对于工业社会迅速到来作出的本能的反应。“台北不是我的家”成为撼动人心的疑问，其后多被引述。
<br>
<br>    18. 如果还有明天/ 薛岳　词曲/ 刘伟仁薛岳的英年早逝常会令听者扼腕痛惜，这首歌正是他对生命的总结。中文歌曲对生命的表述大多太过轻飘，这一次，有薛岳的亲身经历，份量自然重了许多。薛岳和幻眼合唱团的合作是他勇气的体现，一派摇滚曲风，团队形象，在消费方向狭窄的台湾歌坛绝对是个异类，只有以十二分的努力和坚持才能做到。明知自己去日无多，还要强忍病痛，做演唱会，出唱片，其间的执着，已非名、利二字可以解释。这样的勇气和诚意，都在这首绝唱之作里。
<br>
<br>    19. 大约在冬季/ 齐秦　词曲/ 齐秦齐秦是对中国流行音乐发展影响最大的歌手之一，其他的名字还有邓丽君、刘文正、叶佳修、侯德健、苏芮、罗大佑、崔健、童安格和Beyond等。民谣时期的齐秦出奇地拥有音乐和个人气质的完全统一，在那样一个唱片工业还不完全成熟的体制下，他自由地成长起来，这是幸运，也是奇迹。他的天才在各个方面一直地被确认，并且在民谣摇滚的方向上证实了流行音乐的进步与成熟。《大约在冬季》是一首中庸的离别情歌，因其中庸，才得以广为传唱。它有淡淡的不绝望的愁绪。承诺着一个没有未来的未来，一切都完全合乎风华少年的经验与想象。它可以作为范本，写进教科书。
<br>
<br>    20. 开场白/ 李宗盛　词曲/ 李宗盛这样短小的一首歌，透着无比的认真和诚实，不是乖巧的呻呤，也不是故作姿态的呼天抢地，就像朋友之间的对白，简单直接，又意味深长。李宗盛并未把自己当作旁观者，在这首歌里，他很清楚地点明了自己和听者的关系，这样的交流，才更具有可信度。
<br>
<br>    21. 酒干淌卖无/ 苏芮　词曲/ 侯德健侯德健、李寿全、梁弘志、罗大佑、吴念真等人在创作这张唱片的时候，就决心要让歌声里有故事有画面有生活，还要让苏芮成为活在歌里的声音。如果说《一样的月光》是整部电影想要表达的内核，《酒干淌卖无》就是电影情节在歌声中的浓缩。前者是冷静的思索之后热切的追问，后者是深切的感情在瞬间的迸发。由此，电影在理性与感性两个方向上的表达都得到了完整而精准的音乐表达。苏芮的人生经历成为她表达这首歌最重愤怒的天穹之间，那些射日的勇士该去向何处？他们的抗争是为什么？《天问》不仅是问“天”，更是在问芸芸众生。
<br>
<br>    26. 相思三态/ 张洪量　词曲/ 张洪量《有种》是张洪量最好的唱片，惊险奇绝，充满想像力和实验精神。和张洪量其他的一些作品相比，虽然流传度不广，音乐的份量却要重许多。《相思三态》写人爱到极处，为痴情所苦，几欲疯狂的神态和心情，着墨不多，却维妙维肖。
<br>
<br>    27. 我终于失去了你/ 赵传　词/ 沈光远　曲/ 赵传《我终于失去了你》超越了情歌的套路，以赵传的真实经历作为虚拟背景，人物和歌手水乳交融，其间的情感又可作多种解释，比之《我很丑，可是我很温柔》的征婚口号更真诚平实，比《我是一只小小鸟》中的悲伤失落又多了些许力量和勇气，堪称赵传最好的声音。
<br>
<br>    28. 似是故人来/ 梅艳芳　词/ 林夕　曲/ 罗大佑聪明的歌手总是会聪明地表达自己，梅艳芳的聪明就在于她除了“百变”的神话之外，依然留给我们这样隽永的声音。饱经人生历练，阅透世情冷暖之后，依然真情不变，那样的从容不悔，只有这样聪敏通达的歌手才能承担。
<br>
<br>    29. 信天游/ 程琳　词/ 刘志文、侯德健　曲/ 解承强在程琳之前还有一位知名的歌手王斯唱过这首歌，但反响不大。程琳在1987年春节联欢晚会上唱了这首歌，引起轰动，这也是“西北风”的开始。令人惊奇的是，它竟出自岭南音乐人解承强之手。联系当时《红高梁》、《老井》等电影的流行，我们可以想到，它的走红绝不是侥幸和偶然。
<br>
<br>    30. 其实你不懂我的心/ 童安格　词/ 陈桂珠　曲/ 童安格好的流行歌曲除了听觉上的美感之外，更会具有文字上的象征意义。流行歌曲中对爱情的描写常被淡化，变成对社会心态的概括。《其实你不懂我的心》以一个简单的爱情故事，象征了现代人心灵的疏离。童安格的唯美音乐也得以彻底展现。
<br>
<br>    31. 外婆的澎湖湾/ 潘安邦　词曲/ 叶佳修潘安邦的故事，叶佳修的代表作，校园民歌运动的纪念碑，乡土情怀的音乐写真，童年生活的纯真回忆，影响一代人的不朽之作――《外婆的澎湖湾》。
<br>
<br>    32. 葬心/ 黄莺莺　词曲/ 小虫黄莺莺的好歌很多，但大多都显得职业化有余。她的表现太过完美，毫无瑕疵的声音中缺少了凡人心中的激动和热情。这也许和她的声音特质有关。而《葬心》是一首能很好地表现她的冷艳的作品，文字凄楚，音乐缠绵。写成熟女性心灵的苦楚与寂寞，小虫可算是个中高手。
<br>
<br>    33. 光阴的故事/ 张艾嘉　词曲/ 罗大佑青春的风花雪月总是再也难觅，只剩这歌声似乎还在耳边回响，那单纯的日子里轻轻飘来的旋律。些许忧伤，些许怅惘，闪亮的日子，总有这样的故事和那样的希望。写青春的美丽，无出其右。  
