更为纤丽的是，在咖啡馆门口的楼梯上，刚要离去的卓梓竟然遇上了那阵地中海的风。

这一整个早晨都充满着奇迹。

（三）薰衣草茶*Lavender*

第二天，卓梓照常去上班，令她大感意外的是，上司竟然没有为她昨天的请假大发雷霆，一切都很平静，仿佛什么事都没发生过似的。尽管前天走的时候如此的英雄气概，但是她今天还是有些气短的，现在心中的不安总算都放下了。

心情大好。

晚饭后，她又去了田园。

她挑了院子里的位子，因为那刚好可以看到坐在阳台栏杆旁的他，看样子他也是田园的常客。

晚上田园的客人很多，檬来不及招呼她，只好略带歉意地对着卓梓笑笑。卓梓向她点点头，拿起留言簿翻看着。她翻到昨天写字的那一页，发现多了一行蓝色墨水的字迹――约翰·丹佛，署名是老欧。卓梓知道老欧，檬有提起过，他是田园的主人。她很惊讶，他怎么知道自己是在说约翰·丹佛唱的这首歌，而不是其他的什么，奇怪的感觉。

檬送了杯柠檬水过来，卓梓随口问她，檬，你们这院子里为什么圈着篱笆种一堆野草啊，种些花不是很好吗，又养眼。檬大笑起来，卓梓，你那什么眼神啊你，那些都是老欧辛苦找来的香草啊，你没有闻到清香吗？卓梓有些不好意思，脸红了起来，她看到原本低头看书的他看向了这边，脸上似乎还带着些玩味的笑。

夜风送来一阵馥郁的香味，好几簇紫蓝色的小花在风中舞蹈。檬，那是什么香草？卓梓指着那些花儿问。哦，薰衣草。功效是镇静促眠。它们是“宁静的香水植物”。原产自地中海，一般是每年六月开花。可能是因为气候吧，所以到了这八月才开花。檬解说得很专业。

地中海。原来这种精灵一般的香草和他一样都是属于地中海的。

檬，请给我一壶薰衣草。

有股药草的味儿，散发着属于哲思后的浓郁气息。像他。

两个小时后，他还是坐在那，没有一点要走的意思。卓梓只好打消一起走的念头，起身离开。

结帐时，檬递给她一盆薰衣草，笑着说，老欧送的，他说一盆杂草，不成敬意。

卓梓笑了，道了声谢。临走前，她又望了他一眼，本想说声再见，却又说不出口，只好作罢。

回到家，卓梓拿出薰衣草，从塑料袋里掉出一张小纸片。蓝色墨水的字迹，详细地写着薰衣草的种植方法。纸张的背面还有一段没头没尾的话――法国南部的普罗旺斯到处可见大片的薰衣草田，空气里漂浮着神秘的香气。

真是个奇怪有趣的家伙。

当天晚上，不知道是不是薰衣草的神奇功效，卓梓睡得安稳极了，她还梦到自己穿着橘色的衣裙穿梭在紫色的薰衣草田里。

（四）迷迭香茶*Rosemary*

卓梓经常去田园。一般都能遇到他，偶尔他没来，她的心里总会飘过一丝失落。

老欧很神秘，卓梓从没看过他。不过她知道他的魅力和才气。她猜想，老欧应该是一个稳重又不乏风趣的成熟男子，像克林特·伊斯特伍德。卓梓和老欧的交流总是通过文字，有时是在留言簿上，有时是杯子里的小纸片，有时是结帐时檬微笑地递过来的一张精致的卡片。甚至有一次在咖啡馆门口的小黑板上，没有署名，但是卓梓知道那是写给她的。

已是夏末了，微微有些寒意，又是一个雨天，就像卓梓第一次来田园那天的天气。

那个男孩果然在，坐在楼梯旁的位置，他对面的椅子空着，仿佛正热情地邀请着卓梓。好半天她才控制住自己不由自主的脚步，挑了书架旁的位子，他坐过的地方。

门口响起清脆的铜铃声，有人走进来。卓梓下意识地抬眼望去，一个很洋气的女孩，灿烂。她走到那个男孩面前，竟然停下来，并且轻轻地吻了他的脸颊。男孩见到她，一脸的欣喜，接着两个人热烈地交谈起来。男孩的神情是卓梓从来没见过的，那么，那么愉悦。她觉得有酸酸的液体在自己肚子里冒泡，不是胃液，而是其他的东西，譬如吃醋，感觉很难受。

檬送了一壶花茶过来，她说是老欧请的，叫迷迭香。杯里有一张纸条，还是那熟悉的蓝色墨水――

迷迭香，它还有一个美丽的名字叫Dew Of The Sea，“海中之露”。在夏日花草园中绽放清香，代表着爱人之间的忠诚，一如我对你的忠诚，我们能一起吃顿饭吗？

纸片的最下端是莎翁在《哈姆雷特》中撰写的一段话――

迷迭香，是为了帮助回想，亲爱的，请牢记在心。

老欧喜欢她？这是卓梓没有想到的。他们是那么投缘，如果没有那个男孩，卓梓想，自己是会爱上老欧的。可是刚才的反应已经告诉她，自己是喜欢那阵风的，而且比想象中的还要深。如果老欧没有表明心意，卓梓是很高兴能和他见面的，可是现在她绝对不会去吃这顿饭。为什么总是有那么多的如果可是呢？

卓梓娓婉地回绝了老欧的邀约，礼貌，客套，又有些冷淡。

传说中，耶稣赐予迷迭香似晨间森林般清新的味道，它具有神的力量。可是现在，黑暗与安静之中，空气中漂浮的迷迭香却是忧郁的。卓梓不知道，自己的选择是不是也是神的旨意。

（五）欧石楠茶*Erica*

之后有一段时间卓梓都没有去田园，一方面是因为工作比较忙，另一方面是因为老欧，她觉得有些歉意。

可是卓梓想他，非常非常想他，也不知道他还是不是常去田园。

秋分，湛蓝的天空零星地飘着几朵白云，卓梓突然很想去田园走走，她有预感会遇上他。

她失望了，咖啡馆里空荡荡的，除了檬，她是唯一的客人。

檬热情地迎上来，卓梓，你都好久没来了。想喝点什么？

是啊，最近工作太忙了，实在抽不开身。恩，随便吧，你拿主意。

那就喝欧石楠吧，美白效果很好哦。老欧新进的。而且是很浪漫的粉色。老欧说，它的花期很长，从初夏一直盛开到秋季。很美吧？

从夏到秋，刚好是卓梓认识田园的时间，原来自己已经默默的喜欢了他整整一季。为什么不跟他说呢？为什么总是埋在心里开不了口？为什么要顾及什么面子，矜持？为什么不能像老欧一样坦率？

卓梓决定，如果他今天出现在田园，她一定会跟他告白。

卓梓告诉自己，这是神的旨意。

他竟然真的出现了，就站在门口，灰色的V领毛衣，他总喜欢穿深深浅浅的灰。

她鼓足勇气走到他面前，做了她二十二年来最出格的事情。

我喜欢你。卓梓一脸的毅然决然，昂首等待最后的宣判。

他显然吓到了，脸上古怪的神情错综复杂，等他回过神来的时候，脸上多了一抹莫测高深神秘的微笑。

我想，我应该先做一下自我介绍，我是老欧。

这个最后宣判彻底击垮了卓梓。

后来，老欧告诉卓梓，其实欧石楠的花语是孤独，在E·勃朗特的小说《呼啸山庄》中，孤独的辛斯克力夫就葬身在开满欧石楠的荒野上。那天被拒绝后，他就进了这批花茶，原来是为了记录心情，没想到会有出人意料的结果。

老欧的脸上难掩喜悦。

（六）紫罗兰茶*Blue Mallow*

秋天的阳光总是那么的合宜，暖和地照射在老欧和卓梓的身上。紫罗兰在玻璃壶中绽放出神秘高雅的浅紫蓝。

药草学家约翰杰拉德说：“紫罗兰超越其他，拥有帝王般的力量。它，不但让你心中生出欢悦，它的芬郁与触感，更令人神清气爽。有紫罗兰伴随的事物，显得格外细致幽雅。”

紫罗兰茶的别称是Surprise Tea，就像卓梓和老欧的花草茶之恋，充满着惊喜，芬芳和美丽。

老欧把一片柠檬放入杯中，茶色渐渐从浅蓝变成粉红，老欧笑着对卓梓说，这是恋爱的颜色。

远远地飘来花草茶的清香，地中海的风里夹杂着香草的味道，悠扬的旋律响起，爱情开始了。

