“呵呵，你现在才反应过来吗？真的好迟钝哦。”他笑着说了这些话。他是上司，他怎么会对身为秘书的我说了这样的话？“那我刚刚说的你都没注意听咯？”

我有听，可，只是我不记得除了工作还有什么了。我也不想多知道了，转身上楼。   “再见。”

“一个人晚上回家太危险了，还是我送送你吧，我的车在那里。”他突然没厘头的冒出了这么一句话。

“啊。”我想起来了，不会吧那么巧，居然是那个要送我回家的讨厌陌生人？陌生人=老板？我还没办法接受。不过，是谁说过，世上的事情是什么都难以预料的？呵呵。“谢谢，我会考虑的。”现在心情很好。

接下来的日子里，该发生的都发生了，不该发生的什么都没发生。该发生的：工作、吃饭、嫉妒；不该发生的：恋爱――和老板恋爱。在外人看来，确切的说是在公司的女性看来，用她们的话应该是：我和老板打得很是火热，可是知情人都知道我们什么都没有，最多就只是在一起吃吃饭什么的。知情人指的是老总（他老爸），还有翰的妻子。呵呵，是的他有妻子，一个他很爱的女人，他爱她胜过生命的女人，这就是我们什么都没发生的原因，只是，他的妻子一年前为了为他生下一个属于他们的孩子难产而死，而，那个孩子也没有能够活下来，这使他伤心了很长的一段时间，他连死的念头都有了，要不是妻子曾经说要他好好的照顾好他们的双亲，他不会自己一个人苟且偷活到现在的。

我遇到了一个痴情的英俊男人，他说遇到我的那一天，刚好是太太和孩子的忌日，他的心情糟透了，就开着车在这个城市转了一圈又一圈没有停下过，只是到了那个时间，那个地点，他看到了我――一个抬着头看着天，看着那什么都没有的天空的人，而却说是看星星、月亮的人。他感觉到我和他一样的心情，孤单、寂寞、还有伤心……。所以他才将车子停下来，才有了我们两个前面的对话。他说我是一个不想将自己心门打开的人，自己的痛不想让别人知道，苦了自己、痛了自己、害了自己，可是却还是总是笑着面对。他说我活得比他还累，也因为我，他看到了活着其实不一定都是难过的，就算再伤心只要笑着去面对，什么都会好起来的。

三个月后他说他要去找他的第二爱，所以他将手上的工作放下去了摩洛哥，他说那是他和妻子相识的城市，那里应该会找到他想要找的人。也许他不会真的那么快去爱一个人，但是他说他想去试看看，他说那样他的妻子在天堂也会为他高兴的，帮助他的，因为他们是那么的相爱。

一年后我辞职了，因为我等不到我要的爱，所以我走了，我一直没告诉他的是，其实我就是他要找的那个人，他与妻子的爱情，其实在他妻子那里我都听说过了，我因为知道他所以来找他，可是他的选择是去寻找……

而我还是傻傻的等了一年。

手里握着那张一年前的报纸。

