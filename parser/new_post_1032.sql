我们像真正的情侣一样尽情的玩。伍的吉他弹得很棒，海岛的星空很美，他就在沙滩上唱歌给我听。伍忽然指着天空惊呼，纷纷，快看，流星！我急忙抬头，双手合十，在哪呢，我怎么没看见。我后脑勺上结实的挨了一下，哈哈，骗你的啦，傻瓜。我脸一沉，张牙舞爪地就扑上去了，对着伍又抓又打。伍一边躲一边抓住我乱舞的双手，一个翻身，脸慢慢地低下来。我闭上眼睛，感觉到伍温柔的有些潮湿的吻，像海浪轻轻地漫过沙滩。许久，我睁开眼睛，伍微笑的望着我，纷纷，流星都落在你的眼睛里了。

已经是第三天了，我们快乐的相守了三天，什么都不去想，我们的世界里只有彼此。只可惜，我们的世界只有三天。

伍，我真的喜欢你。可是，我该回家了，柳安该着急了。

你真的不和我走吗，纷纷。我想和你在一起。

我把玩着伍的头发，伍，你头发该剪了，总是在不同的城市漂泊，也该抽点时间打理一下自己，别跟个流浪汉似的，恩？

伍修长但却粗糙的手指仔细的划过我的五官。我明白，我的生活不适合你。你应该有个温暖安定的家，然后有个男人好好的宠着你。

纷纷，如果下辈子我不想再漂了，我一定在你出生的时候就去找你，然后守着你一辈子。

（七）夏天结束了

柳安，我回来了。

我在公用电话亭里给他打电话。

那个夏天的廊桥遗梦很美丽，我光着脚在这个梦里漫步。还好，我的甜汤只熬一个夏天。我从梦中醒来，夏天结束了，我的红红黄黄绿绿也结束了。

只不过，这个城市的夏天真的有点长，我的心疼了好久。

