乌龟：“现在后悔太晚了。”

兔子：“打点兴奋剂行吗？”

乌龟：“不行，要打就打你的屁股！”

兔子：“那好，请起腿……”

赛跑似乎过得特别长，因为我好象同时在跟恩只乌龟赛跑，到最后我再也分不清楚乌龟是我的对手还是我自己是我的对手。花什么时候开是有季节的，但是我什么时候醒的却没有动物知道。　　每只动物都想知道，兔子输了一场比赛后，失败的后面是什么，我很想告诉他们，失败之后也没有什么特别，不过是因为喝了半坛老白干。但我知道他们不会听。以他们的性格，不自己喝半坛又怎么会甘心？

＃冯大刚同学交的作文《龟方兔方》

“猫腻！这里面铁定有猫腻！”猪乐呵呵的跟鸡说着。“我就不信了，象兔子这么精神一小动物还给乌龟抢先了！这叫奶牛后面放电扇，吹牛Ｂ！”

鸡皱了下眉：“呀你这人怎么说话忒脏呢！兴许人家乌龟是报名儿参加了兔子他们公司办的好梦一日游吧？”

“说谁呢说谁呢？”乌龟冷不丁冒了出来，肩上还搭一汗巾，他摸摸光头，跟鸡说：“你以为赛跑光不要命就行了？那还得会劈叉！身体要柔软！就你这样的连蜗牛都跑不过！”

鸡说：“切，那跟劈叉有什么关系啊？”

乌龟：“我要不劈叉兔子能拌我脚上摔晕过去吗？”

猪和鸡不禁乐了，抬起前爪前翅：“高！实在是高！”

这时候兔子也闷声不响的进来了。乌龟一看：“哟！您可回来了我们可都惦记着您哪！”

兔子：“有啥好惦记的，不就输一顿晚饭嘛！走，跟我到王府井吃去，带着你！我也不嫌你难看。”

