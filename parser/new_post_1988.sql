<br>（一） 记录将从桑德露亚开始 
<br>
<br>桑德露亚诞生的时候还是和平的年代。 
<br>
<br>她在太平洋上的某个小岛出生并被命名，而后离开，母亲是当地部落的公主。 
<br>
<br>然而整个部落仅仅剩下二十位族人，所以桑德诞生后，小岛没能从千里迢迢远道而来的集历史学家，探险家，同时也是强奸犯――三位一体的桑德的父亲手中保留住珍贵的血脉。 
<br>桑德的母亲没来得及看她一眼就咽了气；桑德的父亲则放弃了为孩子取名的权利，为此，族里的长老为难了一阵。 
<br>最后，桑德是神圣的意思，露亚则代表邪恶。 
<br>她是这个部落五年来的第一个新生儿，同时又给她的母亲带来了死亡，整个部落也因此失去了尊贵的公主。 
<br>并且，她将再不属于她的族人。 
<br>她的父亲举着合金的武器，和他的同伴一起，带她去了文明的世界。 
<br>然后将桑德往全能寄宿学校一扔了事。 
<br>
<br>一年以后，小岛上火山爆发，部落中没有一个人逃出生天，于是桑德的出生地与根源（其中一部分）就此从地球上消失得一干二净。 
<br>
<br>桑德的父亲是狂热的冒险分子，最后一只孟加拉虎折断两颗牙齿，在和她父亲的肉搏战中死于非命。为此，这个男人被通缉了一阵。直到大战开始，欧亚大陆与北美洲不惜跨越整个太平洋互掷各种导弹，连人类都有了灭绝的势头，她父亲换了ID，从寄宿学校接出十四年不见的女儿桑德露亚（难为这位父亲仍然记得）一起移民到了中立国。 
<br>住下的一个月后，中立国封锁国境，加强了治安法。百无聊赖之中，桑德的父亲再婚了。对方是个已经有数次婚史并且带着两个女儿的寡妇。两个女儿一个与桑德同龄，叫都兰，一个只有八岁，叫邦妮。那个时候桑德的父亲叫凯宾.柯本。于是她们变成了都兰.柯本，桑德.柯本（大多数时候桑德都回避露亚这个不吉利的字眼）和邦妮.柯本，都兰是大姐。在他们父亲的有生之年，一家人的姓氏出于各种目的几乎是一年一换。 
<br>
<br>许多年之后，昔日的中立国变成了骗术师的栖身之处，亚瑟城。 
<br>没有了所谓的治安法，但是却平静了许多，骗术师们都各自躲藏在安逸的地方休养生息。 
<br>就仿佛是，蝙蝠集聚的巢穴。 
<br>只是，阳光普照，交通和公共设施一应俱全且有条不紊。 
<br>
<br>桑德十四岁的时候，从她的经历看来，也能明白这样的小孩子将很难天真烂漫。 
<br>十四年过去，并没有什么人被桑德记在心里。 
<br>寄宿学校的生活记忆很少，原因之一恐怕是根本没有被记忆的价值。 
<br>于是，对于桑德来说，与父亲生活在一起，紧随而至的是陌生的妇人，得叫母亲；陌生的孩子，得叫姐姐与妹妹，根本是种不痛不痒但却摆脱不掉的折磨。 
<br>那妇人有善良的心地，只是一生运气太差，遇见了太多差劲的人，以至于不太能与人好好交流，对于桑德，在这种障碍下也就亲切不起来。都兰则是个早熟的女孩子，在她母亲改姓柯本前就已不是处女，烫着卷发，习惯化妆，对新的妹妹远不如新的男朋友来的热心。 
<br>
<br>重点是邦妮。在遇见邦妮以前，桑德没有见过如此自闭的孩子。这个小孩剪着男孩一样的短发，手臂与手腕几乎一般粗，眼睛很大，常常不安的向上看。她从来不说自己之前的事，嘴唇闭起来向内抿。 
<br>
<br>在他们成为一家人之后的一年，一个下大雨的晚上，有巨大的闪电；桑德口渴想去厨房喝水，路过邦妮的房间时发现房门打开着，这时刚好闪电划下，在那短短的几分之几秒里，桑德没看清床上是否有人，只是看见了墙角里有一双惊恐的大眼睛，在一团黑暗之中，空洞的张开着。 
<br>桑德走过去一下子抱住了邦妮。 
<br>
<br>在桑德一生诸多种感情之中，第一个被唤醒的，是母爱。 
<br>这一点，她自己也无能为力。 
<br>
<br>她开始喜欢这个妹妹，喜欢陪她聊天，一起吃一起睡。 
<br>她的初吻是献给邦妮的，因为邦妮说：“桑德姐姐，我喜欢你。我想吻吻你。” 
<br>甚至桑德站在屋子中央，让邦妮脱光她的衣服，连内裤也不剩。 
<br>“桑德姐姐，”邦妮从她的乳沟一直吻到两腿之间，“你是说，我也会长成这个样子？” 
<br>“是的。”桑德突然觉得难以呼吸。之后，她才明白这一次从邦妮那里体验到的是性欲。 
<br>
<br>桑德十六岁的时候，她们开始在家以外幽会。 
<br>
<br>两人的“第一次”发生在中立国如暗涌一般到来的“革命”的前一夜，（关于这场“革命”，由海军高层一手策划，旨在推翻温良的政府，实际上只进行了短短五天便告结束）天气十分阴沉。桑德的学校（首都市立高中）北面与南面的门均被海军陆战队把持着，他们穿着墨蓝的外套，因为不可思议的降温。 
<br>舰队就泊在学校外的码头，暂时无声无息，背景是布满天空的大块黑色积雨云。远处有若隐若现的闪电。 
<br>
<br>那天有种末日的气氛。 
<br>
<br>下午三点，桑德在学校的告示牌上看见了邦妮的留言：入暮，Broadway , 就在床上，我等着你。 
<br>当时桑德的男朋友叫Zebra Jones，在乐队弹贝司，十八岁。一周前买了二手车，自己漆成了斑马色，叫Zebra。 
<br>桑德急忙找到Zebra Jones：“Zebra的钥匙呢？我得出去一趟。” 
<br>Jones惊讶道：“出去？你疯了？” 
<br>桑德没再说话，两人在Jones的教室外对峙着，Jones除下的耳机挂在脖子上，发出吵死人的音乐。 
<br>最后Jones摇摇头，把钥匙递给桑德：“记住，得毫发无伤地还给我。” 
<br>
<br>桑德摘掉车牌。半个小时之后从北面的门冲了出去，五个负责看守的陆战队员被她撞翻在地，不同程度目瞪口呆的目送Zebra扬长而去。 
<br>
<br>Broadway是城内知名的酒廊，尖顶上竖着一个巨大的黑色十字架。座位的价码按离吧台的远近而分出了好几档。创始人曾是百老汇的巨星。 
<br>在雨下来之前，桑德到了目的地，她身上的钱只够呆在离吧台最远的沙发。这是她第一次来到Broadway , 这时候的她自然不会知道她将成为这里的主人，不会知道这里将成为最杰出的骗术师的聚集地，这时候她甚至不知道骗术师。 
<br>
<br>她随便点了一杯饮料，四下打量。大约是革命前夕，人非常少，吧台附近有人吹奏harmonica,偶尔有人从昏暗的灯光中打量她。在黑暗的角落隐约露出楼梯的一角。 
<br>
<br>老板模样的人送来饮料给桑德：“第一次来这里？” 
<br>桑德接过：“嗯，是的。不过我只是来找人。一个叫邦妮的女孩。” 
<br>老板模样的人笑笑：“上二楼看看？” 
<br>桑德点点头起身。 
<br>
<br>二楼没有灯，伸手不见五指，桑德摸索到尽头，总算发现了一支把手。 
<br>她使劲拧开。 
<br>邦妮果然在，有月光照在她身上，她没有穿衣服，薄薄的床单盖在胸部以上，她抬起头来看着桑德。 
<br>桑德感觉自己的心跳已经不正常，她抱住邦妮，用力亲吻她的身体。 
<br>突然桑德感觉到了视线，她猛一回头，看见门外站着一个长发的女孩，脸色十分苍白，胸前挂着银色的十字架。 
<br>“我叫Nola Jones,”女孩说，“哥哥让我来的，他要他的车。他说你已经借走好久了。要看他的ID吗？” 
<br>桑德接过ID，的确是Zebra Jones。 
<br>桑德将钥匙递给她，笑笑：“替我谢谢Zebra。” 
<br>
<br>那其实是桑德遭遇的第一个骗术师。 
<br>他叫谢尔夫，英国人，实际上已经三十四岁了。那天晚上他从桑德将车停在Broadway外面就注意上了她。因为他正赶去四十公里外的一桩生意，革命让公共运输系统瘫痪，不得不想办法搞到一部车。他洞悉了桑德无力抗拒与拒绝的是未成年的女孩子，所以让她看见的是那样一个女孩。果然成功了。 
<br>
<br>第二天桑德回了学校。 
<br>她找到Zebra Jones ：“昨天谢谢你了。不过我怎么不知道你有那么一个可爱的妹妹？” 
<br>“妹妹？”Zebra狐疑道：“什么妹妹？” 
<br>听桑德说完整个经过，Zebra耸耸肩：“不管怎么说，轻易相信陌生人是你不对。你出一半的钱，我去物色辆新车吧？” 
<br>“God！”桑德叫了一声：“我怎可能有那么多钱？” 
<br>“唔……”Zebra想了想：“不如跟我去Broadway演出怎样？我知你能做个好主唱的。” 
<br>
<br>于是五天后革命停止，桑德开始流连于Broadway。 
<br>但新车并没有买，谢尔夫在一星期之后悄悄将它停在了桑德学校的停车场，为表示感谢还在驾驶席上放上了一张支票。 
<br>
<br>一年之后邦妮因为肺炎而死去。 
<br>
<br>又过了一年，Liar来到了中立国，在Broadway遇见了桑德。那时候他已经一百二十八岁了。 
