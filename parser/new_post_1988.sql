（五）　故事的最初

十四岁的boi站在没有尽头的公路中央，两旁是金灿灿的麦田，天空因为卷动着乌云而变得很低。强风一遍一遍的刮过废弃的没有尽头的公路。boi――－牛仔裤，破旧的罩衫和背包，嘴唇因为饥渴而开裂――他将齐肩的黑发别到耳后，望着让人沮丧的公路叹了一口气，蜷着身体晕倒在了公路上。

