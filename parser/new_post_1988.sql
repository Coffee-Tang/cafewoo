<br>（三） Boi为何得以出生呢 
<br>
<br>准确说来，是在桑德一岁零一个月时，她出生的小岛因为一场火山爆发而消失。 
<br>
<br>那个小岛在太平洋上的某个角落，用土语叫做“奇恰卡卡”，来源是某个意大利文单字（意义已不可考）。 
<br>这是因为最初发现这个小岛的，是一群意大利男人。其中一个叫亚度尼斯.杰佛理。 
<br>
<br>那是桑德出生三十年前的事了。在西西里岛出生并长大的亚度尼斯当时不过是组织中老大的七等跟班。发现小岛的时候，他和几个兄弟押着数量可观的海洛因，正乘着燃料将尽的快艇死命逃亡，身后数海里是国际警察的缉毒船“纯洁海豚”号。 
<br>
<br>奇恰卡卡上有亚热带常绿阔叶林，有火山，有至少二十种大陆上所没有的昆虫与哺乳类。还有十几个原住民部落。 
<br>所以，它竟然是被意大利黑手党的小跟班亚度尼斯――一个憎恨科学的男人――发现，无疑是个悲剧。 
<br>
<br>亚度尼斯一行人在岛上蛰伏了一个月，避开了“纯洁海豚”的搜捕。 
<br>心情畅快的时候，他给了岛上的长老一些海洛因，说了一句意义不明的“奇恰卡卡”。 
<br>单纯的长老享受之余，认为亚度尼斯无疑是天降神明，于是小岛也就有了它自己的名字…… 
<br>
<br>总之，“人类历史在不断的发展之中，往往有些乌龙是会超越人类想象能力的极限的。” 
<br>这是伊安（这是另一个骗术师）在他的诊室所说。 
<br>
<br>回到桑德一岁零一个月的时候，奇恰卡卡灭亡。（我们不得不遗憾的说，在它的有生之年，它遇见的不是黑手党就是疯子，从始至终也没能遇见一个能发掘其价值的人类。） 
<br>而同一时间，远在地中海的西西里岛，最大的组织“仙人掌”正腥风血雨的改朝换代。 
<br>亚度尼斯.杰佛理一颗子弹轰掉了老大的头，取代了他的位置。 
<br>
<br>三个月后，在中国两广及香港一带活跃，同时也是中国最大的贩毒集团“总裁”李承先带着自己的小女儿李远出现在了亚度尼斯的儿子小亚度尼斯.杰佛理二十岁寿宴上。 
<br>李承先正值壮年，急于拓展在欧洲的业务。 
<br>亚度尼斯（大）则对中国的市场热情升温中。 
<br>两人决定让孩子们结婚。 
<br>
<br>这纯粹是黑道联姻。 
<br>李远年方二八，当时不巧有个喜欢的人，是她远房的表哥，叫宋克炎。 
<br>所以她根本不想谈这门亲事。 
<br>
<br>小亚度尼斯和她在宴会后跳舞。 
<br>一开始两人就很不合拍。 
<br>两曲之后李远忍无可忍独自出去阳台喝香槟。 
<br>
<br>小亚度尼斯跟了出去，问她：“你怎么了？不喜欢这曲子？” 
<br>李远一米五八，小亚度尼斯一米八五。 
<br>中国女孩仰起娇小可人的脸，说：“嘿，少爷。你知道吗？我能看相。不论是面相，还是手相，都是个中高手。” 
<br>小亚度尼斯笑了：“哦?是吗？” 
<br>“但是，”李远又说：“你的面相就根本不值得一看。” 
<br>她说：“因为你天生就是个窝囊废，血统里的东西在出生前就已经死绝了。成天喜欢些没用的文学啊戏剧啊，黑道有你这样的人简直是羞耻。” 
<br>最后她说：“回家吃奶去吧，少爷。” 
<br>
<br>这些其实是李远为了博取小亚度尼斯的恶感而随便胡诌的一番话。 
<br>但奇迹的是都被她说中了。 
<br>眼前这个身材魁梧，带着墨镜，棕色头发向后梳，眉头紧锁，颧骨突出的青年，刚从英国某大学毕业，主修的是文学与戏剧，不会用枪，防身术半途而废，更不用说任何格斗技。 
<br>对生意也是一窍不通。 
<br>总之，是个对他父亲在黑白两道的生意都毫无助益的“笨蛋儿子”。 
<br>
<br>但是这位在温室里温和的成长的少爷从出生到现在还从未遇见过这么有个性的小姐，何况她还是个美人。 
<br>于是，不管李远如何挣扎也是没用了。 
<br>小亚度尼斯不可自拔的爱上了她，且如愿以偿的娶了她。 
<br>
<br>在桑德十八岁时，她遇见了一百二十八岁的Liar 。同时，Boi――中文名随母姓叫作李博仪――这位Liar最后的客户也出生了。 
