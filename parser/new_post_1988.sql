“我父亲的身体吞下了一把刀子，

他平静的看着天空死去。”

在妈妈取代爸爸的那天，我把我写的第一首诗拿给妈妈看，希望她至少能掉眼泪。

但她没有，她只是转过身去，说：“今天是你妈妈大喜的日子，为什么要说扫兴的事？”

之后的夜晚，我听见了妈妈和表舅宋克炎的声音。表舅低沉的喘息，妈妈高亢的，令人毛骨悚然的尖利的惨叫，仿佛在承受着比死还要严酷的痛苦，而那恰恰是快乐。

妈妈是为了宋克炎才把爸爸杀掉的。

而宋克炎是为了组织。

他得到自己想要的东西之后，就将妈妈狠狠丢弃了。

妈妈用手枪自我了断。

剩下我一个人。

为了完全拥有组织，宋克炎要杀我。

他杀掉了我身边所有发誓效忠我爸爸的人。

可他还是没能杀掉我。

我到处逃亡。

最后，遇到了liar。

我一见他便觉得他是个特别的人，我没办法对他撒谎，没办法拒绝他。

所以我跟他上了直升机。接着乘船，搭机。

他说：“我是个骗术师。我有很多奇怪的能力。比如......比如我现在已经一百四十二岁了。”

我说：“怎么会？你是怎么办到的？”

“骗术，”他说：“只是种超自然的能力......我不知道它从哪里来。等我发现的时候，我已经运用自如了。”

他说：“我带你去的地方，叫亚瑟城。是骗术师居住的地方。”

他说：“很好的地方。”他笑了。

