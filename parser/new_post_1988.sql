<br>（二） Liar有着什么过去 
<br>
<br>首先，他生来就是个王子。 
<br>十六岁的时候成为王储。 
<br>十八岁的时候已经出落得仪表堂堂，颇有少年帝王的风度气魄。 
<br>与他稍显颓废，步入暮年的父亲一块出席皇家赛马会时，后者不禁有些惨不忍睹。 
<br>
<br>但是两年之后整个不列颠都忘记了这位美好的，有着金色软发与灿烂笑容的他们的未来的国王，包括谱系庞大的温莎王室本身。 
<br>
<br>这是因为他停止了成长，并发现了自己奇妙的能力。 
<br>我们称之为骗术。 
<br>
<br>想象能力有限的人类将怎样理解这个玩意？ 
<br>我又该如何解释？ 
<br>
<br>总之，Liar修正了所有人的记忆。 
<br>
<br>首先，他选择了一段轰动的历史。他的姑姑，叛逆的凯瑟琳公主，在Liar十岁的时候曾星夜逃离白金汉宫，与一个印度籍的软体设计师私奔去了美国。当公主拔脚与心爱的人狂奔在伦敦的大道上时，还被好事者拍下了照片，照片上公主衣衫不整，还露出了底裤。 
<br>这几乎成了温莎王室有史以来最大的丑闻。 
<br>
<br>于是，在二十岁生日过后几天，他选择了月光很好的一个晚上。穿着和姑姑一样的衣服，也狂奔出了白金汉宫，身后绑着一个塑胶人偶。 
<br>
<br>所有看见的人被他重叠了记忆，这种记忆辐射了出去，他从人们的记忆中消失了。 
<br>新闻出来之后，变成了公主的叛逃。所有对王子的搜索霎时间停止。 
<br>他的父母经过他的房间，看见各种男孩的衣服，游戏机甚至还有他的照片，但没人想得起来他到底是谁。 
<br>
<br>于是，他怀揣着父亲为他开设的银行账户和自己伪造的ID，得以开心的环游世界。 
<br>
<br>如此过了八十年，他仍然没有丝毫衰老。可能也不会死亡。 
<br>
<br>他开始以骗术为职业。 
<br>他是所有骗术师的始祖。 
