<br/>——写在前面</p>

<p>刚来厦门的时候，陪伴我的只有夜晚窗外飘来的口琴声。不知道是谁吹的，只是觉得很好听很好听，我是一个爱音乐的人，爱到有些挑剔，但我喜欢夜晚的口琴，让我一个人不那么孤单。
<br/>后来爸爸妈妈来了，把家搬来了，带来了以前我所拥有的一切，我的书我的画，还有我的钢琴。我最喜欢的钢琴啊，不是我想象中的那种象牙白，黑色的，最普通的那种，有些旧了。到底陪伴了我十几年，我长大了，它也会老的。
<br/>这座楼里会弹琴的人不少，我只是其中的一个，8点多的时候，很多钢琴都会同时响起来，杂乱无章，大家各弹各的，我从来不在那个时候弹琴，因为我不喜欢，弹琴是需要心情的，在吵闹的环境下是弹不出什么好东西的。于是我把时间选在11点，我把作业做完的时候，也就是以前每晚口琴声响起来的时候，我不想让我的琴声盖过口琴，就轻轻地踩着弱音踏板，弹着一些很安静的曲子。
<br/>口琴还是很准时地在那个时候响起，合着我的琴声，很和谐，尽管我们弹和吹的是不同的曲子。
<br/>我从来不知道她是谁，住在一座楼里也许天天见面，但是，彼此都不知道弹琴的是我，吹口琴的是你。有一天很偶然地见到她的背影，那天11点多，我先盖上琴盖，走到阳台上，突然看见3楼的阳台亮着灯，一个长头发的女孩靠在墙上，后来，口琴声停了，灯也灭了。我直觉觉得是她，那个会吹《EYES ON ME》，《如果云知道》的女孩子。
<br/>我不知道三楼到底住着谁，初来咋到的不好去打听。
<br/>以后就常常看到她的背影了，我觉得她的头发很漂亮，肯定很黑很顺滑。我还喜欢她关灯的时候头发一甩的姿势。背影我是熟悉的，但是人却是很陌生的。
<br/>和朋友在KFC里吃饭，忽然看见一个同楼的女孩走来，相对笑笑，经常见面就当是认识吧，可是从没说过话。她点点头，转身走了，转身那一刹那，她甩头发的姿势让我好熟悉，我一惊之下，站起来很大声地问她：“你，你是那个吹口琴的人？”
<br/>后来她笑我问话怎么会问的那么直接，那天她也楞了一下，然后笑了，笑容很透明，她走过来坐在我旁边，你是弹钢琴的吧？那首《蝶恋》是你弹的？
<br/>那几天我一直在弹《蝶恋》，调子是我从游戏中听出来的，伴奏是我自己配的，没想到她会注意，我也笑了：“你也知道《蝶恋》吗？”然后我们靠在一起笑，有点放肆地笑。</p>

<p>相遇是缘，这话她说的，她来看过我的钢琴，她说我是一个两面人，单独和她在一起的时候会出奇的安静，她说我不应该把真正的自己藏起来，就象我藏钢琴一样。她很了解我，很少甚至没有人知道我会弹或者我有一架属于自己的钢琴，钢琴上盖着一层布，白色的，上面放满了我所有的娃娃，把钢琴严严实实地遮起来，每个人只会注意我那些很漂亮的娃娃而忘了娃娃下面的钢琴。
<br/>她问过我如果有一天我累了，累到没有力气去搬开娃娃，那么我还会弹琴吗？我回答说会，等到我没有力气的那一天，我会搬开钢琴上的一切东西，让它很舒畅地呼吸阳光和空气。
<br/>我弹我最喜欢的《蝶恋》给她听，她说我弹琴的时候一定有一个很安静的心，我点头，她又忽然问我有没有想过把这一刻的安静留下来，永远地留下来？
<br/>我不知道怎么回答。
<br/>后来她拉我去她家，来到她的房间，她的口琴摆在最显眼的位置，她象宝贝一样拿起来，银白色的，看样子是一架很好的口琴，接着她又拿出许多录音带，很神秘地说，还记得我问过你可不可以把一刻的心情留下来，看，这就是我所有的心情。她开了录音机，口琴声传出来，一首接着一首，有得很悠扬，有的很悲伤，就算是同一首曲子给人得却是不同的感觉。然后她告诉我关于一首首曲子的故事，关于她的快乐她的悲伤，还有那个深深伤害过她的男孩子。我静静地听，没有插话，看着她的笑脸和她的眼泪，最后她说，能不能让我听听你的心情呢？
<br/>我点头。
<br/>我花了整整3个月的时间录了很多首我自己弹的曲子，很细心地录，不容有一点瑕疵，还有那首她最喜欢的《蝶恋》，当我把带子交到她手里的时候，她俏皮地问我，这里有我的什么心情呢？我一字一顿地说了五个字“认识你真好。”她又笑了，紧紧拉着我的手。</p>

<p>现在，很少听见她的口琴声了，据她说是因为上了高三，很忙，或许高考完后她又有吹口琴录曲子的心情了。我笑着说不要，那时候我就高三了，那会很吵的。她总会作势要打我。
<br/>有时候在上学的路上碰到她，她总是一副急冲冲的模样，打个招呼就先跑了，看着她甩头发的样子，看着她的背影，我会想，如果没有我的钢琴没有她的口琴，或许，我一辈子都不会认识这个好朋友。缘分的确是种很奇妙又很玄的东西，紧紧牵住我和你。</p>

