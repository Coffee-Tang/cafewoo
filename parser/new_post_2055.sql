对于苏永康我想说的是：长的丑不是你的错，可随便接片糟蹋电影就是你的不对了。排除种种感情因素，有两点事实我们可以肯定：第一，他长的很丑；第二，他唱歌不错。根据这两个特点我们可以发现最适合他的发展路线，就是做一个实力派歌手。事实上他在这个位置上也确实做的不错，就算在酒吧夜总会里抽点大麻也是可以原谅的。不过歌而优则演似乎已成了定律，无论是谁都想过把演戏瘾，何况在永康哥哥看来，自己还是蛮酷的。于是，我们就不得不在一部部感人至深的爱情电影里一次次的忍受他鬼斧神工的长相和拙劣的演技。在《爱情白面包》里，他演了一个自以为风流的公子哥，表现让我们都替他汗颜；在《星愿》里，他又想在任贤齐和张柏芝之间插了一腿；而在唯美到极至的《心动》里，居然还有他那张老脸……我真￥%&#!~* Faint！真想由衷的说一句：小康哥哥，演电影真的不适合你，还是回去做你的歌手这份很有前途的职业去吧！

7 ．林心如

请原谅我又选了一个琼瑶姐姐的手下干将。要说琼瑶姐姐选择演员的眼光还真是独特，几乎每一个演员都是那样的……令人作呕。其实林心如的模样还是比较讨人喜欢的，大大的眼睛，两个可爱的酒窝……不过只要她一出现在琼瑶姐姐的电视剧里，马上就变的谈吐无味面目可憎，这真是种奇妙的魔力啊！最让人难忘的当然是她在《还猪格格》里与尔康的对白，那叫一个酸！我通常只在吃饺子的时候才看《还猪格格》，一边吃一边听他们两个肉麻无比的台词，连醋都省下了。建议琼瑶姐姐以后万一在电视界混不下去了，就去开个醋厂，产品保证畅销。

8 ．马景涛

本来不想写他的，因为印象里他似乎已偃旗息鼓多时了，可最近忽闻他又以某部电视剧强势复出了，而且片酬达到了每集数万元，真让人感叹这个世界的荒谬。想当年小马哥也是琼瑶姐姐麾下的爱将之一，他的特点是有种能让人在一瞬间对一部电视剧失去兴趣的能力--有很多次我都是因为看到他在喊叫而换台的。而说句实话，他那种通过大喊大叫来宣泄感情的表演方式，真的很烂。搞笑的是有人评价他的表演"很有激情"。原来这就是激情。后来马景涛果断的向青霞姐姐学习，跳出雪月风花，投身到轰轰烈烈的武侠世界中去。而他先后糟蹋了张无忌，周怀安等数个英雄形象，堪称李亚鹏的祖师爷。

