<p>在白玉兰花枯萎的季节，我结束了初恋。落花乱雨般的纷飞中，我明白了诺言的可笑。而谁也无法去辩证对错。
<br/>蓓说，这没什么大不了。
<br/>我说，是阿。我还是我。我笑得一脸的安静。</p>

<p>宁是我的名字。简单的笔划如同我明净的双眸。熟识我的人都说我有一张永远不变孩子的脸。于是，他们她们都容忍我一贯迷糊的脾性。我从未曾想过去改变。也未曾感觉到不妥。
<br/>然而，我知道这不是真正的我。
<br/>阳光下流汗的运动从来都拒绝。爬山、打球、游泳。我什么也不会。
<br/>可我喜欢跳迪士高。昏暗的D厅。变幻莫测的灯光。陌生拥挤的人流。我看不见其它，我只感觉到心在跳动。</p>

<p>萱是这个城市里我认识的第一个网友。她做电脑设计。擅长于FLASH。曾做了一张电脑画给我，是条跳跃的鱼儿。自由灵动在游玩的鱼。那鼓鼓而绝望的眼睛，在找寻着什么。萱说真象我。我于是大笑，在QQ上我的名字是小人鱼。
<br/>人鱼，唯美凄酸的童话。可，裂痛之后在阳光下拥有灵魂的重生是否值得？
<br/>当然，也是没办法的事了。</p>

<p>认识萱是在夏末，一个周六的夜晚，我挂在QQ上百无聊赖。萱发来信息说，我柔情似水，请不要拒绝。一瞧，顿时我乐翻天。即按下OK，回一句，渴盼佳期如梦，开聊。
<br/>谈到了最后，萱约我在KK迪厅的门口见面。那时已近零点。
<br/>一人坐在的士里，望着车窗外寂廖的街道，我在想，自己恐怕是出了问题。这三更半夜的，只是想见一个同样也睡不着的女孩。</p>

<p>喜欢跳迪士高是很久的事了。哪一年的平安夜，蓓带我去XO2。我的位置靠近舞池，我看见蓓绚目的身影在眼前扭动。第一次，我知道舞步是可以如此的简单，简单得让你眼花缭乱。当我的舞步日趋熟练，我却慢慢远离了这喧哗的地方。在震天音乐的心跳过后，额上流泻的汗水感觉则更加的虚空。 
<br/>而其实是，我迷上了网络。自言自语地在BBS上贴了些许文字。沉湎于其中难以挣扎。浪费了时间，还有，最初的情感。</p>

<p>萱有着细碎的中长发，染着淡黄的颜色。画着眼线，一点点的媚。那晚，就只我一人在跳舞。萱独自喝完一扎的啤酒。兴奋地想拉萱起来，萱却安静地放不开步子。萱说，宁你的舞步真好，长得却象个孩子。
<br/>突然间，我在想，外表的落差有多么的大。也许没有谁会去刻意的伪装，性格的复杂却非旁人一时能明白。那么，曾经最亲近的那人背叛了你，刹那陌生的感觉也就不足为奇。一段时间过后，那人的容颜其实早就模糊。总是要为自己而去认真生活。在不停的追逐中不断放弃。</p>

<p>和辉在一起是有多么的奇怪。维系在中间的那根线至今我弄不明白是什么。我们会在高高兴兴的时候突然间叽叽歪歪地吵个不停。言语是伤人的利器。我总是悲伤的想终有一天我们会轻轻松松地忘掉了彼此。然而我一天天地更加迷恋他手心里的温暖。在反反复复地折腾中我痛苦着自己。我总在争吵之后怒冲冲对辉说我的骄傲不容许你的存在，却时常在做出妥协时说，这只是不想要自己变得太任性。
<br/>辉总挑衅地望着我说，你愚钝而没有灵气。
<br/>我则选择沉默的离开。我不知道，心是否会因习惯而变得麻木。</p>

<p>童年长着翅膀的梦想。什么时候沉重得再也舞动不了。床头是一排排赶不及打勾的计划。满柜的中英文小说曾是我铺天盖地的激情在汹涌。在现实的琐碎中一点点被磨灭。我依旧认真地工作努力生活。眼睛却在茫然中不知所措。即使这个月我拿了跟工资一样高的奖金，我也不知道这跟希望会有怎样的关联。
<br/>咖啡里有谁拒绝过杜拉斯。我却空前地喜欢。绝望的灵魂在喃喃自语，没有过多情节的语言，如果你没有历经过痛苦漫长的黑夜，是无法体会的。卫慧的文字看了直叫我恶心，岂能拿来相提并论？
<br/>说到这里，不要以为我很偏激。其实我总习惯于乖巧地对着你笑。至今我仍喜好翻阅卡通漫画。很轻松的快乐。快乐得我一个人独自咯咯直笑。</p>

<p>婆婆说过，当世事随缘变迁。去看镜中你的眼睛。
<br/>习惯于在挫折中思考着自己，常常发现答案很可笑。鱼缸里的那条金鱼，从外面看，它的身影会折射幻变得不成形。于是我奇怪，鱼儿怎么转瞬间胖了许多。然而从上面往里瞧，它还是原来的模样。</p>

