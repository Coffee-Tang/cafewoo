最后一个场景放大了他不停往下淌血的手。

我一直深爱鲜红的颜色。

我很讶异自己还能记得这么多的事情，像是强制性地把记忆刻住似的。

我现在是这个城市的一个游民，游民就是跟一条鱼一样在城市这座寂静的海底有动，不理会海面的亮丽，做自己想做的事和漫无目的。

偶尔会写写文章，却总是写不出结局，如同自己的生活。

