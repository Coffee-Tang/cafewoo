<br>我无法专注于一件事情很久很久。
<br>我发现对一件事情投入是需要极大的勇气的，因为随时都得提防被抽离或被抛弃，我们会像失焦的动物一样没有方向。
<br>没有什么比活着但没有方向更难受的事情了吧。但我真的找不到我想找寻的终点，也许哪一天遇到伍，再遇到伍的时候，我会想起我残缺的那部分记忆。
