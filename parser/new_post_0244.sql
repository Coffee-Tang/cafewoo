<br>上课。 
<br>　　关于语文课是否改名的问题，我们已经讨论很久了。因为连上课时唯一认真的人――老师他自己都不相信，这样的课堂能够称之为语文课。按照常规，大家总会在语文课的时候，从书包里摸出面包蛋糕薯片馒头，空气中还弥散着豆浆和咖啡的味道。偶尔一些没有进餐的
<br>同学，也不会浪费这一节难得的“必休课”，他们或闭目养神，韶光养晦；或奋笔疾书，狂补作业。我猜想如果有一天语文老师下岗了，她也一定不会失业。因为她嗓子里发出的那种微妙的声音，不仅刺激着我们的食欲，让我们把馒头嚼得津津有味，而且还能大大提高我们
<br>写作业的效率，笔作行云飞快，几十本作业、弹指一挥间。 
