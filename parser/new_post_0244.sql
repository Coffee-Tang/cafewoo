<br>　　在高三，迟到实在是是在正常不过的了。每当清脆的电铃划破清晨慵懒的阳光的时候，头顶微秃的班主任总会满脸委屈地站在班级的前门，时而盯着教室内意图伺机拷贝作业的同鞋，时而上身前倾，把半个身子伸出门外，铮亮脑门下混浊的秋水，望眼欲穿。 
