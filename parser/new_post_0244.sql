跷课。

跷课和旷课不一样。旷课指的是小规模，短时间的缺课行为；跷课则是大规模，有组织有计划的，全民参与的长时间的缺课行为。跷课通常是以半天为最小计量单位。假如那一天语文老师发现班级只有三分之一的学生跷课的话，那么他脸上一定会重新挂起那颇有亲和力

的笑容的。

很多人的日子是这样度过的：早上第一节课到班级放一下书包、吃一下早点、补充一下能量。然后就迫不及待地赶往网吧报到。在接下来整整一天的时间里，你大可以不必浪费心思，企图在学校中找到他们。因为他们大多时间都不再学校，如果他们不小心进入了班级，

那么目的不外乎两个：睡觉，或者还是睡觉。所以，要找到一个清醒的跷课者，到网吧去或许会更现实一些。

而每当我们跷课在网吧杀的天昏地暗的时候，总会有人不知好歹地说出“其实我是很不想跷课，我是很认真很喜欢学习的”或者“跷课，其实我也是千百个不愿意啊”之类的话，紧接而来的自然是众人暴风雨般的拳点。拳头撞击皮肉的声音弥散在网吧满是烟味的空气

