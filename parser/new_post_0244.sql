烟帮

不可思议的是，我们这个组织竟然在这么一个重点中学的高三年段得到了这么多人的支持和欢迎。据不完全统计，参与烟帮的，应该有８０多人，几乎占了全年段人数的四分之一。规模堪称宏大。

