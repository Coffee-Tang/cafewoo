<br>烟帮 
<br>　　不可思议的是，我们这个组织竟然在这么一个重点中学的高三年段得到了这么多人的支持和欢迎。据不完全统计，参与烟帮的，应该有８０多人，几乎占了全年段人数的四分之一。规模堪称宏大。 
<br>　　食堂是烟帮成员们法定的聚集地。每次举行活动的时候，食堂前的围栏旁总会有二三十个人，他们嘴角叼着各种品牌的香烟，摆出各种或欠揍或陶醉或潇洒或白痴的姿势，坐在宜人的晚风里，继续着永不停止的谈天说地，一直到终了，才依依不舍地离去。 
<br>　　Ｊ哥吸烟的姿势是最特别的，他总是架着二郎腿，用一种迷离的眼神看着侧上方的那片树叶，大拇指和食指轻佻地捏住过滤嘴，放在嘴角处连续不断地练习着吐纳的功夫。他吸烟的速度也堪称一绝，每次当别人半根烟未燃尽的时候，他已经开始点第二根香烟了。或者，
<br>他那不叫吸烟，叫吹烟会更合适一点。 
<br>　　“其实香烟燃烧的是我们的生命。”Ｊ哥意味深长地说道。 
