·爱情麻辣烫

爱情这个词用在这里其实有些不大得体。高三的爱情，有很大一部分属于一种心理需要，而不是一般意义上的爱情。他们喜欢那种面红心跳的感觉，就像麻辣烫一样，速食，味道浓烈，解馋。

人到高三，各方面的压力总会让他们感到无助的的寂寞与空虚。男生从来都在扮演着采花蝴蝶的角色，为了寻求一个愿意跟自己一起去吃饭、中午能够根自己坐一张桌子、放学能够去球场为自己加油鼓劲、半夜能听见她的声音的知己，而不懈奋斗着。而女生的情况并不

