<br>　　“不想听课的都给我出去，我不拦你们！”老师忽然咆哮起来。无奈之余，她也是有一些豪爽的。 
<br>　　常言道，恭敬不如从命。对于这一句话，从来没有谁向我们了解得这么彻底，并且贯彻的这么坚决的。她话音未落，早有７、８个同学从后排拿起了篮球，拍着走出教室了。 
