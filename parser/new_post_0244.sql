<br>星际。 
<br>　　“星际争霸”的魅力恐怕是所有专家学者用再多的语言都无法阐释清楚的。高三年段对星际更是情有独衷。他们义无反顾地迷恋着星际，为星际付出，可谓无怨无悔。君不见多少同学沉浸网吧废寝忘食，君不见多少钞票如大江东去逝者如斯。年段星际界其实颇有一些高手，他们苦心修炼，终成一身盖世武艺，称霸百Ｘ村周边大小十多家网吧。 
