·高三的特权

高三年在学校有着非同一般的威仪和特权。或者可以这么说，高三年年其实就是学校的半个保卫科。他们拥有保卫科所拥有的大部分权力，大的如整顿校园纪律、维护周边环境治安，小的如管理运动场地，没收篮球足球等。

他们从来不穿整套校服，也从来不知道胸卡为何物，门口那群据称是铁面无私、奉公执法检查校服胸卡的小弟弟小妹妹们，竟然也对此视而不见。一般说来，他们是从不鸟那些带着纠察队牌子，煞有介事地检查校园风纪的小同学的。其实他们也是很敬重那些人秉公办事

的家伙的。这一点从我们关爱的眼神终究能看得出来。那是怎样一种慈爱而温暖的眼神啊，以至于让那些学弟们受宠若惊。因惊吓过度而变得惨白的脸色，搭配上血红的嘴唇，其实不失为一道亮丽的校园风景线。

高三年也享有学校默许的管理和使用运动场地的特权。数十万的草皮，不知让多少人垂涎三尺，心动不已。然而，大多数人却只有作观众的份。他们只能乖乖地站在栅栏后面，或者坐在场地边上，看着他们高三的学长飞奔于球场至上，在青草的芬芳中享受足球的乐趣。

当学校对非作歹的学生感到头疼而无力管教的时候，高三年就开始着手整顿校风了。学校并不反对使用暴力。我的意思是说，至少处理暴力事件的部门不反对。保卫科的老大曾私下里告诉他们，打人不要往脸上打，因为那太明显了。他还以亲身经历教会了他们，上乘的

打人方法应该是把人打成内伤而外表上却一点儿也看不出来。

种种的特权羡煞不少学弟学妹。然而我想告诉他们的是，羡慕是没有用的，罗马毕竟不是一天建成的。继续修炼吧。

