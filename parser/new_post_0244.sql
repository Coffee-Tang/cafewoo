<br>&#183;爱情麻辣烫 
<br>　　爱情这个词用在这里其实有些不大得体。高三的爱情，有很大一部分属于一种心理需要，而不是一般意义上的爱情。他们喜欢那种面红心跳的感觉，就像麻辣烫一样，速食，味道浓烈，解馋。 
<br>　　人到高三，各方面的压力总会让他们感到无助的的寂寞与空虚。男生从来都在扮演着采花蝴蝶的角色，为了寻求一个愿意跟自己一起去吃饭、中午能够根自己坐一张桌子、放学能够去球场为自己加油鼓劲、半夜能听见她的声音的知己，而不懈奋斗着。而女生的情况并不
<br>比男的好多少，他们同样寂寞，同样在梦想着罗曼蒂克的不期而至。所以，他们的心里防线空前脆弱。男生有时候仅仅需要一张皱皱的纸条，就能够将原本紧锁的心扉叩开。而电视剧中那么多缠绵悱恻凄婉动人的爱情故事，更是一贴强力的催化剂，催化着Ｘ年段浓烈而热切
<br>的爱情氛围。于是乎，１班的ＡＡ，２班的ＢＢ，３班的ＣＣ……７班的ＭＭ，成了年段上绝大多数人最难以割舍的话题。 
<br>　　在高三年段，出双入对，手拉手，肩并肩者大有人在，其实那真的不算什么。高三年有的是有个性的人，他们的爱是阳光底下最坦白的爱情，他们为爱而疯狂，以至于校长轻轻拍着他们的肩膀的时候，他们仍在楼梯口继续拥吻而浑然不觉。 
