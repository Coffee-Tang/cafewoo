东莫村的意思：人人都像孩子般无忧无虑地生活。然而就是在这个小院儿般大的村子， 5个外来人举着枪对峙了一夜。清晨下着雨，爱淋雨的雨怡脱下袜子认真地帮小战士泽基擦着脸，那一刻，她的笑容让他忘记了手里的手雷，忘记了还有战争，还有敌人。这是爱情吗？清淡如同她耳际的野菊花。雨怡趁机调皮的扯下了手雷上的环，坏笑着跑开，大喊着：“是个戒指！”惊慌中，手雷在粮仓中爆炸了。那些火光的和烟尘实在不该属于东莫村。但你知道吗？粮仓中的谷子炸上天，炸成了爆米花，在空中一朵朵盛开，成了一场不可思议的爆米花雨！爆米花落下来，打在老人的鼻子上，战士们的枪上，那一刻，什么枪啊，立场啊，它们都消失了。

那片草地。他们在那里打野猪，一场大战真正消除了“立场”，让他们成了朋友。他们和男女老少村民一块，玩用草扎成的“美式足球”，还有草扎的草垫，他们坐在上面，从高高的草坡上疯了一样滑下来。沉默寡言的表少尉开始不愿加入，后来也一个人疯玩了起来。似乎是从那片晶莹滴翠的草地开始，战士们的仇恨被融化了——其实，他们之间本就没有仇恨。

是夜，通向东莫村的竹林小径依旧点起了路灯。

路灯？你不知道吧？它们是土坯做成的可爱的人偶，立在小迳两边的竹子间，大头，笑得眼弯弯，中间是空心的，用来点灯。雨怡总是跳啊跳的从中间穿过。她似乎是喜欢村里的金先生。当得知就是金先生说她傻时，她有些有不服气地嘟了嘟嘴。但一抬眼，又发现了新鲜事，欢天喜地的跑了过去。她太快乐，又由于傻，永远不懂爱情的浓苦，她又似谜，有人一眼便望进她心里，有人却不知她为何那般快乐美好。她就像那些人偶路灯，空心的，笑眯眯的，一下子照亮你。

影片后半部分的真正的战争，我不想说一个字。如果你看过，请忘记它吧。

如果在这个星球的某个角落，还有某个像东莫村这样的地方，请带上你的爱人，到东莫村走走吧，那个下过爆米花雨的小院，立着人偶路灯的竹林小道，和那片快乐的化解仇恨的草地，你摘下一朵小花夹在她的耳际。不过请注意，嘘——，别惊扰了他们，请像对待南极一样对待它，来过之后，请你知趣地离开。







	
	
	
