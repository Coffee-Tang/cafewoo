<p>黑咖啡其实只是一个很懒散的组织，其实只是聊天室里一帮很好的网友。夜猫子说要写它，真就写了；LUDO说要接着写，也真接下去了。成员、历史、趣事都被他们说得差不多了，我能写什么呢？信手随笔，写到哪算哪了。</p>

<p>咖啡的版面一换再换，聊天室的朋友也是来来去去，猫子写这篇文章，大概是因为感慨物是人非，旧友散去吧。一干人里面，和猫子最为投缘。最初只是对对联或者改改诗词相互攻击什么的，从诗经、乐府、到唐诗宋词，都被改得七七八八了。纯粹是喜欢玩闹而已，并非真有什么愁绪和哀思，所以有时看到聊友认真地一字一句对诗，反倒会愣神。改过的诗词里记得的不多，摘两首做个纪念吧，当然都是损猫子的，说我坏话的诗词，估计猫子会挑个长点的跟贴在下面。：）</p>

<p>“一剪霉”：
<br/>夜猫腿残不思休。轻解罗裳，跳入阴沟。沟中无鼠腹中饥。猫欲回时，虱子满头。
<br/>猫自飘零水自流。几番寻觅，空手而回。此饥无计可消除，唯有低头，勒紧裤头。</p>

<p>“咖啡悲雨”：
<br/>好猫知时节，思春乃发声。随风入咖啡，但愿见美人。
<br/>关门窗，拉电门。咖啡灯俱黑，夜猫眼独明。晓看猫脸处，尽是巴掌痕。</p>

<p>改诗词到了后来发展为互相设计动作恶心对方，这大概就是现在聊天室里“自设动作”的缘起吧。猫子常用的动作是：“夜猫子带着一队老鼠到百姓的床上，留下一堆XX”与泡加了泻药的咖啡给我喝。当时咖啡人少，肆意玩闹，既不顾忌形象，也不考虑有人看了胃不舒服。而友情就在这么玩闹间出来了，聊的话题也就渐渐深了、广了，有时凌晨2点多，咖啡里只剩下两个人，一个是他，一个是我。猫也来了两次厦门，发誓要把我灌醉丢进阴沟，结果是自己酩酊得倒在我的床上不省人事。</p>

<p>现在聊天室的人气旺了很多，两个人的环境也都有很大的变化，我走出了象牙塔，他又折了回去，所以深聊的时间一次比一次少了，他也乐得不理我，只要有熟悉的妹妹在。</p>

<p>YAYA也是黑咖啡的一员，加入的时间略晚。她的IQ很高很高，虽然她一再谦称自己只是运气好。但是她的爱情EQ不高，这也是我认为她虚长我一岁的原因。和YAYA深聊的几次，都是在谈她的情感往事，说到最后都是毫无头绪，有时说得她自己落泪了，看看窗外天也亮了。这次她从加拿大回来，终于有机会见上一面，依旧是聊到了从前的话题上。关于感情的事，当事人常常是任性随意地做了决定，然后拼命给自己找借口，所以旁人的意见，朋友的关心大多没有用。不过爱会让人长大，这个姐姐现在正在幸福，祝愿她最后能够美满。</p>

<p>和LUDO谈心不多，谈论的大多是音乐，当然也论过贱。LUDO从前做的“咖啡音乐”很不错，可惜后来他跑去风云打工，把这块地荒废了，所以说“有钱LUDO也推磨”。和LUDO联手论贱是件乐事，一度把猫子和水灵光论得只剩胡绞蛮缠和敲省略号的份。可怜的是猫子敲出的“.....”、“.......”和“.........”都被我们解释为“五岳贱派”、“全真七贱”和“独孤九贱”。</p>

<p>还有MIKELEE，还有水灵光……</p>

<p>真的都只是玩闹，但时间就这么过去了。故事在每一个“黑咖啡”成员的身上发生着，有的结束了，有的继续着，有的刚刚开始。只是大家的心态渐渐都有了改变，各自有了各自的幸福或等待，渐渐拥挤的聊天室里不复有从前的嘻打笑闹。故事变成了回忆，但网络上曾经有过这样一帮朋友，确实是很美好的往事。</p>

