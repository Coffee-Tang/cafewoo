<p>晕了------韩国机场一瞥</p>

<p>去洛杉机时途经汉城，是免签证的，有72小时可以自由活动。去的时候航空公司提供五星级的酒店，但回来时要自己解决住宿。280欧元一晚我当然是舍不得的，但机场的服务员给我提供了其它酒店的信息。挑了个70欧元的，打了电话过去，人很快过来接了。</p>

<p>到了地方，原来是公寓楼。不免有些失望。接待的是个小伙子，憨憨厚厚的模样。开车，接人，放行李，安排房间，收钱。全是他一人。总拎着一个篮子，里面放着干净的毛巾。开好房间后，就给你篮子里的毛巾，教你怎样用门的密码锁。
<br/>我一进门，房间还算整齐。只是刚想洗把脸，没电了。赶忙打电话给他。还是他一人，拎着工具老老实实的修理起电来，三下两下房间就亮了。
<br/>我肚子饿了，收拾了一下便到楼下让他带着去吃饭。因为我没有韩元。而这酒店居然没有吃饭的地方。
<br/>下楼的时候，刚好又来了一批人。我只见他又拎着同样的篮子，跑上跑下，忙得不亦乐乎。忽然有些想笑，还真象韩剧里居家的好男人。</p>

<p>人实在太多，等了半天，我只好独自去找吃的。</p>

<p>楼下便有一家小吃店。我进去时有人正煮着热乎乎的面条。熬成白色的骨汤，红艳艳的泡菜，QQ的面条。没人会说英语，我用手不停地比划着，恨不得把脚也用上，但没人明白。后来他们打了个电话给一个沈阳的女孩，居然跟我解释说他们不收美金。</p>

<p>我只好又去找那个小伙子，只见他在电梯前，换了一只手，还是拎着篮子，让我再等等。</p>

<p>我快晕了。</p>

<p>忽然想起之前住过酒店。出来一看就在前边。直奔餐厅，挑了个靠窗的位子刚舒舒服服地坐下，喝了口水，点了菜。服务员笑咪咪地问我房间号，我说我不住这。她又笑咪咪地说，对不起，那您不能在这用餐。</p>

<p>我又拐回去。那小伙子还是拎着个篮子，里面放着不知道是第几批的毛巾。。。。。。</p>

<p>我真的晕了。</p>

<p>。。。。。。
<br/>终于他忙完了。领着我到了第一家小食店。我要了两大碗面条，居然吃的一点不剩。</p>

<p>面条的钱我理直气壮地拒付。</p>

<p>因为太累，第二天起不来。去市区逛逛便泡了汤。还有些许时间，便到机场溜哒溜哒。</p>

<p>都说韩国的女子漂亮。仔细一瞧免税店里的服务生还真的个个面容鲜嫩得似牛奶泡出来的。当然最引人注目是打扫卫生的阿姨，约50岁，还化着精致的妆，涂着鲜艳的唇，穿着裙子，正一丝不苟地扫着地板。</p>

<p>我刚想笑，一抬头，安在旭巨幅海报，帅呆了。</p>

<p>我终于晕了。</p>

