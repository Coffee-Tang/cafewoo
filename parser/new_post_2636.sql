她把他们的照片处理成两只在菩提树上跳跃的精灵，裸着上身，穿着可爱的小皮靴，抱着两颗头大的草莓。

好象这个世界只有他们，她把它设为桌面，用显示器禁锢住，看起来有些扭曲。她笑得合不拢嘴，电话里告诉他，他笑她傻。

很多记忆像水一样，鲜活，跳跃，缤纷灿烂，在血液里沸腾，却永远不再复苏。赖在身体里的一滩死水，深夜里牵动心脏最脆弱的一根神经。

之后做什么都刻意很用力，为了遗忘也为了提醒，大口吃饭，仰脸忍泪，没日没夜的睡，一醒来便行走。到处走走停停，看人群路过，离开。

