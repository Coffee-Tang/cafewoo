<p>
“已是黄昏，桔红色的夕阳懒洋洋地烘烧着潮湿的大地，暖洋洋的。”记得谁曾这样描述着他在属于中国国土上的一景。黄昏，多少有些伤感，多少有些浪漫。不知不觉中我也沉醉在一片金黄中。不曾注意今天的天气，或是容易遗忘的，或是刻意不去知道的。阴天，雨天，晴天，天空有属于自己的心情，而我是否更该多花点时间去了解自己的心情。</p>

<p>“太了解自己的人是可怕的。”我不懂，为什么了解自己也成为一件可怕的事。是否了解自己太多，就会忽略了别人。或是了解自己太多，反而不懂得如何生活。也许只因为可怕这个词，使我没有心情去了解自己，我把这个责任丢给天空。于是，阴天，雨天，晴天也成了我的心情。</p>

<p>在一个生活了5年多的城市游荡，搭乘火车去往一个热闹的地方，让自己更接近那片金黄的天空。看着窗外的景色，那是迷人的，平静得迷人的大地。不愿抬头看天空，害怕自己又看见坏天气，刺痛的寒风已经通知了我。好不容易和朋友相约去咖啡厅，该带着愉快的心情。</p>

<p>半个小时的车程只会傻傻地目视着窗外，并没有其它事可以做，唯有把一片空白的思绪交给美丽的风景，一个好心情也许就从一道灿烂的阳光开始。</p>

<p>和朋友在咖啡厅里轻松的交谈着。才发现生活原来也可以这样简单，没有波澜，没有涟漪，平静一如我现在的心情。</p>

<p>话题还是扯不开爱情，朋友问最近心归何处。我笑了笑没有回答，或许我自己连心在哪里都不知道。这也许就是我最不了解自己的地方。朋友说着别人的爱情，6年的感情在2个月的分离中告终。我们都很难想象相爱了6年的情侣竟然经不起2个月的考验。我们只能傻傻地笑，也许他们并没有真正属于过彼此吧！否则一段真爱不会就这样毁灭。</p>

<p>爱情是脆弱的吗？就因为距离的问题就可以拆散一对原本甜蜜的情侣吗？我们只能祝福他们还能找到彼此真正的幸福。或许这样的结局会比在结婚之后才发现彼此不适合才离婚来得好。</p>

<p>恋爱中的人常常会问对方：“你，爱我什么？”或者会说“我爱你是因为你总陪在我身边。”“我爱你是因为你长得好看。”“我爱你是因为你好有才华。”可是当这些所谓爱的理由失去它原本的意义，是不是就可以说不再爱了，或是感情是不能勉强的之类的话。</p>

<p>也许快乐的时候有人总会想到未来的某一天是否还能像现在这样快乐的微笑。也许我们快乐背后的伤感，更应该是珍惜。珍惜现在这一秒的微笑，即使下一秒迎接的可能是不幸与痛苦。</p>

<p>两个人的咖啡比一个人的咖啡来得欢乐些。一个人的咖啡似乎有种寂寞，忧郁，伤感的感觉。而两个人的咖啡虽然不会把所有的思绪放在沉思中，也不会特别仔细的品尝咖啡的味道。或许是更害怕孤独来敲门，总会很欣慰只因为有朋友的存在。话题从爱情谈到了生活，朋友说生命除了爱情以外还有许多值得讨论的话题。比如说，工作，学习，家庭，友情。。。</p>

<p>朋友说“有空多出来喝喝咖啡，逛逛街！不要老是待在家里孵蛋啦！”我笑笑，虽然是开玩笑式的说话，我还是觉得很欣慰。是的，闲的时间太多，才会觉得很无聊，天天没什么事可以做。或许是该去找份工作，不仅多点零用钱还可以打发无聊的时间，何乐而不为呢？：）</p>

<p>时间过得很快，夜幕也悄悄来临，更喜欢这时候抬头看天空，只是漆黑一片，没有所谓的好天气也没有所谓的坏天气。不必急着去在乎是怎么样的心情，天空懂得释放的情绪人类一样可以做到。</p>

<p>漫步在霓虹街道，这是墨尔本最繁华的街道。来来往往的人群，擦肩而过的人们，不一定在哪天也曾经这样路过，或在未来的某一天还会出现同样的画面。生活像个圆圈，不知不觉又回到相同的点，或是某事的起点，或是某事的终点。还是不会忘记自己曾经绕了一圈，重要的是从这个点再次出发的是更懂事，更成熟的自己。</p>

<p>圆的周长越来越长，路越来越艰难，事情也越来越复杂，只因我们拥有更多的思想，更多的记忆。常常告诉自己“过程比成果来得重要。”记忆是我们收集的财富，数十年后翻阅画面，总会对当时的自己有一番评价，或者好，或者不好。人总是在不断的学习中长大。</p>

<p>离别的时候我们彼此挥挥手Say ByeBye~踏进车门，回头微笑。一天就结束在敲打键盘，哼着音乐的瞬间。</p>

<p>“我要你陪着我，看着那海龟水中游~~
<br/>慢慢地爬在沙滩上数着浪花一朵朵~~
<br/>你不要害怕，你不会寂寞，我会一直陪在你的左右，让你乐忧忧！
<br/>日子一天一天过，我们会慢慢长大
<br/>我知道你不懂我在唱什么
<br/>我知道有一天，你一定会爱上我，因为我觉得我真的很不错
<br/>噢喔~~时光匆匆匆匆溜走，也也也不回头
<br/>美女变成老太婆，哎哟，那那那个时候，我我我我也也已经是个糟老头”</p>

<p>如果人的脑袋是海洋，那么记忆的起伏就是那浪花一朵朵。</p>

