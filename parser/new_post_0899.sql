<br>1.深夜的火车和清晨的巴士   (7月6日――  7月9日)   南京
<br>
<br>在7月6日晚上被塞上火车,没有一丝悲哀的感觉.除了兴奋还是兴奋.之前凌子在网上跟我说她一定睡不着.我说好啊,有我陪你的.结果2点时凌子在火车极有乐感的嘈杂声和着莫文尉独特的寂寞嗓音中沉沉睡去.我则在火车对我的精神摧残中醒醒睡睡,终于在凌晨4点时彻底失眠,蜷缩在底铺,望着窗外漆黑得模糊的风景,孤独地等待黎明的洗礼.5点时,列车管理员过来和我聊天,她叫我休息一下.我说睡不着.然后她迷迷糊糊地和我聊天,5点37分,天终于醒来,我仍旧无辜地望着第一个陌生的清晨,列车管理员坐在我的另一半铺上昏昏欲睡,我看了看她的脸,一张疲惫的中年妇女的脸.6点11分,她醒来,离开,和我说再见.6点19分,临铺的姐姐和我同心协力打开那扇窗,风跟着透了进来,很爽.第一个陌生的清晨,窗外没了高山.我告诉自己我离开了.兔兔在6点31分从后车厢过来,她也兴奋不已.
<br>我不停地看着手表,数着时间,无声地抗拒那种陌生.40个小时,终于到达南京.
<br>7月8日的傍晚,我站在南京某街的公车站.身边是拥挤的人群,我掏出手机,给桔子打电话.她刚刚高考完,她说我这边好吵.我说我在南京街上的.然后听到桔子激动地叫声,凌子一直望着我笑.站在南京的街道,我一直有一种莫名的茫然,耀眼的红绿灯,满大街的十字路口.于是我跟凌子说我不喜欢这里,一点都不喜欢,我是抗拒陌生的.凌子说南京很像成都的,感觉特别亲切,于是我们在南京的街道上大声地笑.耳机里听着品冠的&lt;就叫我孩子&gt;,我越来越觉得我们只不过是孩子.
<br>南京的巴士全是无人售票的.我和凌子换了一身的硬币,一边笑着抱怨,一边把闪亮的硬币抛进铁盒子里,没有回响.
<br>7月9日,清晨的巴士.我把一个硬币放进皮夹的夹层里,想要留作纪念.可是后来,不知道是弄丢了还是一不小心用掉了.
<br>
<br>2.她比烟花更寂寞   (7月10日)   太湖镇
<br>
<br>清晨的巴士.我们背着一大包,赶去湖南路.一个老爷爷在下车时为我让了座位,我又把它让给了刚上车的老奶奶,回头才发现,身后的座位大多是空的.我越来越觉得我在南京是彻底茫然的.凌子突然转过身来问我:我们两个路忙,为什么没能在南京走丢?我差异地摇头,然后开玩笑说:也许今天早上就丢了.可是上帝还是太眷顾我们了,最终我们迟到,还是没能走丢.我和凌子特纳闷.
<br>去太湖镇的空调巴士,在高速公路上走走停停,停停走走.我和凌子聊了我的新作&lt;湖水&gt;的一些想法,然后车子终于彻底的罢工,同我的手表一样.凌子望着窗外的平原发愣,时不时叫着郁闷.我在没有了空调的时候特恼火,甚至提议跳车,凌子及周围一些人急切响应我的号召,最终却还是乖乖地留了下了.兔兔在包里找出了线来玩,我哭笑不得,却佩服得五体投地.没了空调的车厢越来越像个烤箱,我们在等着苏州赶来的巴士救命.领队老师一边傻傻地笑,一边安慰说这也是特别体验啊,头顶上不断地冒汗.凌子说:现在笑得越灿烂的就是越难受的.然后我们咯咯地笑.在我们眼里领队老师狼狈不堪.经过4小时的折腾,我们终于被塞进另一个“冰箱”.
<br>晚上吃了地地道道无锡船菜,好象是打翻了糖罐子,什么都是甜的.让我和凌子一阵恶心,兔兔被那挣扎着的软体半透明白醉虾吓得从饭桌上跳了起来,谁知那虾偏爱她,居然也跟着跳出了饭桌,然后只闻一声尖叫.
<br>晚饭后我们看了烟花.我和凌子极有默契地想起那个叫安妮的女子,我跟凌子说:我想去越南了.因为,那里有安妮.安妮是烟花般的女子.凌子只是对我笑,并且点头.兔兔不知所措.
<br>我很久没看见烟花了,真的很久了.只是懒得抬头,懒得去看.烟花在文字里是太寂寞的东西.站在水中央,身边一片漆黑,照亮我的是烟花那个寂寞的东西.再看见参加这次夏令营的兴致勃勃的小小孩(小学生),我彻底的晕眩.我和凌子真的觉得我们根本是不该属于那里的.
<br>我们想念那个烟花般的女人,她应该有一张写满幸福的脸,笑起来会让人看到淡淡阳光.她却比烟花更加的寂寞……身不由己的寂寞……
<br>我和凌子和一个叫天天的小女孩一起住.她的天真可爱让我和凌子觉得自己是那么的可耻.寂寞的人是可耻的.所以,我和凌子只能一起在这个无法容纳我们的地方想念那个比烟花更加寂寞的女子……
<br>
<br>3.一只鱼的暗地行走   (7月11日)   乌镇
<br>
<br>我和凌子极度不习惯江苏菜的甜腻,并且从心底里抗拒.于是有了2天没吃饭的光荣战绩,我们不停地喝水,喝水,还是喝水……兔兔仍旧做了她的乖孩子,舌头麻痹似的照吃不误.
<br>我笑着跟凌子说我快变鱼了,真的快了.
<br>然后不知为什么我们的光荣战绩一时风靡全营,领队老师特地捎来慰问,并且特别交代大伟带我们出去找点合味的菜吃.我们在晚上9点在大街游荡,最后找了一家餐馆,老板居然是个重庆人.相互间一阵寒暄后,我还特别地激动了一番,以为终于可以好好吃一顿了.谁知菜一上桌还是觉得什么都不对,勉强地填饱了肚子,心情就越来越低落了.
<br>我又开始想念我们家Computer.我和凌子经过一番商讨,决定明天还是继续喝水,喝水……我们实在是经不起江苏式重庆菜的折腾.
<br>看看还有时间,我们让餐馆的一个是重庆人的姐姐带我们去网吧.坐在网吧里,我和凌子越来越沉默,兔兔甚是诧异.我的心情一落千里,我是越来越想念我亲爱的Computer.
<br>12点时,我们走在回宾馆的路上.我突然就悲伤得失去了语言能力,我闷着头不停地走,乌镇的街灯在黑夜里总是显得暗淡.我望着自己的影子映在这个千年古镇斑驳的石板路上,黑暗中岁月破碎的痕迹,我的球鞋摩擦着石板发出的声音,在岁月无声的宁静中显得那般的渺小而无力.我没有和凌子他们说话,只是一个人走在最前面,顾影自怜.
<br>快到宾馆就遇见了一个领队老师,正四处找我们.我们都只是沉默了再沉默.没有人察觉的悲哀.
<br>回到房间我贴着门和凌子说话.因为怕打扰到同房的人,我们没有开灯.我们仿佛突然被浸没在悲哀的无边海洋里,相对无言.于是我们逃出了那个房间,坐在大厅里.我说:凌子,我越来越觉得我们不属于这里,它根本无法容纳我们,就像我们本身对它的抗拒一样.凌子还是沉默.我拉着凌子走到花园里,我打电话回家.我跟妈说我呆不下去了,我根本不属于这里,我想去厦门.我妈突然就急了,我听见妈在电话那头哭了,我知道她一直都很担心我.爸在电话那头很恼火,说这还不是你自己要去的,现在后悔也没办法.我的所有悲哀和气愤在一时间决堤,狠狠地挂了电话,我蹲在花园的一个角落哭了出来.我想我的悲哀终于决堤了,没有任何理由.凌子大概是被我给吓着了,我想她绝对没有想过我会哭.我可以理解凌子的感觉,我看见她红了眼眶.我觉得自己真的是个孩子,一个不懂事的小小孩.
<br>大伟下来了,他和凌子靠在一辆小车上,我仍旧蹲在地上.他们俩尽力的和我说话,我都只是沉默.然后我们3人一起沉默.我穿着长袖长裤,大伟穿着短袖长裤,凌子穿着短袖短裤,正好合蚊子心意,他们被蚊子咬得不行,却都没有作声,一直默默陪着我.(荒郊野外的蚊子可不是吹出来的)
<br>凌晨3点,我和凌子回到房间,我整个人虚脱似的无力.倒在床上很快入睡,迷糊中还坚持自己不属于这里.
<br>我只是一只暗地行走的鱼…… 
