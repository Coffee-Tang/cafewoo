<br>4.像天使一样美丽   (7月12日)   乌镇――长兴
<br> 
<br>早上醒来,我一直趴在床上看同房的人各自忙碌.我不得动弹.心底深处还是那句话在盘旋着:我们不属于这里.我在最后收拾了行李,赶去集合.
<br>领队老师盯上我,问我为什么没穿营服,我没有理会,转身走开.我很讨厌那件象征着纪律的衣服,红色的,突显着青春,却又那么的不自由.我说:我不想要呆在这里了.然后我和凌子在电梯口见到了花衣裳组合,她们和我们问好.
<br>沈编留下大伟和凌子陪我解决这个问题.我们坐在大厅里再次僵持,我一语不发.然后我打电话给妈妈,我说我不要呆在这里.我的心里只容得下这句话了.
<br>我想我不是一个贴心的孩子,我永远的站在我的世界的中央,我一直渴望自由,却又根本不明白自由是什么东西.中午的时候,我终于在沈编的劝慰下决定留下来.
<br>我们赶去了乌镇中学的联谊活动.在准备与乌镇中学文学社交流的时候,沈编介绍了我,凌子还有兔兔给花衣裳认识.雪漫正好是四川自贡人,她很开心的用方言和我们说话.我和雨君聊了一会儿,她有很灿烂的笑容.然后我们6个人合影留念,在乌镇校园午后温暖的阳光下,我们一起跟孩子似的微笑.
<br>在与乌镇中学文学社的交流中,我一直很不自在,那不过是一群写着作文,却活在茅盾这个名字下的孩子. 茅盾可以成为无志者的骄傲,却也可以成为有志者的坟墓,那可以是一个阴影.我和凌子,兔兔挤在一起,肆虐的说笑.我班的小小孩们问一些关于茅盾本身的问题,他们一半认真一半敷衍的回答,有的甚至自己都不清楚.我和凌子一直一直的笑,我小声的跟凌子说:问问他们有不有信心超越茅盾,他们一定会口痴的.呵呵.这几个高中生也不过如此.那个胖胖的领队老师一直有意无意的看我,我却一直都没有安分过.我也从眼角看他,我想他一定在想着,你这个孩子怎么这么没教养,你又能有多了不起.间息的时候,他开始含沙射影的说:营里有些人怎么怎么样,当然我没说是我们班的,只是要引以为戒.我轻轻的微笑,然后站起身来问那群孩子:你们认为高尚的作品和媚俗的作品有何区别?然后我坐下,继续微笑.那边的孩子一下子呆住了,然后没有人站起来.胖胖的领队老师有些急了.我和凌子一直一直的笑,因为答案是根本就没有什么区别.生活本身就是媚俗的.大伟示意我自己起来回答,为了收场.我说了自己的看法,同时坦然的说了自己不是个符合他们理想的孩子,我说领队刚刚说的人就是我.我的见解和那种坦然让那个胖胖的领队惊讶,并且有些无所适从.我坐下的时候仍旧微笑,旁边的孩子看着我笑,我为自己骄傲,因为我可以不是个好孩子,我却可以诚实,坦然.高傲和锐气并不可耻.
<br>和花衣裳组合做了交谈,她们对生活有自己的见解，我很高兴.没营养的东西我不喜欢.她们又和我们班合影,一群穿着红色衣服的孩子,在阳光下叫着茄子.时间永远的停格.那一刻我想起7月11日,我,凌子,兔兔,大伟站在乌镇当铺外写着很大一个“当”字的墙下,微笑的样子,印在胶片和记忆里。
<br>
<br>下午3点多赶去浙江长兴市.我在那里看到了久违的山,一望无际老是给我空空荡荡的感觉.可是这里的山都不大,满山满山黄色的树.有人说是竹子.
<br>花衣裳她们离开了.我还是记得雨君,雪漫,美美的笑脸.我突然发现生活中一直隐藏的一种美丽,快乐时微笑的脸,跟孩子似的单纯,在阳光的照耀下.我突然觉得莫名的快乐.
<br>快乐的微笑,坦然的态度,像天使一样的美丽.
