<br>5.另一种方式   （7月13日）   长兴――宜兴
<br>
<br>第4天的任务是探访太湖源头.我们在太阳的照耀下爬山.一群穿着红色衣服的孩子在写着“太湖源头”的大石头旁边宣誓,握着右拳,高高举着,很认真的样子.我在一边傻傻的笑,看水,很清,没有鱼.同和我一样开溜的2个女孩说了一会儿,然后一起大笑.
<br>然后我们爬山,太阳也正好兴奋着,照得我越来越没劲.小小孩们兴致勃勃的把写着愿望的绿色丝带绑在沿途的大树小树上.(我的至今还揣在凌子的口袋里.)沈编赶上来问我:有没有什么愿望?我一边往上走一边点着头笑:有的.沈编接着说:有就留在太湖源头吧.意思就是叫我把绿丝带绑在一棵自己钟意的树上.我没有回答.后来我跟凌子开玩笑说要是不能实现怎么办呢?那就去找沈编算帐.
<br>爬到一半的时候我和小贝又开溜了.(小贝是南京人,初中毕业,专长是怕鬼,因为在乌镇那天晚上他跑去剪了贝克汉姆在今年世界杯里的发型,而被称之为小贝和鸡公头.) 我们在别人奋发向上的时候开始下山.在一块平坦的大石头上看到了正躺着歇息的冯老师,然后我们坐在水边聊了一会儿,并且一直挪动躲太阳.他教给我一个“唯一论”,在离开的最后我才开始明白的理论.还有就是:现代所谓的垃圾文学是放错地方的宝贝.
<br>我和小贝到处找冰镇可乐,然后在公路边的大条石上聊天.我丢失了望天的习惯,这个地方的天空在印象中老是太苍白.公路下的小溪里奚奚落落的人在踏水,有人整个的摔进水里,空气里飘着另一种愉悦的感觉,湿湿的.
<br>陆续的有人回来,坐上车先走.我和凌子陪着大伟躲在巴士的阴影下,热得不行.却还是会开心的笑.有如释重负的感觉.
<br>终于坐到车上准备离开的时候,我闻到自己左手上有青苔潮湿的味道,条石上的青苔.我希望这个味道能永远留下.可是明知道这是不可能的,就没有再想了.  
<br>
<br>傍晚时到达了宜兴.是个安详而和谐的地方,会让人感觉到一些些的温暖.凌子在超市买了她喜欢的水晶之恋和薄荷糖,我买了可乐和巧克力.然后和凌子,大伟走在街道上,有很安然的感觉.我在一家小小的书店找到了棉棉的&lt;糖&gt;.买了很多书.
<br>晚上在小贝他们房间打80分,一直到3点.和我们同房的姐姐直到4点还因为蚊子嗡嗡嗡的叫而困绕得睡不着,握着手机哭笑不得.然后小贝坐在中间那个床上, 我和凌子坐在右边的床上,大伟坐在左边的床上讲鬼故事.怕鬼是小贝的看家本领,然后他一次又一次的打开电视,又被我们骗着关上.凌子开着空调叫冷,然后用铺盖把自己裹得严严实实的.到5点半,我和小贝还是睡不着,于是决定6点去上网.他用了十几分钟在WC重新树立起他头发的形象,然后我们溜了出去.(因为我们6个学生和老师去住的别的地方,所以没人管.)我们走了很远去网吧,看到网吧的时候我们说饿了,然后又走回去.回到房间还是没人起来,小贝跑去我们房间叫那位被蚊子骚扰得快不行的姐姐起来,费了劲都不见成效,于是小贝倒在我床上睡了.我在凌子床上睡了.6点半的时候. 
<br>
<br>6.阳光下   (7月14日)   宜兴――西山
<br>
<br>那天阳光很好,很亮.我们去看瓷器,我只是看了工序,然后独自留在下边看书.不知道为什么我突然就觉得累了,不想走了,没管那么多就坐到了地上.我独自坐一棵树下,明亮的阳光,我坐在那里看棉棉的&lt;糖&gt;,偶尔路过的人诧异的看我,领队老师叫我去车上,我说我在等人.
<br>那个时候什么都是暖暖的.
<br>傍晚进行了夜行三桥的活动.在很长很空的桥上能嗅到空气里湖水的味道.天色渐渐暗下来,我和凌子,大伟,兔兔落在最后,握着一个电筒悠悠的走着,后来每个人都唱了歌,因为大伟说以后可能没这样的机会了.在第2座桥我们把载着每个人愿望的漂流瓶扔进了湖里.突然有种空空荡荡的感觉.
