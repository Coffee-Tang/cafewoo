1． 我需要

曾在无数个不眠之夜，让心潮去起伏，让思绪去自杀，让时间被诅咒。

曾无数次的让自己感动在人为设计的滥情电影里，狂奔在虚无滂沱的肮脏大雨中，淹没于劣质香烟制造的浓雾里，醉倒在燕京啤酒煽动的情伤中。

曾无数回笼罩在三环路的路灯里，仰望着灰暗的都市天空，叫喊出内心真实的压抑。

大都市的喧闹和我所追求的采菊南山的意境迥然不同，我该去寻找什么？

大学府的索然与我所希冀的百家盛唐的年月谬以千里，我能去申辩什么？

大时代的突变同我所徘徊的吝啬现实的寂寞别于天壤，我需要挖掘什么？

我需要……

……是上帝酒后的过错，丢失了那禁果。扛不住本性的饥饿，我需要尝一颗。是未卜先知的祖先，想到我会厌倦，安排好肮脏游戏充斥虚伪野蛮……

2． 影子

你是影子，还是影子是你？

从前，有个人在外地的小酒馆里遇到一个多年不见的朋友，狂喜之余不免狂饮。醉醺醺之后，这人便提议和那位朋友出去走走，朋友欣然应之。两人结伴而出。不时，这人便发现朋友在烈烈骄阳下竟没有影子，这人心里一惊。仗着几分酒劲，这人便问道，令尊令堂尚健在否？朋友答道，尚在。这人又问道，可喜可贺，二位大人高寿？朋友答道，都是花甲之年。朋友的父母年龄相差不大，生出的孩子应该有影子才对，莫非……想到这里，这人不禁出了一身冷汗，我的朋友可能是鬼，但大白天鬼怎么会出现呢？于是便斗胆问朋友道，您怎么会没有影子？朋友笑曰：一个名医说我是走的太快，凡事都身先士卒，追求功名太甚，以致于影子跟不上。那个名医劝我多考虑考虑家人，别怠慢了他们。所以我现在正往家里赶，没想到走的太快以致于影子又没跟上。这人恍然大悟。

这个故事虽然荒诞，但仔细一想不无道理。我们平时为了各种事情而疲于奔命，到头来又得到些什么？只不过换来一丁点微不足道的所谓的成绩，其实更多的则是搞坏了身体，伤害了家人的期望。我们有没有丢掉自己的影子？

走的远了，有时是需要回头看看自己的影子。千万别把影子弄丢了，而使自己最终变成飘荡在异乡的孤魂野鬼。

影子……

时光就像射出的箭，穿梭曾经许下的诺言。洒落多少变幻的影子，残留在我眼前。身外世界平静依然，现实梦想已变得遥远。埋藏无数失望的影子，挣扎在我心田。飘飘荡荡总有些疲惫，无力抹去流淌的汗水。冷漠心情像我的影子，永远无怨无悔。离开日渐繁忙的角落，重新寻找迷失的自我，你却总想拉住我的影子，怕它再去漂泊。想要掩盖那脆弱，你却消失在身后。梦想所有的一切，让那伤口无法愈合，让那黑夜永在我左右……

3．你是什么

有很多朋友总跟我抱怨，女友如何如何让人难受，自己如何如何貌合神离疲于应付，甚至想结束那段蚀骨的爱情。并且在醉酒的时候问我，这世界上到底有没有真正的爱情。

我很难回答这个高尚的问题。我只是说，你们去看看王朔的爱情小说，女孩子的通病在他的书里都有记载。

但是，这些花花公子有时间去读一读书吗？答案是：没有。所以他们成功了，我失败了。：）

我是一个王家卫式的思想唯美主义者，所以我相信这世上有真正的爱情。虽然我是一个马克思主义唯物论者，但我仍然相信这个世界有缘分，这不一定是唯心主义的观点。马克思主义概率论中说：小概率事件不可忽视！缘分只不过是一个小概率事件，所以一定有缘分。而且不管在何时何地我都会等，一旦让我遇到机会，我一定会死缠烂打，因为我已浪费了太多的缘分，我已输不起了。毕竟是快奔三十的人了，就快人老珠黄了。^_^

最好的东西永远是你得不到的东西，所以喜新厌旧便产生了。受害者们于是暗无天日般的一蹶不振，怀疑一切。然后，像醍醐灌顶般的大彻大悟道：“直到今天才知道你是什么！”

他们真正知道吗？未必。人的主观判断永远都不可能完全可靠。可见，马克思主义唯物辩证法比主观唯心论先进的多，虽然前者比后者少了一些诙谐与睿智。

你是什么，你自己知道吗？你知道自己知道什么吗？

你是什么……

……记得曾为你写的歌，记得你给我所有的寂寞。眼前总是点不亮的灯，身后总是做不圆的梦。天下多少痴心男人，因为梦想太多结果失去了强壮的自我；天下多少负心女子，抛弃真爱又却能得到更多？春夏秋冬风花雪月长长的街冷冷的夜，你尽管去走过；世态炎凉人情冷暖苦苦的酒罪恶的手，你自己去承受。你不要说太多，马上离开我！直到现在依然不知你到底是什么！……

4．梦

在梦里曾无数回被人追赶，甚或追杀，而每次都能死里逃生；在梦里有无数回和死去的亲人说话，并说出他们生前自己的种种不孝并感受着他们慈爱的抚摸；在梦里无数回丢掉虚伪的面孔和自己讨厌的人翻脸无情，出一出现实生活中的压抑；在梦里无数回搂着自己的新娘留下幸福的泪水，搂的紧紧的生怕又是做梦；在梦里无数回驰骋在万里边疆收复丢失的疆土，恨不得和侵略者决一死战；在梦里也曾无数回的考试迟到，或者面对试卷两眼发直、头脑一片空白，醒来时满头大汗；在梦里更无数回像守财奴一样去捡总也捡不完的钱，心中高呼终于有钱了；在梦里自己总是那么雄辩；在梦里自己总是那么勇敢；在梦里自己总是那么胸怀绝技、武艺高强；在梦里自己又总是那么幸运与机智……

在梦里每一回做愉快的、浪漫的、离奇的梦时总会问自己，或者掐自己的大腿验证是不是又在做梦。但是太平盛世下谁不愿意及时行乐、醉生梦死，对他们来说做不做梦都是一个样。

也许只有你我愿意时不时使自己的心灵痛楚一下。

