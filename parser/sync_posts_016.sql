5．欢乐颂

忘记了欢乐，也忘不了忧伤。

大家还记得上世纪末被媒体炒作的沸沸扬扬的九九大劫难、千年虫问题、千禧年，现在觉得不过是昙花一现、过眼云烟。正所谓：一时间江山如画，让人感到人生如梦、长夜漫漫、相逢很晚、造物弄人，多情应笑我，早生华发，一樽还酹江月。

我的1999年最后一天是在中华世纪坛度过的，当是时，才子佳人，春意融融。外国记者在看中国人的热闹（新华社记者语），男女主持人不论从外表还是骨子里都流露出资本主义腐朽的繁华，他们时不时直播现场，时不时采访现场中国学子，不知用意何在。当晚，江主席点燃世纪之火，我们欢呼，我们疯狂挥舞手中的校旗、国旗。江主席等国家领导人发表新千年讲话后，气氛达到高潮，外国记者完全可以从当晚我们的狂热中看出21世纪是属于中国的！

和以往的大型庆典一样：我的嗓子哑了，双手都被兄弟院校的兄弟姐妹们给握红了。

新千年来临之际，我和几个男同学在前方记者区看老外们的热闹、搂搂抱抱。追我的学妹在四处找我，希望我用她的大哥大给家里的老爹老娘问个好。幸好没找到我，要不然劳累一天的父母大人一定会大骂我打扰好梦的。

在新千年来临的时候，我竟没和追我的其实我也喜欢的学妹在一起度过。当时，我应该和她学老外中美丽的女主持人拥抱同事一样拥抱她，可惜我不在……

后来她离开我，现在想想是应该的，这么浪漫的时候我竟不和她在一起……

我将永远后悔当晚的不珍惜……

让我们为21世纪祈祷吧……

欢乐颂……

