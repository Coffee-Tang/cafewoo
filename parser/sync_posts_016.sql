这篇小说我曾经在一个晚上看了五遍，也曾写过一个小东西述说它带给我的沉醉及感动。它太美了。可以说是一篇小说化的抒情诗。王小波成熟期的小说也不能掩盖住它清丽的光辉。

王小波在《我的师承》里还提过：“正如法国新小说的前驱们指出的那样，小说正向诗的方向改变着自己。”用古代文论的话来说，小说应是“无韵之离骚”吧。读他的小说，尤如读某种无韵的叙事诗，时而澎湃，时而和缓，每一个转折都有它的意味。

在《万寿寺》的结尾，王小波说了一句动人心魄的话：“一个人仅仅拥有此生此世是不够的，他还应该拥有诗意的世界。”其实这是“人，诗意的栖居”的另一种表述。我想“诗意的世界”也就是王小波苦心孤诣经营的精神家园，是他赖以生存的自由的思维方式。

