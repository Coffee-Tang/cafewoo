<br>叙述方式
<br>
<br>以前看过卡尔维诺一个叫《寒冬夜归人》的小说，小说由若干个没有结尾的故事组成，故事的时空颠倒，却以一种内在的韵律组织起来，犹如一串美丽晶莹的珍珠，我们能看见它温润的光芒，却看不见串起它的那一根线。
<br>
<br>有些小说读了之后让企图写小说的人绝望，比如我。《万寿寺》即属此列。《万寿寺》里的叙事方式是崭新动人的，唐代薛嵩的故事是通过九十年代一个失忆的历史研究员王二来叙述的，而王二的故事也随之并行发展。王小波在小说里还作了穷尽小说的可能性的实验，你能看见他象洗牌似的将各种故事的元素不断的排列组合，你永远不会知道下把牌是什么。王小波机智的给你看他臆想的各种可能，那种技巧是异常娴熟的，带有浑然天成的美丽。
<br>
<br>未竟的《黑铁时代》向我们呈现了另一个叙事的尝试。可惜的是该文未写完王小波就匆匆离世。电脑写作对作家带来了发展技巧的便利。他们可以方便的COPY和PASTE，可以随心所欲的推翻重来。对此王小波提到过他在电脑上做的各种实验，直到文字组合出一种内在的韵律为止。我们只能叹息天妒英才，王小波没来得及带给我们更多的惊喜。
