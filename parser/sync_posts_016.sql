6．快乐天使

在我的记忆中我就没有几次是真正快乐的。

每一次的快乐之后都是更惨烈的痛苦。

我在明白了痛苦的原因之后写下了下面的诗句：

希望在起点  挣扎在途中  成功在终点

满足在成功之时  失望总是出现在满足之后

永远也也忘不了20世纪经典的话语：

……不开心，长生不死也没用，开心，就算只能活几天也足够了……

……来吧，就像你说的，做人不开心就算长生不死也没用……

……小姐，不可否认我长得很丑，可是我很温柔，而且永远不会说谎……

……经过这两天的朝夕相处，我发觉我已深深的爱上你了……

……爱一个人需要理由吗？……

……以前我看事物是用肉眼去看。但在我死去的那一刹那，我开始用心眼去看这个世界，所有的事物真的可以看得前所未有的那么清楚……原来那个女孩子在我的心里面留下了一滴眼泪，我完全可以感受到当时她是多么的伤心……

