<p>日久才见人心。你轻易地把一个人视为好朋友，这已经错了。我们经常说
<br/>一起吃喝玩乐的不能算是好朋友，我想说的是能同甘共苦的也不见得是好
<br/>朋友。</p>

<p>一个人能不能做好朋友，关键看这个人是否善良。如果你苦他甘的时候，
<br/>他能雪中送炭；你甘他苦的时候，他能锦上添花，那么这样的人是可以视
<br/>为好朋友的。做好朋友倒不在于彼此是不是性情相投，相互了解。举个例
<br/>子：一个公司的两个部门经理，两个人有着相同的学历，相似的成长经历
<br/>，属于同一个阶层，又长期在一起工作，他们都想当上正总经理。应该说
<br/>这两个人相互是很了解的，但是他们不可能成为好朋友，甚至不可能成为
<br/>朋友。</p>

<p>我们的周围善良的人很少。如果你是一个善良的人，并不等于你就会碰到
<br/>另一个善良的人做你的好朋友；如果你不是一个善良的人，你就不会有什
<br/>么好朋友。</p>

<p>扪心自问，我们自己是一个善良的人吗？</p>

<p>如果你为了能找到一个好朋友而去做善良的人，那么你要预见到这样一个
<br/>结果：在这个社会里善良的人总是吃亏，善良的人得到的总是太少。当然
<br/>，如果你真的善良的话，也就不计较得失了，可是你做得到吗？我做不到
<br/>，我也没见过谁做得到。</p>

<p>你可能会说我把好朋友的标准定得太高了，但如果一个事物真的是“好”
<br/>的话，那我们为什么不用高的标准去界定它呢？其实我真正想说的是，这
<br/>个社会更多的是人与人的相互利用，好朋友实在是很虚的。</p>

<p>最后要说的一点是：如果你真的心中有怨气的话，不要随便找朋友倾诉。
<br/>很多所谓的朋友并不是真的关心你，他更愿意的是把你的心情与经历当作
<br/>趣闻和谈资，随便听听说说（当然他在听你说的时候，表情是很关切的）
<br/>。这还是次要的，重要的是你在倾诉的时候，免不了会对原先的朋友做评
<br/>论，这样的评论通过你现在的倾诉对象再转述给别人的时候，就对你不利
<br/>了。“背后莫言人是非”，并不是让自己善良，而是要让自己少惹麻烦。</p>

<p>呵呵，我又不是你什么朋友，和你说这些干什么。</p>

