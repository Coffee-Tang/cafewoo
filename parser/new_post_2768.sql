<p>    “听说了吗，旋怛？和你一同担当公主侍卫的不是死刑犯就是被判无期徒刑的犯人。”
<br/>    他毫不在意地飞起一脚，踢断了三块木板：“听说了啊，公主殿下不是出面解释了么？我们这些人个个身手不凡，而且比一般士兵更忠心，所以才会选上我们啊。”
<br/>    “笨蛋！正好相反吧！这些犯人比兵队难管理上好几倍，而且都还是些小孩子——你好象还是最大的一个，真要打起架来哪能有那些士兵强？”
<br/>    “爷爷你好??嗦呀！我已经十八岁了，不许再说我是小孩子！而且我绝对比那些士兵强，一个打五、六个都没问题！”
<br/>    爷爷沉默地抽了口烟，终于提出了个条件：“我教给你一个招数，从现在起，你每天晚上都要练习它。这三个月内你若是学不会的话，就是死我也不会让你去做侍卫的！”
<br/>    “爷爷你……”旋怛无奈地叹了口气，“说吧，是什么招数？”
<br/>    “来，用你的轻功站到这棵松树顶上去。”爷爷拍了拍屋子门前唯一的一棵树。
<br/>    他轻松地办到了：“然后呢？”
<br/>    “然后你每天晚上都保持这样睡觉。一直练到能够睡饱又不掉下来的地步。”
<br/>    “你疯啦！在这种地方睡觉！还得保持轻功！这根本不可能！”
<br/>    “这是锻炼你即使在睡眠中也能够警醒地探知周围情况。倘若连这点都做不到，你很有可能出行第一天的夜里就被刺死了，笨蛋！”
<br/>    “……”
<br/>    “早上让我看见你的黑眼圈就算不合格！好了，年轻人，加油吧！嗬嗬～～”
<br/>    第一个月内，夜夜都能听见他从树上掉下来时惊天动地的响声；第二个月，他勉强能够入睡了，但到了半夜总会因为过度使用轻功，体力不支而落地；第三个月，他终于能够整夜入睡了，但遇上风雪天还是会被吹下来；第三个月的最后十天，即使在狂风中也能岿然不动的他，开始受到爷爷时不时扔出的暗器的骚扰，这迫使他拥有瞬间清醒的能力。
