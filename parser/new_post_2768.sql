<p>    “首领，公主的护卫军除一人外已经全部被歼灭！”
<br/>    “首领，队里有31人死亡，另有25人受伤！”
<br/>    首领的眼睛眯了起来，他低声说道：“全部都是那一个人干的吗？棘手呢……”</p>

<p>    “公主殿下，请您暂且在这里歇息一下，我去看看还有没有追兵。”
<br/>    “……”公主沉默了一会儿，才忽然被惊醒似的抬头应道：“啊，好的，谢谢你了……嗯……你叫什么？”
<br/>    旋怛的笑容僵了一下，有些颤抖地回答：“旋……旋怛……”
<br/>    “好的，谢谢你了，旋怛！”
<br/>    公主不记得我了……这是很正常的事情……不必要难过呀……
<br/>    他嘴上这么说着，眉间却爬满了哀伤。
<br/>    “那个，旋怛是怎么发现那些……刺客的呢？”公主轻轻地往火堆里拨着小树枝。
<br/>    “啊，这个啊，这是因为……”冬末春初的拂晓实在好冷，旋怛不停拍打着有些僵硬的脸颊，他已经把大部分的衣服都加在公主身上了，但一想到家中那温暖的炕床，以及天天烟不离口的爷爷，身体似乎就暖和了许多，“这是因为爷爷教导有方啊。即使在这种冷天睡着也能够清楚地知道周围的情况，不要说是这附近，100米外的事情我都能知道呢。”
<br/>    “哦，这样啊……”公主似乎一脸忧愁，“旋怛的武功似乎也很不错呢！”
<br/>    “原先就学过一些，出狱后又苦练了三个月，自然有所长进。”旋怛感到些许自豪，“放心吧，公主！我保证您一定能安全到达参国的。”
<br/>    公主用眼角微微地扫了他一眼，又把头埋下去：“其实，旋怛……我……”
<br/>    “快走！”旋怛忽然跳起来，拉着公主匆匆地跑了起来，“有人追过来了！”</p>

<p>    “旋怛……我，我跑不动了……”
<br/>    “不行！……那，我背着你跑吧！”
<br/>    平时整夜地施展轻功，即使现在背了个人，体力也绰绰有余，旋怛背着公主却跑得更快了。后面追兵的人数减减少下去，就这样过了大约一刻钟，只有一个人跟了上来，并且穷追不舍。
<br/>这样下去不是办法……不如停下来把他解决掉吧。
<br/>    旋怛停在了一块多岩石地带，他将公主藏在一块巨石后面，自己独身应战。
<br/>    那个人来了，看他的装束，是首领。
<br/>    启明星渐渐地沉下去了，两人开始了厮杀。
<br/>    旋怛意识到对方使用的是正统的皇族剑法，而自己的则是祖祖辈辈传下来并不断改进的“野”剑法，凝聚着好几世代的智慧，应用性远胜于对方，加上自己刚刚已经稍微休息了一会儿，能赢，绝对能赢！
<br/>    “喝！”气势汹汹的一剑刺了过来，旋怛灵巧地向侧一闪，轻松地避开。随后剑峰一转，横向地扫过去，对方连忙将剑竖起来抵御。
<br/>    “铛！”
<br/>    两剑相撞，趁这个空当，旋怛纵身跃起，一脚向对方脑门踢去，对方一惊，没料到一场剑战中敌人居然用腿来攻击，只好反射性地将剑倾斜。
<br/>    “乒！”一条又细又深的裂缝从剑被踢到的地方迅速蔓延开来，不消一秒钟，剑端“咔嚓咔嚓”地裂开，碎掉了。
<br/>    “结束了！”旋怛高举着剑，将要刺下。
<br/>    “不要，旋怛！”
<br/>    旋怛猛地将对方手中的剑击飞，疑惑地回头问道：“怎么了，公主？”
<br/>    “求你，不要杀他……”公主抓着旋怛的手臂，跪了下来。
<br/>    “怎么回事？”
<br/>    “在下将要娶公主为妻，公主也答应过在下了。”首领拍了拍身上的尘土，朝旋怛作了个揖，“这场比试输给阁下了，甘受处置。”
<br/>    公主一惊，这个向来高傲的男人竟对自己的侍卫用了敬语。
<br/>    “你……你想怎样？”到了这种时候，旋怛反而不知所措起来，“等……等一下，这么说公主和你早已认识了？”
<br/>    “在一次偷袭中偶然相遇，一……一见钟情，遂私定了终身。”首领微微尴尬。
<br/>    “那么这次出嫁参国的事情只是……公主出逃的契机？”
<br/>    “是的……”公主的细眉忧伤地蹙起，“离国之前已放了信鸽报信，他们事先结集好人马，选在半夜下手，没料到旋怛竟那么警觉。”
<br/>    难怪那个时候公主已经醒了……
<br/>    “那么重刑犯和死囚……”
<br/>    两人忽然都沉默了。
<br/>    旋怛疑惑地盯着他俩，突然睁大了双眼：“难……难道，你们一开始就准备把侍卫全部杀掉，所以……所以才选了我们？就因为我们是杀掉也没关系的人吗！？”
<br/>    他听见他们默认的声音。</p>

<p>    锋利的剑，冰冷地，精准地，无声地，刺穿了心脏。暖暖的血，细细描出剑的轮廓。
<br/>    “公主殿下，恕在下不敬，这种事情早说出来不是比较好吗？何必兜了这么大圈子，害你我都多吃了苦头。”
<br/>     公主和首领惊得颤抖了起来，连一句完整的话都给抖碎了。
<br/>    “旋……旋怛，你……你……”
<br/>    “阁下……您……为什么要这样……我们，我们并不想……伤害您啊！”
<br/>    旋怛缓缓抽出剑，血液喷涌，血珠飞溅。
<br/>    “哈……哈……”他有些上气不接下气了，脸上的红润瞬间转为苍白，“你们……以为放了……我，我就可……可以活下去吗？国……国君，对于逃兵……向来……是杀无……赦的……”
<br/>    与其被人所杀，不如现在就让你们欠我一个人情，一个用下辈子来偿还的人情。
<br/>    红色很快扩散开去，土地变成了暗红色。</p>

<p>    启明星完全落下去了，天空出现灰白的曙色。
<br/>    第一束阳光打了下来。
<br/>    “他是一名优秀的侍卫呢，公主。”
<br/>    “嗯，是啊……”
<br/>    两个人最后朝尸体鞠了一躬，转身匆匆地走了。
<br/>    更多的阳光打了下来。
<br/>    黎明已经完全来临了。</p>
