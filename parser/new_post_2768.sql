<br/>    深深陷进去的一双眼睛默默地注视了好一会儿，老人转身走了，一句话也不留。
<br/>    少年轻叹了口气，翻身跃上马背。铁蹄踏在刚扫过雪的路面上，嘀嗒嘀嗒地远去了。
<br/>    老人闭上眼睛，两行浊泪淌过双颊上深深浅浅的皱褶。
<br/>    “旋家的香火就此断了啊……”喃喃自语。</p>

