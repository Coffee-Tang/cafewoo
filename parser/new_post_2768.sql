<br/>    又做梦了，而且还是同一个梦。他踱到水池边洗了把脸，雪地把月光无限传递开去，四野一片通明。
<br/>    十二岁一时冲动杀了不断虐待自己的哥哥，被判了终身监禁，因为答应了公主的要求而破例释放。出狱时已经十八岁，奶奶病逝，父亲外出时被战乱波及而死，母亲改嫁，家里只剩下爷爷和自己相依为命。
<br/>    当爷爷去监狱接自己出来时，那张老脸一会哭一会笑的，搞得旋怛都不知道该安慰他好还是陪着他一块儿高兴。
<br/>    从出狱到护送公主出嫁只有短短三个月不到，每天都有士兵在旋怛家那栋偏僻的老屋附近徘徊——目的很明显，防止旋怛反悔跑掉了。
