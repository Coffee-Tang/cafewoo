【正文】

我喜欢喝咖啡，喝不加糖，不加奶的咖啡。

今天早晨刚刚起床，家里除了我人已经走光了。饿，却懒得弄饭吃，喝杯咖啡吧。我懒洋洋的为自己冲了杯咖啡。

端着咖啡杯呆呆地坐在桌子前随手拿起本书，其实心思并不在书上，只是习惯而已，拿着书想事情。

屋里静悄悄的，喝口咖啡，冷然觉得今天的咖啡出奇的苦，有种难以下咽的感觉，我没有站起来去加糖，还是呆呆的继续想事情。思绪拉回来，我在想今天的咖啡为什么这么苦？我已经有些搞不清楚现在苦的是咖啡还是心情。

从医院回到家里，觉得有些陌生，短短的几天里，发生了太多事情，似乎也失去了很多东西。

在临出院的前一晚不知那来的精神头儿，在网上整整游荡了一夜，什么也没写，只是漫无目的的四跑，直至凌晨数据库系统开始维修，我就更觉得迷惘，没有目的，真的想只没有舵的小船在茫茫大海中飘。医院里很静，静的让人发毛，让我难以入睡。夜终于熬过去了，天亮了，看着外面的阳光，仿佛有一种希望。今天我便可以走出这里，去拥抱阳光。

回到家里，看到家人和朋友，莫名的欢喜，虽然昨夜无眠，但还是神采奕奕。晚上坚持出去玩。坐在酒吧里有种恍然隔世的感觉，朋友刨根问底的问我究竟出了什么事，我一个字也不想说，只是信誓旦旦的答应他：不再动情，不再为情而苦，以后乐我而乐，随心而动。他说我不该做出这样的决定，会伤了包括他在内很多人的心。我本不想做出这样的决定，可在医院那天我已经清楚的想过了，我决定了毕业以后的漂泊，所以不能让自己定下来。自己也不是小孩子了，做事情不会希望没有结果，而动情的结果将是什么？也许会让自己定下来，所以我不能！

爱，注定了一生的漂泊；不爱，也要漂泊。那么我选择不爱了，因为怕，怕再受伤，我试图找个壳把自己藏起来，不再受伤。我不顾朋友说我自私，人都是自私的，尤其是我。不再动情，曾经的到处留情使自己吃尽苦头，这种苦让我心碎，让我流血。

看着书里的故事，浪漫；故事的过程，曲折；而结局往往是美满的。那毕竟是故事，和现实生活相差甚远。写美满故事的人，往往是对现实生活的一种憧憬，对未来的一种寄托。一场梦，梦永远是梦，梦醒一场空，只有残酷的现实。呵~，原来是梦，原来自己什么也不曾拥有，我不喜欢梦，甚至厌恶它，我不能面对拥有后再失去的苦，也许不该有什么希望，即使很消极，但至少不会失望。

我有个坏习惯，在睡不着的时候偏爱咖啡，提提神，可以继续想那困扰我不能入睡的事情，我不想带进梦里，会很累，也很苦。

