人们失望的离开，留下光秃秃的后背在荧幕反射的光中隐隐约约。

萤静静的坐着，背后是苍白灼热的日光，面前是清澈透明的瓶。瓶中安详的躺着一些萤火虫灰蒙蒙的尸体。已经僵硬。轻轻的晃动瓶子，可以听到哗啦啦的清脆响声。萤流着泪，还是明白了，萤火虫是不属于自己的，也不属于任何其他的人。它们都是游离的生命。可以在狂风暴雨的夜坚强挺立在黑暗中，却无法带着明亮的阳光在安逸温暖的玻璃花房里度过半日。
萤火虫飞在夜空，天一亮，就会悲哀的抚着泪相继死去。

无能为力。

它们被萤埋在了破楼厚厚的灰尘中。

那幢破楼，整夜哭泣，第二天天明，轰然倒塌。

萤不哭，没有笑，只是缓缓的转身，渐行渐远，轻轻消失在逆向的人流中。

画面渐渐变暗，直到完全褪成漆黑的颜色。几秒后，灰蓝的天空忽然出现，让人措手不及。一只萤火虫从画面左上方缓缓斜向下飞。画外音，一个略带沙哑的女生安静的声音：

