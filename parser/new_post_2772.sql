哦，请不要责怪我把她描述的这样平淡无奇，大多数孩子最初都会是这样，直到他们遇见了“火”。
萤是在爱上摇滚以后遇见火的。火的吉他是深红色的，一团燃烧在黑夜的烈焰。

萤喜欢摇滚，可是她不喜欢张扬的摇滚，比如已经被无数人喜欢的快要腐烂的Nirvana，比如越来越通俗的Linkin Park……这些人人都能叫出名的音乐载体，萤不喜欢。她唯一的爱就是Lacrimosa ?D?D以泪洗面。

以泪洗面只有两个角色。一个小丑，一个女神。小丑在五光十色的喧闹舞台上迷失，女神拯救了他。小丑在女神怜爱而冰冷的眼神中迷失，女神却要离他而去。于是以泪洗面，小丑的声音如被泪洗。萤觉得，那样的声音应该有棉花糖那么大的直径，粘满了她的双手和耳鼓膜，火笑了，点着她的额头说，你要和小丑化为化合物了。她低着头，声音略带沙哑，谁和谁终于能变成化合物呢？最后都只会在泪的溶液中消融为离子，各自互不干扰，互不萦绕，终于，还是会蜕变成游离的单质。

谁会例外呢？

她说这句话的时候我一定流泪了。因为我看到，在那段35mm的胶片上，一道细细长长的云状痕迹在缓缓沉默。

火给了萤一首歌，是英文的，却没有告诉她歌的名字。萤轻轻的哼唱，荡漾在夜空中的回声几乎让她疯狂。实际上并不疯狂，只是站在破楼最低的那层台阶上，不断的跳上去，跳下来，跳上去，跳下来……

是啊，你在轻轻点头。你看见那样素白的星星闪烁下，镜头越拉越远，直到萤的微笑和咿咿呀呀的旋律定格在夜色中，像划过玻璃瓶口的一道刺眼阳光，只有那一个角度，只有那么一瞬。

只有那么一瞬。

火莫名的出现在空旷的原野中，又莫名的消失在逆向的人潮中。是的，是逆向的人潮。萤总是觉得火离去的方向才是正确的。火去努力争取的，是梦。然而萤，她所拥有的，只有平摊在面前的淡淡的生活。就那么平展展的铺着，没有曲折，没有余地的生活。萤是在这是爱上萤火虫的。
那种时断时续的灰暗，还有歇斯底里坚持到天色泛白的明亮。因为明亮，所以黑暗并不曾让谁感到恐惧。即使是若即若离，即使是星散四方。黑暗只是一种过程。

萤坚持告诉自己，它们还在，它们都还在努力的期待黎明。所以，低下头，望见的不会是黑色冰凉的夜……

