<br>诗性
<br>
<br>王小波起初是写诗的，后来才写小说。诗歌对他的影响是巨大的。在《我的师承》里他写过：
<br>
<br>
<br>“我终于有了勇气来谈谈我在文学上的师承。小时候，有一次我哥哥给我念过查良铮先生译的《青铜骑士》：
<br>
<br>    我爱你，彼得兴建的大城，
<br>    我爱你严肃整齐的面容，
<br>    涅瓦河的水流多么庄严，
<br>    大理石铺在它的两岸……
<br>
<br>    他还告诉我说，这是雍容华贵的英雄体诗，是最好的文字。”
<br>
<br>王小波也有好的诗歌品味。他还谈到他理解中的好诗：
<br>
<br>“比方说，德国诗选里有这样的译诗：
<br>
<br>    朝雾初升，落叶飘零
<br>    让我们把美酒满斟！
<br>
<br>带有一种永难忘记的韵律，这就是诗啊。”
<br>
<br>诗歌带给王小波优美的语感。在他早期的小说《绿毛水怪》里，还有这样的段落：
<br>
<br>“有一天学校开大会，我们出来的时候已经很晚了。那是五月间的事情。白天下了一场雨。可是晚上又很冷。没有风。结果是起了雨雾。天黑得很早。沿街楼房的窗户上喷着一团团白色的光。大街上，水银灯在半天照起了冲天的白雾。人、汽车隐隐约约地出现和消失。我们走到10路汽车站旁。几盏昏暗的路灯下，人们就像在水底一样。我们无言地走着，妖妖忽然问我：“你看这夜雾，我们怎么形容它？”
<br>我鬼使神差的做起诗来，并且马上念出来。要知道我过去根本不认为自己有一点做诗的天分。
<br>我说：‘妖妖，你看那水银灯的灯光象什么？
<br>大团的薄公英浮在街道的河流上，
<br>吞吐着柔软的针一样的光。’
<br>妖妖说：‘好，那么我们在人行道上走呢？这昏黄的路灯呢？’
<br>我抬头看看路灯，它把昏黄的灯光隔着蒙蒙的雾气一直投向地面。
<br>我说：‘我们好像在池塘的水底。从一个月亮走向另一个月亮。’”
<br>
<br>
<br>这篇小说我曾经在一个晚上看了五遍，也曾写过一个小东西述说它带给我的沉醉及感动。它太美了。可以说是一篇小说化的抒情诗。王小波成熟期的小说也不能掩盖住它清丽的光辉。
<br>
<br>王小波在《我的师承》里还提过：“正如法国新小说的前驱们指出的那样，小说正向诗的方向改变着自己。”用古代文论的话来说，小说应是“无韵之离骚”吧。读他的小说，尤如读某种无韵的叙事诗，时而澎湃，时而和缓，每一个转折都有它的意味。
<br>
<br>在《万寿寺》的结尾，王小波说了一句动人心魄的话：“一个人仅仅拥有此生此世是不够的，他还应该拥有诗意的世界。”其实这是“人，诗意的栖居”的另一种表述。我想“诗意的世界”也就是王小波苦心孤诣经营的精神家园，是他赖以生存的自由的思维方式。
