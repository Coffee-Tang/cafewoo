诗性

王小波起初是写诗的，后来才写小说。诗歌对他的影响是巨大的。在《我的师承》里他写过：

“我终于有了勇气来谈谈我在文学上的师承。小时候，有一次我哥哥给我念过查良铮先生译的《青铜骑士》：

我爱你，彼得兴建的大城，

我爱你严肃整齐的面容，

涅瓦河的水流多么庄严，

大理石铺在它的两岸……

他还告诉我说，这是雍容华贵的英雄体诗，是最好的文字。”

王小波也有好的诗歌品味。他还谈到他理解中的好诗：

“比方说，德国诗选里有这样的译诗：

朝雾初升，落叶飘零

让我们把美酒满斟！

带有一种永难忘记的韵律，这就是诗啊。”

