<br>性
<br>
<br>王小波小说中的性描写一直为文学界的道德冬烘所诟病。其实在王小波心里，性应该是一种健康的生活态度，他反对一切压制人性、违背人性的东西。我在《万寿寺》里读到：“性是人间绝顶美丽的事”。这句子让人动容。他仿佛是阳光下的孩子，你无法不把他的纯真所打动。王小波笔下的性是妙趣盎然的，如同恣意的海洋，包括了无穷的想像。《东宫西宫》里，他把同性之爱描写得温柔美丽， 里面的两个角色十分有意思，主角是一名同性恋者阿兰与一名警察小史。警察身上有同性恋倾向而不自知，一个夜晚他审问阿兰，让他交代自己的龌龊行径，于是阿兰给他讲自己作为一个同性恋者的生活，阿兰最终同化了小史，在天亮时他们做了爱。阿兰的职业是作家，好象是谁说过，作家可以通过写作来改变自己，阿兰的陈述带有一种美丽的诱惑，也可以说他用话语改变自己的命运并摧毁了警察小史。而我们通过主角的社会身份（知识分子意味的作家及象征国家机器的警察）又可以看到一个有趣的实例：处于边缘弱势群体的个人如何用话语温柔的颠覆了权力中心。
<br>
<br>另外，如果我们抽离了其中的同性恋因素，里面的爱情让人心碎。从这个意义上说，王小波又是一个言情高手。试举多例：
<br>
<br>“小史收到了阿兰寄来的书，心情非常的兴奋。他的心脏为之狂跳，脸为之涨红，手也为之颤抖；他不愿呆在办公室里让别人看，所以跑了出来。这种心境我们称之为爱情。他仔细回避这些目光，走到公园的一个角落里。这里有一把长椅，一年之前，阿兰就坐在这个椅子上。此时此刻，小史也坐在这个长椅上，拿手遮住自己的脸。阿兰离开他已经有一段时间了，他看不到他，摸不到他的身体，嗅不到他的气味，但是他寄来的一本书却能使他如受电击。这种感觉从未有过。小史自己也说：这就是爱情吧。/ 在火车就要开走时，小史感到了一种无名的冲动，他开始从骨头里往外爱阿兰。在两个女人的注视下，他总禁不住伸出手来，要触摸他。在这时做这样的事，显然是不可以的。越是不可以的事，越想要去做，这种事情人人都遇到过吧――他就是在这时爱上了阿兰。这就是说，他不但承认了自己也是个同性恋者，并且承认了自己和阿兰一样的贱 / 现在他对着小史的相片，想到这些事情，可以发出会心的微笑，但是在当时却不能――因为他正忙于承受小史的爱。所以，阿兰以为，爱情最美好之处，是它可以永远回味。现在他在回味这些的时候，并不觉得自己是贱的。”
<br>
<br>作为阅读者我不由得为这些描写而心醉。这就是一篇好小说的元素，带给你震憾的视觉冲击与目眩神移的美，这种感觉在看《洛丽塔》及《白痴》时我也同样得到过。而坏小说则无趣又缺乏想象力，譬如卫慧的《上海宝贝》，一看就是闭门造车的拙劣的产物，有的闭门造车的东西比真实的存在还要美上一百倍，可惜卫小姐的小说却正好相反。
<br>
<br>《东宫西宫》中还有一段对性角色的描述，非常好：
<br>
<br>“阿兰说到这些话时，非常的女气，而且柔媚。这使小史感到毛骨悚然。但是阿兰讲这番话时反背着手，跷着腿，就如一位淑女，这样子又有些诱人之处。所以他皱着眉头说道：你丫到底是男的还是女的。阿兰说：这不重要。当你想爱的时候，你就是男的，当你想要承受爱的时候，你就是女的。没有比这更不重要的事情了。阿兰举出和那位不知名的小学教师的爱情作为例证。如前所述，那天夜里，在乡下的黄泥巴房子里，小学教师说道：你对我做什么都成之后，阿兰就热吻他，请他平躺在床上，吻他的胸口，肘窝，颈下；爱抚他，使他平静；在不知不觉之中，把做爱的主动权归还给他了。他自己说，那天晚上，开头的时候他想要爱，但忽然感到柔情似水，就转为承受了。你既可以爱，又可以被爱，这是世界上最美好的事
<br>情。”
<br>
<br>我最喜欢的是《黄金时代》里真切而不带有任何矫饰的爱情。陈清扬和王二在一个最缺乏人性的时代相爱，并因此遭受了伤害。在那里，小资们矫情的抚摸没有了，各种物化的符号没有了：
<br>
<br>“陈清扬说她真实的罪孽，是指在清平山上。那时她被架在我的肩上，穿着紧裹住双腿的筒裙，头发低垂下去，直到我的腰际。天上白云匆匆，深山里只有我们两个人。我刚在她屁股上打了两下，打得非常之重，火烧火撩的感觉正在飘散。打过之后我就不管别的事，继续山上攀登。陈清扬说，那一刻她感到浑身无力，就瘫软下来，挂在我肩上。那一刻她觉得如春藤绕树，小鸟依人，她再也不想理会别的事，而且在那一瞬间把一切都遗忘。在那一瞬间她爱上了我，而且这件事永远不能改变。”
<br>
<br>原来爱情并不只存在于小资们出没的酒吧里，爱情也不是一杯调节情绪的卡布基诺。
