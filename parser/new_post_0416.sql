<br>4　第四例：滴泪痣……
<br>　　现在，我们终于看到一个真正的爱情故事：李修文的长篇小说《滴泪痣》（《收获》长篇专号&#183;春夏卷）。但为了让故事发生，“我”和那个叫蓝扣子的女孩儿先得离乡去国。这是“东京爱情故事”，它发生在两个中国人之间，“我”在日本留学，蓝扣子则是流落东京的“黑人”，没有护照、没有身份。这似乎还不够，李修文又分别抹除了他们与故土最基本的联系：“我”是孤儿，蓝扣子呢，父亲死了，母亲找不着了。
<br>　　于是，在我们的生活之外，在远方，两个孤独的同胞相爱了。他们如同两只失群的鸟，周围是意义不明的茫茫世界，千山万径中他们偶然相遇，他们在对方脸上认出了那个神秘的、宿命般的印记：滴泪痣，他们的眼角永远悬着一滴泪。
<br>　　―――尽管我是个冷静的专职读者，读小说时通常不动感情，就像医生不对躺在手术台上的病人动感情，但读《滴泪痣》时，我还是滴了几行泪的。我对自己说，我喜欢这部小说，但这跟眼泪没什么关系，我不能根据阅读时的排泪量来判断一部小说，所以擦干了眼泪，我就开始琢磨我喜欢它的原因。
<br>　　原因一，我喜欢蓝扣子。这没啥不好意思，爱情小说必须让男性读者爱上女主人公。蓝扣子有极端的欢乐和活力，也有极端的忧郁和暴烈。林少华说她有点像《挪威的森林》中的直子，说得是；她们都是“动无常则，若危若安；进止难期，若往若还”（曹植《洛神赋》），她们都“不讲理”、难预测，这使爱她们的男人激情澎湃而又虚弱不堪。但李修文的蓝扣子更具体、更有质感，直子在我的记忆里像个飘渺的影子，而蓝扣子则带着她血液充盈的身体奔跑。
<br>　　这就说到了原因二：身体。这在当今中国文学中是个敏感的词，它照例会引起过敏反应，要么是一听“身体”马上作撇清之哂笑，好像他的身体不是他的；要么一提“身体”就变成了哥伦布，好像那是他刚发现的新大陆。关于后一种，电影导演波兰斯基曾在自传中讲过一件类似的事：此公少年时初次自慰，心中狂喜，断定此事是他“发明”的，忍不住要昭告天下，谁知一说出去，发现此事竟是人人皆知，遂大沮。
<br>　　我们活在21世纪，我认为我们在谈论文学时似乎不必煞有介事地争论“身体”有无之类“天真”的问题。在《滴泪痣》中，感官和欲望都是充分在场的，这是爱情，当然也是身体的狂欢。但是，李修文在其中最为激越的段落暗自设置了一个主题：束缚，我们安居于身体，同时也被身体束缚。这在爱情体验中恰恰表现得最为尖锐，令人伤痛：“上帝安排两个人在人海里相遇了，浮沉了，辗转了，何苦不让他们就此合为一人，何苦还要让他们各自拥有自己的皮囊？比如此刻，我只愿和扣子一起化成一堆粉末，被狂风席卷，被大风冲刷，被蚂蚁吞食，直至消散于无形；但是，世界还是这个世界，夜晚还是这个夜晚，到头来，我们还是端坐在夜之世界里的两个人。只能是两个，不是一个。”
<br>　　这段话引在这里显得比较矫情，人在爱情中的话嵌在日常言谈的上下文中通常不太谐调。但这种从身体中挣脱出来的冲动和身体一样确凿，一定要把其中一面捂住其实都是作态。李修文不过是自然地同时面对人类生活两个相互联系、相互冲突的基本事实，而这种“自然”在今日的小说写作中居然很难做到。
<br>　　另外一件很难做到的事是认真地、老实地写小说，这正是我喜欢《滴泪痣》的第三个原因，它写得认真、老实。我说的“认真、老实”不是笨，《滴泪痣》不笨，它飘洒灵动，像一只飞跑的狐狸。我所指的是李修文在这本书中展现的专业精神和专业技能：当他让我们爱上蓝扣子时，他依靠的是生动细致的性格刻划，他丰盛地、如诗如画地描绘那异国的风景、季节和城市，叙事的节奏像流利的舞步，紧密、迅疾，结构匀称、严整，对话活泼、传神……
<br>　　―――我像是在为一部平庸的小说撰写老生常谈的评语，说来说去都是小说艺术的传统要素。在我们这里，小说写作通常是从对这些要素的蔑视开始的，我们认为我们必须写出不寻常的东西，所以无须守规矩。结果呢？不寻常的东西很少，却有了大批手艺稀松而自命不凡的“业余”小说家。
<br>　　是的，《滴泪痣》是“平庸”的小说，我不认为它有什么不寻常的意义。但它也是一部近于完美的小说，李修文在此表现了一个小说家训练有素的技艺和才能，他甚少失误，准确地达到了目的―――我们看到了纯粹的、合于“海誓山盟”的古老梦想的浪漫爱情。
