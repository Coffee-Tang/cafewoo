<br>3　第三例：姐是水来郎是鱼？
<br>　　读叶兆言的中篇《李诗诗爱陈醉》（《作家》第5期），忽然想起黑陶的《吴谣：情歌》（《山花》第4期），文中说道：“吴地情歌中异于他地、值得探讨的一点是，在恋爱时，女性一方往往表现出较为强烈的母性心理。”“在其他地区的情歌中，女性一般以‘妹’的柔弱者形象出现，而在吴地情歌里，女性则多用‘姐’、‘姊’的口气自称，对男方行尽照顾、关切之能事……一句‘姐是水来郎是鱼，鱼水时刻不分离’，则可以算是吴地恋爱女性母性心理的形象透露，姐是水郎是鱼，而非通常的姐是鱼郎是水，一种女性的包容欲望在此毕现。”
<br>　　叶兆言也是吴人，不知是否听过此类民谣，但当他写李诗诗这个女人时，“姐是水来郎是鱼”的曲调在暗自回响。
<br>　　李诗诗爱陈醉，这种爱是母性的、包容的。这种包容几乎是无限的包容。那个名叫陈醉的男人一直在如鱼得水地折腾，从一次婚姻到下一次婚姻，从一个女人到另一个女人，他欺骗、背叛李诗诗，但每到危难时刻，又会一头扎进李诗诗的怀抱，好像李诗诗是他的娘，无论怎样都没什么不好意思。
<br>　　但李诗诗还是爱陈醉。当然，爱到最后，她感到了某种幻灭，这时陈醉已经折腾老了，差不多就要在她身边消停下来了，“现在，一生追求的幸福似乎就在身边，伸出手去仿佛就能抚摸到，李诗诗突然有些找不着北。她突然失去了生活的目标，像一个断了线的风筝，不知要飘向何方。”
<br>　　―――我无意评论李诗诗的生活，我感兴趣的倒是叶兆言怎样讲故事。现在这个小说家说：我要讲一个故事，关于爱情的。为了让我们信服，小说家采取了一些措施。
<br>　　措施一，落实在题目和人名上。“李诗诗爱陈醉”，这是标准的主谓宾句式，它表明“爱”这个动作是单向的，李诗诗爱着，陈醉被爱或不爱。李诗诗不能是东西、陆涛笔下的那种类型的妻子，她的爱必须超出日常生活尺度，几乎是纯粹的精神，否则，“爱”就不能成立。
<br>　　所以，这个女人叫“李诗诗”，一个“诗”还不够，还得加倍；而“陈醉”就是“沉醉”。加倍的诗而且还爱沉醉，这是无可救药的不现实。
<br>　　措施二，是拖时间。这个故事绵延四十多年，从上世纪50年代直到“现在”，这意味着什么？意味着这个故事、这种爱情只能在过去、在历史的纵深中汲取足够的说服力。如果你把它压扁、熨平，摊在“现实”的平面上，你的读者就会鼓噪：哪有这种事儿呀，这怎么可能？
<br>　　―――所有这些构成了限定条件，这是单向的爱情，这是“不现实”的爱情，然后，叶兆言才能把这个故事讲下去。他与朱辉、陆涛和东西一样，感觉到了讲述爱情的困难，只不过他想努力去克服。
