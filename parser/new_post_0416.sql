<br>5　“浪漫”的可行性
<br>　　2002年5月，我在小说里看到的惟一一段“纯粹的”爱情发生在东京，这多少有点令人困惑。为什么不是北京或上海或广州呢？李修文毕竟是一个中国作家，他写的也是中国人。当然，你可以说，纯粹的爱情太纯粹了，超凡脱俗，它只能发生在离我们很远的陌生地方，所以浪漫主义传统中本来就包含了对异域的向往和肯定；但是，《挪威的森林》呢？那些令无数中国男女为之痴迷的日剧韩剧呢？不会是为了对华出口而专门制造的吧，那些故事却发生在东京或汉城。
<br>　　显然，李修文也感到了某种困难，他觉得不能把故事放在我们熟悉的生活背景中，他非得把人物送走，否则就难以“浪漫”。
<br>　　一般来说，任何地方的生活都是不浪漫的，都琐碎平凡，任何地方的男女都相互爱着，也都在欺骗和背叛，这就是广大人间，处处并无不同。生活是重的，浑浊芜杂，它不轻盈不纯粹。但是，人在生活的同时也在想象生活，人的主要乐趣之一就是你可以在想象中飞起来，对生活的无限可能性作出选择：你将遭遇一场“凄婉美丽”的爱情，或者手里这只股票狂升不已……
<br>　　梦想也许与生活的现实无关，但却与生活赖以运行的价值和意义有关，你梦想发财，因为你知道在你的生活中“金钱”能够衍生一系列意义，你将是“成功”的、“幸福”的，像广告片里所暗示的那样；但当你梦想浪漫的爱情时，当你在想象中体验这种超凡脱俗的激情时，你能够在你的地面上确认它的意义吗？比如“忠贞”？比如“永恒”？
