<br>2　第二例：打着伞，猜到底
<br>　　2002年，当你倾听爱情故事时，你通常会先感到“阵阵寒意”。在陆涛的长篇小说《我们都是伞下人》（《大家》第3期）的开头，丈夫死了，妻子打着雨伞，四处查找丈夫的情人，她不知道那女人是谁，但她断定有这么一个女人，她要把丈夫的骨灰盒送给她，她认为自己无权收留这个男人。
<br>　　接着，据《大家》编者说，“伞下踏着冷艳的黑色旋律而来的，却是另一曲凄婉美丽的爱情”，说白了，就是她爱上了另一个男人。小说我没有读完，不知如何凄婉如何美丽，我感兴趣的是在雨中寻寻觅觅的场景，这里至少包含了两层意思――其一，在我的想象中，死去的丈夫就是朱辉笔下那个“造雨”的男人，陆涛扩展了他的困境：死亡也未能澄清他的伪生活：他爱谁，谁爱他？“它”（骨灰）属于谁，谁安顿“它”？这是对一个人生命意义的最终发问，但没有答案。所以你不得不感叹这个男人生前竟活得像这个时代一样生龙活虎、兴致勃勃，他从虚无中汲取了多么旺盛的精力。其二，如果那位妻子有幸遭遇另一场爱情，那么，这新的爱情是在一片废墟上重新建立起来的。也就是说，她已经体验了背叛、冷漠、虚假，她得由此出发，再找回和确认忠贞、激情和真诚。
<br>　　―――尽管我们相信野火烧不尽，春风吹又生，相信人的生机和生活的弹性，我们反对一朝被蛇咬，十年怕井绳，但你还是得承认，这件事非常困难。
<br>　　在2002年，小说家们意识到了这种困难，他们发现讲一个令人信服的爱情故事几乎不可能，难度不仅在艺术，更在于生活，生活未能为忠贞、激情、真诚等等与爱情相关的价值提供足够的、令人信服的证据。
<br>　　于是，小说家们活像受骗而又醒悟的妻子，用猜忌的目光打量着一对对男女，他们如同侦探，对人性的看法阴郁，不抱幻想，而事情的发展总会证明他们是对的。
<br>　　――东西在《收获》第3期讲了一个《猜到尽头》的故事，妻子怀疑丈夫有了外遇，她机警地追究一处处蛛丝马迹，但丈夫每次都有力地证明了自己的清白无辜，他真是冤哪，老婆是个过敏多疑、无理取闹的泼妇。这女子终于不好意思啦，她终于下决心，相信丈夫，相信爱情。
<br>　　但是，东西揭开了底牌：妻子其实没有猜错，每次都没有猜错。
