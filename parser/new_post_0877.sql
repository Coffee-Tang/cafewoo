1 如果时间倒退五年

如果时间倒退五年。

我觉得我应该按照自己最初的决定，去报考幼儿师范。做一个幼儿园老师，每天和那些柔软透明的小生物在一起。他们无邪的笑容像阳光一样纯粹。他们清澈的眼神像雪山一样遥远。

