<br>　　如果时间倒退五年。 
<br>
<br>　　我觉得我应该按照自己最初的决定，去报考幼儿师范。做一个幼儿园老师，每天和那些柔软透明的小生物在一起。他们无邪的笑容像阳光一样纯粹。他们清澈的眼神像雪山一样遥远。 
<br>　　我要在他们躺在绿色的小木床上午睡的时候，一个人坐在窗台边的地板上，看樱花树在风中摆动。黄昏的雨天，最后一个孩子被母亲接走，然后在空荡荡的教室里弹钢琴。 
<br>　　可以在一个小城市里，一直这样平静地生活下去。 
<br>　　我要嫁给那个高大英俊的男人。他的睫毛就像华丽而伤感的威尼斯。我们曾经相爱。我要在他的身边，不离开他。告诉他，我愿意和他相守到老。 
<br>
<br>　　ROSE在EMAIL 里要我用两百字写一篇“倒退五年” ，在半小时之内发给她。 
<br>　　她常有诸如此类的要求，因为她是我的编辑。我所有的爱情小说都交由她处理，然后每个月去邮局支取她的杂志社寄给我的稿费，用以维持我的生活。 
<br>　　这些钱可以缴付房租，水电煤和电话网络费用。每周一次去超市采购，在冰箱里放上脱脂牛奶，鲜橙汁，燕麦，苹果，新鲜蔬菜和鸡肉……还有出去逛街泡吧。在咖啡店里喝双份ESPRESSO. 给自己买新款香水和粗布裤子。 
<br>　　ROSE在北京。我在上海。我们一直以EMAIL 联系，从未见面或致电。我不知道她的性别，只能暂时认定她为女性。也不知道她是否比我年轻，但这些都已经不重要。有时候身边很多熟悉的人，他们却只如空气般的存在。 
<br>　　请看她在我发出EMAIL5分钟之后给我的回复。亲爱的VIVIAN，我如此依赖你，你好象在我隔壁办公，而且从不曾让我失望。 
<br>　　我微笑。此时已过深夜11点，别人看完电视，许是打着哈欠洗脸刷牙准备上床。而我一天的工作，刚刚开场。窗外的天很蓝很深，五月的夜风清凉里面已经有醺然的暖意。光着脚坐在大藤椅上，一杯泡得浓黑的咖啡，红双喜的特醇香烟，还有空白的电脑文档。我的工作就是在寂静的空气里，听着自己的手指敲击在键盘上，直到把眼前的那一面空白用黑字填满。 
<br>
<br>　　我是以卖字为生的女子。在我25岁的时候。 
<br>　　如果时间倒退五年……也许依然只能如此。 
