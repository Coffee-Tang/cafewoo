可以在一个小城市里，一直这样平静地生活下去。

我要嫁给那个高大英俊的男人。他的睫毛就像华丽而伤感的威尼斯。我们曾经相爱。我要在他的身边，不离开他。告诉他，我愿意和他相守到老。

ROSE在EMAIL 里要我用两百字写一篇“倒退五年” ，在半小时之内发给她。

