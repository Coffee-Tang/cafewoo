<br>　　4 有些人的生命是有阴影的 
<br>
<br>　　我们租下的那套老房子很陈旧。房间光线阴暗，前后院子里种了大片茂盛的橘子树，叶子暗绿得发亮。还有鸢尾，雏菊和玫瑰。绢生把她的羊齿放在卫生间的窗台上。那盆小植物长得很野性。卫生间铺洁白的马赛克，虽然狭小但是干净。可以在里面喝酒，发呆，洗澡的时候收听音乐。 
<br>　　露台的铁栏杆已经完全发锈。有一张厚重的红木雕花书桌，手抚摩上面冰凉光滑，散发隐约的木头清香。 
<br>
<br>　　我的同居伙伴。深夜她光脚在地板上走来走去，散乱着海藻般的黑色长发，湿湿的脖子。像在地穴里穿行的寄生昆虫。当我在电脑前抽烟和写作的时候，她坐在地板上看卡夫卡。 
<br>　　周末的深夜，挤到我的床上，一起看电视的经典黑白老片回放。然后喝威士忌加冰块，配新西兰起士。常常会看得流泪。红着眼睛在那里抽泣。电影打出了END ，于是狠狠咒骂一句，愤然地进卫生间洗脸。 
<br>　　她是那种会把手指甲剪得短而干净的女子。喜欢奢华的黑色蕾丝内衣。并且果然是没有宠物和男人。 
<br>　　一早起床。洗澡，在衣橱里选衣服。她的衣服排列在熏衣草的芳香里，丝缎，纯棉，细麻，麂皮等所有昂贵而难以服伺的天然料子，颜色大部分为黑，白，暗玫瑰红。细细的蕾丝花边，精致的手工刺绣，大红大绿的民俗风情。她的生活极尽奢华。但我知道这里面的缺陷。这所有的一切，都是她以自己的工作获得。 
<br>　　一个没有男人可以依靠的女人。公司里的工作忙碌，常日夜颠倒地加班。有时候打电话过去，话筒里始终是杂乱的声音，电脑，电话，传真，打印机……每天喝泡得浓黑的咖啡来维持睡眠不足的体力。商业社会，不进则退，一旦失去被利用的价值，就是沦落。绢生在销售界的名声刚刚有好的开始。我相信这是她以天分获得，她是散漫的人，性情纯真然而并无上进心。 
<br>
<br>　　我曾去参加过她公司的庆祝酒会。绢生的销售业绩做得如此之好，众人均过来和她招呼寒暄。 
<br>　　她端着酒杯站在她的外籍老板旁边，穿黑色丝绸长裙，肩上的细吊带均为水钻，长发柔滑，胸前别一小束风信子。我看着她在人群里得体地微笑，身体微微有些僵直。可是她是能够控制自己的。 
<br>　　我知道。这是她的外壳，她柔软纯白的灵魂躲藏在里面，小心翼翼地爬行。 
<br>　　半夜她回家。踢掉鞋子先开始洗澡，在卫生间里一泡就是几个小时，在里面香薰沐浴，看小说，听收音机，不亦乐乎。这是绢生放松的时候。我亦知道她在公司里为工作和同事争辩，回来后因为气愤胸痛难忍。 
<br>　　有时候独自衣锦夜行，涂发亮的唇膏，抹了兰蔻的香水，花枝招展地出去。快凌晨的时候回来。手里拿着从超市买来的威士忌和大块起士。卸妆，洗澡，穿着内衣半夜看旧片，一个人坐在阴影里，对着威士忌和香烟。长长的头发披泻在胸前，眼神疲倦。 
<br>　　大部分人的生活未必象我这样目的明确，因为我知道如果不写作就无法生存。而绢生，她是可以有选择的机会。自然她也曾对我说起那些和她在一起的男人。她与他们吃饭，跳舞，看电影，深夜回家，却始终只有一个人。她从不带男人回家或在外留宿。亦不要他们买东西给她。吃饭也要坚持AA制度。因为不爱，所以分得很清楚。 
<br>
<br>　　为什么你似乎不是很快乐呢。我问。 
<br>　　他们想玩的，我未必想奉陪。我想玩的，他们又玩不起。 
<br>　　玩不起吗。 
<br>　　比如诺言，比如责任，这是比金钱更奢侈的东西。她笑。我是很传统的女人，VIVIAN. 我要一个男人养我，然后我给他做饭洗衣服生孩子。就跟两千多年来中国女人做的事情一样。 
<br>　　谁要养你。买条裙子就要一千块钱。 
<br>　　那是我花自己的钱。如果他养我，扯块棉布自己做就行。 
<br>　　这未必能让你感觉安全，绢生。 
<br>　　我现在的感觉更不安全。她说。 
<br>　　谈话结束。绢生独自坐在黑暗里，继续看片子，喝酒，抽烟，她可以把这样的状态持续到凌晨天亮，然后穿上衣服和鞋子，拦出租车去公司上班。一个失眠的女子，可以若无其事地出现在公司里，然后冷静地开始她一天的工作，和同事开会，讨论，打电话，应对…… 
<br>　　半夜她放王菲的《但愿人长久》，这样哀怨的靡靡之音，苏轼的词在王菲的唱腔里让人听着难受。她走来走去，哼着里面的句子，一边轻轻抚摸自己的长发。 
<br>　　我从来未曾把绢生当作普通的女孩。 
<br>　　有些人的生命是有阴影的。 
