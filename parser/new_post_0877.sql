<br>　　3 时间不会走了 
<br>
<br>　　那天下雨，阴冷潮湿。春天缠绵的雨季，使本来已经污浊不堪的城市空气更加粘稠。 
<br>　　我早到20分钟，独自站在大厦门口避雨。作为高级的写字楼，里面汇聚多家著名的集团公司。 
<br>　　现在已到下班时间，旋转门不断有人进出。很多人衣冠楚楚，然而神情困顿。我已经过了很多年没有工作的生活，不太清楚工作的意义和目的。 
<br>　　18岁的时候我去街头冷饮店打工，每天夜晚工作三个小时，推销冰激凌兼收钱送货，月底能拿到几百块钱。迫不及待地去买看了整整一个夏天的碎花裙子…… 
<br>　　毕业以后，进入大机构。很快辞职。 
<br>　　从此不再有工作。多年的无业生涯，很快使我变成一个邋遢的女子。神情时而萎靡时而激越无比。 
<br>　　绢生出来的时候，怀里抱着一盆绿色的羊齿植物。她很瘦，眼睛漆黑。神情冷淡的时候像沧桑的的妇人，笑起来则变成甜美的孩子。大抵只有内心纯真而又经历坎坷的人，才会如此。她穿织锦缎的暗红牡丹短旗袍，下面是破洞的牛仔裤和褐色麂皮靴子。一头海藻般的长发，光泽明亮。 
<br>　　她的名贵靴子一脚就踏进了泥泞里面。 
<br>　　平时喜欢养花？ 
<br>　　不。今天在花市看到，非常喜欢，所以想买下来。她从包里拿出一盒烟。她说，你抽烟吗。 
<br>　　我看到她手里的烟，是一盒红双喜。8 块钱的特醇。我笑。两个人互相低着头点燃了烟。她手里的绿色大叶子轻轻碰在我的皮肤上。 
<br>　　是在接下来的一秒钟。我刚刚直起身体，吐出第一口烟的时候。 
<br>　　那个男人突然掉落下来。他没有任何声音地随着犀利的风速下滑，撞击在前面停留出租车的宽敞空地上。就像一只沉重的米袋子。爆裂的是他的脑壳。白色的红色的液体混杂在一起飞溅。 
<br>　　雨下得不大，他的白色衬衣被泥水包裹。 
<br>　　我惊叫一声。绢生的手迅速地控制住我的肩，一把将我拉到后面。 
<br>
<br>　　我们目睹了此后的过程。保安报警，警察封锁现场，众人围观。死者是某广告公司的副经理。那个男人因为涉嫌贿赂和贪污，已经被调查了一段时间。绢生和我坐在台阶上，看着那具破碎的尸体被装进黑色的塑胶袋里拖走。 
<br>　　他的一只鞋子还在那里。绢生说。 
<br>　　一只黑色的男式皮鞋，孤零零地掉在花坛偏僻的角落里。 
<br>　　不知道他在丧失思维之前，是否会后悔自己穿着鞋子。如果光脚的话，去天堂的路途会走得比较轻松。她说。 
<br>　　我不明白她为什么会笑。这样诡异的笑容。我记得那个男人的脸，是像突然伸过来的手一样，出现在我们面前。他的眼睛睁开着。空白的眼睛。 
<br>　　你害怕死亡吗。她看着我。小时候，家里死人，我站在棺材旁边看，不明白一切为什么可以这样完美地停顿。 
<br>　　手指不会动了，眼泪不会流了，时间不会走了。 
<br>
