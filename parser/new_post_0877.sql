<br>　　2 遇见绢生纯属偶然 
<br>
<br>　　很多女子的25岁，应该会有一个自己的家。即使是小小的家，只要放得下自己的一橱衣服和从小抱着睡的枕头，也会心安。有一个男人。临睡之前他的手指抚摸在头发上，可以闻着他脖子皮肤上的味道闭上眼睛。还会有一个孩子，从此这颗心就放在了身外，跟着另一个人晃晃悠悠。 
<br>　　而我的25岁。我单身。靠着一台电脑和数位杂志编辑的电子信箱生活，并养了一缸热带鱼。 
<br>　　那些美丽的小鱼，它们睡觉的时候也睁着眼睛。不需要爱情，亦从不哭泣。它们是我的榜样。 
<br>
<br>　　ROSE偶尔在EMAIL 里对我说，亲爱的VIVIAN，为什么你的爱情小说总是以分离告终，虽然我喜欢你的文章，但依然困惑不已……我给她回信，亲爱的ROSE，那是因为我曾经被很多男人欺骗，遭受种种劫难，心如死灰……一边打字与她调侃，一边笑着抚摸自己裸露在空气里的冰凉的脚趾。 
<br>　　爱情，那是很遥远的事情了。15岁的时候，和班里的男生恋爱。纯纯的恋情。冬天的黄昏，在自己的房间里，看着他的手笨拙地伸入到胸前，他的呼吸有柠檬的清香。还有他喀哒喀哒响的旧单车，坐在前面的横杠上，他的嘴唇轻轻贴在头发上。美丽的诺言让人看到海枯石烂……10年过去，如果再对爱情欢天喜地，执迷不悟，那才叫可怕。 
<br>　　我想我的生活估计是到不了头。 
<br>　　我所要的，只是一个人。能在我睡觉的时候，轻轻抚摸我的膝盖，把我蜷缩起来的身体扳直。 
<br>　　如果没有，那么一切继续。 
<br>　　虽然有时候我恐惧白雪茫茫般空洞的生活到不了头。 
<br>　　直到我遇见绢生。 
<br>
<br>　　遇见绢生纯属偶然，但非虚构。虚构是我文字里的概念，如果没有虚构，我就无法得到食物和住所，无法像任何一个正常的路人，行走在城市高楼耸立的大街上，即使不踌躇满志，也可以心定气闲。 
<br>　　我喜欢城市的阳光透过污浊的空气和阴冷的楼缝，轻轻抚摸在脸上。 
<br>　　我喜欢在吃完一顿丰富的晚餐以后，想起还可以去哈根达斯买一杯瑞士杏仁香草冰激凌。 
<br>　　自然有时候我的生活也会变得糟糕，比如在这三个月里，一共：抽掉30包红双喜，平均每三天一包烟。由于买烟的地点杂乱，常常抽到假烟。假烟带来的灾难是头痛和呕吐。可是独自在深夜的时候，它像一场往事，让人镇静，并带来泛滥。 
<br>　　逛了80次街。每天下午醒来，在深夜之前的这段空白，时间必须大量挥霍。坐车到陕西路，然后步行至淮海路。有时候只是坐在太平洋前面的石阶上，看着陌生人走来走去。然后在STARBUCK买咖啡。然后往回走。 
<br>　　泡吧50次。有2 次因为滥醉而爬到桌子上。5 次被人拖上出租车送回家。 
<br>　　约会过10个男人。无疾而终。 
<br>　　卖力地写作。写了40万个字，卖掉30万个字。 
<br>　　吃掉镇静剂3 瓶。 
<br>
<br>　　从冬天开始，我的生活就是这样。 
<br>
<br>　　春天到来的时候，我觉得应该找个人同居。仅仅是想更温暖地生活，迎接这个美好的季节。 
<br>　　因为我要努力写稿，争取得到更多的享受，包括我向往已久的去越南和泰国的旅行。或者还可以更远一点，印度或者埃及。我的地点和其他人有所不同。 
<br>　　我决定搬到离市区较近的地方。我在网络上登了一则征求室友的广告。我们可以分担费用。 
<br>　　失眠的时候还能找到一个人说话，即使仅仅是听到彼此发出的声音。万籁俱寂，仿佛失聪。可是我有因为独处而过分灵敏的听觉。 
<br>　　卧室分开。客厅，厨房和卫生间共用。 
<br>　　我留下自己的EMIAL 和电话号码。三天以后收到回音10条。只有一条是对方打电话过来。 
<br>　　你好，VIVIAN，我是绢生。她说。 
<br>　　她的声音仿佛16岁少女一样的清醇。外省人。在一家德国电器公司做事。 
<br>　　我记得我们的对话是这样的。 
<br>　　我说，你现在住哪里。 
<br>　　北京西路。 
<br>　　那里地段很好。 
<br>　　但是晚上找不到水果摊和有热鱼丸出售的小超市。 
<br>　　我会尊重你的自由。包括养宠物或者男人。 
<br>　　前者我没有时间。后者我没有机会。她笑。 
<br>　　这是我喜欢的女子。聪明有流转，说话简洁至极。 
<br>　　我们决定一起去看房子，房子的主人是一个老教授，准备去德国两年，所以想把房子租出去。 
<br>
<br>　　我们约在北京西路。 
