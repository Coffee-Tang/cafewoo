“阿树，我们上医院去吧。你病了。”菲菲蹲了下来，深深地看进她的眼睛。昔日那双明亮的大眼睛，已渐渐黯淡，失去了往日的光彩。潮湿着，越来越厉害，直到两行泪流下来，毫无掩饰地。菲菲也忍不住地酸了酸鼻子。

深深的绝望。

再大的伤口终归要愈合。再深的痛也终有平复的一天。阿树平静地接受了医生的说法――过量服用止疼片产生幻觉。但心底深处始终相信小米是回来过的。那明媚的眼，动人的唇。她用力地克制着泛滥的疼痛。

她知道是牙科诊所的消毒不过关，发炎了她也没吃消炎药。进一步感染，就发烧了。但她有点感谢那位年轻的牙科大夫。却也没有再去的打算。

她告诉菲菲要离开。却没说什么时候，去哪里。因为她不知道如何说再见。

她突然有点明白小米的无言离去。

