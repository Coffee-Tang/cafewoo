忽然间我恐惧起来，我双手遮脸，并从指缝中看他，所有的鹅卵石和花朵整齐地让出一条路，路中央不知何时铺上了一张望不到边际的红毯，他的脚步在上面一下一下压出一个一个雪白的脚印。

那是久经沧桑的标记，走到哪，都带来寒冷。

睡美人的婚礼我有参加，她非常非常的美丽，就像童话书里所说的明哞皓齿。她抱着一只吉娃娃，灵黠地眨着眼睛。

荆棘王子接过吉娃娃，把它放到花篮里，它开始“嗷嗷”的歌唱，嘶哑着喉咙。

那场婚礼已过去几百年，我差不多将他们给忘了。

我用力地将他抱住。我开始害怕他会碎掉，像一个盛满水的杯子碎掉一样，都是因为承载了太多眼泪，所以只能碎掉。

你从来都不知道那种疼痛的感觉，你一辈子除了落地啼哭之外都没有哭泣过，皇宫里的人们将你照顾得很好，舍不得你看见世间的悲惨。你不知道我总是在深夜躲在午夜迪吧的通风口里，听Linkin Park的音乐。我喜欢他们瘦削的脸，气质满满的轮廓，和你很像。但你的脸较为白净，没有磨难过的痕迹，我想你肯定也从来没有偷偷流过泪。他们也不叫Very，他们在为了生活努力，他们不是王子。

有个古希腊哲人说过“人只活一辈子”，你死了之后，我看着你的孩子长大，他的孩子长大，我看着他们如何如何遗忘了你。

Very。回过神来，我忍不住又在唇齿间琢磨你的名字。

荆棘王子肩膀颤了一下，也许他还记得你。但是他从来都不认识我，我不是你的结发妻子，我不过成全了你们。

我，我叫Eyki，你又是谁？荆棘王子看着我。

我是谁，谁能告诉我我应该怎么回答？人鱼？巫婆？神仙？我什么都不是，我连名字都没有，虽然有时候我唾弃它不过是个代号。

于是我不说话，我愣愣地和他对视，蝴蝶偶尔飞到我们之间，旋舞着又飞走。雨突然就“噼哩啪啦”往地上打，花白的脚印到处乱窜，商店突然热闹。

音乐又响起来，石子和花朵又开始疯狂的跳舞。

荆棘王子从他的风衣里掏出包装精美的烟盒，他大口大口的抽了起来。

我一直都在抽各种各样很呛的烟，到那个认识她的地方，看落日，看日出。没日没夜地除了喝水只有点燃手中的烟然后弥漫。

有时哭到无力会怀疑她是否真的存在过，但手指的触感如此清晰。

荆棘王子的声音沉闷而冗厚，过去我以为他和睡美人沉溺在幸福里，每个人都有不为人知的阴郁，正如你我这样。

我试图将胸口的闷热驱逐出去，却毫无办法。这几百年来，我未曾离开这片土地，它经历的灾难传播过的疾病我都尝过了，比你的死亡更让我难过少许。

你的死亡是我永远的疼。

每次想起我爱过你的心，我一睡就流泪，我的哭嚎让天空电闪雷鸣，每一道闪电都能直直地劈中我的伤口。

我希望它们能把我心口的回忆劈碎，如果能发出脆生生的声音更好，那样我会忘得更快。

我忽然流下眼泪，这么些年我发现只要我一哭天就开始下雨，如果我哭得久一些就会有冰雹或大雪，它们很凛冽却不打在我身上。

它们有我最爱的白色和透明，它们是我早期心脏的样子，坚忍易碎。

我流泪天又下雨了，荆棘王子不可思议地看了我一眼，也许他认为是我让天下的雨，也许他又以为自己猜错了所以他转过了头去继续抽他的烟。

他的烟雾有和雪等同缠绵的气质，在掌心飞散开。

荆棘王子说他其实不幸福，我愣住了很久很久。

这里谁都说自己不幸福，人类很难知足的，我知道。知足的都是圣人，我是中间者，如果我能够想你不哭泣那我就是圣人。

我第二次抱住荆棘王子，他身上有温度证明他还是人类，可是他奇迹似的活了那么多年，真是奇迹。我可没那么幸运，我既不能变回人鱼也不能以人类的身份活着，我变成泡沫之后不久我就是游魂了，我的身体是冰冷的。

可是我的头发不停疯长。

小时候我的头发在接触到苔藓之后会长出一大截，那时候我16岁，我每天躲在石头后面偷偷看你，石头上有很丰富的苔藓，于是我的头发长了许多。

偶尔我顶讨厌那些东西缠住我的胸衣，这并不能阻止我注视你，晴天你会出海，我摸清了你游船的路线，阳光常常明晰地勾勒出你的轮廓，比我的姐姐们还幽雅。

我迷恋你直到有个巫婆愿意拿毒药换我的声音。

荆棘王子继续讲起他的故事，我猜想故事一定很凄婉绵长，因为这是一个回忆了几百年的故事，活着的人才会回忆，死去的人多幸福，什么都不知道。

纪念不过是一个借口，我真正渴望再见到的，其实是那些纪念品。荆棘王子说，他去看那些他们一起看过的风景其实只是借纪念的借口去看看那些他也喜欢过的东西。

那些东西有爱人的影子，这样的道理我懂。

当年睡美人的国家面临灭亡的危机，荆棘王子似乎不太愿说这些没人知道的往事，不过他还是说了，毕竟往事埋了几百年这样子很痛苦。

睡美人的父亲为了挽救国土的灭亡才故意让他的女儿装死，所谓的巫婆不过是他请来的佣人，在睡美人沉睡的百年里，不知有多少个王子去亲吻过她的芳泽，他们都因为睽视她的美丽而上当受骗，他们进入荆棘林之后就被国王的杀手砍死，钱财被抢光了，外面的人都以为是被荆棘林困在里面而死亡。

我安静的听荆棘王子说着，我看到他有小小的停顿，因为有些哽咽。

他的烟抽完了，他又掏出一根雪茄。这次他会说很久吧，我想。

他的雪茄是我常看到的那种，美国很多男士们抽它，他们的身边都有香车美女。

那么你呢？我问。

我幸免于难，我在路上看到一小块没有处理干净的血迹，再往前走我又发现几个没有埋好的尸体，我是我们王国最聪明的人，我一下子明白了一切，但是我不死心，我必须要挽救更多在我之后慕名而来的年轻人。

但是你成功了不是吗？我又问。

我躲过了许多机关，我也打伤了很多杀手，我终于负伤累累，在我即将靠近城堡的时候我差点就死在一个黑衣人刀下。于是我大喊：如果你们让她嫁给我，你们将会得到我的全部财产。

他们就为此放过了你？我打断了他的话。

他的雪茄只剩半截了，可是我预感这故事还要讲很久。

他们是放过了我，他们也让我见到了睡美人，我亲吻了她，她多少是有些爱我的，她答应嫁给我。我自然很开心，因为她是我见过的公主里最美丽的一个。

我不计前嫌的原谅了她和她的家人，她和我回到我的国家，我的父亲以我为傲，但最后他还是被他的漂亮媳妇杀死了。我才知道美丽的女人都狠毒。

她杀我父亲的原因不过是因为他跟我说该生一个继承人了，就因为如此，因为她不喜欢孩子，所以我跟她其实也只到接吻的程度而已。我尊重她我爱她，真的爱她。

荆棘王子哭了，男人的眼泪很大颗，我用我的手去接住它们。

后来呢？后来？我问他。

她在外面养了一个情夫，那男人并不比我英俊，应该说是奇丑无比。

03、不说话了代表心里有某些东西在挣扎……

第二日清晨，阳光充溢。整个夜晚我和荆棘王子沉默坐着不语看美国的一个小角落，所谓的不夜城不过是为了满足不喜欢讲话的人们。

