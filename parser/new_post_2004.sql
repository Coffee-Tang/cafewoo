10．故事的结局毫无悬念。我拿到了去加洲的飞机票，因为我已经答应David，我要跟着他嫁到美国去。

我们既然那么巧合地相遇了，我刚好在他梦想中的女孩子存在的城市，而他刚好来自我梦想中的城市，这段缘分不要错过的才好。

而且，我是如此眷恋他任何时候都可以伸向我的温暖的大手。正如他是如此眷恋我莞尔一笑时水灵的双眸。

加洲。我终于带着我的幸福，朝你奔来了。

