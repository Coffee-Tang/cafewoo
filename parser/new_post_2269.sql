>>>> 这是一篇非常好的文章，不同于平时的邮件，我知道很多人不喜欢看长篇文字，但还是希望你们可以看看这篇文字，想想如果当时是自己会怎么样？人性究竟是恶还是善？我们谁也无法说清。看看这篇文章，感觉一下自己的善恶吧。

>>>>

>>>> 在大家没看之前，问大家一个问题：

>>>> 如果你是你们文章的“行人”这个人，你会怎么做？

>>>> 如果你是“浪迹天涯”，你会怎么做？

>>>> 如果你是作者，你又会怎么做？

>>>> 请说真话。

>>>> ----------------------------------------------------------

>>>> 【一】

>>>> 我的生活一直都一帆风顺。

>>>>

>>>> 从小到大，我都不知道什么是坎坷，也不知道什么是困境。我读过的小学、中学、大学都

>>>> 是数一数二的名牌，甚至连幼儿园也是最好的。

>>>>

>>>> 大学毕业以后，我顺利地进入一家外资公司，三年后升任公司市场部经理。这份工作使我

>>>> 的生活质量和生活水平都提升到了一个较高的层次。在很多人眼里，我二十多岁，有车有房

>>>> ，出入高档专卖店，从来不为钱操心，这样的生活无疑是优越和令人羡慕的。

>>>> 但是，我一点也不快乐。

>>>>

>>>> 也许是因为自己的道路太平坦，就象一杯寡淡无味的白开水，总是令我感到无限空虚和不

>>>> 满足。我渴望有不同寻常的经历，渴望自己的生活充满激情，更刺激，更有色彩，总而言之

>>>> ，我不愿这样平凡地过完此生。

>>>>

>>>> 我没完没了地在网上猎奇，去各式各样奇怪的网站看些稀奇古怪的东西。我也常去专门讨

>>>> 论探险活动的论坛，在那儿我有一大帮哥们，我们发贴跟贴，互相肉麻地吹捧、不着边际地

>>>> 胡侃海侃，总希望能侃出一两件惊天动地的事来。

>>>>

>>>> 那天看到浪迹天涯发起五一探险活动时，我惊喜又兴奋，几乎是在他的贴子贴出来的同时

>>>> ，我就报了名。

>>>>

>>>> 浪迹天涯是老朋友了，我们常常在论坛里讨论诸如野营装备、探险之类的问题，彼此之间

>>>> 也比较熟悉。这次他联系了车子，准备组织一大帮论坛上的哥们在五一节期间徒步穿越一片

>>>> 原始森林无人区。那片原始森林在三百公里外的雪山附近，方圆几百平方公里，他的计划是

>>>> 穿越雪山脚下最狭窄的地带。由于那里的森林是依山而下，所以地形比较复杂，一路上必须

>>>> 要越过很多起伏的小丘、山涧，还得渡过两条小河。无疑，这是一次很有挑战性的穿越。

>>>>

>>>> 浪迹天涯的这个提议在BBS上引起了热烈的响应，可惜由于条件所限，最后确定参加这次活

>>>> 动只有我、行人和野山雀三个来自同一城市的网友。我们几个在网上都打过交道，因为大家

>>>> 都是一个地方的人，所以也特别谈得来。接下来的几天，我们四个一直孜孜不倦地反复讨论

>>>> 装备、准备工作和各种细节。几个外地网友也参与了我们的讨论，虽然他们无法参加活动

>>>> ，但还是忍住羡慕的口水，热心地为我们出谋划策。

>>>>

>>>> 那时我发现行人绝对是个悲观的人，他几次提到：虽然我们几个在论坛上都是朋友，但毕

>>>> 竟没有见过面，彼此间缺乏必要的了解。而且大家都是热度很高的理论家，说起来一套一套

>>>> ，可是实际经验一点也没有，全是纸上谈兵。他认为这会产生很多不确定因素，甚至存在一

>>>> 定的危险性。

>>>>

>>>> 我的天！不到二百公里，而且还是直进直出，我们甚至可以不带指北针，光看太阳和树叶

>>>> 就能走完，这也算危险？行人立即被淹没在一片唾沫中。

>>>>

>>>> 我是一个酷爱发白日梦的人。多少次，我幻想自己于千军万马中取敌首级；或有绝顶高强

>>>> 的武功，出生入死，除暴安良……然而现实中的我却不得不中规中距，上班得听老板的旨意

>>>> ，下班后也无所事事，只能看书上网，最多也就是去迪厅蹦腾蹦腾。生活是如此的不堪，可

>>>> 现在仅仅背上背包去穿越一片树林，都还要前怕狼后怕虎！我心中十分不满，而且也很看不

>>>> 惯这个无胆的鼠辈，那天我跟贴：我们带上装备，绕着二环路走七天怎么样？SB！

>>>>

>>>> 如果一次探险活动的一切都是太太平平的，那也就失去了它原本的意义。也许会有一些意

>>>> 外，会有危险，这又算得了什么？我们就是为这个去的。在我的设想中，这应该是一次危机

>>>> 四伏的旅程，种种危险和困难将会接踵而至，而我则沉着应对，用自己的智慧和勇气去解决

>>>> 一切问题。为此，我几乎翻烂了约翰．怀斯曼写的《生存手册》，还专门托朋友买了一把价

>>>> 值两千多元的Cold Steel的“小狗腿”砍刀。

>>>>

>>>> 我想当时的我已处于个人英雄主义极度膨胀的高度亢奋中。

>>>>

>>>>

>>>> 【二】

>>>>

>>>> 四月二十日，我们几个在一个闹哄哄的小酒吧里碰了头。

>>>>

>>>> 出来迎接我的人给我的第一印象是开朗大方、热情洋溢而且精力旺盛，绝对属于团队中的

>>>> 领袖型人物，这与我想象中的浪迹天涯完全吻合，我毫不费力就叫出了他的名字。然而令我

>>>> 尴尬的是，我很自信地把那个健壮挺拔，浑身黝黑的人当做是野山雀，另一个皮肤雪白、全

>>>> 身虚胖，看起来有点胆小怕事的家伙我却一口咬定是行人！于是只好干笑。

>>>>

>>>> 那天的碰头会的主要目的是让大家先见个面，彼此熟悉一下，以便行动时不至于太拘束

>>>> ，这也是行人唯一有价值的提议。短暂的拘谨后，我们的碰头会又恢复了网上那种不着边际

>>>> 、天马行空的气氛。两杯啤酒下肚，野山雀的话多了起来，俨然以户外运动专家自居。他夹

>>>> 七夹八讲了很多，似乎很深奥，在他所提及的很多术语中，我唯一记住的只有“真北”和“磁

>>>> 北”，而且到现在我也不知道是什么意思。反正当时我听得云里雾里，为了掩饰自己什么也

>>>> 不懂，我认真听着，不懂的也不问。看看其他人，都是一脸严肃，听得不住地点头，可他们

>>>> 的眼睛里全是一片茫然。

>>>>

>>>> 说实话，我并没有类似活动的经验，只有满脑子的《生存手册》和健身房里练出来的小块

>>>> 肌肉，我也不相信其他人会好到哪儿去。至于野山雀，他庞大身躯始终令我有些担心，我隐

>>>> 隐觉得搞不好他还会拖累大家。不过他讲得倒是挺精彩，最终使我们相信他还是具备一定素

>>>> 质的，于是大家不再对他那酒桶般的身材耿耿于怀。但我敢打赌：这几天的爬山涉水至少能

>>>> 让他减二十斤膘。

>>>>

>>>> 那次见面是愉快的，我们彼此之间都留下了较好的印象，这是一个良好的开端。后来的日

>>>> 子，我一直处于热切的期待中，而且从来没有象那样看重一个普通的节日，也许我真的有点

>>>> 迫不及待了。

>>>>

>>>> 【三】

>>>>

>>>> 五月一日的清晨，汽车轻快地前进着。一路上，浪迹天涯吹着轻松的口哨，不时和我们开

>>>> 着玩笑；野山雀还没有睡够，独自在后排继续做梦；远离城市，抛下了令人窒息的西装领带

>>>> ，我除了兴奋还是兴奋：目光炯炯，不停地把手掰得噼啪作响，象个职业打手；行人则很舒

>>>> 适地坐在车窗旁看外面的风景，偶尔向我投来一束不解的目光……

>>>>

>>>> 终于站在了森林的边沿！

>>>>

>>>> 望着那茫茫无际的绿色，我的心情为之一畅。浪迹天涯和野山雀掏出指北针和地图开始讨

>>>> 论从哪里进入，我和行人则站在那里欣赏眼前的景致：依山而上的密林，郁郁葱葱，波峦起

>>>> 伏。远处是洁白的雪山，雪山被一片翠绿包围着，高耸入云。呼吸着清新的空气，我们几乎

>>>> 醉了。

>>>>

>>>> 刚进森林，我就迫不及待地抽出了我的小狗腿。它沉重、锋利，重心集中在刀的前端，非

>>>> 常利于劈砍。一刀在手，另外三人垂涎欲滴的神情立现，并且马上变得蚂蚁般渺小，同时我

>>>> 心中诸如万夫莫当、力拔山兮气盖世的豪情有如怒涛狂涌，仿佛想借刀锋一泄而出。我想就

>>>> 算当时有一群熊出现，自己也肯定会毫不迟疑地冲上去将它们一一剁碎。

>>>> 然而现实对我是绝情寡义的，因为我很快发现林中只有低矮的灌木和杂草，如果执意要当

>>>> 开山先锋，就只能采取类似鸡啄米的姿势去斩断那些轻易就能跨越的草木，这当然于我的英

>>>> 雄形象十分不符，同时也令我感到万分索然。

>>>>

>>>> 后面的时间，小狗腿就一直在我背包里。其间偶然遇到有乱枝挡道时，他们就会象发现新

>>>> 大陆一样，毕恭毕敬地把我推到前面，在“酷！一定要酷！”吵嚷声中，依着他们制定的瞳孔

>>>> 收缩、刀闪、藤断三大步骤挥出小狗腿，完事后还得按要求神情落寞地吹去刀身上粘的绿色

>>>> 汁液！

>>>>

>>>> 这班衰人幸灾乐祸的神情和鬼脸使我发现自己已经沦为一个笑料，不过对他们的捉弄我还

>>>> 是很配合的，即使随便带把BUCK的折刀也比小狗腿实用得多，谁让我愚昧呢？

>>>> 浪迹天涯是一家规模不小的电脑公司的经理，应该算是IT精英吧，他头脑灵活，遇事果断

>>>> ，待人诚恳热情，也善于与人沟通，很具有领袖的气质，其实我们一直都是把他当做领队的

>>>> 。

>>>>

>>>> 野山雀是个程序员。走了不到一小时我就看出来，他所吹嘘的自己理论经验和实际经验丰

>>>> 富完全是在胡诌，那厮既白又胖还极端缺乏锻炼，走不了一会就得停下来喘气。猛一眼看上

>>>> 去还觉得他老实可怜，可整人出坏点子全有他。哼！以后我再遇到看上去老实巴交的人，一

>>>> 定要加倍提防。

>>>>

>>>> 胆小怕事的行人倒是游历过许多地方，不过都是坐火车坐飞机去的，徒步旅行之类的事却

>>>> 从没干过。他看起来仿佛挺深沉，话虽不多但还算精辟，就是有点杞人忧天。另外，他皮肤

>>>> 很黑，比炭还黑。

>>>>

>>>> 在嬉戏般的路途上，我们一直都走得很轻松，遇到复杂的路况，大家互相帮助，提携而上

>>>> ，我们彼此之间的好感也在逐渐增加，我开始喜欢他们，喜欢这个团体，那时我觉得我们能

>>>> 走更远的路，甚至可以走到世界上的任何一个角落。

>>>> ……

>>>>

>>>> 林中的景色是别致的，森林仍保持着完好的原始风貌，参天的树木枝繁叶茂，即使此刻外

>>>> 面是阳光普照，林子里也是幽暗深邃，雾气氤氲。这里远离城市的喧嚣，潮湿的空气中充满

>>>> 着草木的清香，我喜欢这里，虽然一路平坦，无险可冒，但我仍然认为不虚此行。

>>>>

>>>> 那天傍晚，我们在一个小水潭边宿营。潭水清澈透底，成群结队的小鱼在其中游弋。为了

>>>> 改善生活，我们冲进水中，把住了四个方向，在动用了刀叉棍棒甚至饭盒等武器下，历时一

>>>> 个多小时，终于弄上来几条小鱼。又经过长达半小时的激烈争论，我、行人、浪迹天涯以压

>>>> 倒性优势战胜了想喝汤的野山雀，小得可怜的鱼迅速被送上了简易烤架。

>>>> 吃着前所未有的美味，我们海阔天空地闲聊，谈人生、谈理想、谈女人……没有酒，就猜

>>>> 拳喝潭水。我们时而瞪着眼争得面红耳赤，时而又摊成一排大字学远处的狼叫……

>>>>

>>>> 后来的几天几乎都是这样度过的，我们成了无话不谈的哥们，称兄道弟。为此大家都很感

>>>> 谢发起这次活动的浪迹天涯，感谢发明互联网的人，惊叹冥冥中的缘分。如果不是天气转坏

>>>> ，让我们不得不呆在各自的帐篷里听雨声，我们或许已经撮土为香，对着明月结拜成兄弟了

>>>> 。

>>>>

>>>> 四】

>>>>

>>>> 从第三天开始，沿途的树木越来越粗，地上的杂草也变得稀疏，我们已经深入了森林腹地

>>>> 。天气变得很糟糕，始终下着蒙蒙细雨，雪山方向的天空更是阴沉得可怕，云层几乎贴在地

>>>> 面上，不时传来隆隆的雷声。林中溪水的流量明显增大，而且十分浑浊，由此我们推断山上

>>>> 肯定在下暴雨。

>>>>

>>>> 然而森林色彩却由此而显得更加动人，空气也更加清新，虽然地面变得泥泞湿滑，虽然浑

>>>> 身都被雨水湿透，但我们全不在意，大家士气高昂，甚至唱起了歌。漫步在雨中的原始森林

>>>> 里，与大自然最真实，最彻底地拥抱，这的确是一种独特的体验，它使我感到前所未有的新

>>>> 奇和快乐。

>>>>

>>>> 如果说当时有什么缺憾的话，那就是野山雀在几天的跋涉中体力消耗巨大，这直接导致了

>>>> 他惊人的食欲。他很快就吃光了自己的全部口粮，并且开始蚕食我们的库存。浪迹天涯带的

>>>> 食品口味很好，所以他受损最为严重，他开玩笑说最近两天一听到野山雀叫饿就紧张，不光

>>>> 心里紧，甚至连脚趾都紧抓着地！

>>>>

>>>> 不过说归说，大家还是很照顾野山雀的，我们三个都自觉地控制了自己的食量，尽量节省

>>>> 出干粮满足他。我想我们几乎是兄弟了，有了困难自然要互相帮助，况且，也没剩太多的路

>>>> 。

>>>>

>>>> 下午，我们到达第一条河。由于暴雨的原因，河水浑浊而湍急，在岸边徘徊了一会儿，浪

>>>> 迹天涯自告奋勇去探路。我们屏住呼吸，盯着浪迹天涯拄着棍子在河里摸索。河水大约齐腰

>>>> 深，很急，他几次险些被冲倒，但终于还是保持住平衡，跌跌撞撞地过了河。看着他在对岸

>>>> 得意地挥手，大家一阵欢呼。

>>>>

>>>> 我们随即沿着浪迹天涯的路线下了水，行人在最前面，我在中间，野山雀势大力沉，断后

>>>> 。河水冲得我们摇摇晃晃，大家不得不手拉着手，小心翼翼地前进，不一会儿就走到了河心

>>>> 。

>>>>

>>>> 【五】

>>>>

>>>> 我曾无数次地想象，自己是如何的英勇，如何从容地化解可能出现的各种危险。然而，真

>>>> 正的危险并不在我预计的时间和地点出现，它潜伏着，观察着，然后猝然出击！它降临的时

>>>> 候，我毫无准备。

>>>>

>>>> 当时我很清楚地听到背后的野山雀叫了一声，接着被他猛然一拉，我本能地使劲攥紧前面

>>>> 的行人，我们三个徒劳地挣扎了几下，最终还是失去平衡，一齐倒在水中，奔腾的激流顿时

>>>> 把我们冲了出去。

>>>>

>>>> 肩上的背包很快就吸饱了水，变得异常沉重。我不停地试图用脚踩河底，却发现下面是空

>>>> 荡荡的。我的水性很好，当时并没有慌张，我一边盘算着从哪里上岸，一边设法除去背包

>>>> ，减轻负重。

>>>>

>>>> 但是，万万没料到身边的野山雀根本不会游泳，突然间他死命地抱住了我！我用尽全力挣

>>>> 扎，但他的手象钢箍一样，指甲也深深地抠进我的肉里。我还没来得及换口气，就被野山雀

>>>> 沉重的身体拖下了水面。

>>>>

>>>> 肺部象是在燃烧，非常地难受。可是我无法摆脱野山雀，也无法解开背包，更无法浮出水

>>>> 面呼吸。这时体内生出一股强大的力量，强迫我打开口腔呼吸。但我不能，也不敢，我只能

>>>> 苦苦地抵御着这种巨大的痛苦。我知道，最终我会因无法忍受缺氧而不由自主地呼吸，大量

>>>> 的水会涌进我的肺，那时，就是终点了。死亡的脚步迅速逼近，我感到十分恐惧……

>>>>

>>>> 不知道当时野山雀有没有想到，要是他松开我，那等于是救了他自己。河并不宽，我肯定

>>>> 能够把他弄上岸。而他却始终象死猪一样紧抱着我，令我不能动弹，这样我们只有一起完蛋

>>>> ！我感到十分愤怒，而且从来没有象那样憎恨过一个人。

>>>>

>>>> 混沌中，行人出现在身旁，他艰难地弄断我们的背带，甩掉背包，又竭尽全力把我俩往上

>>>> 一托。虽然只是短短的一秒钟，但已经足够了。我深深吸了一大口久违了的空气，立刻恢复

>>>> 了镇定。

>>>>

>>>> 狼狈不堪地爬上岸才发现，我们被河水冲出去近百米。我瘫在岸边，看着湍急的河水，不

