――然而你呢，你的摇滚情操呢？

――我和你和他们很象，象得不分彼此，但是那些东西已经过去了，只能用来收藏，不能拿来再用了。

――我很想离开这个地方，不用做什么什么，不用和别人说话，不用想事情。。

