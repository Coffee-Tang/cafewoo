看到这里我握着手机噎住了。我不要你离开我不要你们离开，我们说好在一起，我和你和他们还有摇滚呀音乐呀梦想呀，我们会一直在一起的。

“怎么会让　我也变得　一样　冷漠眼光　为了生活而慌张　什么力量　竟然能让梦想被遗忘　现实让人受伤　真心却悲剧收场　想要解放　面对现实的失望　太多假装　带我离开这地方　这虚伪的世界就是这模样　不要太去在意别人眼光　受了伤　再沮丧　我有自己的向往　而当我察觉到我眼底的光芒　才发现我还是怀抱希望理想　哭一场　在没有人的地方　走我的方向。”

――终归都是太骄傲的人，都在坚持自己想要的东西。沈恬没有错，我相信他只是太爱音乐而一再逼自己接近完美，接近本质，摇滚已不再是他需要的外壳。而我和黄越他们招呼上单纯地喜欢摇滚，这又有什么错，我们一样可以自己坚持。已经没有办法回头，摇滚所在的那种精神深深地植入我们的灵魂，随着血液一起在体内奔流。我们亦没有退路。

然后我就分别给黄越和余涵打了电话。我说你能不能等，如果可以，请我们都好好的努力，等到四五年后大家都回到北京，能够在一起了，我们让IceAge重新开始，并且一直坚持下去。但是一定要给彼此信心，我们需要这样的信念，这几年才能够蛰伏做好自己在乐队的位置。然后他们沉默了几秒，说，好，我能够做到。是很坚定地说的。

听到他们的回答我真的非常非常欣慰，我需要他们给我信心。其实在说那些话之前我真的没有想到自己能够这么果断而坚定地下了那样的决心，这让我对自己感到欣慰和有信心。离开之前我并不知道IceAge于我是那么重要，它是我仅存的代表摇滚精神的那根支柱，是唯一可以亲手一步一步去实现的梦想。我不放手，一放手也许失去的是整个青春的压抑和感伤积筑而成的精神堡垒，是全部关于艺术的崇仰和对梦想的信念。已经挣扎和坚持了那么久，与现实撞得头破血流遍体鳞伤，我不能让那些忍耐和伤痛白白地经受。我想我们在一起，如果我能坚持，如果你们能坚持，我们也许还有机会，在一起共同完成那个信仰。

所以就这样一年过去了，我的大学第一年。现在回想马哲课其实可以好好地听，也犯不着对学生会的人那么抵触，甚至开始参加曾经不屑的那些学校的活动，考话剧团、参加学校的大型演唱会。是从那时起真正开始决定做音乐的，会在听lacremosa时暗暗分解里面的和弦，不知不觉地分析nightwish中大量的转位，记录Ｘ里鼓的节奏，在合唱团里会留意各声部的对位。再不会以自己没有受过音乐专业教育为由逃避问题，开始尝试着写歌。在游泳池遇到沈恬时大声叫住他告诉他我准备在学校的演出上唱歌特金属，并且是以IceAge的女主唱的身份。他很高兴的说好啊，我支持你，我又对他说放心吧我不会给大家丢脸的，然后在从游泳馆玻璃顶上照射下来的阳光中微笑，阳光很好，把水照射得很蓝。

一年后的这个秋天又去了曾经通宵写东西的咖啡馆，因为全校停电，没办法做任何事情。发短信叫人却一个也抓不到，其实在这个城市里我还是很陌生，熟悉的人不在身边。心下有点黯然，不过来到咖啡馆后很巧地赶上了他们的万圣节party，在楼梯上被人拉住要给我化装，我很开心地指着对方的脸说我要画成和你一样猫脸～然后满脸漂亮的油彩和亮粉走上楼，被一个象《scream》里面的面具的鬼唬得大声尖叫。换了一个不同的窗前座位，一样深红的窗帘和外面寒冷的街景。坐在木头的台灯下开始写这一篇，一边捏着三明治象兔子一样大口嚼着里面的生菜。不记得是谁说过胖子似乎都是不开心的人，因为他们不快乐所以总是无意识无节制地大吃。我想我闪亮的猫脸和大吃东西的样子是不是在别人看来也是不快乐的，其实我只是有一点点寂寞，一点点而已。曾经以为好朋友各自远方并无挂碍，因为心里总是在一起。然而终究是会感觉寂寞，他们不在身旁，许多事无从分享无从倾诉。但也会从此坚强，一步步走好在人烟渐少的路上。

１点了，咖啡馆就要打烊，我也该回去了。我想看见归时路上重新亮起的一路灯火，我想看见微笑挂在嘴角，太阳重新普照，我想看见梦想仍然闪着光芒，永远不会被忘掉，看见你们都在我身旁，我们在一起。

