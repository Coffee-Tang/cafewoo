在阳光微弱的洒在街头的下午遇见了那个叫漓的女孩.我坐在一棵在初冬已经掉光了树叶而显得光秃秃的大树下的栏杆上,跟孩子似的微微摇晃着,吃爱尔兰甜酒味的冰淇淋和冰镇可乐.偶尔抬头望见在冬季淡淡的阳光下显得苍白的天空,阳光却怎么也射不进我的眼睛.于是我开始相信自己是只适合黑夜的孩子,喜欢黑暗中被冻得脆弱的灯火.在爱尔兰甜酒味冰淇淋终于在冬天里融化,弄了我满手的时候,漓走到了我的面前,她递给我一张纸巾.我愣愣的盯着看她,她穿着一件不太合身的小蓝白格子的男式衬衣和牛仔裤,蓝色球鞋,很干净的样子.她扬了扬头示意我手中的冰淇淋化掉了,我仍旧坐在栏杆上,一动不动,我看见了太阳映在她眼里的颜色,很漂亮的色彩.她的微微凌乱的中短发在空旷的天空底下随风拂动,我不喜欢苍白的天,没有色彩,一切都是破碎的虚空.

她突然跟我说:我叫漓,你呢?

有雨.

漓在那天问我知道身后的是什么树吗?我摇头.漓说在她出生的地方有一种树,在初冬的时候会满树满树的开花,白色的,在阳光下晶莹的如同雪花.花在第3天就开始凋谢,第6天的早上,就什么都不剩了,连落花都没了踪影.就仿佛是昨天还在出血的伤口在一夜间消失不见,那般的不自在,没有了一丝的疼痛感,只剩下麻木的冬季.

有雨,最喜欢的季节是什么?漓爬上栏杆,问我.

恩……冬天……你呢?

冬天,特别是冬天苍白的天空.很纯净的样子,会让人想念家乡的那种树.我看见漓眼里我所没有的温暖的感觉,心隐隐的疼痛着.漓是一个温暖而别致的女孩.

2.

北方的冬天应该已经落下了第一场雪,而我的逃不脱黑暗的眼睛因夹杂着丝丝寒意的温暖疼痛.

我是生在南方的孩子,7岁那年以后的冬天,我所生长的城市再也没下过雪.冬天的来临总是由一场将令我感冒整整一个冬天的冰雨宣告于世,每一年的那一场冰雨都几乎让我窒息.胜总是在初冬跟我讲述雪的美丽,然后他在那条栽满梧桐树的街道边喝温温的牛奶,午后的阳光映在他的眼里,让我看到丝丝温暖.我是爱着冬季的孩子,胜也是.

胜说过他喜欢冬季午后阳光下的牛奶和温暖的声音.我不能体会,因为,我是适合黑暗的孩子.胜是我从小一起长大的朋友,也是最了解我的人.

在1999年的冬天,我离开所有人,独自去了北方的一个城市.一路上总是有人问我:你觉得初雪什么时候会落下……我总是摇头,不语.手里握着胜最喜欢的牛奶.牛奶在寒冷的冬天里有自带的温存,总是暖暖的,这大概就是胜喜欢牛奶的原因.北方的天空比我想象中的苍白,却也无力.12月24日的下午,我走在陌生的街道,我看见无数张没有温度的笑脸,突然冷得发抖.去便利店买了2盒牛奶6罐可乐和17张卡片,然后我趴在马路边的花台上看来来往往的人群,手掌里死死的握着牛奶.在第2盒牛奶也被喝光的时候,我掏出包里的Parker笔来写卡片.2张寄给皓,3张给胜,然后我再也找不到一个名字去填补那个空白,在2张卡片上写了自己的名字,最后11张只是说:圣诞快乐!然后把它们留在了路边,自己离开.我真的不知道还能给谁,谁又需要这些祝福.

那一刻在阳光底下很快乐,心里空空荡荡的快乐.下大雪前的最后一丝温暖,瞪着光秃秃的枝头,想起胜的蓝白格子衬衣……

我的视力在猛烈的下降,并且对色彩反映越来越迟钝.那个冬天里,我不停的点眼药水,弄得自己感冒发炎的喉咙更加疼痛,干裂.每一天早上,睁开眼睛的时候我都跟自己说:我极有可能成为一个学美术的色盲.尽管如此,我还是不懂得爱护自己的眼睛,一直都是这样的.只是会在眼睛疼痛时点眼药水.

12月25日的晚上给胜打电话,他问我在哪里?我说:不知道,但是这里下雪了.所以我想起了胜.胜沉默了一下,说:为什么要去那么远呢?很远吧.我在电话这头轻轻的点头,微笑.我说:这里下雪了.好美.却也很冷.会让人怀疑一些事情……

如果冷就喝牛奶.

知道.原来可以这么简单.

凌晨的时候一个人走在大街上,想象着大雪纷飞的北方的天空,一切都将要停止的样子.开始明白有一种东西叫岁月.我在街边拾到一张小卡片,上边只是说:希望你快乐!落名是:水.我抬头望天,并且说谢谢.然后我要一辈子记住着一瞬的感动和“水”这个字.

我的那11张卡片又会在哪里,握着它们的人,是否快乐.

北方的冬季,白色.牛奶,我的关于胜的记忆.

回到熟悉的街道的时候,发现自己的背包越来越沉,努力的想想没有什么特别的东西,或者只是心情吧.见到的第一个人是胜,他总是在南方特有的温暖阳光下幸福的笑,然后用奇怪的音调笑着叫我的名字.那个时候我刚刚下车,很累,却还是一直的笑.厌倦了北方天空的苍白,南方微蓝的天空总会让我想要快乐一点.

我很明白,我也许真的会在某个阳光明媚的清晨,告别色彩.深深的跌入别一种黑暗里,万劫不复.我每天点眼药水的频率越来越高,或许,我的眼睛已对它产生抗体,顽强的反抗……

3.生命的最后一瞬是如此的美丽,却又如此的可悲.

胜在2000年的春天认识了一个女孩,在一个咖啡店,那个女孩叫LIE.胜说是存在的意思.很别致的一个女孩,她总是在雨天的下午在那家咖啡店里写歌,抱着心爱的吉他,眼里容不下别人.胜说LIE有一双乌黑的眼睛,容不下一切她不想要保留的东西的眼睛.很漂亮.我没有见过LIE.后来LIE成了胜的女朋友,我可以理解LIE的固执的美丽,虽然我没有见过她.胜说等到今年夏天结束的时候,把LIE介绍给我认识.我说,好.然后那个夏天的一个阳光明媚的清晨,我再也没有看见南方特有的阳光下蔚蓝的天空.而胜,没有等到那个夏天结束,就离开了.那个叫LIE的女孩也就此消失.

我的眼睛,不用再整天整天的点眼药水.它已经失去意义……

我的手,因此,残缺.

我在胜离开的那个下午迷路了.在那个熟悉得陌生的小镇,我以为我会死去……我在雨中走了很久,最后蹲在一个墙角.我看到了胜.我明白,从今天起,我抬头望天的时候,它将因为2个原因而注定苍白.然后,我再没有理由微笑.因为,胜已经离开,我看不见雨后的天空的颜色.

我在2001年的冬天认识了那个叫漓的女孩.那个冬天我迷上了爱尔兰甜酒味的冰淇淋.我不再喝牛奶,却还是会在阳光特别耀眼的下午想起胜的笑脸,握着牛奶的样子,很干净的蓝白格子衬衣.看见漓的时候我就在想,要是胜还在就好了.至少这个冬天我不会那么无聊,至少我不会那么抗拒牛奶,至少我可以在那个夏天结束的时候见到那个叫LIE的别致的女孩.或许我还可以帮她写写歌词.一直以来我都相信着如果我和LIE见面,我会和胜一样喜欢她.所以,在我稍稍思考的那一小会儿,冰淇淋化掉了,弄了我满手.接过纸巾的时候,我抬头看见了那个叫漓的女孩子的笑容,又让我想起了胜.她的眼里有阳光折射出来的色彩.我能感觉得到.

在那天太阳落下的时候,漓和我说再见.我看见她离开的背影,被西斜的阳光拉得好长好长.我跳下栏杆正要离开,漓突然叫我的名字,她说:有雨,我们可以作朋友吗?我很喜欢有雨,一直都是.我突然愣住了,然后轻轻的点头,微笑.转身离开.漓说我们还会见面的.我想是的.

第2次遇见漓是在胜常去的那家咖啡店.我要了冰淇淋和可乐,她要了冰镇牛奶.不知道为什么我突然跟漓说,我一个朋友说冬天的牛奶是不需要加冰的.然后我看到漓灿烂的笑容,她说:恩……这或许是习惯吧.我以前的男朋友总是要冰镇牛奶的.

以前?那现在呢?我望着可乐,它总是那么兴奋,乐此不疲.

他……死了.一次意外.

哦.对不起.漓笑着说没什么.

可是冰镇牛奶没有牛奶的味道.

12月24日的晚上,我一直在网上游荡.11电多的时候突然停了电.我在黑暗里愣了6秒钟,然后摸黑走去客厅.有一些东西让我突然很失落,想要打电话,却不知道可以打给谁.胜已经离开了,拨了皓的电话,响了很久,没人听.我握着电话发愣,莫名其妙的想哭.我开始想念2年前留在街边的11张卡片,和那个署名“水”的人.

刚刚放下电话,就响了起来.我说:喂,我是有雨.然后我听到漓爽朗的笑声,她说:有雨,圣诞快乐!听着漓的声音我开始安静,我说:漓,不要丢下我一个人.我们见面好吗?

漓说好的,她马上过来.

我忘了说谢谢.

漓打开我的冰箱,里边塞满了可乐,还有6盒牛奶.我已经不喝牛奶.可是冬天的时候,去便利店,我还是不会忘记买两盒牛奶.漓递给我可乐,然后问我可以喝牛奶吗?我说好的,你喝吧.反正我又不喝的.我握着冰冷的可乐,坐在窗户旁边的地板上.

漓问我不喝牛奶为什么还要买牛奶呢?

我微微的笑着:或许只是为了纪念一个人和一些事情吧.到了冬天的时候都会这样的.

是你上次说的那个朋友吗?

恩……上次我跟你说他说冬天的牛奶不需要冰冻的,可是也只有这样才能保存长久一点,很讽刺吧.最后,我还是得把它们放进冰箱.

我把可乐放到一边,躺到木地板上:他还说过在黑暗里,如果用心去感受,可以听到灵魂的声音.可惜我是个适合黑暗却有害怕黑暗的孩子.很遗憾.

漓在黑暗中用冰凉的手指抚摩我的发丝和脸颊,冰凉的触感,很美.我说:漓,你知道吗?我的眼睛看不见颜色了,就在他离开的那年夏天的一个早上.当你在一觉醒来,睁开眼睛的时候发现整个世界都变了,你会发疯的.甚至会怀疑以前是否真实……更可笑的是我是一个学美术的疯子,比梵高更惨,还是个色盲……我轻轻的笑,眼泪顺着脸颊往下坠.

