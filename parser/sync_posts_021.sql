<br>司徒英岚最善于从别人的眼中观察他们内心微妙的变化，这一点他自己清楚地意识到并使其成为保护自己的武器。他没有伤害他人的意图，但他懂得怎样保护自己以及怎样避免伤害，他能够凭借自己敏锐的直觉及时辨别人心中一闪而过的念头。也许是受自己过于敏锐的直觉的影响，他的行为常被人看作神经质，再加上他的温闻儒雅，更成为了人群中的另类。也许是从人们的眼神中读到了这种嘲弄，因此他慢慢地将自己与周围的人隔离开来，而仅与少数的几个人有交往。而这少数的几个人当中，就有叶古星。叶古星随时随刻充满了冷漠的眼神中总是无法让人猜透他内心中真实的想法。司徒英岚从叶古星的眼神中既得不到赞同，也得不到蔑视，这就是他能够与叶古星交往的原因。还有一点，就是他与叶古星在文学上共同的爱好。当他读到徐志摩《再别康桥》中“轻轻地我走了，正如我轻轻地来，我轻轻地招手，作别西天的云彩”时，感动的热泪盈眶。他很想将自己的感受与人倾诉，与知己共同分享诗带来的美好，他很想能够与人共同嘘唏感慨唐诗宋词中“墙里秋千墙外道，墙外行人墙里佳人笑，笑渐不闻声渐悄，多情却被无情恼”；共同沉醉于“庭院深深深几许，杨柳堆烟，帘幕无重数”。然而，没有知己，没有听众，只有无聊的庸人们。周围的人不屑于与他讨论，对他的鄙视莫如说是对他所衷情的文学的鄙视。文学在当今这个社会中，更多的是武侠、言情，是卫慧长发美女们的乳房，是网上痞子们与美丽女孩的浪漫，甚至是那些使被性压抑的人们更加压抑的小说。这些就是当今认可的文学，就是肆虐的文学，那些传唱了几千年的诗篇，正与司徒英岚这样的人一样被环境淘汰。司徒英岚在压抑中活着的，压抑不是源自于对自己的不幸，而是无法进行自己喜爱的文学。夜里，他来到学校的湖畔，这里杨柳依依，波光如鳞，月影如女，整个学校中唯有这里还活着一丝诗的生命。因为夜已深，再加上明致的景色勾起了他朗诵的雅兴，因此也就无所顾及，“纤云弄巧，飞星传……”突然他发现叶古星孑然一人坐在堤岸边，默默地注视着皎洁的月亮。叶古星转过头，毫无波澜的双眼瞟了他一下，接着口中念到：“纤云弄巧，飞星传恨，银汉迢迢暗渡。金风玉露一相逢，便胜却人间无数。”
<br>
<br>（三）
<br>关度飞与叶古星并肩走在一起，两个棱角分明的脸上流露着相同的冷峻，将他们与旁人明显的隔离开。
<br>“听说文学社的性质变了？”叶古星问到。
<br>“听谁说的？”关度飞反问一句。
<br>“据说不搞文学，开始搞革命了。”叶古星微微一笑说。
<br>“呵，怎么可能呢！”关度飞顿了顿，继续说，“只不过是对文学社进行了改良，现在更激进一些。”
<br>“谁的主意，你的吗？”叶古星的问。
<br>“算是我提的主意吧，文学社里所有人都表示赞同。”关度飞随意地回答到。
<br>“你考虑过后果吗？”
<br>“后果？我在这个死寂的校园中都要憋闷的窒息了。这里没有激情，没有青春，没有思想，没有文化，没有挑战，没有我所需要的一切，什么都没有。有的只是灰色的水泥，黑色的知识和一群群没有感情、没有思考的机械。”关度飞显得有些激动，朝着被清扫到路旁的积雪踢了一脚，将残雪踢了一地。“我不愿意再这样苟活下去了。我要过真正的生活，富有激情与刺激，我要进取，我要斗争。在这个环境中我消磨的太多，我不希望再这样沉沦下去。我不愿意守着已有的东西而放弃了追求，我要不断地打破自己安稳的局面，在动荡中寻求活着的感觉。这就是我生存的哲理。”关度飞激动的说。
<br>“哲理？”
<br>“对，我就是要让人思考，让他们得出自己的哲理。现在这个世界只有躯壳而没有生命。现代人已经掌握了高度发达的科技知识，可他们却失去了对人生的思考。人从哪来？人到哪去？人生存的意义是什么？现在已经没有人再关心了。我喜爱古希腊，喜欢他们从一草一木中探寻人生的态度，如今还有谁会像亚里斯多德一样盯着大海思索世界的起源；谁会像法布尔一样看着蝴蝶的蛹化观察生命的奇迹；谁会像马克思一样思考社会的本质。”关度飞说到兴奋的地方使劲地挥了挥手，他的手上拿着一本《格瓦拉传记》。
<br>“你觉得依靠自己的力量就可以唤起所有人的觉醒吗？”叶古星平静地说。
<br>“总要有人去做。只要我认为是正确的，我就会义无反顾。”关度飞坚定地说。
<br>“就凭你一个人的力量吗？”
<br>“我有整个文学社的支持。大家的意见都与我相同，我们要用文学的力量改变沉闷的环境，将没有生命的东西消灭。我要驳斥这种旧式的学习模式，打破陈旧的学校管理，创建新的思潮。你不是也常常对我说如果自己有机会改变这种状况，一定会全力以赴吗。现在就有这么一个机会放在你的面前，你不打算加入我们的行列吗。”
<br>“你会为此付出代价的。”
<br>“如果付出代价后能够成功，我愿意被烧死在火刑柱上。”关度飞笑着说。
<br>“而我不愿意让庸人们以我的受刑作为他们荼余饭后的谈资。”
<br>
<br>（四）
<br>“秋风清，秋月明，落叶聚还散，寒鸦栖复惊。相思相见知何日，此时此夜难为情。”司徒英岚一个人对着白茫茫的湖面低吟。
<br>“折花枝，恨花枝，谁拟花开人共卮，开时人去时。怕相思，已相思，轮到相思没处辞，眉间露一丝。”叶古星走到司徒英岚的身后，盯着湖畔旁在寒风中瑟瑟颤粟的光秃秃的柳枝。
<br>“晚上找我出来有什么事吗？”叶古星淡白的脸色在月光中显得更加苍白。
<br>“睡不着，想找个人聊一聊。”司徒英岚喃喃地说，看得出他有心事，可又不便说出口。
<br>“哦，有什么心事？如果方便的话不妨对我说。”叶古星的脸色轻松下来，严峻的表情略微露出少有的温柔。
<br>“呵，我也不知道究竟应该从哪儿说起。”司徒英岚挺难为情地搔搔头，喳吧着嘴，半天也不说话。
<br>“千金纵买相如赋，脉脉此情谁诉？”叶古星看到司徒英岚欲说还休的样子，心里大概也能估计到到底发生了什么事，所以就笑着打趣道。
<br>“唉，差不多吧，我也不知道自己的真实想法和感受，不知道这是不是单相思。”司徒英岚红着脸，尴尬地搓着双手。
<br>“她是哪个系的？”
<br>“文学系的，在诗刊上发表了好几首诗，我挺欣赏她的文采，我觉得她对诗有一种特殊的直觉。”谈到诗后司徒英岚一下子就振作起来了。这就是司徒英岚的特点，也许在很多人的眼中他显得有些萎靡与怯懦，但他对文学的执着与热爱却使他具有超越一般人的认识，只要谈到文学，心立刻就像打足了气的球一样弹起来。他的心单纯的像一个孩子，但对诗的感情却像热恋的恋人。
<br>“你打算怎么办？”叶古星和蔼地问。
<br>“我也不知道该怎么办，所以想征求你的建议，你知道，我现在能够依赖的只有你一个人。只有你才真正地给我解决的办法。”司徒英岚诚恳地说。
<br>“事情需要你自己解决，我只是一个倾听者。”
<br>“我以前从来没有过这种冲动，当我看到她时，我甚至想将她搂入怀中。但我不知道怎样向她表白。”
<br>“你和她交谈过吗？”
<br>“没……有。”司徒英岚讪讪地答道。
<br>“那就和她谈，不管她是否能接受你，你应该鼓足勇气追求自己的幸福。”
<br>“可是我不知道该怎么开口。”
<br>“如果你是一个男人，如果你喜欢她，你就用自己的方式表达，这是你自己的幸福，也是她的幸福，我不能插手，我也无法插手，爱情是两个人之间的事情，旁人是没有发言权的。还有，这份感情是心里最好的回忆，你要将它作为心中最神圣的事情看待，用真心去完成，像一个朝圣的教徒那样虔诚，明白吗？”叶古星严肃地说，眸子中是幽黑的天空与璀璨的群星。
<br>“虽然还是不清楚具体应该怎么办，但我明白了要依靠自己。”司徒英岚的口气已经坚定起来了。
<br>“等着听你的好消息。”
<br>“你有喜欢的人吗？从没听你谈过自己的事。”
<br>“我？呵呵，形影相吊，孑然一身。”叶古星开朗地一笑。
<br>“你有过喜欢的人吗？”
<br>“携手处，今谁在，日边清梦断，镜里朱颜改。过去的事我不想再谈了，好吗？”
<br>
<br>（五）
<br>校园内的梧桐树光秃秃地树干上贴着几张大型的海报，关度飞与文学社的人在树下一起分发着新出版的刊物。树上的积雪早已冻成了冰疙瘩，几只麻雀缩头缩脑地立在灰色的树枝上。此时此刻叶古星突然想起了鲁迅笔下《药》中的最后一幕坟头上的那只乌鸦，同样的缩成一团。
<br>关度飞远远地看见叶古星，大声地吆喝他的名字。叶古星走到他们分发刊物的桌前，用眼角瞟了一下白色刊物的封皮。“拿着吧，这是新时代奏响的第一个音符，一切都会改变的。”关度飞拿出一本刊物递到叶古星的手中。
<br>“这么自信？”叶古星冷冷地问。
<br>“因为我有激情，有理想。”关度飞自信地回答。
<br>“飞蛾扑火，是会引火自焚的。”
<br>“如果有一瞬间的光明，我愿意燃烧自己。”
<br>叶古星没有再说什么，拿起一本刊物，默默地走了。身后是喧哗的人群，所有人都在拼命往人群中挤，人们三三两两地拿着刊物相互讨论着。关度飞的声音在人群中回荡着“我们要理想，我们要自由，我们要思考！”叶古星看了一眼刊物，封面上醒目的刊登着黑色的大标题“垂死的一代”，他楞楞地盯着这个标题，脸上久久地没有表情，最后，他回头看了看那股蜂涌的人群与枝头上的乌鸦，天空缩成一团。
<br>司徒英岚拉着一个女孩的手走过来，拍了一下在雪地中呆站着叶古星的肩膀。叶古星冰冷的眼神使他吃了一惊，叶古星的眼神就像一只受伤的野兽，充满了绝望与愤怒。他努力地挤出笑容算是打了招呼，然后拉着女孩的手走了。叶古星两眼充满了血丝，他抬头看了一眼天空，灰朦朦的，没有太阳。远处昏暗的乌云由远至近，一场暴风雪的将至。
<br>
<br>（六）
<br>叶古星坐在关度飞的对面一言不发，看着关度飞一杯接一杯地往自己口中灌酒。关度飞仿佛一下子憔悴了许多，标致的头发因为未加修饰显得很杂乱。两只眼睛胀的通红，高挺俊拔的鼻梁在雪白的灯光下愈发高挺，更衬托出两只幽黑深陷的眼窝，曾经充满了热情与豪迈的眼中是忿懑与愤怒，两片饱满的嘴唇如今也干瘪了，上面满是牙齿咬出的伤痕。关度飞倒空了瓶中的酒，醉醺醺地叫道：“小姐，拿酒。”
<br>“他妈的，一群混蛋。”关度飞一饮而尽杯中的酒，由于喝的太猛，呛得连咳了好几下，刚喝的酒咳了出来，顺着嘴角流往外流。
<br>刊物一出版，就在各方面引起了极大的轰动，正如关度飞所形容的，一颗石子扔到一潭死水中，把潭中的鱼虾都惊醒了。一场掀然大波也就从这时候开始。在事情之初，还正如关度飞所想的，果然在学校内展开了一场大讨论，文学社也因此成为了校园内要求改革创新的一面旗帜，但事情在过了几天后就开始变味了。课堂上，有部分学生开始公开罢课，有些甚至不允许老师进入教室，罢课派与复课派的阵线也因此形成了。复课派直接将矛头对准了文学社以及关度飞本人，谴责是由于文学社的挑动才造成了混乱的局面。为此，关度飞代表文学社公开发表声明，表示文学社的主张只是旨在激励同学进行思想革新，而不是挑拨学生与学校的关系，他们的声明刚发出不久，复课派还未曾有所表示，罢课派就主动站出来痛斥关度飞以及文学社的叛变，将他们比作学生中的墙头草，是新思想前进道路中的绊脚石。为此，文学社以及关度飞成为了两方面斗争的焦点，双方更多的将对对方的怒气发泄在关度飞的头上。最后，因为学校的正常秩序受到了严重的影响，学校不得不出面进行干涉。而学校第一个决定清理的对象就是引起这场谈战的文学社。关度飞对这种杀鸡祭猴的办法十分地愤怒，找到学校领导强烈要求他们取消这一不合理的举措，但遭到学校领导们的严辞拒绝。并且要求文学社的全体成员对此事向全校同学道歉，每人交一份检讨书认真进行反思。关度飞断然抗拒了这个命令，并号召文学社的其他人一起奋起抵制。当得到学校这一答复时，整个文学社一致表示不能接受这一要求，坚决不执行这一命令。但随着学校方面压力的加大，文学社内部也开始出现了分化，一部分人遮遮掩掩地表达了自己退会的打算，而另一些人在私下里偷偷地写好了检讨书并向学校领导违心地检讨了自己的错误。等学校撤消文学社时，最后一批坚持的人也妥协了，只剩下关度飞一个人。当他无奈地看着文学社的印章被学校没收时，他已经没有力量去阻止了，太多时间的斗争已耗尽了他的精力，看着一批批曾经共同“指点江山，激扬文字”的同志离开文学社，他的心也在一点点地死去。他只想早日结束，他已没有斗志再继续下去了。文学社被关闭后，他回到宿舍里埋头睡了两天两夜，直到叶古星把他从床上一把拽了起来，扇了他几个耳光他才醒过来。然后，叶古星就把他带到了这家饭馆。
<br>“都他妈是混蛋，一群蠢货软蛋。操他妈！”关度飞将手中的空杯子猛地砸向地板，碎裂的玻璃迸射四散，吓得服务小姐转身离开了。
<br>“不要把话埋在心里，有什么想说的想骂的就全部发泄出来。”叶古星冷冷地说。
<br>“哼，我算看透了，什么‘为理想而献身’，什么‘团结一心’都是狗屁，真遇到了困难都退缩了，都胆怯了，都跑的无踪无影了，什么东西！”关度飞大声地嘶喊着，然后啕啕大哭起来，眼泪与鼻涕止不住地流了一脸。叶古星走到他身旁，轻轻地叹了口气，伸手递给他纸巾。
<br>“古星啊！我真的很恨啊！我恨那些临阵脱逃的逃兵，恨那些背叛了自己的理想与誓言的混蛋。恨那些不分清红皂白是非分明只为了图自己快乐的混蛋。他们这是忌妒我的优秀啊。”他接过叶古星手中的低巾，擦了擦脏乱的脸，咬着牙说，“我知道他们都是在等着看我的难堪。不管我做什么事，所有人的目光都会集中在我身上。当我每做好一件事情时，他们表面上赞叹钦佩，实际上骨子里每个人都想看我的笑话，都希望看到我失败后的沮丧与绝望。就是因为我比他们优秀，所以他们就忌妒我，怨恨我，他们永远都赶不上我，所以就诅咒我。我可以清楚地听到这些阴暗心灵中最丑陋的声音。表面上我与他们相处地很融恰，但我在心里鄙视唾弃这些肮脏的灵魂。我之所以选择你做朋友，就是因为在你的心中对他们也有不可抑制的反感，甚至是厌恶。我看得出在内心里你与我同样高傲，同样地对一切丑恶怀着天生的厌恶。对此，我们俩只是表现的方法不同罢了，你把这种愤怒埋在心里，渲泄在自己的身上，而我则将愤怒化做一件件成功嘲讽、刺激这些无赖，从他们扭曲变态的心理中体会复仇的快感。”
