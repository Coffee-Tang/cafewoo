清晨，青灰色的太阳懒懒地停在地平线上，地面的积雪被染成古碑般的青色，显得更加冷清荒凉。树枝被厚厚的积雪覆盖的严严实实，干枯的枝条赖不住重压，不时有僵脆断裂的枝条从空中坠落，划出一道雪弧。操场地面的积雪上布满了杂乱的脚印，深浅不一的印记就像是月球表面不规则的陨坑。经太阳光照射，整个地面升腾起一片淡淡的雾气，弥漫在空中，将整个世界模糊了。

在这个白色的世界中，一个黑色的身影极不协调地出现在画面中，成为整个视野的焦点。叶古星穿着一身纯黑的运动服，戴着一顶黑色的毛绒帽在白色萧刹的操场中独自进行晨炼。在白雪皑皑的大地上，叶古星一袭黑衣的打扮犹为显眼。他像是铆足了劲的弹簧一样一轮接一轮地进行冲刺，像一只出击的猎豹。每一次冲刺都使他精疲力尽，但在略微休息后他又开始下一轮的冲刺。当他休息时，双手撑着膝盖，静静地站在跑道中，大口地喘着粗气，颈上青色的血管在急促的呼吸中抽搐，口中呼出的雾气在眉毛上凝结成一个个微小的冰晶。最后，他缓缓地脱掉一件件上衣，从地上抓起一把松软的积雪擦拭赤裸的上身，升腾而起的雾气将他严实地包裹其中。

关度飞一路走一路与沿途相识的人打招呼。他的人缘不错，这源于他的出色。首先，关度飞高大俊朗的外表就足以使其鹤立鸡群，站在人群中，他那股脱颖而出、锋芒毕露的气质总能不经意间使他成为众多目光的焦点。其次，他的口才，他的文采，以及他似乎天生的领导才能与气质和与众不同的思想总能屡屡使其傲视群雄。特别是他依靠个人的热情与报负开创了在校内与校外影响颇大的文学社的经历，更使他成为了校内的名人。文学社自开办以后，屡屡发表一些针砭时弊、纵横山河，富有青春斗志的文章，得到全校学生的推崇。校外已有多家报社向他发出椭榄枝，希望他能够参加报社的工作，但都被他婉言拒绝。用他自己的话解释，“燕巢岂能容鸿鹄！”

当关度飞看见晨炼归来的叶古星时，远远地招呼道：“古星！”叶古星听到有人叫自己的名字，抬起头顺着声音寻到关度飞，他点了点头向关度飞示意问候。关度飞大跨步走到他的面前，笑着问：“又去进行自我摧残了？”

叶古星淡淡地一笑，没有说什么。

关度飞的性格是高傲的，在其他人面前他会凭借自己的优越从气势上压倒对方。然而在与叶古星的交往中，他却显得很随意。关度飞与叶古星的结识是一次很偶然的机会。在才进入大学时，学校组织了一次游览长城的活动。关度飞不是第一次爬长城了，但他对长城仍然抱有很大的兴趣。他准备了充足的行李，其中还有帐篷，让人看了感觉他更像去野营。别人告诉他有车接送，爬的时间只有几个小时，而且也不在长城上过夜，大可不必准备这么多行李，他对这些话置若罔闻，全然不顾，他有自己的打算。在他看来，这次所去的长城不过是现代人在原有基础上进行大规模维修后的“仿制品”，他倔强并寻求冒险的心里不允许自己只是在这种“今人的玩具”中走过场，他要寻求的是真正的属于自己心中的长城。于是他就独自前往另一段未加修葺的古长城。长城上是残垣断梁，是厚实的地衣和杂生的枯草，一个人走在这段长城上，远离都市的喧嚣，远离现代虚伪的文明，在这种环境中才寻得一份古人修筑长城的苍桑与凄凉，才体会到塞外胡马北风，吹角连营弦惊中历史的艰难与叵测。他是有文学修养的人，他也写过诗，他明白只有此时此刻的心情才能真正意义上的吼上一嗓子富有豪放诗人味的“不到长城非好汉”，而那些只是在观赏用的长城上买几件简单纪念品的人根本就是不解历史与风情的看客。他为自己高高在上，超凡脱俗的见解欣喜，但他也同时感到了曲高和寡的孤独。当他在落日的余辉中爬上长城的最高峰时，他吃惊地看到已经有一个人站在城墙上，张开双臂在风中沐浴落日，深邃的双眼凝神于远方连绵的群峰。这个人就是叶古星，从这一刻起，叶古星就在关度飞的心目中占据了重要的地位。

而真正使关度飞器重叶古星的原因在于叶古星的冷漠。在众人的眼中，关度飞具有无法逾越的优秀。因此他在众人的眼中似乎永远处于高不可攀的地位，与他交往的人都在不自觉中对自己进行了遮掩，这仿佛是人的一种本能，更多的人在他的面前显现的是自卑与怯弱，谁都不敢轻易跨过这条鸿沟，始终游离于这条边线外。而叶古星却漠视这条分界线，不仅如此，可以说叶古星漠视所有的一切。叶古星与关度飞完全是两种类型的性格，如果说关度飞热情如火，那么叶古星则更像一块彻骨的寒冰。如果将关度飞比作澎湃的海潮，那么叶古星则是深不可测的深潭。在所有人眼中，叶古星除了行为比较怪异，常作出一些使人无法捉摸的举动外，对他本人并没有太多的映象。在他们看来，叶古星无足轻重，不过是一个小人物。但关度飞却能看到叶古星冷淡的背后是对世界的嘲笑与不屑，他知道他们俩有着共同的高傲。他特别欣赏叶古星对世界冷漠的态度，用他的话来形容他自己是万兽之尊的猛虎，而叶古星则是一只不羁的独狼，双方都有嗜血的欲望，都有与命运抗争的勇气与决心，并且都是在孤独中做战。

（二）

司徒英岚之所以能够信赖叶古星，在于他从叶古星的眼光中什么也得不到。

司徒英岚最善于从别人的眼中观察他们内心微妙的变化，这一点他自己清楚地意识到并使其成为保护自己的武器。他没有伤害他人的意图，但他懂得怎样保护自己以及怎样避免伤害，他能够凭借自己敏锐的直觉及时辨别人心中一闪而过的念头。也许是受自己过于敏锐的直觉的影响，他的行为常被人看作神经质，再加上他的温闻儒雅，更成为了人群中的另类。也许是从人们的眼神中读到了这种嘲弄，因此他慢慢地将自己与周围的人隔离开来，而仅与少数的几个人有交往。而这少数的几个人当中，就有叶古星。叶古星随时随刻充满了冷漠的眼神中总是无法让人猜透他内心中真实的想法。司徒英岚从叶古星的眼神中既得不到赞同，也得不到蔑视，这就是他能够与叶古星交往的原因。还有一点，就是他与叶古星在文学上共同的爱好。当他读到徐志摩《再别康桥》中“轻轻地我走了，正如我轻轻地来，我轻轻地招手，作别西天的云彩”时，感动的热泪盈眶。他很想将自己的感受与人倾诉，与知己共同分享诗带来的美好，他很想能够与人共同嘘唏感慨唐诗宋词中“墙里秋千墙外道，墙外行人墙里佳人笑，笑渐不闻声渐悄，多情却被无情恼”；共同沉醉于“庭院深深深几许，杨柳堆烟，帘幕无重数”。然而，没有知己，没有听众，只有无聊的庸人们。周围的人不屑于与他讨论，对他的鄙视莫如说是对他所衷情的文学的鄙视。文学在当今这个社会中，更多的是武侠、言情，是卫慧长发美女们的乳房，是网上痞子们与美丽女孩的浪漫，甚至是那些使被性压抑的人们更加压抑的小说。这些就是当今认可的文学，就是肆虐的文学，那些传唱了几千年的诗篇，正与司徒英岚这样的人一样被环境淘汰。司徒英岚在压抑中活着的，压抑不是源自于对自己的不幸，而是无法进行自己喜爱的文学。夜里，他来到学校的湖畔，这里杨柳依依，波光如鳞，月影如女，整个学校中唯有这里还活着一丝诗的生命。因为夜已深，再加上明致的景色勾起了他朗诵的雅兴，因此也就无所顾及，“纤云弄巧，飞星传……”突然他发现叶古星孑然一人坐在堤岸边，默默地注视着皎洁的月亮。叶古星转过头，毫无波澜的双眼瞟了他一下，接着口中念到：“纤云弄巧，飞星传恨，银汉迢迢暗渡。金风玉露一相逢，便胜却人间无数。”

（三）

关度飞与叶古星并肩走在一起，两个棱角分明的脸上流露着相同的冷峻，将他们与旁人明显的隔离开。

“听说文学社的性质变了？”叶古星问到。

“听谁说的？”关度飞反问一句。

“据说不搞文学，开始搞革命了。”叶古星微微一笑说。

“呵，怎么可能呢！”关度飞顿了顿，继续说，“只不过是对文学社进行了改良，现在更激进一些。”

“谁的主意，你的吗？”叶古星的问。

“算是我提的主意吧，文学社里所有人都表示赞同。”关度飞随意地回答到。

“你考虑过后果吗？”

“后果？我在这个死寂的校园中都要憋闷的窒息了。这里没有激情，没有青春，没有思想，没有文化，没有挑战，没有我所需要的一切，什么都没有。有的只是灰色的水泥，黑色的知识和一群群没有感情、没有思考的机械。”关度飞显得有些激动，朝着被清扫到路旁的积雪踢了一脚，将残雪踢了一地。“我不愿意再这样苟活下去了。我要过真正的生活，富有激情与刺激，我要进取，我要斗争。在这个环境中我消磨的太多，我不希望再这样沉沦下去。我不愿意守着已有的东西而放弃了追求，我要不断地打破自己安稳的局面，在动荡中寻求活着的感觉。这就是我生存的哲理。”关度飞激动的说。

“哲理？”

“对，我就是要让人思考，让他们得出自己的哲理。现在这个世界只有躯壳而没有生命。现代人已经掌握了高度发达的科技知识，可他们却失去了对人生的思考。人从哪来？人到哪去？人生存的意义是什么？现在已经没有人再关心了。我喜爱古希腊，喜欢他们从一草一木中探寻人生的态度，如今还有谁会像亚里斯多德一样盯着大海思索世界的起源；谁会像法布尔一样看着蝴蝶的蛹化观察生命的奇迹；谁会像马克思一样思考社会的本质。”关度飞说到兴奋的地方使劲地挥了挥手，他的手上拿着一本《格瓦拉传记》。

“你觉得依靠自己的力量就可以唤起所有人的觉醒吗？”叶古星平静地说。

“总要有人去做。只要我认为是正确的，我就会义无反顾。”关度飞坚定地说。

“就凭你一个人的力量吗？”

“我有整个文学社的支持。大家的意见都与我相同，我们要用文学的力量改变沉闷的环境，将没有生命的东西消灭。我要驳斥这种旧式的学习模式，打破陈旧的学校管理，创建新的思潮。你不是也常常对我说如果自己有机会改变这种状况，一定会全力以赴吗。现在就有这么一个机会放在你的面前，你不打算加入我们的行列吗。”

“你会为此付出代价的。”

“如果付出代价后能够成功，我愿意被烧死在火刑柱上。”关度飞笑着说。

“而我不愿意让庸人们以我的受刑作为他们荼余饭后的谈资。”

（四）

“秋风清，秋月明，落叶聚还散，寒鸦栖复惊。相思相见知何日，此时此夜难为情。”司徒英岚一个人对着白茫茫的湖面低吟。

“折花枝，恨花枝，谁拟花开人共卮，开时人去时。怕相思，已相思，轮到相思没处辞，眉间露一丝。”叶古星走到司徒英岚的身后，盯着湖畔旁在寒风中瑟瑟颤粟的光秃秃的柳枝。

“晚上找我出来有什么事吗？”叶古星淡白的脸色在月光中显得更加苍白。

“睡不着，想找个人聊一聊。”司徒英岚喃喃地说，看得出他有心事，可又不便说出口。

“哦，有什么心事？如果方便的话不妨对我说。”叶古星的脸色轻松下来，严峻的表情略微露出少有的温柔。

“呵，我也不知道究竟应该从哪儿说起。”司徒英岚挺难为情地搔搔头，喳吧着嘴，半天也不说话。

“千金纵买相如赋，脉脉此情谁诉？”叶古星看到司徒英岚欲说还休的样子，心里大概也能估计到到底发生了什么事，所以就笑着打趣道。

“唉，差不多吧，我也不知道自己的真实想法和感受，不知道这是不是单相思。”司徒英岚红着脸，尴尬地搓着双手。

“她是哪个系的？”

“文学系的，在诗刊上发表了好几首诗，我挺欣赏她的文采，我觉得她对诗有一种特殊的直觉。”谈到诗后司徒英岚一下子就振作起来了。这就是司徒英岚的特点，也许在很多人的眼中他显得有些萎靡与怯懦，但他对文学的执着与热爱却使他具有超越一般人的认识，只要谈到文学，心立刻就像打足了气的球一样弹起来。他的心单纯的像一个孩子，但对诗的感情却像热恋的恋人。

“你打算怎么办？”叶古星和蔼地问。

“我也不知道该怎么办，所以想征求你的建议，你知道，我现在能够依赖的只有你一个人。只有你才真正地给我解决的办法。”司徒英岚诚恳地说。

“事情需要你自己解决，我只是一个倾听者。”

“我以前从来没有过这种冲动，当我看到她时，我甚至想将她搂入怀中。但我不知道怎样向她表白。”

“你和她交谈过吗？”

“没……有。”司徒英岚讪讪地答道。

“那就和她谈，不管她是否能接受你，你应该鼓足勇气追求自己的幸福。”

“可是我不知道该怎么开口。”

“如果你是一个男人，如果你喜欢她，你就用自己的方式表达，这是你自己的幸福，也是她的幸福，我不能插手，我也无法插手，爱情是两个人之间的事情，旁人是没有发言权的。还有，这份感情是心里最好的回忆，你要将它作为心中最神圣的事情看待，用真心去完成，像一个朝圣的教徒那样虔诚，明白吗？”叶古星严肃地说，眸子中是幽黑的天空与璀璨的群星。

“虽然还是不清楚具体应该怎么办，但我明白了要依靠自己。”司徒英岚的口气已经坚定起来了。

“等着听你的好消息。”

“你有喜欢的人吗？从没听你谈过自己的事。”

“我？呵呵，形影相吊，孑然一身。”叶古星开朗地一笑。

“你有过喜欢的人吗？”

“携手处，今谁在，日边清梦断，镜里朱颜改。过去的事我不想再谈了，好吗？”

（五）

校园内的梧桐树光秃秃地树干上贴着几张大型的海报，关度飞与文学社的人在树下一起分发着新出版的刊物。树上的积雪早已冻成了冰疙瘩，几只麻雀缩头缩脑地立在灰色的树枝上。此时此刻叶古星突然想起了鲁迅笔下《药》中的最后一幕坟头上的那只乌鸦，同样的缩成一团。

关度飞远远地看见叶古星，大声地吆喝他的名字。叶古星走到他们分发刊物的桌前，用眼角瞟了一下白色刊物的封皮。“拿着吧，这是新时代奏响的第一个音符，一切都会改变的。”关度飞拿出一本刊物递到叶古星的手中。

“这么自信？”叶古星冷冷地问。

“因为我有激情，有理想。”关度飞自信地回答。

“飞蛾扑火，是会引火自焚的。”

“如果有一瞬间的光明，我愿意燃烧自己。”

