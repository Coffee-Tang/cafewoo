<p>我在大学毕业与开始工作的间隙里回到福州，陪陪家人，陪陪女友，
<br/>也抽出一天的时间，和高中的同学一起聚聚。</p>

<p>一群朋友碰头后，实在想不出能上哪去，最后决定去茶馆喝茶。</p>

<p>茶馆颇为雅致，是一个很适合闲谈的场所。可是一班朋友之间的话题
