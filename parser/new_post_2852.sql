<br/>说大学里的趣事，讲讲自己如意或不如意的恋情，谈谈今后的工作，
<br/>接着开始打牌，以消磨掉到午饭前那两小时的时间。</p>

<p>一班朋友中，有好几位都是中学文社里的成员。当时大家聚会时断然
