很多快乐的男人女人挤在一起等待1个小时以后的12月25日。

我对坐在对面的男人说，我怀孕了，明天我会去医院杀死他。

酒吧很吵，我说话的声音很大，我是个嚣张的杀人犯。

对面的男人楞了半天，居然冒出了一句，我们结婚吧。

听到“结婚”两个字的时候，我已经把桌上那白色的烟灰缸向他的白痴脑袋扔了过去。可是，烟灰缸碎了，他的头没事，我没扔准。他甩了句“你这个疯女人”，就走了。

我突然特别想笑，我确定自己真的想笑。

“烟灰缸摔在地上并不能让他的头疼。如果你想笑，我们可以聊天。”

我认识了安瑶和伊果。我们喝酒到打烊。

上个冬天是温暖的幸福的模糊不清的，我还没把我的大衣穿脏它就过去了。

那是个暧昧的冬天。我一直没搞清楚身边的男人女人，没搞清楚什么变了而什么又一直在，没搞清楚我那张大床的方向……

然后，下一个冬天又来了。

<5>我们

我们是藏在这个美丽城市黑暗角落里的三个疯女人。

拥抱，亲吻，然后说我爱你。

我们都很年轻，都没有固定职业，都曾经喜欢白色而现在只爱深色，都有不健康的长头发，都喜欢阳光灿烂的夜晚，都喜欢一个人坐地铁，都不再知道什么是爱情，也许以后也不会知道。

敏感，极端，自闭，肆无忌惮，漫不经心，懒散，冷漠，善良，失眠，没有欲望，不擅长和人打交道，不懂拒绝，偶尔性冷淡，不停行走，因为不自由，有时绝望。

我们乱糟糟的一起活着。没有因为所以。

我喜欢安瑶的高个子。喜欢伊果右下颚那块浅褐色的小胎忌，形状像小猫爪子。她们说喜欢我这样的瘦女人，我确实很瘦。

安瑶曾经因为突然难过，在屋里砸了一打啤酒。

伊果因为饭馆太吵，把一个装满茶的白色茶壶摔了出去。

我朝一个对我说“结婚”的男人的脑袋扔了一个烟灰缸。

这是我们的坏脾气。

<6>身边的人

去爱一些人，然后离开。

想念很多人，离开很多人。

离开才会想念。

爱他，所以站在旁边。

他会一直在……

伊果和梵天。

“梦旅人”，伊果一个人常来的地方，安静，温暖。

她说酒吧的名字是岩井俊二的一部电影，她最喜欢的电影《梦旅人》。

“看的时候你会想把自己的整个头皮扯下来……所以再没有勇气去看第二遍。”

梵天，33岁的安静男人。这是他的酒吧。

我不知道是不是感受过死亡的人都会让他们身边的人感觉到他们心里的平静。

能让人不去想乱七八糟的东西，他们有同样的能力。

梵天有很漂亮的眼睛，但是我感觉不到伊果所说的温暖。也许我理解不了带着疼痛的温暖。

她说梵天是她最特别的朋友，他会一直在……

我不明白为什么伊果总是和不合适她的人在一起，而适合她的却只是她的朋友。

安瑶说，那是因为她是个好奇的孩子，只是停下来看看身边的人，然后很快离开。朋友才会一直陪在她身边。比如我们，比如梵天。其他人都不是，什么也不是。现在伊果身边的大宝和J只是突然出现在她生活里的人，会很快消失，他们什么也不是，她喜欢他们，可这并不代表她可以停下来。她害怕分离，却习惯告别。除了梵天，没有人可以让她停下来。

一直在的，是她和梵天都体会过的、永远失去的……

安瑶，奶糖。

奶糖像极了一个台湾演唱组合B.A.D里的成员Ben Pai。脸上有冷冷邪邪的笑容。

在安瑶结束了和威子六年磨了又磨的感情以后认识了奶糖，不知道这是他们谁的不幸，他们总是争吵，但是谁也不能离开对方。也许是因为威子曾经让安瑶的生活变的太复杂，所以在她认识奶糖的第九天就决定和他生活在一起，但是她不爱他，以后也不会。

奶糖是个简单得不知道自己需要什么的孩子，他是可爱的、不能被伤害的。所以他永远不会知道安瑶在想什么，要表达什么。他也不会知道威子带给安瑶的是什么。

一些东西失去了就再也不能回来，这是我们都明白的道理。

奶糖像夜晚的小魔鬼，他是年轻的漂亮的有着迷人笑容的孩子。他该是我们中最快乐的，不带任何伤害的快乐。他爱安瑶，只有他知道爱情是什么。

他给她简单快乐的生活，他们拉着手一起去市场买菜，一起做饭，他给她洗衣服，一起穿着拖鞋去散步。安瑶需要这样的生活。

只是我们都不知道，会不会一直这样。简单就好。

至于我……

我和我的男人生活在一个很大的房子里，我们谁都不在对方的生活里。

我们是自由的。在一个灰色的壳子里，我们都没有长好可以飞翔的翅膀，我们只能一直在壳子里互相纠缠，互相纠缠，谁也不能挣脱，谁也不知道为什么。

他是我第二个宝宝的爸爸，他是个有着大手的英俊男人，是我见过最英俊的男人。

“你是个可爱的瘦女人，有时候我会想咬死你，然后吃掉，让你生活在我的身体里。”

他是我童话王国里的黑暗精灵。

我们喜欢的朋友

冷冰冰的DUMMY，我们大家的哥哥欣锐，总是和安瑶吵架的小朋，忙碌漂亮的TATA，总能找到好电影好音乐的索四，温暖健康的童，还有我们大家想念的么么……

我们喜欢的朋友会一直在身边。

注定在某个时间相遇，然后在一起。

<7>一个人的旅行

这是个告别的年代。

我不停行走，自己一个人的旅行。

离开，回来。再离开，再回来。

我需要想念。

我去很多美丽的地方，大小城市，看每个陌生地方的人，看他们的笑容，看不同的早晨、夜晚，站在阳光下想念我离开过的地方、人，离开离开……

我的幸福在别处，这是我常常说的话。

在别处……所以行走、告别。

<8>阳

认识CICINI的那个下午有冬天最好的阳光。

我看到她一个人坐在书店门口看对面玻璃窗里映出的人影。

我想带她走，只是离开。

我不知道为什么会莫名其妙的去心疼一个人。她说这是我的劫难。

“我喜欢你的名字，有‘阳’这个字。阳光是无法触及的。”

我不知道什么会一直在……希望她可以一直在我身边，我们应该在一起。

“我身边有很多人，我不寂寞，我谁都不需要。你找到我只是不想一个人难过，因为寂寞。可我不是。我一直这样。”

“告诉我，爱的对面是什么。”

“我不知道。”

“是淡漠，无所谓。”

我说完这句话，她开始笑，不知道她在嘲笑自己还是在嘲笑我说的话。她身上有我所有的不确定。这是件可怕的事情。

“我可以带你离开。”

“我去过的地方比我离开的男人还要多。我是自由的，谁也不能带我离开，谁都没有这样的能力。我是CICINI，我只是CICINI，不要把我当成任何别的人。”

她像只绿色的猫。

关于阳

这个人会突然出现或者消失，我不喜欢他这样随便使用别人时间和生活的人。

他让我觉得人是可以没有感情的。

可我不是简单的动物。

后来，我对他说，滚蛋。

<9>这个冬天真他妈冷

我晃晃悠悠的生活继续，改变不了就继续晃悠。

整个冬天我都在陪伴DUMMY，DUMMY有忧闭恐惧症，他不能坐电梯，不能在狭小黑暗封闭的空间里生活，他现在甚至不说话。

我们很难过很害怕，因为DUMMY一直不能快乐。

伊果和梵天一起去了海南，这个冬天或许真的太冷了……他们逃跑了。

安瑶两个月前回多伦多继续她的小资留学生活，昨天她打电话来告诉我多伦多现在有多冷，她的购物欲有多么夸张，她不再性冷淡可是还是不知道什么是高潮，找到高潮已经成为她的身体作业。

奶糖现在几乎每晚都在酒吧演出，我和DUMMY有时候去看他，他的笑容依旧那么漂亮可爱。他是夜晚迷人的小魔鬼。

这个冬天真冷真冷，我穿着厚厚的大衣，围着大围巾，一个人走来走去，我想明天该去买顶帽子。这想法之后的5分钟里我突然特别想结婚，结婚是件伟大的事情，我哪也不想去了，只想结婚，过NB平淡安静的生活……因为冬天真的太TM冷了！

2002.12.25 01：30

我和我的男人从三里屯北街逛到南街，我不记得我们换了几家酒吧。

路过BLACK SUN的时候我突然觉得很难过，想起了2000年的平安夜、2001年的平安夜……还有我看不到的下一个12.24。

我对他说，我们回家吧。

我们的大房子很暖和，灯光很暗，音乐很响，几乎无法听清对方在说些什么……

我一直在说“你说什么，我什么也听不清，你在说什么……”

于是我们干脆开始做爱。

做爱的时候我的电话不停的响，一些人打电话来说圣诞快乐，我一边做爱一边接电话，一边笑着说谢谢，然后告诉电话里的人我在和男人做爱，外面真冷真冷。

最后一个电话是我第一个男朋友打来的，他说你好吗？

那年我14岁，14岁的12.25我们分手了。

那是我喜欢的第一个男人。

他现在对我说“你好吗”，就像17岁时的他第一次对我说“你好”一样。

我们曾经都是那样天真纯洁的小孩儿。

时间很快就会过去，什么都会很快过去……所有人都是这样长大的。

可究竟什么会一直在？

什么才不会离开？

似乎没有人知道。

好吧，2002就要永远的过去了。

<10>MAIL

阳光照在厚厚的窗帘上，颜色很好看。这是冬天上午11点的阳光，它让我想起小时侯你带我去公园，我一个人坐在飞快的转椅上仰着脸大笑的样子……

你好吗？……

妈妈昨天刚刚从澳大利亚旅行回来，晒黑了一些，她现在很快乐，我喜欢她这样，拿着旅行箱和朋友们到处去玩。她的红色旅行箱是我挑的，很漂亮，很适合她。

那么你呢？现在在哪儿？是不是快乐的呢？

回复我。

