赵学而有首歌叫《细水长流》。在广东时在香港电台“海琪的天空”里听到了，顿时惊为天人，很辛苦的从网上找来MP3，如一江春水的钢琴里，赵学而的歌声喃喃流过。那么美丽美丽的歌词啊！爱极了那一句“若你双眼是深海，便已经浸没我”，还有“若有一天要被分开，我远山也踏破，你那暗涌如在叫唤我，唤我入内，怎可不奔向你”。配器都是钢琴与弦乐，背景做得很丰富。赵学而的唱功也非常好。里面好多歌都非常喜欢。象“谈一场不后悔的恋爱”，“如若今夜不下雨”，“明天是晴天吧”。那样的名字，给了亦舒，又是一个好故事。这样的词，好象充满了模糊的憧憬。可是那内里澎湃的勇气，“天空晴时，雷霆来时，它都长流”。什么也挡不住那隐隐的暗涌。

