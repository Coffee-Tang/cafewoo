过渡。车到同益码头后，方知轮渡每小时一班，小车过渡费为40元，利用候船的20分钟，去小卖部补充烟、软饮料等，给略显疲惫的店主增加了些许的营业额。也开始了一天里的第一瓶惠泉啤酒。

7点稍过，和其他约10辆车上了海达渡轮。泊好车后上到旅客区，发现每5米内必定有“禁止吸烟”的标志，而询问水手及售货员的结果却正好相反：随处都可以吸烟！怡然地在点着烟，暗中嘲笑着那制作还算精美的标志，转而一想我们不少的法规不是这样吗？公司里的不少制度也是这样吗？

