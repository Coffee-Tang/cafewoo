拜拜吧您呐！

上岸后的首件任务是解决住宿的问题，中银开发区内的华商酒店已经不再考虑----没有必要再受千篇一律的“3星”酒店的虐待。电话咨询后，决定下榻在南太武高尔夫俱乐部的小木屋，一番电话上的讨价还价，用一晚300元享受720的待遇，成交。

正在建设的高标准水泥路已经双向开通，工程车扬着漫天的黄沙毫无规则地撒着野，民工的自行车是乎必须载二人方可上路，和他们一起的当然还少不了当地人的摩托车，为了不被意外破坏周末的计划，我只得不时地开开警笛----车灯和喇叭对他们已经不再有任何作用，只有尖利的警笛才能提醒他们在身后可能还有其他车辆。

没有路标，没一个路口必须凭感觉选择。7~8公里的水泥路很快被一堆拦路的泥土挡死，夹杂在其他庞大的工程车中间，右弯上一条狭窄的土路，听着鲍勃马利的《ONE LOVE》，在黄土弥漫的土路上跑了5~6公里，觉得情况不对----这就是去高尔夫的路？恰逢一小店还亮着灯，下车一问，果然已经跑过头----旅程中的第一次问路。

掉头回跑上大路，见有灯光的地方停车再问，答曰：“向前，右手。”遵命向前3公里，果然在黝黑的树丛中发现“南太武高尔夫俱乐部”的金字招牌，而进俱乐部尚须途经一不足2米宽的坎坷土路。

俱乐部到会馆依旧没有路标，地上的指示箭头也相当凌乱，折腾了快10分钟，才摸到会馆门口，空荡荡的停车场告诉我们，我的老捷达是唯一一辆在此过夜的车。

小木屋面积不小，设计装修也不错，空调热水都有，甚满意。客房小姐开床时，我发现洁白的床单有个明显的破洞，本无大碍，为测试其服务质量，向小姐指出，答案和我预计一样：“没关系，明天再换。”

MD，说没关系的应该是我！

住解决了，接下来是肚子的问题了。

往前的路已经走过了，没有啥值得考虑的地方，之后折回头去人口比较密集的招商局中银开发区----和鼓浪屿一水之隔。工厂不少，人也不少，停车后开始徒步寻觅离开厦门后的第一顿饭，闽菜、湘菜、川菜、稀饭馒头，小店不少，而能引起我们兴趣的却没有，约10点半，我们的确定的选择是：路边烧烤！

在一个人气较旺的档口坐下，开口便要先上啤酒，情况也一如预料：有惠泉啤酒，但没有冰的。征得老板同意后，取车后冰箱中自带啤酒，有了冰冽熟悉的惠泉，我不再计较烧烤的口味了。

烧烤档的老板是四川南部县的夫妻，我赶快用四川话套套近乎，没有指望他给我啥优惠价，也不想他能改变一下他那牙签般粗细的肉串，更不敢想他乡故知之类的事情，唯一的希求是别卖变质的食品给我们，这当然不敢直接对他说。

一顿35元的“肉串晚饭” 还算丰盛，还有冰啤酒相佐，觉得今生已别无所求。

微酣轻车熟路，回小木屋3~5分钟就搞定。

借LD冲凉的空挡，信步到海边，落潮后退出一大片的海滩，在当空的皓月下，泛出的月光竟然有些刺眼。

早上醒来是被门外保安聚集的保安吵的，当时他们在我住的小木屋外用早餐。看看手机上的时间，6：02！

翻开《服务指南》准备投诉，却被那薄薄的2页指南彻底吓醒----其中一页为小木屋内的财产及价值：空调8000元、电视5000元、遥控器500元、电话机400元、床单300元、毛巾50元、玻璃杯20元. . . . . .我突然觉得我回到上世纪70年代了！再开开那5000元的19寸彩色电视机瞅瞅，证实了我的感觉：居然有4个频道有声音，好象还是台湾的普通话，而图象完全是些黑白点儿在晃荡，我下意识地摸摸我的眼镜：确定我是否戴着眼镜。

还是别挑剔了吧，叫醒LD后赶快结帐，上路走人。在总台的对话我不得不在这里展示一下：

“小姐请问，去火山口如何走？”

“喔――我知道，在港尾那边。”

“是，是港尾，如何去？前边的水泥路结束后应该往左还是往右？”

“港尾好远啊。”

“我知道远，可有多远10公里？20公里？”

“我不知道该如何说，你要坐公共汽车或摩托车去？”

“不，我自己开车。”

“那你还是坐公共汽车或摩托车去，港尾好远啊。”

多么有用的信息啊！我一脑袋的雾水，灰溜溜的走回车里开始热车，在作车的行前检查的时候，心里面嘀咕着，可能我还没有睡醒。

8：00上路了，无头的苍蝇一样，沿着蜿蜒的土路不紧不慢地开着，超车、会车都小心翼翼，路面狭窄且不平，除了警察外，只要有的东西通通上路了：小巴、卡车、微型车、农用车、摩托车、孩子、牲畜。

没有路标，也别指望有路标，如果要有选择，就照车轱辘印多的选吧。不出一小时，终于到了一大的十字路口，除了路面变成水泥的外，居然还有交通环岛！车靠路边，买水问路----没有付出哪来的认真回答呢？4元钱除了换回4瓶矿泉水，还有更重要的：“向前！30里，全是水泥路！”

果真如此，双向6道的水泥路，视野良好，但别急，你也甭想放松：整个公路的4/5都被当地人用来当作晒场了！留给车走的那1/5还是弯弯曲曲的，还要避让那些在路中间扬场的农夫，不容易啊。

凭着以往的驾驶经验，你不能开车压过他们正在翻晒的粮食，轻则受到漫骂，重则引起交通事故，粮食的颗粒会让轮子抓地不稳，侧滑是经常出现的事情。

穿过港尾镇，经过后石电厂，偶尔从左侧的窗外能看到大海，估摸着离火山口应该不远，放慢车速，努力地注意着路旁的每一个可能出现的路标和岔口。

在离那标志10米处发现了它：漳州国家地质公园！顺小路进去约200米，买票，10元/人，再往前200米，有路障，正在观察周围的时候，路旁店家夫妻2人同时卖力地指挥泊车，我已完全明白其用意，索性主动问：“停车多少钱？”“5元。”“5元一次还是5分钟？”我继续问清楚，“一次”。爽快奉上5元钞票，落车走人。

LD抱怨我不该主动“投案”，我只好讲述我的经验：你主动给，问清楚，这些刁民无话好说；如果不问的话，回来的时候车身可能被刮伤，车胎可能被扎，更可能被敲诈更多。

稍往前，眼前豁然开朗。湛蓝的天空中点缀着一大团白云，在日光的照耀下，云团层次分明，海的颜色也呈现出深邃的蓝，近处的左侧的海湾划出一道优美的弧线，黄色的海沙和大海形成的了一道协调的高反差。再往右，一块黝黑的岩石凸立在岸边，在其周围，还有一大圈的石头，也是黑如焦碳。3~4个人在海滩上搜寻着啥，“鸟鸣山更幽”是指山景，而在空旷的海滩上点缀上三五游人，更衬托出静谧的气氛，又该如何表述呢？

沿着小路下到海边，烈日下开始研究火山口，无论被海水的冲刷变得光滑如江边的鹅卵石，还是棱角依旧的，黑色，黑色，还是黑色。牡蛎是乎不太喜欢这样的颜色，它们更愿意选择褐色的玄武岩，它们斑斑点点的踪迹和身影更让碳黑的火山岩显示出大自然的神奇。再折身回到离岸不远的小山头，顺着小径拾级上到简易的观景台，清晰地看到水线边火山岩的纹理，在近处看到的一块块岩石，此时变得象人头上的发旋，规则地环状排列着，随着时光的冲刷，完整的环已经不复存在，残缺中不难想象当时的壮观。

烈日下的我们已经感到眩晕，身上的衣杉全部汗透，脚下的滚烫的沙让我想起热锅里的蚂蚁，在最近的一处小卖部，拿起冰饮料狂灌，饥不择食：冰----就是现代文明。

回到车上换件衣服继续上路，奔下一站：漳浦湖西镇赵家堡。

路面时好时坏，但对驾驶者来说都不能有半点麻痹：路况差的时候需要小心，而路面宽的地方依旧充满着危险：别忘了，我们是行驶在别人的晒场上呢。和昨天的情况不同的是，在这个路段行车，普通喇叭作用明显，而警笛不是那么的见效。

依旧没有路标，而对路口的选择的自信心在急剧的减少中，每有选择，停车问路逐渐成了习惯，虽然从来没有得到过准确的答案。

车内凉下来了，对音乐也再不挑剔，一片接一片随它去吧。我也开始考虑回程的线路：不准备原路返回，是走国道还是上高速，走着瞧吧。转过一大弯，远处居然出现了久违的蓝底白字路牌！高级！

“漳诏高速赵家堡入口”！哈――原来这里可以上高速！再看路牌，左边岔道去赵家堡。路面上晒的粮食依旧占据着大部分的路幅，而近守高速入口，心底犹如在家门口玩耍的孩子般的踏实，驾驶起来也更加得心应手。

左弯，根据路标再左弯，上土路，一块小牌子挡住了去路：“道路施工，车辆禁行”。不想在烈日和黄沙中下车查看，想着牌子后面可能写着“欢迎你回来SB”的字样，毫不犹豫地掉头去湖西镇，先在那里解决肚子。

午后的烈日让整个镇子死一般的寂静，好不容易找到一家尚在营业的小餐厅坐下，小老板穿着还算整齐，卖力地推荐当地的山珍野味特产：土鸡、石鸡（一种野生的青蛙）、溪鱼等等等等。

肚子问题简单地解决了，在和小老板聊天的过程中，除了了解“顶坛新城”和“诒安堡”就在附近外，还知道了去“赵家堡”的路小型车依旧可以通过。

按小老板指示的方向上路，沿着越来越窄，越来越崎岖的土路，来到一处看来有些年头而依稀能辩当年气派的建筑前，门前依旧是晒场，几个年老的农民在大门外乘凉聊天，这就是我们“五里三城”中的头一站----“顶坛新城”。建筑内除了几户破落的农家外，还分布着鸡圈鸭舍鹅棚牛栏，再穿过一堆堆的柴禾，找到了那楼门嵌蓝廷珍书“日接楼”石额匾。整个建筑看不出任何的管理、保护和修缮，原来的厅堂不少已经垮塌，形成一个个前后坐落的天井，只有那历经风雨的山墙和粗直的木柱木梁，还能嗅出些当年的气息。

回到车上时，烈日烘晒下的车还没发烫，继续往前，没找到“诒安堡”，却回到往“赵家堡”的路上，也罢。

“道路施工，车辆禁行”的小牌子依然孤独地站在那里，一点也不讨人喜欢，而它后面的一小段路更是要命。尽管我已经非常小心，还是2次挂底。

赵家堡的门口空地没有晒粮食，却有不少的杂草，右侧有一幢丑陋的三层楼房，悬挂着“村委会”之类的牌子。听到车声，门开始纷纷开开，睡眼惺忪的老农门来不及擦眼角的分泌物，便匆忙地提出“售票处”的破牌子，再煞有介事地搬出自己坐的木椅：开张啦！

一人10元，我没犹豫就递上钞票。不想老农民还意犹未尽，冒出3个字差点没让我喷饭：“停车费”。

进得堡内，发现和普通的闽南农村没有任何的区别，一样的脏，一样的乱，看不出一丝“国家级重点文物”保护的痕迹。偶遇的村民以奇怪的眼光看着我们，也许一边看我们的展览，一边心里嘲笑着城里的SB。

进入内城，验票的程序挺规范，空荡的一个个房间没有任何让人驻足的东西，在3楼的走廊上，贴着一组蹩脚复印机复印出的《清明上河图》，估计是要显示该建筑和那年头有点关系，而开始褪色的复印件，用胶水直接粘在墙上，有点象废弃的幼稚园墙上贴的小动物，是博物馆？是国家级保护？

