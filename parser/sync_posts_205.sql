<br>微酣轻车熟路，回小木屋3~5分钟就搞定。
<br>
<br>借LD冲凉的空挡，信步到海边，落潮后退出一大片的海滩，在当空的皓月下，泛出的月光竟然有些刺眼。
<br>
<br>早上醒来是被门外保安聚集的保安吵的，当时他们在我住的小木屋外用早餐。看看手机上的时间，6：02！
<br>
<br>翻开《服务指南》准备投诉，却被那薄薄的2页指南彻底吓醒----其中一页为小木屋内的财产及价值：空调8000元、电视5000元、遥控器500元、电话机400元、床单300元、毛巾50元、玻璃杯20元. . . . . .我突然觉得我回到上世纪70年代了！再开开那5000元的19寸彩色电视机瞅瞅，证实了我的感觉：居然有4个频道有声音，好象还是台湾的普通话，而图象完全是些黑白点儿在晃荡，我下意识地摸摸我的眼镜：确定我是否戴着眼镜。
<br>
<br>还是别挑剔了吧，叫醒LD后赶快结帐，上路走人。在总台的对话我不得不在这里展示一下：
<br>“小姐请问，去火山口如何走？”
<br>“喔――我知道，在港尾那边。”
<br>“是，是港尾，如何去？前边的水泥路结束后应该往左还是往右？”
<br>“港尾好远啊。”
<br>“我知道远，可有多远10公里？20公里？”
<br>“我不知道该如何说，你要坐公共汽车或摩托车去？”
<br>“不，我自己开车。”
<br>“那你还是坐公共汽车或摩托车去，港尾好远啊。”
<br>
<br>多么有用的信息啊！我一脑袋的雾水，灰溜溜的走回车里开始热车，在作车的行前检查的时候，心里面嘀咕着，可能我还没有睡醒。
<br>
<br>8：00上路了，无头的苍蝇一样，沿着蜿蜒的土路不紧不慢地开着，超车、会车都小心翼翼，路面狭窄且不平，除了警察外，只要有的东西通通上路了：小巴、卡车、微型车、农用车、摩托车、孩子、牲畜。
<br>
<br>没有路标，也别指望有路标，如果要有选择，就照车轱辘印多的选吧。不出一小时，终于到了一大的十字路口，除了路面变成水泥的外，居然还有交通环岛！车靠路边，买水问路----没有付出哪来的认真回答呢？4元钱除了换回4瓶矿泉水，还有更重要的：“向前！30里，全是水泥路！”
<br>
<br>果真如此，双向6道的水泥路，视野良好，但别急，你也甭想放松：整个公路的4/5都被当地人用来当作晒场了！留给车走的那1/5还是弯弯曲曲的，还要避让那些在路中间扬场的农夫，不容易啊。
<br>
<br>凭着以往的驾驶经验，你不能开车压过他们正在翻晒的粮食，轻则受到漫骂，重则引起交通事故，粮食的颗粒会让轮子抓地不稳，侧滑是经常出现的事情。
<br>
<br>穿过港尾镇，经过后石电厂，偶尔从左侧的窗外能看到大海，估摸着离火山口应该不远，放慢车速，努力地注意着路旁的每一个可能出现的路标和岔口。
<br>
<br>在离那标志10米处发现了它：漳州国家地质公园！顺小路进去约200米，买票，10元/人，再往前200米，有路障，正在观察周围的时候，路旁店家夫妻2人同时卖力地指挥泊车，我已完全明白其用意，索性主动问：“停车多少钱？”“5元。”“5元一次还是5分钟？”我继续问清楚，“一次”。爽快奉上5元钞票，落车走人。
<br>
<br>LD抱怨我不该主动“投案”，我只好讲述我的经验：你主动给，问清楚，这些刁民无话好说；如果不问的话，回来的时候车身可能被刮伤，车胎可能被扎，更可能被敲诈更多。
<br>
<br>稍往前，眼前豁然开朗。湛蓝的天空中点缀着一大团白云，在日光的照耀下，云团层次分明，海的颜色也呈现出深邃的蓝，近处的左侧的海湾划出一道优美的弧线，黄色的海沙和大海形成的了一道协调的高反差。再往右，一块黝黑的岩石凸立在岸边，在其周围，还有一大圈的石头，也是黑如焦碳。3~4个人在海滩上搜寻着啥，“鸟鸣山更幽”是指山景，而在空旷的海滩上点缀上三五游人，更衬托出静谧的气氛，又该如何表述呢？
<br>
<br>沿着小路下到海边，烈日下开始研究火山口，无论被海水的冲刷变得光滑如江边的鹅卵石，还是棱角依旧的，黑色，黑色，还是黑色。牡蛎是乎不太喜欢这样的颜色，它们更愿意选择褐色的玄武岩，它们斑斑点点的踪迹和身影更让碳黑的火山岩显示出大自然的神奇。再折身回到离岸不远的小山头，顺着小径拾级上到简易的观景台，清晰地看到水线边火山岩的纹理，在近处看到的一块块岩石，此时变得象人头上的发旋，规则地环状排列着，随着时光的冲刷，完整的环已经不复存在，残缺中不难想象当时的壮观。
<br>
<br>烈日下的我们已经感到眩晕，身上的衣杉全部汗透，脚下的滚烫的沙让我想起热锅里的蚂蚁，在最近的一处小卖部，拿起冰饮料狂灌，饥不择食：冰----就是现代文明。
<br>
<br>回到车上换件衣服继续上路，奔下一站：漳浦湖西镇赵家堡。
<br>
<br>路面时好时坏，但对驾驶者来说都不能有半点麻痹：路况差的时候需要小心，而路面宽的地方依旧充满着危险：别忘了，我们是行驶在别人的晒场上呢。和昨天的情况不同的是，在这个路段行车，普通喇叭作用明显，而警笛不是那么的见效。
<br>
<br>依旧没有路标，而对路口的选择的自信心在急剧的减少中，每有选择，停车问路逐渐成了习惯，虽然从来没有得到过准确的答案。
<br>
<br>车内凉下来了，对音乐也再不挑剔，一片接一片随它去吧。我也开始考虑回程的线路：不准备原路返回，是走国道还是上高速，走着瞧吧。转过一大弯，远处居然出现了久违的蓝底白字路牌！高级！
<br>
<br>“漳诏高速赵家堡入口”！哈――原来这里可以上高速！再看路牌，左边岔道去赵家堡。路面上晒的粮食依旧占据着大部分的路幅，而近守高速入口，心底犹如在家门口玩耍的孩子般的踏实，驾驶起来也更加得心应手。
<br>
<br>左弯，根据路标再左弯，上土路，一块小牌子挡住了去路：“道路施工，车辆禁行”。不想在烈日和黄沙中下车查看，想着牌子后面可能写着“欢迎你回来SB”的字样，毫不犹豫地掉头去湖西镇，先在那里解决肚子。
<br>
<br>午后的烈日让整个镇子死一般的寂静，好不容易找到一家尚在营业的小餐厅坐下，小老板穿着还算整齐，卖力地推荐当地的山珍野味特产：土鸡、石鸡（一种野生的青蛙）、溪鱼等等等等。
<br>
<br>肚子问题简单地解决了，在和小老板聊天的过程中，除了了解“顶坛新城”和“诒安堡”就在附近外，还知道了去“赵家堡”的路小型车依旧可以通过。
<br>
<br>按小老板指示的方向上路，沿着越来越窄，越来越崎岖的土路，来到一处看来有些年头而依稀能辩当年气派的建筑前，门前依旧是晒场，几个年老的农民在大门外乘凉聊天，这就是我们“五里三城”中的头一站----“顶坛新城”。建筑内除了几户破落的农家外，还分布着鸡圈鸭舍鹅棚牛栏，再穿过一堆堆的柴禾，找到了那楼门嵌蓝廷珍书“日接楼”石额匾。整个建筑看不出任何的管理、保护和修缮，原来的厅堂不少已经垮塌，形成一个个前后坐落的天井，只有那历经风雨的山墙和粗直的木柱木梁，还能嗅出些当年的气息。
<br>
<br>回到车上时，烈日烘晒下的车还没发烫，继续往前，没找到“诒安堡”，却回到往“赵家堡”的路上，也罢。
<br>
<br>“道路施工，车辆禁行”的小牌子依然孤独地站在那里，一点也不讨人喜欢，而它后面的一小段路更是要命。尽管我已经非常小心，还是2次挂底。
<br>
<br>赵家堡的门口空地没有晒粮食，却有不少的杂草，右侧有一幢丑陋的三层楼房，悬挂着“村委会”之类的牌子。听到车声，门开始纷纷开开，睡眼惺忪的老农门来不及擦眼角的分泌物，便匆忙地提出“售票处”的破牌子，再煞有介事地搬出自己坐的木椅：开张啦！
<br>一人10元，我没犹豫就递上钞票。不想老农民还意犹未尽，冒出3个字差点没让我喷饭：“停车费”。
<br>
<br>进得堡内，发现和普通的闽南农村没有任何的区别，一样的脏，一样的乱，看不出一丝“国家级重点文物”保护的痕迹。偶遇的村民以奇怪的眼光看着我们，也许一边看我们的展览，一边心里嘲笑着城里的SB。
<br>
<br>进入内城，验票的程序挺规范，空荡的一个个房间没有任何让人驻足的东西，在3楼的走廊上，贴着一组蹩脚复印机复印出的《清明上河图》，估计是要显示该建筑和那年头有点关系，而开始褪色的复印件，用胶水直接粘在墙上，有点象废弃的幼稚园墙上贴的小动物，是博物馆？是国家级保护？
<br>
<br>回程的路上，LP问我国家级保护是不是国家要拿钱保护，我一时语塞。
<br>
<br>写到这里，游记也该罗嗦够了，一路上波澜不惊，预定的目的地也基本走到了，作为周末的随兴出游，无所谓满意，也无所谓失望。
<br>
<br>不要认为我一直对所见所闻持着批评的态度，在这趟行程中，沿线孩子给我留下的印象是深刻的，他们有的用新奇的目光观察着老捷达，有的甚至用他们的单车和我比赛速度，他们的天真目光和充满童趣的动作我已经久违了。看着他们，我不由得想起我身边的电脑儿童们、钥匙儿童们。。。。。。
