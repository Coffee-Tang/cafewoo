刚进大一的时候杨戴副眼镜，很斯文很中文系――我当时幼稚的感受罢了。也许是因为他的优秀和活跃让我不敢靠近，那时候我把自己看得很猥琐不堪，丑小鸭当然会成为天鹅，但那时我还没有足够的信心相信自己是天鹅的雏。当年我没有写日记的习惯，觉得日记是很骗人的东西，能把一个人那一点内心骗出来变成毫无意义的文字，而且那些纤微的想法从心里走出来以后经历长途跋涉成了支离破碎的东西，很难再把它们和真实联系起来，也就是说日记里的东西很大一部分是陌生的，看日记的时候会惊讶，不会吧，我会这么想过吗？而且我平凡的生活和思想让我没有记日记的必要。虽然心里有杨的影子，那也不必成为日记，只当成幼稚的幻想而已。在我初中的时候喜欢一个男生，喜欢的那么痛彻心扉，肝脑涂地的，也不过只用只言片语作一些记录罢了，比如他向我借涂改液之类再平常不过的事，我能当成莫大的幸福――当时对幸福的概念也就是能和喜欢的男生说话吧，那么现在呢，我们再不会有那种青涩的单纯了，不过大学的恋爱游戏和性还有一层之隔，这也是我想在大学恋爱的原因之一。至少我们不会跨过那条界限，能够把身体看成神圣的物体，虽然也拥抱，但主要是为了取暖――有时候可以找到一个相互取暖的人，在冬天的夜晚，也未尝不是一种得到幸福的表示。我和杨就坐在广场的石凳上，我的背靠着他的胸膛，他环着我，抓紧我冰凉的手，抚摩我的手指，把下巴靠在我的肩膀上，脸贴着脸，有时用唇咬我的耳根。风挺冷的，我们正面迎着风相偎，我心里一直在说，让我们就这样变成塑像吧，变成万人垂涎的雕塑，在福州最大的广场上，就算我们被风干，就算沉默一千年，我认了！如果是在中学，和我喜欢的男生林（到现在我依然相信他也喜欢我，女人是最敏感的动物，她们的神经末梢总能及时尖锐地作出判断）可能最多只是手拉手，脸上打着笑的符号，心里灌了蜜糖，却不会四溢。现在，我对那种纯洁有点嗤之以鼻，毕竟是成年人了，在杨摩挲着我的手背时，难保当时两个人没有性的冲动。我对杨说，鱼认为我更适合一个男人。我还对杨说，我曾觉得自己应该属于一个事业有成的起码大我5岁的男人，他会知道怎么满足我和宠爱我。杨说那就不是简单的爱情了，他对你好只是因他对你有所欲求。想想也对。

开始断断续续地记日记是因为系主任那个地中海脑袋的老头子的一句话，他说大学四年是宝贵的，如果不用日记书写，四年就是一片空白。因为他是老头子，所以说的话多少弥漫着点过来人的味道，至少我那时带着一种崇拜心理――这让我再次承认我不过是个小女生而已，听话的小女生。原来一句话真的会影响一个人的生活，特别是从一个有资本说这句话的人嘴里说出。系主任（那么现在我就不叫他老头子了，他不过是秃了头，并不老，因此带点尊重地不让你们有什么思维定势）每堂课最大的讲资就是爱情，对于校园恋情他似乎四肢赞成，我想也许他经历过一段刻骨铭心的爱，这段爱就像烟头一样在他心里戳了个洞，那青烟却不断地袅袅升腾他就这样被长久笼罩着，日子久了，头发都渐渐被熏得少了少了，这更应证了他关于气体能破坏生理的理论，因为他曾说他的丹田之气上升，升到头顶，并且他有天眼，靠这两点他可以看到某某身上有阴气，他就不靠近那个人。虽然我坚持认定他头顶是那层挥之不去的乃青烟而非什么真气，但我不否认他说话的夸张幽默让我多少喜欢上他的课。

还有个原因是我那是开始了上网聊天。现实当中我几乎没有拥有异性朋友，和我在一起的是许许多多的女生组成的娘子军，浩浩荡荡，唧唧喳喳。但她们坚持认为我是她们中间话最多的一个，如果一个女人等于5000只鸭子，那么我一个就相当于8000只鸭子。5000也好，8000也罢不过是一个虚数，所以我并不在乎她们怎么说我，只是奇怪为什么要把女人比作鸭子呢，这是一种污辱性的比喻，但也许确有它形象的一面。我说过自己曾觉得自己猥琐不堪，在男生面前我沉默寡言，大约是很文静和鲜为人知的，所以他们不可能知道我内心的张扬。杨有一次上网对我说，我怕和你说话，因为你太冷了。在网上我当然可以无所畏忌的说，那你就多穿点再来找我嘛。可一到学校我又变成蔫黄瓜，从不和他说话，甚至当他不存在。我唯一的信念说来好笑，就是距离产生美。我坚持认为我们之间不会有结果，我把他看得优秀得近乎神圣，当然现在看他不过是长期呆在泥菩萨身边而受到浸染的童子罢了，没什么嘛。上网聊天以后我的生活似乎充满了前所未有的欢乐和兴奋，就开始有了写日记的冲动，甚至义无返顾地开始了第一次网恋，他叫隔江烟火――也许也是一个被烟头烫到的人――那个被我形容得讳莫如深的上海男人闯进了我的笔记本，第一次我成了一只受伤的小鹿，《诗经》里的“呦呦鹿鸣”大概就是说鹿受伤的时候却喊不出来的压抑和无力，我写，就算我的心碎得满地，每一片上写的都是“我爱你！”当我发给网友时，他对我这种撕心裂肺的爱顶礼膜拜。文字可以骗人，我不否认。

后来我和隔江烟火通电话，第一次时我的牙怕得打颤，说话都不利索，第二次我就默默听他讲和一个邻居女孩的恋情。我不喜欢听关于他的恋情，每个人的恋爱都是故事，听起来十分不实际，也许因为看多了爱情小说，我早已把它们混为一谈。我们慢慢淡了，是我吧，我这个人有浪漫因子，父母当年那种海誓山盟的浪漫没少遗传给我。二十多年前爸爸被断言心脏病，活不过两年，为了让他女朋友也就是现在的我妈死心，他拒绝见她。可是妈妈冲破层层阻拦以及爸爸的冷漠，单纯而义无返顾地说，就算只有两个月的生命我也要和你结婚！好多次我都把这种爱情当作全人类浪漫主义的颠峰，可我并不打算效仿，而是更多的是看到实际，当我看到隔江烟火同志的一点瑕疵，就认为他既然都不完美了我喜欢他什么？老祖宗的金无足赤，人无完人的古训我都抛诸脑后，一个完美主义的处女座女孩是不能容忍她心目中的神从天上掉入地下，那么“距离产生美”就更加肆无忌惮的占据我的思想，我的日记开始重复，并且坚持身体力行地和别人包括那些喜欢我的人保持距离。

不仅写日记，我还和两个好朋友保持书信联络。一个是泉州的小玮，一个是厦门的虾米。信件是我网恋的心灵史，当时说得最详尽的就是感情。小玮大一的时候玩笔仙，说她大三会交男朋友，现在马上要大三下学期了，不知道那个预言准不准。我和小玮的交往是从高三的下半学期开始的，想不起来是怎么开始老呆在一块，和她一起去楼下自习一起到车棚牵着自行车聊天，每段开始都那么模糊，隐隐约约只剩下过程，断断续续的，连不到一起，那么也许和杨的过程也会变成以后记忆的片断，把第一次的约会记成是第三次之类，成了补丁，随便缝补在我们生命的某一个层面里，然后用将来华丽的希翼掩盖起来，疤痕叠着吻痕，斑斑驳驳的好像月光下的树影晃动，有点残忍。他第一次送我回家临别时在我的左脸印下一个吻的记号，我把他推开，却没能推开那个吻别。这令我多少有点不喜欢，似乎是不喜欢他的轻浮又或者说是小说里的模式，我该承认自己的不解风情和之前的伪浪漫。我们一天里相处了11个钟头，我极尽表现出自己的温柔可人和体贴的一面，我给他买饼干，喂他薯条吃，帮他捶腿等等，其实只是希望自己变成他生活里一道彩虹，我不敢考虑将来。我把将来交给父母之命，而现在，我必须把握自己的自由，控制仅存的属于自己的时间。杨说要我父母把我移交给他，这些话说出来多少有点不近人情，当然以后我是肯定要从父母的统治当中独立出来，我开始同情父母，同情二十多年的亲情付出。亲情是海，广博浩淼，他们的爱让我无地自容，我痛苦地认识到自己的所作所为必将深深刺伤他们的心。和杨在一起时我有沉重的负罪感，我害怕遇见父母，害怕我的亲情因为爱情而破碎，不过现在，我爱父母爱得深过一切，背叛他们是我这辈子都做不到的事情，如果他们反对，我想我会义无反顾地回到他们身边。这并不是说我的父母霸道，事实上他们是很有水准的中年人，在我的口中他们是超脱一般平民夫妇的世俗的人，一种理想。也正因为作为商人的父亲和一直是个领导的母亲是消磨了梦想的实干家，我想他们为我考虑的会是实实在在的东西，他们在乎结果在乎我会得到多少收获多少好处，我才更觉未来的渺茫，梦的渺茫。

如果我告诉鱼和杨已经开始了，他一定说我又犯了一个严重的低级错误。鱼，是一个把自己包裹得很严实很深到最后不知道自己到底是什么的男人。曾几何，在绕来绕去的电话线路中我以为他是非现实的人物，有时候也担心他像《黑客帝国》里的基努·李维斯一样从电话的一头走到我面前。我想他并不希望我开始，因为有开始的事必将有个结束。在网上我说，我一点也不烦恼，只是寂寞而已。你不也是吗？寂寞加寂寞只会有两个寂寞。可杨认为只要坦诚想对的两个人就不会寂寞了。就在这样的互诉衷肠中我们告别了在喧闹的教室里彼此遥望，然后带着遗憾和苦闷转身向两个方向各走各的路的层面，昂首阔步迈向新的征途，为了共同的事业从五湖四海走到了一起。我没有拒绝他拉我的手，而把手放进他温暖的手心，似乎一切都再自然不过。我看到有篇小说题目叫《在开始的地方结束》，觉得很现实，像一个圆形，一道围墙，我们给自己划定界限和圈套让自己走不出去，而现在的我正开始画圆，我和杨已经捅破那层薄薄的纸，我知道珍惜现在是最聪明的做法，可我喜欢的依然是得不到的东西，得不到的才是我的最爱和目标，我不懂珍惜。杨说他喜欢玩猫抓老鼠的游戏，我问那抓到以后呢？这个问题让我想起一个好朋友秀兰。秀兰虽然和晓峰在一起了，但她告诉我，如果那个她爱了8年的男人刘景再次出现在她生命中，如果他说爱她，如果到那时晓峰还没占据她心里依然属于刘景的那部分，她会毫不犹豫地离开晓峰。秀兰说她也知道晓峰会很可怜，说也知道刘景不是最好，他们在一起以后也许不如和晓峰在一起幸福，但因为现在她得不到刘景，才固执地爱着刘。爱本来就是很辛苦的，恋爱中的人都在盲目给自己设那堵围墙，这一点上我们是相似的。

