皇叔篡位。大批的士兵涌进宫里，熟悉的面孔彼此撕杀，用冰冷的兵器结束一批批身世潦草的生命。温热的血喷溅出来，干涸，空气里弥漫着甜腥的味道。那些穿着紫色官服，胸口有麒麟奔腾的大臣们依旧念着祈福的话，用横尸堆砌起另一座象征着神圣和权力的龙椅。父皇被打入了天牢。

乳娘说要回去看看，很坚决。

