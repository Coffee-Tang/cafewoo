帐幔外的景色一点一点变得陌生。荒凉的乱石堆和沙砾，稀疏的灌木丛，野草杂芜。空气里冬天的气息已经很深了。狂风一拔一拔地在空旷的黄土上肆意地穿行。扬起漫天的尘土。

马车颠簸得厉害。手腕上的银镯子一直发出叮叮铛铛的声响。乳娘搂着我轻轻地哼着曲。偶尔有沙砾从帐幔的缝隙里飞进来，打在脸上生疼生疼。我掀起帘子的一角，看到那座我生活了十四年的城池在背后变得面目模糊，风沙狠狠地掠过去，它伫立着，寂寞而苍凉。我已经看不清它的样子了，连弥漫着奢靡的空气也已消失殆尽。

我合上帘子，靠在乳娘的肩头。我轻轻地说，乳娘，你终于能带我离开了。

驼队很缓慢地前行。乳娘握着我的手不说话。阳光漆黑而明亮地洒进来，我看到乳娘的眼里有很沉的忧伤。

二

我叫花葬。出生在三月。娘亲说我出生前的一段时间里她总是有重复而纠缠的梦魇。旷远的黑土，空气中潮湿的味道，天边隐约的绝响。不断闪现的容颜模糊的女子。粉红粉白的花瓣从诡异的空中破裂开来，纷纷扬扬地坠，然后慢慢地侵蚀和倾覆。流离失所的苍茫。这些都是后来乳娘告诉我的。娘亲在我很小的时候就已经离开了。乳娘说那是一场没有归期的旅途。

娘亲把那青苔样暗淡和恍惚的忧伤刻进了我的名字，像掌心中沿用三生的胎记。我轻轻念它的时候仿佛能看见三月里桃花浩浩汤汤地漫天飞扬，无所依傍的茫然无措。而乳娘总是一遍一遍地唤我，花葬，花葬。像是延绵不绝的死亡。

从我记忆开始很清楚的时候开始，我就生活在一个有大朵牡丹花的城市。每年牡丹花开得繁华而忧伤的时候，我生活的红墙里头就会飘散满浓郁的香气。那些香气和后宫嫔妃们的胭脂味在空气里一丝丝地纠缠，然后变得很迷离。皇宫的后院里有太多郁郁寡欢的美丽女人，她们浓妆艳抹，在花园里无所事事地等待衰老，彼此间高傲地抬着头。而她们的眼神总是游移的，她们抓不住能让自己活得光鲜的那个男人，于是变得歇斯底里的敏感。乳娘说娘亲和她们不一样。娘亲永远是美丽的，很柔软很干净的美丽。

我是这个国家的公主，一个被遗忘的公主。在红墙的院子里一天天长大。乳娘常常会在暮色苍茫的时候给我讲关于娘亲的往事，我总是仰着脸很认真地问，乳娘，什么时候我也能像娘亲一样离开。乳娘的眼神在一刹那变得忧伤，像牛湿润的眸子。大片空荡荡的疼痛摊在我面前。从小我就知道了什么叫无可奈何，什么叫无能为力。那些被岁月抹煞了的往事啊，陈旧得如乳娘指间的念珠，泛着黑色的光泽，檀香木的气息淡淡地散发出来，久远的样子。它们锐利地一下一下刺痛我，而我只知道所有故事那端系着的女人，是我的娘亲，一个很早以前就从我生命里开始退出的美丽女子。

我的父亲是这个国家的王。他是个懦弱的男人。我总是看见他所宠爱的妃子在一段时间后就消失匿迹了。偶尔会有死讯在其他妃子间流传。于是宫里有越来越多陌生的面孔，而那些面孔总会丧生在另一张更会妖冶的面孔的权势之下。父亲所喜爱的东西注定会被那个更为恶毒的女人所扼杀。

我在红墙里狭窄的天空下一天天长大。乳娘教我绣花和舞蹈。乳娘曾是这个城里绣红的好手，在大红的绸缎上绣些粉红粉白的桃花。那些花在乳娘指尖小朵的安静的盛开，不喧哗。而我喜欢绣一种叫藤脂的白色小花。一种关于佛教传说中没有怨恨平淡地等着自己转世的花朵。桃花总是让我想起娘亲，想起那个诡异的梦。梦里桃花沸腾，女子的容颜和年年岁岁被凋零的花瓣湮没。一泻到底的荒凉。我始终觉得那个隐约的女子是娘亲和我的影子，有那样宿命的死亡气息。决绝的美丽。

深夜的时候，乳娘替我梳理那顺滑如缎的长发。她说我长得越来越像我的娘亲了。我看着昏黄的铜镜中自己模糊的容颜，想像一个同样容貌的女子怎样白衣飘飘地舞，舞，舞。乳娘说娘亲的舞跳得极好，在风中像艾草样被抚成各种形状。当年父皇就是看中了娘亲的舞和美貌才那样宠爱她的，可惜……每次说到这里乳娘就打住了，愣愣地看着远处出神。我不问她就一直没往下说。

我不问是因为我知道乳娘不愿说，从小生长在宫里我很清楚不该知道的东西就一定不要问。这是生存的规则。有些人就是太贪心了才会丢掉性命，比如那些自恃的受宠的妃子。因为这个国家真正掌权的不是那个穿着龙袍唯唯诺诺的男人，而是帘珠背后那个笑容阴险的女人。

我常常在自己的衣服上绣一些藤脂。

藤脂是带着我的记忆的。它就像我喜欢的那种只有在人烟稀少的地方才会出没的青鸟。忧伤。记得曾有人向父皇进贡过一只青鸟，关在精致的笼子里，眼神黯淡，那些风里的明亮退到了最深处，羽毛没有光泽，声音不再水波般荡漾。我看着它心里疼痛。我觉得我的前世就是一只青鸟，今生却被禁锢了，被精致的物质包围了，没有出口。几天后，青鸟死了。眼是开着的。我不知道这是不是所谓的死不瞑目。想起乳娘的话，青鸟是不属于这个尘嚣的世界的。我为青鸟做了个墓，却没有眼神可以温润它的尸体。我知道，我的那些还没有打开的年华，注定了会像孤魂野鬼样飘荡飘荡没有落脚的地方。

青鸟是我见到的第一只鸟，也许也是最后一只了。而对于父皇而言，他只是少了一样可以耍乐的道具，无关悲喜。红墙里或苍白或昏灰的天空总像发着炎，从来没有鸟群飞过的痕迹。我常常听见它们潮水样扇动翅膀的声音从我不曾触摸过的天空里掠过，然后飞远。

我很安静地长大了，不再问关于娘亲的往昔，不再想着像鸟样拥有自由。我知道自己是没有未来的孩子，就像那只青鸟失去了方向。

有一年秋天，宫里看似平静的生活起了波澜。父皇最宠的妃子被处死了。很残忍的手法，明晃晃的刀光剑影划出长长的伤口，柔软的身体被砍去了双腿和双手，套着白色的衣服装在黑色的罐子里，头上的血凝固了大块大块地掉下来。乳娘说那叫人棍。

一天夜里我问乳娘，这样的日子什么时候才是尽头。乳娘抱着我叹气，身子却在微微地颤抖。乳娘曾告诉过我，人死了以后会变成很深很深的紫，看起来像是黑色，就像那些陈旧的念珠的颜色。那是人积累了一生的压抑。

我突然有一种预感。它像暗黑的小巷子里脚步激起的回声样回响不绝。

一个深夜，乳娘突然被父皇一道暗旨召去了。天很黑，月影摇曳，风过竹梢发出飒飒的声响。屋里的灯盏被吹得明明暗暗，鬼魅一样。我坐在床棱上轻轻抚着那串黯黑的念珠，上面有乳娘指尖的温度。

