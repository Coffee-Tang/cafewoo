我的预感突然就被证实了。

乳娘从柜子里拿出一个精致的木盒子，手巍巍颤抖着打开。里面是块绸布，包着一撮青丝。乳娘说，这是你娘亲唯一的遗物。我轻轻地抚摸着它，像抚摸着娘亲毁掉的容颜。这一刻我感觉自己是如此地接近死亡。落满紫藤枯叶的古井里一点一点失去光线和水分的女尸，长发披散悬在横梁白绫绡上没了呼吸的女尸，怀了父皇的孩子却被施以肉刑的女尸，被砍去了手脚做成人棍的女尸，还有乳娘说的娘亲样深知父皇的怯懦和无能，毫无怨言地走进死亡的女尸。地里漫出阴郁的气息，我看着它森然地上升上升像看着自己即将长大的年华一样不安。我突然明白了女人的美丽从一开始就不属于自己。要么孤独地美下去，一直到那种美丽枯萎得万劫不复，被人们遗忘，就像乳娘。而另一种美丽却注定会带来死亡和灾难。我轻轻地对乳娘说，如果有一天当我们不再需要压抑的时候，我们已经崩溃了。

说这些话的时候，我看见了那年冬天的第一场雪。纷纷扬扬地洒下来，没有声音。

暗潮汹涌。

我在雪地里起舞。雪花狂沙样从我的指缝间穿梭过去，在掌心刻下一道道印痕。冷，红墙里的谄媚变淡了。天地间素净得像我衣服上绣的藤脂。我想像着娘亲柔软的身体在起舞时是怎样倾国倾城的美丽。旋转，旋转。我取下头上猩红的羽饰，掀起的长发像泄露的往事样漫天飞扬。

华丽的雪，华丽的衣裳，华丽的那个年代的花香，一点一点被磨损。皇后说，母亲柔软的身体是有罪的。毁，毁，毁。

一瞬间，所有美丽的圆润的谎言灰飞烟灭。

三

驼队带着我残损的幸福走，走，走。

那一年我十四岁。

行路一个半月。

乳娘带着我到了一个叫若羌的国家，靠近楼兰。很矮的土墙，有颜色明亮的花，漫山遍野地开，在风中掀起一片华丽的色彩。进城的时候看见很多眼神纯朴的人，脸上有风掠过留下的一道一道的印痕，蜷缩着时间的记忆。

