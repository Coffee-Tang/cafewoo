“曾经有个朋友跟我说,如果一个人抽的烟的数目和年龄相乘,大于20万的话,就会死.不过,当时我一点也不害怕,不知为什么,反而很安心.有时,自己也很恼火,千万次都想要戒掉,但,一开始工作便会不停的抽,一天,最起码2包.”她摆了个2的手势,已经猜到了我的想法.

“算算,现在也快有15万了.有时真的会想,有一天,我就如风筝突然从天上栽下来,从此再也飞不起来……”

她愣了一下,又抬头跟我说:“我想还是顺其自然吧!正如同披头士的最后一张专集里所写的:当这个世界上所有伤心的人心向一起,将有一个答案是顺其自然,因为尽管要分离,人们仍会看到,将有一个答案是,顺其自然……”

“那,我只能希望那不是真的……”我苦笑道.

再见寄冰的时候,是在4月.我看见她吃了什么药,却又在抽烟.她说是肝和胃上的问题.

“那还抽烟!”我很气愤.

她仍旧微笑,说:“药是医生叫我不得不吃的,烟却是我不得不抽的.”

“为什么?”

“因为我的工作……”

“你的工作是……”

“作家.”

“身体更重要吧.写作是可以放弃的.”

“你错了.抽烟,吃药是身体的事,而写作却是灵魂的事!”

我一时无话可说,眼看着她走出咖啡屋.耳边回响着披头士的音乐.

