他们错过了第一次。

吴可一直在父母家过完了元宵节才回到自己的住处。她带回了一本挂历和一本存折。农历二月份她的生日快到了。父母问她是不是回家过。她说想一个人自助旅行。他们就要送她来回的机票。母亲说刚好有本活期存折里还有些钱，就都拿给她了。

事实上，她根本哪儿都没去。只是呆在自己的窝里。电话不接，门铃不应的过了两个星期。

她换上新的挂历，在自己生日的那一天画了个蛋糕。到了那一天，她到楼下的便利超市里买了一包阳春面和一打鸡蛋。中国要入世了。现代人流行向老外看齐，连生日也都开始过阳历的了，还顺便开个party.吴可说那是劳民伤财。

她依旧在农历生日的时候吃一碗长寿面和两个平安蛋。从某种意义上，可以说吴可骨子里是个守旧并且恋旧的人。用阿苏的话说，吴可在用最守旧的方式诠释最前卫的生活――和各样的男人交往同居。

日子过得很快。过了二月，是三月。桃花纷飞的季节。吴可刻意提醒自己少出门。这样的时节里容易犯桃花。她下意识地低头看着掌心里零乱的纹路。曾经有个相命先生给她看手相，说是，致命伤是多情。她垂下眼帘。

她多情吗？她不知道。她一直以为自己是个感情平淡的人。甚至可以用心如止水来形容，在阿城离去之后。她隐隐觉得难过起来，她已经很久没有想他了。

正当她冥想之际，电话铃声突然响起，打断了她的思绪。是一个朋友的电话，过两天要结婚了，要请她喝喜酒。寒喧了一阵，终于挂断了电话。

她开始回想这位儿时的玩伴。那时的脸上有几颗小痘痘，几分初恋的甜蜜。和自己挤在一张单人床上，彻夜倾诉心事的纯真少女仿佛就在昨天。昨日情景一切都历历在目。而如今，比自己小两岁的她就要结婚了。

反观自己。卡在青年与中年之间，不尴不尬地老去。她突然觉得自己又老了许多。眼角爬满了皱纹。下意识地伸手摸了摸那儿。她摇了摇头。嘲笑自己的神经质。

接着她又摸了摸荷包。哎，红色炸弹。

出门的时候，她犹豫了一下。最后还是没有随身带几罐酸奶。她决定到朋友的喜宴上喝个够。好歹也把红包的本儿喝点回来。对于这点，她还是有相当的信心的。

吴可说，如果谁养得起我喝酸奶，我就嫁给他。

所有在场的人哄堂大笑。这是在朋友的喜宴上。服务员小姐问她要什么饮料，她说酸奶，小姐说没有。她不习惯喝别的饮料，只要了杯冰水。一边喝着凉水，一边看着新婚夫妇的甜蜜样。感慨之余就说了这么句话，闹了个不大不小的笑话。

