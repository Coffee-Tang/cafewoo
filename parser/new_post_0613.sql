她终于走了。

屋子里恢复了死一般的沉寂。又一次只剩下她一个人独守空城。人们总是憎恨离别。而远离它的最好办法就是躲避相聚。只要在一起，就有分开的那一天。除非，她不在乎对方。

所以，她一个人住。

吴可一个人生活。这样的生活说不上好坏。她不是那种一个人住就觉得是孤寂的女子。事实上她有点乐得一个人清闲。年前刚和第七任男友分手，是该好好休息一下了。这个数字听起来，让人觉得她滥情。可她从来不觉得自己是花心的人。

事实上，她是个被动的人。有人追求她，只要他不讨厌，一般她都不会拒绝。当然，如果对方再提出分手，她也欣然答应。她甚至不知道这些人在她生活中是扮演什么样的角色。总之，他们都是这么样的开始并结束的。

或者，他们叫过客。

这样的情形还会持续多久，她自己也不知道。她是个懒散的人。也不想做太多无谓的改变。觉得这样也挺好。有时候她笑自己是耐得住寂寞。

日子一天天地过去，农历年快到了。她算了算，有点害怕起来。

是的，她突然觉得自己老得太快了。元旦的时候老了一岁，过了农历年又要老一岁，农历二月份是她的生日，到时候还要再老一岁。还有若干的离别也会让她老去。她发现自己正在以比常人快三倍以上的速度迅速老去。

她常常站在镜子前，看着看着，就看见了一位满面皱纹白发苍苍的老妇人。她哑然失笑。

她突然想看看今天是什么日子。这才发现墙上挂的还是去年的旧历。才翻到七月份。已经积上了一层灰了。她从墙上将旧历取下来，可新挂历还没买，换不上新的。墙上陡然添加了一大块刺眼的空白，让原本空洞的屋子更加苍白。她泄气地再将旧历挂会原来的地方。她决定回去拿一卷新挂历回来。

