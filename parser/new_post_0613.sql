吴可是个自由职业者。这个称号时下正流行得如火如荼。似乎还算得上是一种时尚。可她自己知道这个美其名为自由的职业，其实就是“吃了上顿顾不了下顿”的代名词。用她自己的话说，她是“以卖字为生”混口饭吃。

