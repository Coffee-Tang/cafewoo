说到酸奶，只要是吴可的朋友都知道她“嗜酸奶如命”的习性。她喝起酸奶来，用一个词来形容就是“拼命”。情形大概能赶上三天三夜没吃过东西的饿鬼见到食物的样子。更厉害的是，她可以喝得不露痕迹。直到发现边上大堆的空盒才意识到她喝了多少。用一个准确点的参数，大约是两天一箱。

奇怪的是，身边的人都没发现她什么时候开始了这个习惯，也不知道为什么。阿苏说，你的血都快成奶白色的了。她乐得不用去献血。谁也没办法。

