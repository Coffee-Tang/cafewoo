她决定不坐公车。要徒步走回去。只是因为突然怀念起这座城市的味道。她几乎可以闻到女人脸上的彩妆和男人身上的烟草。她快步地在大街小巷里穿梭。像一尾自由的鱼。她大步走路，让人以为是在赶时间。即使是经过大扇橱窗时，不经意回头看一眼自己的侧影，都要相信了自己的“行色匆匆”。

她终于在路边的广场上休息。坐在长凳上，掏出包里的酸奶，一边喝着酸奶，一边睁着眼睛看着广场上来来往往的人。她看他们的鞋，他们的衣服，他们的脸和他们的眼睛。一个个看过去，不厌其烦。

她突然觉得恍惚起来。阳光细碎地洒落在过往行人的身上脸上眼睛上。还有广场边上的车来车往。这一切都在无声有序地上演着，像是天堂。阿城住的地方。她眯起眼睛，就看见阿城站在街对面向她挥手，然后跑过来――四周的一切都暗淡下来，只有他的身影――她在瞬间迷失了方向，一脸茫然。一直感觉到小腹有股涨意。她才回过神来。她看见身边的一大堆酸奶空盒，就知道喝多了。她开始东张西望。

