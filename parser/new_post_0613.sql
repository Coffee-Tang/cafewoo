吴可。穿过马路的时候，她听见熟悉的呼唤。她转过身，看见阿城站在街对面，一脸灿烂的笑容像这春天里的一束明媚的阳光。他一边向她挥手，一边跑了过来。

四周的一切都暗淡了下来。她只看见他奔跑的身影。在他离自己还有两米远的时候，她快乐地张开了双臂，等待着温暖的拥抱――“砰”――他的身体撞上了来不及刹车的车辆，被高高抛起――她快乐的表情甚至还来不及换下，刹时冻结在脸上。她睁大眼睛，却流不出眼泪。张开的嘴唇说不出话。她突然无法呼吸，喘不过气――

她几乎是挣扎着醒过来的，发现胸口压着一本厚厚的小说。把小说放回床头柜上，看了下夜光钟。时间显示为凌晨两点半。她却无论如何也睡不着了。她转过头，看着睡在身旁的阿苏。在暗夜里习惯了黑暗的眼睛，渐渐看清了阿苏紧闭的眼。听着她平稳的呼吸，吴可心里一阵难过。

阿苏明天就要走了。她又做了那个梦。这么多年，每到离别即将来临的时候，她总会做那个梦。也许是潜意识里对离别的恐惧。她叹气。

阿苏在一家外企工作。圣诞节和元旦连着放了一周的假期，她就从相邻的城市过来看自己。到今晚为止，她的假期结束了。明天也要回去工作了。

她感到不安。她不知道几时才能再见到阿苏。虽然彼此就在相邻的城市。可是，自己和阿城就是在面对面的时候走丢的，更何况是整天在四处奔波的阿苏。

吴可终究没有送阿苏。她睡过头了。虽然是故意的。

