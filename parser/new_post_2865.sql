<p>某一刻安安忽然很想掉下泪来。</p>

<p>这是安安一贯生活的状态。
<br/>在空荡荡的房间里听歌。很寂寞很欢乐的各种声音。安安总是对着玻璃瓶内娇嫩的花儿哼哼叽叽地学着唱个不停歇。安安唱得很入神。安安相信每一朵鲜花都颗有静如凉水的灵魂。安安总是坚持自己知道它们究竟想要些什么。安安习惯将点燃的蜡烛置于那一簇鲜花的旁边，安安曾对日说它们是需要温暖的。</p>

<p>可安安不知道自己想要的是什么。温暖。也许是一样的。也许没有区别。</p>

<p>在这个城市里一个人生活。夏季的某个夜晚，安安看着某个男人流着眼泪跟自己说告别。打碎的只是最初的幻想，灌满的全是绝望。安安刹那间在想所有的一切均与眼泪无关。安安于是明白人有的时候很容易入戏。在刀刃上舞蹈，就这么看着鲜血流了下来，疼痛，便只有沉默。还好，游戏早已结束。不再单纯，是否就此充满怀疑？</p>

<p>安安听人们常说，生命是一本书。一日一页。一字一刹那。当翻阅之时，故事便此展开了。安安害怕自己今后的路会复杂成一本厚重的书。也许，一篇简短的小小说就足够了。安安希望自己可以快乐。而人总是因为简单而快乐的。
<br/>谁可以让自己简单？这不仅仅是温暖。</p>

<p>很难忘记那部名叫《金色池塘》的电影。美的画面。丝丝入扣的情节，讲的是一对老人相依为命的内容，会忍不住就泪流满面。安安和日是紧握着手心在一起看这部片子。磕碎的瓜子壳掉得满地板，香甜的味道却留于齿间。午后的阳光透过玻璃就此洒了进来，照得人暖暖烘烘，心也跟着喜悦开来。
<br/>永远会是这瞬间的感觉么。</p>

<p>安安和日有联系之前，只是在网上做着淡淡的，有些陌生的朋友。
<br/>第一次相约外出游玩时，安安穿着贴满补丁的牛仔裤，紧身无袖棉白的T恤。安恍惚地想起17岁那年，一个人外出打工内心所有的惶然和不安。为什么这么些年过去了，依旧是这身打扮，依旧一无所有。
<br/>日对安安说，你这敏感聪明的孩子。有些莫名其妙。
<br/>安安在心里想，我只是孤独。
<br/>陌生的小镇。凌乱的街道。灼热的太阳。安安望着金灿灿的阳光下日流着汗水喜敦敦的神情。日喜欢新鲜的每一样事物，喜欢在大街上东张西望。安安却惧怕热闹人多的地方，安安后来就习惯了，安安慢慢地就喜欢上逛街。
<br/>原来快乐就是这么简单么。抹平绝望如此的容易。</p>

<p>日送给安安一片CD《回声》，有一首歌《永远有多远》。安安听这首歌的刹那，忍不住就流泪。有温度的眼泪。</p>

