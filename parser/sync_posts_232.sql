陈潘那部电影的拍摄不紧不慢地进行着。在正式开拍之前，我们天天被陈潘叫到厦大排练，而且专挑公众场合，说是锻炼临场心理。大家心里把他骂了个死，不过并没有人表现出来，陈潘毕竟是陈潘，有他的张扬和固执，一群人在一起这么多年也算把他的脾性摸透了一些，破坏一个工作狂兼完美主义者的兴致及计划的下场我们是知道的。那期间我每周有三个早上在经济学院听课，想来真是郁闷至极苦读十二年一心想进北大中文系从此永远摆脱数学，没想到最后阴错阳差进了终生与数学为伍的专业，实在是天大的讽刺。于是那阵子常常一大早起来浑浑噩噩灌了一早上的高数内容，下午就到陈潘指定的地点一遍遍排练。有时遇到美院下午有课喵喵和佳树都不能来时，就要一直等到晚上。来不及吃晚饭，喵喵就带些偷偷在宿舍里煮的玉米，用尖尖长长的小刀钻棒心而过，拿在手里啃，一次能吃下三四个。排练完还要赶十点的最后一班公车回家，妈妈晚餐时煮的粥，热热地喝一大碗下去，胃就暖暖的很舒服。就想起林恺胃病住院的时候也是这样地喝过喵喵煮的粥，我的小女孩喵喵也开始给心爱的人温暖了，然而她为什么不让他给她温暖，为什么就不能得到幸福。。。。

也去鼓浪屿取景。四月生长起来的青草插在老巷子的每一条石缝每一处墙头，有着高大雕花石柱和被夕阳晒褪了色的棉布窗帘的租界洋房，哥特式的大门上钉着“１９１４”的厚铜牌子；被疯狂蔓延的枝叶淹没的小路隐晦艰深地引往尽头那座燃着供佛用红烛的斜顶瓦屋，潮湿的花香烛香在闷热的五月雨季里氤氲成杜拉斯笔下湄公河石港的气味。这样的季节和地方总是滋养着无数工夫甚是了得的蚊虫，我不得不把花露水大把大把地往手臂和小腿上抹，在奇怪的浓烈香气里总是嗅到童年夏天的清晰往事。

鼓浪屿数不尽的洋房和老巷让人在白昼里晕眩。就有一次我和佳树为了寻找曾经到过的场景而迷了路，明明感觉就在他们那群人的不远处但就是这样离奇地和他们走散。我们在模糊意识到徒劳时不约而同在某所洋房大门口的石阶上坐下。其时太阳从蒙了几天的阴云下微微露脸，不偏不倚照在我裸露的脚趾前方５厘米的石板上，然后慢慢地移步，在佳树脸上投下的阴影也微妙地变化。即使是在这样的光线下佳树的眼睛仍然明亮干净如清晨树林里的阳光。是的下午四点二十三分的阳光那么恰好地在佳树的眉头和鼻翼轻轻印下灰蓝的翦影，那么恰好地不是其余任何人的组合而是男女主角佳树和露卡迷了路与大家走失坐在石阶上晒到这样的太阳。

我们什么都没有说，各自的脸上融着模糊微倦的笑容，就这样安静地坐在石阶上晒太阳，直到他们找到了我们。

４

去了十几次厦大和五六次鼓浪屿之后排练差不多进入了尾声。接下来的几天陈潘忙于dv机、灯光和后期设备的奔走。其时他把最后一个难题交给我和喵喵。那是电影中的女子与镜中人对话的大量台词的撰写。陈潘把这个任务全权交给喵喵，之后便忙自己的事情去了。第二日喵喵黑着眼圈交给我一份台词，说是边听选好的背景音乐边酝酿了许久的情绪熬夜写出来的一组句子，让我回去修改。我欣然谢了她。

原谅上一段让它空白。我只是不知道如何表达。从前曾经在面对突如其来的青涩恋情以及它同样突然的消失时束手无策，觉得爱情真是可怕它不是一件东西握在手上就是你的，有时候你还来不及想清楚还什么话都没有说它就一语不发地离开了。友情也一样，都是生命中的无常，那些疼痛和眼泪，在依然忧伤的年龄里。――总之那以后喵喵和我就开始冷战了，在我还未明白发生了什么的时候。但我知道那是我的错，我的自以为是和自作主张伤害了她。而我们都只是年轻而骄傲的孩子，这样彼此依偎然后彼此刺伤。然而我不是故意的真的不是，我只是不知道要如何向她说对不起说我很在乎，像小时候打碎玩具的孩子，那么难过却无能为力。在聚会中喝多了酒爬到楼顶水箱上哭，夜风冰凉，星星如同在天空漂浮的孤岛的影象。大声大声哭泣，是这样好的朋友，这样对彼此重要，为什么她就不明白我的爱。

