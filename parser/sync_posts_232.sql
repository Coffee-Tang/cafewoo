她终于杀死了镜中的自己。

血，一滴一滴，滴落在已被砸碎的面具上，越来越多，泛滥了一地。

用来充当血的是佳树带来的水彩颜料，深红色的一小碗，没有血液的粘稠度，稀稀地贴着白瓷碗沿滑下来，如同褪下的丝绸裙裾，凉薄而无力。我用很久没有弹guitar而慢慢长长了的指甲捞起红色颜料，让它们从指尖滴到砸碎了的一半花彩一半纯白的面具上，靛青烟紫雪白殷红，华丽妖冶到近乎残酷。明明灭灭的烛光下所有人都盯着我血红的指尖和血滴落的鲜妍。我知道他们都被魇住了，包括我自己。

那天晚上我的白裙子沾到了喷溅上来的红色颜料。那些血红色生出无数的细小爪子死死抠住白色的棉布，固执地，瞪着我。隔天的整个下午我都在搓洗那些血色斑点，一遍又一遍，而它们不肯褪去。四点的时候阳光转过一个角度从百叶窗照射进来，刺痛了我的眼睛，我突然就泪流满面。喵喵。佳树。我的南方夏天。这样一个有眼泪的无望的下午。原来都是会留下痕迹，哪怕它们不是伤口，

终于它们一点点一点点晕开来，那些会顽强地攀住一段记忆的红色。

终究是会这样过去。这样遗忘。

陈潘打来电话的时候，已经晚上九点多了。他说露卡你现在马上来一下好吗，我们在白城海边，喵喵她喝多了。

我只带上钥匙就冲出家门。

到达白城找到他们时，海滩上已经留下十几个空啤酒罐子。陈潘一抬头见到我，刚想站起来说几句话为我和喵喵之前的冷战打打圆场，喵喵已经转过来看着我，眼睛红红的。

露卡，林恺和系里的一个女生在一起了。他还是什么话都没有说。

我看着她。瞬间脆弱的嘴角。我走过去，想握住她的手，是的喵喵我只想握住你的手，你会没事的，会没事的，对不对。我们都会没事的。

她已经先一步紧紧攥着我的手，攥得我的手痛痛地发红。我看见自己长长的指甲，上面还有没洗掉的红色颜料。是的它们就这样留下痕迹。谁来教我们与遗忘。

突然喵喵的手机响起。她瞥了一眼上面显示的号码，脸色变得更加苍白。是林恺。她抬头求助地望着我。露卡你帮我骂他好吗，狠狠地骂，骂什么都无所谓。我只是没有力气开口。

