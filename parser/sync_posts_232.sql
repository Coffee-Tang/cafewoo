林恺你如果喜欢喵喵或者不再喜欢她都说出来吧，都告诉她吧，不要再让她这样难过了好不好，我不要看见她悲伤，再也不要……

露卡你在说什么啊！喵喵的脸刹那刷白，抢回手机。林恺吗我的朋友喝多了乱说话，对不起对不起……

够了喵喵，你不要再骗自己不要再为他着想了，我们受的伤害还不够吗？我听见自己愤怒的哭腔，眼泪瞬间迸发出来。我只是无能为力，无能为力。。。

露卡……喵喵转身抱住我，泪水汹涌。

我和她紧紧地抱着，像孩子一样大声哭泣，在无人的海滩上，夜空中星星仓促而绝望地浮游在不明的靛色里。Still　cold　like　the　stars。所有的误解和愤怒瞬间退却到五百年前的古井里，在那里消失殆尽。只剩疼痛，青春的午夜梦中悄悄拔节生长的疼痛，薄薄的刀片在心头一直割一直割割个不停的疼痛……我们都束手无策，不知道要如何结束这些伤害和疼痛。一路跌跌撞撞地挣扎着长大，要怎么样才可以平静地说离开。

