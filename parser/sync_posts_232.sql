做电影吧？不好。面试时说的那一大通基本上是从《看电影》上归纳出来的，我经常听的厦广音乐台没有专门的电影节目供我参考，头一回还是不要冒这么大的险。那就音乐好了，厦广的王牌节目便是胡凌的Top10流行风云赛事。但我不要做中文流行，太容易和别人撞车了，当然是外文歌。我翻着四格柜上的CD，有一张学妹前几天寄来的X-Japan的精选，10分钟能讲些什么呢？干脆就介绍X-Japan好了。中间穿插一首Kurenai，再一首Tears。

