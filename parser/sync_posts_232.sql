我已经混在这所南方海边的大学里旁听了一个月的课。一个月前我的大学所在的地方发生了一场史无前例的瘟疫，于是我随着恐慌的归家学生微微窃喜地离开了晃晃悠悠混了大半年的学校，逃离了身边那个终日戴着耳机终日一副无所谓的拽相的人，回到了家，我的南方的城，生活了十八年最终以梦想的名义匆忙逃离的地方。城市靠着海，终年从东南方向吹来潮湿的海风，在五月时候迎来雨季，城里到处是路面水迹的反光和瓦房顶霉湿的青苔。还有一所著名的大学，高晓松和石康流浪到过的地方，以宿舍楼外有海的风景和娇小美丽的姑娘闻名。于是我闲置在家的这一个月以来就是到那所大学混课听，听的是最郁闷的高数和经济学。每日上下课路上必定被人揪住问“啊你怎么跑回来了”之类的话，乃原中学太多同学就读这里的缘故。同时也有好处，横竖走到哪见食堂都能混上饭吃。

和喵喵吃完饭后我们去了她的画室。明亮而凌乱的一间屋子，在美术系那幢靠海的楼的最顶层，满满一屋的画架，画布上都是绮靡的西域女子，眉目流光，裙裾飞扬。还有满满一窗的大海，远处灰蓝色的海平线。我们一首一首地挑音乐，小小的音箱低低哼出的破碎琴音慢慢填满了画室，一点点游向窗外的大海。当放到《挪威的森林》原声配乐那盘cd中西村由纪江《tender　morning》时，陈潘推门而入――就是这首吧，作为他们第一次相遇的背景音乐。

那时我们其实是在策划拍一部电影，二十分钟的dv短片，陈潘他们电影学院布置的。我和陈潘匆匆从北京逃难回来，闲来无事，索性安下心来呆在这里好好地把电影拍出来。高中时在一起的那帮朋友乐得藉此为由重新聚了做这好玩的事情。只是美院的喵喵看了我们写的剧本很有些疑问，她说这种片怎么拍啊一不小心就拍成恐怖片加青春偶像剧。我很不服地抖着那叠薄薄的纸页，怎么会呢怎么会呢你好好看看，明明就是一部很阴郁很唯美的片子嘛。轻度精神分裂的女子，爱上作为灵魂存在的男子。她不断地追逐，奔跑，错过，同时又与镜中的自己艰难地挣扎。最后她杀死了自己。。然而与他存在于同一个世界，终究还是不能相遇。永远的错身而过。永远不会相遇。孤独，原来一直都只是一个人的事。。。

所以，片名就叫《鬼鬼情未了》。陈潘接茬。

去死去死，我们忍不住大笑。

不过说真的你设定的那种形象的男主角太难找，这年头有哪个十几二十岁的男生穿白衬衫好看的？更何况厦大原本便没有几个有气质的男生，还要什么“像戴惯眼镜的人摘了眼镜的那种迷离的眼神”。陈导演你上哪凭空弄这么个人物出来啊。我撇撇嘴。

不会啊，我觉得我们班舞台剧的男主角就可以来试试。喵喵说。

好啊，那么大家明天还是中午１点来喵喵的画室继续选背景音乐，顺便约他出来见一面。陈捷说。

多多少少还是有些意外的，当佳树出现在充溢着《tender　morning》里清澈钢琴声的画室时。他真的穿了一件白色衬衫，浅色的棉布长裤，头发软软地没过脖颈和额头，以及一点点的眉眼。侧面剪影清晰分明。

这是导演陈潘。这是女主角露卡。喵喵向佳树介绍我们。

陈潘煞有介事地伸出他那肥厚如拔了刺的仙人掌的手跟佳树握了握。我反而伸手不是不伸也不是，于是向他微笑点了点头。佳树这才抬眼看我，脸上倒是一直挂着笑容，虽是被额发遮了去的眼神，竟是明亮干净如清晨树林里的阳光。这样的男生应该叫“林曦”，我忍不住暗暗给他胡乱起名。

――他的眼睛可惜太过明亮。陈潘过后这样告诉我。

