露卡，露卡。有人在背后大声叫着我的名字。走吧露卡我们去画室。

我回头。喵喵远远地站在林荫道那端，阳光穿过夏天的树梢，在我和她之间投下一地层层叠叠的浓绿阴影。喵喵一路穿过这重重的绿色向我奔跑而来，脚下清脆的木屐将一地叶子形状的影子踩成夏天的碎片。那以后每每回忆起这年夏初在厦门的日子，总是先看见这涂着鲜明夏天色彩的浓绿，以及喵喵奔跑过来时微微泛红的脸和脸上年轻的汗意。

我们穿过四十年代老宿舍楼有着高高的木拱顶的石头走廊，踏过窄缝里塞满细小牙齿般的青苔并且因年代久远而渗出水珠的石板路，从校园的西门走到东区的美院。美院宿舍建在山坡上，沿着山坡一直往下走，便到达最东边的门，出了门便是一望无际的海，海的上方是灰色的、顶着不均匀面具般的阴沉天空抑或亮蓝色笑脸般的晴朗天空。阴天时候海便呈现出同样寒凉的灰色，晴天时候海也相应翻出幽蓝色那一面的海水。所以那条长长的海平线永远暧昧不明，夹在天与海的情侣颜色之间忧伤地起伏，有海鸟游荡的剪影。我们常常坐在美院高高的台阶上，看远处的天空和海洋，还有暧昧忧伤的海平线。

喵喵就给我讲林恺，那个同样暧昧忧伤的男子，从高中时就和她在一个画室学画，大学时考到了美院的同一个班。喵喵讲起她和他的故事，讲林恺因胃病住院她天天从家里熬了薄薄的粥去看他，讲他们在凌晨空旷寂静的海滩上扔了一地喝光了的啤酒罐被海风吹得啦啦作响，讲冬天的夜晚他伸出了手而她却一念之差闪身躲开触手可及的温暖。从此之后便再没有人提起那句话，他什么也不说，从此沉默而暧昧。

……我静静地听着喵喵努力不带表情的述说，我想说什么，我能说什么。南方的夏天被潮热潮热的海风熏成永恒沉默的石雕一般，谁也没有力气开口说话。一树一树的凤凰花猎猎地开满了这所海边的大学，红艳热烈如同情欲一般燃烧。栀子花却已落了一地，洁白如处女的身体，熬不过长夜的侵袭纷纷死去。它们和我们都被一股脑地放在夏天这口大锅里咕嘟咕嘟地煮，谁知道谁能解救谁谁能炼就谁。高中时那个才华横溢、孤标傲世的孩子喵喵，把非艺术类的平庸的我视为同类、同班两年都一直通信的好友喵喵，在高考中专业分优异却被那所著名的艺术学校一手黑掉之后放弃了她的北方理想留在南方家乡的这所大学，而我独自去了远方。坐在一树火红的凤凰花和一地洁白的栀子花之间眺望远方大海的我们一次次陷入沉默。当初那些选择都对不对呢，如果不是这样那又会怎样。她为什么留了下来我为什么去了远方，她为什么躲开了林恺的手我为什么安于某人的怀抱，凤凰花为什么灿烂栀子花为什么凋零，这些答案没有人停下脚步来回答，我们都会这样长大，这样各自走开，走得越来越远。

