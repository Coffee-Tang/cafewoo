<p>刘若英的歌声清楚明净，似闷热的夏日吞杯冰水，我喜欢。知道她演过一部电影《少女小渔》，向LUDO借来片子看，非常之好。</p>

<p>其实并不大看VCD，家里有太多，廷嗜好买片子。而本是懒散之人，放部片子都嫌麻烦。又有点神经兮兮，过于紧张悬念情节的片子习惯于拒绝。只看些日剧，情节平淡，煸情。或动画片，色彩鲜艳，有趣，简单。让人快乐。比如狮子王，比如白雪公主。
<br/>但还是想说说小女小渔。有些细节实在让人难忘。</p>

<p>少女小渔是个纯洁善良的女孩。男友到美国读书，偷渡过去陪伴在他身边。底层的缝衣工作，随时要抵防移民局的搜寻。小渔活得小心冀冀，但看得出的是她满心的欢喜，因为一切都是为了能跟所爱的人在一起。每天的劳累之后，小渔还抱着厚厚的字典查着每个生硬的单词。当别人惊讶于她流利的表达能力时，小渔只是淡淡说，生活需要，我只有前进。
<br/>没有抱怨，需要多少的勇气？</p>

<p>为取得绿卡，小渔同一意大利老人Mario假结婚。他们在一起生活的片断将每个人思想的变化及挣扎描绘得淋漓尽致。Mario曾是个WRITER，晚年孤独。贫穷。人好赌。不过，基本上算是个好人。
<br/>其实，人生中最可怕的敌人不在于时间，而是寂寞。因为寂寞，生活没有希望，变得空洞。得过且过。其中有个片断是，小渔在给Mario和自己准备中国菜时，浓厚喷香的油烟飘漫到隔壁的住家。邻居不解敲门问出啥事，老人十分HAPPY的答说，Chinese Cooking。真让人动容。孤独太久的老人终于体会到了家的温暖，有人正在为你准备饭菜的感觉有多么的愉悦。呵呵，真是喜欢极了这不起眼的细节。
<br/>生活大抵就是这么回事。到老时有人相依为命，即便是随手递来的一杯水，也是幸福。</p>

<p>当小渔察觉到男友的背叛之时，她哭着对Mario说，这绿卡已经不是她的希望了。
<br/>谁看了都会感到心疼。爱得这般没有自我与纯净。
<br/>小渔的男友是一十分典型的自私者。这样的人应该不少，便没什么指责的必要。都是为了生活，可以这么说。只是可惜了这水般女子所遭受的委屈。
<br/>片尾是，小渔决意留下来陪老人度过最后的时光。当听到男友的破车在楼下咯吱咯吱远走的声音时，小渔捧着Mario的自传泪流满面。为已走的感情？为老人的一生？</p>

<p>片子的导演是张艾嘉。她唱过一首很淡容的歌——爱的代价。走吧，走吧，为自己的心找一个家。。。。。。
</p>
