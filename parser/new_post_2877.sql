　　在一个深夜，木木打电话给我，幽幽的说着，声音像冬天的北风忽忽的吹过，我有种被冰包裹的感觉。身体好像被冰渣刺穿。 

　　“风子，太黑了，我看不见小木木和大木木了。” 

　　木木常常一整夜的不睡觉，静静地看着地上的影子，对着前面的空气笑。木木的继母在木木正对影子说话时切断了电源。突如其来的黑暗罩住了木木的心，她使劲叫，叫声也无法冲破黑暗，引来光明。全世界仿佛只剩下她一个，除了她的哭声和怒吼，世界只留下一片静寂。她在电话里不停的念叨大木木，小木木。忽然感到电话掉的空中的感觉，话筒里传来摔东西的声音，夹杂在其中的是木木呼唤的声音。感觉到从木木脸上滑过的液体滴在了我心里，冰冰的，痛痛的。我在电话里大叫，木木，点蜡烛，点蜡烛…… 

　　经过几分钟的死寂，木木喃喃自语的声音又传入耳里。木木披头散发的坐在地上，微弱的烛光衬着她苍白的脸庞，她盯着地上的影子，暗自泪下。整个夜里，木木一直在说大木木，小木木不要离开我，不要离开我。第二天，我来到木木家楼下，抬头看着阳台上那一排排水仙花，如果在往常现在木木一定会站在花丛中，亲吻那些花朵。而这时那些孤寂水仙花的影子落在纯黑窗帘上，忽隐忽现的轮廓，起起浮浮，让我想到一群正在施展法术的女巫，心里某个地方有不祥的预感和难忍的苦涩。木木把自己关在了房间里，黑布遮住了所有光亮。她开着灯一直坐着，说着。我敲门时，木木像木了一样，听不到任何声音。恼怒的木木继母将电源再次切断。木木像小兽一样冲出房间，向门口跑去。我抱住衣服被抓得破破烂烂的木木，大声说，大木木，小木木累了，她们休息去了，他们告诉我，木木也得休息了。三分钟后，木木像个孩子一样睡在了我的怀里。她乌黑的头发散在我的大腿上，发根有微微的热气。 

　　南方的城市总会有一连几天的阴雨，木木的继母剪断电源。看不见阳光，没有灯光，面无表情像具行尸走肉的木木，目光呆滞，没有一丝亮泽。突然木木问我，如果大木木受惊跑进我身体里不出来怎么办。我摇了摇头。过了会儿，木木高兴的跑回了家，看着木木渐渐消失的背影，忽然觉得像吃薄荷糖一样凉意着窜喉咙。 

　　我是在午睡时接到木木继母的电话。她说木木抱着一盆水仙花不知去向。我的头嗡嗡的直响，大脑里一片空白。想到了那个木木问了我很久的后来。没过几天，事实便证实了。木木为了防止她跳下水时，大木木受惊跑回她的身体不出来，就用铁丝栓在自己身上，并在一米远处弄了个铁圈捆住大木木。那个铁圈在木木发白的身体上留下了无法抹灭殷红的印痕。 

　　我一直在想纳瑟斯的后来和木木的结局。纳瑟斯最后耐不着寂寞，跳进河里，和自己的影子相拥在一起。在他和影子相拥的地方，开出了一朵水仙花。而木木消失的地方从未长出过水仙花，她阳台上的水仙花也在她消失后，再没开过花。 

　　木木的继母把那些很久都未开过花的水仙扔到了垃圾堆里，看着那些埋在土里的翠绿的叶子，它们的绿光在我眼睛里铺散开来，幻化成一片随风摇曳的水仙，木木用她的嘴唇啄了一下其中最美的水仙花，然后说，大木木，小木木，你们知道水仙花的花语吗？ 

　　木木曾告诉过我水仙花的花语，可我忘了。







	
	
	
