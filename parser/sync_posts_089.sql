7月8日的傍晚,我站在南京某街的公车站.身边是拥挤的人群,我掏出手机,给桔子打电话.她刚刚高考完,她说我这边好吵.我说我在南京街上的.然后听到桔子激动地叫声,凌子一直望着我笑.站在南京的街道,我一直有一种莫名的茫然,耀眼的红绿灯,满大街的十字路口.于是我跟凌子说我不喜欢这里,一点都不喜欢,我是抗拒陌生的.凌子说南京很像成都的,感觉特别亲切,于是我们在南京的街道上大声地笑.耳机里听着品冠的<就叫我孩子>,我越来越觉得我们只不过是孩子.

