1.深夜的火车和清晨的巴士   (7月6日――  7月9日)   南京

在7月6日晚上被塞上火车,没有一丝悲哀的感觉.除了兴奋还是兴奋.之前凌子在网上跟我说她一定睡不着.我说好啊,有我陪你的.结果2点时凌子在火车极有乐感的嘈杂声和着莫文尉独特的寂寞嗓音中沉沉睡去.我则在火车对我的精神摧残中醒醒睡睡,终于在凌晨4点时彻底失眠,蜷缩在底铺,望着窗外漆黑得模糊的风景,孤独地等待黎明的洗礼.5点时,列车管理员过来和我聊天,她叫我休息一下.我说睡不着.然后她迷迷糊糊地和我聊天,5点37分,天终于醒来,我仍旧无辜地望着第一个陌生的清晨,列车管理员坐在我的另一半铺上昏昏欲睡,我看了看她的脸,一张疲惫的中年妇女的脸.6点11分,她醒来,离开,和我说再见.6点19分,临铺的姐姐和我同心协力打开那扇窗,风跟着透了进来,很爽.第一个陌生的清晨,窗外没了高山.我告诉自己我离开了.兔兔在6点31分从后车厢过来,她也兴奋不已.

