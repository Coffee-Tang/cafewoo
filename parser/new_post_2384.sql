事业就象手机固定的几群移动公司客户般，丝毫没有变化，停滞不前。

往事难以回首。

几年前还被2所学校开除的问题学生，现在却丝毫没有悔改。

不过对于毕业证书不屑一顾，同渴望的CCNA工程师证书也就形成了鲜明对比。

不过每天在移动公司的工作却愈来愈象技术员的活儿，而不是先前做的还有些工程师样儿调调电信的HUAWEI交换机。

