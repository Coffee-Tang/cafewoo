类是否也曾经有过这样的孤独呢？从法国回来，他像变了一个人似的，变得轻浮。或者是因为静的辜负，或者是因为对杉菜隐忍的浮现的爱恋，我们不得而知。总之，他用一种独特的方式把伤口深深地裹起来。他和女孩调笑，随意地接吻拥抱大笑。可是在深深的夜里，他坐在海边，无助地靠在杉菜的怀里，请求她抱抱他，哪怕几分钟也好。在孤独的夜里，在只有杉菜这个令他迷惑和动容的女孩陪着他的孤独的夜里，他蜕下沉重的伪装，重新变回一个无助的孩子，眼神清澈脆弱。杉菜全心全意地把他抱在怀里。杉菜说，为什么你不能得到幸福。他们彼此依赖相拥，泪水轻轻地落下来。

