ala问，眯眯你今天晚上是怎么了？

我怎么了？我是在嫉妒。我嫉妒小飞得到了S，我嫉妒S得到了小飞，瞧瞧瞧瞧，我是个多么蛮不讲理的女人。这个晚上就这样过去了。

四

第二天我早早到校，ala却更早。她正在整理历史笔记，见我来了，便兴奋地拿出一本画册给我看：“眯眯你看，这些都是我收集的！”我接过画册。我的心情还是没有好起来。封面上是F4的合照，类站在最旁边，远远地轻笑着。下面的标签上用碳素笔写着：F4珍藏版――Ala.’s。丝毫不顾及语法的存在。

但是眼睛亮了一下。翻开来，只是看类。

照片上的类有很多的微笑。类的微笑总是自然的，不掺假。看《流星花园》的那段日子，有时候看到类忽然微微地展露笑容，心里都会感动得想流泪。虽然是从小有自闭症，但类仍然是个真性情的男子。我记得安妮宝贝说过，世界上真性情的人，总会有许多与众不同的想法。在类的沉默之下，一定有许多细小可爱的角落，在心情好的时候，他甚至是调皮的，眼睛里面带一点点狡黠，孩子一般的纯净明朗……

