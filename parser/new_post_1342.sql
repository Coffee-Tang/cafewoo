小飞整个晚上都没有来上晚自习。

我就这样拿着笔对着面前的数学练习册呆坐了一个晚上，一会儿想到小飞，一会儿想到S，一会想到类。我这才发现我的心里有多么深重的阴影。小飞没有告诉我她去了哪里。她甚至没有告诉我她今天要逃晚自习，昨天我们还那么快乐地粘在一起，我还在趴在她怀里哭着，才一天工夫她就这么不声不响的离开我。我知道她去了哪里，好朋友不开心的时候她却欢天喜地地跑出去谈恋爱――没错，她一定是在她的S那里。我看不得她那种幸福的表情。看不得看不得。在我得不到S的同时我还要失去小飞。我真的看不得。

