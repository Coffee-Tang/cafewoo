中午破天荒地回了家，下午也决定翘课――是一心想要静一静。眼睛红着，头发也乱得紧，就这样低着头上了车，找个位子坐下来，拉开窗户吹风。乍起的风吹得我睁不开眼，潮湿的睫毛下满是凉凉的泪。我心里……

