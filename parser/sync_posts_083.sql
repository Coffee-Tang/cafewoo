我苦笑道：“嘿，一家人说什么请。”心中却暗骂自己，思考如何脱身店铺。

那小子喜道：“大侠真大方，若不嫌弃，我们交个朋友。”说着，坐了下来，摘下腰中酒葫芦，叫道：“小二，拿两个酒杯过来。”

我寻思：“莫非他又要叫酒来，为我们交朋友助兴？不止如此，还要顺便往他酒葫芦里装些酒，自己往后享用？这回我可被讹上了。师父说得果然不错，江湖险恶，人心难测。”

店小二拿来酒杯，却不见小叫化叫酒，正自奇怪，只见他摘开葫芦盖，朝每杯倒了一些黄澄澄的酒来。登时，酒香四逸。

我忙道：“小兄弟，我不会喝酒，不必……”

那小子插口道：“不成不成，我们虽不是义结金兰，却也是有缘。我这壶是女儿红，是我爹死前留给我的。”

我一听，心中一凛：看来他并非讹我，倒像是真心相待。他也是有爹的。忙道：“你爹……怎么……”

他脸上的笑容消失了，凄然道：“他被仇人杀死的……”还未说完，已经伤心哭了起来，声音有些奶声奶气，倒有几分像小姑娘。

我试图安慰道：“抱歉，我……那……”却也一时想不出什么好安慰的话。总不能说“我为你爹报仇”吧，且不说自己武功能不能胜对方，又不知对方的底细，况且这小子真正意图未明，倘若他不是真的落难，而我却和人家结上梁子，岂不成了冤大头？而且师父曾告戒我不要没来由地和武林人士结梁子，到时吃不了兜着走，可没人来救你。

