我听得那小子没来由叫我付帐，心中厌烦，但又一想，这碗面吃过后，自己也要成叫化子了，说道：“先不忙……”可一想，自己也真没钱请他。思索半晌，心不由衷道：“我请！”小二一怔，笑着答应了。

我打量着那小子，年纪不过十五六岁，一双水汪汪的眼睛不时朝我调皮嬉笑，笑意从嘴角泛到眉间，露出洁白的牙齿。小二端过面来，他忙不迭地抢过，吃了个精光。“吧唧吧唧”小嘴，向我走了过来。

