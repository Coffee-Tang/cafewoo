次日，阳光射进庙里，我醒了，却见陈鸿，已经起来了。

他问道：“你准备去哪里？”

我困惑道：“也不知道。师父曾说过，江湖在我心里，要用心去感受江湖。”

他念道：“江湖，江湖，你感受到了？”

