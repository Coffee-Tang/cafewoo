我们还是那样五天一礼拜见一次，聊聊天逛逛街。他一直很宠我，让我觉得他是在对待妹妹。心中的暗涌越来越明显，却无处述说。任是咫尺，但我该赁借什么来肯定之间的亲密。开满了樱花的春天，袖底的清香透着说不清道不明的沉默。

爱一个人是这样的。莫名的担心，莫名的空虚，莫名的觉得无意义。爱过一个人，心就从此空掉,除非是他来填满。不然，从此再也没有幸福的感觉。他让你明白，原来你是残缺的。

在壬之后，言是第二个让我有这种感觉的人。我不管将来是怎么样的，可是现在，我需要他在。

我，需，要，他，在。

为了让他不再一直把我当成小孩子，我开始改变形象。穿着趋于成熟化。但反而被他笑我一张娃娃脸还要装老成。没办法，放弃外型装扮。

直到有一天，言对我说：“其实你的行为举止，并不像个小孩子，我该改变对你的看法了。你比我想象中的要成熟。”我忽然感觉到他看着我时的灼热眼神。但我不知如何回应，一时的不知所措。

那时我们正在酒吧里，到处声色浮动，眼波流转。言吸着烟，袅袅上升的烟雾包围着他，看不清他的模样和表情。他修长的手指夹烟的样子孤寂而苍凉。这是他第一次在我面前表现出这样的自己，接着他向我说起了过去。我的耳朵里脑海里就被言的一字一句和酒吧里轰轰作响的摇滚乐充斥着包围着。心一点一点的揪起来，硬生生的疼。

都是有过去的人，过去的一切是否都那么难以忘怀，却都无法挽回。起初的那些痕迹总是洁白的，但在时光的深处，再度俯首观望，发现它们的颜色变成了颓败的黯黄。年华似水，有些事情在岁月长河中真的只算是匆匆一瞥，就像我和壬的三年。人们总是来不及享用太多时间，命运就会插手讨还。于是，所有的情节夭折，所有的缠绵被扼杀。人生原是百转千回，时光如流水，汤汤的，在脚边静静流去。

“人总是得朝前看的。过去的，算了吧。”我缓缓地说。他抬起头看着我，轻轻牵动嘴角，一道美丽的弧线从此就停留在了我心里。心中有股无法说清的情愫不断蔓延着牵扯着近乎疼痛。我目不转睛地看着他，泪水在眼眶中打转着不肯掉下来。

他突然倾身向前紧紧地将我拥在怀里，毫无意识的动作，却像是预演了千百遍。我埋头在他胸前，放肆地哭着像个不懂事的孩子。我渴望他的拥抱，感受着他的温暖包围着我，那种短暂而表面但是我心满意足的幸福，任凭自己陶醉在他怀里，哪怕只是一秒钟。

“我不懂得自己可是我要你懂得我。我不知道自己能不能全心爱你，可是你必须永远在我一回头便看见的那里。”他忽然说。我不知道他是不是喝多了，也不知道他这句话到底是说给我听的还是自言自语般的说给以前的那人听。

我抬起头，他的眼里映着我惶恐的表情。我记得。像某个清晨醒来时的幻觉。

“如果你那句话是说给我听的，那我答应你，只要你一回头，就能看到我。”我想。

I Promise。

那天过去后，我们谁也没有再提当天的事，那个拥抱和那句话也好象没发生过一样。但是彼此心中的暗潮汹涌却是谁也不愿意承认的。就像把金属钠投进水里无，当时的剧烈燃烧，火光四射，可是过后仿佛什么也没有发生，水还是透明清凉的。但我们忽略了一点：水的性质已经改变了。

而我和言之间的关系已趋近于暧昧。不明。但我们都被说不清的各种情愫牵扯，由此而产生的一些些茫然便注定要成为前行路上的海藻，被缠住，挣脱不开。

生命中有些话只说一次，转过身依旧素面朝天。

当街上的人们穿的衣服越来越少的时候我知道夏天来了。

一天傍晚，看看课表，晚上整整3节的心理学。脑神经的第一反映就是，撬课。

然而当我走出学校，在喧哗拥挤的人群中不知道该往哪里去，一家又一家的商店从身边滑过，站在十字路口，迟疑了一会，才走回学校宿舍的方向。而我真正想去的，是他的身旁。

走着走着，眼睛忽然被闪亮的车灯照得睁不开，伸出左手揉了揉双眼，言就这样开着车出现在我面前。他拉下车窗显露出疲惫的侧脸，凝固在夜色里。我很自然的打开右车门坐了进去，仿佛他身旁的位置向来就是为我准备的一样。

许多东西，如果隐忍太久，就会慢慢滋生，如房檐瓦砾间的草籽，蛰伏一冬，春风一扶便挣扎着再生。而生长的速度总是惊人的。

深藏的暗涌也有喷发出来的一天。

在言的房间里，他坐在我面前，低着头不断地抽着烟，烟草的味道弥漫开来，淡淡的，像夕阳的余晖，抹不尽。我喜欢烟雾缭绕的感觉，安全的，真实的幻觉。黑色的夜幕，苍白的灯光，光洁的地板隐隐折射出两个空虚的灵魂。

“把头抬起来。”我说。我深深地看了他一眼，马上转过头去，不敢在他脸上多停留一秒，不敢多仔细看一眼，然而那张脸却清楚印在心上。心潮翻滚不已，但表面却如一泓秋水。可能是有些暧昧不清的东西，变得难以被表达或者无需表达。只是不知道是问题本身还是人。沉默，要说的话都没有说。

言忽然扳过我的双肩，我不知所措的看着他，我看见了混合着喜悦和忧伤的，他的眼眸，这双眼睛照出了我的内心，无所遁逃。我终于知道了什么叫做，命中注定。

“简简，我无法欺骗自己，我爱你。但是我不知道该拿什么来爱你，我是个心理不稳定的人，不知道自己能不能让你快乐。但我一天一天的在努力，我不知道今天我离你的心还有多远，但是我相信应该比昨天近。我不知道我能带给你的幸福能有多少，但是我相信，明天的会比今天多。”

言的一字一句就这样毫不留情地敲打着我心灵的最深处。一阵寒噤从我的脚底一直上升到我的头顶，寒入骨髓。前所未有，我把自己看得如此通透明澈。我向他伸出了手，用全身的力气，抱住他。抱紧他。抱紧我的全部的爱和忧伤。

苏格兰风笛的声音在空气里消散融合，缠绕着我们。在苍白的灯光下，言用手指摩挲着轻轻滑过我的皮肤，没有终结。

像对一个女人那样对我。我再不是你的孩子，再不是。爱我，好好爱我。

冷静的沉默和冷静之后的话语，和蜕变。体会着原始的冲动，未经雕琢，褪去华裳，一切真实到血脉，透彻到骨髓。

几乎所有的事情，我们事后知道的一定都比事前知道的多。但他并不知道我爱他有多深，以前不知道，现在不知道，将来也不会知道。

“言，以前有个算命的说，三是我的命数。我只要能和一个男人走完三年，那就代表着能和他在一起快乐一辈子。反之，就是永远的失去。我们呢，我们能走完三年吗？”

“傻孩子，我要给你的，是永远，三年只是其中的一部分。”

……

灯火暗落。他的手臂轻轻攀过我，相拥而眠。

