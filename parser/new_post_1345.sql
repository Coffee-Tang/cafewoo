刺儿头男孩巴特·辛普森的形象越来越多地在身边出现，但未必人人知道他是谁，从哪里来。

在美国，最受欢迎的电视动画片《辛普森一家》（港译《阿森一族》）已经播到了第１０年。１０年来，春田镇上的这个五口之家用他们荒诞的经历讽刺着当代美国社会生活和文化，据说任何领域内受到关注的任何个人、团体和组织，都有可能成为这部动画片影射的对象。辛普森一家的形象随着电视剧的走红而成为世界性的流行符号，其中爱捣乱的长子巴特·辛普森已经成为一个大众偶像，连《时代》也将他选为２０世纪演艺界最重要的人物之一。

其实巴特在动画片里只能算是令人头痛，捣个乱撒个谎，没事爱跟大人对着干，祸闯大了一样傻眼，这样的小孩世界上何止千千万万。不过当他被推到潮流前线，注册商标就变成了"春田镇的坏男孩"，因为他的坏已经等同于 "酷"，"坏"的一面也被特别放大、突出，大家爱就爱他身上一股天不怕地不怕、我是小孩我怕谁的气质。巴特最典型的形象，是一边向墙角撒尿，一边扭过头来对你说："别找麻烦！"语气恶狠狠的，好像真有谁会怕一个留着平头、ｔ恤短裤球鞋的１０岁小学生。

真正知道并喜欢巴特·辛普森的人，应该是男性居多。自以为还有青春可言、不安于现状又带点反叛的半大小子，希望拥有一些巴特的标志，衣服也好帽子也好背包也好，表明自己已经加入"阿森一族"，巴特的方式我喜欢；稍上些年纪、又喜欢巴特的人，虽然表面上循规蹈矩走传统路线，其实大多心里都有小时候太用功没玩够、"还我童年"的牢骚，也羡慕巴特无拘无束为所欲为，正好寄托一些"愿我年轻二（三）十年"的情怀。而且，巴特的魅力并不应该仅仅是"坏"，在坦率和表白成为时尚、委婉和掩饰已经过时的今天，巴特率性的态度与言行更加值得称道。至于巴特对崇拜者的吸引力是源自相似还是互补，大概各占一半。（贾由）

