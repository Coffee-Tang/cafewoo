<br>原本打算从我三岁的时候开始说起，但是我发现我的记忆有点模糊，有一部分内容是出自父母的口述，还有部分来自长辈的议论。。。这些不够客观的东西可信度不高，所以很难有说服力，尽管那年我有很多可歌可泣的事迹。。。但是我还是决定从比较懂事的年龄说起。
<br>
<br>那么，我就从四岁说起。
<br>
<br>我属羊，小学毕业或者没毕业的人都可以轻松推算出我四岁那一年的年份，但是如果你不想推算的话，我就会告诉你：那一年是1983年。
<br>
<br>1983年对我来说很重要，因为我认识了娇娇----我们幼儿园的园花，其美丽程度在我心中仅次于炖得喷喷香的红烧肉。
<br>我喜欢她，我甚至可以为她不睡午觉，扮演我讨厌的大灰狼，从老师那儿偷橡皮泥。。。如果不跟我抢红烧肉，我甚至愿意天天坐在她身边自己吃饭----这样，阿姨会省很多麻烦，因为我吃饭的时候需要靠在别人温暖的怀里才有胃口。
<br>后来，我发现她也开始喜欢我了----她把她最喜欢的玩具送给了我，那玩具在现在看来没什么，但是在1983年，没有比那更好的礼物了，我想想。。。那应该她用来绑辫子的橡皮筋。然后她对我说，去！小路今天早上弹了我一下，帮我报仇去！。。。
<br>那个时候，我们小小年纪就能利用灵活的食指和大拇指套住橡皮筋做成简易弹弓，满足我们幼小心灵里的战争欲望，在我简单而又复杂的童年生涯里，那是我拥有的第一种武器，后来，我和我的小朋友们还不停地对其进行改良，先是用树杈做成Y形的手持弹弓，攻击力取决于力气大小和橡皮的弹性，而且只能单发，后来又继续改良形成长距离攻击可连续多发的，到最后，居然做出利用火柴头的燃烧物等材料爆炸的能量为推动力的弹弓----那种已经不叫弹弓了，那时候它有个威风凛凛的名字--驳壳。
<br>人类社会从冷兵器时代到大炮的发明用了几千年的时间，而我们仅用了几年的时间，这让我在很长时间里都会为自己和自己的年代骄傲，直到上了小学，学会了谦虚以后，才会红着脸说，其实。。。我只不过是站在巨人的肩膀上而已。
<br>
<br>娇娇是一个坚强而有智慧的女性，但是当时吸引我的只是她的美貌和身上散发出来的花露水味道。我傻傻地喜欢她，愿意拼尽全力保护她，让她快乐。
<br>
<br>她说她被小路欺负，我立刻就行动起来了，我要为她报仇。
<br>小路是我的情敌，除了身材高大一点，长得白一点，识字多一点，数数快一点以外，好象没有比我强的地方。娇娇最喜欢和他玩，因为他的玩具最多，但是他老是欺负娇娇，而且还会装出一脸无辜，好象是娇娇欺负了他一样。我知道他老爸是一个政客，家庭的教育难免有类似如何伪装好人这样的课程，小路学得很好。
<br>
<br>他敢欺负我喜欢的人。。。别看我细胳膊细腿的，我可是很勇猛的。我一面在幼儿园里寻找小路，一面紧握住拳头，轻轻颤抖着。
<br>
<br>后来我在厕所里把小路堵住了，我狠狠地瞪着他说，“小路，我要和你打架！”
<br>小路很惊讶地叫了起来，“我们是好朋友呀！”他一边退，一边说，“老师说，好孩子是不能打架的！”
<br>
<br>我立刻就楞了：对呀，小路是我的好朋友呢，但是他欺负娇娇。。。可是老师说不能打架。。。老师的话对我的影响太大了，那时候老师的话简直可以做我的座右铭，甚至超过了最高理想成为我人生观的最高标准。
<br>不能打架。。。怎么办？----我憋得脸通红通红的。
<br>“你是不是欺负娇娇？！”我大声质问。
<br>“没有呀！”
<br>“你拿橡皮筋弹了她一下，有没有？！”
<br>“没有呀！”
<br>。。。
<br>我们俩的小胸脯用力地顶在一起，我大声说你有，他大声说我没有。
<br>不知道谁喊了一声老师来了，就象现在路边摆满摊子的小贩们听到警察来了一样，场面开始一片混乱，原来站在我们周围的小朋友四下乱跑，趁着混乱，我掏出娇娇给我的橡皮筋，用力地在小路的手上弹了一下，紧接着小路怪叫一声，痛苦地流下了眼泪。
<br>老师来的时候，我很隐蔽地把娇娇给我的橡皮筋丢进了厕所。
<br>
<br>后来，我和小路都被拿掉了一朵小红花，小路哭得很伤心，而我也强忍悲痛，泪水一直在眼框里打转，但是一想到我帮娇娇报了仇，就觉得损失一朵小红花只是很小的牺牲，牺牲得有价值。
<br>
<br>回去的路上，我对娇娇说，“我帮你报仇了！”说这句话的时候我一脸正义感。
<br>那时候，电视热播的是《霍元甲》，最红的明星是国家领导和新闻联播的主持人，我们最崇拜的是一身正气的武林高手。
<br>说那句话，我感觉自己就象一位英雄一样站在遇害的亲友坟墓前，告诉那些在天之灵，我已为他们手刃仇人了。
<br>娇娇看着我，一脸不高兴，问：“我的橡皮筋呢？”
<br>我好惊讶地看着她，你知道，电视上那些手刃仇人的英雄通常会把刀狠狠地插在敌人的胸口上，直到他转身很酷地一脸坚毅地离开时候，那把插在仇人胸膛上的刀还兀自颤动。
<br>我低着头说，“丢到厕所里去了。。。”
<br>
<br>娇娇大哭起来，边哭边说，“你讨厌，你赔。”
<br>我一股英雄气又涌上心头，大声说，“我赔就我赔！”
<br>
<br>第二天，我忘了买一条橡皮筋给她，她也忘了我弄丢了她一条橡皮筋。
<br>所以，至今，我还欠着娇娇东西，我一直没有办法还清。
<br>我想不单单是一条橡皮筋，也许还有别的东西。
<br>
<br>直到有一天，娇娇很难过地对我说，“我们家要搬走了！”
<br>她画了一张很漂亮的小猫送给我，然后在后面写着一句话，我识字少，就认得猫却不识字，或许她写的字我也看不懂吧。
<br>我想了很久，问她：“那你什么时候走呢？”
<br>她说明天，我沉思了一会儿，说，那还有一个晚上。
<br>
<br>我在秋千旁拉着她的手，很认真地对她说：“这样好了，晚上来我家睡，我们一起玩好不好？”
<br>她红了脸，大声说，“不行，我们不一样！”
<br>我也很大声地问，“有什么不一样？”
<br>她低着头，轻轻哭了，“我们就是不一样。你是男的，我是女的。”
<br>我说不出话来，直到她离开。
<br>我的手里拿着她送我的画，没有一点感觉---我以为第二天，娇娇还会来幼儿园，我还可以再看到她。
<br>第二天，她没有来，我对自己说，不要紧，她后天就会来的。
<br>但是，她后天也没有来。
<br>我哭了，我哭的时候，才知道我再也见不到娇娇了。
<br>
<br>在我幼小的心灵深处，在未来的几年里，我一直是带着“为什么不一样”的疑问在成长的，我第一次知道了离别的滋味，开始模模糊糊知道生命里必须承受一些不可避免的痛苦，开始懂得男女之间是有差别的，而我一直不懂的泪水也开始让我长大。
<br>有人一定会说，小时候的伙伴在故事为什么都会搬家呢？是不是故事写不下去而这样找个结尾呢！事实上娇娇的确搬家了，我再也没有见过她，她送我的那副画也早就不见了，只有她和我都记得的橡皮筋，能串起一些模糊的记忆，我忘了她的脸庞，不久前听说幼儿园里对我特别好的阿姨过世了，这才让我意识到，时间到底可怕到什么程度，我留下的蛛丝马迹通通会在时间的冲刷下无影无踪。
<br>
<br>这是我想告诉大家的第一个故事，它让我易伤感的神经又一次脆弱了一下，也让我的心偷偷动了一下，我想有很多人都会和我一样，在仿佛遥远的童年里有一段特别的记忆，象温热的米酒，依然醇香，依然浓烈。
