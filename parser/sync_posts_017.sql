有时候我会想起那首歌谣：摇啊摇，摇到外婆桥。于是想起脏脏的外婆家，想起从楼梯上滚下的童年往事。会不会也有人像我这样地怀想着？

摇啊摇，摇到外婆桥，外婆夸我好宝宝……

