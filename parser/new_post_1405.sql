<br>
<br>小镇上处处积雪，随处可见一群一群穿着雪靴，抗着长长的滑雪板的滑雪者。我又开始幻想起那种在雪地里滑翔的感觉。我们找到了一间出租滑雪器具的商店，换上粗笨的雪靴，调好滑雪板，挑了滑雪棍，又向老板娘问了些基本技巧，就莽莽撞撞的出发了。店的后头就是一个缓缓的大雪坡，连接山上的滑雪道。滑雪道看上去颇陡，我打算先在雪坡上观摩观摩，练练身手再说，但是绝对没想到随后的一个下午都将被困在这里。
<br>
<br>费了好大一番劲才顺利地踩上滑雪板，然后试着蹲低身子，摆好内八字的腿形，把滑雪棍向后一点，可是才滑出两步就失去了平衡，人在身体极度向后弯曲的情况下跪倒。这是最痛苦的时刻，双腿因为滑雪板的缘故不能动弹，严重的弯曲把膝关节拉扯得巨痛难当。我强行扭过身子，让自己变成趴着的姿势，用滑雪棍戳掉了滑雪板，终于站了起来。之后的半个小时，我们就像两只被杀虫剂喷过的蟑螂，一遍又一遍在雪地上摔倒，扭曲，挣扎，站起。
<br>
<br>好不容易可以沿着雪坡缓缓滑下了，可是不会控制速度，也不会“之” 字形拐，到了坡底就怎么也停不下来，只好无奈地倒下。身边时而有丁点儿大的小孩潇洒地滑过，让我对自己越发感到无奈和愤怒。有那么一次，我精疲力尽地趴在雪地里，身边是刚刚艰难挣脱的滑雪板，那一刻我的眼神一定比Luzern的那只石狮子更加悲凉和绝望。
<br>
<br>终于我无奈地接受了滑雪不能自学的事实，不过我想差别也就是摔与不摔罢了。只要不是猛冲，又能尽量侧摔，缓缓的坡度也不至于会把我摔成什么样。于是渐渐我站着滑的时间越来越长，倒下挣扎的时间越来越短，总算是有了一点气定神闲的样子了。
<br>
<br>从踩上滑雪板开始，很长的一段时间我因为担心摔倒，一直是猫着腰，低着头，眼睛紧盯着脚前不远的雪地。当我逐渐熟练后，又一次走到坡顶，准备再冲一次的时候，抬头舒了口气，刹那间被眼前的景色震住了。原来我所处的雪坡正在群山之间，连绵高耸的雪山包围着我，巍峨洁白的少女峰就在抬头仰望的正上方，那一瞬间的感觉就像是一个突然见到天子龙颜的臣民，不由自主地想要跪倒。
<br>
<br>傍晚时分我们还了器具，坐着缆车下山。雪山已经被渐浓的暮色披上了柔和的外衣。从缆车上望去，两山之间的小镇上亮起了点点灯火，错落有致的木屋顶上静静地盖着厚厚的白雪，一切都显得安详平和。
