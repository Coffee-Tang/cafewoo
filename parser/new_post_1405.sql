<br>26/12/02    伯尔尼（Bern）    多云
<br>
<br>在去瑞士之前，如果让我马上说出几个瑞士的城市名，我能想得到的是日内瓦和洛桑，再多想想也许还能说出苏黎世。伯尔尼对我来说是完全陌生的，而当我走在这个城市里的时候，我发现她小到让人难以相信这是一个国家的首都。整座城市没有一座高楼，一大半是由中世纪保存至今的住宅区。
<br>
<br>全城有两处很别致的景点。一个是一座古老的钟楼，楼顶有一口钟和一个铜人，钟盘旁边贴着石墙塑着几个色彩鲜艳的玩偶。整点的时候，钟楼下聚满了游人，看着小人把钟敲响，而那些玩偶也跟着每一次钟响做出不同的伸胳膊踢腿的动作。
<br>
<br>旧城的规划者一定是童心未泯，老街上每隔几十米就有一个小小的喷泉，喷泉中央总是立着一根一人多高的石柱子，柱子顶上是各不相同的彩色雕塑。雕塑让我感觉像是颐和园的长廊上的画，都是取材于某个民间故事。最著名的喷泉雕塑在市中心的大街上，石柱上是一个面目狰狞，手捏着婴儿往嘴里送的巨人，巨人肩上挎着的口袋里，两三个婴儿正挣扎着、哭喊着往外爬。维妙维肖的雕塑虽然血腥，却又让人忍俊不禁。
<br>
<br>Aare河绕着城的三面流过，走过高高的石桥可以见到一个熊坑，里面养着几只大棕熊。据说这座城的第一位公爵决定以狩猎时打中的第一个猎物来命名这个城市，结果一只棕熊不幸倒在他的枪下。
<br>
<br>圣诞节和Boxing Day两天都是全国假期，街上的商店悉数歇业。我们终于逛无可逛，回到Hostel，却惊喜地发现这里居然提供厨房，于是如饿鬼般奔往火车站里特别营业的Migros超市。瑞士的物价出奇的高，以至我们几天来都没正经吃过一顿饭，好几餐都是在火车站卖煎香肠的暴利小摊旁挨过的。再这样下去，估计很快就有人要把手抓婴儿的我塑在喷泉上了。当然老婆的厨艺很快消除了这种可能性。
