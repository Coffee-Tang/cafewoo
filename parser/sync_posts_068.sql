<br>光绪元年，四川东乡一村因农民聚众抗粮，被诬为造反，全村男女老幼400余人被屠杀。当年成都乡试中，来自东乡的十几个童生皆不顾考题和科举前程，而在考卷上状告川督、为民请命。学政张之洞感念其气概而不作追究。这种考生、座师两方面的道德勇气，在今日极权主义治下已不复得见。
