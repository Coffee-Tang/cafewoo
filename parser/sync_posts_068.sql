<br>
<br>作为专业的党化教育课程，政治课在学生的基本知识架构上相对比较独立，意识形态处心积虑天天洗脑也就罢了。但意识形态侵入中小学语文尤其是作文课的教育，我则将之看作极权主义对中国人民犯下的最严重的罪孽之一。因为语言是最基本的知识，语文教育构成了受教育者最基本的思维方式和情感方式，它的目的是让受教育者学会做一个人，进而做一个中国人，做一个文化意义上的人和一个精神层面上的人。但这一切的价值都在排序上给做一个意识形态下控制下的政治人让开了路。它将一种病毒放入了每一个受教育者基本的操作系统中，幼年植下的毒素，即便在成年之后你表示对这一体制的反对，最可悲的莫过于你首先就是这一体制的杰出产品。你的语言方式、甚至你的抑扬顿挫都已经难以完全去掉那些深入骨髓的伤害。
<br>
<br>托克维尔曾经观察到美国语言的日常化与其民主制度之间的关系。因为政客要取悦于民众，就必然倾向于使用民众的语言。只有在专制政体下，才会单独发展出一整套与民众和真实的日常生活格格不入的话语系统来。香港学者吴国光指出，在中文语境中，由于意识形态对于语文教育高屋建瓴的独裁作用，出现了“公共语言与日常语言分裂”的“语言二元化”现象。不但是官员，我们在电视里可以看到就是一个普通的市民，一旦有机会在镜头前公开发言，他就不自觉的采用了另一种“语言”，一种与他日常生活中的语言及其逻辑毫不相同的“假语言”来回答采访。
<br>
<br>这种在民众中根深蒂固的假语言之滥觞，就来自中学语文尤其是其中意识形态化的作文教育。来自于每年高考作文题参与了合谋的意识形态对于中文语言的长期阉割。&nbsp;
<br>
<br>一个成年人使用这种拿腔拿调的假语言，一则出自于语言习惯，一则也出自于对生存语境的体会而采取的将个人愿望隐藏在公共愿望之下进行模拟表达的一种自我保护。而一个天真的未成年人并没有这种自我保护的意识，他若不自觉的在公共场合使用这种“作文体”说话……（此处有一段乱码）“感谢共产党，感谢全国人民。”
<br>
<br>我丝毫不怀疑藏族小女孩写这句话来表达感激之情的真诚，然而问题是一旦她想要表达，多年的意识形态教育的恶果就开始浮出水面，除了这句她从小耳闻目染的代表了最高语言规格的话之外，她所受的教育里并没有为私人间情感的表达留下足够的话语空间。她的语言能力及其逻辑，是一种将一切个体欲望都化为公共欲望，将一切私人空间都拖入公共空间的意识形态语言。正是这种语言构成了政治专制的语言基础和操作平台。而推广和塑造这种虚假的语言，就成为了几十年语文教育的一个终极的甚至赤裸裸的目的。中国人在绝大多数公开场合下，就像每一位考生在高考场上作文一样，必须借助于这样一套话语，来寻求表达的可能，但这种表达，这种可怜的表达，我只能称之为“对表达的一种模拟”，而不是表达本身。&nbsp;
<br>
<br>那位西藏朋友还补充说，小女孩这句话话里有话，尤其是后面半句，除了一般意义上党化教育的影响外，还有另一种意识形态几十年来刻骨铭心的教育成果，即对藏族进行的汉人拯救了藏人的大民族主义的灌输。她分析说，对藏族人而言，不但是共产党救了全中国，而且是汉人救了藏人。在很多藏族人心中，都会把一个汉人下意识的当作共产党和全国人民的化身。
<br>
<br>我无法评判她的看法，但我的问题是，在意识形态的党化教育下，这个藏族小女孩和其他地方的汉族小女孩已经没有了丝毫的区别，那么宗教精神和文化对他们的未来和西藏的未来还会具有价值和影响吗？
