<br>放在平常也就罢了，放在高考场上，我只能将此题理解为赤裸裸的要挟。你若不在文字上表示甘愿牺牲生命，就要在考场上牺牲寒窗十年等来的高等教育机会。这已不仅是在鼓励和暗示孩子牺牲自我而已了，这实在是一场明晃晃的道德绑架，一场以500万学子的锦绣前程和一千万父母十数年含辛茹苦的期待为质押物的道德绑架。在作文中表示舍己救人是不需要勇气的，在这种道德绑架面前敢于表达不同的意见、敢于在弹性最大、分值最高的作文题中冒&quot;政治不正确&quot;的风险而说真话，才是真正需要道德勇气的。
<br>
<br>而这样的道德勇气，却在年复一年道貌岸然实质上卑劣无耻的作文考题中被不断扼杀。究其动因，并不单单是一个&quot;文以载道&quot;的传统使然。据报道，有人高考前在一家BBS发贴，大胆认定今年的作文主题必然是见义勇为、舍己救人。理由是去年以来&quot;以德治国&quot;论的高扬和公民基本道德建设纲领的颁布，他推测今年的作文仍然会在这一方向，又不能原地踏步，必然会对去年的&quot;诚信&quot;题目作进一步深化。拾金不昧已经不过瘾了，非得舍己救人不可。&nbsp;
