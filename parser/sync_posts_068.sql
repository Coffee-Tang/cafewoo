<br>尽管考官处心积虑设计了皆大欢喜的结局，并自作聪明的解释说&quot;经过一番努力，陌生人可以活动了，而登山者也因此暖和了自己的身心&quot;，潜意识要考生得出&quot;救人就是救己&quot;的结论。但很多人指出了这中间的逻辑荒唐和不合常识。这件事在本质上就是舍己救人，在暴风雪中自身不保而去救助冻僵的垂死者，十之八九是要同归于尽的。考官向为500万毕业生竖了一个道德高标，在考题的文字中明白无误的暗示了一种高风亮节的利他选择，而将个体利益的价值向度完全排开，然后要考生在一场决定著自己未来前途的考试中对此表态。&nbsp;
