<br>尽管科举制下“八股取士”的考题亦多三纲五常的道德内涵，却没有道德绑架的意味。经义之外还要考策论。策论者即政论也。历代真知灼见的应试策论并不少见。并且纲常中的道德操守也并非全是“政教合一”的，但历史上没有哪个皇帝敢于将自己打扮为绝对的道德权威。道德权威在中国古代，从来存在于士大夫和民间，而绝不落在皇家。对儒家伦理的强调不仅具有维系君臣纲常的作用，同时也造就了对于在位者强大的道德压力。所谓“德治”首先构成了对皇室的约束，而非对士大夫阶层的约束。这是儒教与现代极权主义意识形态的差别。中国的传统士大夫直到专制主义鼎盛的明清两朝，依然葆有敢说真话、敢逆龙鳞的传统。其背后的力量正是在科举之中得到张扬的儒家纲常。
