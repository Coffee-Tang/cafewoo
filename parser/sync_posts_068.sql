<br>
<br>一个应届毕业生不过十八九岁而已，但于考试一道沉浸已久，对语文教育与意识形态之间的“传教士体位”竟有如此入微的先见之明。近年来国内对于应试教育的讨论很多，却少有触及到教育与极权主义背景的关系。教育为什么要去迎合意识形态，这种迎合是否构成意识形态统治方式必然的组成部分？上个世纪40年代国民党在校园推行“党化教育”，遭到自由主义知识分子和共产党支持下的左翼力量的反对。然而共和国的合法性既然完全建立在“一个主义”的共产党意识形态之上，教育必然要承担的一个责任就是为政权源源不断的输送在价值观上“政治正确”的臣民。意识形态的绝对正确性既然不能通过辩论和观念市场上的竞争得到验证，那就必须通过反复的“教育”来获得保障。换言之即必须维持价值观教育上的“托拉斯”地位，积极培育极权主义意识形态的土壤。因此这一党化教育的传统就被执政后的共产党变本加厉的延续下来。尤其在89年之后，邓小平痛定思痛认为最大的失误在教育，言下之意即是党化教育的失败。
<br>
<br>党化教育怎么才能不失败？如果说社会现实对于一元主义价值观的冲击是当局无法控制的，那么当局有可能控制的就是教育。只有当党化教育的强度压过了社会现实的鲜活度和思想启蒙的强度，才能维持住台面上的平衡。从这个角度说，极权主义统治在今天最大的困境并不在腐败，而在教育上。江泽民近年来以望八之高龄，拼命重申意识形态统治的伪神学基础，意图为如他一般腐朽在即的统治合理性重塑金身，意图将多元利益与价值格局的现代社会大而化之在“意识形态为体、多元利益为用”或“政治极权为体、市场经济为用”的黄粱梦中。一方面将极权主义意识形态作为当代的“三纲五常”供奉起来，纲常不变而洋务恒新，走的是百年前传下的道路。另一方面则将过气的共产主义意识形态加以稀释，大而化之为三个代表和一般意义上的“德治”，心想以更少的内涵来统摄更多的外延。&nbsp;
