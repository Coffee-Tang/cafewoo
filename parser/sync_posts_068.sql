<br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;选取自文学城――――――――', '今年高考作文题名曰“心灵的选择”，实则对考生而言是没得选择。考题设计了一个登山者遇见暴风雪的生死关头，他冒著九死一生的危险救了一名冻僵的陌生人，"最后两人搀扶著走出雪地"。考官问道："在这种情况下，我们（你们）应该怎么选择？为什么会做这样的选择？"

尽管考官处心积虑设计了皆大欢喜的结局，并自作聪明的解释说"经过一番努力，陌生人可以活动了，而登山者也因此暖和了自己的身心"，潜意识要考生得出"救人就是救己"的结论。但很多人指出了这中间的逻辑荒唐和不合常识。这件事在本质上就是舍己救人，在暴风雪中自身不保而去救助冻僵的垂死者，十之八九是要同归于尽的。考官向为500万毕业生竖了一个道德高标，在考题的文字中明白无误的暗示了一种高风亮节的利他选择，而将个体利益的价值向度完全排开，然后要考生在一场决定著自己未来前途的考试中对此表态。

放在平常也就罢了，放在高考场上，我只能将此题理解为赤裸裸的要挟。你若不在文字上表示甘愿牺牲生命，就要在考场上牺牲寒窗十年等来的高等教育机会。这已不仅是在鼓励和暗示孩子牺牲自我而已了，这实在是一场明晃晃的道德绑架，一场以500万学子的锦绣前程和一千万父母十数年含辛茹苦的期待为质押物的道德绑架。在作文中表示舍己救人是不需要勇气的，在这种道德绑架面前敢于表达不同的意见、敢于在弹性最大、分值最高的作文题中冒"政治不正确"的风险而说真话，才是真正需要道德勇气的。

而这样的道德勇气，却在年复一年道貌岸然实质上卑劣无耻的作文考题中被不断扼杀。究其动因，并不单单是一个"文以载道"的传统使然。据报道，有人高考前在一家BBS发贴，大胆认定今年的作文主题必然是见义勇为、舍己救人。理由是去年以来"以德治国"论的高扬和公民基本道德建设纲领的颁布，他推测今年的作文仍然会在这一方向，又不能原地踏步，必然会对去年的"诚信"题目作进一步深化。拾金不昧已经不过瘾了，非得舍己救人不可。

事后记者调查，发贴者为一应届毕业生。

一个应届毕业生不过十八九岁而已，但于考试一道沉浸已久，对语文教育与意识形态之间的“传教士体位”竟有如此入微的先见之明。近年来国内对于应试教育的讨论很多，却少有触及到教育与极权主义背景的关系。教育为什么要去迎合意识形态，这种迎合是否构成意识形态统治方式必然的组成部分？上个世纪40年代国民党在校园推行“党化教育”，遭到自由主义知识分子和共产党支持下的左翼力量的反对。然而共和国的合法性既然完全建立在“一个主义”的共产党意识形态之上，教育必然要承担的一个责任就是为政权源源不断的输送在价值观上“政治正确”的臣民。意识形态的绝对正确性既然不能通过辩论和观念市场上的竞争得到验证，那就必须通过反复的“教育”来获得保障。换言之即必须维持价值观教育上的“托拉斯”地位，积极培育极权主义意识形态的土壤。因此这一党化教育的传统就被执政后的共产党变本加厉的延续下来。尤其在89年之后，邓小平痛定思痛认为最大的失误在教育，言下之意即是党化教育的失败。

党化教育怎么才能不失败？如果说社会现实对于一元主义价值观的冲击是当局无法控制的，那么当局有可能控制的就是教育。只有当党化教育的强度压过了社会现实的鲜活度和思想启蒙的强度，才能维持住台面上的平衡。从这个角度说，极权主义统治在今天最大的困境并不在腐败，而在教育上。江泽民近年来以望八之高龄，拼命重申意识形态统治的伪神学基础，意图为如他一般腐朽在即的统治合理性重塑金身，意图将多元利益与价值格局的现代社会大而化之在“意识形态为体、多元利益为用”或“政治极权为体、市场经济为用”的黄粱梦中。一方面将极权主义意识形态作为当代的“三纲五常”供奉起来，纲常不变而洋务恒新，走的是百年前传下的道路。另一方面则将过气的共产主义意识形态加以稀释，大而化之为三个代表和一般意义上的“德治”，心想以更少的内涵来统摄更多的外延。

尽管科举制下“八股取士”的考题亦多三纲五常的道德内涵，却没有道德绑架的意味。经义之外还要考策论。策论者即政论也。历代真知灼见的应试策论并不少见。并且纲常中的道德操守也并非全是“政教合一”的，但历史上没有哪个皇帝敢于将自己打扮为绝对的道德权威。道德权威在中国古代，从来存在于士大夫和民间，而绝不落在皇家。对儒家伦理的强调不仅具有维系君臣纲常的作用，同时也造就了对于在位者强大的道德压力。所谓“德治”首先构成了对皇室的约束，而非对士大夫阶层的约束。这是儒教与现代极权主义意识形态的差别。中国的传统士大夫直到专制主义鼎盛的明清两朝，依然葆有敢说真话、敢逆龙鳞的传统。其背后的力量正是在科举之中得到张扬的儒家纲常。

光绪元年，四川东乡一村因农民聚众抗粮，被诬为造反，全村男女老幼400余人被屠杀。当年成都乡试中，来自东乡的十几个童生皆不顾考题和科举前程，而在考卷上状告川督、为民请命。学政张之洞感念其气概而不作追究。这种考生、座师两方面的道德勇气，在今日极权主义治下已不复得见。

事实上，今日的极权者亦行进在一场合法性危机的暴风雨中，不知自立立人，却欲通过歌舞升平的谎言和意识形态神话如同义和团式的咒语桃符以求独活。近年来，以加入WTO为标志的洋务运动高峰时期，党化教育不仅没有得到如同80年代中后期一度的弱化，反而顺理成章的愈演愈烈。教育，已经成为统治者维持意识形态统治基础的主战场，成为意识形态的登山者在风雨飘摇之中的垂直极限。一方面是以系统的"马列主义毛泽东思想邓小平理论"为主要内容的、针对未成年人的校园教育，一方面则是以"三讲运动"和"三个代表"为口号的、针对成年人的继续教育。尽管后者的影响力在成人世界中已经接近于零，但这种以扼杀思想多元和个体自由品质为特征的党化教育对于尚不具有足够免疫能力的未成年人世界，依然是一种少儿不宜的颠覆性邪恶力量。这种力量尤其在中小学语文与政治两门课程中（以及历史课）深入骨髓，使一代又一代未成年人沉浸在虚假的公共道德与政治觉悟的面纱下，蒙蔽在单一的价值背景和对伪神学意识形态似是而非的遵从之中，对于真正健康诚信的道德生活和遥远的观念世界日渐失去了判断力和起码的兴趣。

政治课从小学一直延续到研究生教育，是专业的党化课程。近年来大学里的政治课不断加强，包括邓小平思想、江泽民的三个代表都纷纷开设专门课程。以笔者所在学校为例，因为大一大二的课程中英语、计算机等公共课原本较多，加上名目繁多的政治课，有时候整个大一的课程表竟难得看见几门专业课程。加上课程较多，为减少学生考试压力，一般大学每学期指定少数重要的几门课为考试科目，其余的则随堂考查。而所有政治课程根据中共宣传部门的要求，是必须列为考试科目的，这是明知学生不上心而用死记硬背的办法，计划让他们慢性中毒而毒发身亡。笔者所教的法律专业课程就曾多次被政治课挤占，而降为考查科目。

今年初，为减少考研的压力，教育部削减了考研的科目。竟然宁肯将专业知识课删去，而保留了政治课。

作为专业的党化教育课程，政治课在学生的基本知识架构上相对比较独立，意识形态处心积虑天天洗脑也就罢了。但意识形态侵入中小学语文尤其是作文课的教育，我则将之看作极权主义对中国人民犯下的最严重的罪孽之一。因为语言是最基本的知识，语文教育构成了受教育者最基本的思维方式和情感方式，它的目的是让受教育者学会做一个人，进而做一个中国人，做一个文化意义上的人和一个精神层面上的人。但这一切的价值都在排序上给做一个意识形态下控制下的政治人让开了路。它将一种病毒放入了每一个受教育者基本的操作系统中，幼年植下的毒素，即便在成年之后你表示对这一体制的反对，最可悲的莫过于你首先就是这一体制的杰出产品。你的语言方式、甚至你的抑扬顿挫都已经难以完全去掉那些深入骨髓的伤害。

托克维尔曾经观察到美国语言的日常化与其民主制度之间的关系。因为政客要取悦于民众，就必然倾向于使用民众的语言。只有在专制政体下，才会单独发展出一整套与民众和真实的日常生活格格不入的话语系统来。香港学者吴国光指出，在中文语境中，由于意识形态对于语文教育高屋建瓴的独裁作用，出现了“公共语言与日常语言分裂”的“语言二元化”现象。不但是官员，我们在电视里可以看到就是一个普通的市民，一旦有机会在镜头前公开发言，他就不自觉的采用了另一种“语言”，一种与他日常生活中的语言及其逻辑毫不相同的“假语言”来回答采访。

这种在民众中根深蒂固的假语言之滥觞，就来自中学语文尤其是其中意识形态化的作文教育。来自于每年高考作文题参与了合谋的意识形态对于中文语言的长期阉割。

一个成年人使用这种拿腔拿调的假语言，一则出自于语言习惯，一则也出自于对生存语境的体会而采取的将个人愿望隐藏在公共愿望之下进行模拟表达的一种自我保护。而一个天真的未成年人并没有这种自我保护的意识，他若不自觉的在公共场合使用这种“作文体”说话……（此处有一段乱码）“感谢共产党，感谢全国人民。”

我丝毫不怀疑藏族小女孩写这句话来表达感激之情的真诚，然而问题是一旦她想要表达，多年的意识形态教育的恶果就开始浮出水面，除了这句她从小耳闻目染的代表了最高语言规格的话之外，她所受的教育里并没有为私人间情感的表达留下足够的话语空间。她的语言能力及其逻辑，是一种将一切个体欲望都化为公共欲望，将一切私人空间都拖入公共空间的意识形态语言。正是这种语言构成了政治专制的语言基础和操作平台。而推广和塑造这种虚假的语言，就成为了几十年语文教育的一个终极的甚至赤裸裸的目的。中国人在绝大多数公开场合下，就像每一位考生在高考场上作文一样，必须借助于这样一套话语，来寻求表达的可能，但这种表达，这种可怜的表达，我只能称之为“对表达的一种模拟”，而不是表达本身。

那位西藏朋友还补充说，小女孩这句话话里有话，尤其是后面半句，除了一般意义上党化教育的影响外，还有另一种意识形态几十年来刻骨铭心的教育成果，即对藏族进行的汉人拯救了藏人的大民族主义的灌输。她分析说，对藏族人而言，不但是共产党救了全中国，而且是汉人救了藏人。在很多藏族人心中，都会把一个汉人下意识的当作共产党和全国人民的化身。

我无法评判她的看法，但我的问题是，在意识形态的党化教育下，这个藏族小女孩和其他地方的汉族小女孩已经没有了丝毫的区别，那么宗教精神和文化对他们的未来和西藏的未来还会具有价值和影响吗？

