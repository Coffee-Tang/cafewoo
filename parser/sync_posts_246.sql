<br>　　――至少我们可以互相温暖。
<br>　　他伸出双臂过来拥抱我。我们如同悲伤而无从表达的孩子般拥抱。只是取暖。我也只是要温暖而已，不是要你的爱。心里没有了爱，便不会再感到孤独和伤害。你应该早点明白这个道理，这个我为之挣扎了多久才明白的道理。
<br>　　生命就象一个巨大的漏斗，我看见灵魂风化成黑色的粉末纷纷流走，无可挽回。
<br>
<br>　　然而春天一样是如期到来。天气回暖的时候，沈恬陪我去买草席。出了校门，在天黑得越来越晚的春末时节，仍旧明亮的黄昏天空下，我们骑车在人潮汹涌暖雾弥漫的大街上，忽然感觉这一切就是我想要的北京，和一起做梦的兄弟一起混杂在熙攘的街头，身后是满城灯火，前方是一片夕阳。
<br>　　――我的、我的圣城北京呵。
<br>　　然后我终于见到了乐队的成员，贝司手黄越和吉他手余涵。说来真的汗颜加入IceAge一年了才第一次见到自己的同志。那时他们已经放假，从外地的学校回来，而我和沈恬所在的Ｒ大因为非典停的课要补回来而留在七月的学校。沈恬给我发短信说他们俩来了，当时我正在上专业课，直接站起来出了教室冲到学八楼的男生宿舍。
<br>　　――看到两个男生，各背着一把琴，背靠在墙上，低头弄弦。黄越是高高的个子，鹰钩鼻，极短的平头和晒黑的皮肤是军校里的痕迹。余涵有着修长的手指，很瘦，笑起来竟然和我一样有酒窝。他们同时抬头，冲我笑，有一点傻。我想我在那个时候就完全接受了他们，是作为知道从此将一直并肩作战的兄弟接受下来的。那时学院的各个班陆陆续续下课有人回来了，那些男生呆呆地看着飙着吉他的他们。我们就这样肆无忌惮地在男生宿舍的走廊上说话，弹琴，争论每个和弦的配法。所谓的少年轻狂大抵也不过如此吧，然而后来回想才知是多么难得的快乐的时光。那时我们在一起。
