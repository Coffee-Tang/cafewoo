杨：我不是，是我的不是还是你的不是！你带我上威虎山来给三爷助兴，快进山了，你说你在山下有个小蜜，要去会会，让我自己上山，害的我在山上转了三天，才找到三爷这儿， 你说，是我的不是还是你的不是！

栾：你，你你，你胡说！

崔：什么？栾平，你小子私自去会小蜜，怎么又让共军抓住啦？啊？

栾：三爷，他不是胡彪呀！

崔：对，他不是过去的胡彪啦，他现在是我威虎山上的老九，栾平，还不快去见过九爷！

栾：你，……你是共军，三爷，他不是六子，是共子！他是从共军那来的！

杨：栾平，咱俩谁是从共军那来的？你不是让共军抓住了吗？怎么又把你给放了？你刚一回山，我胡彪就成了共军，你要是在山上住两天，那克林顿、泰森哥儿几个，不就全成共匪了吗！

克：他妈的！栾平这小子，准是让共军收买了！

泰：敢挑拨离间，我割他的耳朵下酒！

栾：三爷，他真是共军！在共军那，就是他审问的我！

英：他审问的你？他怎么懂得黑话？怎么对山上的事一清二楚？甭问，是你小子招供了，你把三爷出卖了！栾：不，不，我没招，我我，没招！

杨：三爷，我胡彪初来乍到，不懂山上的规矩，也不知怎么得罪了这条疯狗，被他反咬一口！三爷，今儿威虎山上是有他没我，有我没他，留我留他，三爷您看着办吧！

崔：（叫板）哇呀……这，这，谁是六子，谁是共子，这如何是好？不如，干脆，把两个都毙了，最干净，对，两个都毙了。

英：哥，你要是敢碰我们家阿彪，我就出家当尼姑去！

崔：小姑奶奶，别，你消消气。

四匪：三爷，九爷不能走啊！

范：三爷，栾平那小子，准是被共军收买了，咱不能信他的！

崔：这样吧，我这儿有个钢蹦儿，正面是栾平，背面是胡彪，哪面朝上，谁就是共匪！（抛，众人凑上）

四匪：啊……正面，正面，栾平是共子，九爷是好人！

杨：我当然是好人！

崔：哎呀，胡彪兄弟，老九，消消气，三爷我火眼金睛，能上栾平的当吗？

杨：三爷信不过我，，我不干啦，我走啦！

英：哥，胡彪要是走了，我就当尼姑去！

崔：老九，老九，你别走，我们可少不了你呀，

（唱《only you》） only you，能保我威虎山，

only you，能办我百鸡宴。

背黑锅我来，送死你去！

only you！

栾平，你这条疯狗，你投降了共军，还敢回来挑拨离间！范志毅，把他拉出去毙了喂野狗！

栾：三爷，三爷，饶命！胡彪兄弟，你饶了我吧！我不是人，我是野狗！

杨：慢，毙了他就便宜他了！我倒有个办法，够他消受。

崔，四匪：什么办法？

杨：咱们山后不是有个大粪坑吗？

崔，四匪：怎么样？

杨：把这小子大头朝下，，种到粪坑里，明年开春长出个小栾平来。

崔，四匪：哈哈，高，高，还是老九有办法，种了他种了他！

杨：交给我了。

杨：（拉栾平到一侧）栾平，你罪恶滔天，屡教不改，今天我代表人民，――种了你！下去吧，你！

