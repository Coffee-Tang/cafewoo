（唱京韵大鼓）耳听栾平回了山，

不由心中起慌乱。

同志们看管太不严，

放虎归山他成了祸患。

我欲上前，一枪打死他算，

（白）唉，不行呀！

不如跟他当面对质，

把那是非反呐。

栾：（小跑上场）三爷，三爷。小的这趟下山，栽大发了，胡彪没找到，倒让共子抓了去，差点丢了脑袋！

