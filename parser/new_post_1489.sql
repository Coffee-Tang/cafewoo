小姨。那天她从家拖着她的一箱衣服抱着一盒化妆品在冬天里戴着墨镜掩饰着眼角的伤来到我家时，大概也是一模一样的。一模一样的。

唯一属于自己的物质上的东西，带走。带，走。以此来掩盖住什么，再依赖住什么。

那时候小姨年轻漂亮。在时尚中摸爬滚打，却有着最浪漫和单纯的憧憬。一直纠缠的一份感情，和一个突然出现的男子。用家里的钱晃荡，甜蜜而惬意，直至结婚。结婚是她提出来的，固执而倔强，缠了几个月，男子终于同意。可是她没有工作。他也没有。并且，同时，她的家人与她闹得很僵，而他的父母，似乎并没有真正出现在故事里面。于是两个人开始无依无靠。从公主和驸马降到布衣。餐厅里工作过，服装店工作过，酒吧工作过，因为总不能持久，贫穷依旧。那一天男人酗酒在家里打过她，然后她把租来的逼仄的房子里唯一属于她的东西带走。却倔强着不肯离婚。再后来便发现有了孩子。男人终于心疼，把她接回去，耗到25岁过后，终于平定下来，也终于磨掉了性子打算背负起已经无法更改的现实。然而她已不再年轻了，也不再美丽了。虽然顽强地走了过来，岁月的风尘却早已刻得如此明显。

我一直记得那个抱着花盆的女子。美丽的面容，她有。美丽的感情，她一定也有。我是多么多么希望她可以过上美丽的日子，在苍白的或者透明的天空下。那个花盆，也许是她和他第一次一起买的，她往其中种下希望和明天。最后离开，依然舍不得遗弃。倔强如劲草，就总可以坚持下去吧。

也许我们都不很清楚自己要的是什么。俯下头看见大堆的作业，我微笑。这是我拼命抓住的东西。它是否可以把我带到另一个彼岸去。虽说霓赏彩衣毕竟虚幻，却一如梦想斑斓。

