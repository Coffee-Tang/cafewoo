电脑始终重复播发着范逸臣的《I BELIEVE》，我也正用这首歌来麻痹自己。

（三）

“喂，还活着吗？”苦熬了两个月后，我终于等到了她的电话。心情异常激动兴奋的我险些握不住话筒。

“废话！我当然健在，你呢？还在害人吗？这么久没消息！搞隐身啊！”我向她炮轰自己一连串的问题。

“呵呵！我考进交大拉！家人带我去欧洲旅游。这几个月玩的好爽啊！今天刚回来就给你打电话了，高兴吧？！”话筒的另一头传来她轻悦的声音，我们就这样聊着，聊着。。。。。。

在抵达光明之前，我们必须经历黑暗。无助的我却在黑暗中跌到了。。。。。。

开学前一天，我出了车祸。。。。。。

当你发觉时间流逝之快后，你已经习惯了时间。

在病院里，渡过一生最黑暗的三过月后，我终于被获准出院了。

之后的日子里，我打给她电话说：“我要去内蒙古，88。”话完后毅然断绝了与她的联系，只身去了内蒙古。那里有印象中荫绿的草原，聆听着清风而过的声音，享受着芳草的清香，感受着纯自然的气息。我也好不吝啬的把这些温馨的感觉保存在自己的心中。我亦觉察到自己脸上出现了到那久违的笑容，猛得发现没有她的日子里，也能快乐得活着。至少，我学会了如何坚强地去生活。

人，总需要经历挫折，由此才能试着长大。

（四）

从内蒙古回来后，听朋友说过几天有一场她的演出。但是我没告诉她我回来了，只是一个人在比赛那天买了张票，坐在最后一排看着她的演出。

今天的她同往常一样漂亮迷人，青秀的长发披在肩上，很飘逸。演出开始了，她有节奏的拨动着手指，双眼注视着面前的曲谱。渐渐地，听众们开始专注着她的一切，没人敢发出一丝声响来破坏这份和谐。悦耳的琴声在这剧场里穿荡着，蹿入每个人的神经里，观众们的任务便是好好享受这一切。

。。。。。。最后一曲开始了：“《献给艾丽丝》，我想把这首曲献给我的朋友，我坚信他今天一定会来的。”听到这，我猛的站起来，同时感觉她已经注意到我的动作。于是拼命准备离开这里，但身后是熟悉的脚本声飞快的的追逐过来。

“我绕圆圈顺时针走动，她绕圆圈逆时针走动，在两人绕完半个圈后就一定会相遇。”

在我走出剧院之际，她扯住了我：“为什么要躲着我？还有，你的――眼睛――怎么了？”话中带着她的哭泣声，难道这是久别重逢激动的原因吗？

“出了车祸，碎片割破了眼球，现在的我――瞎了。”

“你知道，我不介意的！我们还有机会，对吗？”她试图挽救些什么。我也无法判断她是说出来还是哭出来的。

“晚了，抱歉。。。。。。”我努力压抑着自己的情绪，故作冷淡的回答她。

我只感觉她冲到我的面前：“不，至少我还没抛掉希望。看到流星后，便能许下一个愿望，你看！”

“现在是白天，拜托！”可在这一刹那里，我看到了流星。生平第一次如此清楚确定自己的感觉。我此间的思绪彻底泛滥，眼泪从眼眶涌出，因为流星正在她的指尖闪烁着。。。。。。

