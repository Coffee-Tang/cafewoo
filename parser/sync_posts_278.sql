<br/>　　是啊，那些在饭店里的图西族人所能做的就只有指望上帝和他们的饭店经理保罗了。《卢旺达饭店》的电影广告词告诉我们：“当世界闭上眼睛时，他却张开了双臂。”是啊，当人权已经不值一文的时候，我们所能依靠的也就只有人性了。
<br/>　　
<br/>　　我们总是觉得大屠杀的惨剧距离我们很遥远，辛德勒的故事不会再重演，但是我们是否真的认真思考过，发生在非洲大陆的这场种族浩劫距离我们真的不遥远，它只有十年。我在一篇介绍该影片的的影评中看到这样一段话：有人说面对历史，我们不该忘却。可仅仅只是熟背了“大事年份表”就会有用吗？“1994年4月到7月的100天里，卢旺达共有91万人被屠杀。”这个句子，顶多只是一张考试试题上的2分罢了，也许它还根本不会考到。是的，扪心自问，当各位读到白起坑杀40万赵军战俘，清兵扬州十日、嘉定三屠，南京大屠杀死亡30万人的时候，我们是对其真正了解呢，还是认为这个只是一道或许会考到的历史考题？如果我们每个学生在学习历史的时候是真正的去了解那时候的人性，心理和环境，真正的以史为鉴，而不是喊着世界和平，民族平等这些空话。那么这个世界也许真的会安宁许多。
<br/>　　
