<br/>　　
<br/>　　影片公认最为感人的一幕出现在西方世界的军队到来的时候。那时，饭店的人们欣喜若狂，以为已经抓住了那生的权利。但是，他们错了，西方世界的军队是来保护他们的侨民离开的。当载着侨民的汽车被武装保护离开的时候，天正下着大雨，饭店里的图西族人眼神里充满了绝望和无助，连唱者天籁般歌声赶来，以为得救的孤儿们，也无法感动那些可以保护他们的人。雨很大很大，保罗在雨中是如此的悲伤，就像整个卢旺达都在为他们哭泣。那些撤离的白人，面对这些站在雨中的女人还孩子们，他们是否会为他们国家的作为感到羞耻？也许就像那为西方记者的话那样，“上帝啊，我感到羞耻”。
