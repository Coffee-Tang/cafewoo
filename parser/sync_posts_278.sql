<br/>　　影片一开始，就出现了煽动性的电台广播和胡图族人的游行，然而身为饭店经理的保罗依然相信事情不会那么糟糕，冲突不会发生。直到4月6日，一架载着卢旺达胡图族总统哈比亚利马纳和布隆迪总统的座机在卢旺达首都基加利上空被火箭击落，两国元首同时罹难。胡图族极端分子得到了充分的借口，开始了一场空前的种族大屠杀。影片的多数时候虽然是在刻画保罗在这场浩劫中如何地运用自己的智慧和圆滑的处世方式拯救着饭店里的上千个图西族人，但是真正的主题却是在控诉那些所谓民主的人权大国在这场浩劫中的袖手旁观。
<br/>　　
<br/>　　当这场灾难发生十年有余的今天，我们突然发现似乎有太多的人权大国领导人表达了对当年在这场灾难的反省，发表了催人泪下的演讲，信誓旦旦的向全世界，主要是向自己的国民表了决心，决不让悲剧重演。对此，我们所能做的，只有向那些放出如此狗屁的政客们伸出我们五个指头中最长的中指。当刚果，艾塞尔比亚等贫穷的非洲国家仍然是饿殍遍地的时候，当世界上最富有的那几个国家领导人还悠哉地躲在美丽的苏格兰高地煞有介事地谈论那个已经谈论多年的非洲问题的时候，你教人们怎能相信如此的誓言？
