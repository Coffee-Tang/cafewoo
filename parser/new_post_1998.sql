彼彼。她总是骂我，每天都要唠叨我几句。但是在去年圣诞节的时候，她送了我一张贺卡，那只是张我们在逛街的时候发现的我随口说了句我喜欢的卡片。那上面画着搞笑但是很实际的漫画。圣诞节的那一天，我收到了那张卡片还有一张绿色的纸，彼彼在上面写：

“Dear 若琳：

希望总有一天，你会像‘万人迷’一样，那时候，你就没必要整天把‘相亲’挂在嘴边了。

别太辛苦喽！希望你尽早有个从晚上十点钟起的美梦。

在平安夜凉风攻击下的彼彼

2002．12．24 晚8：08”

那段时间，我每天都在抱怨晚上太迟睡觉。那个时候对床是有那么一种特别的感觉啊。而且每天都在戏谑地说要相亲相亲，闹得人人见我都给我介绍对象呢。呵呵。时间飞快地过去，如今想起，那些天真的过去真是很快乐的，而真正让我动容的却是彼彼玩笑但却关切的话。夏天的深夜，我翻出那张纸，看着上面的字，心情变得潮湿起来，幸福就是那样地降落下来，我却是那么地想哭。为我瞬间握住的幸福，为我们逝去的时光。幸福就像是抓在手里一大把的阳光，细碎温暖，从手心漫溢出来，到处都是。都是明晃晃的幸福。

很快的，七月来了。这意味着我已经初三。那天，我问彼彼为什么初一的都不要补课啊？她吃惊地看着我，说初一的早上不是在上英语吗？我摇摇头说，那不是9月份新的一届么？她看着我说，我们已经初三了。

我们已经初三了。那一刻，像幸福一样的夏天的阳光就那样沉重地砸下来，我感觉眼前头昏眼花。我终于接受，喃喃地说，我们已经初三了。然后慢慢离开。

我们于7月21日报名，第二天上课。报名那天，我和涂涂去得很早，越蝶也去得很早，还有倾倾，都在那儿。我看着她们，笑着大声说，我要好好记住这一天。因为这一天我们升初三了。这是个重要的日子。我要记住。我心里流过一条冰冷的河，河水凉得让我心里痛。可是我还是笑着的，要快乐地跨过迈入初三的这道成长的门槛，我对自己这么说。她们看着我宽容地微笑，说我孩子气。我眨眨眼睛，眼里差点流出某种液体。又一次成长的蜕变就是在那一瞬间张开它巨大的翅膀笼罩住了我明亮的眼睛，一晃而过。一只大飞鸟拍着翅膀越过屋檐，我什么也没有听见。

假期里的校园很寂寞。很寂寞。我们在四楼上课，没有什么人会下楼，更没有人到后操场去。10点过后，上完英语课的初一的孩子们哄地离开了学校。校园又变得很安静很安静，没有人说话。阳光下时光偷偷溜走，没留下任何告别。下课后我和彼彼一起看楼下的后操场，阳光灿烂，可是那么冷清，寂静得没有声音。我朝窗外大声地叫，叫啊叫。那样撕心裂肺地叫很容易让人哭的，眼泪就是那样在高空中出现，然后流下来，可能会坠落四楼，最后蒸发，也有可能还未落地就已经消失。我记得很久以前，有一天上午的课间休息，我就是那样很大声地叫的。感觉心里突然空旷了起来，然后眼泪迅速地出现，风吹过去，很凉很凉的很细微的感觉滑过我的脸。那时候，谁也劝不住我，日日还跑过来，一脸紧张的神情在我旁边叫我，娘，娘。我心里一波一波的感动荡漾开，我以前一直要求当他的妈妈。他是个讨人喜欢的孩子，皮肤白皙且细嫩，又高又瘦，笑起来有点坏坏的却像孩子一样稚气调皮。可是，现在呢。我想，再也不会那样的场面了，那个他紧张地叫我娘的场景永远也不会再出现了，它已经被淹没在汹涌的时光里，随时间奔腾着离开了我。离开了已经初三的我，离开了现在心里空旷的我。留我一人，那么孤单。

夏天的中午放学后，我飞快地穿过炙热的大街和小巷，偶尔抬头怅然地看湛蓝的天空里飘着白色的云朵，阳光洒在我的脸上，瞬间成了阴影。那如阳光般的幸福呢？会不会也变成了阴影？那时候还是不是幸福？我又开始漫无边际地发呆和失神，直到听见皮肤在阳光下发出细碎的爆裂声才瞬间清醒，便立刻冲回家去。

有太多话我都不曾说过，那是埋藏在我心底很深切的东西。还有那些回忆，一直在我心里低低地唱，像恩雅的声音一样婉转纠缠。它们伴随我幸福和泪流的感觉，在这个七月涌了出来，涌了出来。跋涉过整个心灵荒漠。就像7月17号的时候我去考钢琴一样，那时候要穿过一条走廊才能到考试的教室去。在走过那天的走廊的时候，外面的阳光那么明亮，有一些透过绿色的树叶斑斑点点地散在我的脸上。我突然想，以后可能再也不会有这样的瞬间了。我在享受这一刻的惬意时已经和它做了告别。我们每时每刻都在告别，和时光告别，和爱告别，和自己告别。告别过去的自己，告别那些珍藏着的过往，我总是那么珍惜它们，抱着它们不肯放手。可是，时间像风一样迅速地吹过，离开，一切都被带走。我除了微笑着把它们藏起来，等以后有一天再找出来感叹之外，什么也不能做。就那样看着它们离开，心里平静得没有一点波纹，可是轻轻吹一口气都会丝丝地疼。

七月的天空从未有过的湛蓝，我养成了仰着头看天空发呆的习惯。那片湛蓝广阔得没有边际，像平静的湖面，我在上面总能看到时光的倒影，在呼啸而过的黑色的风里，我看着那些过去模糊的画面像昏黄的胶片电影一样重演，就微笑着，然后眼泪掉下来。

