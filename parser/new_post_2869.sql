我觉得自己负担不起这样沉重的曲式了。有人说，剪掉头发，那会为你带来全新的生活。可是……可是，乔答应要用一只金黄的蝴蝶发卡束起我的长发啊……乔，那只蝴蝶呢？它是不是跟你一起飞走了？你们能飞到哪里呢？乔，你不知道蝴蝶是注定飞不过沧海的么？
还是你们已经轻轻落入平静的深海了……
每个人都可以在某一个存在于这个空间的事物上找到自己。这是现在我唯一深信不疑的了。我在哪儿呢？……寻觅……寻觅……一无所有啊。书中没有我的影子，生活中没有我的地位……连一个最终的归宿都无人肯施舍。我再不寄希望于别人了。我已经深深明白，别人身上找不到我的希望。因为，我的希望是灰色的。我理解，只有自己容忍这样阴暗的心。
