“谁呀？又是哪个明星大帅哥在大街上被你认出来了？”小米说她有一次在大街上认出了微服私访的一大腕，冲上去又是握手，又是签名的，把对方唬得一楞一楞的，临走了还送一飞吻，高喊着拜拜。回来后说这事儿差点没把大伙给乐死。其实那人压根就不是什么大腕，就是那天她心血来潮，想看看一般人被认错成名人会有什么反应。她还美其名曰“街头艺术”。大伙啼笑皆非。

“不是，不是，”她略显烦躁地否认了我的揶揄，“是莫非――”

我的手突然抖了一下，杯里的茶险些泼了出来。我努力控制着自己脸上的表情，试图让自己看起来不动声色。面部肌肉僵硬得很难受。我知道小米两眼正像探照灯一样盯着我，即使此刻我没看向她。我有些心虚地将手缩回到桌下。

我不知道小米是否看出点什么，或者什么都没看出来。但她继续开口了，这让我松了口气。

“那天我去逛宜家的时候看见他们的，”我的心一紧，他们，“那女的蛮眼熟的，纤纤细细的，但隔得远，也看不清是谁。他们站在一组沙发前，后来还找售货员借了量尺量了下大小，估计是要装饰新居。”我的胸口一阵憋闷。

“隔得那么远，女的看不清楚，男的你就看得清啦。”我没好气地应她。

“那当然了！好歹他也跟你――”她猛地打住，知道说错话了。我知道这是我自找的。不怪她，我没那么问，她自然不会这么说。

小米好久都没说话，那一刻我的脸色一定难看极了。我觉得自己很没用，很是沮丧。沉没让气氛显得有些尴尬，好在我们彼此已经十分熟��。

许久，小米试探性地问了一句。“宁宁，你还没忘了他？”

我牵强地笑了笑，一定笑得比哭还难看。“怎么能忘记呢？一辈子都不会忘的，他是我的初恋。”也是我的第一个男人，我在心底默默地说。

可我却不是他的第一，我恨恨地想着。

6.

秋高气爽的日子。整座城市也变得亮晃晃的，我都有点睁不开眼了。天气开始干燥起来。我甚至可以听到空气在我耳边噼啪作响的爆裂声。

我牵着简爱在公园附近溜哒。大概是受到天气的影响，简爱显得很兴奋。不停地窜来窜去，我都快控制不住它了。以前莫非就总笑话我说不是我溜简爱，是简爱溜我。

简爱还是一刻也不停地扭来扭去，突然朝着某个方向大叫了两声，就奋力地窜了过去。我没有控制它，只好跟着它跑。我心里委屈极了，几乎不想管它了。我不明白自己为什么留着一条总是跟自己作对的狗，它不过是莫非留给我的一条狗命！

简爱一直跑到公园里的某个角落。冲着一位正坐在长椅休息的少妇狂吠了一阵，把她吓了一跳。我用力地拉住它，“简爱，不许胡闹。”一边不住地对她道歉。

对方倒是通情达理的人，定了定心神，“没关系，没关系。”我很不好意思，反倒是她先开口解围，“你的狗叫简爱吗？”“嗯。”我点点头。“好别致的名字，”她趋前想逗逗它，简爱冲着她又是一阵呲牙咧嘴的，又把她吓回去。我只好窘迫地道歉，“真对不起，以前没有管教好，现在脾气很暴。”“没关系的，如此更有个性呢。”她笑眯眯地说，“我也想养一只狗的，可我老公总不答应。”

好不容易让简爱安静下来。缓过劲来打量对方。竟觉得有些面熟。她的脸上也渐显讶意。

“你是江小蕾？”“张宁宁？”

居然是她。她是我高一年的同学。关系不错。后来文理分班，都选了文科，但我在六班，她在八班，楼上楼下的，也就疏远了。我们后来还进了同一所大学，只是我念商科，她选中文。两个院系隔得远，也难得见上一次面。毕业三四年，杳无音信。没想到会在这里碰上。世界真的很小。

多年不见的老同学，尽管不是很熟悉，却也感觉格外亲切。“真的好巧，四五年没见，竟在这儿碰上了。”我坐到她身边。“你怎么一个人在这儿？等人吗？”“是呀，等我老公，我口渴，他去给我买饮料了。”我纳闷着怎么如此娇贵，无意间瞥到她一直放在小腹上的手，恍然大悟。她大概也看见了我的表情，有些不好意思。

“恭喜呀，要当妈妈了。”“谢谢。”

“几个月了？”“快三个月了，还看不出来。”怎么看不出来呢？身材还是纤细的，但脸上的表情已经是带着些许母性的慈爱。我有点羡慕她。

“你也住这儿附近吗？”我问她。

“是啊。前阵子刚付的首期。”她一脸甜蜜幸福的样子。

“你结婚多久了？怎么也不通知一声呢，真不够意思。”我状似埋怨地揶揄她，她的脸一下红了起来。“我们月底才结婚。”我顿时也不好意思起来，气氛有些尴尬。我只好掩饰着抬起头，打量着四周，嘴上说道，“最近几天天气还不错呢――”话音未落，看见远处渐渐走近的身影，手里拿着一罐饮料。我傻眼了。世界真的很小。

看得出他的脚步也有些迟缓下来。真的是他。当他站在我们面前，我的嗓子开始发干。

“医生交代不要喝含咖啡因的饮料。”他还是很镇定地把矿泉水交到江小蕾手中，并体贴地打开了瓶盖，这个细小的动作让我的心抽搐般地疼痛了一下。江小蕾温顺地点了点头，一脸幸福地接过水。

那一刻，我突然很恨他。简爱竟心意相通似的冲上去咬住他的裤腿不放。我真想就让它那么咬下去，但我还是站了起来，使劲地把它拉住，好不容易控制它了。他的裤子也被撕出了一个大口子。“真对不起。”我的喉咙干极了。

“没事。”他轻轻地说。江小蕾也在一边附和，“没事的，莫非，你看，多淘气的小狗呀。你为什么不让我也养一只呢。”我突然觉得恶心。

他也显得有些尴尬。淡淡地对我说，“你还留着它？”我点点头，故意不看他的眼睛。

江小蕾不乐意了，“原来你们认识呀。”我抢在他前面说，“大家都是校友嘛。我们好象在一起上过一门选修课吧？！”我用力地盯着他，他只是沉默地点点头。“这样啊。”她不再发问。

“我还要去给简爱买些东西。先走一步了。有空再联系。”我们留下了彼此的联系方式。江小蕾还叮咛我记得去喝他们俩的喜酒。我落荒而逃。

回去的路上，几乎都是简爱带着我走。大概它也可怜我。我回想一切，终于明白为什么那日会在家附近遇到他。简爱之所以会冲到江小蕾的身边，是因为它闻到了莫非的味道。没想到它还认得。

只是，他的气味已经属于另一个女人。

7.

给小米打电话，她的声音听起来很兴奋。“宁宁吗？告诉你一个好消息。阿天要带我去西藏旅游呢。”

话到嘴边，又艰难地咽了下去。不忍扫她的兴。“是吗？你家阿呆终于开窍了。”

“是呀。好不容易啊。诶，找我有什么事儿吗？”小米这时候问起，我已经说不出口了。“没有，打个电话问候一下。就得到了你的好消息。”

“嗯，谢谢。哈哈，不跟你说了，我要整理行李了。时间很紧呢。”小米一心一意扑在进藏的准备上，匆匆挂线。

放下电话，心里空落落的，发现自己很失败。出来这么多年了，出了小米，连个说得上话的朋友都没有。

给简爱的碗里放了足够的狗粮，穿上外套，我决定到街上走走。再把自己关在这小小的空间会疯掉的。

走在大街上，又后悔了。在家里那个小小的空间里寂寞，在这条人潮汹涌的大街上，却依然感到深入骨髓的孤独与寒冷。还是无济于事。

走到一个十字路口，我停住了前行的脚步。茫茫然不知道该往哪儿走。身边等待红绿灯的行人来来往往。呆看着红绿灯上的跳秒。十字路口上的时间总是让人心焦，我却无动于衷。

“给你30秒考虑。爱我还是不爱我！”身边响起一个年轻女孩的声音。年轻真好，任性或骄傲都有本钱。男孩似乎有点恼羞成怒，“你别闹了。”

“哈，我闹？！好啊，绿灯亮起之前没有给个答案，我们就说拜拜吧，那样我就再也不跟你闹了。”我忍不住打量起这对起争执的年轻情侣。

女孩一脸飞扬跋扈的样子，但慌乱的眼神还是泄露了她的紧张。眉清目秀的男孩皱着眉头试图解释，“你要相信我，我和苏眉真的没什么――”

“5，4，3――”女孩打断他，径自倒数起来，“2――，1――”绿灯亮了起来，等待的人群又匆匆向前流动起来。女孩大声地说，“好！周立伟！算你狠，我们完了！”女孩很激动，掩着脸转身跑了。男孩还是紧皱着眉头，但分明松了口气。

所有的女人爱上男人的同时也爱上了他的承诺，而男人却不断试图逃避。如果爱情是场追逐，我想我已经精疲力尽了。

8.

或者我不需要爱情。但当寂寞来临时，我还是无力招架。我终于向叶维发出了求救。

叶维很是爽快。周末带我去郊游。抛开城市的喧嚣，走进了青山绿水间，再多的烦恼忧愁竟也变得渺小起来。快乐像是儿时吹出的肥皂泡泡，一串串的在阳光下五彩缤纷地飞升。有多久没有如此放松的呼吸了呢？我有刹那的惘然，但随即被我抛之脑后，如此良辰美景，为何还要自寻烦恼？

时间也过得飞快。只是眨眼的工夫。车子开进市区时，我还缓不过神来。叶维把车停了下来，对我说到了。我瞬间想起了那些肥皂泡泡在空气中一个个破裂的情景。那首歌是怎么唱的？“好花不常开，好景不常在……”青春年少的那段美好光阴，已经被我遗失了。那时候身边的人呢？我若有所失。

我走下车了，抬头才发现不是我家。我疑惑地看向叶维，他笑着说已经在这里订了张桌子，一起吃顿便饭。我不禁感动于他的体贴入微。

在最脆弱的时候有这么一个贴心人陪伴着，再加上两颗相爱的心，一定是非常完美的。如果我可以爱叶维，我现在就是世界上最幸福的女人。但我怎么能保证，如果是爱他的，会不会紧张他也是如此对待其他女人。没有天生了解女人的男人，这样一个温柔体贴的男人也是由若干的女人培训出来的。

“叶维，跟你在一起真是一种享受。”我由衷地感叹。

“能让你感觉到是享受，这是我的荣幸。”他的脸上不掩得意。

在这一刻，我觉得他也蛮可爱的，我以为只要努力也许就会爱上他。我抬起头，对他笑了笑。

由于我的座位对着进口，越过他的肩膀，我看见一个大腹便便的女人由一位男士扶着走进餐厅。好不容易平静的心又纷乱了起来。说不清是嫉妒还是羡慕。只是怔怔地看着他们。

叶维看见了我表情的变化。顺着我的目光看见了那位孕妇。回过头来看着我，毫不掩饰地对我说，“我不喜欢孕妇。”我的注意力被他的话引了回来。真让人惊讶，我以为男人想到自己要当爸爸是很兴奋的，就像莫非。想到这，不禁黯然。

他继续说，“怀孕的女人身体变形不说，整个人都变得患得患失的。生完孩子体形恢复不了不说，一心扑在孩子身上。孩子又闹得要死。”说着，他做个无可奈何状地耸了耸肩。

我突然觉得胃口全无。我不知道是不是我刚才的眼神让他做出了这样错误的判断来附和我。但能把这样的事情说得如此绝情，大概也不会做得很漂亮。小米说得对，他原来的名声不好。这样的男人太自私了。只顾着自己快乐。

我又忍不住拿他与莫非比。这一刻，我突然非常想念莫非。我不知道，那个时刻我不在想他。

9.

再后来，叶维联系我，只推说工作太忙。

之后，竟真的开始忙碌起来。公司的业务不断拓展，决定在北京建立分公司。我的上司杨斯明被派到北京管理分公司，而我作为他的助手，也一并派往北京协助他工作。

如此忙碌的生活常常让人焦头烂额。但我却隐隐庆幸。我的时间被适应新工作和新的生活环境一一瓜分。没有多余的时间去多愁善感。换了城市，也不会时不时地触景伤情。每天晚上头一碰到枕头，就呼呼大睡。像一只幸福的猪。

唯一让我无法适应的是北方的天气。入冬后，气温骤降。每天从住所出来到地铁的那段不到500米的路是最叫人痛苦的。即使是涂上一层厚厚的润肤霜，依然可以听到皮肤在天寒地冻中轻微的碎裂声。

我很害怕会因此而长冻疮，但束手无策。在那一瞬间，我明白了身在异乡的孤单与无助。仅仅是因为我害怕冻疮，不知该如何预防却求助无门。原来道理都是体现在生活里的那些细微末节上的。

有一次逛商场的时候发现了一种薄荷巧克力。以前从没吃过薄荷味的巧克力，并且对巧克力这种高糖高热的食物一向敬而远之。但在这个遥远的北国城市，我却要借助它的力量来抵御这个寒冷孤独的冬天。

后来，我竟爱上了那种薄荷巧克力。口袋里总是放着几块。记得许多年前看的周润发演的《赌神》，那时不明白为什么会有那样嗜巧克力如命的人。但后来，我知道了。那种感觉是会上瘾的。清凉的前味先让整个人心旷神怡起来，然后是巧克力滑爽甜腻的口感。之后这两种味道完全融合在一起。真的有舍不得咽下的感觉。

曾几何时，我还满心欢喜地以为，我和莫非就像今时今日的这块巧克力一样。两种味道的融合就是最完美的集合体。但我却忽略了，巧克力不单单只有薄荷味，薄荷味的食品也不单单只有巧克力。当它们结合时，觉得彼此是唯一，但分开呢？

