我是看的清楚的，看到了本质，也看到最初的目的。可还是一样明知顾犯了错误，我劝他们要的少一点，自己却总想要的更多。在离开的前夕，一切都变的更加清晰，痛定思过。猛然间才发现地上的雪都化了，化成一滩无处可寻的水，黑的像泥。

我好勇敢，为了明白自己的能力，不惜伤害自己。

我和他还不认识，我们不曾进行交谈，不曾有过相识，可是我们却彼此理解。就这样，一个在梅雨的地方，一个在海洋性气候里，说着，说着。

我的话让他疼痛。即使伤害了，也不曾爱过。而她说，简直不可以再这么痛。

伸开掌心，画在那里的三条线中，我找不到我的归宿。

或者找的到。

也不愿意面对。我是懦弱的，懦弱的连自己都要逃避掉。不是什么样的结果会使人清醒，我坚信，我是清醒的，我要时刻告诉自己，我是清醒的。

人必须这样时刻提醒自己，想要什么想得到什么，才能避免误入了歧途。

因为这些，躺在那里，我问自己到底想要什么。一个女人不肯告诉我那些过去，她是怕我难过，因为她知道我放不开手，她的话对我只能是伤害。她劝我承认自己的软弱，我怎么能搬起石头砸自己的脚？当然是不肯承认的。我微笑着对她说，不。

我渐渐分不清楚，我到底想要什么，是爱，还是被爱。

五。

我就在这条路上化为一个玩偶，勇气和坚强荡然无存，在这里站着、嗫啜着走着，轻轻靠近你。其实，从开始走的那一天，我就知道你的存在，我看的到你，我猜的到你的模样，可是我不敢走过去，我只是介乎于走和未走之间等你，等你过来。

找到和找不到，又有什么区别呢？我轻轻收回我的手掌。一次又一次的欺骗让我痛不欲生，我的原谅和说服如何苍白，如何形容，简直不可以再这么自欺欺人。我好象是才明白，欺骗自己原来不仅仅能得到麻木，还有一种难以忍受的痛苦――那是一种对自己做出背叛和放弃的痛苦，而且永远都无法再弥补。

永远。

六。

我承认自己很卑鄙，很卑鄙。用了卑鄙的手段，看到了什么也不敢出声。每次我走到这里，都叫自己放轻松些，每次都捏着心脏呼吸。我不敢哭，尤其不敢放声大哭，因为我说过，我已经不会哭――我必须要保持这种姿态，不是吗？

有人说，我们可以欺骗所有的人但就是不能欺骗那个爱你的人。

有多少人作到了这一点？有多少人欺骗的不是那个爱你的人？就连我以为善良的我，还是一样欺骗了那些爱我的人，我的隐瞒和不诚实就是欺骗，是我最恨的欺骗。曾经提及一个男人，那时候他就说过，我最恨的也是我最擅长的。我记的很清楚，当时只是轻轻叹息了，然后关闭了窗口：无言以对。

当我们说到往事，我的心跳的很快，我看到那个躲在角落里蜷缩着的女孩子。那些曾经经历过的事情被拿出来，恍然又重新来过，但结局却是看到的、定好的。这是怀旧还是沉溺？

我想那都是一种美丽。

我是最明白的，看的到结局，却控制不了自己。我总想着有一个男人的肩膀能让我好好的依靠。我是那么需要一双温暖的手和一个真诚的怀抱。可是人们怕我，人们不相信我，因为我说过，我是坚强的，自私的，冷漠的，绝望的。

也只是，看不到希望地活着。还是得这样活着，我还忘记了我的懦弱。

七。

当我准备把信任交给你的时候，你却让我发现了厌倦。我好怕。这是一个女子的担心，可是你以为，我就此回头了吗？

真让我难过，我不是轻言放弃的人。我是不轻易爱的，可是如果爱了，就拿我的整个生命来爱那个男人，哪怕在别人的眼里他只是一堆插满鲜花的垃圾。我把生命都拿了出来，又怎么能轻言放弃？

你太看轻我了。

疼，细微。

接着。

渲染成一副重重的油彩，即使是在阳光下，也把光线全部吸收去了。一点点的过渡也没有，不停地压着，不停地让我窒息。

走遍了这里，还是没有一个可以说服自己放下武器的理由。

总是让自己不要注意那些细节，却总是忍不住一遍遍地看，从来都是这样，爱揭开伤疤看伤口有多深，有什么化脓。

痛苦也是一种享受，不是吗？

感谢你。

我还是遵守了曾经说的话：我说，我会感谢你的，如果你伤害了我。

八。

太多的谎言无法包住自己的火。

我站在时间的旁边失去了理智，用依然冰凉却潮湿的手指捏起细沙看它们如水流过，从骨头打造的船上下来，我赫然成为了你的敌人。我就是你的敌人，你攻击了我，我还以为你爱着我。

九。

我哭了。写到这里。没有音乐，身后有人。我还是哭了。

你否认过的，却被我看到千真万确的证实。你解释过的，却让我听到最坏的答案。他们是什么也不肯告诉我的，他们都还愿意保护我，宁可撕裂我的心脏，看我流血，逼我沉浸进去。你听他们说：“去。去。去。你去爱吧。我们是为你好。”我该听，还是不该听？

珍惜手中已有的。

我多想那样做。可是不能在接踵而来的欺骗里继续欺骗自己――我这样试过了，成功了，却痛的更厉害。

我始终不明白，你为什么欺骗我？为什么？为什么？

为什么要欺骗我？

如果仅仅是为了某种目的，你的目的已经达到了，你还不放手? 还不放开我？还要我的什么？我的骨头吗？我的血液吗？还是我的皮肤？或是我的长发？

拿走，一切都可以拿走，只要你放了我。

摊开手掌，就算是从来没拥有过什么，就算是一场关于玻璃鞋子的梦。灰姑娘依然回到厨房去烧火，而你，好好继续你的欺骗，好好地去狩你的猎。无论是好或不好的开始，还是好或不好的结局，走过了，也算是幸福。

