再后来，我在易水边上，遇到了一个叫做荆柯的男人。是一个傍晚，残阳如血，我坐在水边看对岸的灯火。荆柯抱着大把的荼草，从板木桥上摇摇晃晃地走来。正是荼草繁盛的时节，绒毛一样白色的细碎的荼花飘散在空气里，落满他的发梢和肩头，他脸上的表情分外平和。我斜斜地看着他，从对岸一直走到我的身后，坐到我的身边。然后我问他，你是谁。他说他的名字叫荆柯。就是一支荆棘，无根无叶。我说是个好名字，适合流浪，迁徙，分离。

