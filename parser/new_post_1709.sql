好。我爬到床上，睡在娘的里边。娘熄了灯。屋子里一下子变得漆黑，盈淡的月光一点点洒在窗的角落里，显得微不足道。从很小的时候起，就是我和娘相依为命。娘四处唱歌以维持生计，偶尔也绣些花拿了去换布。生活一直很平淡，在这个族群里像花开花落样悄无声息地延续。然后有一天庄周带着他的瘦马很莫名的出现在村落的易水边上，然后娘的脸上开始有了忧伤。庄周是个贫穷的书生，靠给人占卦赚些盘缠，因此总是喂不饱他的马。那匹马声音黯哑，颧骨高耸，一脸沧桑的样子，很有庄周的风范。我经常摸着它的皮毛看到暮年样陨落的年华。

庄周的占卜向来灵验。于是村落里的族人纷拥着请他去算卦。活计多的时候庄周也是两个村子来回着跑，常常会与娘于路上见着。虽说庄周平日里一副口若悬河的样子，倒也常常会说些让人心惊的话。一针见血，道尽前古后世。娘在提起庄周的时候总说，那个人的眼是锐气的很的。

我侧过身的时候听见娘轻微的呼吸起伏的声音。已经是入睡很久了。

翌日一早我起来，娘在井边洗她的褂裙，就是在唱冢歌的时候穿得那件麻色的衣裳，娘用黑线绣了精巧的边纹。阳光稀薄，雾气淡淡弥漫。我向娘道了安，俯下身去看那株三叶草。娘口气淡淡地说，图伊，你要记得好好地把这草养下去。我有些迷惑，问娘为什么。娘不语，只让我过去帮着她洗褂裙。其实娘穿褂裙的时候很好看，皮肤隐隐的映出麻色安静而孤诣的感觉。像个不食人间烟火的女子，唱尽哀怨凄凉。落了水的褂裙有些苍茫的颜色，边缝处的黑色针线更是显得妖冶。娘用手一遍遍地抹着前襟，有些心不在焉的样子。突然她开口问我，图伊，庄周走之前有说过什么吗。

没什么，娘。我沉默了一下，他只是给我算了一卦。他说，将来会有一个男人，来带我走。娘的眼泪突然急促地掉下来，落在水盆里溅起水沫。我不知所以。

后来的很长一段日子，娘变得沉默不语。常常会出神，绣花的时候愣愣地停下手来。那些花瓣如裂开的魂，破碎地洒在底色阴沉的布匹上。娘的脸上写满追忆，那是我无法触及的时光。也许是久远以前，也许是与庄周在路上的那些恍然的时刻。而我始终不明白的是，娘的眼泪。我在娘身边生活了十一年终而不见的清澈泪滴，在那个清晨微凉的风里，不可遏制的掉落下来。

