放学后，还是习惯性的经过音乐教室。

里面有琴声传来，却已不是《Waiting for your love》了。

演奏者，我想应该也不是晓佩了吧？

踏着撒满金光的路，突然发现我放学后已经不会乱晃了，而是直接回我现在的单人住所。

就是那种普通的单人公寓，一房一厅，自由又舒适。

唯一的麻烦是一切家务都要包下来。不过有洗衣机，三餐都在外面解决，所以也不算沉重。

但是就是乱，原因自然是因为没有人整理。象个狗窝。

于是我就叫它狗窝。

狗窝离学校不太远，大概20分钟的路程，正好和晓佩家是相反的方向。

也许是刻意的选择吧，我趴在毫无准备的情况下遇见她。

不过“换个地方换个心情”的这个方式似乎真的有效，觉得最近自己恢复了许多。

除了突然想起她的时候。

“对不起，可以请你帮个忙吗？”正当我准备找锁匙开门的时候，身后传来一个女生的声音。

“恩？什么事？”我回过头，看见一个大概十五、六岁，穿着超大件T-shirt做睡裙，小腿上套着泡泡袜，踩着大头拖鞋的女生，站在从楼上到这层的楼梯上。

“想请你帮我挪一下柜子，我一个人不行啊。”她边拍了拍身上的灰尘，应该是正在大扫除。

“恩，没问题。”我顺着楼梯往上走。

是和我的“狗窝 ”一模一样的结构的一套房子，正好楼下就是狗窝的位置。

虽然因为在整理，显得有些凌乱，但是可以看的出来里面的摆设还是很有女生味的。

帮她挪好了柜子，我起身告辞。

“现在可以了吧？那我先走咯。”

“恩，谢谢你哦！”

“不用。”我笑了笑。

“你就住楼下？”

“恩，是啊。”

“那可不可以经常找你玩？一个人住很闷啊~”

“可以啊~ 我也是一个人住。很高兴认识你~”

“我也是~~”

“那，我先下去咯？”

“等一下~~~”

“恩？”

“我忘记给你倒水了~~~|||”

“不用了不用了，有事再找我吧。Bye~”

“恩恩恩~ bye~~”

回到狗窝里，舒舒服服的洗了一个热水澡。

洗去了一天的困倦疲惫，也洗去了一点点对她的思念。

一天一点点，离忘记她应该不会太遥远吧？

