<br/>   后来更多的人见到了玛亚，那是开学后的第一节美术课。教室里吵闹的声音彼此追逐，印着敦煌飞天封面的教材被卷起用来打闹，各式各样的耳机在不同的耳朵上闪耀，我用手支着头端详墙上梵高的《向日葵》，然后玛亚走进教室，安静迅速蔓延。无一例外惊艳的眼光，粉笔划过光洁的玻璃黑板，明亮干净的字体，她的名字，玛亚。“我是新来的美术老师，玛亚。”她微笑的说，向日葵的色彩被渲染开来，仿佛又回到那个微凉的晚上，才后知后觉的发现原来那时的颜色是这样温暖而疯狂的橙黄。她开始讲课，最基本的线条和简单的几何体，粉笔灰交错的掉落，耳机陆续被取下，窃窃私语声渐渐停止，所有人都开始认真的听课。玛亚的魔力，就像贯穿天空的彩虹，在一片悠扬的蓝白色中让人不由自主的被吸引。
