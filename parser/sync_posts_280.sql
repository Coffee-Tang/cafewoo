<center><table><tr><td><br/><br/>', '操场边上堆着很多圆形的木头，后面有巨大的树，枝叶繁茂，罩出一片阴凉，我喜欢拿着蓝色雪碧和草莓味的可爱多到那里去。木头很干燥，坐在上面有轻软的气息，树上开满了香气四溢的梨花，洁白犹如透明的心情，我向后躺去，那种白的让人微笑的颜色瞬间注入眼睛，可爱多很香甜，伴着雪碧薄荷的清凉，端详着自己手腕上粉红色的十字架，满足的闭上了眼睛。
   那时我13岁，喜欢一个人安静的走路，观察周围人群的生活，成绩还算过得去，用中午的休息时间去圆木堆上喝水吃可爱多，然后在栀子花浓烈的香味中睡去，我觉得这样非常幸福。在自己生日的时候，买了一条黑色手链，有银色的小花，缀着粉红的十字架，看见了就毫不犹豫的买下。当时我没有过多的忧郁和痛苦，读清冽的宋词，看安徒生的童话，最爱的书是《哆啦A梦》和《基督山伯爵》，穿简单的T-SHIRT和牛仔裤，留着到长不短的头发，常常坐在自己的位置上诡异的发笑，对别人异样的眼光一笑了之。
   认识的人有很多，路上一个个的打招呼，他们无一例外的都叫着“小刀”，我就眯着眼睛笑，然后扬起左手，十字架摩擦着肌肤下坠，停止，小花将阳光反射，心情是路边法梧的青翠。
  放学之后穿过窄小的楼道去美术教室，教室门前有淡粉色的樱花，一棵孤零零的站在那里，层叠的光影随着花瓣飞落，偶尔有一两片会飘进教室，停留在几何体或者静物旁边，室内的采光设计并不好，只有尽头有一扇窗户，阳光穿过窗外的灌木，斜斜的流进来，仿佛有一点疲惫，却强打精神抚摩那些石膏像，使得它们苍白的眉眼里透出种暖暖的寂寞。我蹲在门边，安静的削着铅笔，银灰的尖细，微微反光。
   暮色四合，打开了所有的灯。铅笔在白纸上来回游走，描绘着石膏强硬的面孔，风摩擦过头发，盛夏时唯一的冰凉。胶擦不小心掉在了地上，当弯腰45度，手指正要触碰的时候，门被打开，黑色纤细的剪影落进我的眼眸，回到90度，抬头，日光灯的明暗突然变得敏感，一个高挑细瘦的长发女子站在门口，双眼微微往两边挑，左眼角下有一颗泪痣，嘴唇泛着粉红柔软的光，刺绣的衣服和LEE的细脚牛仔裤，有微小的惊诧和和善的笑意，“这么晚了，还一个人在这里画画？”如同地下河水暗涌，声音略略的沙哑然后清爽无比，我笑着，不准备回答，“我是新来的美术老师，玛亚。”她低头，过来看我的画，樱花的味道。
   第一次与玛亚的遇见，她就像那株樱花的化身，粉红的色泽，温暖而美好，21岁从美院毕业的女子，拒绝父母的帮助，只身来到这所重点学校工作。第一天傍晚在宿舍放好行李后，拿起美术室的钥匙走近樱花树下的教室，开门却看见了一个认真的学生在用力的画石膏像。
   那天玛亚给我的画指出很多毛病，我对她的印象，也从樱花飞舞的华美转到落地归根的朴实，最后她给我画了一张半身速写像，速写像的我眼神漠然而空洞，我没有问她为什么是这样，尽管很多朋友都说小刀的眼睛明亮或者暗淡。
   后来更多的人见到了玛亚，那是开学后的第一节美术课。教室里吵闹的声音彼此追逐，印着敦煌飞天封面的教材被卷起用来打闹，各式各样的耳机在不同的耳朵上闪耀，我用手支着头端详墙上梵高的《向日葵》，然后玛亚走进教室，安静迅速蔓延。无一例外惊艳的眼光，粉笔划过光洁的玻璃黑板，明亮干净的字体，她的名字，玛亚。“我是新来的美术老师，玛亚。”她微笑的说，向日葵的色彩被渲染开来，仿佛又回到那个微凉的晚上，才后知后觉的发现原来那时的颜色是这样温暖而疯狂的橙黄。她开始讲课，最基本的线条和简单的几何体，粉笔灰交错的掉落，耳机陆续被取下，窃窃私语声渐渐停止，所有人都开始认真的听课。玛亚的魔力，就像贯穿天空的彩虹，在一片悠扬的蓝白色中让人不由自主的被吸引。
   我开始在中午的时候放弃圆木堆，梨花及睡眠，而是带着可爱多和薄荷雪碧去美术教室跟玛亚学画，聊天。她说我的颜色太过沉重、酷烈，让我尝试清新一点的颜色，一个星期之后，我终于成功的调出了碧蓝色的雪碧瓶，色彩是清新的爽朗，仿佛可以看到汽水的流动。玛亚很高兴，眼睛明亮得好像夜空里闪耀的星，第一次叫出了“小刀”，锐利的名字瞬间柔软。我靠在门上，看她灿烂的笑容，仿佛一阵清香的风，夹杂着碎散的花瓣，飞扬。
   红陪我去逛街。外表上看他是眼睛吊吊的男生，头发支棱着，眉眼间写满了大刺刺的无所谓。其实红心地比任何人都要善良，每个月定期去一次敬老院，用零用钱捐助着一个山区女孩，收养无家可归的小狗小猫，喜欢照顾花花草草。这些好象不是红这种看起来酷到不行的男生做的，可是他却做得很快乐。红不爱讲话，因此也只是看我挑挑选选，陪我进出一家又一家商店，毫无怨言。阳光很明媚，街道两旁有繁茂的树，叶子大片的给予阴凉。我略略有些疲惫，其实只是想给玛亚挑个什么东西而已，感谢她的教导和友情般的鼓励。红依然沉默着走路，我侧过头去，看到班驳的光影在他坚硬的下巴上翻叠，勾勒出好看的线条，视线延伸出去，是一家白色的小店，装饰却异常华丽，给人一种七窍玲珑的感觉。拉着红走了进去，原来是卖水晶饰品，流光溢彩的扑入眼帘，终于找到了一块樱花样的紫水晶，五片带缺口的花瓣躺在掌心里，柔和温婉，轻盈透明。找老板一问，是根本无法承担的价格，遗憾地走出店外，心里涌动着不甘的情绪，无法言语。我们经过了中心广场，白色和平鸽海潮一般涌动，红买了喂食袋，撕开来倒在我手上，于是鸽子们朝这边走来，捉食，伴随轻微的疼。红站在旁边，微微地笑着，远处一个小孩子跌跌撞撞地跑进来，惊起大片鸽群，于是无数双翅膀掠过我们的头顶，洁白的颜色恍若梦中悠缓的浮云，影子流过眼睛，唤醒心灵。
  一个人呆在教室里做很厚的习题集，蓝色窗帘切开了外面浓郁的阳光，可操场上的喧闹仍然传了进来，在四壁碰撞，晃荡，是绵长而慵懒的午后时光。习题集的下面，有玛亚刚给的通知，是一个全国级别的绘画大赛，本来不想参加，可是看到奖金后改变了主意，即使是末等奖也有4位数字，我想到那条樱花紫水晶项链，隐约透明，宛如玛亚温和的笑颜。
  选了5楼上一间废弃的教室作画，拒绝玛亚的参观，只因画中人是她。却还是要不时下去询问她一些色彩之类的技巧，因为这次比赛没有年龄限制，所以想要获奖，非常不易。雨季无声无息的到来，每天放学后踏着细碎的雨滴走向5楼，空旷的教室里摆满了各种画具，颜料，还有水桶。黑板上还有残余的公式，粉笔以被抛弃的姿势出现在每个角落，一两滴雨水偶尔飞溅进来，在它们白色的身体上留下深色的水痕。我开始往画布上涂抹，心里有点颤抖，可是雨声时大时小的变换着，仿佛有安抚人的力量，心情渐渐平稳，像荡漾的池中的睡莲，有点散淡，却又是异样的安心。气氛迷幻中，颜色也渲染开来。
  开始上色的第二天，我在美术教室里见到了玛亚的男友泽翌，是沉稳安静的人，有光亮柔顺的黑发和细长的眼睛，笑起来的时候会出现和蔼的笑纹。玛亚带着甜蜜的表情，为他理好领带，扶平衣上的褶皱，然后看我出现，有些不好意思的红脸。我笑着看他们，那个人真的和玛亚很配，仿佛是专门为她才有的存在，细致，体贴入微。我又问了玛亚一些技巧方面的问题，她很认真的讲解，泽翌坐在对面的椅子上注视着她，眼神温柔而幸福。
红带来晚饭给我，是德克士的套餐。我们靠坐在教室的墙角打开塑料袋， 鸡翅很香，番茄酱被倒在可乐盖子上，暑条金黄，红眯起眼睛看未完成的画。窗外天色是沉默的蓝黑，风流窜，树影迷离，枝叶发出沙沙声响，潮湿的雨意闷出炎热，我没有对红说，我累的仿佛要失去右手。与色彩纠缠着，几乎厌烦得快要放弃，可是想到玛亚的语笑嫣然，想到她和泽翌一起的幸福快乐，我想无论如何也要继续。红侧过头，笑起来，他一定看懂了我的画。拿起了笔，至少有人能懂，我要完成它。
  雨，下了一整夜，樱花瓣湿润着碎掉，第二天我看到它们的尸体，散布在屋檐下，苍白而单薄的凄凉。
 终于在截止日期的前三天完成了画，却没有时间去邮局。正预备翘掉下午的自习时，泽翌出现在校门口，他似乎一眼就看穿了我的意图，嘴角勾起意味深长的弧度，细长的眼睛闪着和善的光，“我帮你去寄吧，不给玛亚看的。”声音低沉柔软，像香醇的酒。我不知觉的就把装画桶和报名表递给了他，他接过它们，手指线条修长而优美，好象有默契一般相视微笑，我道了声谢谢，疲惫和困倦终于能够落地。
  买了久违的薄荷雪碧和草莓可爱多去圆木堆，梨花掉光了，圆木经过雨水的洗礼，也不复温暖和干燥，可是玛亚却站在那里等我，樱花的香味淡淡蔓延，牵起笑容。那天玛亚教我撕开桉树的叶子，破碎的叶脉间涌出植物新鲜的清香，忘记了告诉她泽翌来过，只看见她香甜的笑，像小时侯在路上遇见同伴一起走向夕阳的美好感觉。
接下来的日子用来应付学校里的考试，放学依旧去美术室找玛亚，周末给红打电话。时间走进流年，日复一日安静的过，可是心底始终压抑着另一种期待，那副寄去的画，失了音讯，有隐约失望的惆怅。
  红拍拍我的肩膀，说没有关系，在我心里，你是第一名。我开始释然，雨季过去，我们还去作画的教室，不过大多是用喷罐涂鸦或者在黑板上乱画，玛亚有时会来看，常常是三个人把头探出窗户外，然后疯作一团。 
  当我快要把那副画完全遗忘的时候，玛亚拿来两张票，是一个大型画展，展出那次比赛的获奖作品。我忐忑不安的跟随她走进巨大的会场，却在中央展区看见了我的画，第一名，画名《惊鸿》。画上是夜晚美术教室前的樱花树，树下站着一个樱花般的女子，黑色长发，桃花样上挑的眼，精巧细小的泪痣，淡粉色嘴唇蕴着一抹笑意，风带起她的发，花瓣漫天飞舞，颜色是氤氲的粉红还有一点橙黄，交织在墨蓝的背景上，惊鸿照影，芳华绝代。玛亚的微笑之花全部绽放，有喜悦也有感动，我却感到体温在一点点降低，冰冷生硬的东西勒紧了心脏，力气几乎消失，小小的探照灯光束打在署名上，描摹着字体的形状，那个曾经感觉是这么温暖的男人，泽翌。身后响起脚步声，耳旁掠过玛亚惊喜的低呼，接着是衣衫摩擦的声音。两人紧紧相拥的影子漫过我的脚旁，像针，要刺进胸口。艰难的回过头，看见泽翌抱着玛亚，得意的笑着，笑得纯美如恶魔。我只想撕碎这个笑容，撕碎笑容里的卑劣与黑暗。可是我不能，因为他是玛亚的爱人，我一点也不想看，玛亚的眼泪。
  悄然离开了会场，茫然的坐在街心花园的长椅上。心像被摔碎的钢琴键盘，黑白色剥落，木质削开尖锐的角，血乎乎绞成一团。红在接到短信后，匆匆赶来。我看着他，喉咙被难过堵塞，只能大滴大滴的掉眼泪。他蹲下来，握住我的手腕，支撑的力量。终于哭出声来，有那么多不甘，最后也只能化做泪水，宣泄悲伤。我不能说，不能对玛亚说出真相，她是那样的爱着泽翌，爱他的一切，她曾经对我讲述她与他之间的的悲欢离合，故事里每一寸细枝末节都蕴藏着爱恋。眼睛很痛，眼泪却还是抑制不住的流出，红沉默着，手上却加重了力量，我只想永远睡去 ，梦里有很多朋友，有玛亚也有红，远离欺骗的幸福生活。

  那一年夏季似乎就在那一天停止，往后漫长的暑假里失去该有的清凉与妩媚，玛亚终于知道了这件事，在那棵樱花树下，她伸出了手，向她深爱的男人脸上扇去。抚过他脸庞的手，帮他打过领带的手，拥抱过他的手，被他亲吻过的手，终结一切的手。玛亚的眼泪我没有看到，只是一日打开门，看见她消瘦苍白的脸，送我一串指链，便转身离开。背影摇晃，如同断线的风筝。
  第二天得知她要去迈阿密，赶去机场送行，始终没有温度的距离，简单的话语，却让她流下了泪。就这样带着遗憾离去，我知道，她的伤口比我深很多。

玛亚生日那天，在第二年春光明媚的三月，寄去了我最爱的那条粉色十字架手链和樱花样的紫水晶，始终是温柔脆弱喜欢浪漫的双鱼座，仿佛看见她收到后展现甜美而清爽的笑容，散发着淡淡的樱花香味，一如我们初次遇见的夜晚。
时间淡化掉伤口，人总要学会开启新的生活。
我叫小刀，在那个未央的夏季里，我学会了一个人安静的走，走过四季，永不回头。

 
   

   









	
	
	
