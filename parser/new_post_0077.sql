<br>　　 身边有一张碟，盗版，没有名字，只知道是三、四十年代红歌星们原人原唱的经典歌曲集。大二的时候辗转找来，再没舍得丢开。带到电台去放，在沉静的春天的夜晚，梧桐树沙沙地响，靡靡之音回荡在那个不擅怀旧的南方都市的上空。
<br>　　
<br>　　 封套上是旧上海一张月份牌，双妹墨的广告，一个老牌的花露水厂家，习惯性用两个女孩子做形象代言，几十年如一日，图片上衣着时尚依附时代变化，人却绝不改变，以谓情比姐妹深。
<br>　　 这张三十年代的图片上，两个女子，一例的短袖，露出雪白的臂膀，清辉玉臂寒，旧上海的摩登，含蓄中隐隐的诱惑。手提花篮窈窕地立着，并没有画出全身像，但是当时的画技，几可叫人看到旗袍上的花纹，觉得袍角都是生风的。两个女孩子，微微笑着，一个端凝一些，另一个，则是佻达的，眼角斜斜地飞出去，一个眼风，一个姿势，叫你明白她，断不会是一个苍凉的手势，你可以想见她今生的传奇与风华。
<br>　　
<br>　　 爱一个人待在深夜里听这些歌。关上房门，音乐响起来，最原始粗糙的录音棚的效果，但是不妨碍歌星们的好声音。音乐转起来，会觉得整个屋子也转起来，时间和空间在那一刹那像一个刹车，忽然失了灵，倒转起来，整个时代是流动的，恍惚间，一个过错，人便身在四十年代，《长恨歌》里，属于王绮瑶的40年代。
<br>　　
<br>　　 第一首歌便是《夜上海》，跳跃的音符，鼓点俏皮地打上来，舞榭歌台，红的灯绿的酒，流转的光波，就这么笑盈盈地迎将上来，歌唱着这个不夜城啊。周璇的声音尖而细，是唱针在唱片上微微的划痕，过去了，不留下什么。曾经一直放她的一张碟：《青春之歌》，从头到尾，根本不去注意在唱什么。但是她就这么切切地问起，何日君再来啊。再怎样的铁骨铮铮，也经不起这样的软语生香罢？
<br>　　 只是究竟还是呆的，总缺乏那么一点感情在内。像《马路天使》里的小红，和师傅赌起气来，在茶楼卖唱，偏过头去，持是技艺精练，满不在乎地，嘟着嘴，编起辫子来，拍子来了，就这么随随便便声音一亮：“春季到来绿满窗，大姑娘窗下绣鸳鸯”……但或许那才是永远的周璇，天真不解风情的，不经意间却备极诱惑。就这么端端地，咿呀地唱起来：“小妹妹似线郎似针呀，哎呀穿在一起不离分呀”。绝不是后来和舒适一起演出清宫戏里珍妃的模样。所以我拒绝看所有她成名后的电影，曾经试过在川大文化路生生错过那张群星云集的《红楼梦》。想想觉得像《圆舞》里的毛病，只爱她春花初绽的少女时代。隐隐觉得周璇像刘若英唱过的《很爱很爱你》里面的小女生，充满着希望，相信爱情，没有怨怼的。“便是在最最彷徨的时候，她也不过是问，何日君再来，始终是阳光的。”当然世界不是这样的，所以她的那位君终于没有再回来。
<br>　　
<br>　　 白光给人别样的感受，低沉的女中音，慵懒而充满感情的，充溢着霓虹灯的泛华与美酒的沉迷。看尽繁华，一点点的颓丧，并不在乎别人怎么看，她不过管自寂寞着喧嚣着，庄重着轻佻着。
<br>　　 《玫瑰的故事》里，亦舒写玫瑰失恋后，跑到英国去读书，一径地过起中规中矩安分淡然的学生日子，把往事埋在心里，但是某日在留学生楼下听到有人放老歌：“如果没有你，日子怎么过？”，一时呆住.这首歌是有那样情致的，是那种豁达的态度，不是不知道应该离开，但终于要清醒地沦陷，所谓的“一代妖姬”，终于也要唱“我等着你回来，我等着你回来”，缠绵有致，是小女人发痴耍赖的格调。写到这里，想到《万象》里有文章写到她和日本军官山家亨，笑，想来是李碧华借了这个名字。
<br>　　 她的另一个性格侧面表现在《假惺惺》里，把世事看得如此洞明，“红着脸，跳着心，你的灵魂早已经”，末了挑挑的一声“何必呢”？直如一只小虫子爬到人心里去，抓得痒痒的，再跳不出那声音的魔障。这首歌90年代初叶玉卿翻唱过，歌里隐约还有白光的风骨，但是飞图拍的MTV烂无可烂，也没什么好说起。
<br>　　 晚年的白光隐居在马来西亚，不肯轻易出来见人，像白先勇笔下的金大班。觉得这样蛮好，白茫茫一片真干净。
<br>　　
<br>　　 
<br>　　 
<br>　　 看到过40年代中李香兰在上海和张爱玲的合影，玲珑娇小，张和她一站一坐，仍然显得骨骼清奇，究竟是高大了。她的歌声也是那样，小鸟一样，飞旋的。《夜来香》总算不错，《恨不相逢未嫁时》原来也是她的原唱，惆怅旧欢，恁是深情。
<br>　　 其实李香兰的歌远没有她的身世令人唏嘘，浮华使人忘却了种族和羞辱，但也不是在一个歌女身上找来泄吐。很庆幸对于数十年后的我们，李香兰只是一个歌者而非其他。
<br>　　 葛兰这个名字未有听说过，但是那首《说不出的快活》真是叫人快活，欢快的恰恰的调子，不自主的脚想跟着打拍子，还要一壁晃着脑袋。旧年伊能静翻唱过，收在《百乐小艳红之快活歌》里，感觉也很不错，但是要怀旧，究竟还是原唱地道。
<br>　　 “我得不到你的爱情，像冬夜里得不到光明”，很幽雅的jazz的曲调。会觉得叹惋，真的不知道这样的词句是怎么想出来的，最质朴的话，最真挚的告白，我更惊叹的是歌中女子的勇气。
<br>　　
<br>　　 私心里最喜欢的，其实是吴莺音的《我有一段情》。
<br>　　 要听过这首歌才知道什么叫做沉吟。――青青子衿，悠悠我心。旧式女子的情感总是含蓄，不过而今这样的时代，反更见出含蓄的美来。
<br>　　 老早就听过这首歌，爱极开头两句，还记得17岁小女生在文章里写：“像老歌里唱的那样，我有一段情呵，说给谁来听，而我，选择了说给纸和笔去听。”回头看过去，根本不敢去面对当年那张纯情如斯的脸。
<br>　　
