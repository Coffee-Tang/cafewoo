麦田里的绝望者，岩井俊二 
1963年岩井俊二出生，在他出生前3年，28岁的大岛渚拍出了《残酷青春物语》，表达他作为战后一代，在经过学运，建设过幻灭过后，直面社会的所见及反应；1996年，49岁的北野武推出半自传青春片《坏孩子的天空》，此片沉稳绵长的叙事，感情深挚的关注是他对青春的一次遥遥回顾，也使他下一步义无返顾的进入《花火》的成人世界。而同在这年，33岁的岩井俊二正忙于《梦旅人》、《燕尾蝶》的拍摄，〈燕尾蝶〉的庞大庞杂凌乱凌厉为他赢得影迷巨大拥戴，但岩井俊二真正令人动容的是现在，2002，〈莉莉周的一切〉，依然是少年成长中的焦灼，还是一群死于青春的灵魂，岩井俊二褪去〈燕尾蝶〉的急促，在39岁时，以自信的语气，从容讲述又一个残酷青春。
　　但上面那一段都是瞎扯，不过是翻书搜索出来的资料堆积。
　　在深夜2点看完电影，我先发了会儿呆，觉得这不是〈情书〉和〈四月物语〉时的岩井俊二，依然有他招牌式的美仑美奂画面：多次出现的青翠麦田随风摆动，低头沉浸音乐的白衣少年，或者是脑海中突然出现的红色天空，群鸟在飞，美到令人惊怖，美到极至，就是绝望。——可〈莉莉周的一切〉不仅是这些。
　　也许多的是生命的厚重。在岩井俊二天衣无缝的时间之流中，所有青春尖锐的痛苦、飞溅的碎片有了厚重的质感，而不再是《燕尾蝶》的庞杂，《情书》时的空灵。
　　依然是交流的困难，所有人站在同样的麦田，在同一块BBS听着同一个歌声，莉莉周空虚温暖的声音是他们现实之外的宗教、休息、空气与清水，他们用歌声分担痛苦，而歌声之外，人们相互毁灭。——你可以把这看做是对日本这个孤岛国家的一次寓言，无论是逼仄的本土空间，或者是国际空间。
　　如果说北野武〈坏孩子的天空〉是一首李宗盛与林夕合作的励志歌曲，经惊怖颓废仍能全力以赴心中的梦，〈莉莉周的一切〉则是林夕与刘以达合作的电子乐，温暖旋律之外，歌词是极端的漠然与绝望。
　　〈坏孩子的天空〉结尾处，两个辉煌过失败过的孩子回到起点：“我们完了吗？”“傻瓜，还没开始呢！”
　　〈莉莉周的一切〉结束时，雄一在空荡的教室里听久野弹琴。他们都曾被命运屈辱，雄一杀了现实和网络中都曾是他最好朋友的星野，而久野被强奸后，剃了光头穿过人群坐回教室，他们都付出生命的一部分来抵抗屈辱奋力成长，这生长如此惨烈，以血以死为代价，没有赢家。
　　无论怎样，他们活了下来，15岁，还只是中学，一如〈大逃杀〉的结尾，人生刚刚开始。
　　在报纸上看，日本去年自杀3万人，因为失业、负债的就有6845人，大多为中年人，男性。于是深作欣二拍〈大逃杀〉，所以川端康成笔下美丽端娴的日本，到了岩井俊二这里已成凄厉。
　　少年的绝望折射成人的焦虑，青春的残酷反映成人的竞争惨烈，只是已经没有大岛渚的理想主义的关照，在〈残酷青春物语〉中出现的“责任与自由并重、梦想与现实共建”的词语再不会出现，比较他们的经历：大岛渚在拍电影前做过学运领袖，因为反对神化天皇而几乎找不到工作，岩井俊二在做电影导演之前，则从事MTV、广告的拍摄和电视节目导演工作——所以，尽管都是残酷青春，岩井俊二的光影里，少了一些什么东西，是什么，你告诉我。
而比较岩井俊二与北野武，我时常觉得他们是日本文化里开出来的双生花，一走极端优美，一走极端暴力，但他们镜下同样出现对死亡近于迷恋的执著。
　　日本和中国一样，没有终极意义上的宗教，天皇是他们的世俗宗教，而在二战后，在一次次天皇光环的破坏后，他们对宗教的需要回到传统文化中对死亡的迷恋——回不去时，回到终点——他们选择死亡作为再次的开始、罪恶的洗净。
　　“生在死之后出现，花朵生长在溃烂的伤口上。对抗中的事物没有经历缓和的过程，直接就是汇合，”——余华用来写川端康成和卡夫卡的文字，也可以用来比喻岩井俊二与北野武的相遇之地。
　　只是，当莉莉周的音乐响起在同一片麦田，在青葱的四月豆蔻华年，我想始终存在一种交流、流传的可能，从《残酷青春物语》到《坏孩子的天空》、到《莉莉周的一切》，从大岛渚到北野武到岩井俊二，那也许是希望，是每一代人经历、思想如何不同却都会有的希望。
　　莉莉周的音乐很美，一望无际的麦田很美，在看过雄一把刀捅进星野身体，该刹那身体有部分自动尖叫，在这一切过去后的寂静里，我在想，你是那个在麦田边缘徒劳巡视的捕手、是尖声叫喊奔跑的孩子、还是麦田中间那部不动声色捕捉画面的摄象机？
　　而莉莉周的音乐，如此隔绝，如此亲切，如此遥远，如此近。
 








	
	
	
