  到《四月物语》出来已是惨不忍睹,那样一个极度风格化的导演居然回到老路,开始搔首弄姿,大肆煽情.....没等结束,我就抽身去翻杂志了,实在是不忍心看.多年前,有个人跟我说过,好的电影就是不煽情的电影,这话对任何艺术门类都适用,煽情是情绪,是感慨,太多人把情绪误当了情感.然而说这话的人我知道他考入电影学院的作业是一个煽情讨巧的东西,这又说明了一点,煽情始终是打动掏钱买票的人的最为有效的手段,你想不煽情讲点骨子里的东西,那就先煽情获得发言权.  
  
  《燕尾蝶》属于这样一种作品:最大程度的消耗创作者自身资源的作品,整部片子都在靠一股激情支撑,这激情想必蕴蓄已久而且无法稀释.属于青春作品.然而我们不能总是青春冲动,所有艺术都要回归到人本身,都要老老实实讲点人和人的事,这是大师和天才的区别.  
  
  同属日本影人的黑泽和北野,还有今村昌平,大岛渚,看他们的电影是一次真正的心灵之旅,让我们知道电影还可以用来思考.已经厌烦了这样的所谓的"影评":  
  
  "我看了........我被.......感动了,""是啊是啊,我也是........,我也被.....感动了."说的其实跟电影一点关系没有,翻来覆去让我知道原来大家生活的都很苍白,无非是找个机会让自己感动一把,更为不可救药地是感动地一塌糊涂,不知所云.看电影如同谈恋爱,还是两人并肩而行,谈谈话,聊聊天的好.时刻带着纸巾进影院,时刻准备感动一把的人不是在意淫电影就是在意淫自己.  
  
