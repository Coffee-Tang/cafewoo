<p>傍晚，在国贸边上的麦当劳要了杯橙汁。站在天桥上低头看下面拥挤的车流。初冬的冷风，飕飕地钻进脖子里，鼻尖大概也冻得通红。一个老人伸了手过来，掏出袋里的零钱给他。这样的情况遇得多了，却做不到视若无睹。是真是假分不清楚，在这样冻冻的冷风里让瘦小的老人失望终是做不到的。谁不想在温暖的灯光下，一家人笑嘻嘻的围着用餐阿？忽然想起，哪天在中山街上，有个拉二胡的老人。闭着眼睛，旁若无人地拉着二泉映月。曲子清亮。动人。二胡上吊着个放钱的碗，却看得人落泪。在熙熙攘攘的人流中，没人会去注意寂寞的角落里汩汩流出的音乐。而在那曲子的背后，仅仅只是生存的辛酸？</p>

<p>而自己，又能有什么样的理由来抱怨生活的苍白与空洞？爱的真诚和虚伪？</p>

<p>儿时斑斓的梦想。什么时候就这么长着翅膀飞走了。风中的诺言，是不留痕迹的。在我13岁的时候，曾极其欢喜地想象着10年后会长成什么样子。盼望着长大可以不用听爸妈的唠叨，不用做很多的作业。可以自由的穿长长的裙子，扎粗粗的麻花辫子。简单的心灵里，丁点的感动就能够满足。一路上微笑地走了过来，笑容却慢慢的停滞。日子，在20岁以前随着自己的心愿一页页翻了过去。照片中的自己毫无顾岂的长大了。随心所欲成了任性的代名词。已无法再肆无忌惮。</p>

<p>哪天有网友说，你的文字读起来酸酸的。人却不尽然。是的，阳光下我的笑容总是灿烂的。暗夜里的悲伤唯有化成文字说给自己听。生活里没有永远的快乐，就如阳光下见得着的都是灰尘。也许是因为有的人健忘，便有话说糊涂是福。而有的人敏锐而直白，容易捕捉每一缕空气，无论伤感抑或欢乐。</p>

<p>将喝完橙汁的纸杯丢进垃圾筒里。冰冰的手握在一起取暖。随后伸开手心，看上面细密的掌纹。好象就在去年吧，有天跟妹妹一起，也是在这天桥。一位老人硬拉着我要看相。听他乱七八糟地胡说一气。笑嘻嘻地扔下钱就走。只记得老人说，掌纹是伴随人的一生而变化的，极其准确。他说，孩子你面善，会有好报。从不相信命运之说，却也觉得自己没有理由不过得好。那时还没谈过恋爱，也许经历空白便成天傻乐着。</p>

<p>包里的电话响了。是宣。她说要来找我。霓虹灯开始璀璨，这城市的夜晚远远望去是一溜的灯河。宣来了。头发长了好多，下巴也尖了起来。疲倦地垂着头，脖子细细的，宽宽的外套像个大口袋罩住了她。她磨擦着双手，一个劲地说有点冷有点冷。宣说他回来了。宣是我在QQ上认识的一个网友。莫名其妙地投缘。知道她大学的男友出国，为了这样的理由而分开。日子在挣扎中恢复了平静，现在人却回来了。我看到宣的头发在路灯下泛着幽蓝的光。我只好说着一些人们通常说的安慰的话。我感到自己的话很苍白，而且言不由衷。最后，急了，我说蹦迪去。
<br/>说完，才发现自己穿着红毛衣窄窄的筒裙短靴。走路都得细细碎碎的，如何去D厅。而宣早就笑了起来。宣说，玲你总是有办法找乐子。
<br/>第一次听到有人这样说我。宣是那种明白我却不太熟悉我的人。一阵风过来，便有滴泪落下。</p>

<p>和宣，就这么站在天桥上说话。尽管，今夜的天空没有闪烁的星子。心，却变得满满的。人，是需要自己去改变的。
<br/>回来时，在路边的花店买支玫瑰。放入齐豫的CD。听熟悉的那首歌，遥望那扇窗，想象着那人的灯还是亮着的。以为这样，便可以回到那已远走的世界。至少至少，闭上眼睛，感觉自己真的在缤纷之中。
</p>
