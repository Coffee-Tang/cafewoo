<br/>也就是说，她们俩都曾单独呆在宿舍。而且没有上人进来。
<br/>嫌疑似乎就集中在她们两个人身上。尽管她们谁也不承认自己拿了钱。
<br/>最后事情无果而终。
<br/>李子沐向大家做了检讨并赔偿300元现金，因为收钱的事毕竟是她负责。
<br/>方果果了向大家道了歉并赔偿200元，因为钱毕竟是经她手后才丢的。
<br/>剩下的钱只好由大家积积德，平摊了。也算是帮个忙，毕竟这么一大笔钱让她们俩负担实在是太重了。
