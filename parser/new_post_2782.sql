<br/>方果果先哭了起来。
<br/>紧接下来的是在全班女生监督下的大排查。毕竟丢了这么多钱，谁也担不起。因此李子沐和方果果必须详细报告她们这两天的行踪。幸而407宿舍只住了她们两个人。
