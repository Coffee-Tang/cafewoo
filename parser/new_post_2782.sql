<br/>她们互相看了一眼，彼此都能看进对方心里一些。
<br/>所以你也知道了，拿钱的原来是林小翘。她无意中发现了藏在窗台的钥匙，便好奇地开了进去。但真正促使她拿那些钱的原因还是苏希柜子上那些昂贵的化妆品。她也多么想要一套啊！为什么自己的脸上老是斑斑点点的呢？为什么自己不能像苏希一样漂亮呢？于是她轻而易举不留痕迹地取走了那笔钱。
<br/>可是她一分都没有花它们。林小翘第一次体会到偷别人东西的滋味，她怕她一买贵重的东西就会被人怀疑“巨额资产来源不明”，同时看到方果果和李子沐因为她而被人猜疑彼此冷漠，心里万分折磨。“偷！”她一想到这个字眼便一阵发抖……</p>

<p>几天后那笔钱原封不动地回到了李子沐的桌子上。李子沐不动声色地告诉大家钱在桌缝里找到了，并且一一归还给大家。李子沐和方果果对于同学的怀疑只是笑而答。
<br/>因为失而复得的钱，方果果也不再尿床了。
<br/>林小翘也回复了往日的活泼。
<br/>而苏希还是一如既往地淡定。
<br/>只是在某些时候，有一道掠过眼角的目光划痕在她们之间悄悄地传递。那是她们共守的小秘密。</p>

