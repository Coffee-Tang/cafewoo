<br/>第二张纸条：
<br/>我最近总是尿床，大概是压力的原因吧。很丢人吧。
<br/>第三张纸条：
<br/>我一生下来就是双性人。直到二十岁的时候才做了变性手术。
<br/>第四张纸条：
<br/>钱是我拿的。</p>

<p>亲爱的读者，你能猜出这些字条分别属于谁吗？
<br/>第三张字条无疑最好猜，是属于苏希的。这也解释了她转学的原因，她身高的原因，而她淡然的性格或许也是双重性别所经历比寻常人多一些而造成的。至于为什么她到二十岁才做变性手术，便不得而知了。
