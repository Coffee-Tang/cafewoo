分宿舍的时候，在林小翘的强烈要求下，苏希住进了女生寝室408的一张空床。林小翘天生就是个好奇的丫头，见到有新人来就喜欢和她做朋友。对此她的另两个室友也表示了简单的欢迎。
收拾东西时林小翘忍不住偷偷观察着苏希。她显然很有钱，东西多而且精致，化妆品清一色全是Clinique，想想就令人咋舌。她忍不住问，苏希，你是哪里人啊？
杭州人。
你以前在杭州读书吗？ 
她摇摇头，在上海。上海大学。
天哪！那你为什么要转到我们这个小地方来呢？ 
苏希放下手中的东西迳直过来捏住小翘的鼻子，轻轻柔柔地说，我乐意，怎么样？ 
这时隔壁宿舍的方果果走进来，眨着眼睛问，苏希，你要报名普通话考试吗？我们班都报了。
要的，苏希赶紧说，怎么报名？
交80块钱给我就行。
咦，收钱不是李子沐的事吗？ 
子沐请假回家了，我只好无替她收着。方果果抱怨道，我还没管过那么钱呢，搞不好让我全给私吞了。
大家一起笑起来。

两天后。
我放在你桌子上的。方果果说。
没有了。李子沐紧张地说。
再找找吧，也许压在什么书下面？
没有，我今天早上全翻过了。
李子沐脸色苍白。
全班21个女生，除了2个没有住校的还没交，和李子沐亲自收的11个人的钱外，一共丢了720元。
方果果先哭了起来。
紧接下来的是在全班女生监督下的大排查。毕竟丢了这么多钱，谁也担不起。因此李子沐和方果果必须详细报告她们这两天的行踪。幸而407宿舍只住了她们两个人。
排查的结果是两天的周末里方果果一直呆在宿舍，即使下楼打饭也是锁了门才出去的。而李子沐今天上午才从家里回来，在上课前到宿舍取了课本，此时方果果已经去教室了。
也就是说，她们俩都曾单独呆在宿舍。而且没有上人进来。
嫌疑似乎就集中在她们两个人身上。尽管她们谁也不承认自己拿了钱。
最后事情无果而终。
李子沐向大家做了检讨并赔偿300元现金，因为收钱的事毕竟是她负责。
方果果了向大家道了歉并赔偿200元，因为钱毕竟是经她手后才丢的。
剩下的钱只好由大家积积德，平摊了。也算是帮个忙，毕竟这么一大笔钱让她们俩负担实在是太重了。
但大家明里不说，暗里还是有些唏嘘。这事实在有些蹊跷：谁能保证不是她们两个中的一个私吞了这笔钱呢？或者也可能是她们俩合伙拿了？谁知道呢。但普通话考试在即，谁还有那个心思去追究。好在平摊的钱并不多，想想也就算了。

普通话考完大家决定好好出去放松放松，初定是到梅洲岛玩。同学们都很兴奋，尤其是外地的学生，来莆田读书两年了，岂能连它最著名的风景区都没去过？于是梅洲岛两日游就被定在了这个周末。
方果果和李子沐因为心情都不太好，发生了那样的事她们都尽是避免和大家在一起。所以便没有去。
苏希因为对海水过敏，也留了下来。同宿舍的林小翘因为周六上午有家教，也没有去。
苏希这段时间里慢慢融入了这个班级，大家彼此间都渐渐熟悉起来。她发现原本活泼开朗的林小翘似乎被什么不开心的事困扰着。要么是特别安静，要么突然特别疯。苏希几想关心关心她，小翘都转开了话题。
苏希单纯地猜测着，小翘或许是失恋了吧。苏希突然发现自己对女生的心理其实并不特别了解。
挺奇怪的。

星期六早上，同学们都兴冲冲地背着行囊出门了。林小翘去做家教，留下苏希一个人在宿舍。她倒是耐得住寂寞，独自一人坐在床上练练瑜珈看看小说，一晃就是大半天。方果果和李子沐虽然在同一宿舍，但从那件事后她们之间也有了罅隙，变得没有太多话可讲。两个人各自在桌前不知做着什么。苏希有时来串门，也感到空气中的压抑。
下午时分林小翘拖着一身疲惫回来了，洗头洗澡后来找方果果借电风吹。果果说，电风吹烧坏了。
小翘“哦”了一声，见苏希也在，便坐在床上夸张地伸了个懒腰，累啊！洗个澡真是舒服！
苏希忽然灵机一动，说，他们得明天才回来，这么无聊，不如我们来玩个游戏吧。
李子沐真正闲得无聊，见她这么说不由回头望了一眼。
林小翘问，什么游戏？
来来来，先坐下来再说。果果你也来嘛。
于是大家按照苏希的要求在地上围坐成圈，中间放着一个盘子，盘中是一把瓷匙。
游戏名字叫“真心话大冒险”。苏希宣布，就由我开始吧，我转这把汤匙，停下来时匙柄对着谁，谁就要受到“惩罚”。受罚者可选择“真心话”还是“大冒险”。选前者的话，必须回答我提出的任何问题，选后者的话就得做我要求的任何动作。
大家面面相觑，都觉得这游戏实在有些刺激。大家都同意了。

由苏希开始。她“哐当”一转，一圈又一圈，最后匙柄慢慢地停下来，指向方果果的位置。她有些紧张地望向苏希。
真心话还是大冒险？
……真心话吧。
嗯……你最喜欢的食物是什么？
方果果松了一口气答，欧乐美的鲔鱼披萨。我还以为你会问我初吻发生在什么时候呢。
李子沐突然笑了一下。
方果果装作没看到，开始转汤匙。
……
游戏到后来发展得有些不可收拾。除了苏希学了三声狗叫外，没有人选择大冒险。方果果和李子沐仿佛较着什么劲似的。而林小翘显然在避重就轻。大家互相问的问题却越来越隐私。
苏希终于忍不住说，停一下。
大家都看着她。
既然大家都想知道别人的秘密，不如我们换个游戏来玩。苏希边说边撕下一张白纸，再撕成四等份。
每个人在上面写一个自己的秘密，写完就对折两次放在这里，我们轮流看。纸是一样的，没有人知道哪张是谁的。怎么样？
苏希笑咪咪地问。
沉默。
林小翘突然说，苏希，我怎么觉得你像个女巫？
苏希耸耸肩说，这样对大家都有好处。

苏希是最先写完的。她从宿舍里拿出一个废弃的蜂蜜罐，把每个人写好的纸条都装进去。有一瞬间大家犹豫了一下，但最后还是放了进去了。
苏希把瓶子盖上，像个调酒师似的把它上下摇晃了一会儿，最后往地上一扣。她说，这是我们四人的秘密，要发誓不能说出去哦。
林小翘首先举起手来，我发誓。
方果果和李子沐也马上说，我发誓。
苏希缓缓地掀开瓶子。四人各拿起一张纸条，平静地看完，然后顺时针交换，直到每个人都看过四张纸条为止。

第一张纸条：
有一把407的备用钥匙，我把它放在窗台的夹缝里。是我怕忘带钥匙偷偷放的。我很抱歉但我一直不敢说出来。
第二张纸条：
我最近总是尿床，大概是压力的原因吧。很丢人吧。
第三张纸条：
我一生下来就是双性人。直到二十岁的时候才做了变性手术。
第四张纸条：
钱是我拿的。

亲爱的读者，你能猜出这些字条分别属于谁吗？
第三张字条无疑最好猜，是属于苏希的。这也解释了她转学的原因，她身高的原因，而她淡然的性格或许也是双重性别所经历比寻常人多一些而造成的。至于为什么她到二十岁才做变性手术，便不得而知了。
方果果和李子沐彼此都应该知道对方的秘密吧。钥匙如果不是她放的，那无疑是李子沐了。这显然无意中方便了偷钱的那个人。而李子沐每天早晨将醒未醒时闻到的那股尿骚味似乎也不难解释了。
她们互相看了一眼，彼此都能看进对方心里一些。
所以你也知道了，拿钱的原来是林小翘。她无意中发现了藏在窗台的钥匙，便好奇地开了进去。但真正促使她拿那些钱的原因还是苏希柜子上那些昂贵的化妆品。她也多么想要一套啊！为什么自己的脸上老是斑斑点点的呢？为什么自己不能像苏希一样漂亮呢？于是她轻而易举不留痕迹地取走了那笔钱。
可是她一分都没有花它们。林小翘第一次体会到偷别人东西的滋味，她怕她一买贵重的东西就会被人怀疑“巨额资产来源不明”，同时看到方果果和李子沐因为她而被人猜疑彼此冷漠，心里万分折磨。“偷！”她一想到这个字眼便一阵发抖……

几天后那笔钱原封不动地回到了李子沐的桌子上。李子沐不动声色地告诉大家钱在桌缝里找到了，并且一一归还给大家。李子沐和方果果对于同学的怀疑只是笑而答。
因为失而复得的钱，方果果也不再尿床了。
林小翘也回复了往日的活泼。
而苏希还是一如既往地淡定。
只是在某些时候，有一道掠过眼角的目光划痕在她们之间悄悄地传递。那是她们共守的小秘密。









	
	
	
