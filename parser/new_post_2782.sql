<br/>杭州人。
<br/>你以前在杭州读书吗？ 
<br/>她摇摇头，在上海。上海大学。
<br/>天哪！那你为什么要转到我们这个小地方来呢？ 
<br/>苏希放下手中的东西迳直过来捏住小翘的鼻子，轻轻柔柔地说，我乐意，怎么样？ 
<br/>这时隔壁宿舍的方果果走进来，眨着眼睛问，苏希，你要报名普通话考试吗？我们班都报了。
<br/>要的，苏希赶紧说，怎么报名？
<br/>交80块钱给我就行。
<br/>咦，收钱不是李子沐的事吗？ 
<br/>子沐请假回家了，我只好无替她收着。方果果抱怨道，我还没管过那么钱呢，搞不好让我全给私吞了。
<br/>大家一起笑起来。</p>

<p>两天后。
<br/>我放在你桌子上的。方果果说。
<br/>没有了。李子沐紧张地说。
<br/>再找找吧，也许压在什么书下面？
<br/>没有，我今天早上全翻过了。
