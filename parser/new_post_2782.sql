<br/>沉默。
<br/>林小翘突然说，苏希，我怎么觉得你像个女巫？
<br/>苏希耸耸肩说，这样对大家都有好处。</p>

<p>苏希是最先写完的。她从宿舍里拿出一个废弃的蜂蜜罐，把每个人写好的纸条都装进去。有一瞬间大家犹豫了一下，但最后还是放了进去了。
<br/>苏希把瓶子盖上，像个调酒师似的把它上下摇晃了一会儿，最后往地上一扣。她说，这是我们四人的秘密，要发誓不能说出去哦。
