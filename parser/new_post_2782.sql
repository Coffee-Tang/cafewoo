<br/>新来的美女果然没有让大家失望。她是属于古典那一型的，但个子颇高，目测过去大概有一米六八以上。她的自我介绍却只有简单的一句：
<br/>“大家好，我叫苏希。”
<br/>据后来的传言说，那天英文系021班的同学不得不清理掉课桌上的口水才开始上课；并且当美女苏希选择了男生阿处旁边的座位坐下后，他紧张地把书本不断拿出塞进多达五次。</p>

<p>分宿舍的时候，在林小翘的强烈要求下，苏希住进了女生寝室408的一张空床。林小翘天生就是个好奇的丫头，见到有新人来就喜欢和她做朋友。对此她的另两个室友也表示了简单的欢迎。
