<p>普通话考完大家决定好好出去放松放松，初定是到梅洲岛玩。同学们都很兴奋，尤其是外地的学生，来莆田读书两年了，岂能连它最著名的风景区都没去过？于是梅洲岛两日游就被定在了这个周末。
<br/>方果果和李子沐因为心情都不太好，发生了那样的事她们都尽是避免和大家在一起。所以便没有去。
<br/>苏希因为对海水过敏，也留了下来。同宿舍的林小翘因为周六上午有家教，也没有去。
<br/>苏希这段时间里慢慢融入了这个班级，大家彼此间都渐渐熟悉起来。她发现原本活泼开朗的林小翘似乎被什么不开心的事困扰着。要么是特别安静，要么突然特别疯。苏希几想关心关心她，小翘都转开了话题。
<br/>苏希单纯地猜测着，小翘或许是失恋了吧。苏希突然发现自己对女生的心理其实并不特别了解。
<br/>挺奇怪的。</p>

<p>星期六早上，同学们都兴冲冲地背着行囊出门了。林小翘去做家教，留下苏希一个人在宿舍。她倒是耐得住寂寞，独自一人坐在床上练练瑜珈看看小说，一晃就是大半天。方果果和李子沐虽然在同一宿舍，但从那件事后她们之间也有了罅隙，变得没有太多话可讲。两个人各自在桌前不知做着什么。苏希有时来串门，也感到空气中的压抑。
<br/>下午时分林小翘拖着一身疲惫回来了，洗头洗澡后来找方果果借电风吹。果果说，电风吹烧坏了。
<br/>小翘“哦”了一声，见苏希也在，便坐在床上夸张地伸了个懒腰，累啊！洗个澡真是舒服！
<br/>苏希忽然灵机一动，说，他们得明天才回来，这么无聊，不如我们来玩个游戏吧。
<br/>李子沐真正闲得无聊，见她这么说不由回头望了一眼。
<br/>林小翘问，什么游戏？
<br/>来来来，先坐下来再说。果果你也来嘛。
<br/>于是大家按照苏希的要求在地上围坐成圈，中间放着一个盘子，盘中是一把瓷匙。
<br/>游戏名字叫“真心话大冒险”。苏希宣布，就由我开始吧，我转这把汤匙，停下来时匙柄对着谁，谁就要受到“惩罚”。受罚者可选择“真心话”还是“大冒险”。选前者的话，必须回答我提出的任何问题，选后者的话就得做我要求的任何动作。
