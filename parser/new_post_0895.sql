<br>����刚来上海的时候，有人听到你是来自哈尔滨的，时常会问到：“你们恨日本吗？” 
<br>����那时，我都会以东北人的身份告诉他：“东北人都恨日本，他们侵略过我们”。但是说心里话，让我说具体我恨他什么？我根本说不出来，因为在那时的我的印象里，我是没有任何情感的累积的，只是出于道义和理所应当的层面上才会说这些。 
<br>
<br>����直到那一天。 
<br>
<br>����那时2000年冬天的一个晚上。我和几个好朋友在复旦旁边的一个漫画pub（乐静宜开的漫画吧）过通宵。那个pub在上海算是小有名气的。里面都是日本的漫画和其他的书籍，氛围弄得很好。…… 
<br>
<br>����这时，旁边走过来一个服务生，头上系着头巾，很恭敬的在旁边问道，“先生，可以聊聊吗？”，我们高兴的欢迎了他，当然，第一句话是礼节性的问了一句“您是哪里人啊？” 
<br>
<br>����他的回答很巧妙。“我是哪里人不重要，我在哪里就是哪里人，你们觉得我是哪里人哪？然后用上海话说：“那晓得伐？”，听到朋友说它是广东人，就用广东话说：“你知o吾知嘎？” 
<br>
<br>������然后用北京话一样的北方音调的普通话对我说：“您说哪？” 
<br>
<br>������由于他的相貌和举止，我们一致猜他是日本人，他不置可否，对我们笑笑说：“这个不重要，我问你们一个问题好吗？” 
<br>
<br>������他向我们借了笔和纸，然后，很认真地看着我，说：“你是东北人，我问你一个地方你知道吗？” 
<br>
<br>������然后在纸上很流利地写下了两个很漂亮的汉字：“奉天”。 
<br>
<br>������写好之后，他抬头看看我说：“这里本来可以做都城的，可惜。” 
<br>
<br>������我马上觉得来者不善，可能因为面对的是外国人所以激发了我的感情，我很正经的和他说：“先生，您写错了，现在已经没有叫做奉天的城市了，“奉天”现在叫“沈阳”，叫做“奉天”的那个年代，一去不复返了。” 
<br>
<br>������他笑笑说：“那可不一定”。 
<br>
<br>������然后他在纸上画了一张世界地图说： 
<br>
<br>����“这个就是世界，你们对世界有什么看法吗？没有，因为你们是不思进取的民族。我们不是，我们的目标不只是奉天，东北，甚至中国。我们的目标就是……”，他用笔在地图上画了一个大圈，然后很有力量的说：“全世界”。 
<br>
<br>������我笑了笑说：“世界属于谁不是你们定义的，什么事情都有他的规律。你凭什么说中国人不进取？你凭什么瞧不起一个这么有文化底蕴的民族？” 
<br>
<br>����“你们总在谈文化，我很敬重中国的文化，我觉得我一辈子也不能完全理解其中的一点点内容，这也是我一直学习中国文化的原因，但是你们哪？你们中国人，自己的文化知道多少？甚至我可以说，中国人，懂中国的文化，绝对没有我们日本人懂得多。儒学的思想是什么？为什么孔孟会被尊奉到那么高的地位？你们谁能说出来？” 
<br>
<br>����“中庸、仁，这些就是儒学的精髓”我将我当时仅有的有关中国文化的知识搬出来。我那时的心情相当复杂。是啊，他问更多哪？我还能知道了吗？我们这一代认真的又很少人对中国的文化感兴趣甚至研究过。一个连自己文化都不懂的人，怎么配和别人理论文化的问题啊？ 
<br>
<br>������我们马上问了他很多关于中国的问题，看他是否真地对中国的文化有所了解，让我们吃惊的是：第一次大一统、唐代盛世、元清的少数民族统治、甚至“春天的故事”，这所有的东西，他都能很清晰甚至很透彻地告诉你经过和影响。他自豪地说：“一个不研究自己文化的民族，就是落后和愚昧的。是被别人瞧不起的。” 
<br>
<br>������我马上回击：“你们日本人就被别人瞧得起吗？别忘了，黄种人在美国是什么地位。” 
<br>
<br>������“你去过美国吗？你了解美国吗？如果没有，请不要妄加判断。我在美国生活过两年，我想，在美国，亚洲人如何，我应该比你有发言权。” 
<br>
<br>����我哑口。 
<br>
<br>������“有一次我去买首饰，选了好久，服务生都态度很生硬。而当我告诉她我是日本人的时候，他马上很恭敬的帮我选，还抱歉地说：‘对不起，我还以为您是香港人哪’，这就是尊重，是你们永远享受不到的尊重。” 
<br>
<br>������“你当人家真的尊重你啊？还不是因为日本人的钱？” 
<br>
<br>������“就算是为了钱，怎么样？事实就是人们尊重日本人，不只是因为钱，更是因为日本强大，因为日本人的素质比中国人高。” 
<br>
<br>������“你凭什么说中国人素质低？你去过几个地方？” 
<br>
<br>������“我去过十几个省，绝对比你去过的地方多，让我说为什么，我不举别的地方，我现在在的城市是上海，这算是你们中国最好的城市了。可是怎么样哪？人们像盲人一样根本不看红灯的过马路。乘坐交通工具的时候向发狂一样的挤来挤去。随地吐痰，晚辈在街上辱骂长辈，这些都是真的吧？” 
<br>
<br>������我又哑口，朋友接到说：“有些人是这样，但是不代表所有人是这样啊。我们深圳人就不会乱抢位子，乱穿马路。” 
<br>
<br>������“噢？是这样的吗？对不起，我没有去过深圳，如果有机会，我一定会去看看。”他的表情变得恭敬起来。这更接近我过去对日本人的看法。但是，这没有维持多久。 
<br>
<br>������“素质，什么叫素质？你们的孩子都被大人包在家里。生怕孩子吃苦。我小的时候，爸爸每天都让我四点起床去跑步，当时我很不理解，还有些恨他，可是现在，我感激我的爸爸，是因为这样的锻炼，使我形成了坚毅的意志。让我形成了日本人的精神。这么差的基础。怎么可能会有一个强大的民族？ 
<br>
<br>������我在美国做过hotel的manager，当时有两个人被辞掉了。一个是日本人，一个是中国人。他们都归我管。两个人都来求我，说自己能胜任现在的工作，让我帮忙。我并没有因为其中有日本人而偏袒他。我让他们做同样的工作，谁做好了把谁留下。很不幸的，两个人真的都不是很熟练。我当时同时的教了他们怎么做，让他们继续做好，那个日本人很认真地听我每一句话，最后做得很好，而中国的又懒又不努力，我都不知道他搞了些什么。最后我忍无可忍。我没有把他当人的对他喊：‘你给我滚！’ 
<br>
<br>������…… 
<br>
<br>������“无可否认有素质差的人，但是这并不能代表全部，难道你的意思是每个中国人都这样？我们有很多人很努力的工作，很敬业，难道你就没看到这些？” 
<br>
<br>������“工作？你们中国人最擅长的就是互相排挤，懂什么叫团队吗？你们连两个地方的人都会互相看不起，不要说一个团队里来自各个地方了。中国人是最恶心的民族。到处都是一个地方的人看不起另一个地方的。” 
<br>
<br>������其实他说的这些有很多是我一直想说的，但是作为中国人，我只能抱着复杂的心情和他理论到。 
<br>
<br>������“你有什么资格说这些？你对中国的地域了解多少？” 
<br>
<br>������“南方中国人和北方中国人我都研究过，我还做过类似的报告给日本政府。”他看着我说：“日本人比较看得起你们，你们像日本人，是真正的男人，在遇到侵略的时候会团结起来抗争。这也让我们吃了很多苦头。而南中国人比较聪明，他们不会愿意卷入牺牲和努力。所以他们愿意发展经济。但是一旦有战争发生，他们将是最容易制服的亡国奴。” 
<br>
<br>������“不许你诬蔑我的同胞！”我义正言辞的告诉他。 
<br>
<br>������“你不要觉得中国人多团结伟大，韩国人在国家出现困难的时候可以全国人民捐金子给政府，在你们中国，可能吗？？” 
<br>
<br>������我完全怔住了。 
<br>
<br>������是啊，他说的千真万确啊！可能吗？大家也可以自己想象，如果是你，你会吗？ 
<br>
<br>������“这个可以不谈，每个人有自己的想法。”他回到那张纸上，指着地图说： 
<br>
<br>��������“我还是给你们介绍我们的大一统吧。这是世界，十五年后，他就是我们日本人的。” 
<br>
<br>��������他在纸上用繁体字写下：“大一统”。 
<br>
<br>������“我们不用繁体字，对不起先生。请不要再写了。我们看不懂。”我们很生气他的做法。 
<br>
<br>������“没关系，其实这也是你们文化的一部分。你们连祖先写的字都放弃了。”他轻蔑地笑着说。 
<br>
<br>������“我们国家是有规划的，全世界都在看我们的动画片，我们的电视剧，用我们的电器，很多人疯狂的崇拜日本，在中国，也不再少数吧？你们接触的都是外面的文化，而中国人又对自己的文化不感兴趣，当有一天你们一觉醒来，发现身边的一切都是别人的的时候。你们连后悔都来不及啦！顺理成章的，总有一天，你们的国土也会变成我们的，因为你们中很多人已经认同我们的东西，我们的思想了。” 
