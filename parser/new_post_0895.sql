<br>������可以说，在相当长的一段时间里，我并没有对日本有任何心理上的戒备和敌意，甚至，可以说是有点谄媚的向往。我当然也听到过“南京大屠杀”“731细菌部队”“旅顺大屠杀”和数不清的日本人在侵略时的历史，但是，我始终认为，哪些是过去了，记着就可以了，干吗要抱着不放哪？两国友好不是很好吗？这些和他们这一代日本人没关系啊……等等如是的想法。 
<br>
<br>������2000年，我从东北的哈尔滨来到了上海，异乡的生活，给了我更多的机会接触不同的人和事，也让我有机会能够更深的思考一些问题。 
