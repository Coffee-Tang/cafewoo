<br>
<br>������那个时候觉得日本的动画片很好看，日本的小电器很好用，甚至连日本的忍者和剑道高手，都是我心中的偶像。小时候拿着木棍模仿日本人劈剑的经历，我现在都记忆犹新。渐渐的，人也长大了。有机会听到见到更多关于日本的东西。这中间当然有那段不短的历史，有日本国内对中国的看法，有日本经济的强大，和时常能听到的“友好邻邦，一衣带水”之类的宣传。 
