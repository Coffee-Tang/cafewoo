陶子的声音在身后响起，“晚上好好睡一觉，再做决定转科噢！”

一个星期后，我终于转到了文科班，觉得一切都是那么地亲切，仿佛上辈子就注定了我要呆在这里一样。文科班上的我如鱼得水，再也不用理会那些物理公式。见鬼去吧！我自由了！

