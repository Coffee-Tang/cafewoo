二  一直在告别之中

我亲爱的小檬，我从来不喝咖啡，我只喝牛奶和橘子水。开始是故意的，到后来，就成为了习惯。我也是这样单薄的孩子的，并不是如很多人所想的那样快乐，我微笑，开始我是故意的，到后来，真的成为了习惯。对于这些，我是真的无能为力。我编很多很多的故事，来安慰我自己，来安慰我自己冰凉的手，可是有一天，我的老师写信来给我说，这个世界上有许多的小孩，有的会唱歌有的会跳舞，有的会编故事，一切都是正常的。我突然就觉得我赖以生存的世界崩塌了，非常突然。真的很突然，可是我还是编我的故事，或许，这是我唯一的安慰了。如此而已，虽然它们欺骗了我，可是无论怎样，我都会原谅它们。因为我无法离开它们而快乐生存下去。我竟然真的是那样单薄的孩子，我感到突然的悲哀。或者，我长久以来的悲哀突然被我发现。任何的，都是一样。

我不希望自己成为虚张的孩子，我想安稳和平的生活。我们都是这样的小孩，你会唱歌，我会编故事，没什么大不了。太阳照样旋转。可是，这些，都是我在我的轮回中，忧伤地寻找的小幸福。我的微小的幸福。

关于爱情，我不愿意谈起。去年的冬天，我曾经全心全意地想要好好长大，考一所北方的大学，来嫁给一个有着干燥声音的北方男人，所有的一切，都是这里，我的转瞬即逝的微小的幸福，我的只开一瞬的爱情。他给我寄来雪的照片，蓝色的照片，我非常爱着，可是从未见过的雪。那个时候，我真的想要好好长大，然后，到北方去寻找他。可是就像一个蹩脚的肥皂剧，我短暂却浮华的爱情，消失不见。

我真的还只是一个孩子。后来，我不愿意相信任何东西，我真的是一个脆弱的孩子，你知道吗，小檬，这样轻轻开放又凋谢的花朵，把我的一切都消逝。于是，现在了，我只是会对着陌生人习惯性的微笑。我是容易破碎的，我不动声色，不愿意和任何人讲，但是，我真的这样了，我只能这样了。

我亲爱的小檬，我现在无比的喜欢这个名字了。小檬。我就是这样一个单薄的孩子，但是我还是愿意在这里，叫你，小檬。并且，真的，希望你快乐生活下去。一切都会好起来的，有一天，回头以后，发现，一切原来都没有存在过。那时候，可以微笑的。我想我可以真心的微笑了。这是我的梦想。

当我看到颜歌附在《只是寂寞》后面的长长的的评论时，眼泪那么轻易地就掉了下来。

我想起刚刚接到的三年前的那个男孩子的信。他说你告诉我这些年有没有人在你身边呵护你。那是一个很阳光的男孩子，笑的时候一脸英俊。可是后来他没有属于我。我告诉他，当我和你说再见的时候就已经决定要一个人慢慢地勇敢地走。

然后我给颜歌回帖：颜歌，我看过你写的字以后哭了。颜歌，我一直以为许多外表明朗的人其实有着大片的措手不及的空洞，以及隐匿着的巨大忧伤。我们都是易碎的孩子，我们都在用劲地让自己微笑，去支撑那一点点可怜的勇气，然后小心地保护好柔弱的外壳。颜歌，我们都要好好地勇敢地过，我们只能靠自己。可是，我知道，终于有一天，所有的疼痛都将消失，我们就不再活在告别之中了。

写下这些的时候电话铃响了，然后传来Michael的声音：Monica，我申请到了美国的大学。

那一刻我抓着听筒的手瘁不及防地颤抖。我知道，其实我一直都知道，Michael是终究要飞走的。

我想我一直都无法忘记那些叠起来很厚的信和以小时计算的长途话费。我陪着他放肆地笑陪着他发疯甚至为了那一纸通知郁闷得想要死掉。可是今天当他告诉我他申请到了美国的大学，当我清楚地知道他即将去圆梦的时候，我们居然都平静了下来，然后去努力地面对将至的离别。

