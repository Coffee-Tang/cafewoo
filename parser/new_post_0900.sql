高晓松的《青春无悔》。每当我抄下这首歌词的时候，就觉得所有的语言都苍白了。所以我无数次地想要提笔去写它，可是又无数次地搁了笔。

很多感觉，都是没有办法去描述的。

我一直以为老狼的声音是真正可以刺痛我的。虽然没有一点点的凌厉和尖锐，但是可以刺痛我。

老狼在唱这首歌的时候在录音棚里哭了。他说他想起了和女友在八中校门口的树上刻下的字。所以我在听的时候会为他哭泣，因为他唱出了让我掉泪的声音。

我们校园操场边上的几棵桦树上也刻满了许许多多的名字。当我伸出手去抚摩它们的时候就会想起《东京爱情故事》里那棵古老的木头柱子，然后眼眶微微发胀。那些名字被刻得那样用劲，认真而稚嫩的样子。可是我知道，当风无数次地吹拂过的时候，它们就可以随着岁月一起苍老了。是的，他们会一起苍老。就像“永尾完治”旁边的“赤名莉香”。他们会一起留在那里，一生一世。无论他们相爱，或者分开。

有些东西，是可以存活很久很久的，就像树杆上那些名字，永不磨灭。

