在机场上我和Michael深深地拥抱，很久很久似乎忘了放手，心里平静地疼痛。可是我已经不想再去流泪了。我非常害怕在这样的情节无法抑制地哭出声来，那样一切都会变得难以收拾。我非常害怕总有一天我的朋友会一个一个地从身边离开，最后只留下我一个人安静地寂寞地走。

