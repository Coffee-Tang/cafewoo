<br>
<br>烧烤档的老板是四川南部县的夫妻，我赶快用四川话套套近乎，没有指望他给我啥优惠价，也不想他能改变一下他那牙签般粗细的肉串，更不敢想他乡故知之类的事情，唯一的希求是别卖变质的食品给我们，这当然不敢直接对他说。
