<br>果真如此，双向6道的水泥路，视野良好，但别急，你也甭想放松：整个公路的4/5都被当地人用来当作晒场了！留给车走的那1/5还是弯弯曲曲的，还要避让那些在路中间扬场的农夫，不容易啊。
