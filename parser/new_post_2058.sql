<br>一，	龙海市港尾火山口
<br>港尾附近隆教畲族乡白塘村海岸的火山口表面长椭圆形，高约40米，宽约70米，由北向东伸展，长约200米。涨潮时为波涛所淹没，退潮时露出容貌。有人戏称为“神奇的海底兵马俑”，因为这一片礁石，像一排排露出半身的偶像，肥头大脑，铁骨铮铮，排列得井然有序，气势恢宏，才为此命名的。与“海上兵马俑”相连的另一片礁石，则是一排排如车锯刀削的石柱，高约六、七十厘米，或呈四方形，或呈六角形，规格一律，高低错落，纵横十余丈，绵延起伏，场面十分壮观。中外地质专家誉牛头山火山口为“世界罕见的古火山口博物馆”。只有身入这海上石林，方知其中奥妙。
<br>据专家的科学论证，这座火山口的环太平洋火山地震带西部，由于太平洋板块俯冲，插入欧亚板块，形成福建沿海地质历史上自长乐至南澳的深断裂带。这座火山口就在这断裂带上，是距今约700万年（有人说是2000～2200万年）从80～100公里的地壳深处向上喷发岩浆的遗迹。火山口中心是直径约30公分的圆形喷气孔。地质历史上的三次火山喷发熔岩界线明显可辨：最初喷发的一次，形成橄榄石色体的橄榄玄武岩，露出在最外一圈；内圈的含角砾粗玄武岩则是最后一次喷发所形成；中间是第二次喷发的粗玄武岩。这些粗玄武岩已经发育成柱头节理石柱，横断面有的呈五边形，有的呈六边形。
<br>  这火山口是观察深部秘密的窗口，引起中外很多学者的兴趣，纷纷前来考察研究，它的奥秘尚待继续探研中。
