<br>上岸后的首件任务是解决住宿的问题，中银开发区内的华商酒店已经不再考虑----没有必要再受千篇一律的“3星”酒店的虐待。电话咨询后，决定下榻在南太武高尔夫俱乐部的小木屋，一番电话上的讨价还价，用一晚300元享受720的待遇，成交。
<br>
<br>正在建设的高标准水泥路已经双向开通，工程车扬着漫天的黄沙毫无规则地撒着野，民工的自行车是乎必须载二人方可上路，和他们一起的当然还少不了当地人的摩托车，为了不被意外破坏周末的计划，我只得不时地开开警笛----车灯和喇叭对他们已经不再有任何作用，只有尖利的警笛才能提醒他们在身后可能还有其他车辆。
