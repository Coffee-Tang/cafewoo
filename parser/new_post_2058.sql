<br>二，赵家堡：
<br>座落于漳浦县湖西乡硕高山麓，距漳州市区90公里。南宋亡时，末代皇族闽冲郡王赵若和（宋太祖赵匡胤之弟赵匡美第十世孙）曾逃难隐居于此。明万历年间开始建堡，历时二十年方才完工。城堡仿北宋京城汴京式样修建，整个建筑群由两部分组成，初建的旧楼，旧堡为主体的部分。楼分为三层四合式四方楼，高20米，占地484多平方米，每层16间，共48间，楼门上镌刻着&quot;完璧楼&quot;三字，取&quot;完璧归赵&quot;之意。楼外环以石基土堡，三合土堡墙高6米，宽2米多，上有垛口。设东西南北四个城门，东门题刻&quot;东方钜障&quot;，西门题&quot;硕高居胜&quot;，南门题&quot;丹鼎钟祥&quot;，唯北门没有题额。西门设瓮城，立有赵范&quot;硕高筑堡记&quot;碑。东门内有百米石街，天井中筑一条地道通东门外。&quot;完譬楼&quot;及其环卫土堡俗称&quot;内城&quot;。另一部分是赵义扩建的五座五进府第，每座30间，共150间，俗称&quot;官厅&quot;。每座第5进为2层楼，系内眷住宅。现仍保存完好。官厅前是铺石广场，场上有&quot;修竹&quot;、&quot;父子大夫&quot;石坊，广场前有内外水池，池上筑有精致的&quot;汴派桥&quot;。此外还有修竹花园、辑卿小院、聚佛宝塔、古井、武庙等建筑;有&quot;悟石&quot;、&quot;读书处&quot;、&quot;云巢&quot;、&quot;咫尺玄门&quot;及摹自宋代书法家米芾手迹的&quot;墨池&quot;、摹自禹碑的岣嵝碑等石刻。这是我省目前保存最完好的古城堡建筑群。周围约一公里半，面积约半平方公里。
