背景材料：

一，	龙海市港尾火山口

港尾附近隆教畲族乡白塘村海岸的火山口表面长椭圆形，高约40米，宽约70米，由北向东伸展，长约200米。涨潮时为波涛所淹没，退潮时露出容貌。有人戏称为“神奇的海底兵马俑”，因为这一片礁石，像一排排露出半身的偶像，肥头大脑，铁骨铮铮，排列得井然有序，气势恢宏，才为此命名的。与“海上兵马俑”相连的另一片礁石，则是一排排如车锯刀削的石柱，高约六、七十厘米，或呈四方形，或呈六角形，规格一律，高低错落，纵横十余丈，绵延起伏，场面十分壮观。中外地质专家誉牛头山火山口为“世界罕见的古火山口博物馆”。只有身入这海上石林，方知其中奥妙。

据专家的科学论证，这座火山口的环太平洋火山地震带西部，由于太平洋板块俯冲，插入欧亚板块，形成福建沿海地质历史上自长乐至南澳的深断裂带。这座火山口就在这断裂带上，是距今约700万年（有人说是2000～2200万年）从80～100公里的地壳深处向上喷发岩浆的遗迹。火山口中心是直径约30公分的圆形喷气孔。地质历史上的三次火山喷发熔岩界线明显可辨：最初喷发的一次，形成橄榄石色体的橄榄玄武岩，露出在最外一圈；内圈的含角砾粗玄武岩则是最后一次喷发所形成；中间是第二次喷发的粗玄武岩。这些粗玄武岩已经发育成柱头节理石柱，横断面有的呈五边形，有的呈六边形。

这火山口是观察深部秘密的窗口，引起中外很多学者的兴趣，纷纷前来考察研究，它的奥秘尚待继续探研中。

二，赵家堡：

座落于漳浦县湖西乡硕高山麓，距漳州市区90公里。南宋亡时，末代皇族闽冲郡王赵若和（宋太祖赵匡胤之弟赵匡美第十世孙）曾逃难隐居于此。明万历年间开始建堡，历时二十年方才完工。城堡仿北宋京城汴京式样修建，整个建筑群由两部分组成，初建的旧楼，旧堡为主体的部分。楼分为三层四合式四方楼，高20米，占地484多平方米，每层16间，共48间，楼门上镌刻着"完璧楼"三字，取"完璧归赵"之意。楼外环以石基土堡，三合土堡墙高6米，宽2米多，上有垛口。设东西南北四个城门，东门题刻"东方钜障"，西门题"硕高居胜"，南门题"丹鼎钟祥"，唯北门没有题额。西门设瓮城，立有赵范"硕高筑堡记"碑。东门内有百米石街，天井中筑一条地道通东门外。"完譬楼"及其环卫土堡俗称"内城"。另一部分是赵义扩建的五座五进府第，每座30间，共150间，俗称"官厅"。每座第5进为2层楼，系内眷住宅。现仍保存完好。官厅前是铺石广场，场上有"修竹"、"父子大夫"石坊，广场前有内外水池，池上筑有精致的"汴派桥"。此外还有修竹花园、辑卿小院、聚佛宝塔、古井、武庙等建筑;有"悟石"、"读书处"、"云巢"、"咫尺玄门"及摹自宋代书法家米芾手迹的"墨池"、摹自禹碑的岣嵝碑等石刻。这是我省目前保存最完好的古城堡建筑群。周围约一公里半，面积约半平方公里。

三，新城：

漳浦的湖西乡旧有“五里三城”闻名于港台及海外。所谓“三城”者，即赵家堡、诒安堡和蓝提督府第，由于后者建造时间较晚，故称之为“新城”。这新城的主人就是蓝廷珍。顶坛新城是蓝廷珍的府第。在湖西顶坛村。康熙末至雍正初蓝廷珍修建。蓝廷珍府第东向，宽50米、深86米，占地6.5亩，系以中轴线上的门厅、正堂、后堂、主楼和左右后三组厢房组成的院城，故称"新城"。主楼为第四进，系二层方形土楼，楼门嵌蓝廷珍书"日接楼"石额匾。

