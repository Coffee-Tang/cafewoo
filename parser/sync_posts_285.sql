<p>和辉在一起是有多么的奇怪。维系在中间的那根线至今我弄不明白是什么。我们会在高高兴兴的时候突然间叽叽歪歪地吵个不停。言语是伤人的利器。我总是悲伤的想终有一天我们会轻轻松松地忘掉了彼此。然而我一天天地更加迷恋他手心里的温暖。在反反复复地折腾中我痛苦着自己。我总在争吵之后怒冲冲对辉说我的骄傲不容许你的存在，却时常在做出妥协时说，这只是不想要自己变得太任性。
<br/>辉总挑衅地望着我说，你愚钝而没有灵气。
<br/>我则选择沉默的离开。我不知道，心是否会因习惯而变得麻木。</p>

<p>童年长着翅膀的梦想。什么时候沉重得再也舞动不了。床头是一排排赶不及打勾的计划。满柜的中英文小说曾是我铺天盖地的激情在汹涌。在现实的琐碎中一点点被磨灭。我依旧认真地工作努力生活。眼睛却在茫然中不知所措。即使这个月我拿了跟工资一样高的奖金，我也不知道这跟希望会有怎样的关联。
<br/>咖啡里有谁拒绝过杜拉斯。我却空前地喜欢。绝望的灵魂在喃喃自语，没有过多情节的语言，如果你没有历经过痛苦漫长的黑夜，是无法体会的。卫慧的文字看了直叫我恶心，岂能拿来相提并论？
<br/>说到这里，不要以为我很偏激。其实我总习惯于乖巧地对着你笑。至今我仍喜好翻阅卡通漫画。很轻松的快乐。快乐得我一个人独自咯咯直笑。</p>

<p>婆婆说过，当世事随缘变迁。去看镜中你的眼睛。
