<p>在白玉兰花枯萎的季节，我结束了初恋。落花乱雨般的纷飞中，我明白了诺言的可笑。而谁也无法去辩证对错。
<br/>蓓说，这没什么大不了。
<br/>我说，是阿。我还是我。我笑得一脸的安静。</p>

<p>宁是我的名字。简单的笔划如同我明净的双眸。熟识我的人都说我有一张永远不变孩子的脸。于是，他们她们都容忍我一贯迷糊的脾性。我从未曾想过去改变。也未曾感觉到不妥。
<br/>然而，我知道这不是真正的我。
<br/>阳光下流汗的运动从来都拒绝。爬山、打球、游泳。我什么也不会。
