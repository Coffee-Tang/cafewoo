<p>喜欢跳迪士高是很久的事了。哪一年的平安夜，蓓带我去XO2。我的位置靠近舞池，我看见蓓绚目的身影在眼前扭动。第一次，我知道舞步是可以如此的简单，简单得让你眼花缭乱。当我的舞步日趋熟练，我却慢慢远离了这喧哗的地方。在震天音乐的心跳过后，额上流泻的汗水感觉则更加的虚空。 
<br/>而其实是，我迷上了网络。自言自语地在BBS上贴了些许文字。沉湎于其中难以挣扎。浪费了时间，还有，最初的情感。</p>

<p>萱有着细碎的中长发，染着淡黄的颜色。画着眼线，一点点的媚。那晚，就只我一人在跳舞。萱独自喝完一扎的啤酒。兴奋地想拉萱起来，萱却安静地放不开步子。萱说，宁你的舞步真好，长得却象个孩子。
