那天下午路过厦大，想想反正无事可做，便到几个老师家去坐坐，叙
叙旧。

从老师家出来的时候，天色尚早，就在厦大一条街上闲逛。走过"温
莎堡"、"蔚蓝海岸"、"林家鸭庄"，每一间店都能勾起一丝大学时的
记忆。其实严格说来，这只是一条小小的商业街而已，一间挨一间的
大多是小饭馆、小百货和精品屋。但我总能感到浓浓的校园气息从厦
大里弥漫出来，弥漫在这整条街上。因为什么呢，是因为来来往往的
厦大学子吗？不是的，至少现在不是，因为放假了，学生大多已经回
去了。是因为沿街的凤凰树吗？也许是吧，但今年的花开得不如往年
烂漫，只是稀稀落落地散在枝头与叶间。

一定还有别的原因。

                        二
算算看，自己已经很久没好好逛书店了，于是在路过"晓窗书屋"的时
候，决定进去看看。进了书店，也就找到了答案。这整条街上最有校
园气质的，正是这几间相隔数十步的书店。每一间书店都不大，但都
很雅致。店里的书新而且好，一些在大书店找不到的学术专著在这里
反到能够看见。在书店里哪怕只是逛逛，随手翻翻看看，都是一种享
受。

大学四年在这些书店里买的书加起来也有几十本了，有的看了几遍，
有的看到一半就扔在一边，有的到现在还是崭新的。刚进大学的时候
曾给自己定下一个每年至少看十本书的计划，当时定计划已经觉得自
己偷懒了，但实际上是一年看的书比一年少，到最后一年，就剩下六
、七本的水平了。当初看书的时候只是因为喜欢看，并不想为什么而
看，现在想想，这些书给自己带来了什么样的改变呢？思维敏捷了，
谈吐风趣了，想问题更深了，还有什么呢？应该没有了。喜欢就是喜
欢吧，没有原因的。

但为什么现在变得越来越懒于读书了？

曾经读过这么一篇文章：在大学毕业前的晚会上，一位教授临别赠言
："希望你们将来每年至少读一本好书。"学生们都不以为然，因为他
们觉得这太容易了。十年后的校庆，当年的学生都回到了母校，那位
教授提起他十年前的那个期望，所有的学生都无言以对，他们没有人
可以做到。

看来越来越懒于读书的不止我一个。

                       三
太久没逛书店了，突然觉得新书特别的多。翻了翻怀念钱钟书的《一
寸千思》和北大博士的《看上去很丑》，看了看新的《天涯》、《大
家》和《生活周刊》，看到了孔庆东的一篇关于知识分子无奈于贫富
不均、社会腐败的文章。读着文章，脑子里又回到了刚才在老师家的
闲谈叙旧。老师的家是一套几十平米大的居室，客厅与卧室都堆满了
