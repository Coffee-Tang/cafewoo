团。

我想给自己找一个答案，但这样的答案显然不是一进书店就能够找到
的，这样的答案似乎在自己读过的书里和接触到的现实里都不能看见
，这样的答案好象已经超出了自己的年龄所能承受的范围。

                        四
我因为找不到答案而困惑。这样的困惑在脑子里潜伏了几天后，我突
然觉得是不是自己过于夸大了读书的作用？

是的。

现代社会是不允许年青人淡泊宁静的，我们在读书的同时还想得到很
多的东西，我们希望能一步步的成功，当读书并没有带给我们想得到
的东西的时候，我们开始置疑读书，懒于读书，放弃读书。为什么在
十年前很容易做到的一年读一本书在十年后却做不到呢？因为在这十
年中生活的环境改变了，追求的目标不同了，琐碎的事情太多了。谈
恋爱、找工作、结婚、升职、买房子，这么多事情光靠读书是办不到
