在拇指族的眼里，短信是生活的调味品，字数限制本是一憾事，但短小精悍的短信精品却能让人眼前一亮。恶作剧短信是短信家族中最受欢迎的一员，当你用拇指摁下手机阅读键的时候，跳出的短信内容是“据调查，百分之九十以上的同性恋者用大拇指查看短消息，不用改了，已经来不及了”。令人无可奈何而又忍俊不禁；温情派的短信承载着温暖的爱意，在第一片落叶飘下时，发一条“天气变得好快，凉气悄悄袭来，因为你的可爱，所以给你关怀，晚上毯子要盖，别把猪脚冻坏，没事叼根骨头，那样可以补钙，不要再骂我坏，祝你天天愉快”。这种幽默和狡黠，能让朋友发出会心的笑。在“三·八”节里，“如果漂亮是一种罪，你已经罪恶滔天！如果气质是一种错，你已经一错再错！如果智慧要受惩罚，你岂不是要千刀万剐！预祝天下第一美女‘三·八’节快乐！”这种短信准能帮男性朋友赢得美人归。

