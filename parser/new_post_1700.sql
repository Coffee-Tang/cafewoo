早晨，伴随着清晨第一缕阳光的是来自挚友的短信问候：“一天好心情！”就像清晨的第一口燕麦茶，那是我一天的动力。

中午，当我疲倦时，挚友又发来短信：“茶，要喝浓的，直到淡而无味；酒，要喝醉的，永远不想醒来；人，要深爱的，要下辈子接着爱的那种。朋友，要永远的，就是看手机的这个。”那一刻，手机蓝色屏幕有一种空灵的美。

晚上，挚友没发短信，但电视里传来一句熟悉的广告词：“听，是鸟叫！”“不，是短信……”（拉拉）

