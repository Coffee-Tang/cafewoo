人类正处于一个“漫游”时代，正朝着“任何人用任何工具以任何方式在任何时间任何地点将任何内容传输给任何对象”的趋势发展。手机短信的产生不可否认是技术舞台的一个开拓，但是不可避免地凸显了背后的文化矛盾。解剖短信也是一个解剖一种社会关系一种文化积淀一种心理层面的过程：渴望与人交往与渴望拥有私人空间；渴望得到心理慰藉与害怕被人影响等这些矛盾总是会相应存在着。（黄熙雯）

新闻系老师

短信作为一种新的传播方式，它快捷，方便，便宜，它使新闻可以更快且随时随地地传播开。最大的新闻往往是最先通过口头传达的，但人们并不可能在知道新闻的第一时间遇见想要传达给的朋友，一般也不会刻意地去打电话传达，这就影响了新闻的时效性。于是，发个短信便成为经济迅速的途径。（陈鹤翎）

