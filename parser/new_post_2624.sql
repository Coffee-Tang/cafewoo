有人一定会说，小时候的伙伴在故事为什么都会搬家呢？是不是故事写不下去而这样找个结尾呢！事实上娇娇的确搬家了，我再也没有见过她，她送我的那副画也早就不见了，只有她和我都记得的橡皮筋，能串起一些模糊的记忆，我忘了她的脸庞，不久前听说幼儿园里对我特别好的阿姨过世了，这才让我意识到，时间到底可怕到什么程度，我留下的蛛丝马迹通通会在时间的冲刷下无影无踪。

这是我想告诉大家的第一个故事，它让我易伤感的神经又一次脆弱了一下，也让我的心偷偷动了一下，我想有很多人都会和我一样，在仿佛遥远的童年里有一段特别的记忆，象温热的米酒，依然醇香，依然浓烈。

以上故事，如有雷同，纯属偶然。

