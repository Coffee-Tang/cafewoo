玉林坊里大乱，堂上堂下奔忙一片去找太医。

一片混乱中，阶前只多添了几片落叶，李凭便被尹鄂悄悄地引出后堂，她还不清楚发生了什么。这不过是公元910年一个平凡的秋天。

