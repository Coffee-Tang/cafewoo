老伎李凭在洛阳光明寺教弟子“归国遥“，已经是赵家天下，如今东京在汴梁，又一朝繁华春梦，这思唐的旧曲，入不了新声了。

“金翡翠！为我南飞传我意。“

从小弟子的歌唱中，她只能依稀记得当年成都的一个秋天，她也是这般无忧无虑地唱了一句，然后门外走过一个书生。李凭一生飘摇，故园早已万里之遥，归路也不堪思量。兵荒马乱的年代，没有辗转填沟渠算是万幸，她本以为那个秋天会改变她的人生，隔了几朝几代的春草昭阳回望过去，那人那事，其实是如此微不足道。

她不觉走了神，衰寺的壁上还留着唐时的壁画，是阿弥驼经变，伎乐飞天隐约在西方净土，菩萨的身形美好窈窕，这斑驳剥落的极乐世界。李凭想起当年猝死在玉林坊的左散骑常侍韦庄，多年之后，她已然明了当时的一切，叹息间，只是低头把箜篌撩拨。

处身洛阳的萧寺，她带着老妇人的伤感，叹息着因缘果报，喃喃自语，小弟子终于唱到结末一句：

“旧欢如梦里。“

