当尹鄂回到宿舍时安睡，准备明天一早起来带女朋友去爬山时，李凭北美的一个小城醒来，又一个北国明净平凡的秋天。她出了门去学校，这一带有名的是秋天的红叶，秋山明净如妆，绿树最高枝嫣红了几团，好像微醺的脸，待秋阳再一烘，便要沉醉了。

朝阳把她的影子拉得长长的，薄薄地贴在地上，她步履轻快地往前，想起曾经在黄昏走过那条柱廊，那些肌理丰富的灰色光影显得那么遥远。她忽然觉得现在早晨的空气洁净得令人窒息。长空雁鸣，她抬起头正看见头顶一大片柔软的翅膀向南掠去，那样高那样远，决绝而飘忽，阳光下好像没有影子，李凭莫名地感到一阵惆怅，她知道那些翅膀的影子落在她心里。

李凭一生完美无暇，只是她并不识得尹鄂。

