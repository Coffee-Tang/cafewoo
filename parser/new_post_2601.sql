注：关于菩萨蛮――古代部分韦庄和老妇人是关键人物，韦庄五阕菩萨蛮，可以想象为其人一段风流遭遇。清张惠言《词选》卷一按为：此词盖留蜀后寄意之作。

韦庄菩萨蛮可看成叙事词，这个人很有趣，年轻时是游历大江南北的风流才子，后来奉使入蜀，和蜀主王建关系很好，所以第一阕“红楼别夜堪惆怅”言奉使之志，本欲速归。李凭的师父，中原口音的老妇人，是昔日洛阳歌女，在我的构思里，便是“美人和泪辞”，他的中原老情人

第二章“游人只合江南老”言蜀地繁华，人物多情“满楼红袖招”，中原沸乱，故言“还乡须断肠”。

第三章“如今却忆江南乐”，是韦庄终于践约回到中原和歌女相会，但是已经难忘蜀国旖旎生涯，加上朱温之乱，使得他决意永远离开中原，劝王建称帝，荣华富贵和江南风情的诱惑，政治野心,虚荣心和对战乱的厌倦使他“白首誓不归”，决定抛弃中原的一切情分。

第四章“樽前莫话明朝事”是韦庄和歌女诀别。或许有韦假意许诺日后富贵不相忘的情节，但歌女已经知道“人生能几何”，此后可能是渺茫。

第五章“洛阳城里风光好，洛阳才子他乡老”，韦庄在蜀富贵荣华，而致思唐怀旧之意。衰朽和惆怅使之心绪复杂，独自承担着对人生难言的失望和忏悔之心。

被遗弃的洛阳歌女几经劫难，辗转来到成都，心怀怨伤，得到韦庄这几阕菩萨蛮，于是暗的教会当地一个小歌妓李凭中州曲调，韦庄为蜀主主持成都梨园，当然会有机会注意到李凭，老歌女希望由此讥刺韦庄。因此在机会终于来临时，她嘱咐李凭在韦庄面前唱菩萨蛮。

韦庄一听便察觉是故人声调，追问之下知道老歌女未死，已经到了成都，衰老和羞赧之下猝死。

李凭在不自觉中充当了老歌女向韦庄报复的工具，而这报复效果太强烈了些，她到暮年自然明白其中的恩怨和隐情。

在故事里，尹鄂对应早年的韦庄，李凭对应早年的洛阳歌女，他们之间的遭遇，影射韦庄的情事，自然李凭的晚年，也映射着老歌女。邂逅钟情离乱背叛，兵戈频仍中人生的无常和轮回之感。

如果铺开了写，可以是从唐亡到王建蜀国的一段长篇。王建一朝，君臣风流才子颇多，也是很有意思的，这个尹鄂，是个当时的小词人。

但我无能去沉下心去研究揣摩历史，而且当时的动机是一个极短篇的时空映照实验，抒发一点虚无的人生感慨，人类意志的软弱和世事的无常，人的胆怯和永恒的不满足，貌似完整的人生充满残损。所以一些必要的情节交代都压缩在五阕菩萨蛮里，而叙述一些场景和隐约碎片。

显然韦教授对应韦庄，留学回国后不满意故土，这要铺开写大概可以批评一通国内学术圈对人才的消耗，故国虽多情，而令韦庄失望。他无法克服自己的虚荣和事业上的野心，最终选择了抛弃妻子永远离开中国。

尹鄂和李凭的对应就没有韦教授那么直接。邂逅和眷恋看似对抗不了命运的大潮，但其实只要有足够强烈的意志挽留，命运也可以被改变，可惜人们总是随波逐流，那些停留在原地和漂泊异乡的，其实都不知道自己真正的归宿在那里，都是被时代推着走的。这和古代部分的感触是一致的。后蜀的尹鄂和李凭，他们的分离原因其实是等于现代的尹鄂和李凭，年轻时的钟情和邂逅那么清淡美好，好像能够注定终身，其实却由于人难以克服的软弱，生命如此之轻，无法对抗时光的冲刷。

