<br/>                                               ------希望在人间 </p>

<p>今天看了篇传记，是关于丁玲。
<br/>传记里这么说
<br/>她是一个生于1904年的女作家。
<br/>丁玲的父亲在四岁的时候挥霍光了家财，潇洒地离开了尘世
<br/>她与母亲投奔了舅舅，在她少女时期，五四运动爆发，跟随着瞿秋白的女友到了上海
<br/>在那，她遇到了生命中的第一个男人：胡也频
<br/>他们相爱了，不顾一切闲言闲语两人开始同居，直到她生命里认定的文学天才冯雪峰的出现
<br/>在她的文字里这么写着她对冯的感觉：我停止写作，只有一个念头--听这个人说“我爱你”
<br/>是的，她在与胡也频相爱的同时，爱上了冯雪峰
<br/>她对胡也频说：“我必须离开你，现在我知道爱情是什么，我爱上他了”</p>

<p>而后，丁玲追随冯雪峰到上海，杭州，胡也频也随之而去。
<br/>三人一起同居，举国哗然
<br/>只是这种局面维持不久，理性战胜了情感，丁玲重新回到了胡也频的身边</p>

<p>而这并没有完结，在不久的日子，胡也频作为左联五烈士之一被枪决与上海，冯雪峰也遭遇同样的不幸
<br/>此后，她无论和谁在一起，都摆脱不了对胡的纪念和对冯的怀念。</p>

<p>1931年的11月，她与她生命里的第三个男人冯达结婚了，并度过了三年的平静生活，直到被国民党双双逮捕
<br/>并在1934年生下了一个女婴。</p>

<p>1937年2月，她爱上生命里最后一个恋人，陈明
<br/>1942年，38岁的丁玲与25岁的陈明正式结婚，并没有举行婚礼也没有请客吃饭
<br/>1955年，丁玲遭到批判，流放北大荒长达8年，之后又被投入北京秦城监狱，5年后再被送到山西乡村。
<br/>就这样，她后半辈子的人生都在苦难中经过，而在这25年中，陈明一直陪伴着她。
<br/>1986年3月4日，82岁的丁玲最后向她丈夫说：你再亲亲我，我是爱你的，我只担心你你太苦了。</p>

<p>那一辈子，她勇敢地去爱，不去在乎任何世俗的眼光与流言蜚语
<br/>虽然苦，但是我相信她从不后悔与埋怨</p>

<p>其实，爱情是两个人的事情
<br/>在我们的年代，或许觉得她与胡也频的未婚同居，她与胡与冯的三角关系，她找了比她小12岁的恋人这三件事情并不希奇
<br/>只是，在那个思想封建的年代，一个弱女子这样的勇敢为爱
<br/>我感慨她的际遇，也感慨上苍对她的不公平，生的如此多情的女子，却没有幸福的结局
<br/>感谢她，这为爱奋不顾身的女人，是她诠释了爱情的含义，也是她让我知道希望在人间</p>

<p>而现在
<br/>放眼望去，到处都是堕落与懦弱的灵魂
<br/>可谁又有权利去指责那些人与事？
<br/>你又如何知道他她不是为了爱情勇敢地离经叛道？
<br/>你又如何知道他她不是为了爱情而选择背道而弛？</p>

<p>太苦了，这人世太苦了，上苍如有眼，请让我下世不再做人</p>
