会觉得其实，有限的这些生命里，你可以做一些很有意义的事情，自己让自己活
的快乐。那把自己的梦想，其实，能够完成的就完成，尽量完成，那，也可以帮
助别人完成他的梦想。不管，是时间花的快还是慢，我觉得，你有去做，你已经
尽力了，你就已经成功了。我觉得，这样来讲，你对死亡，就不会那么恐惧了。”

