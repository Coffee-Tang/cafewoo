度空间》让他再度成为关注的焦点。一种先天的遗传疾病，让这位年轻艺人的有
生之年面对预约的死亡。这一切都属于他——周杰伦。）
 

                          其实我也会害怕
  “是遗传性的一个僵直性脊椎炎，那表演的时候不能说不跳舞或者怎么样，所以
必须要精彩的话。有时候可能吃药控制自己的病情，痛起来的时候，其实我是满
在乎的。那现在，有时候不痛我就觉得没什么，痛起来会有一些害怕，痛起来连
