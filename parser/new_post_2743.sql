可能就是靠药物，你可能，就是做一些复健的运动。大家看到的我，希望就是很
健康的，所以，我希望，还是把很好的一面给大家。这样。”

                           还是要面对它
   “其实对生命来讲，我觉得每个人应该对死亡，都会有一种恐惧。但是，我觉得
