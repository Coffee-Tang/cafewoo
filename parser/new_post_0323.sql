从日本回来后，我就离职了，离开了和涂飞共处四年的城市。

是的，这座城市不会下雪。今晚，我又走公路了。

网上，有个人问我：“你认为网络上有真正的友谊吗？”

我很想说：“你几岁？问这么无聊的问题。”

但我突然想，也许现在网络上，甚或现实中这么认真问问题的人已经不多了，或许以后他也会成为我这个样子，但是至少现在他们是真实的，认真的…

我说：“有，但是不是永远。”

“介绍你去个不错的网站，好不？”那人说。

“好啊。”我正闲在网上发荒。

“它叫<忘情网>”那人说。

很意外的，那一天那个网站进不去。那人说，这种事情是非常少发生的。

走公路，上网，我的生活多了个

