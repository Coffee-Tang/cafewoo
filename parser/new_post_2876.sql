<p>安小沫</p>

<p>我在鼓浪屿的日光下沐浴着我的整个夏天.</p>

<p>在这个7平方公里小岛上,可以看见阳光如喷泉一样溢满了每个人的眼睛,触感新鲜,没有一点城市里的喧嚣尘土气息.</p>

<p>在这座小岛中找到的只有安静,太精致的一切让人总是体会不到寂寞的矜持,整个城市的艺术气息总是聚集在这里,忧郁眼睛背着画板的男孩子,还有演奏钢琴的小女生,总是这座小岛最经常的风景.我在鼓浪屿的夏日里,是一个不安定的份子,谁都不知道我会在什么时间,出现在什么地方.</p>

<p>清晨的时候去鼓浪屿的英语教室,看着它未曾装修过的脸,青苔的颜色从古旧的墙壁上爬出来,空气有水洗过一样的味道.甚至一进门就可以看见柔软的灰尘慢慢地飞舞在寂静的教室里。在X东方，X航道等等英语学校如雨后春笋一样在城市里拔地而起的时候，这个属于英语的课堂就显得格格不入。可是我就是选择了它。不因为什么，只由于它是安静的，安静不张扬的。</p>

<p>后来我发现我错得实在离谱，讲台上那个据说精通6门外语的老外讲得唾沫横飞，乐此不疲，下面却坐着一群不知所谓的学生，听歌发短信梳妆打扮说话调情，应有尽有。我手里那着那杆蓝色的签字笔，记下一个个黑板上的单词，冗长的，一如着夏日的午后，长到让人有点昏昏欲睡。身边的一个女生拿着刚买的白金的头饰向其他人炫耀，银光闪到我有些头疼得不知所措。</p>

<p>这么爱现的话干吗不直接顶个金元宝在头上？我心里想着，哑然失笑。手轻轻掂了两下，竟然感觉轻得有些不自然，我笑得更加自然。</p>

