<p>“假的？”我的脑袋需要好好对这话进行消化：“是假的？”“是啊。”秦阳点点头，然后慢条斯理地把衣服脱下来：“是假的，真的CK很贵的不是吗？我买不起。”说完贼贼地笑：“不信么？不信的话我撕给你看吧？”说完刷刷刷刷那件CK就成了破布一块，让我不得不佩服男生的行动力和衣服的伪劣性，原来撕得那么轻松啊。“所以安小沫，我……”秦阳这话没说完，我就听见一声尖叫，然后我们就被莫名其妙地赶到教堂外面去了。在一连串闽南乡音中我和秦阳被牧师伯伯好生教育，从当代人的开放观念到教堂是神圣之地，在牧师伯伯缅怀着他们那个年代的青春美好时我发现了问题的所在，我身边站着个半身赤裸的裸男秦阳，顿时，我哈哈大笑。在路上，我想起他拉住我的手爬墙时候的感觉，不由窃笑了好几下，细细回味一下手上温热的触感，可惜是夏天呢，如果是冬天就好了。我顿时有点贪心地在想。</p>

<p>不过我想我也想不了太久了，当秦阳终于学会如何用英文问路的时候我发觉这个夏天竟然要过去了。英语班结束的时候，我和秦阳请老外吃沙茶面，红红的汤汁晃得我眼睛疲惫。那个老外忽然用中文对我和秦阳说：“你和他都是好学生，唯二的。”我对此的评价是这个老外中文水平不过关，没听说过中文有“唯二”这么个词。秦阳对此的评价是：“你为什么不早用中文讲课，我听得都累死了。”老外走后，我们俩笑得春光灿烂，我说秦阳，你在国外再也不怕迷路了。他说，你再在我身边呆下去都胖成猪了。我笑着说对啊对啊，顺便再叫了一碗面，然后依照惯例地加上丸子瘦肉大肠鸭血青菜和油条，用我的话来说是饯别礼物，我马上要回家去了，在我依然吃地淅沥哗啦的时候，秦阳微笑地递给我纸巾擦嘴，也叫了一碗跟我一模一样的沙茶面，他说，这也是跟他即将远行的礼物。</p>

<p>我离开的那天天干物燥，轮渡口人声鼎沸，秦阳正好要去拿机票所以没有来送我，我点头表示对他的谅解，可上了船才忽然想起那天我们去教堂的之前他就跟我说拿过票了，顿时有点痛心疾首地骂他小子不厚道，肯定是要睡懒觉才故意不来送我。这越想就越难受，我在拥挤的船上有点透不过气来，忽然旁边有个人挤了我一下，我一个不小心就摔在地上，只是我坐在地上一直没有起来，那一瞬间，我非常想有一双手能拉我一把，最好那种有着温热触感的手能够拉我一把。</p>

<p>汽笛的鸣声打断了我的思绪，我看着逐渐远去的鼓浪屿，在阳光的照射下显得如此恋旧的美。只是我离开了，不对！那一瞬间，我才想起，其实真正离开的不是我，而是他，秦阳一直都是个要离开这里出国的人。我一直没有想起来，我只知道他在这里，在这个在夏天漂亮得不可思议的小岛上的人，只是我忘记了他存在的期限而已，从这个夏天开始，到这个夏天结束。</p>

<p>忽然我很自然地想起书上看到的一句话：七月里一片铁马冰河，八月等待，九月离开。</p>

<p>秦阳
<br/>谢菲尔德在冬天里弥漫着漫天雪花，温带海洋气候的冬天潮湿寒冷。我躲在这个英伦小国里回忆着鼓浪屿温暖的阳光。</p>

<p>2005年夏天，我去上了出国前的最后一个英语班，第一天，我赖床，差点迟到，用冲刺的速度爬了5个坡，然后用喘不过来的声音向一个老外问路，声音嘶哑，没有音准，然后一个路过的女孩子帮我进行了翻译，那一天我第一次遇见安小沫。安小沫这个女孩子就这么一心认定我的英语很差劲，还露出不以为然的笑。</p>

<p>我在课堂上看见安小沫在轻蔑另一个女生的白金头饰，尽管她带着笑。也许那家伙，根本就是鄙视着一切奢侈品而已。我望着自己穿的CK，忽然很讽刺地笑起来。</p>

<p>我忽然开始注意这个可爱的女孩子，注意她认真听课的神情和吃沙茶面的样子。我无德地用一天一碗沙茶面的价钱收买了安小沫当我的英文老师。我就这么样假装地让她认为，我是个没她帮助在国外连路都不会问的英文白痴。承认这样做是有点私心而已，只是因为这个女孩子，让我看了一眼，又看一眼，就让我收不回眼光来。</p>

<p>和她在一起日子总是过得很快，不过她走的那天我找个借口没有去送她，因为我欺骗她太多的东西，我又快要离开了，实在是没有时间解释也解释不来的。我就这样站着阳台上看着一辆辆船远去，忽然我想起我的借口已经用过了，真是失败。
<br/> 
我一直在扮演欺骗她的角色。我没有告诉她，我的英文很好，甚至比她还要好,根本不需要任何人帮我补习。我没有告诉她，其实我一点都不爱吃沙茶面，我陪着她吃，只是喜欢看她吃的东西的样子。我没有告诉她，其实我那件CK是真的，真真正正2840块钱的东西，我撕了它，只是因为她不喜欢这些奢侈品而已，不过说实话，那衣服质量真好得让我撕得手都在痛。我也没有告诉她，其实那天我没有去拿飞机票，我只是在她的的窗口下想了一整个晚上，我要怎么告诉她我喜欢她。我更没有告诉她，其实那天我拉她去教堂，我想告诉她秘密是我一直想让她告诉我用英文怎么问路，不过这条路，是通向她心里。
</p>
