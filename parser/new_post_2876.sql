<p>沙茶面肯定是一种平民的食物，很简单的2块5一碗，再加点其他的东西最多不超过5块。红红的汤汁里少许面条，再加点青菜油条，红绿黄搭配得蛮好看。不理会路过的班上的女生们很诧异的目光我依然埋下头吃得淅沥哗啦。我知道她们还是不会选择这种比较粗俗的食物的，她们宁愿千里迢迢跑到M记旁边的英式的咖啡馆里喝点下午茶，还有很漂亮的起司送。简单地提前适应国外的生活。优雅的让人不知所措，唯一美中不足的那里的窗子看不见漂亮的海景，只能看到一拨一拨的游客，大多数还戴着旅游的帽子拿着照相机。所以对我来说，沙茶面的选择还是要好得多至少我能看见很多很多老式的房子藏在郁郁葱葱地树阴里面，偶尔露出个漂亮的棱角来暧昧地招摇。</p>

