<br>　　光德里之金镶玉，开元年间名妓，色艺双绝，一时倾城，然不知以何故，归隐白云观为女冠，以琴操自娱，境界日进，人以“琴圣”呼之。以一妓而圣名之，不亦异乎？惜曲谱久佚，仙乐不再矣 
