	<span style="display:block;"><p>医院。</p>

<p>7月27日 主持人休息室</p>

<p>亲爱的宝宝：
<br/>医院。
<br/>你抵达这里以后，第一个过夜的地方。
<br/>很多婴儿都会跟你一样，先在医院住一段日子。但却从来没有听说谁就因此把医院当成了第一个家。
<br/>大家对医院都出奇的冷淡，没有听说哪个生产的女生偷偷在那张她分娩的床边刻下自己的名字;没有听说哪对情侣约会时带彼此去看自己出生的医院;没有听说谁把自己的病历张贴在征友的版面上;没有听说谁自己胸腔的X光片裱起来挂在房里。
<br/>我们这么多人在医院出生，但一点也不想把医院当成我们第一个家。我们有意无意地略过和医院有关的一切，觉得在人生的剧院里，医院应该永远被摆在“后台”。
<br/>我们会一辈子对医院保持警戒，每次进去都只想尽快离开，我们一点也不觉得亲切，也一点也没有回到儿时母校的感怀。
<br/>就这样保持冷淡，直到最后。是后，我们很多人又躺回医院的床上，但还是有几个人会固执地说：“让我回家，我要死在自己家里……”</p>

<p>我们既不肯承认医院是我们的第一个家，也不肯承认医院是我们的最后一个家。
<br/>我们真别扭。</p>

<p>KTV</p>

<p>7月28日 KTV包厢里</p>

<p>亲爱的宝宝：
<br/>KTV。
<br/>一个人们为了自己想唱歌，而不得不同时忍受着听别人唱歌的地方。</p>

<p>7月28日 KTV包厢里</p>

<p>亲爱的宝宝：
<br/>KTV。
<br/>现代的希腊悲剧：明明每个人都知道自己不可能比原唱者唱得更好，但每个人都还是要唱。</p>

<p>地球</p>

<p>7月29日 清晨，咖啡壶旁</p>

<p>亲爱的宝宝：
<br/>地球，你所在的星球。
<br/>以这颗球表面水和陆地占的比例来说，地球好像应该叫“水球”才对。
<br/>但因为人类住在地上，不住在水里，所以理所当然把这里叫做地球。你以后没事可以注意我们人类帮其他东西取名字的态度，看看我们多会以自己为宇宙的中心。
