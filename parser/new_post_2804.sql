	<span style="display:block;"><p>我为什么喜欢那个女生</p>

<p>7月24日 书架前的凳子</p>

<p>亲爱的宝宝：
<br/>理书理到一本《华氏451度》，是小说，说那个世界里，拥有书是违法的，家里有书一律烧掉。结果舍不得书的人，就纷纷沿着废弃的铁轨逃亡，大家聚在一起，渐渐形成一群怀抱秘密的人。他们彼此约定，每个人负责一字不漏地完全记住一本书，靠这样，把已经被烧掉的书，保留给将来的人。
<br/>于是，在那里的废墟之间，你看到《诗经》围着围巾在火堆旁取暖、《十日谈》在玩跳格子、穿美丽洋装在唱着歌的是《王尔德童话集》、正在烤鸡腿的是《希腊悲剧》。
<br/>你怀念哪本书的时卢，就去找那个“书人”，让他把那本书再次呈现在你眼前。
<br/>“我会想变成哪本书呢？”我忍不住沉吟起来。</p>

<p>7月25日 主持人休息室</p>

<p>亲爱的宝宝：
<br/>和你最亲密的那个女生，我为什么喜欢她？
<br/>先说我最没兴趣的一种女生好了：从小被保护到大，以自己为中心的公主。
<br/>这种公主，我小时候见过一些，长大以后继续见到。我其实不太懂为什么很多男生喜欢这些公主型的女生，我连在日本漫画或武侠小说里看到她们出场，都会不耐烦地加速翻过去。
<br/>没有错，大家都是娇嫩美丽的玫瑰，但对于偏激的我来说，娇嫩美丽往往是无趣的。公主的娇嫩美丽，必须或多或少地挽救这个烂世界，让世界再往“值得生存”的方向移动几公分都好。她的娇嫩美丽不能和世界无关，不能把烂世界映照的更烂更不堪。
<br/>我当然知道那种“与世界无关”的美。对这种美，我大概既不感动，也不相信。
<br/>亲爱的宝宝，等你长大以后，你所看到那个我喜欢的女生，很可能跟我讲的很不一样了。人和人的相遇都只有一段，我会错过我们，你也会错过你的。公平</p>

<p>宝贝儿呀，或许你的妈妈就是蔡康永叔叔（妈妈想，你出生时，应该是很久以后了）所讲的那种公主，那种从小被保护到大，以自己为中心的公主。但那并不是妈妈的错呀，因为没人能决定自己的出生。包括宝贝儿你，对吗？
<br/>但宝贝儿呀，你知道吗？公主并不都是娇嫩美丽的，也并全是快乐的。妈妈并不快乐，并不因为是公主而快乐，妈妈是个孤单的公主……在孤单得等待你的到临。</p>

<p>树。</p>

<p>7月27日 主持人休息室</p>

<p>亲爱的宝宝：
<br/>树。
<br/>看起来一直不理你。
<br/>实际上会一直给你力量。
