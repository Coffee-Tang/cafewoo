<p>蔡康永写给小小S的宝宝日记</p>

<p>开栏语：这世界每分钟都有新的生命到来。我想在迎接其中一个新生命的时候，也顺便跟最原来的我说说话，温习一下我最早对这个世界的期望。</p>

<p>宝贝，你会有个名字 </p>

<p>　 2005年7月15日　主持人休息室里
<br/>　　 亲爱的宝宝:
<br/>　　 在你还没有正式抵达这个世界之前，已经有人拜托了我，要我先跟你解释一下，解释一下世界大概是怎么回事情。
<br/>　　 我不应该答应这个请求。这世界怎么回事，我根本没搞懂。
<br/>　　 但是拜托我的人，是我很爱的女生，我不想拒绝她。
<br/>　　 那就让我试试看吧。反正等你到了这里，如果发现事情跟我说的不一样，再跟我说就好了。从哪里开始呢？
<br/>　　 名字，如何？</p>

<p>　　2005年7月16日　车子后座</p>

<p>　　 亲爱的宝宝:
<br/>　　 你会有一个名字。
<br/>　　 这代表我们这里有人在乎你，对你有期望。
<br/>　　 如果他们后来对你失望了，会不会变得不在乎你？
<br/>　　 有可能，但没关系，到那时候，通常会有别人在乎你。
<br/>　　 你的名字，还是会有人呼唤，那就够了。
<br/>　　 名字是给人呼唤的。如果全世界只有你一个，你就用不着名字。
<br/>　　 比方说，人类想像中创造宇宙的那一位，就没有明确的名字。一定是因为还没创造宇宙之前，翻来覆去就只有他自己一个。
<br/>　　 想想他也很苦，没有比他厉害的、也没有比他烂的；没有谁来看他脸色、也没有谁来给他脸色看。
<br/>　　 他连个名字都没有。
<br/>　　 他不创个宇宙，我看也没有第二条路可走。
<br/>　　 我们这边现在很多人喜欢嫌他造宇宙造得不够好，漏洞百出捉襟见肘的，我听见这些抱怨，还真为他觉得委屈。
<br/>　　 他哪知道他会造个什么东西出来？！
<br/>　　 没打过蛋的人，不用太低能也完全可以把蛋捏个稀巴烂。
<br/>　　 (亲爱的宝宝，哥哥我就是个活生生不会打蛋的人。)
<br/>　　 关于到底有没有创世界的造物者这件事，你那边应该比我这边消息更确实才对。我们这边有很多人说和他认识、跟他说过话，但是大家连他的样子都各说各话，有的不准你画他的脸，有的画出来却各不相同，留络腮胡子的也有，剃光头的也有。
<br/>　　 所幸他的名字倒是有好几个，有的用这个字母开头、有的用另外一个字母开头。如果当初他是因为没有名字而感觉寂寞的话，也算是押对宝了。</p>

<p>宝宝，你是最早有名的 </p>

<p>2005年7月16日 电视台咖啡厅
<br/>亲爱的宝宝：
<br/>要我跟你说话的那个女生，在我们这里，很有名。
<br/>也就是说，很多人知道她的名字。
<br/>你大概很难想像，定宝你也因此变得很有名呢。起码在跟你同时出生的所有宝宝里面，你是最早就有名的。
<br/>但因为你的名气并不是靠自己得来的，所以并不很可靠。如果有其他婴儿出生后一个月就会倒立，那他的名气应该有一段时间会盖过你。
<br/>出名很好吗？
<br/>说实话，还不错。
<br/>尤其是在你知道名气是怎么回事的时候。
<br/>人会想要被别人知道，应该是因为想要确定自己存在过吧。
<br/>问你一个有名的问题（当然你不必回答拉）。
<br/>深山里有一只小鸟，唱了有史以来小鸟能够唱出的，最好听的一段歌。唱完以后，小鸟就飞走了。
<br/>没有任何人听过这段歌声。
<br/>这段歌声，真的在这个世界上存在过吗？
<br/>我身边有很多人，因为不同的原因变成名人，他们暂时逃过“唱完了却没人听见”的测验题。他们的屎运不错。
<br/>（“屎运”不是很优雅的词，但跟你最亲的那个女生，是常常把屎尿屁挂在嘴上的，你也可以习惯一下。）
<br/>那如果一辈子都不出名呢？
<br/>像那个唐朝诗人写的，山里的红花，自己静静的开了，红了，静静的谢了，落在土里。
<br/>也许有一两只经过的鹿看见，也许没有。
<br/>你问我这样的的人生如何的话，宝宝，我已经没有资格回答这个问题了，我也是那批“屎运人”里的一个，我只能凭想像回答：“听起来也很美好”。
<br/>我没有资格回答的问题太多了，而且，我是常常凭着想像活下去的。</p>

<p>床和充电器</p>

<p>7月17日 床上</p>

<p>　　亲爱的宝宝：
<br/>　　当你像个小太空人那样，从你小小的无重力太空舱漫步而出的时候，会有几双手把你接来接去。
<br/>　　然后，你就会被放在一个东西上。
<br/>　　那个东西叫做床。
<br/>　　你如果知道接下来的人生，你会有多少时间躺在这个东西上面，你恐怕会忍不住撑开眼睛用力看它几眼。
<br/>　　我们会在上面，经历一些连大人也意料之外的事。有些好甜蜜、有些则令人悔恨以及一些甜蜜但终究令人悔恨的。
<br/>　　我们还会在床上做一些梦，像有个不甘心的人背着你在乱翻人生的抽屉，翻完了也不恢复原状就随手又把乱七八糟的抽屉给关上了。
<br/>　　床也会见证很多我们脆弱的时刻。有时只是太累，躺在床上仰望天花板，怀疑把自己搞这么累人生还剩什么意义。有时则是心碎、趴在床上哭。有时生了病，和自己的身体吵架却又没办法甩门一走了之。
<br/>　　床见到我们的时候，我们都这么像小孩。床会不会以为我们从出生以后，就从来没有长大过，然后有一天就躺在床上，死掉了？</p>

<p>7月18日 床上</p>

<p>　　肚脐眼。
<br/>　　亲爱的宝宝，那是你的充电插座。
<br/>　　比起手机所需要的充电次数，我们的性能算很不错了。</p>

<p>她真的什么都有 </p>

<p>　　7月18日 湖边</p>

<p>　　亲爱的宝宝：
<br/>　　 每一滴水，都有它出生的地方。只是当水滴遇到别的水滴时，它们就变成了海。每一滴水再也不必去认它的出生地。
<br/>　　如果水滴一定要在证件上填写“出生地”的话，很放松地写上“地球”两个字就可以了。
<br/>　　我们每个人也都会有我们出生的地方。我们和水滴不一样，我们大概会一辈子被辨认我们是哪里出生的。
<br/>　　你会出生在台北，一个我很熟悉的地方。
<br/>　　这个城市很多地方看起来随随便便的，跟我很像。这里常常有地震、台风，是我们的“大自然”。地震和台风严重的时候，真的很可怕，但家人和情侣，会因此有机会感觉彼此的依赖，很少城市的市民，像我们这样在恐惧中感觉甜蜜。</p>

<p>　　7月19日 空的咖啡厅</p>

<p>　　亲爱的宝宝：
<br/>　　 有人送我一件很紧的T恤，T恤的上面印着这行字：“我要当芭比，那个贱货什么都有！”
<br/>　　 哈哈，亲爱的宝宝，她真的什么都有，她只是没有生命而已。</p>

<p>宝贝儿，你相信吗？你等待10个月将要来到的这个世界上，真的有很多人认为什么都有，比生命更重要的。</p>

<p>　　7月20日 空的咖啡厅</p>

<p>　　亲爱的宝宝：
<br/>　　 人类做很多事来打发时间，看电视、喝咖啡、聊天、谈论很多和自己一点也不相干的人的事。
<br/>　　 看起来，我们真的有好多时间需要打发喔，那我们为什么还这么努力地要再活久一点？
<br/>　　 亲爱的宝宝，应该是：人生最令我们留恋的，都是一些我们也说不清楚的事吧。</p>

<p>　　7月21日 湖边</p>

<p>　　亲爱的宝宝：
<br/>　　　　听说有人在电视里面找深度耶。我好诧异。
<br/>　　　　电视很方便，但很肤浅，在电视里面找深度，太看得起电视了，太看不起电视没出现前的文明史了。
<br/>　　　　何苦看电视找深度啊？为什么不去看书呢？</p>

<p>　　7月22日从湖边回家的路上</p>

<p>　　亲爱的宝宝：
<br/>　　　　和你最亲的那个女生，跟我是因为电视才认识的。光凭着这一点，我就很喜欢电视。
<br/>　　　　我和她都是做电视节目的人。但我不会因为这样，就对电视放水。相反的，我们应该要比一般人更了解电视做得到的事和做不不到的事。就像养鸡的人，不应该假装鸡既会生蛋，又会打毛线。
<br/>　　　　电视只是吉卜赛算命师桌上的水晶球。我们透过它看到一些别人的事，就这样。
<br/>　　　　我们看到别人踢足球，但是我们自己瘫在沙发上。我们看到有人在打仗，有的房子被火烧，但是我们只有力气烦心我们的背痛和青春痘。我们关心一堆存在或不存在过的皇帝大官格格大侠煞有介事的活着，但是那些人永远不会关心我们，连看都永远不会看我们一眼。
<br/>　　　　我们见证各国人种在我们眼前抵死缠绵的恋爱，但我们自己好寂寞。
<br/>　　　　亲爱的宝宝，电视没有那么不好，电视只是让我们误以为好多人好多事都跟我们有关，却忘了提醒我们一声：
<br/>　　　　其实那些通通不是我们的人生。</p>

<p>我记得有人说过，我们每天到会花一半以上的时间去谈论别人的事，还会因此争执不断。宝贝儿呀，你以后就会知道，只有在妈妈的肚子中，才是最安静的。</p>

<p>　　7月23日 书架前的凳子上</p>

<p>　　亲爱的宝宝：
<br/>　　　　我又在丢书了。
<br/>　　　　不是我几本几本地丢，而是几千本的丢。
<br/>　　　　捐掉、分送、弃之不顾，都只是手段的不同，感觉是一样的，就是丢书。就当放他们去别的地方了。
<br/>　　　　以前不舍得的，这几年都舍得了，因为知道这辈子剩下的时间，看不了这些书，或者，不会想看这些书了。
<br/>　　　　“得到的时候，好珍惜喔……”翻着某些书，心里还是忍不住这样想，然后，默黙地把它放到标示着“不要”的箱子里。
<br/>　　　　和宝宝你最亲密的那个女生，习惯把我分到“读书人”的类别。
<br/>　　　　虽然有被简化的感觉，但她也没说错。我是很依赖书传递力量给自己。相对地，我刚常常把她归类为“妖女”。整本西游记里，唐三藏最愉快的，难道不是跟蜘蛛精共度的那段时光吗？
<br/>　　　　我很少拿书给她看。我觉得生活中向人推荐书，太干扰别人了。何况书和阅读者的关系很私人，旁人代劳，不太对得准。
<br/>　　　　更何况，我连自己和自己的书，都常常对不准啊。我看着一箱箱本来一心以为这辈子会读的书，只被翻了几页，就又被自己送走，送到下一个怀抱希望的人手上去，我虽然嘴上没有叹气，心里却感到生命的叶子，一片接一片的落下。
<br/>　　　　亲爱的宝宝，我们人哪，从出生以后，就不断被塞了满手的希望。机警的，会一路把别人硬塞给我们的希望随手丢掉，把手空出来抱自己的希望。不机警的，不这么抱着别人硬塞给我们的乖乖活下去，也没什么不可以，甚至也不见得比较不幸。
<br/>　　　　但是书啊，是我们塞给自己的希望，就算只是些妄想，割舍也不免惆怅。这，在还没出生的你看起来，挺傻气吧。
</p>
