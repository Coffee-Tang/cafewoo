<br>
<br>最后的一个刹那是什么呢？ 订婚前， 有一天我和艾伦在一起走。 那时我举棋不定。 我对艾伦说， 有人向我求婚呢。 他没说什么。 当时他好象是拿了一盘CD要刻， 不知道为什么在我的机器上放着听。 他坐在那里， 选了一首歌， 问我，“这首歌， 你听过的吧？” 我看了歌名， 是ALL 4 ONE的I KNEW I LOVE YOU。 我没有读出歌名的后三个字， 他站起来走开了。 那时公司里一些人在谈论艾伦和一个十八岁的女孩子在一起。 我见过她， 是个白净秀气的女孩。 我想起艾伦说他喜欢秀气的女孩子。
<br>
<br>就这样了。 九月二十二日我和世伟领了结婚证。 当天我没有回家， 跟世伟住在酒店里。 晚上他有一个电话会议。 我躺在床上， 不知为什么突然哭了起来， 越哭越伤心。 我知道， 是有什么东西被我远远地丢在身后了， 那是我再也不能企及的。 我好象是一下子就站在了河的对岸， 望着河的那一岸， 就象是望着自己的前尘往事， 再也回不去了。 
<br>
<br>艾伦， 在那一刹那， 我发现自己是爱你的。 爱你爱得那么心甘情愿， 爱你爱得那么小心翼翼。 小心翼翼到不肯轻易承认对你的感情。 
<br>
<br>然后又是那些梦了， 在以后很长的时间里纠缠我。 在无数个关于你的梦里， 我们隔着街。 我可以看见你的脸， 让我心痛的脸。 我向你招手， 唤你的名字， 一声比一声急切， 一声比一声远， 一声比一声的心碎。 你平静如水的脸庞， 我心里沉到深渊的痛， 没有人知道， 也无从说起。 你无法体会。 也许， 也许这一切都是一个叫做于哓哓的爱做梦的女子的错误领会， 她的另外一个纠缠不休的梦。 而属于你的， 是永远的平静如水， 不动声色。 
<br>
<br>我哭了， 在又一个梦到你的深夜里清醒过来后， 泪水打湿了枕巾。 
<br>
<br>如果你是一个和我一样每天都要穿过黄庄路口的人， 如果你够细心， 或是你无意发现， 在北边从西向东的路面上， 有一枚被嵌在了柏油路面上的钥匙， 铜质地， 黄灿灿地像是上世被谁遗失了的心， 孤零零地躺在那里， 卡在一个尘世的路口， 动弹不得。
