<p>《蓝色蛹  金色蝴蝶》
<br/>蓝色
<br/>－－流动的忧郁</p>

<p>   我想我是迷路了。
<br/>   城市里的道路迷迷转转，我却总也找不到出口；高高耸立的秘密的楼群将天空割离成一张破碎的脸。除了年轻，我什么也没有。
<br/>   母亲临死前冰凉的手指，父亲的叹息，后母的面孔……在我眼前飞转，织成一张网，我想逃，无奈逃不出去。
<br/>   我站在堤岸上看海，那些蓝色的波涛向我涌来。我深深的理解堤岸的悲哀：想远远退开，却又动弹不得。蓝色的忧郁从我的眼睛里流出，流过我的脸庞。</p>

<p>蛹
<br/>－－自缚的悲哀</p>

<p>   我拒绝那个女人介入我的生命。我无法将她当作我的母亲。
<br/>   我拒绝了这个世界，我当作整个世界都是不爱我的。
<br/>   我看过一幅画：一个孩子站在广阔的芦苇丛中，没有云，没有走出去的方向，只有寂寞来来回回穿行的声音。
<br/>   我也知道，我疏远了爱，疏远了世界。
<br/>   梦中，一只小小的蓝色蛹在轻轻哭泣。</p>

<p>金色
<br/>－－我爱上了世界</p>

<p>   今天，她带我去看日出。
<br/>   我看着金灿灿的阳光一点点将海水忧郁的蓝色吞没。她送给我一张CD－－《New Baby Born》，我不知道如何翻译，她告诉我是：“新生的孩子”。
<br/>   她在晨光中对我微笑，握住我的手，对我说这个世界其实很美，有很多人在爱我：我的父亲，在天堂的母亲，还有她。
<br/>   我转过身去，悄悄擦掉眼泪。</p>

<p>蝴蝶
<br/>－－破茧后的美丽</p>

<p>   我感到背后隐隐的痛，转过头，惊奇地看到了一双金色的翅膀。
<br/>   原来，我正在一步一步接近爱。亲近美好，才会认识美好。
<br/>   当我冷眼旁观时，生命是一只蓝色的蛹；当我热情相拥时，生命是一只金色的蝴蝶。破茧而出的过程，也是感情变化的过程，亲近世界的过程。
<br/>   用爱的眼睛看世界，我也如此美丽！</p>
