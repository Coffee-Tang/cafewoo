<p>《心灵的选择》 </p>

<p>　　汉代著名学士许慎一日同众人游玩，尽兴之时，中人均觉得饥渴难忍，这时，几棵果实累累的梨树突然映入眼帘。梨树长在路旁的院子里，树旁紧锁的房门和门上的灰尘说明主人已经很久没有来过了。很多人一拥而上，争先恐后吃这又大又甜的梨。有人劝许慎也去摘梨吃，许慎却不为所动。人们说：“为什么不呢？反正这梨树没有主人。”许慎答道：“梨无主，而我心有主。” 
<br/>　　好一个“我心有主”，多少人做了“心灵的选择”的最好诠释，激励了多少仁人志士为自己的理想奉献终身！ 
<br/>　　是的，古往今来，有许许多多像许慎一样的英杰为我们诠释了“心灵的选择”。 
<br/>　　楚国诗人屈原忧国忧民，为了自己的祖国能够繁荣兴盛而不日益衰败，何不开明的君主抗争，最终投江而死；布鲁诺为了维护科学真理同封建天主教会抗争，最终竟被活活烧死。“包青天”包拯为了“正大光明”，不畏权贵，一心为民；德兰秀女以她广博的爱心为各种病人、孤儿、穷人服务，终生不悔…… 
<br/>　　诺贝尔选择了为科学奉献自己，即使被炸药炸伤了自己无数次，也要继续自己的研究；居里夫人选择了努力钻研来报效自己的祖国，她把自己发现的第一种放射性元素定为“钋”，寓意自己的祖国波兰；董存瑞选择了为中国的解放独立事业奉献了自己的生命，手托炸药包炸毁了敌人的碉堡；林肯选择了为了美国国土的统一、黑人的解放而非心尽力，以致树敌众多，最终被黑奴狂热着暗杀；雷锋选择了为人民服务，奉献他的所有热心爱心；许虎选择了“天下人之事即为我之事”的观念，不嫌脏不嫌累…… 
<br/>　　这就是他们的选择，无论前面有多难，有多苦，他们都会勇往直前。 
<br/>　　朋友，你做好心灵的选择吗？ </p>

