<p>“是不是这样的夜晚你才会这样的想起我”，都知道这首歌吧，也都只会唱这一句吧？很少人知道这首歌的原唱居然是吴宗宪－－这个现在整天在台湾综艺节目上搞笑的主持人。好象之前我从来没完整听过这首歌，印象最深的是初中时候看的一片录像里，学生每次骑着自行车载漂亮的女老师回家，老师下车后，他径自就骑走了，嘴里哼着就是这句“是不是这样的夜晚你才会这样的想起我”。再然后呢，每次去KTV唱歌，这首歌永远列在所有点歌本的最后，因为它名字的那个长哟。</p>

<p>那天早上这句歌词莫名其妙地就跑到了脑袋里，于是一整天嘴里就哼着这一句了。确实是莫名其妙，因为它与当时的事情和心情毫无关系。办完事后，已经下午了，约了朋友出来吃饭，再和他一起逛逛街，感受一下这个城市...在街边一个小小的音像店里居然看到有这盘CD，居然就看着CD笑出声来，觉得它似乎就是在这个陌生的城市等我的。</p>

<p>9点多的时候和朋友一起去了网吧----其实说不上是网吧，只是在一个挺大的咖啡屋的一角摆了几台电脑给人上网。咖啡屋里有人在用小提琴咿咿呀呀地拉张惠妹的曲子。一边上网聊天一边听着她频繁走调的表演，忍不住对朋友说了一句，“这个要拉到什么时候啊？”没想到拉的人不专心表演，竟然听到了客人的声音，于是一曲终了，再不见人影。</p>

<p>没有音乐也不好吧。我对咖啡屋的经理说“帮忙放一首歌吧，就这首就好了。”她接过CD看着，突然冒出一句“好好听啊！”，之后的两个多小时就在吴宗宪的声音里过去了。也只是一个很普通的声音而已，但是，有“是不是这样的夜晚你才会这样的想起我....”</p>

<p>离开的时候，听到经理在身后和别人说“这么好听就没了.....”。呵呵。</p>

<p>“是不是这样的夜晚你才会这样的想起我？”，很好用的一句话。这样是什么样？可以套到不同的人的记忆里去吧。这样的想是怎么想，不同的人也有不同的想吧。有的时候确实会突然想到一个人，就象这句歌会突然跑到心里一样，就象突然想起下铺的良斌那个憨里带奸的笑容，突然接到向榕打来的一个电话。</p>

<p>看着我们和营销班的留言里都在说毕业一年了。一年多的时间里，除了和留厦的同学出来玩之外，这样或者那样想起同学的时间也真是很少。你们呢，也是这样吗？</p>

<p>这样的夜晚，去白城的海边坐着，不知道会不会想起一年前那个沙滩上的通宵，破晓时的红霞满天，和冯志华那语重心长的叮嘱.....</p>

<p>呵，这样的夜晚，这样的想念。</p>
