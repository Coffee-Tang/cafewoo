<br>
<br>驼铃。扬沙。荒漠。
<br>天空昏黄。
<br>帐幔外的景色一点一点变得陌生。荒凉的乱石堆和沙砾，稀疏的灌木丛，野草杂芜。空气里冬天的气息已经很深了。狂风一拔一拔地在空旷的黄土上肆意地穿行。扬起漫天的尘土。
<br>马车颠簸得厉害。手腕上的银镯子一直发出叮叮铛铛的声响。乳娘搂着我轻轻地哼着曲。偶尔有沙砾从帐幔的缝隙里飞进来，打在脸上生疼生疼。我掀起帘子的一角，看到那座我生活了十四年的城池在背后变得面目模糊，风沙狠狠地掠过去，它伫立着，寂寞而苍凉。我已经看不清它的样子了，连弥漫着奢靡的空气也已消失殆尽。
<br>我合上帘子，靠在乳娘的肩头。我轻轻地说，乳娘，你终于能带我离开了。
<br>驼队很缓慢地前行。乳娘握着我的手不说话。阳光漆黑而明亮地洒进来，我看到乳娘的眼里有很沉的忧伤。
<br>
<br>
<br>二
<br>
<br>我叫花葬。出生在三月。娘亲说我出生前的一段时间里她总是有重复而纠缠的梦魇。旷远的黑土，空气中潮湿的味道，天边隐约的绝响。不断闪现的容颜模糊的女子。粉红粉白的花瓣从诡异的空中破裂开来，纷纷扬扬地坠，然后慢慢地侵蚀和倾覆。流离失所的苍茫。这些都是后来乳娘告诉我的。娘亲在我很小的时候就已经离开了。乳娘说那是一场没有归期的旅途。
<br>娘亲把那青苔样暗淡和恍惚的忧伤刻进了我的名字，像掌心中沿用三生的胎记。我轻轻念它的时候仿佛能看见三月里桃花浩浩汤汤地漫天飞扬，无所依傍的茫然无措。而乳娘总是一遍一遍地唤我，花葬，花葬。像是延绵不绝的死亡。
<br>
<br>从我记忆开始很清楚的时候开始，我就生活在一个有大朵牡丹花的城市。每年牡丹花开得繁华而忧伤的时候，我生活的红墙里头就会飘散满浓郁的香气。那些香气和后宫嫔妃们的胭脂味在空气里一丝丝地纠缠，然后变得很迷离。皇宫的后院里有太多郁郁寡欢的美丽女人，她们浓妆艳抹，在花园里无所事事地等待衰老，彼此间高傲地抬着头。而她们的眼神总是游移的，她们抓不住能让自己活得光鲜的那个男人，于是变得歇斯底里的敏感。乳娘说娘亲和她们不一样。娘亲永远是美丽的，很柔软很干净的美丽。
<br>我是这个国家的公主，一个被遗忘的公主。在红墙的院子里一天天长大。乳娘常常会在暮色苍茫的时候给我讲关于娘亲的往事，我总是仰着脸很认真地问，乳娘，什么时候我也能像娘亲一样离开。乳娘的眼神在一刹那变得忧伤，像牛湿润的眸子。大片空荡荡的疼痛摊在我面前。从小我就知道了什么叫无可奈何，什么叫无能为力。那些被岁月抹煞了的往事啊，陈旧得如乳娘指间的念珠，泛着黑色的光泽，檀香木的气息淡淡地散发出来，久远的样子。它们锐利地一下一下刺痛我，而我只知道所有故事那端系着的女人，是我的娘亲，一个很早以前就从我生命里开始退出的美丽女子。
<br>我的父亲是这个国家的王。他是个懦弱的男人。我总是看见他所宠爱的妃子在一段时间后就消失匿迹了。偶尔会有死讯在其他妃子间流传。于是宫里有越来越多陌生的面孔，而那些面孔总会丧生在另一张更会妖冶的面孔的权势之下。父亲所喜爱的东西注定会被那个更为恶毒的女人所扼杀。
<br>
<br>我在红墙里狭窄的天空下一天天长大。乳娘教我绣花和舞蹈。乳娘曾是这个城里绣红的好手，在大红的绸缎上绣些粉红粉白的桃花。那些花在乳娘指尖小朵的安静的盛开，不喧哗。而我喜欢绣一种叫藤脂的白色小花。一种关于佛教传说中没有怨恨平淡地等着自己转世的花朵。桃花总是让我想起娘亲，想起那个诡异的梦。梦里桃花沸腾，女子的容颜和年年岁岁被凋零的花瓣湮没。一泻到底的荒凉。我始终觉得那个隐约的女子是娘亲和我的影子，有那样宿命的死亡气息。决绝的美丽。
<br>深夜的时候，乳娘替我梳理那顺滑如缎的长发。她说我长得越来越像我的娘亲了。我看着昏黄的铜镜中自己模糊的容颜，想像一个同样容貌的女子怎样白衣飘飘地舞，舞，舞。乳娘说娘亲的舞跳得极好，在风中像艾草样被抚成各种形状。当年父皇就是看中了娘亲的舞和美貌才那样宠爱她的，可惜……每次说到这里乳娘就打住了，愣愣地看着远处出神。我不问她就一直没往下说。
<br>我不问是因为我知道乳娘不愿说，从小生长在宫里我很清楚不该知道的东西就一定不要问。这是生存的规则。有些人就是太贪心了才会丢掉性命，比如那些自恃的受宠的妃子。因为这个国家真正掌权的不是那个穿着龙袍唯唯诺诺的男人，而是帘珠背后那个笑容阴险的女人。
<br>
<br>我常常在自己的衣服上绣一些藤脂。
<br>藤脂是带着我的记忆的。它就像我喜欢的那种只有在人烟稀少的地方才会出没的青鸟。忧伤。记得曾有人向父皇进贡过一只青鸟，关在精致的笼子里，眼神黯淡，那些风里的明亮退到了最深处，羽毛没有光泽，声音不再水波般荡漾。我看着它心里疼痛。我觉得我的前世就是一只青鸟，今生却被禁锢了，被精致的物质包围了，没有出口。几天后，青鸟死了。眼是开着的。我不知道这是不是所谓的死不瞑目。想起乳娘的话，青鸟是不属于这个尘嚣的世界的。我为青鸟做了个墓，却没有眼神可以温润它的尸体。我知道，我的那些还没有打开的年华，注定了会像孤魂野鬼样飘荡飘荡没有落脚的地方。
<br>青鸟是我见到的第一只鸟，也许也是最后一只了。而对于父皇而言，他只是少了一样可以耍乐的道具，无关悲喜。红墙里或苍白或昏灰的天空总像发着炎，从来没有鸟群飞过的痕迹。我常常听见它们潮水样扇动翅膀的声音从我不曾触摸过的天空里掠过，然后飞远。
<br>我很安静地长大了，不再问关于娘亲的往昔，不再想着像鸟样拥有自由。我知道自己是没有未来的孩子，就像那只青鸟失去了方向。
<br>
<br>有一年秋天，宫里看似平静的生活起了波澜。父皇最宠的妃子被处死了。很残忍的手法，明晃晃的刀光剑影划出长长的伤口，柔软的身体被砍去了双腿和双手，套着白色的衣服装在黑色的罐子里，头上的血凝固了大块大块地掉下来。乳娘说那叫人棍。
<br>一天夜里我问乳娘，这样的日子什么时候才是尽头。乳娘抱着我叹气，身子却在微微地颤抖。乳娘曾告诉过我，人死了以后会变成很深很深的紫，看起来像是黑色，就像那些陈旧的念珠的颜色。那是人积累了一生的压抑。
<br>我突然有一种预感。它像暗黑的小巷子里脚步激起的回声样回响不绝。
<br>
<br>一个深夜，乳娘突然被父皇一道暗旨召去了。天很黑，月影摇曳，风过竹梢发出飒飒的声响。屋里的灯盏被吹得明明暗暗，鬼魅一样。我坐在床棱上轻轻抚着那串黯黑的念珠，上面有乳娘指尖的温度。
<br>子夜的时候，乳娘很轻地推门进来。她在我身边坐下，表情凝重地握着我的手。花葬，早点睡吧。有什么事吗，我问。乳娘摸摸我的头发，三天后，我们就要离开这了。我看着乳娘，很平静。是父皇的意思吗。乳娘别过头，花葬，快些睡吧，不要问那么多了。我很顺从地点点头，躺下。乳娘准备起身的一刹那，我拉住她的手。乳娘，我轻轻地问，娘亲在哪呀。是不是娘亲早已死了，和她们一样。乳娘怔怔地回过头，眼泪突然大颗大颗地掉下来，在手背上破成荒凉的样子。乳娘说，花葬，你还是想到了。
<br>我的预感突然就被证实了。
<br>乳娘从柜子里拿出一个精致的木盒子，手巍巍颤抖着打开。里面是块绸布，包着一撮青丝。乳娘说，这是你娘亲唯一的遗物。我轻轻地抚摸着它，像抚摸着娘亲毁掉的容颜。这一刻我感觉自己是如此地接近死亡。落满紫藤枯叶的古井里一点一点失去光线和水分的女尸，长发披散悬在横梁白绫绡上没了呼吸的女尸，怀了父皇的孩子却被施以肉刑的女尸，被砍去了手脚做成人棍的女尸，还有乳娘说的娘亲样深知父皇的怯懦和无能，毫无怨言地走进死亡的女尸。地里漫出阴郁的气息，我看着它森然地上升上升像看着自己即将长大的年华一样不安。我突然明白了女人的美丽从一开始就不属于自己。要么孤独地美下去，一直到那种美丽枯萎得万劫不复，被人们遗忘，就像乳娘。而另一种美丽却注定会带来死亡和灾难。我轻轻地对乳娘说，如果有一天当我们不再需要压抑的时候，我们已经崩溃了。
<br>说这些话的时候，我看见了那年冬天的第一场雪。纷纷扬扬地洒下来，没有声音。
<br>暗潮汹涌。
<br>我在雪地里起舞。雪花狂沙样从我的指缝间穿梭过去，在掌心刻下一道道印痕。冷，红墙里的谄媚变淡了。天地间素净得像我衣服上绣的藤脂。我想像着娘亲柔软的身体在起舞时是怎样倾国倾城的美丽。旋转，旋转。我取下头上猩红的羽饰，掀起的长发像泄露的往事样漫天飞扬。
<br>华丽的雪，华丽的衣裳，华丽的那个年代的花香，一点一点被磨损。皇后说，母亲柔软的身体是有罪的。毁，毁，毁。
<br>一瞬间，所有美丽的圆润的谎言灰飞烟灭。
<br>
<br>
<br>三
<br>
<br>驼队带着我残损的幸福走，走，走。
<br>那一年我十四岁。
<br>行路一个半月。
<br>乳娘带着我到了一个叫若羌的国家，靠近楼兰。很矮的土墙，有颜色明亮的花，漫山遍野地开，在风中掀起一片华丽的色彩。进城的时候看见很多眼神纯朴的人，脸上有风掠过留下的一道一道的印痕，蜷缩着时间的记忆。
<br>我和乳娘在城郊住下了。这里很干燥，尘土飞扬。乳娘用黑色的面纱蒙住我的脸。她说，过了这一刻所有的往事所有的爱恨都要忘记了。我觉得我和乳娘就像两只被遗忘的鸟，孤孤单单的存活。乳娘说，其实鸟死的时候眼睛就是一直看着天空的。它只是想知道它前世是什么，这世记住了，来生就可以避开那些曾经伤害过自己的影子。一直到安静的死，没有眼泪，没有仇恨。
<br>
<br>日子很平淡的过去。乳娘每天绣花然后拿到集市去换取一些东西回来。偶尔狂沙来的时候，我就把手伸在风里感受沙砾的流动，阳光冰冷地照下来，打在脸上像伤口样明亮。我总是会在夜幕降临的时候看见往事的忧伤长了翅像鸟一样在空中远远近近地飞，不停歇。可是乳娘说过的，要将它们忘记了。忘记那些谢去的年华，忘记那些残缺的仇恨。
<br>
<br>这年秋天，我们突然收到了远方的噩讯。
<br>皇叔篡位。大批的士兵涌进宫里，熟悉的面孔彼此撕杀，用冰冷的兵器结束一批批身世潦草的生命。温热的血喷溅出来，干涸，空气里弥漫着甜腥的味道。那些穿着紫色官服，胸口有麒麟奔腾的大臣们依旧念着祈福的话，用横尸堆砌起另一座象征着神圣和权力的龙椅。父皇被打入了天牢。
<br>乳娘说要回去看看，很坚决。
<br>临行的前一个晚上，我和乳娘一起睡，像小时候一样。乳娘在铜境前坐下，取下头上的羽饰。头发散开的一刹那我看见那些黑色下面竟然埋葬了那么多的白丝。那个搂着我哼歌，轻轻地唤我名字的乳娘就这样老了啊。一瞬间我觉得自己像在告别，告别曾经刹那芬芳的幸福，告别那在乳娘的手间长大的灼灼年华。
<br>我知道乳娘这一去，也许就是诀别。
<br>躺在床上。乳娘突然说，花葬，你要好好地过。只有三年了。我拉着乳娘的手突然握紧了。乳娘看着我微笑，眼泪顺着眼角滑下来。这一刻我终于明白，为什么乳娘叫我忘记过去。若羌太干燥了，眼泪还没掉下来，就会被风干。
<br> 这一晚乳娘的话很多。我终于知道父皇是早就定了要把我嫁给楼兰未来的王的。在我十七岁那年。而乳娘早早地带我离开，只是因为父皇意识到我再也无法安全地在皇宫里生活。
<br>我恍然大悟，想起离开前不久在花园里和父皇、皇后的偶然相遇。父皇看见我的时候很震惊，眼神在一贯的奢靡和颓唐中变得温暖。他仿佛看见了自己年轻时候爱过的那个影子。乳娘说过的，我比娘亲更漂亮。父皇的脸上荡漾着对往事的无能为力，身为一个王的全部的忧伤就那样写在他的脸上，像宫中潮湿的纠缠的胭脂味一样。父皇问我，你会跳舞吗。我点头。他说你跳。阳光晃动，我的身子像鸟样轻灵，那些寂寞的姿势，还有敞开的疼痛。他看着我好像看见他无力保护的那个女人，同样还有这个他无力保护的女人的孩子。
<br>我生命里的最后一些舞蹈，跳给那个叫做父亲的人看，还有那个在一旁始终笑容妖媚眼神恶毒的女人。
<br>乳娘走了。她说，花葬，留在这里，等我回来。
<br>
<br>二年很快的过去。乳娘始终没有回来。
<br>
<br>第三年的春天。从集市回来的路上看见寒冷的阳光停止在一个潮湿的角落里。一只青岛安静地停在一个人的掌心。翅膀收拢了，眼里是满满的明亮，没有一丝惊瑟。那是个干净的男子，没有若羌人尘土飞扬的干燥。我走进那块潮湿，问，为什么它没有睡在风里。他看着我的眼睛，微笑。
<br>他牵着我的手上马。然后把我带到一个偏僻的地方，那里有大片的桃花林，开得浩浩汤汤。我喜欢坐在树上摇晃着树枝，看温暖起来的风夹杂着花瓣落在我的黑色面纱上。
<br>我想这些树冷了一个冬终于等来这一场繁华，而我终究也等到了一个爱我的男人将我的面纱掀起将桃花插在我的长发上。那种爱温暖而平淡，远离杀戮。
<br>我喜欢这个男人。他将我的面纱扬起的时候我看见他的眼里充满疼惜。
<br>他叫刹。很短的一瞬。
<br>我在桃花林的小屋里度过了两个月。我几乎没有语言把我的事情完整的讲出来。也许是没有勇气回头，也许是我不知道从哪里开始。我只是在深夜给他讲一些破碎的往事，讲乳娘的苍老，讲我的等待，讲娘亲的那个梦。
<br>我说，我叫花葬。桃花的花，埋葬的葬。
<br>也许这两个月是我一生中最美好的时光。可我终究是要回到我既定的命运里去的。刹的手无法承受起那么多的往事。他需要的只是一个没有背景，没有阴暗的女子，和他一起平平淡淡地过日子。这是一场游戏。而从一开始，我就丧失了参与的资格。
<br>
<br>二个月后，我嫁给了楼兰未来的王。
<br>刹在殿堂外熙攘的人群里看着我，眼神清澈而忧伤。青鸟依旧安静地停在他的掌心里。突然想起乳娘话，青鸟是代表传说中的幸福和随后而来不可言语的悲伤。
<br>我看着他，轻轻地说，梦。刹点头。
<br>我知道一切就这么结束了。
<br>转过头，仿佛看见一个白衣的美丽女子在半空中平静的微笑，长发飘扬。粉红粉白的花瓣从空中破裂开来，降，降，降，尘封起所有的过往。
<br>就让那些仇恨，那些忧伤都溃烂掉。没有诉说，没有回望。
<br>三月走远了就永远地去了。花葬结束了就留在历史里了。
