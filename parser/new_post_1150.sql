花音觉得有些眩目。那笑容让她迷醉。

（四）

我居住的地方没有地球那么美，虽然科技很发达，但整个星球却很冰冷，不是指气候，而是人与人之间。星球上大部分的东西都是人造的，很精致但也是冰冷的。不过还是有地方是温暖的。那就是我住的房子。我每次到地球都会带一些有我喜欢的味道的东西回去。上次我还带了些花的种子回去，在我来之前都开花了，和地球上的一样漂亮。

懒懒的太阳照着草地，两个人躺在蓝天下，邦给花音讲那个遥远星球的故事。

“那你这次回去想要带走什么？”花音很讶异自己在讲这句话的时候为什么心会整个抽紧。

邦又用那种专注的眼神凝视着花音。

传说中，在一个午后，有一只蝴蝶在阳光下舞蹈，正当她扇动美丽的翅膀快乐地舞着时，一滴松脂落下来，一个灵动的生命被永远禁锢了。花音想，如果邦的眼睛是琥珀的话，那么里头被禁锢的一定是一个鲜活的自己。

“我第一次踏上这个蓝色的星球时，觉得像是被一股奇怪的力量牵引着，好象这里有什么属于我的东西。我一直都不知道究竟我想从这里带走什么？遇见你之后，我知道我想带走的是我永远都带不走的东西，爱情。”黄昏的暮霭下，邦始终望着天空，他的忧伤遮蔽了整个苍穹。

花音也望着天空，看天边的云朵一朵朵被夕阳染红，就像她现在的脸颊。

“我们相见的那天是我的生日，我想，你一定是上天赐给我的礼物。邦，让我们相爱吧。”

邦愕然地看着花音，她的眼中透出不容置疑的光芒，挂在脸上的笑绚烂并且坚定。

在新鲜的青草香中，邦吻了花音，有他喜欢的味道。

邦的吻有种温柔的魔力，像宇宙中的黑洞，神秘的力量深深的吸引住花音。

（五）

没有谁主动提起关于离开的话题，他们只是绝望而热烈地爱着，每一天对于他们来说都可能是相守的最后一天。

平安夜，天气很好，邦说要送一个圣诞礼物给花音。

一次飞翔。

邦紧紧地抱着花音，在黑色天鹅绒般的夜幕下，他们在星星间穿行。花音从来没有想过自己会离天空那么近，仿佛一伸手就能摘下星星。城市的灯光都暗了下去，整个城市都睡着了。没有人知道，今晚，在这个城市的上空有一次飞翔。

花音双手搂着邦，幸福地笑着，“听着，我要嫁给你！不管我们有没有明天，我要做你的妻子！”她的脸上有着和他们接吻那天一样的神情。

满天的繁星作证，他们互许了终生。

坐在阳台的平台上，邦安静地听花音演奏约翰·丹佛尔的《Country Road》。自从邦来了以后，她已经有很久没有拉过小提琴了。

听说，平安夜里每个人都是被祝福的，花音希望这是真的。

（六）

邦的飞行器已经修复了，他知道这预示着分离的日子即将到来。

他最害怕的事情终于还是发生了。那天夜里，邦的组织找到了他，没有选择，他必须得离开，立刻。并且两人的这段记忆都将被洗去。他们绝对不允许不同空间的两种生物相恋。他们要将这两个月的回忆从邦和花音的记忆里抹杀得干干净净，连一点尘埃都不会遗落在宇宙里。当然，邦还有另一个选择。

临走前，邦深情地望着熟睡的花音，耳语般告诉她：“我会化作一阵风，回来看你，然后，陪你到老。”

花音从梦中惊醒，她不明白，为什么自己脸上会有未干的泪痕，为什么自己的心会莫名其妙的疼痛，为什么耳朵像火烧般滚烫。

邦走后不久，就选择了死亡。不管他们给了他多大的包容，甚至愿意既往不咎。他还是选择带着和花音那段凄美的爱情死去，因为，他不容许自己忘了花音，他的妻子。

邦走了，花音也恢复了从前的生活，一切都很平静，就像轻轻地刮过一阵风，除了一瞬的颤栗，什么都没留下。只是她再也没有拉过小提琴，因为不知道为什么，一拉动琴弦，她全身就开始颤抖。

窗台下的三色堇还是灿烂地盛开着，可是花音的心却已经是一朵枯萎的花苞。

（七）

第二年的春天，在一个镶满金边的午后，花音独自坐在窗前，像故事开始前一样，安静地看书。

突然，一阵风吹开了窗子，温柔的，久久的停伫在她身上，她周围。

花音困惑了，那无以名状的熟悉感，那心深处微微的刺痛，那沉沉浓重的哀怨……

她深吸一口气，麦香茶的香味在空气里漂浮。

公车投币箱里硬币清脆的声音。

夜幕中满天的繁星，两个人在空中飞翔，其中一个是自己，那么另一个是谁？

熟练的架上小提琴，拉弓，曲子很美，音符跳跃着，有一张干净而明亮的笑脸。

不知不觉的，花音泪留满面，她的记忆在一点一点的复苏。

邦的话一遍遍地在空气中回响：“我会化作一阵风，回来看你……”

他回来了，真的回来看她了，他没有忘了她。那阵风久久的盘旋着，在花音身边安静地流动。

风，渐渐变小，直至完全消失，邦走了，他给花音留下了幸福。

（八）

许多年后，花音有了一个美满的家，一如邦所希望的，她过得很幸福。

经常的，花音会觉得迎面吹来的风很熟悉，她知道，那是邦又回来看她了。他仍然守护着她，永远。

她温情地笑了，像阵和煦的微风，轻声低喃：“我很好，真的很好。”

“五十七年前，当我第一次遇到花音，我就知道，她是我一生逃不开的宿命。现在，我还在她身边，守护着她的幸福。”

――邦

