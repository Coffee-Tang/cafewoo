我想，我应该先做一下自我介绍，我是老欧。

这个最后宣判彻底击垮了卓梓。

后来，老欧告诉卓梓，其实欧石楠的花语是孤独，在E·勃朗特的小说《呼啸山庄》中，孤独的辛斯克力夫就葬身在开满欧石楠的荒野上。那天被拒绝后，他就进了这批花茶，原来是为了记录心情，没想到会有出人意料的结果。

老欧的脸上难掩喜悦。

（六）紫罗兰茶*Blue Mallow*

