（二）薄荷冰茶*Pepermint*

今年的夏天似乎特别的炎热，人的情绪也容易烦躁不安起来。昨天快下班时，卓梓竟然和上司吵了起来，平时她总能忍的，可不知为什么，当时竟没忍住上司的无理取闹，也许是因为天气吧。

今天她干脆请一天假不去了，想到总是趾高气昂的草包上司手忙脚乱的样子，她就憋不住笑意。难得有一天的空闲，没有死党的聚会，没有同事的逛街，窝在家里睡觉又似乎太对不起这个革命日。

于是卓梓决定一个人出门晃晃，她去了田园。

