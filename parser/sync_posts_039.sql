<br>Vol.7 往事 
<br>因为我和桃子只有半年左右的时间，所以我们格外珍惜我们在一起的每一分钟。 
<br>我在寝室的时间少了，丸子正好一有空就用我的电脑玩游戏。--觉得丸子堕落了 
<br>大学快上了三年半了，这三年半里，我们三个人中，变化最大的应该属丸子了。刚上大一的时候，丸子第一次走出那个北方的小城，还不会讲普通话，所幸那一口北方口音大家都能听懂。那会儿，丸子给大家的印象就是一个纯朴腼腆的北方男孩，认真地做好每一件事情，认真地上每一节课，每天准时大便（我也会每天准时去拉丸子正在蹲着的那个厕所的冲水绳子）。 
<br>那时候丸子的兴趣也不多，象棋和看书。经常看见他手捧一本棋谱，自己跟自己下象棋。看着这个阵势，我一直很怵，觉得丸子象棋一定很厉害。所以，偶尔丸子研究棋谱腻了以后会找活人陪他下棋，而我看到他有这个倾向的时候，就赶紧往床上逃，说好累啊，睡觉了--我可不想被吃的一个字都不剩，要知道我小学毕业以后就没再碰过象棋。 
<br>当然，躲得过初一躲不过十五，终于有一天我起床的时候被丸子看见，不好意思推却，就陪他杀上一盘。毕竟好几年没碰象棋了，我都忘了开局该怎么摆了，丸子帮我摆好以后，我们就开始下了。我说丸子你要让我啊！结果第一句我赢了。丸子说，第一句先让你赢，第二局我就要认真下了阿。结果第二局还是我赢了，我说丸子你又让了我一局阿？丸子嘿嘿一笑，说再来再来。第三局还是我赢，丸子脸色开始难看了，说再来。结果那天一直下一直下，丸子的脸色越来越难看，就是没赢过。最后我实在想吃午饭了，就偷偷拿走了自己的“车”，就像刚刚一开始就老偷偷拿走他的“车马炮”一样，丸子终于赢了。我来得及吃上了午饭，丸子从那天起就再也不和活人下棋了。当然我至今也没有告诉过丸子那天为什么他会输那么惨，以丸子的性格是绝对不能容忍下棋作弊的。六年级的时候我下棋偷棋子的手段达到了登峰造极的地步，以至于大家都不敢和我下棋了，这也是为什么六年级以后我就没下象棋的原因。 
<br>后来，丸子不再下象棋了，连自己跟自己都不下了，我感到很内疚。丸子迷上了乒乓，这是我内疚的心理好过了点。 
<br>打乒乓的人总是很多，于是为了最大限度利用资源，总是会双打。有一天丸子打完乒乓以后回来就开始魂不守舍了，问了和他一起去打乒乓的山贼才知道，原来那天和丸子双打的那个女孩子，和丸子配合得十分默契，而且后来发现那个女孩子还是丸子一个省的老乡--丸子的一见钟情。 
<br>于是丸子一有空就去打乒乓，与其说是打乒乓，不如说是占乒乓桌。占到了乒乓桌就等他老乡过来--之所以只说是他老乡，是因为那时候丸子还不知道她的名字。 
<br>接下来的几天，丸子总是一脸笑容，我们说丸子，请吃饭吧，丸子二话没说就会爽快地答应。每天我们都能知道一点新的资讯，知道了那女孩叫娓娓，知道了她除了打乒乓球还会打羽毛球甚至还在运动会上扔过铅球。 
<br>丸子的笑容并没有持续多久。学期快结束的时候，我们三个一起去买饭，一边排队还一边嘴巴里嘟囔着中国革命史的一些内容。忽然丸子的饭碗掉地上了--��！山贼脱口而出：《醒世钟》；前面的人都回头看，山贼又脱口而出：《猛回头》。一看，发现丸子嘴巴张得大大的，眼睛也睁得大大的。顺着他呆滞的目光看过去，发现了娓娓，正在和一个男生互相喂饭呢。 
<br>这个打击对丸子来说是巨大的。他整整一个礼拜没有洗脸，没有刷牙，没有洗脚，整天躺在床上，饭都是我们帮他买的，连碗都是我们帮着洗的。终于一个礼拜后丸子从床上起来了，说要自己买饭。我们都很高兴，说丸子你终于从悲痛中站起来了？他怒气冲冲的说，我躺了一天以后就已经不再悲痛了，不过，继续躺着的话，有人会帮我买饭洗碗，所以就懒得起来了，结果，你们每天都给我买鱼，我最讨厌吃鱼的，而且，你们帮我洗的碗，跟没洗有什么区别？我实在受不了了！ 
<br>就这样，丸子从悲痛中站了起来，却也是开始堕落了。 
<br>丸子那几天装睡的行为受到了我们的一致谴责，甚至还遭了天谴：躺床上一星期没有看书，六个学分的微积分被灭掉了。 
<br>那时候的山贼，还是个事业心很重的男人，他参加了很多什么学生会啊社团啊。那时候我看了好多古惑仔电影，山贼说他参加了社团的时候，我还以为是“洪兴”“东星”之类的社团，还劝他不要参加黑社会呢。 
<br>后来我们发现，山贼参加的东西和黑社会一点关系都没有，不过好像是民工组织。从此以后，老看到山贼在学校里搬桌子、搬花盆、拉横幅。不过，大二以后，山贼就不做这种事情了，变成包工头了，开始指挥大一新生搬东西。从那时候起，山贼开始有一大堆哥们，多的好像可以填满小花园的臭水池塘。学校里经常会有演出，原本演出和我们一点便都沾不上。自从山贼变成包工头以后，我们每场演出就都能拿到票了--而且是前排的。山贼说，这叫一人飞升仙及鸡犬；因为这句不恰当的比喻，我和丸子把山贼赶到门口，然后用门狠狠地夹他。 
<br>兄弟是用来出卖的，看多了古惑仔的我深知这一点。后来，事实证明了这一点，山贼的一个兄弟犯了点错误，可怜的讲义气的山贼替他的兄弟背了黑锅，辞职离开社团了。原来社团里面的那帮兄弟都忘记了世上还有山贼这个人；那个山贼为之背黑锅的兄弟没事了以后，忘记了是自己犯的错误，经常和舆论一起谴责“山贼那次的错误”。 
