在下午放学的时候，Mr.L报了一遍座次表，在听到，水吟和KUKU的名字时，泪水就在眼睛里面开始打转，然后是她们刺耳的笑声。

我再也抑制不住了。

泪，哗哗的落了下来。

然后，我说我要去厕所，便先跑掉了踪影。

蹲在厕所里，世界苍茫一片…

感觉自己好不争气，这么容易就被打败了。

二十三、

那个夜晚，可以说是我有生以来，所度过的最难过的夜晚。

我终于体会到了，失去一分友情的悲哀。

听着王菲空灵的声音在寂寞的房间中打着转儿，泪滑了下来。

…………

二十四、

然后，我不知道过了多少天,我才真正的把这件事情看得很淡.

好象从那以后，我就把友谊看得很淡了。

曾经相信友谊天长地久的我，好象就在一瞬间，消失了……

二十五、

还是和优诺坐。

慢慢的，我开始，寻找到了快乐。

直到后来，我才发现，生活原本就是如此的美好。

只看你怎么去看待它。

二十六、

再后来,我认识了VIVIAN。

我在痛苦的抹杀我的记忆的时候，VIVIAN出现了。

她拯救了落入水中的我。

与她彻彻底底的谈过一次心后，我坦白的告诉了她自己的所有感情。

我不知道那样做到底对不对，可是我需要一个人的聆听，那样，我会好受些。

然后，她也坦白了她的所有感情。

这种感觉，就像和水吟的感觉，

是透明的~

二十七、

和VIVIAN玩了一阵之后，最后，我还是选择了继续和优诺玩下去、。

因为她毕竟是我的同桌，而且，她在我最需要关怀的时候，没有离开我。

而且，我也不想拆散那好好的三个人。

但是，还是和VIVIAN保持着朋友的关系。很高兴，她是个重情谊的人。

感觉会有些对不起她.

不过一切也都不重要了.

重要的是快乐~

每个人都快乐……

二十八、

我想经历了一次变故之后的我，应该长大了许多吧~

不想再提起感情，因为我已经放下了……

就像粱静茹在歌中所唱到的：

