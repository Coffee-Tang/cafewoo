<br/>……
<br/>隔了几天再来到雕刻是刚参加完同事的婚礼。心里害怕自己喜爱的地方会被拆掉，像所有你想留住的但最终还是会失去的东西一样，为此惴惴不安。雨后拆迁的胡同显得泥泞杂乱，从倒塌房屋堆积的瓦砾上小心翼翼的走过。不远处很多人围着一棵老树努力把它拉倒，人语喧哗，似乎还有叶子的哭泣声。"轰"的一声，回过头去看树轰然倒下，人们欢呼。许多细小的尘土扬起，在阳光照射下微微的闪亮，还有很小很小的飞虫。一个夏季荫凉的梦被惊醒，弹起来，再飘扬着落下……</p>

<p>?D?D?DKitty，  《咖啡》  1999-8-31</p>

<p>音乐响起来了，那一双双手牵起来了。我看到你的裙摆在飘，我看到你的唇角在笑，我看到你的眼睛在说，牵起我的手，让我们跳舞吧。 
<br/>……
<br/>还记得那时候我牵你的手吗，你的手在我的掌中微微颤抖，但你仍毫不在意地笑着，只是眼中隐隐有一层亮亮的光辉。还记得那时候你牵我的手吗，你明亮的眼睛象闪烁的星星，我轻轻地迈动脚步，让飘荡的衣裙和我的心情一起快乐地飞扬。</p>

<p>我在转圈的时候感到了我的成长，感到我飘起的头发又落下，柔柔地有几根飘到我的眼睛上，而我的身体不停地在生长，向上，向上，我的手臂在扬起的瞬间挽出一朵花，盛开的芬芳的花。我转头去看我身边的同学们，每个人的脸上都洋溢着自然甜蜜的笑容。那一刻我知道我一生里再也忘不了。</p>

<p>?D?D?D 回声，  《牵起我的手，让我们跳舞吧》  2000-5-28</p>

<p>像是这古旧的房子，许多年来默默承载着一些或悲或喜的生命。我怎么也不会忘记曾经有过两对长住于此各有生活目的的夫妇，一群当年坚定过属于自己时代信仰的狂热青年，还有过两个爱跳舞有着人生追求的漂亮女孩，兴许将来，你还可以平静的想起这里还有过林的足迹。我在这房子里听着，看着，感受着你们曾经或许都有些无奈的亲情，友情，爱情，人生，理想———我们有时就是这样，过着无从选择的生活。</p>

<p>?D?D?D 卷帏望月，  《似水流年》  2000-8-15</p>

<p>转眼之间，十年过去了，以往的同学散落在城市的各个角落，没有聚集过，因为班长从来没有组织。我不知道，当我们聚在一起的时候，现在成为女人与男人的同学之间，是否还会如以前一般，惊慌地转过脸去，回头看看老师的眼睛是否看到这一幕。</p>

<p>那时花开，应该有花自然开放的年纪，就这样转眼过去。因为那一年，有一个特别炽热的夏天，烤得我们闭紧了美丽新鲜的花瓣，害怕盛放的那刻，那怕只让人远远欣赏。 </p>

<p>?D?D?D 水流花放，  《那时花开》  2000-6-14</p>

