<p>哥去了北京一个月后我去北京实习，和他一起再呆了将近一个月。那时我已经是决定一年后就将出国，实习结束了哥送我回学校。到了车站，我把行李放上了车再下车来跟哥说再见时看到他一个人孤零零的站在站台上就已经是泣不成声。两人在北京的站台上痛哭了一阵后约定我们两人都要好好的努力，好好的活着让父母都开心，要好好的挣钱以后让父母有个开开心心舒舒适适的晚年。</p>

<p>?D?D?D yaya，  《哥》  1999-7-2</p>

<p>如果还住在那里的话，我想这个月食会不会看的很惬意，就象以前那般？我跟M打电话说，周末去找他一起吃个饭喝个酒，他说好的没问题，我想只是说说而已，就算真去了，还能找到当时的感觉吗？我知道，有些日子再也不能回头啦。我知道的，只是，回忆是可以回头的。所以看到这条月食的新闻，总是会忍不住想想那段时间，M和他的女友之间的争吵和甜蜜，L对我的关心，我们几个在一起的快乐，还有一些岁月烙下的痕迹。</p>

