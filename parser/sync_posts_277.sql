标题：幻灭是成长的开始 
发信站：台大计中椰林风情站 
 
「陈文德的作文写的很好，你们要向他多多学习。」今天国文老师当着全班同学的面这样夸讲我，让我觉得有点小小的不好意思。其实我只是在作文时多用了几句古人说、孔子说之类的屁话罢了。 
 
不过，国文老师最后加了一句：「不过，『明月几时有，把酒问青天』是苏东坡说的，不是孔子，孔子又不会作诗。」 
 
天啊！我今天才知道孔子不会作诗，孔子不是十项全能吗？我的偶像破灭了，不过电视上不是常说──幻灭是成长的开始。我想我长大了。 
 
我回家的时候，把这件事告诉老爸。老爸只是拍拍我的头说：「作文好不好并不是重点，重点是你的数学、英文、理化都不好。」 
 
虽然老爸泼我的冷水，但是没关系。因为今天老师在称赞我的时候，我看见周月雅回头对我微笑。套一句老爸常用的口头?R：「作文好真的不是重点，重点是『周月雅』对我微笑，所以作文好就有用了。」 
 
我想，我今天作梦一定会到周月雅，她一定会说：「阿德，你好棒喔！偶像、偶像」，天啊！怎么还没睡就开始在作梦了，还有孔子竟然不会写诗，真是太可耻了，他不够资作我的偶像。好！从今天起，我的偶像要改，改成谁好哪？ 
 
张菲，太老了。胡瓜、贺一航天色了。吴宗宪又太会哈啦了，不符合我的本性，小董、舜子又太耸了。天啊！怎么都是些搞笑节目主持人？ 
 
不行，搞笑不长久，换一下好了，古时候的人也行啊！就……就……就……。完了！古时候的人我只认识孔子和苏东坡。 
 
「……常常有人问我创作的灵感是什么，我想通常只来自最初也最原始的感动，……呼答啦、呼答啦！」电视传来吴念真的广告。 
 
喔！吴念真最近好象很红喔！他除了会编剧、当导演、写小说、主持电视、广播节目、拍广告，最重要的是他好象赚满多钱的。好！就这么决定了，我的偶像改成吴念真。 
 
──呼答啦、呼答啦！ 








	
		
		 
		
	


	
		
		
		
			
				







