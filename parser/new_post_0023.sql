俄罗斯女性，是饱有热情与勇敢的异秉。从屠格涅夫笔下的叶连娜与阿霞，陀斯妥也夫斯基的娜斯塔霞，托尔斯泰的安娜，到后来白银时代的女诗人阿赫玛托娃、茨维塔耶娃，都是纯真而抒情意味浓厚的女性，不幸都有着被侮辱与被损害的命运。阿赫玛托娃年轻时美丽多情，二十世纪初游历巴黎时曾经与大画家毕加索、莫迪格里亚尼交好。她的丈夫是白银时期的著名诗人古米廖夫，在大清洗时期被枪决，她本人被斯大林时期的文化高官日丹诺夫骂为“娼妓与修女的结合体”。可是当那些小丑的叫嚣终于沉寂时，阿赫玛托娃的形象犹如一朵金玫瑰被时光雕刻得煜煜闪光。到了六十年代苏联解冻时期，她终于有机会去海外参加文化活动，那时英国学者伯林见到她时说，她的形象竟如叶卡捷琳娜二世一样高贵凛然。因此有人这么形容阿赫玛托娃：“她是纯洁、完美、道德上全无瑕疵的当头棒喝。”记得看过一本叫《三诗人书简》的书信集，是茨维塔耶娃与里尔克的书信。两位我最喜爱的诗人竟有过精神之爱。

所以当叶卡捷琳娜堡博物馆镇馆之宝------克拉姆斯科依的《妇女肖像》-----出现在我面前时，我想惊呼一声：那是多么完美的女性！高贵，优雅，纤细。那件白色细麻布的衣裙使她更加朴素美丽。她秀丽的鹅蛋脸有着细细窄窄的鼻子，蓬松的卷发结结实实的挽成发髻，手里拿着一本书，微扬着头专注的读着，一缕光线抹在她的脸颊上，闪着玫瑰色的光芒。莫名的觉得她看的应该是《叶甫盖尼 · 奥涅金》，是《皇村回忆》。看了她之后，再看列宾那幅同名的应酬之作《妇女肖像》，觉得里面那个傲慢的贵族女人实在粗俗难耐。

