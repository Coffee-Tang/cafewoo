<p>最喜欢《为什么这样子。。。怎么这样子》那段的旋律，周杰伦的这样子，打开了我疑问的铁盒。我想说什么？说一些事关于生活，关于爱，关于心情。有个问题从失去爱情到现在我都想问“为什么这样子？怎么这样子？”很傻吗？或许成长中的人总会在疑问过后明白一个道理。在每一秒，每一刻，都可能是领悟到一个道理的瞬间。代价是什么？也许就是那些所谓的伤，所谓的痛。小的时候可以喊痛，妈妈就会帮孩子包扎伤口，长大了，也可以喊痛吗？这次的伤口要自己医疗。这样子的道理我明白的，你明白的，受过伤的人都明白。</p>

<p>为什么这样子？曾经用心的对待我会渐渐遗忘，是潜意识不想在和回忆过生活，还是本身人类的记忆容量是有限的。有些时候真的什么都不想去考虑，把问题交给时间，这种方式是另一种方式的依赖吗？依赖时间可以帮忙解决问题，或者这是种逃避的方式？</p>

<p>怎么这样子？爱这个字渐渐模糊，是它离我越来越远，还是我在向它的方向背道而驰？爱就代表要在一起吗？在一起的人就会长久吗？有人说爱情一定会受伤就看你有没有勇气去承受。伤了之后，痛了之后该用多少时间去抹灭这样的伤痕？还是擦干眼泪继续前进？</p>

<p>为什么这样子？常常喜欢安慰别人，总是在自己难过的时候，什么道理都明白的，却不愿意给自己讲道理，只是让心情随它的轨道行驶。该去哪里的就往哪里飘流。还是别人的劝说会比自己独自调节来得更有意义。那我们是在等待被安慰的人吗？</p>

<p>怎么这样子？情绪浮澡的时候总想让自己平心静气去面对眼前的一切，却还是忍不住发起脾气，是不是这就是人类应有情绪，或者是另一种借口？发泄是不是一种生理需要？
<br/>为什么这样子？看见情侣在我面前经过我只羡慕他们此时的甜蜜，却总会开始想着他们什么时候，什么地点会彼此遗忘，分道扬镳？难道注定年少时的爱情没有长久可言？难道只是拉拉手，逛逛街，看看美景，这就是曾经拥有就已足够？难道熬不过时间就是命运的安排？命运为什么总会选择我们，让我们看起来如此软弱？</p>

<p>怎么这样子？非要有第一次失去，才会懂得下一次的珍惜。有人说人的最爱一生中有两次，第一次往往以失去告终才明白那是种真爱，以至于有第二次最爱的出现，爱就是那种感觉。是这样子吗？那么，那个第一位就应该成为回忆里永恒的话题吗？那个第二位就会是陪着自己走到最后的那个人吗？谁又能保证？或许安慰自己的时候应该说珍惜过就不会有遗憾，起码那时的勇气和信心没有缺席过。</p>

<p>为什么这样子？常常有人说和自己走到最后的未必是那个最爱的人，那么他去哪里了？回忆？未来？还是失去的，得不到的永远都是最让人渴望的，于是它就很自然成为了最珍贵的，最爱的。那现在的呢？也是那个要珍惜的人吧，那还回忆过去做什么呢？当作参考吗？爱情也是一道数学题吗？如果真的如此简单，世界或许不会有复杂这个字。一切都用公式结算就有答案，那无解的答案又该怎么办？或许没有答案会是最好的答案。</p>

<p>本来不断的问问题就是在无病呻吟，这样子好过点了，原来十万个为什么的答案会让人懂得很多道理。问问题的同时本身就是一种学习，知道了答案后细嚼慢咽，脑袋像海绵一样慢慢吸收，总会渐渐明白其中的意思。半岛铁盒都有钥匙孔，人类脑细胞应该也不会太生锈。</p>

