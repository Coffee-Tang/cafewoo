这是那回想起来依然会狠狠揪心的疼痛。过去的是否真的已经过去，而未来的将来的一切都还没有到来。那些被我喝醉的霓虹，那些嵌在咽喉上的风，那些素素的十指上敞开的日月，那些转身的哭泣和曾放在彼此手心的快乐，一起留在我们再也不能回去的青春年少，留在昨天。想着想着就觉得一身尘土的悲伤。再也抓不住了，明明晃晃的就消失在树的尽头。哪一个季节的尽头。

好想站在山顶对着空空的天大声地喊，回忆慢一点，再慢一点。

让我记得你的脸。

然后展翅向着未来高飞。不低头。不后悔。

三.

每天都有记忆在流失，我不知道我们是否真的可以了无牵挂。

曾经锥心刺骨的疼痛，残局上的阴影在群岚中寻找出口，等下一阵雾的伤口上盛开自己所有的激情和绝望。就像井水一样，从最深处蔓延的冷和阳光都已经在时间里刻在肌肤上。一瞬间弦歌如水弹指灭。

我总是淡漠地看着那些陌生人的面孔，看谁的脸上可以衍生出天荒地老。谁可以这样月随云走竟不移，岸逐舟行终自若。但现实总是导致物是人非。

“物是人非事事休”，很残忍的一句话。

我们到底是拿一些人去记忆一段时光，还是拿一段时光去记忆一些人。因为就算我够坚强，我不愿意忘，可是一些事情一些人，我还是这样开始忘记了，在我左转走过几个路口的时候却发现我已经想不起那些曾经陪伴我走过一段时光的人的脸庞。

那些我爱的人，那些离逝的风，那些永远的誓言一遍一遍。

那些爱我的人，那些沉淀的泪，那些永远的誓言一遍一遍。

感谢那些我爱的和爱我的人，感谢你们陪我走过生命中无法磨灭的一段。瞬间的温暖，就是无可替代的永远。谁给我刻骨的伤害，我给谁铭心的痛楚，那都是时光中不可避免的磨砺。我以天父的名义赦免我们的罪。弦凝绝后空阔的前尘，彼此那么坚定的离开。记得，记得，始终都是记得的。蝶漫如渊。

现在的我们，站在不同的方向，看着同一片我们看过的地方，就像看着我们小时候那些装在玻璃瓶的阳光一般的幸福，这些摇摇晃晃的幸福，我们带着走天涯。哪怕我不记得你，但我依然会记得，我们经过幸福时的样子，开心得像个无忧无虑的小孩子，只有小孩子才说永远。

淡。这些话说给你们听。如果可以，请不要念念不忘。伤口好了，就要舍得离开。彼此毅然决然地挥手，你往左走，我往右走，当你生命里别的人事，我们再没有机会去回头。没有怨恨。不想等到彼此老得连手都不能握住的时候才说出对不起，才听见一句回答，没有关系。我们不要去回忆，不要哭，但也不会再为同一个季节微笑，因为再不会遇见，已经是缘尽的人。我知道，我想你们也知道。

有人走，一些人来了一些人就要走了。说我们还会再见，因为我知道很多时候很多人分开了走散了挥手了告别了怎么都不会再见了。我开始在心里对生命中一些人说再见，再见了。说的时候我心脏边浸透过很多划伤的痕迹，我轻轻的捂住，怕一松手，我心脏的温度会全部散去。哪怕我对你们已经无话可说，但是我依然会怀念你们。

你之于我，我之于你，谢绝城池若流萤。黑夜里那些温暖的曾经。

只是难过不能陪你一起老，再也没有机会看到你的笑。

认真说了再见的人再不会相见。

亲爱的，再见了，再见了，再见了。

素面的城市，迢迢的伤痕。我再也找不到你等我的地方。

四.

曾经一个人坚强地站起来，以为躲避过时光尖锐而呼啸的冲刷，躲避了那些破空而来的明明暗暗的忧伤，就可以不再恐慌。可是那一瞬间才知道自己心里已经落满了多少的风霜。

孤单是一个人的狂欢，狂欢是一群人的孤单。原谅生命中不为人知的孤独，适当的孤独就是使命，包括现在自己蜕变的成长。年轻的时候注定要错过一些人的，也被一些人错过。再回首已百年身。

可我还在寻觅，因为我还有希望，那个传说中潘朵拉的盒子里最后跑出来的东西。即使爱是绝望，我也会一如既往。

想有个人能让自己全心全意的对他好，我能只听一个人的话，只为一个人乖。快乐的时候有人分享，哭泣的时候有人为我张开怀抱。想有个人能和我肩并肩看满天灿烂的烟花，看日升月落，看花开花谢。想有个人能在我过马路的时候牵着我的手。……

为什么我老是在一些细微的地方幸福着。我要的那么少，可为什么我总是得不到？

生命中匆匆过客，来来往往。回首往昔，漫游在阴霾的苍穹之下，依然会泪流满面，仰望天空。蒲公英的悲哀，散落在阴暗角落；稻草人的失落，守望在绿野麦田；永恒的幸福，徘徊在朝啼之间。

幸福是我们可以思索可以认真的实践却不能定义的词语，就好象疼痛可以帮你完成成长，却需要如此靠近，需要短兵相接，需要你在某一瞬间抗起很多你根本无法承担伤害，无论你多骄傲的抬着头不愿意落泪。而我珍藏幸福的时候，总是愿意这样屏住呼吸，节俭的用于以后我不在路上的日子。

还记得曾经看过的一个故事。一个老奶奶和一个老爷爷的游戏，他们相亲相爱几十年，每天都会将纸条上写着shmily的纸条藏起来，在家的某个角落，橱柜，床单，白色的窗帘后面，厨房的咖啡杯下。让对方寻找，直到生死。最动人的情话，已经听过，这个世间的温暖如此苍茫。See how much I love u.这句话他们悄无声息的对对方说了几十年。

什么时候我才能在哪找到这样的一张纸条？

如果真的可以。我只想找个喜欢的地方，养一只喜欢的小猫，整个房间有我喜欢的CD和书，可以躺在旷野里听音乐的声音一直弥漫到天空的褶皱里。我想拉着喜欢的人一起走世界上那些我们喜欢的地方，不要诺言，不要婚礼，不要婚纱，不要酒宴。一起躺在沙发上像两个笨孩子一样一起看书，一起去那些卖碟的店，一起拍下生命中的点滴，一起躺在蓝天下不着边际的聊天聊梦想。

在路上，看云，堆砌时候风情万种。多好。一个旅途完了，还会开始。下一站，我们还会这样一起亡命天涯的走下去。天涯处处有鲜花。幸福就这么实现在生命的前方。就让童话停留在那一页，再不翻过去，那一页我们都在彼此身边，保持着一种地老天荒的姿势。相亲相爱，不离不弃。

谁是我们值得一辈子去爱的人，前世今生都想要回的人。

爱可不可以投递，我可不可以飞岩走壁找到你。

