我听得那小子没来由叫我付帐，心中厌烦，但又一想，这碗面吃过后，自己也要成叫化子了，说道：“先不忙……”可一想，自己也真没钱请他。思索半晌，心不由衷道：“我请！”小二一怔，笑着答应了。

我打量着那小子，年纪不过十五六岁，一双水汪汪的眼睛不时朝我调皮嬉笑，笑意从嘴角泛到眉间，露出洁白的牙齿。小二端过面来，他忙不迭地抢过，吃了个精光。“吧唧吧唧”小嘴，向我走了过来。

“这位大侠尊姓大名？”他那只黑乎乎的脏手拍在我肩头。

我淡淡说道：“大侠是免了，在下陈�F。”

那小子惊喜叫道：“呀，巧得很，我也姓陈！咱们一家人。这顿面条你请定啦。”

我苦笑道：“嘿，一家人说什么请。”心中却暗骂自己，思考如何脱身店铺。

那小子喜道：“大侠真大方，若不嫌弃，我们交个朋友。”说着，坐了下来，摘下腰中酒葫芦，叫道：“小二，拿两个酒杯过来。”

我寻思：“莫非他又要叫酒来，为我们交朋友助兴？不止如此，还要顺便往他酒葫芦里装些酒，自己往后享用？这回我可被讹上了。师父说得果然不错，江湖险恶，人心难测。”

店小二拿来酒杯，却不见小叫化叫酒，正自奇怪，只见他摘开葫芦盖，朝每杯倒了一些黄澄澄的酒来。登时，酒香四逸。

我忙道：“小兄弟，我不会喝酒，不必……”

那小子插口道：“不成不成，我们虽不是义结金兰，却也是有缘。我这壶是女儿红，是我爹死前留给我的。”

我一听，心中一凛：看来他并非讹我，倒像是真心相待。他也是有爹的。忙道：“你爹……怎么……”

他脸上的笑容消失了，凄然道：“他被仇人杀死的……”还未说完，已经伤心哭了起来，声音有些奶声奶气，倒有几分像小姑娘。

我试图安慰道：“抱歉，我……那……”却也一时想不出什么好安慰的话。总不能说“我为你爹报仇”吧，且不说自己武功能不能胜对方，又不知对方的底细，况且这小子真正意图未明，倘若他不是真的落难，而我却和人家结上梁子，岂不成了冤大头？而且师父曾告戒我不要没来由地和武林人士结梁子，到时吃不了兜着走，可没人来救你。

他见我言语结巴，忽然笑道：“呵呵，你要帮我报仇么？……算了，你打不过人家的……来，我们喝了这杯酒吧。”

我从没沾过酒，正自迟疑。

他笑道：“莫非陈大哥怀疑我酒中下毒？”脖子一仰，喝干了那杯女儿红。

我也爽快地喝下了酒。我第一次喝酒，只觉一股呛人的酒气冲入鼻子，喉咙似有火烧，又觉脸颊红热。一会儿，倒也觉得口齿流香，不愧是名酒女儿红。

他又笑道：“看来陈大哥果真是个滴酒不沾的君子。也是个江湖中的稀有动物了，哈哈……”格格而笑，笑声清脆。不过，他才十四五岁，声音未变型，不足为奇。

我苦笑道：“兄弟笑话了，在下我也才初涉江湖，武功和酒力自然低微。”……

聊了会儿，他见我脸有愁色，问道：“陈大哥有什么心事？”

我只笑笑，不置可否。

他想了想，笑道：“原来如此，陈大哥看小弟给你露俩手。”说罢，站起身来，走向掌柜。

我正惊奇，只听他对掌柜说道：“那位少侠还想点些好菜，驼峰熊掌的那些大菜，他也吃腻了，这样吧，别的怕你这里没有，烧鹅翅和卤鸭舌应该有吧。来几碟作下酒菜。”

掌柜大疑：“可是这位少侠可从来不点大菜呀，总是吃碗牛肉面敷衍肚子，今儿个……”

他叫道：“你不想做生意啦？我大哥，也就是这位少侠，今儿认识了我这小兄弟，心里一开心，不就得开个桌儿喝几盏？怎么，这些小菜，你这地儿没有？”

掌柜忙道：“有，有。我吩咐厨子去办。请客官稍坐会儿。”说完，张罗店中的仅有三个厨子兼小二出去采点食材。只剩下掌柜与我和小兄弟了。

小兄弟又对掌柜叫来：“这位少侠有赏。”

我大惊失色，正要训斥他。那掌柜对我躬身一拜，背朝小兄弟。小兄弟乘机点了他的穴道，拉起我，往外跑。

跑了一段路，这才停住。

我怪道：“你怎么点了人家的穴道？又说我要赏钱，点菜什么的。”

那小子笑道：“怎么了，帮大侠解围，不许么？”说着，一掌打了过来，我轻轻避过，他的掌法虽然无多大的威力，但很有章法，十分精妙，显然是出自名家门下。我避过他三十六掌，见他堪堪打完一套掌法，身子一纵，格开来掌，使出小擒拿手，手腕撞向他胸前的“神藏穴”，忽感到一股软绵绵的着力处，正疑之时，他满脸通红的用力打了我一掌。喝道：“乱来！”

我向他扮了个鬼脸，道：“算你厉害，我可拉不下这脸皮，用点穴方法对付不会武功的人来吃白食。”

他急道：“那还不是帮你？嘿，你可别以为我叫化子常用这种手段来骗吃骗喝。……只因我点穴手段不高明，所以才把小二他们支出去，成功机率才会高。”

我道：“看不出来，你还是会武功的，适才的奔跑，你轻功不赖嘛。”

他得意之色，流露于脸，笑道：“没你厉害啦。”想了想，忽然变色道：“你……你是千水派的？邓龙是你什么人？”

我奇道：“何出此言？我还是万水派的呢，哈哈，千水派掌门邓龙是我大师兄。”

这时，小兄弟怔怔看着我。

我不解道：“怎么回事？我说错了吗？……邓龙师兄和千水派，你怎么会认识？……对了，还没问你叫什么名字呢？”

他淡淡地道：“我叫……我叫陈鸿，江边鸟的红。我是看你武功路子很像邓龙。”

我拍了拍他的胸膛，正欲说：“废话，我们都是同一师父的弟子。”忽然感到触手处软绵绵的，有些不对劲。只见他低着头，满脸煤灰，看不出脸色。

晚上，我们找了间破庙。我已倦困，不一会儿，在一尊泥神像下睡着，半夜醒来，却见陈鸿仍在思索。

我睡眼惺忪地问道：“怎么，还不睡啊？”

他支支唔晤道：“那里好难躺，我……我还是到……那个供桌上去睡。”

我倦极了，也不理会，又躺下进入梦乡。

次日，阳光射进庙里，我醒了，却见陈鸿，已经起来了。

他问道：“你准备去哪里？”

我困惑道：“也不知道。师父曾说过，江湖在我心里，要用心去感受江湖。”

他念道：“江湖，江湖，你感受到了？”

我迷茫道：“嘿，我下山两个月了，所经历之事却是平平无奇，除了认识你这个小叫化之外……对了，你是不是丐帮的？”

他笑道：“江湖第一大帮会哪里会收我这个本事低微的臭小子。好了，别说丐帮，我问你，邓龙那里你会去么？”

我道：“江湖之大，不知所归。我大师兄那里也不能去，还有二师兄熊虎。这是门规。”

他睁大眼睛问道：“熊虎？莫不是‘千竹飞刀’竹联帮主？你师父什么名头，竟然教出这么一些江湖上的大人物。”

我接口道：“就我一个，仍然在武林中充当无名小卒。”

他若有所思，又道：“这么说，你的武功绝非泛泛，八成能与当今两大高手邓熊打成平手喽。”不等我谦虚，他又问道：“你和他们交情如何？”

我道：“他们与我好多年不见了。记得他们还未下山之时，很是疼我。”

他道：“那现在呢？”

我道：“下山之后，只当谁也不认识睡。”

他道：“不就连同门之谊也没有了吗？”

我也不知如何回答。

