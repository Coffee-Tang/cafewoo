我记得我们第一次见面时的情景。 艾伦来应聘公司的技术支持职位。 门铃响的一刹那， 我看见他。

那一刹那间， 两个人的表情是停顿了的吧？ 就像是突然遇见什么， 而又混沌着说不清是什么的表情。 或许， 在那刹那间什么事也没发生， 只是生活中一个普通的日常片段， 在你回忆的时候， 带有陈旧黑白片的味道。 是我记错了， 我和艾伦之间， 有过太多这样那样的刹那， 也许， 这一切都是我错误的领会。

那天的艾伦穿一件浅色衬衣， 卡其布长裤， 黑色的茄克。 我穿了一件藏蓝色短T恤， 黑色裙子， 小小的珍珠坠子项链。 他留了下来， 面试后我给他做了ORIENTATION培训。 我记住了他的生日， 五月二十五日， 双子座。

六月的天， 渐渐热起来。 艾伦在另外一个OFFICE工作， 每日里不常相见。 很奇怪的是， 我们彼此写EMAIL。 通常是， 我用中文写很长的一篇， 他用英文言简意赅地回复。 在结婚前， 我删掉了他所有的EMAIL。 我是个做梦的女子， 从梦里醒来的心总会很痛很痛。 只是那一次例外。

那一次的梦是关于艾伦的。 我梦见小时候家门前的那座山， 身后有很多人。 有一个胖胖的叫JENNY的外国同事， 领我一直爬到山顶。 山顶有一座小而美丽的花园。 我走到花园尽头的小门， 低头一看， 却发现自己站在一处陡峭的悬崖上， 脚下是万丈深渊。 那种无路可退的焦急与恐惧充斥了我的全身， 我动弹不得。 艾伦不知什么时候从哪里走出来， 牵着我的手慢慢下山。 是回家的路。 我从来没感觉过那种巨大的平静与安全。 好象是全世界都被抛在了身后， 天地安静下来， 只有艾伦牵着我的手， 慢慢地向回家的方向走。

我想， 幸福， 是不是就是内心巨大的平静和安全？

我不是可以藏住心事的女子。 我写EMAIL告诉了艾伦。 那天下午， 忘记了他是因为什么事来我这边的OFFICE了。 艾伦隔着桌子站在那里和我说话， 眼睛亮亮地看着我。 我不能确定， 也不敢望着他。 我们之间， 有太多这样的瞬间。 这样心跳的瞬间， 构成了我们之间。

这是我第一个关于艾伦的梦， 奇异得没有来由。 告诉他， 没有什么期盼和想法， 我只是困惑于这样的梦 --- 那种无边的恐惧和巨大的安宁。 而在这巨大无边里， 有艾伦牵着我的手。

以后的日子什么也没发生。 我也没有再做那样的梦。 我们两个人和公司里的其他人一样， 每日里或忙碌或悠闲。 我依旧会写信给艾伦， 讲那些梦给他。 他也会有短短的回复。 有时候我想， 艾伦是一个深沉的人， 轻易不让人看到他的喜怒哀乐。 但是在他的只言片语里， 我还是可以感受到他的情绪起伏。

在公司里艾伦是个很受女子喜欢的人。 时间长了， 自然是有些事端出来。 先是他的女上司跑到我这里来， 说：“哓哓， 艾伦那么好， 你喜欢不喜欢他？ 要不要我来帮你们？” 接下来是我的上司， 一日无意中说起公司内的职员， “哓哓你和艾伦蛮般配的， 为什么不在一起呢？” 我无言以对。 喜欢不喜欢， 艾伦你自己难道不知道么？ 我才不去主动说起呢！

可是还是有烦心的事情来了。 公司里的小秘书美欣总是有事没事地跟我提起艾伦。 在电梯间里， 美欣当着艾伦的面问我： 哓哓， 你看艾伦长得像谁？ 艾伦站在那里， 有点儿尴尬地看着我。 我真的认真看了去， 说： 像我爸爸。 他们不解。 可是艾伦真的是很像我爸爸年轻时的模样 --- 瘦瘦的， 高高的， 穿很干净的衬衣， 走路腰板挺得直直的， 很清瘦精神的样子。

那时候艾伦组织公司里的人去九寨沟。 开始的时候很多人报名， 可到头来只剩下艾伦， 美欣和我。 于是艾伦又叫上他父母同去。 美欣打电话来：“哓哓我觉得艾伦对你很好。” 我不说话。 她接着说：“他对我就没有对你好。 你知道他向旅行社报名， 把我的名字都写错了。 所以下班后他又返回旅行社帮我把机票上的名字改过来。” 美欣酸酸的语气里有微微的炫耀。 我暗暗压住自己懊恼的心， 听她继续讲下去：“其实啊， 平时在这面的OFFICE里艾伦对我也挺好的。 有时候我不高兴了， 他就会安慰我。 他很关心我的。 但是我还是觉得他对你很好。”

“美欣， 我不去了。” 不知道这是不是她想要的结果。

“啊！ 这怎么行呢？ 你要是不去的话我也不会去的！” 美欣在那边叫起来。

九月三十一日到十月五日。 很奇怪一个自小数学成绩很糟糕的人会很清楚地记住一些数字、一些日期的吧？ 我就是这样的一个人。 比如说， 在我第一次见到艾伦的时候， 我就记住了他的生日 --- 一九七四年五月二十五日， 双子座。

九月三十一日， 我们离开北京。 在飞机起飞的刹那， 我有种冲动， 很想自己的手被艾伦握着。 可是我装做若无其事的样子， 和美欣换了座位。 靠窗， 一个人望着窗外。 我不知道， 在以后一连串接踵而来的关于艾伦的梦， 就是从那时开始的。 那时， 我在飞机上， 靠窗的座位， 指尖冰凉， 有种想哭的感觉。

入住成都的第一个晚上， 美欣又开始了。 “哓哓， 你为什么不让艾伦做你的男朋友呢？ 他人很好， 我觉得你们两个很合适， 不像我和他， 在一起时总是吵嘴。” 第二个晚上， 第三个晚上……， 我终于忍不住了。 美欣是女孩子家的小心眼， 名义上是劝我要和艾伦在一起， 其实是在告诉我艾伦对她有多好。 “美欣， 我不想现在找男友， 再说， 又都是一个公司的， 不好。 如果你喜欢他， 就直接去跟他说去好了。”

“我才不喜欢他呢！” 美欣小声嘀咕着。

“谁喜欢他谁就是小狗。” 我裹紧了被窝， 转过身去。

哓哓也是小狗， 哓哓是自己在骗自己。 其实， 我不在意是否是同一个公司的， 只是记得有一次艾伦告诉我他的爸爸妈妈在同一所大学里， 他觉得这样子没意思。 我的心是那么沉， 沉向一个不知名的黑洞里去。 同时， 我也懊恼艾伦的若无其事， 一副暧昧态度。

是十月四日的清晨吧？ 我们的车坏了。 司机在等人把零件从成都带到川主寺。 一大早美欣跑去艾伦的房间聊天， 她穿了一件艾伦妈妈的背心， 斜靠在床上。 我背了包出去转， 认识了小店里的康磋卓玛， 从她那里买了一把牛角梳子和一串香珠。 我给她看自己戴的北京景泰蓝手镯， 最后脱下来留给了她。

在外面转了一圈买了些零碎的东西。 回到房间， 美欣不在， 有几个同屋的小孩子在玩儿扑克牌。 我百无聊赖， 想还是去寺里看看吧。 艾伦看见我， 叫：“哓哓， 你去哪儿？ 我们一起去照相。” 美欣也跟着出来。

路上遇见康磋卓玛， 我们去川主寺。 艾伦抓拍了很多的相。 有一张是回到北京洗出来后我才知道的。 是我的背影， 手扶着寺里的转经轮走过。 片子有点儿虚， 是刹那经过的感觉。 现在我想， 也许， 在我们彼此的生命里， 有的， 也只能是这些刹那了吧。 可是就是这些刹那， 在一颗暗暗喜欢的心里， 也是一场盛大的宴席了。

转完转经轮， 我进到寺里很快地许了愿。 出来后看到艾伦还拿着相机站在门口。 我笑笑地对他说：“我许愿了呢！ 呵呵， 不告诉你！”

艾伦不知道我许了什么愿， 他永远也不会知道了。

离开川主寺前， 我把头上的景泰蓝发卡也留给了康磋卓玛。 记得很早以前读过一篇小说， 说是人死后灵魂会重新返回去捡拾留在世上的脚印。 一个脚印一个脚印地去找。 即使有些脚印会被陌生的脚印覆盖， 待灵魂找到那个地方时， 脚印会自动浮上来， 从万丈红尘中轻轻浅浅地浮上来， 等着灵魂来相认。 如果是这样的话， 我想自己是遗失了太多的东西在那片土地上。 对于一个叫做于哓哓的爱做梦的女子， 常常分不清梦里梦外的心情。

还有一次， 我记得， 从飞机下来在车上， 我和艾伦面对面站着， 很近， 在我抬头的一刹那， 我看见艾伦的眼睛里闪动着温柔的光芒。 那一次， 我没有躲。 我望着他乌黑而有些发蓝的眼睛注视着我， 感到太阳暖暖地笼罩住了自己。

我们之间， 就是由这些刹那组成。

十月五日， 我们回到北京。 一年后的十月五日， 我和世伟举行了婚礼。 艾伦帮我们在婚礼上照相。

我不知道艾伦对我结婚的事是怎么想的。 艾伦对于我， 是一阵忽远忽近的风。 于媚不喜欢他。

从九寨沟回来， 那些梦开始缠绕我， 搞得我心神不定。 我会对艾伦讲， 他是这样的男子， 深沉得平静如水； 我对好友于媚说， 她叹气。

二十世纪最后一个夜晚， 我们一帮同事、朋友在酒吧聚会， 去大钟寺听钟声， 许愿。 于媚去了。 后来于媚说，“哓哓， 忘掉他， 他是个态度暧昧的男子， 我不喜欢他。” 可是， 谁来告诉我那些梦？ 谁来告诉我它们的起源？ 谁又让它们日夜以来纠缠着不停息？

生活似乎平静了一段日子， 除了那些懊恼我的梦。 新年过后不久， 世伟遇到我。 他追我追得紧。 第一次相识写信给我； 第二次见到我要我做他的女友； 第三次给了我订婚戒指； 第四次， 第四次我们结婚。 前后不到一年的时间。

最后的一个刹那是什么呢？ 订婚前， 有一天我和艾伦在一起走。 那时我举棋不定。 我对艾伦说， 有人向我求婚呢。 他没说什么。 当时他好象是拿了一盘CD要刻， 不知道为什么在我的机器上放着听。 他坐在那里， 选了一首歌， 问我，“这首歌， 你听过的吧？” 我看了歌名， 是ALL 4 ONE的I KNEW I LOVE YOU。 我没有读出歌名的后三个字， 他站起来走开了。 那时公司里一些人在谈论艾伦和一个十八岁的女孩子在一起。 我见过她， 是个白净秀气的女孩。 我想起艾伦说他喜欢秀气的女孩子。

就这样了。 九月二十二日我和世伟领了结婚证。 当天我没有回家， 跟世伟住在酒店里。 晚上他有一个电话会议。 我躺在床上， 不知为什么突然哭了起来， 越哭越伤心。 我知道， 是有什么东西被我远远地丢在身后了， 那是我再也不能企及的。 我好象是一下子就站在了河的对岸， 望着河的那一岸， 就象是望着自己的前尘往事， 再也回不去了。

艾伦， 在那一刹那， 我发现自己是爱你的。 爱你爱得那么心甘情愿， 爱你爱得那么小心翼翼。 小心翼翼到不肯轻易承认对你的感情。

然后又是那些梦了， 在以后很长的时间里纠缠我。 在无数个关于你的梦里， 我们隔着街。 我可以看见你的脸， 让我心痛的脸。 我向你招手， 唤你的名字， 一声比一声急切， 一声比一声远， 一声比一声的心碎。 你平静如水的脸庞， 我心里沉到深渊的痛， 没有人知道， 也无从说起。 你无法体会。 也许， 也许这一切都是一个叫做于哓哓的爱做梦的女子的错误领会， 她的另外一个纠缠不休的梦。 而属于你的， 是永远的平静如水， 不动声色。

我哭了， 在又一个梦到你的深夜里清醒过来后， 泪水打湿了枕巾。

如果你是一个和我一样每天都要穿过黄庄路口的人， 如果你够细心， 或是你无意发现， 在北边从西向东的路面上， 有一枚被嵌在了柏油路面上的钥匙， 铜质地， 黄灿灿地像是上世被谁遗失了的心， 孤零零地躺在那里， 卡在一个尘世的路口， 动弹不得。

24：24

