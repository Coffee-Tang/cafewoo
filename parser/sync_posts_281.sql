<p>　　很久以后都没有再看到杰，无意中看到疗养院的车子停在学校的操场上，杰的父母到处找着杰，大脑里浮现出他们把杰扔上疗养院的车上，像扔掉一件垃圾一样扔掉杰时，眼前仿佛不是杰的父母，而是两只变了形的猴子。 </p>

