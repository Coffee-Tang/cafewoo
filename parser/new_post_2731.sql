<p>
无聊的生活过太久，总想祈祷上帝赐于一些所谓的“心跳”。
<br/>并不是说现在的自己没有了心跳，只是生活失去了激情，而往往平淡的日子总让人不满足。
<br/>想追求更美好的事物，更理想化的生活。无可厚非人是有欲望的。
<br/>据说心跳太快人的身体会支持不住的，那时就糟糕了。可是没有起伏的感觉似乎人生就失去它的精彩。
<br/>矛和盾在某时同时出现在思想斗争里。我是这场战争的观赏者，更是受害者。无论它们的成败与否，我都只能无可奈何的接受。
<br/>一个无解的问题留给上帝来回答。</p>

<p>置身于异国他乡的我常常认为自己已是“生锈”的钉子，一只丑陋的钉子，钻进一个小洞口里，一个伸手不见五指的小洞。
<br/>我那生锈的身躯结实的固定在洞里，就如我一成不变的生活。
<br/>我是否该安于现状？还是努力寻找属于自己的一片天。
<br/>曾经我坚信这个小黑洞就是我的栖身之处，然而听上帝说外面的世界更美好。我想钻出去，却又害怕另一种环境会让自己适应不了的。所以我想问问上帝我该怎么办？</p>

<p>今天的天气还是一样阴，虽然太阳偶尔会出来打个招呼，可是它似乎很忙，会不会是忙着打麻将？就找乌云小姐来代班呀！
<br/>真害怕乌云小姐一下情绪激动一把鼻涕一把眼泪，到时候就凄惨啦！
<br/>很久没有出门，坐在姐姐开的车上享受着外国的风，似乎和中国没有什么区别，但是心情却是不同的。
<br/>没有太多熟悉的脸庞，没有熟悉的气息，没有熟悉的街道，没有童年的记忆。然而眼前的一切并不是不熟悉，只是没有厦门的味道。那种可以让人怀念很久的味道。毕竟5年的熟悉度和13年的熟悉度还是有区别的，童年也是最值得怀念的。
<br/>于是也常常想到如果有天我站在厦门的街道是否也会怀念起墨尔本的街道。是我天生太爱念旧，还是我收藏了太多挥之不去的记忆。
<br/>念旧这个问题不想多说，明知道自己太爱收集，还不如少见少说为妙。所以我学会一种释放情绪的方式就是“笑”。</p>

<p>爱上笑之后，我感觉生活愉快了许多，不管为了什么，给自己一个微笑，心中的烦恼就会偷偷地躲起来，虽然它还是调皮的不肯回家，我却为自己高兴只因找到一个所谓是快乐的方式。
<br/>无论是真的笑，假的笑。这两种是有区分的。前者是用心去感受快乐，后者是用笑去掩饰一种不快乐。
<br/>笑多了，也就渐渐不去分析真假之间的道理。或者说无论是真的快乐还是假的快乐都是一种所谓的“快乐”。
<br/>还记得那句话“能安慰自己的人比较容易快乐。”笑也是一种用来安慰自己的方式。</p>

<p>“仁慈的父啊，请你告诉我，学会满足是否也是一种快乐？我什么时候可以学会呢？”</p>

