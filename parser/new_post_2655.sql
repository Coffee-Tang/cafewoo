二

转眼，小晴进公司已经一个多星期了，阿枫固定每天晚上给她打电话，有时也和她一起吃饭。

一天晚上，阿枫一如既往地打电话给小晴。小晴用抽泣的声音接了电话。

“小晴，你怎么了？”阿枫问。

“没……没有。”

“你哭了？什么事呢？”

“没事，真的没事。”

“我能帮你做些什么吗？”

“不用了，谢谢。”

