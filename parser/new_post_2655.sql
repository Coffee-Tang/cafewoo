两人坐了好一会，已经11点了。阿枫问道：“你累了吗？想不想回去？”

“嗯，也可以，走吧。”

这时街上人不多，显得很安静，两人走得很慢，阿枫犹豫了很久，才慢慢开口：“你有男朋友吗？”

