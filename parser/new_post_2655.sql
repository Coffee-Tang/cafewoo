阿枫知道小晴始终深爱着和她相处四年的男友，自己根本就没有改变她的希望，但又不理解她昨晚在电话里说的那些话。“我不想当别人的替代品。”，“你会选择现在拥有的，还是怀恋已经失去的东西呢？”。

此事之后，阿枫似乎和小晴变得陌生，说的话也越来越少……

一个月后，小晴突然向公司辞职。

阿枫意外地问道：“你找到新工作了？”

“不，我打算回老家去。”

阿枫的心情显得很沉重，又问：“什么时候走呢？还来吗？”

“这星期天吧，可能不再来了。我想回到家里，在那里重新生活。”

“哦，那祝你好运吧！”阿枫说完，回头工作去了。

阿枫很想让小晴留下来，可是他知道自己无法改变这个现实，也只有默默的接受了。

