喜欢看到稚气的脸，牵在一起的手外加开心的笑容。

喜欢躺在柔软的小山坡上，身后是缓缓流动的湖和漂亮绿色的树。

阳光洒满全身，孤独得没有时间难过。

有时候，会想找份工作，或者找个人，让自己停留下来。

有一次我去算命，那个算命的老头子看到我的手后很讶异地看着我，因为我的手指很长，尤其是中指更突兀得厉害，所以我常会幻想自己前世会是一种什么动物，猿猴这类的，虽然我希望自己可以是只兔子。

他说我的命很硬，也许终身一个人。

我突然想到郑秀文唱的《终身美丽》，一个人也是美丽的，但大抵上没人懂得欣赏。

大概是这样地过去了，一直在遇到人，也一直在离别。

最后呢？空留一堆极度沉重的想念。

除了牛仔裤和T恤，什么都不套在身上。睡觉的时候也只是一件长在膝盖的白色棉衣，上面的图案被我抠成碎碎的小点。

