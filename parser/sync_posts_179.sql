<br>02――快乐是一种沦陷
<br>我站在天桥上往下看，车呼啸而过，残留一堆余味和刺鼻的味道。
<br>平行地看前放，是高低不平的楼房刻在大片大片的白云和后面的蓝天上，云流动的时候像房子在游动。
<br>可爱不已。
<br>
<br>整个城市的喧嚣在眼前横行无阻，即使是夜晚。
<br>在这个城市已经不能再看到星星了，星星是寂寞的产物，而这里是一座快乐沦陷的城堡，走在路上都是亮眼的灯和行人。
<br>我突然想起伍。
<br>
<br>小的时候，很喜欢剪纸，和弟弟在家里的阁楼躲着，找各种各样的纸。
<br>阁楼的窗台上有一颗很小的白色的花长在一小撮泥土边，窗玻璃上刻着几条醒目的裂痕，似乎风一吹就会“哗啦啦”地全部碎裂。
<br>却从未有过，也许它们也懂得互相依靠。
<br>
<br>那时候不知道弟弟是谁家的孩子，也从没看过弟弟的父母，隐约记得是开始会说话时就有弟弟了，家里没什么人，只有小姑和奶奶照顾我们。
<br>大家管弟弟叫“小波”，他年纪小我四天，头发微卷带点棕红。有时我会从家里的楼梯栏杆上快速往下滑边亮亮地叫一声：“红毛小波！”。
<br>小孩子总是可以找到很多乐趣的。
<br>剪纸，滑楼梯，爬窗台，甩贴纸……
<br>剪纸也只会剪马的形状，也只喜欢大红色的纸。
<br>弟弟总是认真地把剪剩的纸收集起来，在本子上贴成各种图案，五颜六色的，像弟弟的笑容一样。
<br>
<br>和弟弟睡觉事弟弟的头都靠着我的头，他的呼吸打在我脸上，我的脸在睡醒之后通红通红的。
<br>我很容易做梦，几乎每天都会。有一次梦到一只大大的黑蝴蝶把窗台那朵小白花咬走了，然后眼泪就流了下来，滴到弟弟脸上。
<br>弟弟于是醒了，不停地轻拍着我直到我呢喃着睡着。
<br>从那时候起，我不管弟弟叫“小波”，而叫他“伍”。
<br>
<br>“伍”是一部电影里的主角名字，长得不帅的他遇到美丽的女主角。
<br>记得看这部电影时我才6岁，奶奶是很严厉的，几乎不让我们看电视，那一次她加班不在家，我偷偷打开电视，拉了弟弟坐在旁边看。
<br>结局好象是女主角搬到了国外，而傻呼呼的他在家自杀而死，很老套的一个故事，但是六岁的我哭了。
<br>最后一个场景放大了他不停往下淌血的手。
<br>我一直深爱鲜红的颜色。
<br>
<br>我很讶异自己还能记得这么多的事情，像是强制性地把记忆刻住似的。
<br>我现在是这个城市的一个游民，游民就是跟一条鱼一样在城市这座寂静的海底有动，不理会海面的亮丽，做自己想做的事和漫无目的。
<br>偶尔会写写文章，却总是写不出结局，如同自己的生活。
<br>
<br>很经常到大学里面去，因为会有很多的情侣，让我的眼睛感受别人的幸福。
<br>喜欢看到稚气的脸，牵在一起的手外加开心的笑容。
<br>喜欢躺在柔软的小山坡上，身后是缓缓流动的湖和漂亮绿色的树。
<br>阳光洒满全身，孤独得没有时间难过。
<br>
<br>有时候，会想找份工作，或者找个人，让自己停留下来。
<br>有一次我去算命，那个算命的老头子看到我的手后很讶异地看着我，因为我的手指很长，尤其是中指更突兀得厉害，所以我常会幻想自己前世会是一种什么动物，猿猴这类的，虽然我希望自己可以是只兔子。
<br>他说我的命很硬，也许终身一个人。
<br>我突然想到郑秀文唱的《终身美丽》，一个人也是美丽的，但大抵上没人懂得欣赏。
<br>大概是这样地过去了，一直在遇到人，也一直在离别。
<br>最后呢？空留一堆极度沉重的想念。
<br>
<br>除了牛仔裤和T恤，什么都不套在身上。睡觉的时候也只是一件长在膝盖的白色棉衣，上面的图案被我抠成碎碎的小点。
<br>已经很久没有剪纸了，偶尔经过美术用品店，耳边都会响起儿时快乐的笑声，又浮现了和弟弟开心的挑彩纸的画面。
<br>但是再也不去触碰那些东西了，因为快乐是一种沦陷，沉溺其中不能自拔。
