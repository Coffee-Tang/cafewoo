<br>05
<br>春暖花开，我的生日在春至，万雪消融的后一天。
<br>每一年我给自己买一个戒指，从我离开家之后我现在有十来个戒指，它们闪着漂亮的光泽，灿烂的银色。
<br>
<br>我用尼龙绳把它们串起来，戴在脖子上，叮叮当当，很好听。
