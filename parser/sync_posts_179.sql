<br>阳台上晾的牛仔裤已完全赶透，又多了一条泛白的条纹。
<br>穿上去感觉空荡荡的，总是会往下滑，耷拉着。
<br>我又瘦了，我不喜欢用皮带，因为除了衣服我不能容许任何其它事物来束缚着自己，
<br>走在街上，感觉自己是有分量的。
<br>
<br>我在思考伍是用什么心情写下那句话的，是要我等他吗？等待他的爱情还是别的什么？
