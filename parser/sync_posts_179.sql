<br>07――过期的褪色液体
<br>我在街上漫无目的地晃，跑到“雅坡”随便扒了几口饭，便开心了起来。
<br>天气微凉，是下过雨的清新，还混合着泥土和树叶的清香。
<br>走过有落地玻璃窗的商店总会忍不住看自己的影子，削得很碎的头发披在肩上，凌乱无序，朦胧的眼神看到的，依然只有自己。
<br>我这样自恋。
<br>
<br>夏天就这么突然到了，举起手，
<br>猛地就抓住一把阳光。
<br>
<br>因为找不到工作,我继续窝在家里,这是早就预料到的结局.
<br>长时间盯着同一个地方,很久很久,也许是天花板,也许是电脑屏幕,也许是镜子,
