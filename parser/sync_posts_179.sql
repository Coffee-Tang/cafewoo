<br>04
<br>什么东西都是可以被风吹散的吧。我在想。
<br>回忆也好，片段也好，心情或者故事等等，即使是最永恒的天空和土地，
<br>也不会永远停在那里。
<br>
<br>我把贴纸牢牢地粘在我的手臂上，过几分钟后狠狠地把它撕下来，
<br>动作迅速。
<br>拿到阳光下会看到我的手臂有一块方形的红肿，而贴纸背面会有皮肤上的碎屑。
<br>我的食指上圈着一条很长的鱼线，顶端是一只风筝在风里飞，
<br>像人的感情，期待却又不敢前进。
<br>
<br>钱包里有一张发黄而且皱皱的相片，是和伍拍的，那时我们小学一年级。
<br>家里养着一只肥肥的大兔子，我和伍总是“肥兔肥兔”的叫它，它跳的时候肚子上的肉也跟着一颤一颤，我和伍便狂笑不止。
<br>那张相片是我和伍坐着，背后是电影院的楼顶，苍白的建筑苍白的墙，我们抱着肥兔咧开了嘴笑。
<br>我们三个笑成一个样，纯真的脸。
<br>
<br>伍后来去了哪呢？我一直都没想起来，但伍说的话却一直记得。
<br>第一次跟我说话，伍说你是我的姐姐吗？是不是当姐姐的，眼睛都比较大？
<br>4岁的伍说，每个人的周围都好象一条街似的，就像我去找你，从一楼爬到阁楼，都是在走路一样。
<br>伍那时就很渴望行走，离开这个家，离开最爱他的人，和我一起离家出走。
<br>7岁生日的伍说，太阳为什么总是热的？为什么我的生日在夏天？我最讨厌夏天了。
<br>最后一次伍说，我们要拒绝一切爱情，无论对谁。那一年我10岁，他小我4天。
<br>
<br>我一直也没有恋爱过，也没有喜欢上任何人过。
<br>有时会对着镜子学情侣间的幸福表情，然后再无奈地看看自己从未被别人牵过的干净的手，委屈得想哭。
<br>其实流眼泪真的是一件很奇特的事，有的人哭过后什么事都能释怀，有的人越哭越伤心。我没有经常流泪，但眼泪却是说来即来的。
<br>
<br>那一天半夜醒来，看到窗外黑漆漆一片。
<br>心里于是想，再黑的天也有变蓝的时候，莫名的就一阵狂喜。我的窗台边还是种着一颗白色植物，夏天枯萎，秋天盛开。
<br>
<br>第二天早上下起了很大的雨。
<br>清晨就开始下着，大颗大颗的雨点用力地往下砸，屋顶发出“噼里啪啦”的响声。
<br>我把自己埋在被子里，缩成一团，抱着一条很旧的浴巾瑟瑟发抖。
<br>
<br>我睡觉一定会抱着这条浴巾，在小的时候是粉红色的，在十年之后已失去了柔软。离开伍那时我8岁，晚上睡觉总会想起伍急促的呼吸声和身体的温度。
<br>于是不停哭，把枕头哭湿一大片，累了困了也只能在枕头上找很小一块干燥的地方睡去。
<br>后来就找了条浴巾来抱着，一抱就已好多年。十多年的眼泪都在那里面了。
<br>
<br>8岁生日后不久，妈妈来奶奶这里把我接走了，什么都没带走除了那本伍贴图的本子。
<br>那个晚上伍微笑着看我和妈妈消失在楼梯口，走到巷口心里突然一阵纠痛，我哭着往回跑，站在奶奶家门口，白色的木门关得严严实实的，里头传来奶奶训斥伍的声音。
<br>我突然感觉这道门，如此遥远。
<br>
<br>每个礼拜天我都会回到这里，每个早晨伍总是笑咪咪的真在门口看我进门，拉着我的手到阁楼，看他新种下的仙人球。
<br>种在一个红色的小花盆里。伍问，为什么你喜欢红色呢？
<br>因为真实，耀眼，令人安全。我说。
<br>
<br>礼拜一到礼拜六，每天晚上我都会不停翻看伍的贴图本子，
<br>轻轻的抚摸着，有他的味道，还有阁楼的味道。
