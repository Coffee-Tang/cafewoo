<br>06――他不说话已经第七天
<br>之后有一个月我患了湿疹，导致发烧，一直都没有回到奶奶那儿去，直到痊愈后我急忙跑回去，伍却已经不在了。
<br>奶奶说他每个礼拜天都在等我，后来他爸爸来把他接走了，他要求他爸爸再给他一个礼拜，那个礼拜他不说一句话。
<br>
<br>奶奶给我一封伍留下来的信。
<br>伍说，我们要拒绝一切爱情，无论对谁。
<br>我泪如雨下。
<br>
<br>十年前的我，站在家门口等待。
<br>十年后的我，还是在等待，只是物换境迁。
<br>
<br>我相信他会回来，也许很久。
<br>
<br>雨下了一整个早上，下午却出现大太阳。
<br>懒懒的，就挂在天的那一边，金黄色的闪耀着。
<br>我睡到了中午，从被窝里钻出来的脸闷得发烫，软弱无力的手打开冰箱，里面空无一物。
<br>真该找份工作了，我想。
<br>
<br>肚子饿的时候躺在床上可以想到很多事情，因为那是最原始的渴望。
<br>我觉得任何事物都像你的胃一样，待久了即会被融蚀，一点渣偶不剩，你毫无选择。
<br>听肚子发出的声音，如此欲求不满。突然觉得很可笑，我是一个连自己都无法喂饱的人，居然还在奢求等待什么。
