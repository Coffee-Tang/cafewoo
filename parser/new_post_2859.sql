<p>在阳光灿烂的日子里，我习惯于繁忙的空隙里偷偷抬起头来瞧瞧外面变幻的云朵，嗅嗅透过玻璃洒了进来阳光的味道，这是我心里小小的欢喜。我为这样莫名其妙的感动快乐得一塌糊涂。可这些天老是下雨。下雨的天人总有点儿的懒。窗外只是灰蒙蒙的一片，除了层层的高楼我看不见那蓝湛湛的天那白悠悠的云。</p>

<p>下午的电话特别少，办公室显得很安静。只是感觉有点冷，我真象一尾体温冰凉的鱼阿我浑身发抖。我只好使着劲儿搓揉着冰冰的手指哈着热气。我以为自己百无聊赖我只想发发呆，其实手头的事还有好一些。</p>

<p>接着，婷来了。她来对帐。帐很清楚，事情一会儿就OK。在我的电脑室里我们挤在一块嘻嘻哈哈的聊天，这当儿经理正好不在。
<br/>婷跟我同龄。却帮她的父亲打理一个有几百个工人的厂。会常常给我捎来好听的CD和日剧。她漫不经心的模样和我很相似。今天我们甚至穿着一样的布鞋一样粉色的毛衣。我们可以叽叽哩哩的谈上好几个小时。从她小时候曾一口气吃下15根冰棍闹肚子疼到我可以整个周末都躺在床上梦游周公48小时不吃一餐饭，我们无所不聊。</p>

<p>婷总是一脸的倦容，带着些许天然的纯真。她不是那种生来好强的女孩，却不得已要天天忙忙碌碌。婷笑着说，这段日子四处要货款，会有几个公司拖拖拉拉惹得心烦。晚上回到家里，屋子空荡荡连个说话的人都没有。忍不住就想流泪。于是冲着空气大声嚷，不许哭不许哭。结果隔壁有人敲门，问，出啥事？</p>

<p>婷把这当笑话讲，直听得我心里酸酸的。随着新年的脚步慢慢逼近，生命的年轮上又绕上了一圈。我知道自己早过了如花的季节。行走的路却还看不见尽头，摸不着方向。生活还不至于将我磨练得珠厘必较，我却是慢慢看到了心的妥协。我听见了梦想破碎的声音。哗啦啦的在响。我无处诉说。我连大声叫嚷的勇气也没有。</p>

<p>曾经，我一直在做着一个绮丽关于流浪的梦。那是心中一块芳草地。然而，打从踏上这个城市，我几年不变做着同一份疲累的工作。我有稳定的生活我可以自己买喜好的玩意儿可我知道自己依然一无所有。我不敢放弃，一切依旧模糊。
<br/>梅里美笔下的嘉乐曼，那自由自在四海为家的吉普赛人，火红的罂粟和海妖的歌声。我看到了它远走的步履。终于，我落下了一滴泪。
</p>
