<br>“‘尾巴’！”我兴奋地叫起来，然而猫似乎早已把我忘了。是了，猫是认地方的动物。一时也没有吃的东西诱它过来，只能看着它转身离去，那条黑白灰相间、“装错了”的尾巴高高地竖在屁股后面。看样子已经成了一只厉害的老猫。
<br>
