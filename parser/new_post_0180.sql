八年后我参加了辰的葬礼。

“纯，”通知我的人是睿，他后来做了美术商人，帮了辰不少忙。“介绍一下，”他平举手掌引见了身旁的女子，“这位就是桔子。”

桔子淡淡地微笑。她佩戴着玲珑的藏饰，看起来出人意料地年轻，是一个小巧给人好感的女人。

我不知该怎样回应，也只量礼貌性地微笑着点头。小学毕业后便没再见过辰了，我并不知道他去了西藏，也根本没想过会参加他的葬礼，并且见到了桔子。他们――就像我母亲――已经是属于“过去”的某一段时光，与“现在”的联系几乎在我意识中断结了。

葬礼很简单，辰死于飞机失事，只有象征性的骨灰作为遗体。我无从想像现时的辰是什么模样，或许这样反而好。我所认识的辰与这里的其他人所认识的不同，他是一个单薄、温和、明亮的美院学生，画着躺在草地上的孩子，孩子肚子上蜷缩的猫，以及肥得像企鹅一样在草地悠闲啄食草籽的鸽子……他带走了那幅散发太阳气息的画，带走了永远的1992年……

“……很意外你会来。”睿坐在灵桌边，双手勾在椅背背后。

“你给了我电话。”我说，心不在焉地看着桔子挂满藏饰的手。那是一双不怎么白嫩的手，被颜料侵蚀的指甲没有上指甲油。

“……最后一次看见你，你只有这么大，”睿伸手比了比桌子高一点的高度。“现在呢？有没有180？”

“176。”我轻声说。

“为什么不继续学画？你很有天份的。”

“……”

桔子站起身：“对不起，我有点事先走了。”

桌边的人有的对她挥挥手。

“我也……”我在桌上撑起身，睿却伸手过来，用巨大的手掌覆盖住我的手背：

“我有些事想告诉你，等我一下。”

他站起来，走进灵堂向辰的父母亲属打了招呼，出来又和桌旁的朋友告别，然后才走向已在路旁等候良久的我。

“你……”他抬起手，略微犹豫了一下，还是搭在我肩上，“辰后来对我说了……你的事，你母亲的事……我很抱歉。”

“没关系。”

“……你真的……不再画画了？”

“嗯。”

“为什么？你真的很有天份，辰相当看好……”

“我已经决定了。”

“是啊，这是你的事……辰有一些东西给你，放在我这里，你现在有时间吗？”

我点头。于是我们去了他的画廊。

对仿欧式的装潢一向无甚好感，看到辰的作品被蛋白色的维纳斯落地灯和古铜色丘比特室内喷水装置逼仄着，对睿仅存的一点感激之情也烟消云散。我始终无法理解辰何以选择这样一个世俗的人作为他最好的朋友。

“我还是想问一下，八年前你说我像桔子……”临去时我忍不住问。

“那个啊……”睿笑了，指指我抱在怀里的辰的纸箱，“从前的事，它都会告诉你。”

“哦，谢谢……”

“等一下，我差点忘了……”睿在身上的口袋四下翻找，“这个，”他最终摸出院一串钥匙，我认出辰公寓的。“辰的父母买下了那套公寓，一段时间内那里不会有人，你可以去看看。”

我抱着纸箱直接去了辰的公寓。八年前跑出去的那天之后，我再未踏入这里过。现在，无甚讶异地，我发现这里老了。植物不加修剪地漫天疯长，原来的草地几近灌木丛了；砖砌的矮墙有一部分已经坍塌，其它则整个蒙上了一层灰。

我最讨厌的灰色。

花了很长时间打扫，成效也仅仅是开辟出了可以栖身的场所。我躺在用洗衣粉水拖过风干不久的外廊地板上，廊檐比印象中矮得多。房子是需要人照管的东西，辰去西藏半年，他的公寓因寂寞而破败了不少；一条四角蛇从我腿上蹿了过去，我一个激灵坐起来，瞥见围墙坍塌的洞旁一个白影――

“‘尾巴’！”我兴奋地叫起来，然而猫似乎早已把我忘了。是了，猫是认地方的动物。一时也没有吃的东西诱它过来，只能看着它转身离去，那条黑白灰相间、“装错了”的尾巴高高地竖在屁股后面。看样子已经成了一只厉害的老猫。

中午我才想起来打电话叫钟点工――早已不再是阿霞――不用做饭了。于是只来得及告诉她不用做晚饭，中饭仍是回家吃。纸箱就搁在了辰那里。

心情静得出乎意料。对于辰已经去世这件事，我似乎是将其当作类似“辰已定居爱斯基摩人的冰屋”这样的概念接受下来；四年前考到别市的专门学校时便已做好了不再见面的思想准备，如今只是再度确定了一下。

只是，并不是很想看他留给我的东西。

下午出门的时候，桔子拨通了我的手机（虽然不明白号码何以被她得知，但也并不是很有兴趣追问）。

