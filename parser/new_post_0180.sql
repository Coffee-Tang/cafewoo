“辰，他很像桔子……”睿对脸色发青的辰说，声音遥远却令人无法忍受……辰突然对他挥起拳头，睿高大的身体轰然倒地，整个世界应声摇撼起来，我摇晃着，脚轻飘飘地向外跑去，背后追赶着睿的叫声：

“辰，去西藏是桔子自己的选择……！！”

