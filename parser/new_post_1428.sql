<br>24/12/02   莱茵河瀑布 --&gt; Schaffhausen --&gt; Winterhur --&gt;  Basel      阴 
<br>
<br>
<br>从导游手册上得知火车会停靠在一个木制的，不足十平米的小站台，绕过站台，走不太久就能看到欧洲最大的瀑布－－莱茵河瀑布。但是奇怪的是，我们一路停靠的都是正儿八经的大站台，纳闷之际，只好询问正好走过的检票员。他一愣，指着车窗外说：“这就是了。”我们惊讶地向车窗外望去，原来火车正经过一座桥，而桥的一侧，赫然是一席宽阔的瀑布。原来冬天时火车在小站台是不停的。 
<br>
<br>在最近的一站下了车，很快寻到了莱茵河边，沿着水流的方向向瀑布走去。有很长的一段路，我们的周围寂静得只有水流的声音。河水湍急，却又清澈见底。河面上浮着一群群不时扎个猛子的水鸭。河边有人垂钓，可是水清则无鱼，看着他频繁地收线，甩杆，心想要在这样的河水里钓到鱼，实在是需要姜太公的耐心。时而有人遛着狗迎面走来，有只小狗突然一阵兴奋冲进河里，在水里刨了几下又回到岸上，甩了主人一身湿。河两岸的山坡上有许多小屋，在这样的环境里居住，想要不长寿估计也是件不容易的事。 
<br>
<br>当一对老夫妇从身边走过的时候，我们请他们帮我们照个合照。照完之后，两位老人兴致盎然地与我们聊天。老太太只会说德语，老先生会几句简单的英语，四个人只能鸡同鸭讲，比手划脚。老先生推荐我们去Schaffhausen，一个离瀑布不远的小镇，说是那里有一个很值得一看的古堡。一听到古堡，老太太难掩兴奋之情，眉飞色舞地说了好一阵子，老先生才不太好意思地解释说他们几年前曾经参加过一个在古堡里举行的舞会，舞会要求的着装非常正式，老太太觉得自己那天的礼服特别的漂亮。 
<br>
<br>告别了这对可爱的老夫妇，又走了不远，穿过之前火车经过的桥，就来到了瀑布边。瀑布的落差并不大，感觉比起在电视上见过的黄果树瀑布和尼亚加拉瀑布要逊色很多，但是由于河面宽阔，还是有几分磅礴的气势。 
<br>
<br>河对岸的山顶上有座教堂似的尖顶建筑，从桥上走过，上到那里，才发现它已经被改建成了宾馆。但是在那里发现了一个路标，写着“Best View Point”。我们大为好奇，就沿着路标所指的小路从山坡另一侧走了下去，穿过一个小小的山洞，就来到一处平台。在巨大的轰鸣声中，抬眼见到咆哮奔流的瀑布竟然近在咫尺，伸手可及。在这样近的距离欣赏瀑布，是会让人感到害怕的，就像站在高楼的顶层凭栏下望，明知危险，又有一种想纵身跃下的欲望。 
<br>
<br>--------
<br>
<br>Schaffhausen像欧洲其他地方的小镇一样，一条商业主街就是整个镇的中心。在这样的商业街上，即便是麦当劳，通常也只会开在一间外观老旧的屋子里。大概因为今晚是平安夜的缘故，街上的人并不多。街头艺人用竖琴弹奏着圣诞节的歌曲，欢快清脆的琴声传出很远。 
<br>
<br>按图索骥找到了那对老夫妇所说的古堡，古堡建在山上，要走上一段长长的石阶才能到达。古堡的外观就像是一个装生啤的酒桶，不同的是酒桶顶上开了四个大洞。古堡内的结构也很简单，白天的照明就靠从壁上的箭剁口和顶上的四个洞漏进来的光。依此判断建古堡的初衷只是为了单纯的军事防御。从堡内一侧的旋转石阶可以上到顶层，在那里可以俯瞰整个小镇。顶层上排了不少长椅，四个大洞也都盖上了粗铁网，估计这就是那位老太太所说的舞池了。有趣的是顶层的造型与常见的圆顶截然相反，像个浅浅的漏斗，四周高，中间低。我想这样的堡顶实在是宜古宜今，因为从前在古堡被围困的时候，它能用于积蓄雨水，现在它又能保证人们在跳舞时不会因为转得太欢快而从古堡边摔了出去。 
<br>
<br>下了古堡，心情舒畅，回到主街上。两天的旅行让我感到瑞士人不论男女，都极爱抽雪茄，不论是室内还是户外人多的地方，都能闻到浓浓的雪茄味。我又一次向自己的好奇屈服，拐进一家雪茄店，把它当博物馆一般参观了各式各样的烟丝和烟斗，最后决定买上一盒。迫不及待抽出一根点上，难抽无比，几欲昏厥。 
