<br>23/12/02     苏黎世         多云 
<br>
<br>
<br>出了苏黎世机场，乘上开往市区的列车，十分钟后就抵达了市中心的Main Station。 
<br>
<br>在苏黎世的旅游线路非常简单，只需要沿着苏黎世河两岸走一圈，就把这个城市的精华看尽了。Main Station里有棵高高的圣诞树，引得那些过惯了圣诞节的老外也举着相机拍个不停。以圣诞树为中心是一个集市，卖些小吃和工艺品，但是一切井然有序，没有吆喝，没有讨价还价，也就没有了热闹的感觉。 
<br>
<br>Main Station是Bahnhof街的起点。据说这是条与香榭丽舍齐名的商业街，据说瑞士是手表的天堂，沿街一路走下来，两种据说都得到了印证。圣诞节的灯饰让这条原本就珠光宝气的大街显得更加华丽。走在这样的地方，很容易让人走向两个极端：要么从此发愤图强，努力赚钱；要么把心一横，偷盗抢劫。一条路难，一条路险，我选择了中间：在街口小贩处买了一包炒板栗，既填填肚子，也算是消费了一把。只是连板栗都那么贵，心痛不已。 
<br>
<br>走到Bahnhof街的一半可以拐上山坡，石砖铺的小路两边是小资情调的鲜花店和时装店。坡顶则是一片开阔地，可以眺望市区的全貌，能看到繁华的河畔商业区和稍远处的住宅区，还有河口处教堂的双塔。这让我想到在国内的时候，城市里没有明显的商业区和住宅区的分别，从高处看一个城市的夜色总是让人觉得很平和，觉得一处灯光下都是一户人家，一户人家的故事都是一部电影。 
<br>
<br>三个小时之后，我们回到了Main Station，在这个城市的游览就算结束了。城市的设计者实在是有些卑劣，城市里没有太多景观，游人很快就会觉得百无聊赖，百无聊赖就要去找些事做，而在这里唯一能做的就是消费。不过消费原本也就在计划之中，因为这里有瑞士军刀和巧克力。 
<br>
<br>不知道有多少男人喜欢那把小小的瑞士军刀，反正我是暗恋已久了。把玩一把军刀，倒不觉得它会增添什么男人味，也和小资无关，只是觉得这样一把多功能的刀很有份量，够结实。常见的瑞士军刀出自两个厂家：Victorinox和Wenger，但是只有前者才真正为瑞士军队提供小刀。有趣的是，真正为瑞士军人所使用的”Soldier’s Knife” 并不是我们在商店里见惯的那种红柄小刀，而是银白色金属柄的一款，总共只有四个刀身：一个刀锋，一个改锥，一个可用作螺丝刀的开罐头刀，和一个可以剥离电线的起瓶刀。Elsener家族由1891年起开始为瑞士军队提供刀具，之后在1897年推出来一款风靡一时的“Officer’s Knife” ，而现在常见的红柄刀全都是在这一款刀的基础上发展起来的。商家实在是深谙消费者的心理，告诉我这么一大段历史，其实只是让我做一件事：买把红刀，再买把银刀。不过既来之，则买之，因为价格比起从前所见的军刀实在是低了很多。此后一路把玩，心满意足。 
<br>
<br>巧克力对老婆来说是个致命的诱惑，巧克力专卖店里摆的据说都是手工制作的巧克力。手工也好，机器也罢，好吃到让人上瘾是千真万确的事。吉百利容易发腻，在英国一年到头被大小超市轮流打折。而瑞士专卖店里那些暴贵的巧克力大都做得很精细，硬巧克力的外层包着软巧克力的馅，酒心果味各不相同，再用不同颜色的巧克力浆加上花纹，咬下去香甜酥软，回味无穷。卖家更是卑劣：先拿出些小的样品让你免费品尝，然后把你留在那里一边回味，一边看着价格。既来之，则吃之，可是内心的挣扎还是让我夺门而出，留下老婆在里头每式两份地精挑细选。 
