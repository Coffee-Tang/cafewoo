<br>Luzern的山光水色受到了太多关注，以至于只要在网络上查查资料，看看照片，就感觉已经在那里游览了一圈。市中心的Luzern湖上有一座曲折的木桥，连通湖中心的一座石头砌成的水塔。可惜的是旧桥在十年前的火灾中被烧掉了，现在游人走过的是随后依原样重建的新桥。 
<br>
<br>Luzern的另一处景观就是那只著名的垂死的石狮，用来纪念在大革命时期为法国皇室效力而死去的四万瑞士雇佣兵。不论是雇佣兵还是保皇的军队，都不是一个光彩的名称，但是死去的都是那些贫贱出身，毫无野心的农民子弟，倒下的都是只为图衣食饱暖的血肉之躯。石狮子靠在盾牌上，眼神悲凉绝望，健壮的上肢无力地垂着，背上的利刃直插至柄。这样的雕塑让人不忍瞻仰。离石狮纪念碑不远有间店面颇大的商店，圣诞节也照常营业，卖着各式各样的纪念品。历史是作家挂小说的钉子，也是商家摆放纪念品的橱窗。', '原本计划利用圣诞节假期去荷兰和比利时旅游，结果因为来不及申请签证，改为去瑞士。之前她在我眼里是一个有趣的国度:一个中立国，三种官方语言，许多国际组织的总部所在地。现在她在我眼里是一个美如仙境的国度，因为她的雪山和湖泊。结束了八天的精彩旅程后，记述一路的精彩片段，除了和大家分享外，也为自己留个纪念。

