出了食堂没走几步就被一只迎面飞来的足球把脑袋砸得稀里哗啦，我气愤地大声嚷嚷讨厌死了今天怎么这么倒楣！Fancy说别生气了，今天楣都倒完了明天一定走运。

果然第二天吃午饭时那个可爱的大师傅给我多打了一勺菜还少算了五毛钱，我偷偷地美了一中午。Fancy说你看运气来了吧，去买彩票吧，一定会中的。我就听话地用两块钱投了一注彩，然后傻咧咧地梦想自己中五百万的头奖。最后我中了一个五块钱的末奖但心里还是很美很美，然后我拿着净赚的三块钱请Fancy吃夹肉饼。

五

我在一个飘着很大片雪花的夜里开始给我的朋友们写贺卡。我身边摞着很厚的一叠刚刚买来的斑斓的小卡片。我把朋友们的名字在纸上列成长长的一串。我在贺卡上写满了我自由飞舞的美丽字迹。那时我心里有一种厚重踏实的幸福。

我开始真真切切地想念我的朋友。我一直相信他们是我心底最深处的依赖和眷恋。所以即使由于各自的忙碌很久很久都无法见面甚至没有一点点的音讯，我都会感觉他们就在我的身边不曾远离。就像我会选择在这样一个温暖的夜里用我平淡和简单的方式去祝福他们。一生快乐，这是很久以前我们许下的承诺。

然后我在灯下幸福地睡着了。

平安夜的晚饭学校的餐厅挂满了彩带和气球，圣诞颂的歌声在高高的屋檐下回旋，一派温情。大师傅们带着尖尖的红色帽子，脸颊被喜悦和热气熏成红扑扑的样子，一股热腾腾的温暖气息。我很奢侈地买了一只羊肉砂锅，大师傅递给我一瓶牛奶说这是圣诞礼物，我就大声地笑着对他说谢谢。

晚自习的时候学校很给面子地停电了，在一大片的欢呼声中很多很多的学生们冲出了校门，我想我们都太渴望这样一个自由的平安夜了吧。

街上的路灯长长地排到了很远很远，灯光打在安安静静的雪地上反射出琥珀一样柔和的光。许多许多裹着厚厚的棉袄的孩子样子笨拙地在上面滑雪，空气中荡漾着他们明朗的笑声。

午夜十二点时我将窗户打开，让温柔的凉风吹起我额头前碎碎的刘海。我很虔诚地坐在窗前双手合十地许愿。我想那一定是个很浪漫的姿势。

睡觉之前我特地将一只袜子挂在了床头。尽管我知道圣诞老人不会从烟囱里爬进来塞礼物在我的袜子里，可我依然固执地重复着小时候的习惯。因为我知道这样我就会做个好梦。

第二天清晨一睁开眼睛我就看到了整个世界沐浴着阳光。我轻轻地微笑并对自己说了一声：圣诞快乐！

六

十二月的最后一天我和我最好的朋友毛毛一起去看了那部很低级的搞笑片《大腕》可最终还是笑出了眼泪。走出电影院的时候我们看到每一个小小的角落里都用张扬的字体写满了“HAPPY NEW YEAR”，人们裹着厚厚的衣服匆匆地走，脸上带着许久不见的温暖的笑容。很多很多的妈妈手里牵着很多很多的举着气球的可爱的小孩子，五颜六色的漂亮的霓虹灯把整个城市照得异常明亮。

毛毛说小萌我们明年就要毕业了好快呀。我说对啊到时候就不知道会在哪个城市里过新年了。然后我看到毛毛的眼睛里一闪一闪的。她突然转过头来说小萌我们考到同一个城市去吧以后每一个新年我们都会在一起了。我兴奋地拍着手说好呀好呀就这么定了。毛毛的眼睛在灯光下亮晶晶的，很漂亮。

我们在一间别致的小店里为自己买了一条精美的小手链，然后毛毛送我去车站。等车的间隙里我给她唱了三首温暖的情歌，车站上的人们一直在微笑地注视我们。

上车后我在拥挤的人群中透过玻璃窗和毛毛挥挥手。当车子越走越远直到看不见她的时候我突然发觉眼睛湿了。我用手指在雾蒙蒙的玻璃窗上写下“新年快乐”。车子开得很慢很慢，车上的人们在安静地微笑。我看着车窗外过往的车辆人群和闪烁的霓虹，站在这最后一天的尾巴上，我开始真真切切地怀念我的二零零一了。

跳下车子的时候我深深地吸了一口气。前面不远处就是我的家。那里亮着温暖的灯光。我的爸爸妈妈一定会烧了一大桌的菜在等我回家。想到这些我就幸福地笑了。

再见吧，我的二零零一！

