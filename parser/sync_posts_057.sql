<br>倾听自然，倾听民谣流域--美国民谣之Simon &amp; Garfunkel
<br>
<br> 
<br>在近期的民谣流域节目中，我们将重温Simon &amp; Garfunkel 的经典老歌，Simon &amp; Garfunkel可以说是本世纪最出色的二重唱组，Simon 清澈嘹亮的嗓音加上Garfunkel时而高亢、时而婉转的和声把二重唱的优势发挥得淋漓尽致。如果当年你也被他们配唱的影片《毕业生》所感动过，那么千万别错过我们的民谣流域节目。
<br>
<br>Simon 和 Garfunkel 都出生于1941年，他们在纽约的皇后区一起长大。15岁时就建立了&quot;Tom &amp; Jerry&quot;乐队（不是动画片猫和老鼠）--一个类似于&quot;艾弗利兄弟的&quot;的演唱组，推出了一首热门歌曲《嘿，女生》（Hi，Schoolgirl），并出现在美国野外音乐台的节目中。Simon &amp; Garfunkel早期的作品都是由Columbia公司录制的，但最初并不引人注意。后来制作人Tom Wilson把他们的那首《寂静之声》加上了摇滚背景，才成为了一首打榜歌曲。随后Simon 又开始了同Garfunkel的合作，还举行了巡回演出，开创了他俩随后5年的合作生涯。
<br>
<br>在录音方面，Simon &amp; Garfunkel颇花费了一些时间才找到了把歌曲和演唱完美相配的乐器模式。1970年Simon &amp; Garfunkel迎来了他们生活中最辉煌的时候。专辑《忧愁河上的桥》以及同名单曲均成为排行榜冠军。歌中唱到： &quot;当你感到疲倦和自卑，当你眼中有泪，我将为你试去，我站在你的一边，当世事不顺，当找不到朋友的时候，我将用我的身躯为你架设一座急流上的桥……&quot;。任何处于孤独无助中的心灵，听到这样亲切感人的歌词，在耳边先低吟、后高唱，仿佛就在此刻遇到了自己最知心的朋友，因而为之动容。难怪这首配器简单，节奏缓慢的Bridge over troubled water在排行榜上停留了三十个月的时间，创下了一个几乎无人能够企及的记录。还有一首始终贯穿影片的插曲Scarborough Fair，也同样给我留下深刻的印象。这首歌就像一部印象派的风景画，如果你闭上眼睛做一次深呼吸，一定还能闻到空气中夹杂着各种香草的气息。Scarborough Fair是一首取材于民间题材的歌，他运用的是苏格兰曲调，而另一首If I Could，采用的则是南美印第安曲调，但两首歌听上去又都是那么的完美绝伦。
<br>
