<br>1976年，李双泽、杨弦所倡导的民歌运动引发了之后的民歌风潮。
<br>李双泽从国外游历回来，因不满当时许多歌手抄袭或改编美国等乡村及流行歌曲的乐坛状况，经常在朋友的小型音乐聚会中演唱内地电影《上甘岭》中的插曲《我的祖国》（一条大河波浪宽，风吹稻花香两岸。。。），由此激发其创作热情。
<br>1975年的6月6日，杨弦和他的朋友在台北中山纪念堂举行“中国现代民歌之夜”，杨弦演唱了自己创作的8首歌曲，歌词采用的是台湾著名诗人余光中的作品。这些新颖的作品受到了民众的极大欢迎，录制的专辑在台湾岛内再版十余次。 并由此引发社会民众大讨论，有人质问：
<br>“这种由少数人制作演唱的歌曲是否能称作民歌？民歌需要根基深厚，需要天长地久的磨练，是由下而上的转变，怎么能像杨弦这样由上而下的[炮制]民谣？”
<br>一时间台湾社会上引起了空前的民谣争论风潮，台湾岛内的各大报刊乃至学校纷纷举办各项活动，来探讨这种被杨弦标榜为“中国现代民歌”的音乐风潮。
<br>这场自上而下，从校园逐渐波及大众的讨论，不仅使杨弦等人在校园的象牙塔里创作的歌曲走向了大众，也同样使台湾的大众接受了这种发起于校园的“民歌”，因此，二十六年前的六月六日所开的那一场“中国现代民歌演唱会”，也就被业内人士视为后来席卷全台湾岛的“民歌运动”的正式开端，也就是民歌纪念日的由来。
<br>1977年，新格唱片公司发现这一流传于校园内部的民歌所能带来的巨大的商业机会，开始创办了金韵奖民歌大赛，正式开始介入民歌的制作与发行、全台湾各大专院校喜欢民谣的学生都可以参加，无疑为民谣的发展提供了更多契机。
<br>但在当时的台湾，更多的人对民歌的发展抱的是怀疑态度，他们质疑，会有那么多学生能唱歌？会有那么多学生能写歌？在这一片怀疑声中，一九七七年夏，首届金韵奖青年歌谣大赛诞生，开始了一个真正属于年轻人的歌唱时代。金韵奖大赛最大程度上丰富了民歌的表现形式，并使得更多的年轻人得以参与到流行乐的改造中。
<br>在六届金韵奖大赛中分别涌现出了陈明韶、包美圣、杨耀东、邰肇玫、范广慧、齐豫、王梦麟、李健复、黄大诚、木吉他、王海玲、郑怡、王新莲、马宜中、施孝荣、杨芳仪、徐晓菁、郑人文、苏来、郑华娟等现代民歌的代表人物。
<br>民歌在七十年代已逐渐深入到了当时台湾人民生活的每一个角落。
<br>在民歌时期主要有两家唱片公司在不断的耕耘，一个是新格另一个是海山。新格代表着金韵奖，海山则承办着民谣风大赛，海山的代表人物包括蔡琴和叶佳修。两家公司在当时通过各自举办的大赛挖掘歌手，并为获奖歌手推出合辑，在当时影响非常巨大。另外老牌唱片公司歌林公司也推出了金曲奖与上两者竞争。
<br>自1978年，由台湾中国广播电台出资，著名音乐主持陶晓清一手策划了民歌电台排行榜，并组织了大量的校园演出致力于台湾民歌的发掘与推广。
<br>商业元素的介入固然给了民歌运动以极大的推动力，但纯粹商业利益的驱动，造成民歌歌手泛滥一时，尤以单纯校园风格的象牙塔式歌曲为最，以至于留传到今日，大家提起来最熟悉的台湾民歌也大多都是些单纯的爱情小品之类。
<br>进入八十年代之后，单一的音乐路线，无疑为现代民歌敲响了警钟。对于新一代的民歌继承者而言，他们更希望通过自身的尝试来扭转这一现状。这个时期，正是台湾民歌史上创作多样化、台湾乐坛百花齐放的初春。
<br>在作词方面，从早期的以现代诗入歌（杨弦《乡愁四韵》、李双泽《少年中国》）进而演变出以古诗入歌（邰有人《一剪梅》、《如梦令》）、古曲改编填词（吴楚楚《阳关三叠》）、各地童谣民谣改编以及全新的创作歌曲，在作曲编曲及演唱形式等方面，也进了多方尝试。山地音乐风格的《拜访春天》（首唱施孝荣），蒙古民歌风格的《出塞曲》（首唱蔡琴）、《柴拉可汗》（首唱李建复，同时也是《龙的传人》在台湾的首唱），乡村民谣风味的《外婆的澎湖湾》，《走在乡间的小路上》（作曲者均为叶佳修）甚至包括艺术歌曲风格的齐豫、校园派男声合唱风格的木吉它、杨芳仪、许晓菁首开的女声二重唱组合都可以算是当时的侪中翘楚。
<br>这些努力的尝试还有那些在这场民歌运动中涌现出来的歌手及创作者，无不为台湾流行音乐的发展起到了不可估量的推动作用，即使。
<br>也因为此，虽然台湾民歌运动的兴起和衰退都离现在时隔许久，但它却孕含了一代无邪的期盼与梦想，同时也是台湾乐坛发展的一个重要思程碑。
<br>
<br>后 记
<br>其实自己，也包括许多在八十年代成长起来的同龄人，最早熟悉台湾校园民歌，都是通过一些地下渠道的传播，比如海外电台或是一些有海外关系的人带回来的三洋双喇叭放音机，和邓丽君、刘文正一起来到我们身边。同时，更多地是通过一些大陆歌手，或是台湾歌手后来的翻唱，才能让这些单纯而美丽的音乐也飞过海峡，传到我们的身边。
<br>叶佳修的《外婆的澎湖湾》是最早流传到大陆的台湾校园民谣之一，但在上一世纪八十年代初，大陆人更熟悉的是王洁实谢莉斯版本。后来王洁实谢莉斯还翻唱了叶佳修的《踏着夕阳归去》、《赤足走在田埂上》等作品让我等那时尚是小学生中学生的人大饱耳福。叶佳修在1982年随陶晓清组织的现代民歌乐府赴美国演出时，就有来自内地的留学生追着叶佳修问：
<br>“啥时到咱们北京去演出呀？”
<br>在陶晓清主持的“民歌时间”里，可以听见陶晓清停了一下，改变自己的台湾国语腔调，用北京话说出这样一句话，听来真让人忍俊不禁。
<br>台湾著名音乐人侯德健，台湾民歌运动中的著名人物返回大陆，也把台湾的许多民歌带了回来，其中也包括他自己创作的《捉泥鳅》、《归去来兮》等作品，由程琳等人演唱出来，传遍大江南北。
<br>接下来在内地涌现出来的诸多歌手如张蔷、张行、吕念祖、周峰等人，因为那个时候大陆原创音乐作品非常贫乏，因此无不大量地采用了当时台湾的流行音乐，甚至更早期的民歌。尽管张蔷等人的嗓音条件完全不适合唱民歌，也让当时没什么见识的自己听得摇头晃脑，追星不已。
<br>费翔算是最早在大陆出版专辑的台湾歌手之一，他当年红得是如此之快，仓猝之间到哪儿去找那么多好歌来唱呢，于是在他的专辑里也出现了大量的台湾民歌，如《想飞》和《月琴》（在费翔的专辑里名为《思想起》）都是翻自郑怡的作品。
<br>进入九十年代之后，经历了商业歌曲的喧嚣，那个时候的台湾乐坛在极大的繁荣之下掩盖着更多的贫乏，唱片公司一改从民歌时代挖掘金嗓子，挖掘有特色的嗓音条件的习惯，改为挖掘美丽的脸蛋，有许多歌手，是凭一出电视广告甚至一张艺术照片，就能堂而皇之地经过短时期的训练，进入录音棚录歌甚至是一句句地“拼歌”。台湾岛内开始有大量的翻唱民歌专辑出现，在事隔多年后的今天，那些翻唱专辑往往是我们了解台湾民歌的另一扇窗口。其中较为知名的有张清芳的《出塞曲》专辑，南方二重唱的五张《回归线》专辑，老一辈的民歌人物蔡琴也曾出版过《民歌蔡琴》和《遇见》等翻唱专辑。无论在台湾还是在大陆，都成为热销之作。
<br>当我们拂去岁月的尘埃，让我们的目光穿越那雾霭茫茫的台湾海峡，突然间发现这一怀旧的年代不仅连我们已知的回忆都要怀旧，连未知的回忆都可以令我们继续感怀了。
