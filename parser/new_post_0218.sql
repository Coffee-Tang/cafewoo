清晨，青灰色的太阳懒懒地停在地平线上，地面的积雪被染成古碑般的青色，显得更加冷清荒凉。树枝被厚厚的积雪覆盖的严严实实，干枯的枝条赖不住重压，不时有僵脆断裂的枝条从空中坠落，划出一道雪弧。操场地面的积雪上布满了杂乱的脚印，深浅不一的印记就像是月球表面不规则的陨坑。经太阳光照射，整个地面升腾起一片淡淡的雾气，弥漫在空中，将整个世界模糊了。

在这个白色的世界中，一个黑色的身影极不协调地出现在画面中，成为整个视野的焦点。叶古星穿着一身纯黑的运动服，戴着一顶黑色的毛绒帽在白色萧刹的操场中独自进行晨炼。在白雪皑皑的大地上，叶古星一袭黑衣的打扮犹为显眼。他像是铆足了劲的弹簧一样一轮接一轮地进行冲刺，像一只出击的猎豹。每一次冲刺都使他精疲力尽，但在略微休息后他又开始下一轮的冲刺。当他休息时，双手撑着膝盖，静静地站在跑道中，大口地喘着粗气，颈上青色的血管在急促的呼吸中抽搐，口中呼出的雾气在眉毛上凝结成一个个微小的冰晶。最后，他缓缓地脱掉一件件上衣，从地上抓起一把松软的积雪擦拭赤裸的上身，升腾而起的雾气将他严实地包裹其中。

关度飞一路走一路与沿途相识的人打招呼。他的人缘不错，这源于他的出色。首先，关度飞高大俊朗的外表就足以使其鹤立鸡群，站在人群中，他那股脱颖而出、锋芒毕露的气质总能不经意间使他成为众多目光的焦点。其次，他的口才，他的文采，以及他似乎天生的领导才能与气质和与众不同的思想总能屡屡使其傲视群雄。特别是他依靠个人的热情与报负开创了在校内与校外影响颇大的文学社的经历，更使他成为了校内的名人。文学社自开办以后，屡屡发表一些针砭时弊、纵横山河，富有青春斗志的文章，得到全校学生的推崇。校外已有多家报社向他发出椭榄枝，希望他能够参加报社的工作，但都被他婉言拒绝。用他自己的话解释，“燕巢岂能容鸿鹄！”

