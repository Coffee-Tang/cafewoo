<br>清晨，青灰色的太阳懒懒地停在地平线上，地面的积雪被染成古碑般的青色，显得更加冷清荒凉。树枝被厚厚的积雪覆盖的严严实实，干枯的枝条赖不住重压，不时有僵脆断裂的枝条从空中坠落，划出一道雪弧。操场地面的积雪上布满了杂乱的脚印，深浅不一的印记就像是月球表面不规则的陨坑。经太阳光照射，整个地面升腾起一片淡淡的雾气，弥漫在空中，将整个世界模糊了。
<br>在这个白色的世界中，一个黑色的身影极不协调地出现在画面中，成为整个视野的焦点。叶古星穿着一身纯黑的运动服，戴着一顶黑色的毛绒帽在白色萧刹的操场中独自进行晨炼。在白雪皑皑的大地上，叶古星一袭黑衣的打扮犹为显眼。他像是铆足了劲的弹簧一样一轮接一轮地进行冲刺，像一只出击的猎豹。每一次冲刺都使他精疲力尽，但在略微休息后他又开始下一轮的冲刺。当他休息时，双手撑着膝盖，静静地站在跑道中，大口地喘着粗气，颈上青色的血管在急促的呼吸中抽搐，口中呼出的雾气在眉毛上凝结成一个个微小的冰晶。最后，他缓缓地脱掉一件件上衣，从地上抓起一把松软的积雪擦拭赤裸的上身，升腾而起的雾气将他严实地包裹其中。  
<br>关度飞一路走一路与沿途相识的人打招呼。他的人缘不错，这源于他的出色。首先，关度飞高大俊朗的外表就足以使其鹤立鸡群，站在人群中，他那股脱颖而出、锋芒毕露的气质总能不经意间使他成为众多目光的焦点。其次，他的口才，他的文采，以及他似乎天生的领导才能与气质和与众不同的思想总能屡屡使其傲视群雄。特别是他依靠个人的热情与报负开创了在校内与校外影响颇大的文学社的经历，更使他成为了校内的名人。文学社自开办以后，屡屡发表一些针砭时弊、纵横山河，富有青春斗志的文章，得到全校学生的推崇。校外已有多家报社向他发出椭榄枝，希望他能够参加报社的工作，但都被他婉言拒绝。用他自己的话解释，“燕巢岂能容鸿鹄！”
<br>当关度飞看见晨炼归来的叶古星时，远远地招呼道：“古星！”叶古星听到有人叫自己的名字，抬起头顺着声音寻到关度飞，他点了点头向关度飞示意问候。关度飞大跨步走到他的面前，笑着问：“又去进行自我摧残了？”
<br>叶古星淡淡地一笑，没有说什么。
<br>关度飞的性格是高傲的，在其他人面前他会凭借自己的优越从气势上压倒对方。然而在与叶古星的交往中，他却显得很随意。关度飞与叶古星的结识是一次很偶然的机会。在才进入大学时，学校组织了一次游览长城的活动。关度飞不是第一次爬长城了，但他对长城仍然抱有很大的兴趣。他准备了充足的行李，其中还有帐篷，让人看了感觉他更像去野营。别人告诉他有车接送，爬的时间只有几个小时，而且也不在长城上过夜，大可不必准备这么多行李，他对这些话置若罔闻，全然不顾，他有自己的打算。在他看来，这次所去的长城不过是现代人在原有基础上进行大规模维修后的“仿制品”，他倔强并寻求冒险的心里不允许自己只是在这种“今人的玩具”中走过场，他要寻求的是真正的属于自己心中的长城。于是他就独自前往另一段未加修葺的古长城。长城上是残垣断梁，是厚实的地衣和杂生的枯草，一个人走在这段长城上，远离都市的喧嚣，远离现代虚伪的文明，在这种环境中才寻得一份古人修筑长城的苍桑与凄凉，才体会到塞外胡马北风，吹角连营弦惊中历史的艰难与叵测。他是有文学修养的人，他也写过诗，他明白只有此时此刻的心情才能真正意义上的吼上一嗓子富有豪放诗人味的“不到长城非好汉”，而那些只是在观赏用的长城上买几件简单纪念品的人根本就是不解历史与风情的看客。他为自己高高在上，超凡脱俗的见解欣喜，但他也同时感到了曲高和寡的孤独。当他在落日的余辉中爬上长城的最高峰时，他吃惊地看到已经有一个人站在城墙上，张开双臂在风中沐浴落日，深邃的双眼凝神于远方连绵的群峰。这个人就是叶古星，从这一刻起，叶古星就在关度飞的心目中占据了重要的地位。
<br>而真正使关度飞器重叶古星的原因在于叶古星的冷漠。在众人的眼中，关度飞具有无法逾越的优秀。因此他在众人的眼中似乎永远处于高不可攀的地位，与他交往的人都在不自觉中对自己进行了遮掩，这仿佛是人的一种本能，更多的人在他的面前显现的是自卑与怯弱，谁都不敢轻易跨过这条鸿沟，始终游离于这条边线外。而叶古星却漠视这条分界线，不仅如此，可以说叶古星漠视所有的一切。叶古星与关度飞完全是两种类型的性格，如果说关度飞热情如火，那么叶古星则更像一块彻骨的寒冰。如果将关度飞比作澎湃的海潮，那么叶古星则是深不可测的深潭。在所有人眼中，叶古星除了行为比较怪异，常作出一些使人无法捉摸的举动外，对他本人并没有太多的映象。在他们看来，叶古星无足轻重，不过是一个小人物。但关度飞却能看到叶古星冷淡的背后是对世界的嘲笑与不屑，他知道他们俩有着共同的高傲。他特别欣赏叶古星对世界冷漠的态度，用他的话来形容他自己是万兽之尊的猛虎，而叶古星则是一只不羁的独狼，双方都有嗜血的欲望，都有与命运抗争的勇气与决心，并且都是在孤独中做战。
