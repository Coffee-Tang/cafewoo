关度飞的性格是高傲的，在其他人面前他会凭借自己的优越从气势上压倒对方。然而在与叶古星的交往中，他却显得很随意。关度飞与叶古星的结识是一次很偶然的机会。在才进入大学时，学校组织了一次游览长城的活动。关度飞不是第一次爬长城了，但他对长城仍然抱有很大的兴趣。他准备了充足的行李，其中还有帐篷，让人看了感觉他更像去野营。别人告诉他有车接送，爬的时间只有几个小时，而且也不在长城上过夜，大可不必准备这么多行李，他对这些话置若罔闻，全然不顾，他有自己的打算。在他看来，这次所去的长城不过是现代人在原有基础上进行大规模维修后的“仿制品”，他倔强并寻求冒险的心里不允许自己只是在这种“今人的玩具”中走过场，他要寻求的是真正的属于自己心中的长城。于是他就独自前往另一段未加修葺的古长城。长城上是残垣断梁，是厚实的地衣和杂生的枯草，一个人走在这段长城上，远离都市的喧嚣，远离现代虚伪的文明，在这种环境中才寻得一份古人修筑长城的苍桑与凄凉，才体会到塞外胡马北风，吹角连营弦惊中历史的艰难与叵测。他是有文学修养的人，他也写过诗，他明白只有此时此刻的心情才能真正意义上的吼上一嗓子富有豪放诗人味的“不到长城非好汉”，而那些只是在观赏用的长城上买几件简单纪念品的人根本就是不解历史与风情的看客。他为自己高高在上，超凡脱俗的见解欣喜，但他也同时感到了曲高和寡的孤独。当他在落日的余辉中爬上长城的最高峰时，他吃惊地看到已经有一个人站在城墙上，张开双臂在风中沐浴落日，深邃的双眼凝神于远方连绵的群峰。这个人就是叶古星，从这一刻起，叶古星就在关度飞的心目中占据了重要的地位。

