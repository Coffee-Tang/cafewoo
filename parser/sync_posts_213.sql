和黄昏的见面越来越多了。我开始每天的黄昏和黄昏相约见面。我们两个每天都会骑着自行车，像黎明和张曼玉一样在大街和小巷里穿行，然后找一处草坪坐下来，说说话的同时再吃着冰淇淋。黄昏特别喜欢吃肉夹馍，这一点正好和我一样。于是，我们的晚餐会是肉夹馍和着砂锅一起吃，有时候也会吃一些烤肉。

行进在平日里走了许多回的路上，我和黄昏总是说着一些轻松的话题，以至于嘻嘻哈哈的一路上都是我们的笑声。这一天，我和黄昏在一起直到天明。

黄昏的肌肤很滑，我们在一起的第一个晚上我就告诉黄昏说我喜欢这样的感觉。在夜幕里我们相互拥抱着感受这种感觉，夏夜的风悄悄的拂过肢体，我知道我们已经很实质的体会到了爱的滋味。

每天的黄昏时分，我会打电话约黄昏见面的地方。其实也总是在后街的某个地方而已，其实黄昏跟我是同一个学校的。看着人流在眼前交织，目光停留在一个又一个人的身上，最后总能找到黄昏的笑脸。

其实，我也知道我就要离开古城了，可是黄昏却选择了古城生活。其实这一点我一开始便知道。知道我总会和黄昏分开的，知道我和黄昏在一起的日子不多了。只是这一点黄昏并不知道，并不知道我要去何处谋生，并不知道我何时会走。

于是，这个夏季的夜晚，我和黄昏一次又一次的睡在了一起。

我与六月的电话总是在每天的夜晚。六月在另外一个城市里生活。我们说好了我毕业后会去那里见面。

我们每天的EM的内容也更趋炽热。会讲述一些内心的渴望与冲动，只是当电话连接我们时，我们又羞于启齿说出内心的涌动。这种欲望与渴望在憧憬中交织着，我们计划着相见的日子是越来越近了。我和六月已经走进了七月。

我告诉了六月我曾经爱过，六月也讲述了自己的情史并且告诉我现在正谈着一个。

我们犹豫着，我们抉择着，我们权衡着，我们进行着。在一天一天过去的日子里，我们仍旧兴致盎然的沟通着彼此的喜怒哀乐，要么是我在电话这头静静的听着六月的表述，要么是我在这头努力表达着现在的心情。我知道六月很慎重，六月总是在我说完后总结几句再引伸几句。

一位网友从哈尔滨来到了古城，我们相见了。那是我离开古城前五天的一个晚上，我们约好在十点钟在后街尽头的东东包门前见面。在这里就以十点来称呼这们人物的出现吧。

十点打电话来与我相约见面事宜时，是晚上九点半钟。十点说要不明天现见？在我的要求下我们约好了地点和时间。可是由于是第一次相见，十点说会拿一本七月的读者在手里，会穿一件深蓝色的衬衣。我告诉十点说我会淡蓝色的恤衫淡蓝色的仔裤和淡蓝色的帆布鞋在那里等着。

看着走过来一个人，完全符合十点描述的装束。我就挺直了身子站在那看着十点，当十点的目光停留在我身上时，我左嘴角上扬不露牙齿的笑了笑。

选择了路边的一家砂锅店坐下，局促的十点似乎不知道该说些什么，我于是也选择了聆听。将沉默进行到底。

幸好有东西吃，可以占住嘴。我们就漫步走向高新区的大路上，简单的谈谈彼此简单的谈谈一些问题的看法。

我和十点再次相见时是早上九点。随后我们去了十点住的地方，十点给我看了早已答应给我看一些文字。这些记录十点以往情史的文字中我知道了十点更多的感情世界，也促使我开始清醒。

