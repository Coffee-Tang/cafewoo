早晨的风从森林的方向出发了，带来淡淡的嫩叶味，微微拨动着老猫稀疏的白毛。我伸出食指轻搔老猫的下巴，它享受般地微闭上双眼，只剩下一小条缝隙填充阳光，像两枚金色的新月，孤零零地挂在前额稀疏的乱毛后。

它喜欢这里吗？我时常这样想。除了可以到处跑跑走走，捉几只蟋蟀蚱蜢来戏弄以外，也只能在午后溜到屋顶去晒晒太阳。庄园是不养猫的，所以几乎没有人喜欢它，甚至我也只是出于同情才肯收养它，要不然它也不会落到现在这个下场――还有两个月就要和死神约会去了。

老猫其实一点儿也不老，它的眼中依旧满载着青春，满载着过去绚丽多彩的绮梦，它在想什么呢？是在回忆上位主人温和的笑容，还是在考虑今晚该睡在谷仓或是储物的暗阁楼？

森林风的衣褶上沾满了苜蓿花的花粉，染成一片淡粉色。老猫突然直起身子，敏感地抽动着鼻翼，不失风度地打了个喷嚏，胡须上多了些粉色的斑点，像个毫无知觉的老顽童。

我无端生起一丝爱怜，细心地帮它抹去那些斑点，把它抱到长裙上，拿出随身携带的小木梳，一缕一缕地梳理它有些干涩的毛发。老猫很轻，放在腿上就像一张温暖的薄毯，身体干瘪得仿佛只剩下些许零星而苍白的回忆，还残留着眷恋的双眼，默默地，默默地遥望着那森林，就像一个垂暮老人安详地翻看着泛黄的童年照片。

――正午，阴云笼罩着天空，隐隐可以感觉到太阳大功率的辐射，庄园充斥着躁动的闷湿的空气，野玫瑰的花瓣上布着一层细密的水珠，空中灰色的压迫感让人喘不过气来。

“铃铛，吃饭了！”我端着银盘满庄园地找老猫。别担心，家人和佣人正在午休，不会发现银盘被我拿来装鱼了，嘻……不过话说回来，这个要死的家伙还真会跑，庄园已经被我翻得底朝天了，居然连根猫毛都没找到，难道它溜到庄园外去了？

正想着，一个瘦不啦叽的影子从屋顶“唿”地一下蹿到了我脚边的草地上，直奔向庄园的大铁门。“铃铛！”我喊道，那个影子扭过头来，像看陌生人似的瞥了我一眼后，自顾自地越过铁门，目标直指我从未去过的大森林！

“喂，别跑！”大门锁上了，只好跳过去了。幸好刚刚换上了长裤，跑起来快多了，隐约能够看见老猫那瘦弱但敏捷的身影。这家伙肯定溜到森林里很多次了！看它那轻车熟路的样子，净拣平坦的小路走。真是，难得从厨房偷了条鲜鱼来给你补充营养，跑什么跑，看我等下怎么收拾你！

10分钟后，我跑进了森林的心脏，这里比外面还要糟，树木蒸腾作用而产生的水气已经大面积地附着在脸上、手上和脖子上，湿漉漉的。四周的树干都现出潮湿的棕褐色，树枝间甚至有薄薄的水雾。刚才的那阵剧烈运动本已使我热得够呛，现在连呼吸都感到困难，我不行了，放弃对铃铛的“追捕”，我要回去了！

讨厌，人家从出生以来就没敢踏进这片森林，鬼知道这里这么恐怖，铃铛它没事跑到这里来干吗，庄园比这里还恐怖吗？

但是，很快我便没有机会再来想这些了，因为，我发现了我这一生中铸下的一个大错――擅自闯进密林的报应――我迷路了！要死啊！不要说太迟回去会被妈妈和爷爷臭骂一顿，我能活着走出森林就已经阿弥陀佛了！

第一反应自然是四处乱窜，情形如同在迷宫中作智商测验的小白鼠。直到森林渐暗，我的动力耗尽，紧接着，一件更倒霉的事让我有幸再一次验证了祸不单行这个颠扑不破的真理。那个欺软怕硬的老天竟然开始下雨了，而且居然还是……滂沱大雨！天呀，我不就是喝汤不用勺子、半夜睡在树上嘛，犯不着这样惩罚我吧！要命，这雨连大树都挡不住，我我我……我肚子饿了怎么办！唉！我真是笨得可以，竟然为了那只破病猫把自己搞到这步田地，今天早上还同情它，笨蛋，笨蛋，笨蛋！

眼看着原本就坎坷不平的小道被雨水修饰得更加泥泞不堪，我即将罢工的脑袋神经质地闪过一个念头――去睡觉，到树上去睡觉，睡一觉就没事了。这种古怪的想法也许是受了《艾丽丝梦游仙境》的影响，那是在家庭教师的威逼利诱之下用15分钟读完的。

然后我理所当然地爬上一棵自认为安全可靠湿度与坚韧度兼有品质保证的小树――树太高容易被淋湿――摆好标准的防摔pose，随着一声猫叫合上了眼…

猫？就是那个把我害成这个狼狈样的东西？我能放过它吗？不，不能。大脑坚决地下达了死也要拉个垫背的指令。

放弃了舒适的睡姿，我抓着树干努力让自己坐稳，像一只饥饿的野兽般注视着传来声音的方向，嘿嘿，等着瞧，我等下要上演生擒活捉的好戏！我激动的甚至来不及思考那声猫叫究竟是铃铛发出的还是林中野猫在叫春。

等了约5分钟，毫无动静；10分钟，饥饿和疲劳以及一身湿漉漉的便服提醒了我的处境；一刻钟，睡意渐起；20分钟后，我已完全丧失信心，长发服服帖帖地与脸颊“结伴”，我现在看起来一定很像鬼，但是如果熬不过今晚，我就真的是鬼了；25分钟，头昏得厉害，恍惚中看见天使的光辉渐渐接近了……咦？这个天使为什么没有光圈和翅膀？莫非是天堂的人手不够用，胡乱派了个人来凑数？不过这个天使看起来还不错，总比死神强吧……

等……等等！光芒为什么变远了？难不成我真的罪大恶极到要下地狱的程度？我不要！我还要去天堂里向奶奶讨零花钱，怎么可以这样轻易就把机会放走？

情绪高昂的我身子往前一探，刚要高声呼喊之际，猛觉头重脚轻，整个人就像木偶一样痴呆地被烂泥地面吸引了下去，无论是场景还是音效都颇为壮观，俨然有彗星撞地球的意味。在半昏迷的状态中，天使冰凉的手搭在了我的额头上，莫不是在做天国之旅前的祷告？祷告词早已听不清楚了，只求快点结束，让我脱离苦海。

……很暖和……

身体有规律地轻轻摇晃，像童年时的摇篮，充满回忆的味道。

犹如身处蓝色的水波

古老的故事在轻轻晃动

谁也不知那静谧的角落

隐藏着如此悲哀的轮廓

微冷的世界

弥漫着困惑的雾色

神圣朦胧的亮光中

我看见了……

我看见了……

睁开眼。

我看见了温暖的阳光，充满在流动的空气中，像要溢出。

我习惯性地一侧身，想坐起，却不料“扑通”一下跌落到地上。头晕得不得了，抬起脑袋居然看见地上停着几只小巧的鸟儿，黑亮的眼睛一眨一眨地充满好奇。莫不是我摔昏了？

有脚步声渐渐接近，推门声，再接近，最后停在我跟前。

“你趴在地上做什么？”

“摔……摔倒了。”我含糊不清地应道，撑着脑袋摇摇晃晃地爬起，来人便立在眼前了。

那人的两手分别拎着一把斧子和一小捆柴禾，砍柴？很古老的活儿呢，这就是天堂？

对方不再言语，径自走到一边，跪下做着什么，不一会儿便传来了木柴燃烧时噼噼啪啪的声响，还有哗哗的水声。

我扫视着周围，不过是间普通的木屋，刚才让我“趴在地上”的是个自制的吊床，窗外郁郁葱葱的是森林。

我还没死？真奇怪。

那人重新走到我面前，原来只是个男孩，浅绿色的眼睛仿佛溶入了森林的血液。

“你去洗个澡吧。”

“呃？”我诧异。

男孩的脸微红起来，“嗯…这样烧会退得快一点。”

“呃？”仍旧诧异。

脸更红，“……当然…我会出去的…”

说完，匆匆地走出，还带上了门。

“奇怪的人……喂，浴室在哪里啊！？”

“转……转身！”

火烘式的浴缸……现在已经很少见了，比电热式的舒服多了，这原始的热源无声无息地渗入到水中，温暖，柔和。

雨后的树叶味儿，忽闪忽闪地藏在润湿的空气里，人也一阵一阵地感到清爽，感到自己真实的存在。木头的门，木头的地板，木头的墙壁，所有的一切都轻易地融入自然中，此刻的我，似乎也成了这片自然应有的一部分。

放衣台上是一件宽宽松松的衬衫，还有长长的裤子，我不得不把袖口和裤管都卷起来，像个没事干的小孩胡乱穿着大人的衣服。要不是昨天的衣服全淋湿了，我才不要穿成这样。

“喂，吃饭了。”男孩敲了下浴室的门。

“马上就好。”我一边擦头一边推开门，冷不防一个黑影蹿上来，幸好我抓住了门框，否则又得像昨天晚上一样摔个大包。定睛一看――

“铃铛！别跑！！看我怎么收拾你！！！”

“喂！不要在浴室里捉猫好不好！”

结局：由于那像古装一样麻烦的衣服，我又一次败下阵来，但我会再接再厉！

铃铛趴在男孩的头上，跟我大眼瞪着小眼，一副胜利者悠然自得的姿态，我头上的青筋暴跳。

“你就是外面那个大庄园的小姐吧？”男孩转过身来递给我一盘从未见过的绿东西，他自己面前也放着一盘。

“对啊，你认识我么？”我自豪的笑着，心想自家庄园真是威名远扬呵！

男孩看也不看我一眼，自顾自坐下吃那盘里的东西，只说：“东西比不上你们庄园的，吃不吃由你。”话中带着不屑。

若说这天下我最讨厌的事，便是被别人当成爱摆架子的娇贵千金，况且肚子也着实饿了，索性连餐具也不用，端起盘子来个囫囵吞枣，差点没被噎死，男孩惊愕又好笑。

“你是个不一样的人。”对方的脸上终于出现了些许笑容，却只像是借用一下笑容这个陌生的工具。

“你似乎并不喜欢笑呢。”我也笑。

“不…只是很久没笑过了，险些忘记该怎样做。”

“是吗。”

“是啊。”

…………

“那个，铃铛和你很要好？”

“铃铛？你说它？”男孩指了指头顶那团黑色，“啊，算是吧，它常来这儿，吃得还挺多……不会是你从不喂它吧？”

“怎…怎么会？呵呵，呵呵……”冒冷汗，被他说中了！“那个……嗯……多谢你昨天出手相救。”

“是前天，”男孩从容不迫地喝了口水，“你可真会睡。”

我睡了一天？不会吧？

猛站起。

“我要回去了！爷爷肯定发火了！”“还是送你出去吧，你前天刚迷的路。”

“对哦，我怎么忘了？”“它――”说铃铛――“好像暂时不想走。”“是吗，那就送给你好了。”“它本来就是我的。”“呃……？”“前天要不是它发现了你，你早是孤魂了。”“……”

看着沉默的我，男孩冷笑了一声，推门而出，铃铛紧随其后。我也低着头跟了出去，只感到耳根一阵火辣辣的烫。

“那么，你是他原来的主人罗？”我陪着笑脸想补救一下。

“我们是朋友关系，不是主仆。”男孩走在前头不冷不热地说，只有铃铛回头瞄了我一眼。自讨没趣，真失败，只好一路沉默到森林和外界的交接处，才忍不住问他的名字。

“枫竹。”男孩说，转身走了回去。

枫竹吗？真好听的名字。不过一点也不像他，深黑的头发，绿色的眼眸，讲起话来如同音符寥寥的八音盒，捉摸不定的性格再加上些许让人回忆的味道，仿佛一片飘落河面的新叶，一切皆顺其自然。

