我不知道该如何形容知道这个电话的心情，我只是想起多年以前我六岁的时候亲眼看见的车祸，我看见一个男人躺在地上，他的妻子扑上去喊他的名字，可是他就是没有醒，他妻子扑上去的那一刻，很多车呼啸而过，不过没有撞到她身上，然后医生来了，把他装到一辆救护车里去，开走了。

我不知道这个男人的结局，当时我只听见站在我身边的繇对我说，有一天如果我被车撞了，你千万不要扑过来。

但现在繇被车撞了，我同样不能扑过去。我们之间隔着一个日本海。

亭亭在电话里面哭得很伤心。

他死了吗？我很冷静地问。

亭亭莫名惊诧，然后挂了电话。

后来证实他只是左腿粉碎性骨折，繇打电话的时候我对他说，我还是没有扑过去。

繇就笑了，我可以想象他的笑容，和许多许多年前一样。

如果是从前，我一定会哭着对他说，你回来吧，不管你是瘸了还是再也不能站起来了我一定会照顾你一辈子的。但是现在我只是对他说，我还是没有扑过去呢。

当时我还是个不知道一辈子的时间到底有多久的小孩子，而我现在知道了，那是一段很长很长的时间，长到现在的我不能够想象。所以现在的我只对他说，我还是没有扑过去呢。

我听见繇对我说，十，对不起，今年没有办法给你寄樱花了。

我听着就在那里默默地微笑着，无声地微笑着。我笑的时候眼睛看向很远很远的地方，我看见那个我情愿用一生喜欢的男孩子就这样离我远去，已经远到再也不能回来。

今年我果真没有收到属于二月的樱花。

二月里，我只能沉默地看着木棉从校园的大树上一朵朵飘下。

三月收到小游的来信，北京玉渊潭的樱花开了，没有肖宁口中的那般颓废，反而开得如此纵情，照片上小游的笑脸一如樱花盛开那般美丽。

另一封信里是樱花，仿佛是一种补偿，补偿那些再也不会从日本寄来的二月的樱花。小游说，樱花的花期很短的，那般灿烂的花朵，好象一瞬间就枯萎了。

拿着小游寄来的信和樱花，我在很深的夜里想起了我追求了多年的爱情，终于被打击的粉碎。

我象一个小孩子一样消沉了很久，那个时候我竟然已经忘记了自己的苍老。

然后日子如流水，我的快乐和悲伤就在这如水的日子里流荡，在这些如水的日子里，我只能向前跑着，没有任何理由回头。我想去北京，支持我的只是这个念头而已。现在的我根本想不起那些黑暗的日子，我常常会在夜里醒来，然后沉默。

原来我已经习惯了一个人沉默而自己却不自知。

原来我还是一个很适合沉默的人。

原来的原来，只有在想不起来的时候，才能发现我们到底拥有了什么。

快要高考的时候我见到了秋叶，大哥站在学校的大门外面向我招手，他的脸是灿烂的，一如五月晴朗的天空，我站在学校大门的铁栏内，活象一个囚禁很久很久的犯人，我们形成了一个鲜明的对比，我不由笑了起来，我对我自己说，我马上就要逃出去了，逃出去，永远不再回来。

高考那几天没有太阳，阴有小雨。

当我坐在高考的考场上望着薄薄的考卷竟然觉得很不堪，原来一年来多少多少的努力，只能在这些纸上体现出来，这一年的快乐与不快乐最终还是终结在这几张纸上。

考完最后一科的时候我独自坐在桌子上，好象一直追求的一切忽然消失不见，只剩下心底的一片惘然。我终于发现，无论高考，无论童年，已经离我远去。

原来这一年来我的苍老一直都在蔓延着，只是有的时候我感觉不到而已。

甚至我怀疑自己是不是越来越苍老了。已经老得不成样子，脆弱到不能再轻易承受任何打击任何期待了。

我最终还是没有去成北京，没有去成那个可以在三月里看樱花，十二月里看雪花的城市，我再也不能听见寒冷的北风在城市里哭泣的声音，好象已经注定了一样，我没有办法改变这个可能算是注定好的结局。

我最终往更南的方向走去，穿过北回归线，去一个小但是美丽的地方，去那个不可能有雪的地方。

