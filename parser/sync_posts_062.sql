<br>　　情绪高昂的我身子往前一探，刚要高声呼喊之际，猛觉头重脚轻，整个人就像木偶一样痴呆地被烂泥地面吸引了下去，无论是场景还是音效都颇为壮观，俨然有彗星撞地球的意味。在半昏迷的状态中，天使冰凉的手搭在了我的额头上，莫不是在做天国之旅前的祷告？祷告词早已听不清楚了，只求快点结束，让我脱离苦海。
<br>
<br>　　……很暖和……
<br>
<br>　　身体有规律地轻轻摇晃，像童年时的摇篮，充满回忆的味道。
<br>
<br>　　犹如身处蓝色的水波
<br>　　古老的故事在轻轻晃动
<br>　　谁也不知那静谧的角落
<br>　　隐藏着如此悲哀的轮廓
<br>
<br>　　微冷的世界
<br>　　弥漫着困惑的雾色
<br>　　神圣朦胧的亮光中
