<br>　　“铃铛，吃饭了！”我端着银盘满庄园地找老猫。别担心，家人和佣人正在午休，不会发现银盘被我拿来装鱼了，嘻……不过话说回来，这个要死的家伙还真会跑，庄园已经被我翻得底朝天了，居然连根猫毛都没找到，难道它溜到庄园外去了？
