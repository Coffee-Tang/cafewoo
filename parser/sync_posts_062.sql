<br>　　――正午，阴云笼罩着天空，隐隐可以感觉到太阳大功率的辐射，庄园充斥着躁动的闷湿的空气，野玫瑰的花瓣上布着一层细密的水珠，空中灰色的压迫感让人喘不过气来。
