<p>我也来说说俄格冲突</p>

<p>八月八日，北京奥运会开幕式之时，俄格发生了冲突，表面上看，格鲁吉亚为维护本国领土的完整，攻击了南奥塞梯自治区，造成了俄罗斯的维和人员的伤亡，俄罗斯因为有维和任务，所以奋起反击，开始时，俄罗斯似乎不占优势，但后来战局完全倒向俄罗斯。（南奥塞梯局势急剧恶化 俄罗斯展开军事行动 <a href="https://web.archive.orghttp://www.qhnews.com/newscenter" target="blank">http://www.qhnews.com/newscenter</a> ... /10/002575994.shtml）</p>

<p>以下为个人分析，与本网站无关</p>

<p>格鲁吉亚为何敢在奥运会开幕时动俄罗斯保护的南奥塞梯呢？为何在俄罗斯一动手后立刻示弱呢？西方媒体，西方领导人为何随后纷纷指责俄罗斯呢？为何没人指责格鲁吉亚无端攻击平民呢？（西方对俄格冲突态度扑朔迷离<a href="https://web.archive.orghttp://world.huanqiu.com/roll/2008-08/188257" target="blank">http://world.huanqiu.com/roll/2008-08/188257</a>.html）联想近日，驻阿富汗联军在美国指挥下杀死几十名平民（美国对驻阿富汗联军误伤平民表示遗憾<a href="https://web.archive.orghttp://military.people.com.cn/GB/1077/52985/7718926" target="blank">http://military.people.com.cn/GB/1077/52985/7718926</a>.html），似乎高举维护人权大旗的媒体，组织也没吭声呢？</p>

<p>再看看地图，黑海，黑海就像一把匕首顶在俄罗斯的小腹上，目前虽是俄罗斯的黑海舰队占据控制权，但乌克兰和格鲁吉亚都有黑海的港口，似乎支持格鲁吉亚开第一枪的势力想进入黑海，于是让格鲁吉亚打第一枪，然后在俄罗斯宣战后迅速示弱，给国际一个弱者的形象，通过炒作，重新分配黑海的势力，我个人认为有这一企图。看看国际上西方媒体的炒作跟奥运会之前攻击中国的方法如出一辙，所谓西方公平自由的媒体集团，事实上从事着政府指令下肮脏不堪的勾当，其实战争一直没有结束，舆论战天天开战。</p>

<p>从现在看，这一手，对分离俄中关系有着相当大的杀伤力，中国目前所处情况跟格鲁吉亚相似，台湾就像南奥塞梯一样，如果中国支持俄罗斯，则将来美国等也会支持台湾，而这个时期又是俄罗斯最需要朋友的时期，对中国现任领导的决策将是非常大的考验。</p>

<p>我为何觉得俄罗斯在此冲突中一直处于被动状态的呢？由以下几个点，一，俄罗斯总理普京提早离开直接飞前线处理，证明，一开始俄罗斯是没准备的，否则一开始不会那么被动，话语权全掌握在格鲁吉亚总统和西方媒体的手上。二，俄罗斯在决定是否用武力或政治这个问题上出现反复，最近看来，俄罗斯是找算用政治手法来处理的。三，西方政府与媒体的一致性，也反衬在这个问题上相当可疑。跟污蔑中国时一样，仿佛一夜间全是我们的敌人了。四，常识，如果俄罗斯想搞格鲁吉亚，我相信损失不会那么大，情报工作一定很完整的，而且，这个事到了最后，只会朝着将西方势力带入黑海这个结果发展。
