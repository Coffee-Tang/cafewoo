<p>桃  源
<br/>——《欢迎来到东莫村》
<br/> 
出品：Showbox &amp; Mediaplex
<br/>导演：朴光铉
<br/>主演：申河均、郑在泳、姜慧贞
<br/>国家地区：韩国
<br/>类型：战争/剧情
<br/>年代：2005年
<br/>MPAA定级：South Korea: 12
<br/>IMDB评分：7.9/10
<br/> 
故事发生在1950年的朝鲜战争时期。</p>

<p>电影的第一个镜头让人怀疑这是否真是一部战争片。草地上的傻丫头雨怡天真无邪的脸庞，撮的头发好奇的望着天空，耳边还插着两朵野花，镜头慢慢拉起，雨怡咧开嘴笑了起来，没有原因的开心着，向天空挥挥手。音乐在山谷间回荡。——紧接着一架飞机惊险地贴着地面从她头顶上擦过，轰然坠落在草地上。</p>

<p>《东莫村》是个充满黑色幽默的战争喜剧——其实我不知道喜剧悲剧应该怎样算，特别是当一部电影一直都很好笑，最后却以悲剧收场时。也许这就叫黑色幽默吧。《东莫村》中有战争，有幽默，有黑色，还有淡淡的不知不觉的爱情。它正是陶渊明笔下的桃花园，村子里的人依然男耕女织，善良乐助，穿着古朴。没人见过枪，也没人知道战争，朝代。有三个人民军、两个盟军的家伙，还有那个坠机的美国大兵阴差阳错的闯进了村子。开始，由于立场上的敌对，他们不得不成天端着枪警惕对方。但东莫村是个特殊的地方，没有人理会你的枪，一场野猪大战后，他们都成了东莫村的村民，幸福地生活着，直到有一天联合国军杀了过来。结局你可以想象，他们为了保护东莫村，牺牲了自己。</p>

<p>傻丫头雨怡是看过所有电影中我最心爱心疼的角色之一。她的笑容灿烂美丽得近乎虚幻，总是没有理由的欢天喜地，说起话来手舞足蹈，头发向上翘，好像要飞起来，她喜欢淋雨，喜欢在耳边的发迹中别上野花，即使是愤怒地举枪对着她的“首长同志”，她也要傻乎乎的问他“我漂不漂亮”。</p>

<p>东莫村的意思：人人都像孩子般无忧无虑地生活。然而就是在这个小院儿般大的村子， 5个外来人举着枪对峙了一夜。清晨下着雨，爱淋雨的雨怡脱下袜子认真地帮小战士泽基擦着脸，那一刻，她的笑容让他忘记了手里的手雷，忘记了还有战争，还有敌人。这是爱情吗？清淡如同她耳际的野菊花。雨怡趁机调皮的扯下了手雷上的环，坏笑着跑开，大喊着：“是个戒指！”惊慌中，手雷在粮仓中爆炸了。那些火光的和烟尘实在不该属于东莫村。但你知道吗？粮仓中的谷子炸上天，炸成了爆米花，在空中一朵朵盛开，成了一场不可思议的爆米花雨！爆米花落下来，打在老人的鼻子上，战士们的枪上，那一刻，什么枪啊，立场啊，它们都消失了。</p>

<p>那片草地。他们在那里打野猪，一场大战真正消除了“立场”，让他们成了朋友。他们和男女老少村民一块，玩用草扎成的“美式足球”，还有草扎的草垫，他们坐在上面，从高高的草坡上疯了一样滑下来。沉默寡言的表少尉开始不愿加入，后来也一个人疯玩了起来。似乎是从那片晶莹滴翠的草地开始，战士们的仇恨被融化了——其实，他们之间本就没有仇恨。</p>

<p>是夜，通向东莫村的竹林小径依旧点起了路灯。</p>

<p>路灯？你不知道吧？它们是土坯做成的可爱的人偶，立在小迳两边的竹子间，大头，笑得眼弯弯，中间是空心的，用来点灯。雨怡总是跳啊跳的从中间穿过。她似乎是喜欢村里的金先生。当得知就是金先生说她傻时，她有些有不服气地嘟了嘟嘴。但一抬眼，又发现了新鲜事，欢天喜地的跑了过去。她太快乐，又由于傻，永远不懂爱情的浓苦，她又似谜，有人一眼便望进她心里，有人却不知她为何那般快乐美好。她就像那些人偶路灯，空心的，笑眯眯的，一下子照亮你。</p>

<p>影片后半部分的真正的战争，我不想说一个字。如果你看过，请忘记它吧。</p>

<p>如果在这个星球的某个角落，还有某个像东莫村这样的地方，请带上你的爱人，到东莫村走走吧，那个下过爆米花雨的小院，立着人偶路灯的竹林小道，和那片快乐的化解仇恨的草地，你摘下一朵小花夹在她的耳际。不过请注意，嘘——，别惊扰了他们，请像对待南极一样对待它，来过之后，请你知趣地离开。</p>
