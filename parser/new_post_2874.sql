这个城市安然地静立在海边，散发着她年轻温柔的气息。这里有上个世纪遗留下的古老的有轨电车，从身边缓缓行驶过发出当当的响声，有俄式高大尖顶的古老建筑，有精致优雅的咖啡屋。有湛蓝的海水和夕阳。她有时会想自己是否是前世在这里滞留的一粒顽皮沙砾，固执地停留只是为了今生的再次相逢。

上课铃响后15分钟，安静的教室开始骚动。班长在焦急地给辅导员打电话。她转头看窗外，阳光从木头窗户里射进来，灰尘在轻轻起舞。然后她看到一个男人从一辆白色的车里出来，开始飞奔。尽管距离遥远，但她觉得他奔跑的样子像一头年轻的狮子，那么骄傲而冷漠。
然后这阵风在讲台上停下。对不起我来晚了。一句话，轻描淡写。
她明显感受到了周围的不满。抬头，这个男人穿着一条洗得发白的牛仔，一件灰蓝色的T恤松松垮垮地斜在身上，手里捏着一顶棒球帽，斜挎一只苍绿色背包。肤色白皙，眼神犀利。短而硬的黑发。
下面开始上课。他在背包中找课本。
今天走的匆忙，这位同学，你的先借我用一下，你和同桌一起看吧。
她惊异地看着眼前这个男人，但仍递出了课本。
……
课居然是在一片笑声中结束。班里所有人却开始觉得，这个第一堂课就迟到，忘记带教科书，年轻并且高傲的男人，其实异常可爱。他有着纯正的发音，丰富的阅历，幽默的细胞，孩子气的笑容。并且，他对于日本的文化，似乎是自身已经融入其中的深刻了解。

千道岩。东京大学毕业。祖父是日本人，因此有四分之一日本血统。父亲是有名的画家。长年在日本生活。今年刚来大陆。第一次教书。
她微笑着看着身边的女孩莎：你真是辛苦了调查得这么详细。
谁让他是我们系唯一拿得出手的帅哥老师呢。莎两眼放光：还有最重要的一点，他仍然单身。
呵呵难道你有什么想法。她盯住莎，两个人一下笑出声来。

开始习惯，他的迟到，他讲课忽略语法的方式，他花掉整正一堂课时间说他在日本的趣闻，他拿起生茶的瓶子嘴角下扬独特的喝水的样子，他肆无忌惮地吹得天花乱坠。
然后在国庆节前得知回来还要补一天课时，皱了皱眉吐出三个字：烦死了。
呵。烦死了。
这个懒散的家伙。
开始觉得，这个男人实在可爱无比。

七天长假之后班里开始准备秋游。定了杭州。
很自然的，请了先生一起。
她私下和莎习惯性地称他先生。先生在日语有老师的意思。她喜欢先生这个词，有种旧时淡淡的韵味。
他很开心地毫不犹豫地答应了。你们要请我去吧。顽皮地笑，大孩子一般。当然还是付了钱。
去了西湖，看了断桥，赏了满觉陇的桂花，玩了十八涧的溪水。
那日，回程前一天，她开始眷恋这个安静恬淡的城市。那么痴迷，仿佛前世的恋人。

她坐在旅馆外的河边。他看到她手中的《彼岸花》。
知道吗？彼岸花源自日语：ひがんばな。在日语里只是一种植物的名字而已。别称曼殊沙華，秋天开红色的细碎花朵。有毒，可入药。冬天长出叶子，次年春天枯萎。
她惊异地看着他的眼睛，闪亮而温柔。那一刻，天上没有一颗星星。
那一年,她19,他24。

她是个一贯懒散的人。于是即使是在他的课上，在听了那么多繁复的日语感到头疼之后，她从包里摸出铅笔和本子，迅速地描了一眼他，开始勾勒。而台上的这个英俊的男人似乎一无所知。仍然在一大帮女生景仰的目光中讲得异常投入。
不知道过了多久，身边的莎突然碰了她一下。然后画被他不露声色地拿走。她温和地盯着他。他只丢给她一个含义不明的微笑。

母亲打电话来说，小寒死了。在失踪后的第3天，在雪地里找到了它。
她买了当天的火车票飞奔回家。
小寒。她轻轻抚摸着它冰凉的脊梁。忆起它那曾经在黑夜里闪烁的蓝色瞳仁。突然间将脸埋在臂弯里，泪如雨下。
它陪伴了她三年。却在她走后的第一个冬天这样离去。
是这样重感情的女子。

三天后，他的课。提问。她低头不语。
课后他将她留下。她说：我的猫死了。
他直直地凝视着她，轻轻地放她离开：你的画很漂亮。

大二。
她做家教回来的路上担负起宿舍聚餐的任务，去临近的超市采购。拎了满满三大袋。
在公交车站等车的时候，一辆白色的TOYOTA在身边骤然停下。上来我送你吧。是他。
车行驶在灯火通明的街道。一路上他一直在说话。她坐在副驾驶的座位上，微笑着安静聆听。他精彩的大学生活，他热爱的跆拳道，他曾经疯狂的逃课……
那一刻她感觉到的，竟然是这些年都不曾有过的幸福。尽管微小。

生活安静而丰富，这样的日子似乎会一直持续到天荒地老。
和莎行走在这个城市最繁华的商业街上。莎总是这样不厌其烦地拉着她在两个相似的手机挂件之间做艰难的选择。她宽容地陪着她一家又一家的挑选。
她走到门口轻轻呼吸新鲜空气，然后她看到他。
他微笑着和身边一个清瘦高挑的女孩交谈。女孩挽着他的胳膊，这个动作看起来如此自然，似乎他们本来就应该以这样的姿态出现。
她静静地看着他们从店门口经过。然后听到莎在喊她：快来帮我看看哪个好……
她无声地折回。
而一切只是水面轻轻漾起的一个涟漪，瞬间就归于平静。

暑假。
她说：我一直希望，有一日，可以和自己爱的人一起，牵手走在丽江的石板路上。
还是一个人跳上了去丽江的火车。她在那个遥远的土地上行走。她看着身边的人们，安详而知足。流水的节奏，深沉而缓慢。她摘了一朵暗红的花别在头上，孩子一般傻笑。她在夜晚在河水里放下一盏莲花灯，看它静静漂走。她拍下清晨的第一缕阳光和午夜天空中闪亮的星辰。她买下两个刻着一样神秘文字的铃铛。
然后突然觉得累了，于是回去。
她坐在火车上，用淡色的圆珠笔在白色封面的笔记本里写，我走了那么远的路，在旅途颠沛流离。从未停留。我看见沿途美丽的风景，却从不留恋。因为我知道它们不是我的。可现在，连我自己都不清楚，你是否是我的终点。
昏沉入梦。

她始终记得那个奇异的梦境。
树林里繁花盛开，温暖湿润。空中却是大雪纷飞，漫天飘舞。

这么多那么多，仿佛是一个世纪一般长久。

她在房间里坐着，静默。从暮色四合的黄昏到大雨倾盆的凌晨。
仿佛暗夜里盛放的洁白花朵。
期间她取出一盒ESSE，想了想，终究自嘲地笑了下，放了回去。
在解决了第二十一只橘子之后她终于还是点开了那封邮件。只有短短的两句：
夕顔さん、お元気ですか。やっぱり分かれましょう。
在漫长的三年七个月零三天的等待之后，终究还是这般结局。

大三那年，他回日本。
从此再无音迅。
走之前，毫无预兆地，他对她说：你等我三年。
她惊异于他的自信。她默不作声地点头。
他们在机场大厅安静地拥吻。
他微笑着和她挥别。
飞机在城市上空划过优美的弧线，消失在云端。

三年七个月零三天后。
她对自己说，我要对得起自己的心。这是我自己的感情。我究竟要它走向何处。
曾经，是多么独立的女子，坚决不依靠诺言和等待生活。而最终妥协。
她辞掉工作。开始办理出国的手续。纷繁复杂。
拿到签证的那天，阳光明亮。她在房间里整理行李。
然后她看到那本《彼岸花》，随手翻过，是两年前她在课堂上的那幅画。在杭州最后的那个夜晚，他来借这本书。
她看到自己稚嫩的笔触和线条。

是如此坚韧而脆弱的女子。
坚韧而脆弱，丝毫不矛盾。

机场大厅。莎和她拥抱。
照顾好自己。眼睛是湿的。
然后她一个人背着大包走向安检。
飞机上，她坐在窗边，看外面一望无际的云层。突然觉得，这一切，只是一场告别。而她，仅仅是想来看看他曾经生长过的地方。

北海道。
她走下飞机。机场被白色覆盖。刚下过一场大雪。她裹紧了身上灰色的风衣。
邻座的开朗女孩被一辆开着黑色的雪铁龙的年轻男人接走，她想起那个夜晚白色的TOYOTA。
来到小樽。一个人在运河边上看夕阳。远处传来隐约的三弦声。
在温泉旅馆住下。泡温泉的时候旁边是一簇簇盛开的红色花朵。小小的雪花开始沙沙地落下。
她忆起那个梦境。
发了十几封E-MAIL，没有任何回音。

一个人坐在电影院里。
放映。结束。散场。
灯光全部打开。明亮耀眼。
身边恢复安静。
她突然开始怀念金城武最后的那个笑容。

—THE END







	
	
	
