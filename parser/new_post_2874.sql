<br/>下面开始上课。他在背包中找课本。
<br/>今天走的匆忙，这位同学，你的先借我用一下，你和同桌一起看吧。
<br/>她惊异地看着眼前这个男人，但仍递出了课本。
<br/>……
<br/>课居然是在一片笑声中结束。班里所有人却开始觉得，这个第一堂课就迟到，忘记带教科书，年轻并且高傲的男人，其实异常可爱。他有着纯正的发音，丰富的阅历，幽默的细胞，孩子气的笑容。并且，他对于日本的文化，似乎是自身已经融入其中的深刻了解。</p>

<p>千道岩。东京大学毕业。祖父是日本人，因此有四分之一日本血统。父亲是有名的画家。长年在日本生活。今年刚来大陆。第一次教书。
