<p>上课铃响后15分钟，安静的教室开始骚动。班长在焦急地给辅导员打电话。她转头看窗外，阳光从木头窗户里射进来，灰尘在轻轻起舞。然后她看到一个男人从一辆白色的车里出来，开始飞奔。尽管距离遥远，但她觉得他奔跑的样子像一头年轻的狮子，那么骄傲而冷漠。
