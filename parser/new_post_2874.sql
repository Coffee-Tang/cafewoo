<br/>呵呵难道你有什么想法。她盯住莎，两个人一下笑出声来。</p>

<p>开始习惯，他的迟到，他讲课忽略语法的方式，他花掉整正一堂课时间说他在日本的趣闻，他拿起生茶的瓶子嘴角下扬独特的喝水的样子，他肆无忌惮地吹得天花乱坠。
<br/>然后在国庆节前得知回来还要补一天课时，皱了皱眉吐出三个字：烦死了。
<br/>呵。烦死了。
<br/>这个懒散的家伙。
<br/>开始觉得，这个男人实在可爱无比。</p>

<p>七天长假之后班里开始准备秋游。定了杭州。
<br/>很自然的，请了先生一起。
<br/>她私下和莎习惯性地称他先生。先生在日语有老师的意思。她喜欢先生这个词，有种旧时淡淡的韵味。
<br/>他很开心地毫不犹豫地答应了。你们要请我去吧。顽皮地笑，大孩子一般。当然还是付了钱。
<br/>去了西湖，看了断桥，赏了满觉陇的桂花，玩了十八涧的溪水。
<br/>那日，回程前一天，她开始眷恋这个安静恬淡的城市。那么痴迷，仿佛前世的恋人。</p>

<p>她坐在旅馆外的河边。他看到她手中的《彼岸花》。
<br/>知道吗？彼岸花源自日语：ひがんばな。在日语里只是一种植物的名字而已。别称曼殊沙華，秋天开红色的细碎花朵。有毒，可入药。冬天长出叶子，次年春天枯萎。
<br/>她惊异地看着他的眼睛，闪亮而温柔。那一刻，天上没有一颗星星。
<br/>那一年,她19,他24。</p>

<p>她是个一贯懒散的人。于是即使是在他的课上，在听了那么多繁复的日语感到头疼之后，她从包里摸出铅笔和本子，迅速地描了一眼他，开始勾勒。而台上的这个英俊的男人似乎一无所知。仍然在一大帮女生景仰的目光中讲得异常投入。
<br/>不知道过了多久，身边的莎突然碰了她一下。然后画被他不露声色地拿走。她温和地盯着他。他只丢给她一个含义不明的微笑。</p>

<p>母亲打电话来说，小寒死了。在失踪后的第3天，在雪地里找到了它。
<br/>她买了当天的火车票飞奔回家。
<br/>小寒。她轻轻抚摸着它冰凉的脊梁。忆起它那曾经在黑夜里闪烁的蓝色瞳仁。突然间将脸埋在臂弯里，泪如雨下。
<br/>它陪伴了她三年。却在她走后的第一个冬天这样离去。
<br/>是这样重感情的女子。</p>

<p>三天后，他的课。提问。她低头不语。
<br/>课后他将她留下。她说：我的猫死了。
<br/>他直直地凝视着她，轻轻地放她离开：你的画很漂亮。</p>

<p>大二。
<br/>她做家教回来的路上担负起宿舍聚餐的任务，去临近的超市采购。拎了满满三大袋。
<br/>在公交车站等车的时候，一辆白色的TOYOTA在身边骤然停下。上来我送你吧。是他。
<br/>车行驶在灯火通明的街道。一路上他一直在说话。她坐在副驾驶的座位上，微笑着安静聆听。他精彩的大学生活，他热爱的跆拳道，他曾经疯狂的逃课……
<br/>那一刻她感觉到的，竟然是这些年都不曾有过的幸福。尽管微小。</p>

<p>生活安静而丰富，这样的日子似乎会一直持续到天荒地老。
<br/>和莎行走在这个城市最繁华的商业街上。莎总是这样不厌其烦地拉着她在两个相似的手机挂件之间做艰难的选择。她宽容地陪着她一家又一家的挑选。
<br/>她走到门口轻轻呼吸新鲜空气，然后她看到他。
<br/>他微笑着和身边一个清瘦高挑的女孩交谈。女孩挽着他的胳膊，这个动作看起来如此自然，似乎他们本来就应该以这样的姿态出现。
<br/>她静静地看着他们从店门口经过。然后听到莎在喊她：快来帮我看看哪个好……
<br/>她无声地折回。
<br/>而一切只是水面轻轻漾起的一个涟漪，瞬间就归于平静。</p>

<p>暑假。
<br/>她说：我一直希望，有一日，可以和自己爱的人一起，牵手走在丽江的石板路上。
<br/>还是一个人跳上了去丽江的火车。她在那个遥远的土地上行走。她看着身边的人们，安详而知足。流水的节奏，深沉而缓慢。她摘了一朵暗红的花别在头上，孩子一般傻笑。她在夜晚在河水里放下一盏莲花灯，看它静静漂走。她拍下清晨的第一缕阳光和午夜天空中闪亮的星辰。她买下两个刻着一样神秘文字的铃铛。
<br/>然后突然觉得累了，于是回去。
<br/>她坐在火车上，用淡色的圆珠笔在白色封面的笔记本里写，我走了那么远的路，在旅途颠沛流离。从未停留。我看见沿途美丽的风景，却从不留恋。因为我知道它们不是我的。可现在，连我自己都不清楚，你是否是我的终点。
<br/>昏沉入梦。</p>

<p>她始终记得那个奇异的梦境。
<br/>树林里繁花盛开，温暖湿润。空中却是大雪纷飞，漫天飘舞。</p>

<p>这么多那么多，仿佛是一个世纪一般长久。</p>

<p>她在房间里坐着，静默。从暮色四合的黄昏到大雨倾盆的凌晨。
<br/>仿佛暗夜里盛放的洁白花朵。
<br/>期间她取出一盒ESSE，想了想，终究自嘲地笑了下，放了回去。
<br/>在解决了第二十一只橘子之后她终于还是点开了那封邮件。只有短短的两句：
<br/>夕顔さん、お元気ですか。やっぱり分かれましょう。
<br/>在漫长的三年七个月零三天的等待之后，终究还是这般结局。</p>

<p>大三那年，他回日本。
<br/>从此再无音迅。
<br/>走之前，毫无预兆地，他对她说：你等我三年。
<br/>她惊异于他的自信。她默不作声地点头。
<br/>他们在机场大厅安静地拥吻。
<br/>他微笑着和她挥别。
<br/>飞机在城市上空划过优美的弧线，消失在云端。</p>

<p>三年七个月零三天后。
<br/>她对自己说，我要对得起自己的心。这是我自己的感情。我究竟要它走向何处。
<br/>曾经，是多么独立的女子，坚决不依靠诺言和等待生活。而最终妥协。
<br/>她辞掉工作。开始办理出国的手续。纷繁复杂。
<br/>拿到签证的那天，阳光明亮。她在房间里整理行李。
<br/>然后她看到那本《彼岸花》，随手翻过，是两年前她在课堂上的那幅画。在杭州最后的那个夜晚，他来借这本书。
<br/>她看到自己稚嫩的笔触和线条。</p>

<p>是如此坚韧而脆弱的女子。
<br/>坚韧而脆弱，丝毫不矛盾。</p>

<p>机场大厅。莎和她拥抱。
<br/>照顾好自己。眼睛是湿的。
<br/>然后她一个人背着大包走向安检。
<br/>飞机上，她坐在窗边，看外面一望无际的云层。突然觉得，这一切，只是一场告别。而她，仅仅是想来看看他曾经生长过的地方。</p>

<p>北海道。
<br/>她走下飞机。机场被白色覆盖。刚下过一场大雪。她裹紧了身上灰色的风衣。
<br/>邻座的开朗女孩被一辆开着黑色的雪铁龙的年轻男人接走，她想起那个夜晚白色的TOYOTA。
<br/>来到小樽。一个人在运河边上看夕阳。远处传来隐约的三弦声。
<br/>在温泉旅馆住下。泡温泉的时候旁边是一簇簇盛开的红色花朵。小小的雪花开始沙沙地落下。
<br/>她忆起那个梦境。
<br/>发了十几封E-MAIL，没有任何回音。</p>

<p>一个人坐在电影院里。
<br/>放映。结束。散场。
<br/>灯光全部打开。明亮耀眼。
<br/>身边恢复安静。
<br/>她突然开始怀念金城武最后的那个笑容。</p>

<p>—THE END</p>
